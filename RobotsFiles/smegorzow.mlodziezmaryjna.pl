<h4 style="text-align: justify;"><strong>Jestesmy grupa dziecieco &#8211; mlodziezowa, ktora powstala przy parafii <span style="color: #993300;">sw. Stanislawa BM w Smegorzowie</span><span style="color: #993300;">.</span> Inicjatorka rozpoczecia naszej dzialalnosci byla katechetka <span style="color: #993300;">P. Anna Krawczyk</span>, ktora za zgoda <span style="color: #993300;">Ksiedza Proboszcza Bogdana Lebryka</span> zaprosila&nbsp; do szkoly Siostry z Dabrowy Tarnowskiej <span style="color: #339966;">(s. Beate Bialkowska i s. Magdalene Rybak) </span>na lekcje religii. Siostry Szarytki&nbsp; opowiedzialy nam o wielkim <span style="text-decoration: underline; color: #ff0000;"><em>pragnieniu</em></span> Matki Bozej.</strong></h4>
<h4 style="text-align: center;"><strong><span style="color: #0000ff;">,,Pragne, aby powstalo Stowarzyszenie Dzieci i Mlodziezy Maryjnej, ktorym obiecuje wiele lask".</span></strong></h4>
<h4 style="text-align: justify;"><strong>Wiele osob (okolo 40)&nbsp; z naszej szkoly odpowiedzialo ochoczym sercem na to zaproszenie Maryi i <span style="color: #800080;">27.09.2011 r.</span> przyszlismy na pierwsze spotkanie do salki parafialnej. Stowarzyszenie, do ktorego przynalezymy, ma swoje normy i zwyczaje, a swoim zasiegiem obejmuje wszystkie kraje na swiecie.</strong></h4>
<h4 style="text-align: center;"><strong><span style="color: #ff0000;">Nasze spotkania odbywaja sie&nbsp;&nbsp; systematycznie co tydzien:</span></strong></h4>
<h4 style="text-align: center;"><strong><span style="color: #ff0000;">w kazdy poniedzialek i srody e grupach wiekowych&nbsp; w salce parafialnej .&nbsp;&nbsp;&nbsp;</span></strong></h4>
<h4 style="text-align: center;"><strong><em><span style="color: #0000ff;"> Grupa najstarsza &nbsp;w&nbsp;poniedzialki&nbsp;(VII, gimnazjum i starsi )- 16.30</span></em></strong></h4>
<h4 style="text-align: center;"><strong><em><span style="color: #008000;">w srody grupa srednia (klasy IV-VI)- 16.00</span></em></strong></h4>
<h4 style="text-align: center;"><strong><em> <span style="color: #ff99cc;">i w srody o godz.15.00 grupa najmlodsza (nowi czlonkowie od przedszkola do&nbsp;V klasy).</span></em></strong></h4>
<h4 style="text-align: justify;"><strong>Na spotkaniach wspolnie modlimy sie, rozmawiamy na rozne tematy, uczymy sie nowych piosenek i bierzemy aktywny udzial w uroczystosciach parafialnych.</strong></h4>
<h4 style="text-align: center;"><strong><span style="color: #00ffff;">Staramy sie tez lepiej poznac Maryje i nasladowac Ja w naszym codziennym zyciu.</span></strong></h4>
<h4 style="text-align: justify;"><strong>W wakacje, ferie zimowe i nie tylko, nasze Siostry organizuja rozne wyjazdy wypoczynkowo-rekolekcyjne, ogniska i zabawy, np. karnawalowe. Wszyscy razem czujemy sie jak jedna wielka rodzina, bo Maryja jest nasza Matka.</strong></h4>
<h4 style="text-align: center;"><strong><span style="text-decoration: underline;"><span style="color: #cc99ff; text-decoration: underline;">Wraz z Maryja zapraszamy wszystkich chetnych do naszej wspolnoty !!!</span></span></strong></h4>
<h4 style="text-align: justify;"><strong>Dnia 20 czerwca 1847 roku Stowarzyszenie zostalo zatwierdzone przez Piusa IX. Obecnie jest ono zarejestrowane w Papieskiej Radzie ds. Swieckich. W Polsce istnieje ono i dziala od 1852 roku. W okresie powojennym, chociaz oficjalnie zostalo rozwiazane, w miare istniejacych mozliwosci rozwijalo swoja dzialalnosc pod opieka Ksiezy Misjonarzy sw. Wincentego a Paulo i Siostr Milosierdzia. Do Stowarzyszenia naleza dziewczeta i chlopcy, ktorzy chca zyc duchem maryjnym i szerzyc czesc Najswietszej Maryi Panny Niepokalanej.</strong></h4>
