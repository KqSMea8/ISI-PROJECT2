<h4><strong><span style="color: #339966;">&nbsp;<em>NASZE CELE</em></span></strong></h4>
<h4 style="text-align: justify;"><strong>Celem wychowawczym, ktory pragniemy osiagnac we wspolnocie jest upodobnienie sie do Maryi i nasladowanie Tej, ktora Sobor Watykanski II nazywa najznakomitszym wzorem i swiadectwem wiary, milosci i doskonalego zjednoczenia z Chrystusem. Uczestnicy, wybierajac Maryje jako wzor osobowy i ideal wlasnego zycia staraja sie ksztaltowac w sobie Jej rysy przy wydatnej pomocy Grupy. Ucza sie odpowiedzialnosci za otoczenie, prawidlowego kontaktu z bliznimi i wyrabiaja w sobie postawe sluzby, co warunkuje powstanie prawdziwej wspolnoty. Aktywne przezywanie malej wspolnoty wyrabia poczucie odpowiedzialnosci za wspolnote parafialna, za Kosciol.</strong></h4>
