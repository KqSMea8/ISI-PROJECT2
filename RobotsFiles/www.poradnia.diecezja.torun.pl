<h1>Nasza dzialalnosc</h1><ul class="imagesList" id="imagesList2"><li><a href="files/poradniaNowa1.jpg" class="mlbox[pages]" title=""><img src="files/200/poradniaNowa1.jpg" alt="" /></a></li><li><a href="files/poradniaNowa2.jpg" class="mlbox[pages]" title=""><img src="files/200/poradniaNowa2.jpg" alt="" /></a></li><li><a href="files/strzlka.JPG" class="mlbox[pages]" title=""><img src="files/200/strzlka.JPG" alt="" /></a></li></ul><div class="content" id="pageDescription"><p style="text-align: center;" align="justify"> </p>
<p style="text-align: center;" align="justify"><span style="font-size: small; color: #000000;"><strong>Katolicki Osrodek Psychologiczno - Pastoralny "<em>Cyrenejczyk</em>" zostal erygowany dekretem Biskupa Torunskiego z dnia 27 wrzesnia 2006 roku.</strong></span></p>
<p style="color: #000080; font-family: Verdana; font-size: medium; text-align: -webkit-center;" align="center"><span style="color: #000000; font-size: small;"><strong>zobacz: <a href="?statut,4"><span class="link2" style="color: #000000; font-size: 10pt; font-family: verdana; text-decoration: none;">DEKRET</span> oraz <span class="link2" style="color: #000000; font-size: 10pt; font-family: verdana; text-decoration: none;">STATUT</span></a></strong></span></p>
<hr style="color: #000080; font-family: Verdana; font-size: medium; text-align: -webkit-center;" width="50%" />
<p style="color: #000080; font-family: Verdana; font-size: medium;" align="justify"><span style="color: #000000; font-size: small;">Podstawowym zadaniem Osrodka jest ksztaltowanie osoby ludzkiej i swiadczenie pomocy w oparciu o zintegrowana koncepcje czlowieka uwzgledniajaca wymiary: biologiczny, psychiczny, spoleczny i duchowy</span></p>
<p style="color: #000080; font-family: Verdana; font-size: medium; text-align: -webkit-center;" align="center"><span style="color: #d01c02;"><strong>komu chcemy pomagac?</strong></span></p>
<p style="color: #000080; font-family: Verdana; font-size: medium;" align="justify"><span style="color: #000000; font-size: small;">Jestesmy otwarci na kazdego, kto przyjdzie do nas i poprosi o pomoc. W sposob szczegolny nasza oferte kierujemy do ludzi, ktorzy znalezli sie w trudnej sytuacji zyciowej: stres, nerwica, depresja, stany lekowe, utrata osoby bliskiej, choroba, kryzysy malzenskie i rodzinne, problemy wychowawcze, nalogi itd.</span></p>
<p style="color: #000080; font-family: Verdana; font-size: medium;" align="justify"><span style="color: #000000; font-size: small;">Wreszcie pragniemy rowniez pomoc w przygotowaniu osob doroslych do sakramentu malzenstwa.</span></p>
<p style="color: #000080; font-family: Verdana; font-size: medium; text-align: -webkit-center;" align="center"><strong><span style="color: #d01c02;">jakiego rodzaju dzialalnosc prowadzimy?</span></strong></p>
<p style="color: #000080; font-family: Verdana; font-size: medium;" align="justify"><strong><span style="color: #000000; font-size: small;">Proponujemy nastepujace formy pomocy:</span></strong></p>
<ul>
<li>doradztwo psychologiczne i duchowe;</li>
<li>psychoterapie indywidualna i grupowa<br />zarowno dorazna jak i dlugoterminowa;</li>
<li>prowadzenie kursow, konferencji, szkolen i sympozjow;</li>
<li>przygotowanie do malzenstwa;</li>
<li>nauczanie i propagowanie naturalnych metod planowania rodziny;</li>
<li>tworzenie grup wsparcia i koordynacja ich pracy;</li>
<li>dzielanie porad prawnych z zakresu prawa cywilnego, rodzinnego i opiekunczego, karnego i gospodarczego<br />i koscielnego</li>
</ul>
<p align="center"><span style="color: #d01c02; font-size: medium;"><strong>na czym polega nasza dzialalnosc?</strong></span></p>
<p><span style="color: #000000;"><strong>W naszym Osrodku zajmujemy sie:</strong></span></p>
<ul>
<li>formacja osoby ludzkiej przygotowujaca do czynnego udzialu w zyciu spolecznym i do dojrzalego podejmowania poslugi w Kosciele i spolecznosci swieckiej;</li>
<li>pomoca w rozeznaniu powolania i w rozwiazywaniu trudnosci zwiazanych dokonywanym wyborem;</li>
<li>wypelnianiem nauczycielskiej misji Kosciola i promowanie wartosci chrzescijanskich;</li>
<li>udzielaniem pomocy psychologicznej i religijnej osobom przezywajacym trudnosci zyciowe;</li>
<li>propagowaniem pelnego rozwoju osoby ludzkiej, uwzgledniajacego dazenie do zintegrowanego rozwoju psychicznego i duchowego;</li>
<li>troska o malzenstwo i rodzine, pomoc malzenstwom i rodzinom znajdujacym sie w kryzysie;</li>
<li>przygotowaniem do malzenstwa i odpowiedzialnego rodzicielstwa;</li>
<li>poznawaniem i upowszechnianiem chrzescijanskiej koncepcji osoby ludzkiej;</li>
<li>profilaktyka antyuzaleznieniowa oraz pomoca osobom uzaleznionym i wspoluzaleznionym.</li>
</ul></div></div>
