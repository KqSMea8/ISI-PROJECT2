<div style="font-stretch: normal; font-size: 21px; line-height: normal; color: rgb(61, 59, 54); text-align: center;">
<h1><span style="font-size: 11pt;"><strong><span style="font-size: medium;"><span style="font-family: Verdana;">Szkolenie &ndash; Warsztaty z&nbsp;Radiestezji i&nbsp;Radiestezji Terapeutycznej</span></span></strong></span></h1>
<h1>
<div align="center" style="padding-top: 10px; padding-bottom: 10px; color: rgb(62, 58, 46); font-size: 18px; font-weight: 400;">
<div align="center" style="padding-top: 10px; padding-bottom: 10px;">
<p style="text-align: left;"><span style="font-size: small;"><span style="font-family: Verdana;">Celem szkolenia jest:<br />
&bull;	Przedstawienie podstawowych pojec i&nbsp;technik z&nbsp;dziedziny radiestezji,<br />
&bull;	Nabycie przez uczestnikow umiejetnosci poslugiwania sie przyrzadami radiestezyjnymi (wahadlo, rozdzka),<br />
&bull;	Zapoznanie sie i&nbsp;przecwiczenie podstawowych technik pracy radiestety.<br />
Omowione zostana:<br />
&bull;	Podstawowe pojecia w&nbsp;radiestezji.<br />
&bull;	Zdolnosci radiestezyjne i&nbsp;nie tylko.<br />
&bull;	Sprzet radiestezyjny (m.in. ksztalt, budowa, zastosowanie).<br />
&bull;	Metody i&nbsp;praca z&nbsp;wahadlem: wybor wahadla, jak poslugiwac sie wahadlem - zajecia praktyczne, interpretacja ruchow wahadla.<br />
&bull;	sprawdzanie wrazliwosci radiestezyjnej - zajecia praktyczne.&nbsp;<br />
&bull;	Praca na biometrach.&nbsp;<br />
&bull;	Podstawy teleradiestezji.<br />
&bull;	Podstawy chromoterapii.<br />
&bull;	Praca z&nbsp;wybranym sprzetem radiestezyjnym w&nbsp;pracy terapeutycznej.<br />
&bull;	Lokalizacja zaburzen energetycznych w&nbsp;organizmach zywych.&nbsp;<br />
&bull;	Promieniowania wystepujace w&nbsp;radiestezji ich rozpoznawanie, interpretacja przy pomocy przyrzadow radiestezyjnych (w tym ciekow - zyl wodnych).&nbsp;<br />
&bull;	Ocena energetyczna pomieszczen.<br />
&bull;	Promieniowanie ksztaltow.<br />
&bull;	Lokalizacja promieniowan geopatycznych.<br />
&bull;	Metody zabezpieczenia przed promieniowaniem geopatycznym.<br />
&bull;	Przyrzady radiestezyjne jako pomocnicze narzedzie pracy bioenergoterapeuty przy diagnostyce energetycznej (lokalizacja dysfunkcji energetycznych) - zajecia praktyczne.<br />
&bull;	omowienie i&nbsp;prezentacja wybranych wahadel terapeutycznych, ich zastosowanie - zajecia praktyczne.<br />
&bull;	BHP - techniki oczyszczajace i&nbsp;zabezpieczajace.<br />
Szkolenie przeznaczone jest dla:<br />
&bull;	Osob zainteresowanych radiestezja oraz osob zainteresowanych stosowaniem technik radiestezyjnych w&nbsp;roznych dziedzinach w&nbsp;tym bioenergoterapii.<br />
&bull;	Dla radiestetow chcacych poszerzyc swoja wiedze.<br />
&bull;	Dla bioenergoterapeutow lub osob po kursach bioenergoterapii.<br />
&bull;	Osob zainteresowanych profesjonalna praca w&nbsp;zakresie radiestezji.</span></span></p>
<p style="text-align: left;"><span style="font-size: small;"><span style="font-family: Verdana;">Uczestnik szkolenia otrzymuje materialy pomocnicze.<br />
Zaswiadczenie o&nbsp;ukonczeniu szkolenia.<br />
Wszystkich chetnych serdecznie zapraszam.</span></span></p>
<p style="text-align: left;"><span style="font-size: small;"><span style="font-family: Verdana;">Czas trwania: okolo 16 godzin.<br />
Termin:</span></span></p>
<p style="text-align: left;"><span style="font-size: small;"><span style="font-family: Verdana;">30 - 31 marzec 2019 r.<br />
