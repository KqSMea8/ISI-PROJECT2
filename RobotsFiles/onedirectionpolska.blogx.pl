<div class="entry-meta">
<span class="meta-prep meta-prep-author">Opublikowano</span> <a href="http://onedirectionpolska.blogx.pl/?p=13" title="14:37" rel="bookmark"><span class="entry-date">20 stycznia 2012</span></a>, <span class="meta-sep">autor:</span> <span class="author vcard"><a class="url fn n" href="http://onedirectionpolska.blogx.pl/?author=36162" title="Zobacz wszystkie wpisy, ktorych autorem jest onedirection">onedirection</a></span>			</div><!-- .entry-meta -->

<div class="entry-content">
<p>Zayn, Harry, Niall, Louis i Liam oficjalnie zostali zespolem 23 lipca 2010 roku o godzinie 20:22.</p>
<p><em>&#8222;Gotta be you&#8221;</em> zostalo napisane przez Harry'ego i Niall'a.</p>
<p>Niall napisal 5 piosenek na album</p>
<p>Niall gdy mial 4 lata przebral sie na Halloween za Po z Teletubisiow</p>
<p>Niall kiedy odpadli z X Factor plakal najmocniej z calej piatki</p>
<p>Niall zawsze, gdy fanki pytaja go, czy sie z nimi ozeni, odpowiada TAK.</p>
<p>Niall w szkole nie rozmawial z dziewczynami, bo sie ich wstydzil</p>
<p>Kiedys podczas wywiadu z Niall'em gdzies w tle wyglupiala sie reszta chlopakow. Niall krzyknal zdenerwowany <em>,,Zamknac sie ku*wa , wywiad robie".</em></p>
<p>Niall wierzy w to, ze mozna nie uprawiac seksu przed slubem.</p>
<p>Harry kiedys mial wielka ochote pocalowac jedna fanke.</p>
<p>Harry:  <em>Jezeli jego dziewczyna usnelaby na jego kolanach, zrobilby jej zdjecie, by co dzien rano je ogladac i mowic jej jaka jest piekna.</em></p>
<p>Harry czeka na dziewczyne, ktora powali go na kolana od razu, gdy ja zobaczy.</p>
<p>Harry: <em>&#8222;Dopoki nie znajde idealnej dziewczyny, mam Louisa&#8221;</em></p>
<p>Harry chcialby pocalowac sie z dziewczyna w deszczu</p>
<p>Harry jezeli sam zacznie rozmawiac z fanka, lub nie moze oderwac od niej wzroku, oznacza to, ze mu sie podoba.</p>
<p>Liam w podstawowce byl bity przez rowiesnikow.</p>
<p>Liam napisal kiedys dla dziewczyny piosenke pytajac potem, czy zostanie jego dziewczyna, nastepnego dnia z nim zerwala.</p>
<p>Liam lubi ciche lecz radosne dziewczyny, uwielbia widziec usmiech na twarzy swojej wybranki</p>
<p>Liam boi sie lyzek, ale jesli mialby poprawic jedzeniem lyzka dziewczynie humor, zrobilby to.</p>
<p>Liam chcialby przesiedziec caly dzien ogladajac bajki Disney'a ze swoja dziewczyna.</p>
<p>Liam: <em>Najgorsza rzecz jaka chlopak moze zrobic dziewczynie to ignorowanie jej wtedy, gdy ta kocha Cie calym sercem.</em></p>
<p>Liam podobno jest najromantyczniejszy</p>
<p>Zayn kiedys oddal fance swoja czapke.</p>
<p>Zayn kiedys powiedzial, ze gdyby byli cala 5 w jednej szkole, najprawdopodobniej nigdy by sie nie zaprzyjaznili, poniewaz sa calkiem rozni.</p>
<p>Chlopaki uwazaja, ze gdyby Zayn nie byl w zespole, moglby zajac sie modelingiem.</p>
<p>Zayn nie chodzi na imprezy, ale kiedys po swietach poszedl na jedna i obudzil sie w czyjejs lazience.</p>
<p>Zayn napisal swoja zwrotke w<em> What Makes You Beautiful.</em></p>
<p>Zayn pali &#8222;od czasu do czasu&#8221; , to nawyk sprzed X Factora, chcialby to rzucic, stara sie.</p>
<p>Zayn gdyby byl fanka 1D jego ulubionym chlopakiem bylby Louis.</p>
<p>Zayn kiedys wpadl na placzaca dziewczyne, stanal i zaczal ja uspokajac do momentu az nie przestala plakac.</p>
<p>Zayn gdyby mogl zmienic glos, chcialby miec taki jak Niall.</p>
<p>Kiedys fanka powiedziala do niego &#8222;<em>Jestes piekny&#8221;</em> Zayn jej odpowiedzial <em>&#8222;Nie tak jak ty&#8221;.</em></p>
<p>Zayn nie caluje sie na pierwszej randce no chyba, ze to TO.</p>
