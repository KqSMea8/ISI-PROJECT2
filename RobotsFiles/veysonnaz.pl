<h1>VEYSONNAZ</h1>
<h2>Czyli, u bram Czterech Dolin</h2>
<p>Veysonnaz to mala, urocza wioska, ktora znajduje sie w samym sercu Valais w okazalym otoczeniu, w odleglosci 13 km od Sion.</p>
<p>Ta mala wioska to cieple i przyjazne miejsce, gdzie odwiedzajacy moga spedzic przyjemnie czas na spotkaniach wsrod pieknych widokow szwajcarskich Alp. Jednym z prawdziwych skarbow regionu jest bez watpienia jego doskonala lokalizacja. Wznoszace sie na przepieknym zboczu, jak orle gniazdo Veysonnaz, laczy w sobie ogromna dawke slonca (srednio 300 dni w roku jest sloneczne) oraz wspanialy widok na doline Rodanu. I zapiera dech w piersiach. W starej wsi z kolei, oczaruja Cie tradycyjne domy i ich mieszkancy.</p>
