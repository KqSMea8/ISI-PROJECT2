<section id="nasze-produkty" class="container showrounded content4show">
<div class="row">
<div class="col-xs-12 paddingtoplarge paddingbottomsmall">
<blockquote><p class="redbg">Sukces to drabina, po ktorej nie sposob wspiac sie z&nbsp;rekami w&nbsp;kieszeniach.</p>
<cite class="redbg">Philip Wylie</cite></blockquote>
</div>
</div>
<div class="row">
<div class="col-md-12 justify"><p>Bycie handlowcem jest trudniejsze niz sie wiekszosci ludzi wydaje.<br> Dobry
handlowiec sprzedaje przede wszystkim swoj wlasny wizerunek oraz korzysci, jakie uzyska klient z&nbsp;zakupu
towaru czy uslugi przez niego oferowanych.</p>

<p>Programy szkoleniowe dostosowujemy do potrzeb i&nbsp;oczekiwan naszych Uczestnikow. Bierzemy pod uwage
zarowno ich wiedze jak i&nbsp;doswiadczenie praktyczne.</p>

<h3>Opierajac sie na tych dwoch zmiennych wyroznilismy cztery glowne typy handlowcow:</h3></div>
</div>
<div class="row paddingtopsmall">
<div class="col-sm-3 text-center col-sm-offset-0 col-xs-6 col-xs-offset-6"><img src="img/debiutant.jpg"
alt="Debiutant"
class="img-responsive rounded">
</div>
<div class="col-sm-9 justify">
<div class="paddingtopsmall hidden-xs"></div>
<h3>Debiutant</h3>

<p>Od pierwszego dnia pracy pracownik dzialu handlowego staje przed wyzwaniami zwiazanymi z&nbsp;poznaniem
firmy, jej kultury, struktury, zespolu, strategii oraz celow biznesowych. Rozpoczyna sie czas nauki i&nbsp;pozyskiwania
wiedzy o&nbsp;produktach, kanalach dystrybucji jak rowniez rozwijanie kompetencji sprzedazowych. Moze je
pozyskiwac wykorzystujac kilka mozliwosci: wlasne doswiadczenia w&nbsp;codziennej pracy, rady i&nbsp;wskazowki
kolegow, coaching przelozonego, szkolenia wewnetrzne.</p>

<p>Wzrost kompetencji merytorycznych, a&nbsp;tym samym wyzszy poziom skutecznosci w&nbsp;sprzedazy moze
uzyskac poprzez udzial w&nbsp;projektach rozwojowych organizowanych przez firmy zewnetrzne.</p>

<p>EIRK wspiera debiutantow w&nbsp;rozwoju kompetencji sprzedazowych poprzez ich udzial w&nbsp;cyklu
warsztatow realizowanych w&nbsp;ramach programu ,,Akademia Sprzedazy". Podczas zajec uczestnicy otrzymuja
pakiet narzedzi do natychmiastowego wykorzystania w&nbsp;praktyce, poznaja i&nbsp;cwicza techniki
zwiekszajace skutecznosc sprzedazy.</p>

<p>Debiutanci rozwijaja umiejetnosci zwiazane z&nbsp;poszukiwaniem nowych klientow, umawianiem spotkan,
zdobywaja wiedze i&nbsp;cwicza umiejetnosci planowania dzialan sprzedazowych, organizowania pracy
wlasnej, poznaja sposoby intensyfikowania kontaktow z&nbsp;klientami. Cwicza przygotowanie do rozmow
handlowych oraz budowania pozytywnych relacji z&nbsp;klientami. Kreowanie postawy proaktywnej jest
nieodzownym elementem procesu rozwojowego.</p></div>
</div>
<div class="row paddingtopsmall">
<div class="col-sm-3 text-center col-sm-offset-0 col-xs-6 col-xs-offset-6"><img src="img/adept.jpg" alt="Adept"
class="img-responsive rounded">
</div>
<div class="col-sm-9 justify">
<div class="paddingtopsmall hidden-xs"></div>
<h3>Adept</h3>

<p>Proces uczenia sie i&nbsp;poszerzania doswiadczen rosnie wraz z&nbsp;kolejnymi miesiacami intensywnej
pracy. Handlowiec wykorzystuje w&nbsp;swojej praktyce zarowno wiedze jak i&nbsp;pozyskane umiejetnosci.
Adept sprawdza co ,,dziala" a&nbsp;co wymaga dalszej, intensywnej pracy i&nbsp;doskonalenia. Zarowno
sukcesy jak i&nbsp;popelnione bledy buduja jego swiadomosc i&nbsp;chec dalszego rozwoju.</p>

<p>W&nbsp;tym okresie wiedza, dzielenie sie doswiadczeniem z&nbsp;innymi w&nbsp;obszarze tworzenia bazy
klientow, budowania i&nbsp;utrzymywania relacji biznesowych jest kluczem do dalszych sukcesow. <br>Koncentracja
na rezultatach biznesowych, dostarczenie Klientom oczekiwanych rozwiazywan, obsluga posprzedazowa, maja
ogromny wplyw na sukcesy Adepta.</p>

<p><strong>Na tym etapie proponujemy udzial w warsztatach rozwijajacych umiejetnosci budowania relacji z
trudnym klientem,</strong> odswiezenie dobrych praktyk i&nbsp;technik komunikacyjnych w&nbsp;procesie
sprzedazy, proponowania kreatywnych rozwiazan odpowiadajacych na indywidualne potrzeby klienta,
finalizowanie procesu sprzedazy. Skuteczna argumentacja, techniki negocjacyjne, wywieranie wplywu to
tematy warsztatow kierowanych do Adeptow.</p></div>
</div>
<div class="row paddingtopsmall">
<div class="col-sm-3 text-center col-sm-offset-0 col-xs-6 col-xs-offset-6"><img src="img/praktyk.jpg"
alt="Praktyk"
class="img-responsive rounded">
</div>
<div class="col-sm-9 justify">
<div class="paddingtopsmall hidden-xs"></div>
<h3>Praktyk</h3>

<p>Wypracowane narzedzia, sprawdzone techniki, biegla komunikacja w&nbsp;procesie sprzedazy procentuja.
Swiadomosc mocnych stron handlowca jest duza, rosnie potrzeba kolejnych sukcesow ale wyzwania rynkowe,
coraz wyzsze cele biznesowe, czasami monotonia pracy powoduja spadek motywacji. Rowniez chec
wprowadzania kolejnych zmian zwiazanych z&nbsp;nowymi zadaniami czy sposobami pracy, czyli kolejny
wysilek spotyka sie z&nbsp;wewnetrznym oporem. Handlowiec potrzebuje wsparcia od swojego szefa i&nbsp;wzrostu
automotywacji.</p>

<p><strong>Praktykom dedykujemy warsztaty z&nbsp;zakresu: perswazji w&nbsp;biznesie, budowania i&nbsp;rozwijania
efektywnosci osobistej i&nbsp;zawodowej, automotywacji. Wzrostowi kompetencji interpersonalnych powinno
towarzyszyc poglebianie kompetencji z&nbsp;zakresu trudnych negocjacji oraz cross i&nbsp;up&nbsp;sellingu.</strong>
</p></div>
</div>
<div class="row paddingtopsmall">
<div class="col-sm-3 text-center col-sm-offset-0 col-xs-6 col-xs-offset-6"><img src="img/mistrz.jpg"
alt="Mistrz"
class="img-responsive rounded">
</div>
<div class="col-sm-9 justify">
<div class="paddingtopsmall hidden-xs"></div>
<h3>Mistrz</h3>

<p>Na tym etapie rozwoju handlowiec posiada bardzo duze doswiadczenie biznesowe, merytoryczne i&nbsp;interpersonalne
oraz i&nbsp;wysoki poziom zaangazowania. Samodzielnosc w&nbsp;pracy pozwala mu czuc sie pewnie.
Zazwyczaj jest dla siebie ,,sterem, zeglarzem, okretem". Pelni funkcje coacha i&nbsp;mentora dla innych,
jest odpowiedzialny za wdrozenie i&nbsp;rozwoj nowozatrudnionych pracownikow w&nbsp;sieci sprzedazy. To
handlowiec, ktory ,,Sprzeda Eskimosowi lodowke". <br>Mimo to pracownik na tym etapie potrzebuje kolejnych
inwestycji w&nbsp;swoj rozwoj. Najczesciej zwiazane jest to z&nbsp;dookreslenie dalszej sciezki kariery.
</p>

<p><strong>Aby okreslic Indywidualny Plan Rozwoju EIRK proponuje tej grupie handlowcow udzial procesie
Development Center.</strong> Zachecamy do udzialu w&nbsp;takich programach jak:</p>
<ul>
<li>Rewitalizacja sil handlowych</li>
<li>Trudne negocjacje</li>
<li>Wypracowanie i&nbsp;wdrozenie standardow sprzedazy i&nbsp;obslugi klienta</li>
<li>Skuteczne dzielenie sie wiedza</li>
</ul>
</div>
