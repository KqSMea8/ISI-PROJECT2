<h3 class="ozdobnik">Kancelaria</h3>
<div class="blok_textu">
<p>
Kancelaria Florkowska Makowska Konarski zostala utworzona w Krakowie w 2001 roku. &nbsp; &nbsp;</p>
<p>
Zasadniczym przedmiotem naszej dzialalnosci jest swiadczenie uslug doradztwa podatkowego, doradztwa ksiegowego oraz uslug audytorskich. Nasza oferte uzupelniaja uslugi ksiegowe tj. prowadzenie ksiag rachunkowych oraz rozliczen placowych. Prowadzimy takze dzialalnosc szkoleniowa w zakresie prawa podatkowego i rachunkowosci.&nbsp;</p>
<p>
Kancelarie tworza dwie sp&oacute;lki:</p>
<p>
1) Sp&oacute;lka Doradztwa Podatkowego<br />
&nbsp; <strong>Florkowska Makowska Konarski Sp. z. o.o. </strong></p>
<p>
2)podmiot uprawniony do badania sprawozdan finansowych - nr KIBR 3116<br />
&nbsp; <strong>FMK AUDYT Sp. z. o.o.</strong></p>
<p>
Nasi doradcy posiadaja bogate doswiadczenie zawodowe, ugruntowane wieloletnia praktyka. Pracujemy w jezyku polskim, niemieckim i angielskim.</p>
<p>
Dzieki znajomosci miedzynarodowego prawa podatkowego i miedzynarodowych standard&oacute;w rachunkowosci oraz scislej wsp&oacute;lpracy z zagranicznymi doradcami gwarantujemy profesjonalna obsluge podatkowa i ksiegowa polskim podmiotom prowadzacym dzialalnosc za granica oraz zagranicznym inwestorom w Polsce.<br />
<br />
Celem naszej Kancelarii jest zapewnienie kompleksowej obslugi, zgodnej z indywidualnymi potrzebami i oczekiwaniami naszych Klient&oacute;w.</p>

