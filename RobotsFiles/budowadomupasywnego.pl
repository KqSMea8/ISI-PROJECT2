<td style="vertical-align:top; font-family: Tahoma; font-size: 16px;">
<p>Dom pasywny jest budynkiem ktory ma znacznie nizsze zapotrzebowanie na zuzycie energii w stosunku do budynkow energooszczednych. Podstawowym kryterium dla oceny czy budynek spelnia swoimi parametrami cechy domu pasywnego jest zuzycie energii  potrzebnej na ogrzanie budynku, wynosi ona 1.5 litra oleju na m2 rocznie.</p>
<p>Ogolnie rozpatrujac koncepcje budowy domu pasywnego nalezy miec swiadomosc, ze budynek pasywny to nie jest to samo co budynek energooszczedny. </p>
<p>Budowa domu pasywnego wymaga trzymania sie scisle zalozonych kryteriow i wszelkie odstepstwa od nich juz na etapie projektu, jak rowniez popelnione bledy w trakcie budowy (zwlaszcza brak doswiadczenia lub nawyki poszczegolnych ekip budowlanych) moga sie przyczynic do tego, ze wybudowany budynek bedzie spelnial co najwyzej normy budynku ocieplonego lub w korzystniejszym wariancie energooszczednego ale nie bedzie spelnial norm domu pasywnego ani niskoenergetycznego.</p>
<p>Ilustrujac to prostym przykladem nie wystarczy tylko ocieplic budynek i zamontowac rekuperator czy pompe ciepla zeby miec dom pasywny. Jak rowniez nie sa to jedyne dostepne rozwiazania techniczne (rekuperator, pompa ciepla), ktore umozliwiaja spelnienie w naszej planowanej inwestycji parametrow i cech domu pasywnego.</p>
<p>Ponizej w tabelach pokazujemy roznice w zapotrzebowaniu na energie i klasyfikacje energetyczne budynkow.</p>
