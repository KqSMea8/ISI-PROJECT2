<div style="text-align:center;"><h1>O nas w kilku slowach</h1><br><br>Firma Handlowo Uslugowa Tarm juz od dwudziestu lat jest przedstawicielem firmy MarS w Polsce. Dzieki naszej dzialalnosci do Polski w 1997 roku trafily pierwsze spadochrony tej firmy. Firma MarS produkuje spadochrony ratownicze, glowne, zapasowe oraz pokrowce spadochronowe. W swojej ofercie mamy spadochrony desantowe, taktyczne, szkolne, wyczynowe, i tandemowe.    Nasze spadochrony naleza do najlepszych na Swiecie, a spadochron tandemowy HOP 330 wedlug wielu instruktorow jest najlepszym spadochronem tandemowym na Swiecie. Firma MarS znana jest tez w Europie i na Swiecie ze spadochronow hamujacych do samolotow odrzutowych. MarS produkuje tez konfekcje specjalna, kabury, kamizelki taktyczne, kuloodporne i wiele innych artykulow dla wojska i policji.    Do najnowszych produktow firmy nalezy spadochron towarowy typu skrzydlo zdalnie sterowany, wykorzystywany do zadan specjalnych. Do firmy Mars naleza rowniez firmy KRAS i JOJO WINGS ktore staly sie czesciami firmy MarS. Dzieki temu macie Panstwo dostep do szerokiej gamy spadochronow i akcesorii spadochronowych. Zawsze moga Panstwo liczyc na nasza pomoc, profesjonalna obsluge i fachowe doradztwo..</div>

