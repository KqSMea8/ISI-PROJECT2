<h2><span>O</span> konferencji</h2>
</header>
<div class="content-inner editor_content">
<p><em>Szanowni Panstwo,</em></p>

<p><em>Ordynatorzy Oddzialow Chirurgii Ogolnej i Kierownicy Klinik Chirurgii Ogolnej</em></p>

<p>
<br>
</p>

<p>W ramach krajowego nadzoru specjalistycznego tradycyjnie zapraszam na czwarta juz Konferencje Ordynatorow i Kierownikow Klinik Chirurgii Ogolnej, ktora odbedzie sie w Olsztynie w dniach 27-28 wrzesnia 2019 roku. Tematy tegorocznej Konferencji skupiaja sie wokol szeroko pojetej problematyki zwiazanej z dzialalnoscia jednostek chirurgii ogolnej. Jednym z najwazniejszych elementow naszej pracy w 2019 roku jest proces wdrozenia przez Ministerstwo Zdrowia ,,Koncepcji organizacji i funkcjonowania Krajowej Sieci Onkologicznej" w Polsce, co w zasadniczy sposob wplynie na mozliwosc swiadczenia uslug w zakresie chirurgicznego leczenia nowotworow na oddzialach chirurgii ogolnej. Wlasciwe przygotowanie sie do nowej strategii w dziedzinie diagnostyki i terapii nowotworow bedzie mialo znaczenie dla uzyskania z NFZ kontraktow i aktywnego udzialu jednostek chirurgii ogolnej w strukturach Krajowej Sieci Onkologicznej.</p>

<p>
<br>
</p>

<p>Biorac pod uwage strukture organizacyjna systemu ochrony zdrowia w Polsce, wciaz jej najwazniejszym elementem w zakresie chirurgii ogolnej sa terenowe jednostki chirurgii ogolnej, w ktorych wykonywana jest zdecydowana wiekszosc swiadczonych procedur chirurgicznych i na ktorych opiera sie bezpieczenstwo obywateli naszego kraju. Dlatego jednym z tematow IV Konferencji sa problemy funkcjonowania oddzialow chirurgii w szpitalach podleglych administracji i samorzadom lokalnym.</p>

<p>
<br>
</p>

<p>W kolejnej sesji zostana przedstawione wazne tematy zwiazane ze starzeniem sie naszego spoleczenstwa i ze stale narastajacym problemem oraz koniecznoscia udzielania pomocy chirurgicznej pacjentom w wieku podeszlym. Umiejetnosc wlasciwej diagnostyki, kwalifikacji do operacji, leczenia chirurgicznego oraz wiedza dotyczaca zwiazanych z tym zagrozen i powiklan to kolejne wyzwania, przed ktorymi staja oddzialy chirurgii ogolnej.</p>

<p>
<br>
</p>

<p>Tradycyjnie tematyke Konferencji Ordynatorow i Kierownikow Klinik uzupelniaja problemy edukacji zawodowej w codziennej praktyce klinicznej chirurga ogolnego, a takze aktualne problemy wynikajace z zadan i obowiazkow Konsultanta Krajowego w dziedzinie Chirurgii Ogolnej. Do udzialu w Konferencji zostali zaproszeni: przedstawiciel Ministerstwa Zdrowia, Konsultant Krajowy w dziedzinie Anestezjologii i Intensywnej Terapii prof. Radoslaw Owczuk, Przewodniczacy Zespolu Ekspertow do opracowania projektu koncepcji i organizacji funkcjonowania Narodowego Instytutu Onkologii prof. Piotr Czauderna, a takze grono wykladowcow reprezentujacych chirurgie ogolna.</p>

<p>
<br>
</p>

<p>Goraco zachecajac Panstwa do udzialu w spotkaniu, mam nadzieje, ze IV Ogolnopolska Konferencja Ordynatorow i Kierownikow Klinik Chirurgii Ogolnej w Olsztynie spotka sie z Panstwa zainteresowaniem. Jak zwykle w ramach integracji naszego srodowiska poza programem merytorycznym Konferencji przewidziane jest spotkanie towarzyskie z czescia artystyczna.</p>

