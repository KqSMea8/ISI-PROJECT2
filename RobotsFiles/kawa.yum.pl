<h1>Dlaczego kawa jest kwasna ?</h1>
<p><span>kawa.yum.pl</span></p>
<p>
Nalezy rozroznic dwa podstawowe pojecia.
Chodzi o:<br />

kwasowosc - zrownowazony smak kawy, ktory poprawia jej walory smakowe, efekt pozadany, np. kawy kenijskie, kawy z Kostaryki czy Meksyku
urzekaja Nas cytrusowymi, slodkimi nutami<br />
kwasnosc - efekt niepozadany w kawie, ktory podczas jej spozywania, przyprawia nas o dreszcze i wykrzywia buzki
w rozne strony :)
<br /><br />
Co wplywa na kwasnosc kawy?
Temperatura zaparzania - im nizsza temperatura, tym napar jest bardziej kwasny;
<br />
Ubicie kawy (przy ekspresach kolbowych)-  dla doswiadczonego baristy wystarczy doslownie chwila by zaparzyc nam rewelacyjne
espresso.
<br />
Niedostosowany czas zaparzania - czas ekstrakcji ze zmielonych ziaren kawy ma ogromny wplyw na walory smakowe naparu.
W trakcie tego procesu wyplukiewane sa najcenniejsze aromaty i smaki. Kazdy gatunek, rodzaj kawy wymaga od nas
roznego czasu parzenia - czasu, po ktorym kawa bedzie najlepiej smakowac, dlatego warto eksperymentowac zarowno z czasem i
temperatura dla mieszanki kawy.
Rodzaj kawy (gatunek : arabica czy robusta)-arabika jest zawsze bardziej kwaskowata z nutami owocowo-kwiatowymi,
robusta ziemista z czkoladowym, dymnym aromatem.
<br />
Stopien wypalenia kawy - ciemniejsze- niweluje kwasnosc kawy i wprowadza nutke goryczki, jasniejsze - uwypukla kwasowosc kawy.
<br />
Higiena - pamietaj, ze urzadzenie w ktorym przygotowujemy kawe musi byc utrzymane w nienagannym stanie.
Codzienne czyszczenie i regularna konserwacja maja ogromne znaczenie na jakosc.

