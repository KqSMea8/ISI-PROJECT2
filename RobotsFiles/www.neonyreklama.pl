Producent neonow. Posiadamy wlasna, profesjonalnie wyposazona  neonownie
z nowoczesna pompa prozniowa marki DACO NEON EQUIPMENT. Obecnie wspolpracujemy
z firmami DNA (Krakow) i ART PROJEKT (Krakow) dla ktorych wykonujemy neony CINEMA CITY.
Nowoczesny sprzet i dobrze wykwalfikowani pracowniicy to gwarancja wysokiej jakosci
produktow, ktora mozemy sie poszczycic.
