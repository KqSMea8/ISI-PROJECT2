<b><div style="font-size:16;">KaMa Care</div></b> to preznie dzialajaca firma zajmujaca sie posrednictwem pracy w opiece na terenie Niemiec. Tworzy ja zespol doswiadczonych i kompetentnych osob.<br  /><br  />
Nasi podopieczni, czesto osoby chore lub niepelnosprawne, w zyciu codziennym potrzebuja wsparcia osob trzecich. Zdajemy sobie sprawe  jak waznym dla nich rozwiazaniem jest mozliwosc spedzenia jesieni zycia w zaciszu wlasnego domu.<br  /><br  />
<b>Poszukujemy</b> osob chetnych podjac prace przy opiece nad seniorami i osobami niepelnosprawnymi. Przyjmujemy zgloszenia z terenu calej Polski od wszystkich, dla ktorych slowo "opiekunka" oznacza nie tylko zawod, ale wiaze sie takze z pewnym rodzajem powolania.<br  /><br  />
Naszych pracownikow zatrudniamy na umowe o prace w Polsce w oparciu o przepisy polskiego prawa jak i w Niemczech. Poza legalnym zatrudnieniem zapewniamy takze bezpieczenstwo. <br  /><br  />
<p style="text-align:center;">Kazde miejsce oddelegowania opiekuna jest przez nas <b>dokladnie sprawdzane.</b><br  /></p>
