p_string = p_string.replace(/&lt;/g, '<');
p_string = p_string.replace(/&gt;/g, '>');
p_string = p_string.replace(/&quot;/g, '"');
p_string = p_string.replace(/&#039;/g, "'");
return p_string;
};

