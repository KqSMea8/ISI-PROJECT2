<div class="panel panel-accordion panel-aqua">
<div class="panel-heading"><a data-toggle="collapse" data-parent="#index-faq" class="" href="#collapse1">Dla kogo przeznaczony jest konkurs?<i class="fa fa-chevron-up"></i></a></div>
<div id="collapse1" class="panel-collapse collapse panel-body in">
<div class="panel-body-inner">
<p>Sw&oacute;j pomysl do&nbsp;konkursu moze zglosic kazda dorosla osoba. Szczeg&oacute;lnie liczymy na&nbsp;<span>stowarzyszenia i&nbsp;fundacje</span> prowadzace dzialalnosc na&nbsp;rzecz dzieci, ludzi aktywnych spolecznie i&nbsp;rodzic&oacute;w&nbsp;dzieci. Zapraszamy tez samorzady i&nbsp;podlegle im jednostki, zwlaszcza przedszkola, szkoly, swietlice, domy kultury.</p>
</div>
</div>
</div>
<div class="panel panel-accordion panel-aqua">
<div class="panel-heading"><a data-toggle="collapse" data-parent="#index-faq" class="collapsed" href="#collapse2">Jakie inicjatywy mozna zglaszac w&nbsp;konkursie?<i class="fa fa-chevron-up"></i></a></div>
<div id="collapse2" class="panel-collapse collapse panel-body ">
<div class="panel-body-inner">
<p>Piata edycja konkursu grantowego ma na&nbsp;celu wsparcie dzieci w&nbsp;ich rozwoju, pomoc w&nbsp;edukacji oraz&nbsp;wzmacnianie ich pasji i&nbsp;talentow.</p>
<p>Liczymy na&nbsp;pomyslowosc, aktywnosc spoleczna i&nbsp;lokalne potrzeby. Kazda propozycja powinna sluzyc dobru wsp&oacute;lnemu, czyli korzysci z&nbsp;jej realizacji powinna odczuwac szersza spolecznosc. Inicjatywy moga miec charakter inwestycji lub&nbsp;lokalnych program&oacute;w&nbsp;spolecznych. W&nbsp;szczeg&oacute;lnosci zachecamy do&nbsp;zglaszania inicjatywy typu doposazenie swietlic, zakup sprzetu edukacyjnego, pomocy naukowych, sprzetu pomagajacego rozwijac dzieciece hobby, stworzenie aplikacji mobilnych pomagajacych w&nbsp;zdobywaniu wiedzy. Wszystko to, co pomoze dzieciom w&nbsp;ich rozwoju, zdobywaniu wiedzy i&nbsp;nowych umiejetnosci. W&nbsp;tegorocznej edycji wylaczona zostaje mozliwosc tworzenia nowych plac&oacute;w&nbsp;zabaw oraz&nbsp;boisk sportowych, gdyz podobne inicjatywy byly szeroko reprezentowane w&nbsp;poprzednich edycjach konkursu.</p>
</div>
</div>
</div>
<div class="panel panel-accordion panel-aqua">
<div class="panel-heading"><a data-toggle="collapse" data-parent="#index-faq" class="collapsed" href="#collapse3">Z&nbsp;jakich narzedzi mozna korzystac, by wypromowac swoj pomysl?<i class="fa fa-chevron-up"></i></a></div>
<div id="collapse3" class="panel-collapse collapse panel-body ">
<div class="panel-body-inner">
<p>Na&nbsp;stronie&nbsp;<a href="http://www.todlamniewazne.pl/">www.todlamniewazne.pl</a>&nbsp;przygotowalismy specjalny zestaw narzedzi. Korzystajac z&nbsp;nich mozna latwo przygotowac i&nbsp;wydrukowac kolorowy plakat lub&nbsp;ulotke promujace lokalnie inicjatywe. Mozna stworzyc r&oacute;zne materialy do&nbsp;internetu, m.in. na&nbsp;Facebooka. Warto tez skorzystac z&nbsp;porad, jak&nbsp;i&nbsp;gdzie promowac swoja inicjatywe, zeby zdobywac glosy. Poradniki i&nbsp;materialy sa dostepne po&nbsp;zgloszeniu inicjatywy na&nbsp;stronie <span>konkursu</span>.</p>
</div>
</div>
</div>
<div class="panel panel-accordion panel-aqua">
<div class="panel-heading"><a data-toggle="collapse" data-parent="#index-faq" class="collapsed" href="#collapse4">Kto zdecyduje o&nbsp;przyznaniu grantow?<i class="fa fa-chevron-up"></i></a></div>
<div id="collapse4" class="panel-collapse collapse panel-body ">
<div class="panel-body-inner">
<p>Inicjatywy z&nbsp;najwiekszym poparciem w&nbsp;glosowaniu internaut&oacute;w&nbsp;zostana przedstawione jury, kt&oacute;re rozdzieli pule grant&oacute;w. Wiekszosc czlonk&oacute;w&nbsp;jury to osoby, kt&oacute;re ciesza&nbsp;sie autorytetem w&nbsp;sprawach waznych dla dzieci, i&nbsp;kt&oacute;re jednoczesnie nie&nbsp;sa zwiazane z&nbsp;Fundacja Aviva. Ze&nbsp;skladem jury mozna zapoznac&nbsp;sie pod linkiem <a href="http://todlamniewazne.pl/jury.html">http://todlamniewazne.pl/jury.html</a></p>
</div>
</div>
</div>
<div class="panel panel-accordion panel-aqua">
<div class="panel-heading"><a data-toggle="collapse" data-parent="#index-faq" class="collapsed" href="#collapse5">Przebieg konkursu<i class="fa fa-chevron-up"></i></a></div>
<div id="collapse5" class="panel-collapse collapse panel-body ">
<div class="panel-body-inner">
<ul class="bullets">
<li>Nalezy wypelnic formularz zgloszeniowy na&nbsp;www.todlamniewazne.pl</li>
<li>Jesli inicjatywa spelnia wymogi regulaminu, moderator dopuszcza ja do&nbsp;udzialu w&nbsp;konkursie &ndash; odtad jest widoczna na&nbsp;stronie</li>
<li>Uczestnik uzyskuje poparcie internaut&oacute;w&nbsp;dla zgloszonego pomyslu, wykorzystujac narzedzia udostepnione przez&nbsp;organizatora</li>
<li>Internauci, kt&oacute;rzy chca poprzec dana inicjatywe, glosuja na&nbsp;nia na&nbsp;stronie lub&nbsp;korzystaja z&nbsp;zalet crowdfundingu (1 dotacja w&nbsp;crowdfundingu to 1 dodatkowy glos dla wybranej inicjatywy, a&nbsp;Fundacja Aviva podwoi najlepszym zebrana kwote - szczeg&oacute;ly w&nbsp;Regulaminie konkursu na&nbsp;todlamniewazne.pl)</li>
<li>20 inicjatyw z&nbsp;najwieksza liczba jest przedstawiane jury, kt&oacute;re podejmuje decyzje o&nbsp;liczbie i&nbsp;wysokosci grant&oacute;w</li>
<li>Fundacja Aviva oglasza wyniki konkursu i&nbsp;podpisuje umowy z&nbsp;beneficjentami grant&oacute;w</li>
<li>Beneficjenci wykorzystuja srodki z&nbsp;grantu na&nbsp;realizacje inicjatyw, po&nbsp;czym podsumowuja i&nbsp;rozliczaja zrealizowane projekty</li>
</ul>
</div>
</div>
