<h1><span style="font-size:14px">Biuro podr&oacute;zy kolonie w Polsce obozy mlodziezowe zagranica wycieczki szkolne zielone szkoly wyjazdy grupowe dla nauczycieli i firm L&oacute;dz</span></h1>

<p><span style="font-size:18px">Galaktyka Travel to biuro podr&oacute;zy, kt&oacute;re zadowala swoich klient&oacute;w niezapomnianymi wyjazdami juz od 20 lat. Jestesmy zgranym zespolem profesjonalist&oacute;w, kt&oacute;ry dba o to zeby kazdy wyjazd byl wyjatkowo udany. Specjalizujemy sie w przygotowywaniu imprez na specjalne zam&oacute;wienie. Turystyka nie ma dla nas tajemnic. Organizujemy: wycieczki szkolne, zielone szkoly, kolonie w Polsce, obozy mlodziezowe, warsztaty jezykowe a takze wyjazdy motywacyjne i biznesowe dla firm.<br />
Ponadto w naszej ofercie znajdziecie wakacje lotnicze i autokarowe w kazdy zakatek swiata m.in.: Grecja, Chorwacja, Bulgaria, Turcja, Egipt, Maroko, Wyspy Kanaryjskie, Wlochy, Albania, Cypr i wiele innych. Nie ma dla nas rzeczy niemozliwych. Wypoczynek nad morzem to dla Ciebie nuda? Moze nurkowanie ze sprzetem albo kitesurfing? Propozycji jest wiele! Specjalizujemy sie takze w organizacji kurs&oacute;w jezyka angielskiego dla najmlodszych.</span><br/>

<p><span style="font-size:18px">Galaktyka Travel przynalezy do Polskiej Izby Turystyki co sprawia, ze jest firma godna zaufania i spelnia wszelkie wymogi niezbedne do organizowania wyjazd&oacute;w we wlasciwy spos&oacute;b. Jestesmy sprawdzonym organizatorem, potwierdzaja to Wpis do Rejestru Organizator&oacute;w oraz Gwarancja Ubezpieczeniowa Europa. Niezaleznie od tego czy macie juz swoja koncepcje wyjazdu lub dopiero szukacie dobrego pomyslu, mozecie na nas liczyc.</span><br/>

