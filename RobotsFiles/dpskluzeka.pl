<h1><span class="img_txt gray d_">WITAMY</span> <span class="img_txt blue b_">W NASZYM DOMU!</span></h1>
</div>
<div class="article article_text">
<p>
Dom Pomocy Spolecznej ul. Kluzeka 6 w Krakowie jest placowka stacjonarna pobytu stalego zapewniajaca calodobowa opieke dla 90 osob w podeszlym wieku. Najwyzszy poziom uslug zapewniajacych klientom DPS bezpieczenstwo i zadowolenie jest celem kazdego pracownika Domu.
</p>
<p>
Dom umozliwia mieszkancom udzial w terapii zajeciowej, zaspokojenie potrzeb religijnych i kulturalnych. Dom zapewnia warunki do rozwoju samorzadnosci mieszkancow. Dba o utrzymanie i rozwijanie kontaktow z rodzinami mieszkancow oraz srodowiskiem. Dom zapewnia opieke dla mieszkancow w czasie zajec zorganizowanych na jego terenie.
</p>
<p>
Zapraszmy do poznania nas blizej.
</p>
