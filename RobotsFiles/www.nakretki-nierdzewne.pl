<h2>Sruba</h2>
<p>Kazdy z nas zwlaszcza majsterkowiczow spotkal sie z czyms takim jak sruba. Sruba znajduje szerokie zastosowanie w budownictwie, budowie maszyn i innych urzadzen. Na rynku istnieje mnostwo odmian srub. Roznia sie wielkoscia, ksztaltem lba, trzpienia, oraz zakonczeniem. Do najpopularniejszych mozemy zaliczyc sruby z lbem szesciokatnym, czworokatnym, oczkowym, sruba z uchem, z gwintem na calej dlugosci trzpienia, sruba oczkowa itp.<img src='images/0.jpg'  class='ac' /> Jakie zastosowanie ma sruba. Jest wspanialym elementem dzieki ktoremu mozemy laczyc roznego rodzaju czesci skladowe. Poprzez slowo sruba rozumiemy tez cos co pelni funkcje przenosnika ruchu i sily. O historii sruby wiele nie mozemy powiedziec. Do dzisiaj nie wiadomo kto jest wynalazca. Z przeprowadzonych wykopalisk wynika, ze byla ona juz znana 2000 lat temu i sluzyla do laczenia elementow. Wynalazek ten przypisuje sie Archytasowi z Tarentu ktory zyl w IVw p.n.e. Tak naprawde sruba na szeroka skale zawojowala rynek w XVIII wieku, wtedy tez wynaleziono srubokret, co ulatwilo prace glownie stolarzom. Za jej pomoca mocowanie elementow bylo trwalsze niz przy uzyciu gwozdzi.</p>
