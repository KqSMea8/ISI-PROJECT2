<div id="box2-A03"><h3>O mnie</h3></div>
<div id="box2-A04">
<p><h2>Jakub Derwich: Tworca rzezby ludowej w kamieniu <a href="#box6">z Zywca</a></h2></p>
<p>Jestem uczniem znanego zywieckiego rzezbiarza ludowego Leszka Cieslika.</p>
<p>Moje rzezby w kamieniu mieszcza sie w kanonach sztuki ludowej. Trescia prac jest tematyka sakralna, wyrazona w rzezbie figuralnej i plaskorzezbie.</p>
<p>Wielokrotnie bralem udzial w konkursach organizowanych na terenie kraju oraz na Slowacji. W konkursie &quot;Ludowi muzykanci w sztuce&quot; zdobylem II nagrode. W roku 2006 uczestniczylem w cyklicznym konkursie &quot;Patroni naszych kosciolow&quot;, organizowanym przez Osrodek Kultury w Stryszawie i Starostwo Powiatowe w Suchej Beskidzkiej (sw. Stanislaw - biskup meczennik).</p>
<p>Wspolpracuje z regionalnym Osrodkiem Kultury w Bielskku-Bialej, biorac udzial w wystawach sztuki ludowej, organizowanych na terenie Bielska-Bialej i innych miastach. W ramach tej wspolpracy doskonale swoj warsztat i technike rzezbiarska, starajac sie zachowac indywidualny i oryginalny styl swoich prac.</p>
<p>Od roku 2009 jestem czlonkiem STL-u (Stowarzyszenie Tworcow Ludowych).<br />Aktualnie laczye swoja prace rzezbiarska z obowiaskami zawodowymi.</p>
<p>Nadal korzystam z zaproszen na wystawy i konkursy rzezby w kamieniu,<br />gdzie zdobywam wyroznienia i nagrody.</p>
