<h1>o nas</h1>
<blockquote><p></p></blockquote>

<p>Wyjatkowe&nbsp;dekoracje slubne i weselne&nbsp; to nasza pasja. Zaprowadzimy Ciebie do bajkowej krainy&nbsp;niezapomnianych aranzacji. Centrum Slubne to miejsce, gdzie przyszla Mloda Para znajdzie wszystko, aby ich swieto pozostawilo wszystkim uczestnikom niezapomniane wrazenia. Nasze uslugi to nie tylko bukiety slubne, zaproszenia slubne,dekoracje slubne, alejki slubne,&nbsp;dekoracje weselne, oprawa muzyczna slub&oacute;w i wesel, ale calkowita organizacja slubu i wesela. Posiadamy r&oacute;wniez<span class="st"> sklep i wypozyczalnia dekoracji slubnych!</span><span class="st"> Stw&oacute;rz swoja wlasna wymarzona dekoracje dzieki elementom z </span><span class="st">naszej wypozyczalni!</span></p>
<p style="text-align: center;"><strong><span style="text-decoration: underline;">Zajmujemy sie takze konsultacja slubna.</span></strong></p>
<h1><img style="float: left; margin-top: 5pxpx; margin-bottom: 5pxpx;" src="../DATA/pliki/mainsite.jpg" alt="" width="512" height="341" /></h1>
<h2 style="text-align: center; clear: both;">Dzialamy na terenie calego wojew&oacute;dztwa kujawsko-pomorskiego.</h2>
<p style="text-align: justify; clear:both;">&nbsp;Trafiajac na nasza strone pozyskujecie firme, kt&oacute;ra posiada szeroka game swiadczonych uslug, zapewni zaspokojenie najwybredniejszych gust&oacute;w oraz najbardziej wymagajacych klient&oacute;w.</p>
<p style="text-align: justify; clear:both;">Z mila checia wezmiemy na nasze barki ciezar, kt&oacute;ry sprawia organizacja wesela, abyscie mogli cieszyc sie Wasza uroczystoscia i ta najwazniejsza chwila w Waszym zyciu.</p>
<p style="text-align: justify; clear:both;">Dzieki nam problemy weselne nie zaistnieja w waszym zyciu, a Wasze wesele kojarzyc sie Wam bedzie z pieknym swietem, a zapewniona przez nas oprawa muzyczna oraz dekoracje weselne i slubne zapamietacie na cale zycie.</p>




