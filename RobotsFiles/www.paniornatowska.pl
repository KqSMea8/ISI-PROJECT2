<h1>O firmie</h1>
<p>Herbaty Pani Ornatowskiej, to przede wszystkim tradycja. Receptury mieszanek naszych herbat pochodza z przekaz&oacute;w przodk&oacute;w Ornatowskich powierzanych z pokolenia na pokolenie! Proporcje skladnik&oacute;w, scisle przez Nas chronione, sa skomponowane w spos&oacute;b zapewniajacy najlepsze dzialanie i wlasciwosci prozdrowotne. Cala paleta barw i smak&oacute;w, nutka aromatu to zasluga wysokiej biologicznie jakosci skladnik&oacute;w.</p>
<p>Nasze produkty pochodza ze zbiorowisk lesnych oraz z upraw ekologicznych. W trosce o srodowisko zrezygnowalismy z mechanicznego zbioru plod&oacute;w rolnych, aby zapewnic jak najmniejsza ingerencje w przyrode. Zar&oacute;wno ziola, rosliny zielne jak i owoce sa zbierane recznie. Skladniki naszych herbat sa pozbawione pestycyd&oacute;w i srodk&oacute;w ochrony roslin-co czyni nas zdrowszym. Kupujac nasze produkty przyczyniacie sie Panstwo do ochrony srodowiska: gleby, w&oacute;d, krajobrazu oraz calego srodowiska naturalnego.</p>
<p>Rodzina Ornatowskich, to tradycjonalisci! Dla Nas bardzo wazne jest przywiazanie do ziemi-bo jak wiadomo- najlepiej przyswajane przez organizmu produkty roslinne, pochodza z najblizszego nam otoczenia. Srodowisko, klimat jak i roslinnosc sa specyficzne dla danego regionu i strefy klimatycznej. Dlatego nasze produkty pochodza z najzdrowszych teren&oacute;w Polski. Dodatkowo prawie wszystkie komponenty susz&oacute;w herbat Pani Ornatowskiej sa rodzimymi produktami, a to zapewnia lepsza przyswajalnosc skladnik&oacute;w, witamin jak i mikro-i makroelement&oacute;w.</p>
<p>Panstwo Ornatowscy, aby sprostac wymaganiom wszystkich swoich Klient&oacute;w, proponuja bardzo bogaty wachlarz herbat. Poczynajac od herbatek ziolowo- owocowych, kt&oacute;re zapewniaja chwile wytchnienia, wspomnienie dziecinstwa i smak owoc&oacute;w pochodzacych z babcinego ogrodu. Poprzez herbatki ziolowe, bezcenne w zwalczaniu wszelakich dolegliwosci zdrowotnych. Konczac na ziolach, kt&oacute;re w tradycji Panstwa Ornatowskich traktowane byly jako naturalne lekarstwa na wiele chor&oacute;b, a obecnie uwazane za bezkonkurencyjna alternatywe dla farmaceutyk&oacute;w.</p>
<p>Jestesmy przekonani, ze nasze herbaty to r&oacute;wniez swietny pomysl na wyjatkowy i zdrowy prezent. Dlatego specjalnie dla Panstwa, nasza firma przygotowala r&oacute;wniez szeroka game zestaw&oacute;w okolicznosciowych jak i swiatecznych. Niech nutka aromatu i smaku gosci r&oacute;wniez na stolach Waszych najblizszych, aby razem z Panstwem mogli sie cieszyc filizanka z naparem luksusu i relaksu.</p>
