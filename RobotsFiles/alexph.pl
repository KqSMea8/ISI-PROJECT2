<h1 class="header">O firmie</h1>
<span class="header-desc">Zacznij tu</span>
<div class="content">
<p style="text-align: justify;"><strong>ALEX SP.J.</strong>&nbsp;jest jednym z pierwszych, dzialajacych w regionie swietokrzyskim dystrybutorem wyrob&oacute;w alkoholowych,&nbsp; napoj&oacute;w i w&oacute;d mineralnych oraz wyrob&oacute;w tytoniowych. Specjalizujemy sie w sprzedazy wysokiej klasy trunk&oacute;w oraz piwa.</p>
<p style="text-align: justify;">Dzialamy nieprzerwanie na rynku od 1992 roku.</p>
<p style="text-align: justify;">Obslugujemy kilkuset odbiorc&oacute;w zar&oacute;wno indywidualnych jak i sieciowych. &nbsp;Korzystajac z bogatego asortymentu kompleksowo zaopatrujemy najpopularniejsze kluby i restauracje w Kielcach i regionie.</p>
<p style="text-align: justify;">Posiadamy wlasna baze magazynowa oraz swiadczymy uslugi dowozu towaru do klienta wlasnym transportem.</p>			</div>
