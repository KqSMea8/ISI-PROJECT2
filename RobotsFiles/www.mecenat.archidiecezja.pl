<h2><strong>Akcja Archiwum Archidiecezjalnego w Gnieznie</strong></h2>
<p> </p>
<p style="text-align: justify;">Nasze Archiwum to prawdziwy "dom skarbow slowa": slowa, zawartego w setkach tysiecy kart dokumentow, listow, ksiag. Sa to slowa, ktorymi przemawia do nas historia Polski, Europy, Kosciola. Bardzo wiele sposrod naszych skarbow slowa, ze wzgledu na ich zly stan zachowania, wola o ratunek. Czy mozemy dopuscic, by te slowa ulegly zniszczeniu, by nieodwracalnie zamilkla historia, ktora wciaz w nich zyje?</p>
<p style="text-align: justify;">Dla ratowania dawnych ksiag i dokumentow z naszego zasobu prowadzimy od 1 stycznia 2012 roku <strong>pod patronatem Prymasa Polski</strong> akcje "Mecenat Skarbow Slowa". Chcemy zachecic Panstwa: osoby prywatne, firmy, instytucje, do "zaadoptowania" konkretnego zabytku slowa pisanego, wymagajacego pilnej konserwacji. Mozna sfinansowac zarowno cala jego konserwacje, jak i zlozyc sie na nia z innymi milosnikami historii. Zasadniczo zabytki beda poddawane konserwacji w siedzibie naszego Archiwum.</p>
<p>Dla duzych firm i instytucji jestesmy gotowi wytypowac rowniez zabytek, wymagajacy sporego nakladu finansowego, spoza listy projektow do realizacji.</p>
<p>Naszych mecenasow honorujemy trzema tytulami:</p>
<ul>
<li style="text-align: justify;"><strong>Wielki Mecenas Skarbow Slowa</strong> (wplata min. 10 000 zl lub rownowartosc; do konca roku 2016: 2 500 zl): wpis do Zlotej Ksiegi Mecenatu, pamiatkowy dyplom, zaproszenie na ekskluzywna wizyte w Archiwum, polaczona z pokazaniem cennych zabytkow od Sredniowiecza do czasow wspolczesnych; zaproszenie na wystawy i inne wydarzenia, ktorych wspolorganizatorem jest Archiwum; dla chetnych: podanie nazwiska do publicznej wiadomosci na stronie internetowej Archiwum oraz zorganizowanie briefingu prasowego a takze oprowadzenie po Muzeum Archidiecezji Gnieznienskiej;</li>
</ul>
<br />
<ul>
<li style="text-align: justify;"><strong>Mecenas Skarbow Slowa</strong> (wplata min. 3 000 zl; do konca roku 2016: 1 500 zl): wpis do Zlotej Ksiegi Mecenatu, pamiatkowy dyplom, zaproszenie do wizyty w Archiwum, polaczonej z pokazaniem niektorych cennych zabytkow; dla chetnych: podanie nazwiska do publicznej wiadomosci na stronie internetowej Archiwum;</li>
</ul>
<br />
<ul>
<ul>
<li style="text-align: justify;"><strong>Milosnik Skarbow Slowa</strong>(wplata min. 700 zl; do konca roku 2016: 500 zl): wpis do Zlotej Ksiegi Mecenatu, pisemne podziekowanie, zaproszenie do wizyty w Archiwum i zapoznanie z jego praca; dla chetnych: podanie nazwiska do publicznej wiadomosci na stronie internetowej Archiwum.</li>
</ul>
</ul>
<br />
<ul>
<li style="text-align: justify;"><strong>Mecenas Archiwum </strong>(od grudnia 2016: wplata min. 5 000 zl na cele inne, niz konserwacja).</li>
</ul>
<br />
<p>Przyjmiemy chetnie rowniez mniejsze kwoty.</p>
<p>Partnerem merytorycznym Mecenatu jest dyplomowany konserwator zabytkow, p. Marzena Szczerkowska.</p>
