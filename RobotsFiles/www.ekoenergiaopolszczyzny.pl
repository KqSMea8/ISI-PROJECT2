<p style="text-align: justify;">  Tematami  debat beda  rozwiazania w zakresie pozyskania energii ze slonca, biogazu i powietrza. Przedstawione zostana rozwiazania w zakresie fotowoltaiki nie wymagajace koncesji oraz duze inwestycje z koncesja na sprzedaz energii.</p>
<p style="text-align: justify;">  Energia z biogazu to szansa na zagospodarowanie odpadow rolnych. Zaprezentowane beda mikro-biogazownie z dobrym skutkiem stosowane na fermach hodowlanych. Ponadto zostana zaprezentowane przyklady wykorzystania energii wiatru oraz technologie geotermalne, zarowno dla duzych inwestycji jak rowniez dla prywatnych inwestorow. Prelegentami beda praktycy z branzy ekoenergii, ktorzy przedstawia zrealizowane inwestycje, co gwarantuje ciekawa debate z Panstwa udzialem.</p>
<p style="text-align: justify;">  Serdecznie zapraszamy przedstawicieli gmin, prywatnych inwestorow oraz pasjonatow wykorzystania odnawialnych zrodel energii.</p>
<p style="text-align: justify;">  Debata zostala zorganizowana na terenie wystawy rolniczej OPOLAGRA w namiocie konferencyjnym, dlatego liczba miejsc jest ograniczona. Pierwszenstwo do miejsc siedzacych maja osoby, ktore zglosza swoja obecnosc, wysylajac informacje na adres  e-mail:
