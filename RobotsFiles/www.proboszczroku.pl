<h1 style="color: black; margin: 0; ">sw. Jan Vianney</h1>
<p style="margin: 0; color: #888;"><b>PATRON PROBOSZCZOW</b></p>
<div class="img"><img src="/_style/vianney.jpg" alt="Jan Vianney, patron proboszczow" /></div>
<p>Sw. Jan Vianney urodzil sie we Francji w 1786 r. i wzrastal w czasach, gdy Kosciol byl przesladowany przez rewolucje francuska. Pierwsza Komunie Swieta przyjal konspiracyjnie, w szopie zamienionej na prowizoryczna kaplice. Pozno nauczyl sie czytac i pisac, a nauka przez cale zycie sprawiala mu duze klopoty. Z tego powodu mial nawet opuscic seminarium, ale dzieki interwencji swojego proboszcza oraz ze wzgledu na owczesny brak kaplanow zostal wyswiecony w 1815 r. majac 29 lat.</p>
<p>Po trzech latach kaplanstwa zostal skierowany do Ars, bardzo zaniedbanej religijnie wioski, gdzie zylo zaledwie 230 wiernych. Mieszkancy innych miejscowosci mowili o nich pogardliwie, ze tylko chrzest rozni ich od bydlat. Nawet w niedziele kosciol byl pusty, natomiast pelne byly trzy istniejace we wsi karczmy.</p>
<p>W Ars sw. Jan Vianney przebywal przez 41 lat. Wiodl tam bardzo surowe zycie: nieustannie poscil, nosil wlosiennice, dlugie godziny spedzal w konfesjonale, niewiele spal. Nocami byl dreczony przez szatana, czesto w sposob fizyczny: byl bity, zrzucany z lozka, duszony. Inni ksieza podejrzewali, ze sa to omamy wywolane zbyt surowym postem, ale wielu z nich przebywajac w Ars naocznie przekonalo sie o atakach zlego ducha.</p>
<p>Przez swoja dobroc, prostote, skrajne ubostwo, surowa pokute i modlitwe Proboszcz z Ars stopniowo przemienil swoja parafie. Rownoczesnie jednak doswiadczal cierpien duchowych, dreczyly go oschlosc i skrupuly, poczucie winy, ze jest tak wielu ludzi niewierzacych, obawa przed sadem Bozym. Prosil nawet biskupa o zwolnienie z obowiazkow proboszcza, pragnac osiasc w jakims klasztorze, ale po otrzymanej odmowie poslusznie wrocil do parafii i dalej w niej gorliwie poslugiwal.</p>
<p>Jako spowiednik sw. Jan zaslynal na cala Francje. Mial dar czytania w sumieniach ludzkich i dar widzenia przyszlosci. Do jego konfesjonalu ciagnely tlumy ludzi prostych i najwybitniejszych postaci we Francji. Tylko w ostatnim roku zycia wyspowiadal 80 tysiecy ludzi, a lacznie w ciagu 41 lat poslugi w Ars - okolo miliona.</p>
<p>Zmarl 4 sierpnia 1859 r. W jego pogrzebie uczestniczylo okolo 6 tys. wiernych, w tym 300 kaplanow. Papiez Pius X beatyfikowal go w 1905 r. a kanonizacji dokonal w 1925 r. Pius XI, ktory tez cztery lata pozniej oglosil go patronem wszystkich proboszczow.</p>
<p>Ars, gdzie znajduje sie grob sw. Jana Marii Vianneya, jest do dzisiaj jednym z najczesciej odwiedzanych sanktuariow we Francji.</p>
