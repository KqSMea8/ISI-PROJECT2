<h1 class="o_mnie">Dlaczego Ja? O mnie</h1>
<p class="content">


<p>
<img src="img/agata-pawlicka-czarny.jpg?v=1" alt="Agata Pawlicka-Czarny" class="agata" style="float: left; margin: 0 10px 0 0; overflow: hidden;">
Ukonczylam 4-letnie Studium psychoterapii w Instytucie Psychoterapii Integratywnej w Krakowie, w ramach ktorego, odbylam szkolenie certyfikacyjne w zawodzie psychoterapeuty, zgodnie z wytycznymi Ministerstwa Zdrowia i Polskiej Rady Psychoterapii. Jestem terapeutka certyfikowana przez Polskie Towarzystwo Psychoterapii Integratywnej. Swoja prace superwizuje u certyfikowanych superwizorow Polskiej Federacjii Psychoterapii. Odbylam 6 miesieczny staz w Zamknietym Oddziale Psychiatrycznym, przy Specjalistycznym Zespole Psychiatrycznej Opieki Zdrowotnej we Wroclawiu.
</p>
<p style="border-top: none !important;">
Caly czas uzupelniam swoja wiedze praktyczna, ukonczylam nastepujace szkolenia: Diagnoza kliniczna DSM IV - I i II stopien, Integratywna Psychoterapia Krotkoterminowa, Integratywna Psychoterapia Zaburzen Lekowych, Integratywna Psychoterapia Depresji, Hipnoza kliniczna - szkolenie podstawowe i zaawansowane. Psychosomatoterapia, praca z cialem, mindfulness, Terapia pacjentow z zaburzeniem jedzenia i Psychoterapia par.
Od listopada 2016 roku uczestnicze w szkoleniu specjalistycznym "Metody pracy z cialem w praktyce psychoterapeuty", prowadzonym przez Katarzyne Weglorz Makuch trenera i superwizora PFP i EAGT.
</p> <p>
Od kiedy pamietam interesuje sie czlowiekiem jako osoba spoleczna i jako zintegrowana jednostka, ktora stara sie odpowiedziec na pytania, kim jest, dokad zmierza, czy jest wolna, co moze, co powinna itd. Oprocz psychoterapii, poszerzania wiedzy i technik zwiazanych z ta dziedzina, interesuje sie tez rebirthingiem czyli swiadomym oddychaniem, ktore redukuje stres i sprawia, ze mam wiecej energii i witalnosci. Uwielbiam spacery z moim psem, czytanie ksiazek, ktore pomagaja zagladac w siebie i marzyc. Uwielbiam tez spotkania z przyjaciolmi. Od czasu do czasu podrozuje, zeby nabrac dystansu do codziennosci.
</p> <p>
Moja fascynacja ludzmi rozpoczela sie juz w liceum kiedy to, podjelam nauke na profilu pedagogicznym. Posiadam tytul magistra Zarzadzania Kadrami i Psychologii Zarzadzania. Studiowalam tez Filozofie i ukonczylam studia podyplomowe na Wydziale Psychologii Zarzadzania na Uniwersytecie Wroclawskim.
</p>
</p>
