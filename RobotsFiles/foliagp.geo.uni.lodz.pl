<h2 style="text-align: center;"><strong>Acta Universitatis Lodziensis. Folia Geographica Physica</strong></h2>
<div style="text-align: center;"><strong><br /></strong></div>
<p style="text-align: center;"> </p>
<p style="text-align: center;">DOI:10.18778/1427-9711</p>
<p style="text-align: center;"> <a href="http://dspace.uni.lodz.pl:8080/xmlui/handle/11089/2841">URI: 11089/2841</a></p>
<p style="text-align: center;">ISSN 1427-9711 (print)   eISSN 2353-6063 (online)</p>
<h6> <a style="font-size: 1em; line-height: 1.4em; text-align: center;" href="http://dspace.uni.lodz.pl:8080/xmlui/handle/11089/2841"><img style="display: block; margin-left: auto; margin-right: auto;" src="uploads/images/repoz_ul.jpg" alt="logo repozytorium" width="280" height="110" /></a> </h6>
<p style="text-align: center;"><a href="https://www.czasopisma-test.uni.lodz.pl/geophysica"><strong>Folia Geographica Physica - na stronie Wydawnictwa Uniwersytetu Lodzkiego </strong></a></p>
<p style="text-align: center;"><a href="https://www.czasopisma-test.uni.lodz.pl/geophysica"><strong>(OPEN JOURNAL SYSTEMS)</strong></a></p>
<p style="text-align: center;">e-mail:folia.geo.physica@uni.lodz.pl</p>
<p><strong>,,Acta Universitatis Lodziensis. Folia Geographica Physica</strong>" jest czasopismem wydawanym przez Uniwersytet Lodzki od 1997 roku. Od 2013 r. czasopismo ukazuje sie jako rocznik. Zakres tematyczny publikowanych prac obejmuje problematyke roznych dziedzin geografii fizycznej i dyscyplin pokrewnych, w szczegolnosci: geomorfologii, geoekologii, geologii i paleogeografii czwartorzedu, meteorologii i klimatologii, ochrony srodowiska oraz zastosowania GIS w badaniach srodowiska.</p>
<p>Czasopismo publikuje glownie recenzowane artykuly naukowe, prezentujace wyniki oryginalnych badan o charakterze empirycznym, teoretycznym, technicznym lub analitycznym. W uzasadnionych przypadkach ukazuja sie inne prace: krotkie doniesienia naukowe (komunikaty naukowe), materialy konferencyjne, recenzje, a takze wspomnienia i sprawozdania z waznych wydarzen w swiecie geografii fizycznej i nauk pokrewnych.</p>
<p><span style="font-size: 1em; line-height: 1.4em;">Czasopismo jest indeksowane - znajduje sie na liscie B Ministerstwa Nauki i Szkolnictwa Wyzszego. </span></p>
<p><span style="font-size: 1em; line-height: 1.4em;">Wszystkie publikacje czasopisma dostepne sa online. <br /></span></p>
