<h2>Rok 2019</h2>
<p class="small">23.01.2019</p>
<h3><strong><em>Rok 2019</em></strong><em><br></em></h3>
<h3><em>        Serdecznie zapraszamy obecnych i nowych klientow do pielgrzymowania z naszym biurem. </em></h3>
<h3><em>       Jak co roku zachecamy do poglebienia wiary poprzez zapoznanie pielgrzyma z postaciami swietych, miejscami objawien i przede wszystkim mozliwosci modlitwy w cudownych miejscach ... </em><em>     </em><em>                                                                                                                                                                                                        </em><em>          Jak co roku szczegolnie zapraszamy do pielgrzymek, gdzie wiare czuc na kazdym kroku m.in.: do <strong>Stolicy Piotrowej, Fatimy, Medjugorie, Lourdes, Meksyku, Ziemi Swietej, Libanu, Armenii i Gruzji</strong>. </em></h3>
<h3><em>      Jako renomowane i profesjonalne biuro pielgrzymkowe zapewniamy  indywidualne podejscie do kazdej grupy, mozliwosc modyfikacji programu  oraz dostosowania oferty do oczekiwan grupy. Kazdego dnia pielgrzymki  zapewniamy rezerwacje Mszy Swietej w miejscach szczegolnych na trasie  zwiedzania. Naszym grupom zapewniamy zakwaterowanie w sprawdzonych  hotelach na trasie calej pielgrzymki. Cena pielgrzymki kalkulowana jest  zawsze indywidualnie dla grupy i jest uzalezniona od wielu czynnikow,  przede wszystkim o wybranego terminu pielgrzymki i liczby osob w  grupie. </em></h3>
<h3><em>      W zwiazku z  tym, ze czesto nasze programy pielgrzymkowe byly kopiowane przez inne  biura, to na naszej stronie Internetowej nie umieszczamy programow. W  razie zainteresowania konkretnym wyjazdem, prosimy o kontakt  telefoniczny lub mailowy.<strong> </strong>Pielgrzymow indywidualnych  informujemy jak co roku, ze nie prowadzimy wolnego naboru, ale czasami  zdarza sie, ze wybrana parafia nie ma kompletu pielgrzymow i wowczas  jest mozliwosc dopisania sie do listy uczestnikow. Prosimy o sledzenie  naszej strony internetowej.<br></em></h3>
<h3><em>                                 W Roku liturgicznym 2019 zyczymy wszystkim przemiany serc, sily  do wytrwania w tym co dobre, opieki Matki Bozej i Wszystkich swietych.<br>                                                                                   </em></h3>
<h3><em>                                                                                               Sylwia Roszkowska - Skowron</em>                                                                                                                  </h3>



