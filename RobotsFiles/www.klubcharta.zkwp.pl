<div class="entry-content">
Szanowni Panstwo.

Przez dwanascie lat Klub Charta w Polsce - Komisja ZG ZKwP prowadzil ranking wystawowy, w ktorym wzielo udzial 541 psow. Wszystko zaczelo sie w 2006 roku z inicjatywy pana Rafala Zielinskiego. Byly lata z udzialem bardzo wielu psow (2007 rok - 80 psow, 2010 rok - 69 psow)
ale i takie gdzie liczba uczestnikow byla bardzo skromna. Przez te lata ranking w poszczegolnych rasach prowadzilo spolecznie wiele osob.

W 2018 roku Zarzad Glowny Zwiazku Kynologicznego w Polsce wprowadzil ranking ogolnopolski.
W tej sytuacji doszlismy do wniosku, ze nie ma potrzeby powielania wynikow i prowadzenia dwoch rankingow dla chartow.

Dlatego ta dwunasta edycja jest ostatnia.

W imieniu Klubu Charta pragne bardzo serdecznie pogratulowac zwyciezcom oraz Wszystkim uczestnikom, zarowno tegorocznej edycji jak i jedenastu poprzednich. Pragne tez bardzo serdecznie podziekowac Wszystkim za prowadzenie rankingu. Zyczymy powodzenia na wystawach i sukcesow
w ogolnopolskim rankingu.

W imieniu Klubu Charta - Komisji ZG ZKwP
