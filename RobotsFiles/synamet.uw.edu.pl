<h1 class="entry-title">O  projekcie</h1>
</header>

<div class="entry-content">
<p style="text-align: justify">Metafora jako zjawisko z pogranicza systemu pojeciowego i jezyka jest przedmiotem zainteresowania badaczy reprezentujacych rozne dziedziny badan - psychologie, neurologie, teorie literatury, jezykoznawstwo, przetwarzanie jezyka naturalnego itp. Zainteresowanie to przeklada sie na niezwykle bogata literature przedmiotu. Poniewaz metafora jest tak zlozonym zjawiskiem, nasze badania chcemy zaczac od ograniczonego obszaru badawczego i skupic sie na analizie autentycznych, niepreparowanych tekstow. W projekcie SYNAMET ograniczamy sie do analizy metafor synestezyjnych. Metafora synestezyjna polega na uzyciu jednej nazwy do opisu rozmaitych wrazen zmyslowych, np. <em>slodki zapach/kolor/dzwiek</em>. Metafore synestezyjna w niektorych ujeciach rozszerza sie na wszystkie wyrazenia, w ktorych wystepuja wyrazy odnoszace sie prymarnie do jakiejs domeny percepcyjnej, czyli beda to zarowno frazy, typu <em>jasny dzwiek</em> czy <em>slodki zapach</em>, jak konstrukcje <em>jasny umysl</em> lub <em>ciemna sprawa</em>. Wybor metafory synestezyjnej jako przedmiotu badan pozwoli z jednej strony na dosc precyzyjne wyznaczenie obszaru badan, z drugiej strony zas zapewni wystarczajaco bogaty i zroznicowany material do analiz.<a href="http://synamet.uw.edu.pl/wp-content/uploads/sites/61/2015/05/piec-zmyslow.jpg" rel="attachment wp-att-138"><img class="alignright wp-image-138 size-large" src="http://synamet.uw.edu.pl/wp-content/uploads/sites/61/2015/05/piec-zmyslow-1024x576.jpg" alt="piec zmyslow" width="665" height="374" srcset="http://synamet.uw.edu.pl/wp-content/uploads/sites/61/2015/05/piec-zmyslow-1024x576.jpg 1024w, http://synamet.uw.edu.pl/wp-content/uploads/sites/61/2015/05/piec-zmyslow-300x169.jpg 300w, http://synamet.uw.edu.pl/wp-content/uploads/sites/61/2015/05/piec-zmyslow-660x371.jpg 660w, http://synamet.uw.edu.pl/wp-content/uploads/sites/61/2015/05/piec-zmyslow.jpg 1280w" sizes="(max-width: 665px) 100vw, 665px" /></a></p>
<p style="text-align: justify">Niniejszy projekt stawia sobie zatem nastepujace cele:</p>
<ol>
<li style="text-align: justify">Stworzenie bazowego korpusu metafor synestezyjnych, wykorzystujacego niepreparowane teksty z blogow.</li>
<li style="text-align: justify">Opracowanie nowej, efektywnej metody identyfikacji metafor w dyskursie i ich analizy.</li>
<li style="text-align: justify">Zaprojektowanie specjalnych narzedzi do anotacji metafor, analizy wynikow i ich prezentacji.</li>
<li style="text-align: justify">Opracowanie wlasnosci gramatycznych i semantycznych metafory synestezyjnej.</li>
<li style="text-align: justify">Stworzenie modelu synestezji dla jezyka polskiego.</li>
</ol>
<div style="text-align: justify"></div>
<p style="text-align: justify"><span style="font-size: 8pt"><strong><span style="color: #800000">Projekt nr <span class="bold">2014/15/B/HS2/00182, finansowany przez Narodowe Centrum Nauki, <em>Synamet</em> &#8211; <em>m</em></span></span></strong><span style="color: #800000"><em><b><em>i</em>krokorpus</b> <b>metafor </b><b>synestezyjnych. </b><strong>Formalizacja opisu i wypracowanie efektywnych metod analizy metafor w dyskursie.</strong></em></span></span></p>
