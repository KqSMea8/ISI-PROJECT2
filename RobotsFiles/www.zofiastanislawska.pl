<div class="header-body">O mnie</div>

<div>
<p style="text-align: justify;"><strong>O sobie</strong></p>
<p style="text-align: justify;">O mnie, czyli o mojej grafice. Swiat grafiki jest pelen sprzecznosci. To polaczenie surowego warsztatu i ciezkiej pracy fizycznej z radoscia tworzenia. W pracowni grafika obok nozy do ciecia blachy, pilnik&oacute;w i dlut stoja pojemniki z &nbsp;benzyna, denaturatem, roztworem kwasu azotowego, sloiki z krysztalkami siarczanu miedzi i kalafonia. Ale mozna tam takze znalezc cienkie igly, rylce o rozmaitych ksztaltach, szlachetny papier graficzny i kolorowe farby. Po pracochlonnej obr&oacute;bce matrycy przychodzi czas na zabawe z kolorem przy wykonywaniu odbitek. Grafika jest polaczeniem tego, co surowe z tym, co ulotne, skupienia i precyzji z radoscia improwizacji.&nbsp;</p>
<p style="text-align: justify;"><strong>Inni o mnie</strong></p>
<div>,,Ze zdyscyplinowanych, grubych i cienkich kresek, kolorowych plam buduje nastr&oacute;j. Konstruuje swoje obrazy jakby od wewnatrz; jak architekt obudowujacy szkielet budowli. Dalekie i bliskie plany ukladaja sie w niekonczace sie przestrzenie.&nbsp;<span style="text-align: justify; ">Wypukla faktura grafiki kusi, by jej dotknac. W tym bogactwie ton&oacute;w jest zachwyt swiatem, ale tez zadziwienie, nostalgia, smutek, emocje &ndash; dyskretne, jak gdyby skrywane za niewidzialna siatka&quot;.</span><span style="text-align: justify; ">&nbsp;</span></div>
