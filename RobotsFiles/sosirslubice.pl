<div class="csc-header csc-header-n1"><h1 style="text-align:center;" class="csc-firstHeader">WITAMY NA STRONIE SOSiR Sp. z o.o.</h1></div>
<!--  Header: [end] -->

<!--  Text: [begin] -->
<p class="align-center align-justify">Slubicki Osrodek Sportu i Rekreacji jest spolka dzialajaca na przedmiesciach Slubic - iscie malowniczej miejscowosci po zachodniej stronie Polski w wojewodztwie lubuskim. Bliskosc z sasiadujacymi tuz za rzeka Odra - Niemcami nadaje naszemu miastu charakter wyjatkowy, a jego nietypowe- transgraniczne polozenie (w centrum Europy) czyni je niezwykle atrakcyjnym dla licznych turystow, takze spoza granic naszego kraju, stajac sie idealna baza wypadowa na wschod i zachod Europy (w kierunku zachodu Berlin - 80km, na wschod Warszawa - 470km ok. 4h jazdy autostrada A2). </p>
<p class="align-justify">Sposrod obiektow sportowo-rekreacyjnych wyroznic nalezy stadion lekkoatletyczny z&nbsp;nowoczesna 6-torowa bieznia i boiskiem pilkarskim z automatycznym systemem nawadniania, pole golfowe, plac do gry w mini-golfa, 4 pelnowymiarowe boiska pilkarskie, w tym jedno z oswietleniem i sztuczna nawierzchnia, 2 boiska do siatkowki plazowej, strzelnice (obiekt oddany w dzierzawe), sciezke spacerowo-biegowa i tor saneczkowy, hale gimnastyczne, basen plywacki otwarty o&nbsp;wymiarach olimpijskich (czynny w okresie letnim), silownie oraz centrum odnowy biologicznej (obecnie oddane w dzierzawe).</p>
<p class="align-justify">Uwaga! Baza hotelowo- gastronomiczna obecnie jest oddana w dzierzawe. Wszelkie informacje z zakladce &quot;Hotel Gastronomia&quot;. SOSiR Slubice scisle wspolpracuje z dzierzawca oferujac wykorzystanie naszych obiektow sportowo- rekreacyjnych.</p>
<p class="align-justify">Nasi wspolni goscie maja do dyspozycji 58 pokoi hotelowych, restauracje, kawiarnie i bar. Baza hotelowa, mogaca jednoczesnie pomiescic 132 osoby posiada bogate zaplecze gastronomiczne, zachwycajac swym niepowtarzalnym smakiem. Aktualnie trwa generalny remont obiektow hotelowych, po ktorym baza zostanie znaczaco poprawiona oraz zwiekszona.&nbsp;</p>
<p class="align-justify">Dla klubow sportowych posiadamy specjalna oferte eventow, obozow i zgrupowan. Firmy moga liczyc na nasze doswiadczenie w organizacji bankietow, prezentacji, konferencji i szkolen. </p>
<p class="align-justify">Jestesmy przekonani, ze ciekawa propozycja sportowo-rekreacyjna w polaczeniu z niepowtarzalnym krajobrazem zachwyci swym pieknem niejednego milosnika turystyki i przyrody, albowiem bezposrednia lokalizacja w kompleksie parkowo-lesnym zdaje sie szczegolnie sprzyjac osobom lubiacym aktywny wypoczynek, co stwarza doskonale warunki do&nbsp; uprawiania wielu dyscyplin sportu tak relaksacyjnie jak i wyczynowo. </p>
<p class="align-justify">Nasza firma z uwagi na wieloletnia dzialalnosc oraz praktyke w organizacji tysiecy wydarzen sportowych o randze wojewodzkiej, ogolnopolskiej i miedzynarodowej, jak rowniez w prowadzeniu imprez kulturalnych, okolicznosciowych jak i integracyjnych stawia na profesjonalizm oraz jakosc oferowanych uslug, a co za tym idzie - zadowolenie klientow. Wykwalifikowany personel dokonuje wszelkich staran w spelnieniu oczekiwan naszych gosci, tak by pobyt w&nbsp;kompleksie pod haslem: sport, relaks i wypoczynek byl wykorzystany w pelni i pozostal na dlugo w ich pamieci.</p>
<p class="align-center">Jezeli chcecie Panstwo przezyc niezapomniane chwile w klimacie sportu i rekreacji serdecznie zapraszamy do&nbsp;skorzystania z naszych uslug.</p>
