<h1>Laryngologia</h1>
<h3>lekarz laryngolog - Malgorzata Sikorska-Zuk</h3>
<p>Leczymy:</p>
<ul>
<li>dolegliwosci zatok i nosa</li>
<li>dolegliwosci uszu i zawroty glowy</li>
<li>problemy ze sluchem</li>
<li>chrapanie</li>
<li>problemy z gardlem, migdalami i krtania</li>
</ul>
<p>Badanie endoskopowe, ktore pozwala na ekranie monitora zobaczyc badane
narzady co ulatwia lekarzowi postawienie trafnej diagnozy.</p>

<p>Obraz z ekranu zapisywany jest w pamieci komputera i wydrukowany. Dzieki temu
mozliwe jest porownanie stanu nosa i okolic ujscia zatok z wynikami wczesniej
przeprowadzonych badan. Po badaniu pacjent otrzymuje wydruk z badania
endoskopowego oraz zalecenia do dalszego postepowania i leczenia farmakologicznego.</p>

<p>Jesli podczas badania endoskopowego lekarz uzna, ze wymagane jest
przeprowadzenie dodatkowych badan diagnostycznych, wowczas pacjent dostaje
skierowanie na takie badanie. W niektorych wypadkach trzeba wykonac tomografie
komputerowa zatok.</p>			</div>
