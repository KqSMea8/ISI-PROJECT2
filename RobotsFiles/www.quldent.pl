<section class="leczenie">
<div class="content">
<h1>Leczenie</h1>
<div>
<div><a href="../implanty"><img src="data/images/leczenie-implantologia.jpg" alt="leczenie implantologia" /></a>
<h4>Implantologia</h4>
<p>Implanty dentystyczne stanowia bezpieczna, potwierdzona klinicznie i estetyczna alternatywe dla tradycyjnych metod odbudowy brak&oacute;w uzebienia (koron, most&oacute;w, protez). Odbudowa protetyczna na wszczepionym implancie wyglada i jest odczuwana jak naturalny zab zapewniajac wysoki poziom komfortu, bezpieczenstwa, oraz pewnosci przewyzszajacej tradycyjne metody leczenia.&nbsp;</p>
</div>
<div><a href="../wybielanie"><img src="data/images/leczenie-stomatologia-estetyczna.jpg" alt="leczenie stomatologia estetyczna" /></a>
<h4>Stomatologia estetyczna</h4>
<p>W naszej klinice bardzo mocny nacisk kladziemy na estetyke. Proponujemy wam takie zabiegi jak: wybielanie, skaling oraz piaskowanie, kt&oacute;re w bardzo duzym stopniu wplyna na koloryt waszego uzebienia. Dodatkowo w naszym labolatorium stworzymy dla ciebie lic&oacute;wki, most oraz korony zaprojektowane w systemach wirtualnych Cad/Cam&nbsp;</p>
</div>
<div><a href="../ortodoncja"><img src="data/images/leczenie-ortodoncja.jpg" alt="leczenie ortodoncja" /></a>
<h4>Ortodoncja</h4>
<p>W naszej klinice stomatologicznej Qul-Dent przyjmuje doktor nauk medycznych z zakresu ortodoncji Krzysztof Kachniarz. Krzyszytof ukonczyl Wroclawska Akademie Medyczna, jest czlonkiem Polskiego Towarzystwa Stomatologicznego, Polskiego Towarzystwa Ortodontycznego oraz Europejskiego Towarzystwa Ortodontycznego. Dodatkowo po wiecej informacji zapraszamy na strone internetowa www.drkachniarz.pl&nbsp;</p>
</div>
<div><a href="../protezy"><img src="data/images/leczenie-protetyka.jpg" alt="leczenie protetyka" /></a>
<h4>Protetyka</h4>
<p>Dzial w kt&oacute;rym gl&oacute;wnie specjalizuje sie nasza klinika. U nas wszystko tworzone jest w oparciu o najnowsze systemy komputerowe. Wyciski pobierane sa skanerem wewnatrzustnym nastepnie przesylane sa do naszego centrum frezowania kt&oacute;re znajduje sie na miejscu w klinice, nastepnie projektujemy prace w systemie Cad/Cam i frezujemy w komputerowej frezarece. Tak przygotowana praca jest wpelni precyzyjna, funkcjonalna, i niebywale estetyczna.</p>
</div>
<div><a href="../endodoncja"><img src="data/images/leczenie-endodoncja.jpg" alt="leczenie endodoncja" /></a>
<h4>Endodoncja</h4>
<p>Leczenie kanalowe pod mikroskopem to dla nas podstawa wsp&oacute;lczesnej stomatologi. Bardzo precyzyjne oraz dokladne leczenie wykonujemy przy uzyciu mikroskopu z optyka Zeiss. My widzimy to czego nie wiidza inni.&nbsp;</p>
</div>
<div><a href="../panorama"><img src="data/images/leczenie-RTG.jpg" alt="leczenie RTG" /></a>
<h4>RTG</h4>
<p>W naszej klinice posiadamy bardzo dobrze wyposazone pomieszczenie radiologiczne. Wykonujemy zdjecia Tomograficzne glowy, pantomograficzne, cefalometryczne, przeswietlenia dloni, oraz zdjecia pojedynczych zeb&oacute;w. Dodatkowo dbajac o wasze zdrowie kazde z urzadzen podaje bardzo niska dawke promieniowania.</p>
</div>
<div><a href="../thewand"><img src="data/images/leczenie-leczenie-bez-bolu.jpg" alt="leczenie bez b&oacute;lu" /></a>
<h4>Leczenie bez b&oacute;lu</h4>
<p>Mila atmosfera, podtlenek azotu, komputerowe znieczulenie to tylko czesc naszego arsenalu w walce z b&oacute;lem. W naszej pracy bardzo czesto stosujemy znieczulenie The Wand, srodek znieczulajacy podawany jes z bardzo malym cisnienie a igla jest na tyle mala ze nasi pacjeci nie odczuwaja dyskomfortu podczas podawania znieczulenia, oraz podczas dalszego leczenia.</p>
</div>
</div>		</div>
