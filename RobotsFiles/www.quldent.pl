{
var resetItem = true,
parents	  = $( e.target ).parents();

for( var i = 0; i < parents.length; i++ )
if( parents[ i ] == curItem[ 0 ] )
resetItem = false;

if( resetItem )
curItem = false;
});
});
return this;
};
