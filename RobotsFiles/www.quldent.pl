<div class="content">
<div class="left-col">
<h1>Elektroradiologia</h1>
<h3>Panorama</h3>
<h3>Tomografia</h3>
<p>Tomograf VOLUX 9 firmy Genoray laczy w sobie duze pole obrazowania o wyzszej rozdzielczosci z zaawansowana technologia rekonstrukcji 3D i automatycznym systemem pozycjonowania.</p>
<p>Duze pole obrazowania umozliwia precyzyjny skan odpowiedniego obszaru twarzoczaszki podczas jednego badania. Moga to byc oba luki zebowe z zatokami szczekowymi i stawami skroniowo-zuchwowymi lub wszystkie zatoki oboczne nosa, a nawet caly kregoslup szyjny oraz kosc skroniowa z uchem.</p>
<p>Badanie wykonywane jest w pozycji siedzacej z wykorzystaniem w pelni automatycznego systemu pozycjonowania SCOUT ze wstepnym podgladem wybranego obszaru badania. Bardzo kr&oacute;tki czas skanu i dobra stabilizacja pacjenta zapewniaja wysoka jakosc badania przy bardzo malej dawce promieniowania.</p>
<p>Innowacyjny system VOI (Volume of Interest) zapewnia uzyskanie podwyzszonej rozdzielczosci wybranego obszaru, nawet ponizej 0,1 mm.</p>
<h3>Radiowizjografia</h3>
<p>Radiowizjografia cyfrowa to polaczenie badania USG z rentgenem daje szerokie mozliwosci badawcze, kt&oacute;re maja bezposredni wplyw na skuteczne leczenie pacjenta i zaoszczedzenie mu niepotrzebnych stres&oacute;w zwiazanych z dodatkowymi wizytami w gabinecie stomatologicznym. Dzieki radiowizjografii dentysta jest w stanie ocenic dlugosc kanalu zeba, gestosc kosci, poprawnosc wypelnienia kanalu itp. Uzyskany obraz mozna powiekszac, skupiajac sie nad wybranym fragmentem tkanki. Dzieki temu badaniu, mozna wykryc wczesne stadium najczesciej wystepujacej choroby zeb&oacute;w - pr&oacute;chnicy. Podczas badania stomatolog przyklada konc&oacute;wke rekawa aparatury i na ekranie monitora natychmiast widac wynik badania - obraz zeba.</p>
</div>
