<p><b>Drodzy Przyjaciele swietego Jana Bosko.</b></p>
<p>Od 1997 roku nasza wspolnota parafialna i zakonna wznosi swiatynie, ktorej patronem jest Ojciec i Nauczyciel Mlodziezy - swiety Jan Bosko (1815-1888). W 2015 roku minela dwusetna rocznica Jego urodzin. To wydarzenie zmobilizowalo nas po raz kolejny do wzmozonego wysilku przy budowie.</p>
<p><b>Jestesmy wdzieczni za kazda, nawet najmniejsza ofiare. </b></p>
<p>Ofiarodawcom, ktorzy poprzez systematyczne wplaty (np. na przestrzeni dwoch, trzech lat) przekaza w sumie kwote powyzej 1000&nbsp;zl poswiecimy pamiatkowa tablice na murach kosciola. </p>
<p>Pamietamy i jestesmy wdzieczni wszystkim ofiarodawcom. Zapewniamy o naszej modlitwie zwlaszcza podczas Mszy Swietej w kazdym ostatnim dniu miesiaca.</p>
<p>Zachecamy do odwiedzenia naszej strony internetowej w celu wyboru konkretnego miejsca zamontowania pamiatkowej tablicy.</p>
