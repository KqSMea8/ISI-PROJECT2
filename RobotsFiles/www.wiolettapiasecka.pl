<div class="author">
<div class="author_header">Wioletta Piasecka </div>

<div class="author_header_2">Autorka bajek i basni dla dzieci.</div>

<br />

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Urodzona. 4 czerwca 1970r. w Elblagu. Ukonczyla Uniwersytet Gdanski Wydzial Historyczno-Filologiczny - Filologia Polska. Prace magisterska napisala pod okiem prof. dr hab. Gertrudy Skotnickiej z dziedziny literatury dla dzieci i mlodziezy. Temat pracy ,,Basnie Hansa Chrystiana Andersena znane i nieznane - badania nad czytelnictwem." <b>Jest autorka ponad 40 basni i bajek dla dzieci, dwoch biografii m.in. Hansa Chrystiana Andersena i Faustyny Morzyckiej, czterech sztuk teatralnych, wielu sluchowisk radiowych.</b></p><br />

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Zadebiutowala w 1999 roku zbiorem pt. <b><i>Zaczarowana kraina bajek</i></b>, wydanym przez wydawnictwo Qvo Vadis. Znalazlo sie tam siedem utworow. W 2001r. po raz pierwszy ukazala sie w Krakowie, wydana przez wydawnictwo Skrzat, biografia H.CH. Andersena pt. <b><i>W poszukiwaniu szczescia. Basniowa biografia Andersena.</i></b></p><br />

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Jednak sukces wydawniczy i autorski przyniosla wspolpraca z wydawnictwem DROZD Elblag, ktora zaowocowala seria pt. ,,Basnie Wioletty Piaseckiej" Seria zawiera 12 zbiorow basni, sa to:</p><br />
-<b><i> Zaczarowane babelki,</i></b> <br />
-<b><i> Bajka na dobranoc,</i></b> <br />
-<b><i> Dwie Malgosie,</i></b> <br />
-<b><i> Bajka o krolewnie,</i></b> <br />
-<b><i> Szesc zyczen Natalki,</i></b> <br />
-<b><i> Dawno temu...,</i></b><br />
-<b><i> W poszukiwaniu szczescia. Basniowa biografia Andersena,</i></b><br />
-<b><i> Wloczega,</i></b> <br />
-<b><i> Domek marzen,</i></b><br />
-<b><i> Zasluzyc na fiolki. Basn biograficzna o Faustynie Morzyckiej,</i></b><br />
<p>-<b><i> Ksiezniczka dzieciecych serc,</i></b></p><br />

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ponadto basn <b><i>Dawno temu w Betlejem</i></b> zostala wydana przez Wydawnictwo sw. Wojciecha.</p><br />

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;W 2003r. miala debiut pierwsza sztuka teatralna pt. <b><i>,,O krolewnie, piekarczyku i pieczeniu chleba"</i></b> zagrana przez agencje artystyczna Qfer, podczas uroczystego zakonczenia Jarmarku sw. Dominika w Gdansku. Sztuka ta otrzymala rowniez pierwsza nagrode podczas ogolnopolskiego przegladu sztuk teatralnych fArt w Klodzku.</p><br />

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;W 2005r. ksiazka <b><i>Dawno temu...</i></b> zostala uznana za najlepsza ksiazke roku i otrzymala glowna nagrode Fundacji Elblag. W 2004r. Wioletta Piasecka za swoja tworczosc otrzymala wpis honorowy w Zlotej Ksiedze Ludzi Wielkiego Umyslu Talentu i Serca Fundacji Zdazyc z Pomoca w Warszawie.</p><br />

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Wspolpracuje z Radiem Lublin S.A., gdzie na antenie od listopada 2003r. niemal codziennie emitowane sa sluchowiska pt. ,,Basnie Wioletty Piaseckiej" </p><br />

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Od listopada 2003r. do chwili obecnej na zaproszenie bibliotek, szkol, przedszkoli i ksiegarn odbyla okolo tysiaca spotkan autorskich na terenie calego kraju i za granica.</p><br />

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Basnie Wioletty Piaseckiej wielokrotnie wykorzystywano jako basnie terapeutyczne m. in. w Osrodku Szkolno Wychowawczym w Skarzysku Kamiennej, w Osrodku Szkolno-Wychowawczym w Szerzawach k. Mogilna, w Osrodku Szkolno-Wychowawczym im. Korczaka w Elblagu i wielu innych. Basnie Wioletty Piaseckiej byly tematem wielu prac magisterskich.</p><br />

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ksiazki Wioletty Piaseckiej wielokrotnie wygrywaly plebiscyty czytelnicze m. in. w Bibliotece Publicznej w Warszawie Dzielnicy Wilanow, Bibliotece Publicznej w Pawlowicach, Bibliotece Publicznej w Slawnie, Szkole Podstawowej Nr 12 w Elblagu, Bibliotece Publicznej w Siedlcach i wielu innych. </p><br />
