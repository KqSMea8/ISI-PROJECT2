<h3><strong>Szanowni Panstwo</strong></h3>
<p style="text-align:justify;">Nasza firma ma duze doswiadczenie w doborze drewna. Wiemy jak je przygotowac, aby spelnilo oczekiwania najbardziej wymagajacych klientow. Jestesmy wiec doskonalym partnerem dla tych osob, ktore chca najwyzszej jakosci.</p>
<p style="text-align:justify;">Kochamy przyrode i doskonale wiemy jak czerpac z jej zasobow nie obciazajac jej niepotrzebnie. Oferujemy Panstwu najlepszej  jakosci drewno do kominka w przystepnej cenie. W naszej ofercie znalezc mozna wiele rodzajow drewna - zarowno sezonowanych jak i niesezonowanych.<br />Z pewnoscia nasze drewno kominkowe przyczyni sie do tego, ze Panstwa dom bedzie zawsze cieply i przytulny.</p>
<p style="text-align:justify;">Cieplo domowego ogniska wydatnie podgrzeje kominek, do ktorego niewatpliwie potrzebne jest odpowiednie drewno kominkowe. Najlepiej nabyc je w naszej firmie, gdyz lupki pochodza wylacznie z Lasow Panstwowych i sa bardzo kaloryczne, co przeklada sie na piekny plomien i walory grzewcze. Proponujemy przede wszystkim drewno kominkowe z drzew lisciastych, z uwagi na ich zdecydowanie lepsze wlasciwosci grzewcze i zapachowe.</p>
