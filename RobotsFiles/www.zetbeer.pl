<h1 class="title">O nas</h1>



<p>Przedsiebiorstwo Produkcyjno-Budowlane "ZETBEER" Sp. z&nbsp;o.o. w&nbsp;Orzeszkowie jest na rynku drzewnym od 1992 roku. Doswiadczenie i&nbsp;solidnosc sa naszymi najwiekszymi atutami a&nbsp;nowoczesny park maszynowy i&nbsp;fachowa obsluga gwarantuje terminowosc zamowien.</p>

<p>Jestesmy producentem tarcicy iglastej i&nbsp;lisciastej , specjalizujemy sie w&nbsp;konstrukcjach dachowych, mamy rowniez wlasny program ogrodowy. W naszej ofercie znajda Panstwo zarowno deski, krawedziaki, laty, kontrlaty jak i&nbsp;paliki ogrodowe, rolbordery i&nbsp;ploty.</p>

<p>Wysoka jakosc naszych wyrobow zapewnia zespol suszarni oraz autoklaw do impregnacji drewna metoda prozniowo-cisnieniowa. Stosowane przez nas srodki posiadaja wymagane atesty a&nbsp;jakosc produktow potwierdza certyfikat FSC(r) oraz NTR - patrz CERTYFIKATY.</p>


