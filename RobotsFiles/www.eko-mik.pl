<p class="title">EKO-MIK Hieronim Mikolajczak</p>
<p>
Jestesmy autoryzowanym dystrybutorem produktow firmy EKOPOL. Montujemy <b>oczyszczalnie sciekow w Poznaniu</b> i calej wielkoposce.
W naszej ofercie znajda panstwo: oczyszczalnie biologiczne, oczyszczalnie ekologiczne, zbiorniki bezodplywowe oraz uslugi: montaz oczyszczalni i zbiornikow, uslugi koparko-ladowarka, ukladanie kostki brukowej, nawadnianie upraw i ogrodow.
</p>
