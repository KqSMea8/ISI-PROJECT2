<p class="title">OCZYSZCZALNIE BIOLOGICZNE TYPU BIO</p>
<p><img src="obrazy/biologiczne_glowna.jpg" align="left">
Polecane przez nasza firme biologiczne oczyszczalnie sciekow pracuja w oparciu o zloze biologiczne i osad czynny. Sa to kompletne zestawy sluzace do oczyszczania sciekow bytowych i przemyslowych. Sprawnosc oczyszczalni biologicznych wynosi 97 % i z latwoscia spelnia surowe normy prawne jakie nalezy spelnic podczas wprowadzania sciekow do wod i do ziemi.  Tak oczyszczona wode mozemy zgodnie z prawem odprowadzic bezposrednio do rowu, rzeki, studni chlonnej jak i do drenazu rozsaczajacego. Oczyszczona wode mozemy takze wykorzystac do celow gospodarczych na przyklad do nawadniania ogrodu stosujac do tego celu zraszacze.
Na wszystkie sprzedawane przez nas wyroby, uslugi montazowe oraz prawidlowe dzialanie instalacji udzielamy 10 letniej gwarancji.
