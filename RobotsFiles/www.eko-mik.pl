<p class="title">OCZYSZCZALNIE EKOLOGICZNE TYPU EKO</p>
<p>
<img src="obrazy/ekologiczne_glowna.jpg" align="left">
Oczyszczalnie ekologiczne typu EKO oparte sa o dwu lub trzykomorowy zbiornik z wbudowanym separatorem tluszczu i filtrem umiejscowionym na wyjsciu sciekow. Oczyszczalnie tego typu montuje sie w dobrych warunkach gruntowo wodnych
( grunt dobrze przepuszczalny, wody gruntowe min. 1, 5 m ponizej nitki rozsaczajacej) i wylacznie z drenazem rozsaczajacym. Oczyszczalnia wykonana jest z zywic poliestrowych i wlokna szklanego. Material ten zapewnia calkowita szczelnosc wyrobow i wysoka wytrzymalosc konstrukcji. Jest on odporny na dzialanie wszelkich substancji chemicznych znajdujacych sie w sciekach. Na wszystkie sprzedawane przez nas wyroby, uslugi montazowe oraz prawidlowe dzialanie instalacji udzielamy 10 letniej gwarancji.
