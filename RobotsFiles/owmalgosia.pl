<li>rejsy wycieczkowe (w tym na Bornholm), wedkarskie,</li>
<li>punkty handlowe i gastronomiczne, poczta, kosciol,</li>
<li>park wodny z morska woda.</li>
</ul>
<p align="justify">Osrodek Wypoczynkowy Malgosia to rowniez doskonale miejsce na  organizacje takich wydarzen jak:</p>
<ul>
<li>szkolenia i konferencje, </li>
<li>spotkania biznesowe
i integracyjne. </li>
</ul>                      </td>
