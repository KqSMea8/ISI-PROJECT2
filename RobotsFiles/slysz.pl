<h2>Ceny transferowe</h2>
<p class="item-intro text-muted">Wyjasnienie terminu i oferta Kancelarii</p>
<p>Pojecie ,,ceny transferowe" stanowi polski odpowiednik miedzynarodowego terminu, pochodzacego z jezyka angielskiego, ,,transfer pricing". W literaturze podaje sie rozne jego pierwotne zrodla, jednak najczesciej mozna znalezc informacje, iz wywodzi sie ono z teorii programowania matematycznego Jacka Hirshleifera.</p>

<p>Termin ,,ceny transferowe" przyjal sie na gruncie krajowego prawa podatkowego i od dluzszego czasu sluzy do okreslania cen, wedlug ktorych przedsiebiorstwo wykonuje uslugi, dostarcza towary lub wartosci niematerialne i prawne przedsiebiorstwom powiazanym. Ustawodawca w Ordynacji Podatkowej posluguje sie terminem ,,ceny transakcyjnej" zamiast ,,ceny transferowej", mozna jednak przyjac, ze zakres tych pojec jest identyczny.</p>

<p>Ustalenie prawidlowego poziomu cen transferowych nie jest rzecza prosta. Prawidlowo ustalony poziom cen transferowych musi znalezc swoje odbicie w planowaniu podatkowym, nalezy wziac pod uwage niezmiernie duzo informacji, nie tylko z zakresu prawa podatkowego czy prawa celnego, ale takze kursy walut czy elementy ekonomiczne, jakie maja wplyw na okreslenie poziomu zysku netto z danej transakcji a przez to bezposrednio wplywaja na wysokosc ceny transferowej, ktora musi zachowac poziom rynkowy. Niezaleznie os sposobu ustalania poziomu cen transferowych, przywolane dane musza byc uwzglednione w dokonywanych na potrzeby wlasciwej dokumentacji obliczeniach.</p>

<p>Nalezy pamietac, ze ustalony poziom cen transferowych podlega kontroli podatkowej, ktora ma za zadanie wykazac, czy ceny transferowe ustalone pomiedzy podmiotami powiazanymi sa wspolmierne w relacji do swiadczen wzajemnych, tzn. wykazac czy poziom na ktorym znajduja sie ceny transferowe jest poziomem rynkowym.</p>

<p>Obecnie w krajowym porzadku prawnym regulacje dotyczace cen transferowych zamieszczone sa w ustawie o podatku dochodowym od osob prawnych w art. 9a i art. 11, ustawie o podatku dochodowym od osob fizycznych w art. 25 i art. 25a, a takze w rozporzadzeniu Ministra Finansow w sprawie sposobu i trybu okreslania dochodow podatnikow w drodze oszacowania cen w transakcjach dokonywanych przez tych podatnikow, Minister Finansow w osobnym rozporzadzeniu okreslil ponadto liste krajow i terytoriow stosujacych szkodliwa konkurencje podatkowa.</p>

<p>Uregulowania dotyczace uprzednich porozumien cenowych znajduja sie w Dziale IIa Ordynacji Podatkowej i weszly w zycie z poczatkiem 2006 roku, daja one mozliwosc podmiotom krajowym uzyskania potwierdzenia dotyczacego prawidlowego wyboru i stosowania metody ustalania ceny transakcyjnej.</p>

