Systematyczny wzrost ilosci obslugiwanych firm i klientow umozliwil ciagly rozwoj Biura. W roku 1994 siedziba zostala przeniesiona na ul. Sienkiewicza 11, zmienila sie takze nazwa - Biuro Podatkowo-Ksiegowe. Od poczatku istnienia Biura, kieruje nim osobiscie wlasciciel - mgr Tadeusz Slysz, absolwent Wydzialu Prawa i Administracji Uniwersytetu Jagiellonskiego w Krakowie. Tadeusz Slysz specjalizuje sie ponadto w prawie upadlosciowym. Od 1993 roku ustanawiany jest przez Sady Syndykiem Masy Upadlosci. Powolany jest rowniez przez akcjonariuszy jako likwidator w spolkach kapitalowych. Zajmuje sie restrukturyzacja firm oraz audytem podatkowym. </p>
