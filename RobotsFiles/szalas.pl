<h1>Noclegi Koscielisko &#8211; Zakopane</h1>
<p><a title="Szalas pod Szymoszkowa" rel="milkbox" href="galeria/4_Widoki/4a_widok.jpg"><img class="alignright size-full z_ramka" style="margin: 0px;" title="Szalas pod Szymoszkowa" src="http://szalas.pl/panelik/wp-content/uploads/2011/05/zdjecia-widoki3.jpg" alt="Widok na Szalas pod Szymoszkowa zima" width="190" height="134" /></a>Witamy Panstwa na stronach Szalasu pod Szymoszkowa.</p>
<p>Oferujemy Panstwu pokoje goscinne w pieknie  wykonczonym drewnianym domu, usytuowanym w urokliwym miejscu z widokiem na rozlegla panorame Tatr. Dom polozony jest w poblizu:</p>
<ul>
<li>nowoczesnego kompleksu Rekreacyjno-Wypoczynkowego Polana Szymoszkowa (Stacja narciarska oswietlona, z nowoczesnymi wyciagami krzeselkowymi i kapielisko z ciepla woda obok hotelu Mercury &#8211; Kasprowy &#8211; odleglosc 500m.),</li>
<li>wyciagu widokowego Butorowy Wierch,</li>
<li>wyciagu narciarskiego Budzowski Wierch (dwa wyciagi orczykowe,oswietlone, dla mniej zaawansowanych narciarzy, tania wypozyczalnia sprzetu, szkolka narciarska z instruktorami, parking, odleglosc 300m.),</li>
<li>Sanktuarium Matki Bozej Fatimskiej na Krzeptowkach.</li>
</ul>
<p>Szalas pod Szymoszkowa posiada bardzo dogodna lokalizacje do planowania wycieczek gorskich w Tatry. Mozna stad latwo dostac sie m.in. do Doliny Malej Laki, Doliny za Bramka, Doliny Koscieliskiej , Doliny Chocholowskiej czy Doliny Strazyskiej.</p>
