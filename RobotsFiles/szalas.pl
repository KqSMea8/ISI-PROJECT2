<h2>Komfortowe Pokoje Koscielisko</h2>
<p style="text-align: left;"><a href="galeria/1_Pokoje/1_1_B12A_pokoje.jpg" rel="milkbox"><img class="alignright size-full z_ramka" style="margin: 0px 0px 0px 10px;" src="http://szalas.pl/panelik/wp-content/uploads/2011/03/zdjecia-pokoje2.jpg" alt="Zdjecie pokoju" width="200" height="133" /></a>Do dyspozycji Panstwa sa pokoje dwu, trzy oraz czteroosobowe z lazienkami, wyposazone w TV i szerokopasmowy internet bezprzewodowy oraz przewodowy. Z okien pokoi roztacza sie widok na panorame Tatr.</p>
<p><a title="Galeria" href="galeria.php">Zapraszamy do obejrzenia zdjec pokoi w naszej galerii</a> oraz do zapoznania sie z <a title="wiecej szczegolow" href="pokoje.php">szczegolami oferty noclegow</a></p>
<p style="text-align: left;"><a title="Izba do Gwarzenia" href="http://www.szalas.pl/panelik/wp-content/uploads/2011/03/IMG_2773_v6.jpg" rel="milkbox"><img class="alignright size-medium z_ramka" style="margin: 0px 0px 0px 20px;" title="Izba do Gwarzenia" src="http://www.szalas.pl/panelik/wp-content/uploads/2011/03/IMG_2773_v6-199x300.jpg" alt="Zdjecie Izby do Gwarzenia" width="159" height="240" /></a>Przygotowujemy dla Panstwa smaczne sniadania, ktore podajemy w stylowej Izbie do Jedzenia. Jest takze mozliwosc samodzielnego przyrzadzania sniadan i kolacji w osobnym, ogolnodostepnym aneksie kuchennym. Nasi goscie po powrocie z wycieczki gorskiej czy nart, moga odpoczac, spotkac sie towarzysko czy ogrzac przy kominku w Izbie do Gwarzenia. Wiecej zdjec obu izb mozna obejrzec w <a title="galeria" href="galeria.php">galerii.</a></p>
