<div class="TextContent Justify">
<span class="Marg">Kancelaria Radcy Prawnego "In extenso" w Krakowie zostala utworzona w 1997 roku przez radce prawnego Jana Stalmacha.
Kancelaria prowadzi tez dorazna wspolprace z innymi adwokatami i radcami prawnymi w zaleznosci od aktualnych potrzeb i rodzaju
prowadzonych spraw.</span><br /><br />
<span class="Marg">Na stanowisku pracownikow merytorycznych w charakterze konsultantow prawnych zatrudnieni sa absolwenci Wydzialu Prawa.
Prawnicy Kancelarii tworza profesjonalny zespol reprezentujacy wysoki poziom wiedzy prawnej.</span><br /><br />
<span class="Marg">Kancelaria prowadzi sprawy <b>cywilne</b>, w tym gospodarcze <b>w pelnym zakresie</b>, a takze sprawy z zakresu prawa pracy i ubezpieczen
spolecznych, prawa i postepowania administracyjnego, w tym postepowania przed sadami administracyjnymi. Specjalizuje sie w sprawach majatkowych,
w tym z zakresu prawa rzeczowego, zobowiazan i prawa spadkowego.<br>
Sprawuje zastepstwo procesowe we wszystkich klasycznych sprawach cywilnych, w tym gospodarczych.</span><br /><br />
<span class="Marg">Kancelaria prowadzi nadto sprawy <b>rodzinne</b>, a w szczegolnosci zwiazane z problematyka prawa malzenskiego
(<b>rozwody, separacje</b>, majatkowe), osobowego oraz sprawy opiekuncze.</span><br /><br />
<span class="Marg">Podstawowa forma dzialania Kancelarii jest <b>swiadczenie stalej pomocy prawnej</b> dla uczestnikow obrotu prawnego, w tym <b>spolek prawa
handlowego</b> i innych podmiotow gospodarczych, <b>stowarzyszen, jednostek samorzadu terytorialnego</b> w tym urzedow gmin i jednostek
organizacyjnych gmin, <b>zakladow opieki zdrowotnej</b>. Dla wymienionych tu jednostek organizacyjnych swiadczona jest tez dorazna pomoc
prawna w zakresie spraw powierzonych.</span><br /><br />
<span class="Marg">Prowadzimy takze kompleksowa <b>obsluge prawna przedsiebiorcow</b>, w szczegolnosci prowadzacych dzialalnosc gospodarcza w formie spolek
prawa handlowego, izb gospodarczych, organizacji pracodawcow.</span><br /><br />
<span class="Marg">Swiadczenie pomocy prawnej obejmuje wszystkie sprawy zwiazane z dzialalnoscia mocodawcy i polega na zastepstwie procesowym w postepowaniu
sadowym <b>w procesie</b>, w postepowaniu <b>nieprocesowym</b>, jak tez w postepowaniu <b>rejestrowym, wieczystoksiegowym i egzekucyjnym</b>.
Pomoc prawna obejmuje takze konsultacje prawne, przygotowywanie i opiniowanie projektow aktow prawnych, projektow umow,
udzial w rokowaniach, zastepstwo prawne. Dodatkowa usluga swiadczona dla stalych klientow jest prowadzenie <b>stalych dyzurow w siedzibie
zleceniodawcy</b>.</span><br /><br />
