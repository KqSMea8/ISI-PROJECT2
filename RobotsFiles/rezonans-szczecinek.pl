<p>Dysponujemy 32-warstwowym tomografem komputerowym SOMATOM PERSPECTIVE 32 firmy SIEMENS. Tomografia komputerowa jest badaniem bardzo wszechstronnym, umozliwia dokladne obrazowanie czaszki, klatki piersiowej, jamy brzusznej, calego kregoslupa oraz miednicy.</p>
