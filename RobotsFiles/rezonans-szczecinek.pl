<h1>Rezonans magnetyczny i tomografia komputerowa</h1>
<p>Zarowno rezonans magnetyczny, jak i tomografia komputerowa to dwie bardzo wazne metody badawcze, obrazujace wnetrze ludzkiego ciala. Sa one powszechnie wykorzystywane w diagnostyce i roznicowaniu rozmaitych schorzen. Na czym polegaja i czym sie od siebie roznia?</p>
<p>Rezonans magnetyczny (MRI) wykorzystuje pole magnetyczne, a tomografia komputerowa (TK) - promieniowanie rentgenowskie. Zarowno MRI jak i TK wykonywane sa miedzy innymi w badaniach ukladu miesniowo-szkieletowego, w diagnostyce nowotworow, naczyn krwionosnych, osrodkowego ukladu nerwowego, srodpiersia, miednicy oraz wszelkich tkanek miekkich (w tym urazow oraz guzow).</p>
<p>Ze wzgledu na promieniowanie rentgenowskie badanie tomograficzne uznawane jest za bardziej inwazyjne dla organizmu i posiada pewne ograniczenia, jak chocby zakaz przeprowadzania go u kobiet ciezarnych. W przypadku badania rezonansem takie ryzyko nie wystepuje. TK jest badaniem tanszym od MRI i bardziej powszechnym. Wybor badania zalezy m.in. od indywidualnych wskazan u danego pacjenta i decyzji lekarza.</p>
<p>W Osrodku Diagnostyki Obrazowej w Szczecinku realizujemy badania MRI i TK komercyjnie. Szczegolowe informacje dostepne w naszej rejestracji.</p>
</div>

