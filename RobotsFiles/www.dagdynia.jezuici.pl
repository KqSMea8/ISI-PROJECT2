<p style="text-align: center;"><strong>Serdecznie zapraszamy na Wzg&oacute;rze sw. Stanislawa Kostki!</strong></p>
<p style="text-align: justify;">W roku akademickim <strong>2014/2015</strong> powitalismy <strong>nowego duszpasterza &#8211; ojca Czeslawa Wasilewskiego</strong> i zaplanowalismy wiele ciekawych form wsp&oacute;lnego spedzania wolnego czasu. Nie zabraknie modlitwy, spotkan formacyjnych, ale takze dobrej rozrywki i tanc&oacute;w do bialego rana. Wiecej szczeg&oacute;l&oacute;w w dziale <a href="http://www.dagdynia.jezuici.pl/?cat=22">Aktualnosci</a>.</p>
<p style="text-align: justify;">&nbsp;</p>
<p style="text-align: justify;"><span style="color: #000000;"><strong>Wsp&oacute;lnota Akademicka STER</strong></span> dziala przy gdynskiej parafii jezuit&oacute;w od grudnia 2003 roku. Rozpoczela sie od inicjatywy student&oacute;w, kt&oacute;rzy szukali duszpasterza &#8211; i znalezli go wlasnie na Wzg&oacute;rzu Sw. Stanislawa Kostki.</p>
<p style="text-align: justify;">Obecnie w zyciu duszpasterstwa uczestniczy aktywnie kilkanascie os&oacute;b. Sa to studenci i absolwenci r&oacute;znych tr&oacute;jmiejskich uczelni. Swoje miejsce znajduja tu r&oacute;wniez doktoranci, mlodzi pracownicy naukowi oraz mlode osoby pracujace. Z duszpasterstwa wyrosla wsp&oacute;lnota modlitwy uwielbienia, otwarta na ludzi w kazdym wieku, a takze Wsp&oacute;lnota mlodych Malzenstw. W programie duszpasterstwa jest miejsce na cotygodniowa Eucharystie, spotkania formacyjne, wyjazdy oraz zabawe. Chcemy w ten spos&oacute;b poglebiac nasza relacje z Bogiem, drugim czlowiekiem oraz samymi soba.</p>
