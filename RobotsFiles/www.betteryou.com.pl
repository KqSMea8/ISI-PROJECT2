<h2><a href="http://www.betteryou.com.pl/" rel="bookmark">O coachingu</a></h2>
<p><b>Czym jest coaching i dlaczego warto z niego skorzystac?</b></p>
<p align="JUSTIFY"><a name="_GoBack"></a>Moje motto brzmi: ,,Coaching jest jak podlewanie rosliny, ktora rosnie, rozkwita i rodzi owoce."</p>
<p align="JUSTIFY">Korzenie coachingu siegaja starozytnosci. Juz Sokrates, za pomoca trafnie dobranych pytan, inspirowal wspolczesnych sobie.</p>
<p align="JUSTIFY">Slowo ,,coach" pochodzi od sredniowiecznego angielskiego slowa ,,coche", ktore oznaczalo ,,wagon". Bardziej doslownie oznaczalo pojazd, ktorym przewozi sie ludzi do miejsc, w ktorych chca sie znalezc.</p>
<p align="JUSTIFY">Wedlug ICF - Miedzynarodowej Federacji Coachow - coaching jest partnerskim towarzyszeniem klientom w kreatywnym procesie, inspirujacym ich do maksymalizowania ich osobistego i zawodowego potencjalu.</p>
<p align="JUSTIFY">W praktyce oznacza to, ze klient okresla swoj cel osobisty lub zawodowy. Coach pomaga klientowi w opracowaniu planu realizacji jego celu. Nastepnie, dzieki specjalnie dobranym pytaniom, coach pomaga przejsc klientowi przez proces zmiany i wdrozenia planu w zycie. Podczas pracy coach wspiera klienta i motywuje go do dzialania. Zalozeniem coachingu jest to, ze coach zna pytania, ktore powinien zadac klientowi, ale to klient zna najlepsze dla siebie odpowiedzi. Dzieki takiemu podejsciu, klient aktywnie pracuje nad swoimi celami, zyskuje wieksza wiare w siebie i poczucie, ze moze kierowac wlasnym zyciem.</p>
<p align="JUSTIFY">W duzym skrocie coaching rozni sie od terapii tym, ze koncentruje sie na terazniejszosci i przyszlosci klienta, a mniej na jego przeszlosci. Coaching jest bardziej skoncentrowany na dzialaniu i adresowany jest do osob, ktore stosunkowo dobrze radza sobie w zyciu i nie maja zbyt powaznych zaburzen psychicznych lub osobowosci.</p>
<p align="JUSTIFY"><b>Wyroznia sie kilka rodzajow coachingu, na przyklad:</b></p>
<p align="JUSTIFY"><b>Coaching zycia </b>lub<b> personalny</b> dotyczy zycia osobistego. Na przyklad rownowagi miedzy praca a zyciem osobistym, okreslenia celow zyciowych, poprawienia relacji z bliskimi, okreslenia sposobu spedzania wolnego czasu (hobby, sport itp.).</p>
<p align="JUSTIFY"><b>Coaching kariery</b> to pomoc w szukaniu albo zmianie pracy. Moze polegac na okresleniu mocnych stron klienta, jego doswiadczenia zawodowego i sposobu szukania upragnionej pracy.</p>
<p align="JUSTIFY"><b>Coaching biznesowy</b> polega na osiagnieciu celow zawodowych, poprawy relacji ze wspolpracownikami, okresleniu priorytetow i strategii w prowadzonym biznesie.</p>
<p align="JUSTIFY"><b>Coaching miedzykulturowy</b> - przydaje sie osobom, ktore zmienily kraj lub miasto zamieszkania, a takze menedzerom i osobom wspolpracujacym z innymi ludzmi, osobom ktore zmienily miejsce pracy oraz tym, ktorzy pracuja z obcokrajowcami. Celem coachingu miedzykulturowego jest zlagodzenie tzw. szoku kulturowego, lagodne przejscie przez proces integracji z nowym srodowiskiem oraz zrozumienie roznic w sposobie myslenia ludzi i mozliwych roznic kulturowych.</p>
<p align="JUSTIFY"><b>Coaching kryzysowy </b> sprawdza sie u osob przezywajacych trudnosci zyciowe lub emocjonalne, na przyklad po utracie pracy, bliskiej osoby lub borykajacych sie z innymi trudnymi sytuacjami.</p>
<p align="JUSTIFY">Sesje coachingowe odbywaja sie zwykle raz w tygodniu lub raz na dwa tygodnie, a jedna sesja trwa zwykle jedna godzine zegarowa. W coachingu kryzysowym sesje moga odbywacsie czesciej. </p>
<p align="JUSTIFY">Miedzy sesjami klient moze wykonywac zalecona prace domowa.</p>

