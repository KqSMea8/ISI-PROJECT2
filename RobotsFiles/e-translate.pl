<h1>O Nas</h1>
<div class="spread_child_p">
<p>Biuro tlumaczen TRANSLATE prowadzone jest przez mgr Katarzyne Starzynska,
tlumacza przysieglego jezyka angielskiego.</p>
<p>Biuro tlumaczen TRANSLATE zapewnia profesjonalne uslugi w zakresie tlumaczen
pisemnych zwyklych i przysieglych (uwierzytelnionych) oraz tlumaczen ustnych
<strong>z jezyka angielskiego i na jezyk angielski</strong>.
Naszymi klientami sa instytucje panstwowe, firmy oraz osoby prywatne.</p>
<p><strong>Zapewniamy profesjonalne i terminowe wykonanie tlumaczen, wysoka jakosc przekladu,
poufnosc otrzymanych materialow.</strong></p>
