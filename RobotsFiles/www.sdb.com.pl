var date = moment().tz(mtzTimeZone);
var angle;
var secHandLength = 60;

// CLEAR EVERYTHING ON THE CANVAS. RE-DRAW NEW ELEMENTS EVERY SECOND.
ctx.clearRect(0, 0, canvas.width, canvas.height);

OUTER_DIAL1();
OUTER_DIAL2();
CENTER_DIAL();
MARK_THE_HOURS();
MARK_THE_SECONDS();

SHOW_SECONDS();
SHOW_MINUTES();
