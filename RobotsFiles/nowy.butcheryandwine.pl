<section id="mieso" class="section">
<h2>MIESO</h2>

<div class="h2-underline"></div>

<div class="caption">
Polskie mieso kupujemy bezposrednio od producentow oraz zaprzyjaznionych ubojni. W dobrej wolowinie liczy sie nie tylko rasa. Kontrolujemy sposob hodowli i odzywiania zwierzat, ich droge do zakladu, uboj i wszystko to, co dzieje sie z miesem pozniej. W jakich warunkach jest rozbierane, sezonowane i przygotowywane do wysylki.
Mieso zagraniczne sprowadzamy bezposrednio od najlepszych producentow z Australii, USA, Irlandii, Urugwaju i Wielkiej Brytanii. Trzymamy sie zasady, ze dobre mieso to mieso ktore znamy.
</div></section>

<section id="sezonowanie" class="section">
<h2>SEZONOWANIE</h2>

<div class="h2-underline"></div>

<div class="caption">
Sezonujemy mieso "na sucho" przez minimum 28 dni. W trakcie tego procesu kontrolujemy temperature, wilgotnosc i przeplyw powietrza. Sezonowanie nadaje naszym stekom niepowtarzalny, wyrazisty i gleboki smak.

</div></section>

<section id="ciecie" class="section">
<h2>CIECIE</h2>

<div class="h2-underline"></div>

<div class="caption">
Do ciecia miesa podchodzimy bardzo powaznie. Specjalna pila pozwala nam na precyzyjne ciecia stekow z duzych, niestandardowych kawalkow miesa. Dzieki temu nasi goscie moga zamowic steki o dowolnej grubosci.
</div></section>

<section id="grilowanie" class="section">
<h2>GRILLOWANIE & STEKI</h2>

<div class="h2-underline"></div>

<div class="caption">
Jako pierwsi w Polsce do grillowania stekow uzywamy specjalnego grilla/pieca opalanego weglem drzewnym oraz drzewem. Temperatura dochodzaca do 500 st. C gwarantuje idealna karmelizacje miesa, a grillowanie na weglu drzewnym nadaje miesu niepowtarzalny, pelny smak. W menu znajdziecie pelen przekroj stekow, m.in. Rib Eye, New York, T-bone, Porterhouse, Flat Iron, Rump, Bavette, Chateaubriand i poledwice.
</div></section>

<section id="wino" class="section">
<h2>WINO & BAR</h2>

<div class="h2-underline"></div>

<h3>WINO</h3>

