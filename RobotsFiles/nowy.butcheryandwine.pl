sprowadzanych nie tylko z duzych winnic, ale rowniez od malych, mniej znanych producentow.
Wspolpracujemy tylko z najlepszymi importerami, czesto osobiscie znamy winiarzy, ktorych
wina polecamy. Nasza karta zostala doceniona przez przewodnik Michelin oraz wielokrotnie
przez prase branzowa.
</div></section>

