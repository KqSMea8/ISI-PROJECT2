<p><strong><u>Typ zamowienia </u></strong></p>
<p><strong>dostawa:</strong></p>
<p><strong>,,Dostawa laptopow" - Realizacja projektu: Mieszkancy gminy Gruta aktywni w sieci.</strong></p>
<p><strong> </strong></p>
<p><strong> </strong></p>
<ol>
<li><strong>Dane dotyczace Zamawiajacego:</strong></li>
</ol>
<p>Stowarzyszenie ,,Przyjaciele Boguszewa", Boguszewo 88,  86-330 Melno, NIP 8762425392</p>
<p>&nbsp;</p>
<ol start="2">
<li><strong>Tryb udzielenia zamowienia:</strong></li>
</ol>
<p>Zamawiajacy zamierza udzielic zamowienia w trybie art. art. 70<sup>1 </sup>ustawy z dnia z dnia 23 kwietnia 1964 r. Kodeks cywilny (Dz.U. z 2018 r. poz. 1986 ze zm.)</p>
<p>&nbsp;</p>
<ol start="3">
<li><strong>Opis przedmiotu zamowienia:</strong></li>
</ol>
<p><strong> </strong></p>
<p>Dostawa <strong>12 sztuk laptopow  zgodnie z opisem:</strong></p>
<p>- procesor klasy i5 i wyzszej, o czestotliwosci minimum 2,3 GHz;</p>
<p>- czas pracy na baterii umozliwiajacy calodzienna eksploatacje (min. 6h pracy na baterii);</p>
<p>- pamiec RAM minimum 4GB;</p>
<p>- pamiec masowa minimum 32GB;</p>
<p>- ekran dotykowy IPS o wielkosci nie mniejszej niz 14''(preferowany 15,6) i rozdzielczosci 1980x1080 lub wyzszej;</p>
<p>- mozliwosc podlaczenia do sieci WiFi o standardzie  802.11ac;</p>
<p>- zlacza i lacznosc: USB 3.0, HDMI lub rownowazne, Bluetooth w standardzie minimum 4.0;</p>
<p>- zintegrowane komponenty multimedialne: kamera, mikrofon,  karta audio i  glosniki;</p>
<p>- mozliwosc odlaczania klawiatury lub obracania ekranu i korzystania z urzadzenia w trybie</p>
<p>Tabletu.</p>
<p>&#8211; okres gwarancji na dostarczony sprzet IT minimum 24 mc-e.</p>
<p>&#8211; rysik,</p>
<p>&#8211; torba do transportu,</p>
<p>&#8211; <em>konstrukcja</em> o zwiekszonej odpornosci na uszkodzenia mechaniczne (odpornosc na upadek z</p>
<p>wysokosci minimum 70 cm),</p>
<p>&#8211; konstrukcje o zwiekszonej odpornosci na kontakt z plynami (minimum na poziomie klawiatury i touchpada),</p>
<p>&#8211; zintegrowane czujniki (minimum zyroskop, akcelerometr, kompas),</p>
<p>&#8211; wage (im mniej tym lepiej),</p>
<p>&#8211; zuzycie pradu (im mniej tym lepiej)</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>Laptopy zakupione beda do realizacji projektu <strong>,,E-AKTYWNI MIESZKANCY WOJEWODZTWA KUJAWSKO-POMORSKIEGO I LODZKIEGO" </strong>- cyklicznych szkolen z obslugi Internetu, programow dedykowanych poszczegolnym grupom mieszkancow Gminy Gruty  niezbednych do prowadzenia dzialalnosci gospodarczej, rozwoju hobby.</p>
<p>Po zakonczeniu projektu laptopy /z zachowaniem warunkow gwarancji/ zostana przekazane wskazanej przez Partnerow (Gmina Gruta, Stowarzyszenie Przyjaciele Boguszewa) realizujacych projekt, wybranej szkole podstawowej na terenie Gminy Gruta.</p>
<p>&nbsp;</p>
<p><u>Zaplata za wykonana dostawe w terminie 7 dni od dnia dostarczenia prawidlowo wystawionej faktury po bezusterkowym odbiorze laptopow i wyposazenia dodatkowo potwierdzonym protokolem zdawczo-odbiorczym.</u></p>
<p>&nbsp;</p>
<ol start="4">
<li><strong>Termin wykonania zamowienia: </strong>od dnia podpisania umowy do dnia <strong>19 grudnia 2018 r.</strong></li>
</ol>
<p>&nbsp;</p>
<ol start="5">
<li><strong>Warunki udzialu w postepowaniu oraz opis dokonywania oceny spelniania tych warunkow.</strong></li>
</ol>
<p>Warunkiem udzialu w postepowaniu jest prawidlowe wypelnienie zalaczonych wzorow dokumentow i dostarczenie ich wraz z prawidlowo wystawionymi wymaganymi dokumentami przez osoby uprawnione w wyznaczonym przez Zamawiajacego miejscu i terminie skladania ofert.</p>
<p>1) Wymagane dokumenty:</p>
<ol>
<li>a) przedlozenie oferty na formularzu stanowiacego zalacznik nr 1 do zapytania ofertowego wraz z wyszczegolnieniem dostarczonego sprzetu, oprogramowania i wyposazenia.</li>
<li>b) zalaczenie do oferty odpisu z wlasciwego rejestru lub z centralnej ewidencji i informacji o dzialalnosci gospodarczej wystawiony nie wczesniej niz 6 miesiecy przed uplywem terminu skladania ofert. Wykonawca moze wskazac dostepnosc dokumentow w formie elektronicznej pod okreslonymi adresami stron internetowych ogolnodostepnych i bezplatnych baz danych. Zamawiajacy samodzielnie pobierze z tych baz danych wskazane przez Wykonawce dokumenty.</li>
</ol>
