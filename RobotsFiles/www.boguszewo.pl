<div class="ex-right"><p style="text-align: justify;">Nasza strona jest efektem wspolnej pasji mieszkancow, pragnacych zachowac resztki dziedzictwa kulturalnego i historycznego naszej miejscowosci. Powstala dzieki realizacji minigrantu FIO, realizowanego we wspolpracy z Towarzystwem Rozwoju Gminy Pluznica oraz stowarzyszeniem ,,Przyjaciele Boguszewa", ktore bedzie oficjalnym administratorem tej witryny. Naszym celem glownym jest digitalizacja i zgromadzenie  w roznorodnej formie tego, co swiadczy o tradycji tej miejscowosci. Beda to zebrane fakty historyczne na jej temat, wspomnienia swiadkow historii Boguszewa, zdjecia , nagrania wideo i audio z wywiadow, zabytki materialne oraz inne formy dokumentacji. Pragniemy ich zachowania glownie ze wzgledu na przyszle pokolenia ,,boguszewiakow", aby  znali historie ich ,,malej ojczyzny". Burzliwe losy tych terenow i niezwykle dzieje ich mieszkancow beda wspaniala lekcja historii dla mlodziezy. Wszystkie ,,skarby" zamierzamy skatalogowac i poprzez zapis cyfrowy sprawic, ze stana sie niesmiertelne. Dlatego zwracamy sie z prosba do wszystkich, ktorzy chcieliby sie przylaczyc do naszej inicjatywy, aby zglaszali sie do czlonkow grupy nieformalnej. Mozna to uczynic  poprzez  zakladke FORUM, wyslac wiadomosc email na adres <a href="mailto:boguszewo.@gmail.com">boguszewo.@gmail.com</a> lub zadzwonic pod nr 606200926. Liczymy na pomoc i zaangazowanie wszystkich, dla ktorych ten temat jest wazny i potrzebny. Informujemy, ze w dniu 11 listopada 2014 roku odbedzie sie wspolne spotkanie pasjonatow i uczestnikow projektu, aby wzorem przedwojennej tradycji Boguszewa uczcic Swieto Niepodleglosci.</p>
