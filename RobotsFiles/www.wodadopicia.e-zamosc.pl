<div class="post-content"><p>Spozywanie wody to nieodlaczny element zycia codziennego kazdego czlowieka. Woda to niezbedny skladnik do prawidlowego funkcjonowania organizmu ludzkiego. Jednak jak dobrac odpowiednia dla nas wode?
Na rynku istnieje bardzo wiele roznego rodzaju wod, ktorych opisy na etykietach nic nam nie mowia. Zatem jak odroznic wode mineralna od wody zrodlanej? Glownym i tak naprawde jedynym kryterium jest tutaj poziom nasycenia skladnikami mineralnymi i odzywczymi. W wodzie mineralnej utrzymuje sie on w granicach 1000mg na litr, natomiast w wodzie zrodlanej jest to troche ponad 500mg na litr.  Poza tym sposob wydobycia obu wod jest taki sam. Szczegolowo okreslaja to przepisy wydane przez Ministerstwo Zdrowia.</p></div>


