<h2 class="post-title"><a
href="http://www.wodadopicia.e-zamosc.pl/index.php?post/2011/06/29/Zapraszam">Jak wydobywa sie wode mineralna?</a></h2>

<p class="post-info">By admin admin    on Wednesday, June 29 2011, 12:19        - <a href="http://www.wodadopicia.e-zamosc.pl/index.php?category/Woda">Woda</a>
</p>




<div class="post-content"><p>Woda mineralna lub zrodlana to woda bardzo bogata w skladniki i elementy mineralne niezbedne do prawidlowej funkcji organizmu kazdego czlowieka. Dlatego tak waznym aspektem jest wyuczenie nawyku picia wody mineralnej juz przez male dzieci.
Woda mineralna jest bogata w mineraly i jony nasycone. Jest to mozliwe dzieki temu, ze wydobywa sie ja z najglebszych zakamarkow naszej ziemi. W Polsce wode mineralna najczesciej pozyskuje sie z glebin skalnych, ktore nie zostaly wyeksploatowane przez czlowieka. Dzieki temu w jednym litrze wody mineralnej znajduje sie az 1000mg roznego rodzaju soli mineralnych i skladnikow odzywczych, niezbednych do prawidlowej funkcji organizmu. Warto zaznaczyc, ze wode mineralna wydobywa sie dzieki specjalnym odwiertom. Firmy, ktore sie tym zajmuja musza jednak dostac specjalna koncesje z Ministerstwa Zdrowia.</p></div>


