<h1 style="text-align: center;"><strong>Integrowana ochrona roslin</strong></h1>
<p style="text-align: justify;"><span style="color: #000000;">Od 1 stycznia 2014 r. obowiazuja regulacje prawne dotyczace stosowania ogolnych zasad integrowanej ochrony roslin, tzn. wykorzystania wszystkich mozliwych i dostepnych metod zwalczania agrofagow w szczegolnosci metod niechemicznych, w przypadku gdy w produkcji rolnej stosuje sie srodki ochrony roslin.</span></p>
<p style="text-align: justify;"><span style="color: #000000;">W integrowanej ochronie roslin nalezy wykorzystywac w pelni wiedze o agrofagach (w szczegolnosci o ich biologii, wystepowaniu i szkodliwosci) w celu okreslenia optymalnych terminow dla podejmowania dzialan zwalczajacych te organizmy, a takze wykorzystywac naturalne wystepowanie organizmow pozytecznych, w tym drapiezcow i pasozytow organizmow szkodliwych dla roslin lub ich introdukcje. Tym samym stosowanie integrowanej ochrony roslin pozwala ograniczyc stosowanie chemicznych srodkow ochrony roslin do niezbednego minimum i w ten sposob ograniczyc presje na srodowisko naturalne oraz chronic bioroznorodnosc srodowiska rolniczego.</span></p>
<p style="text-align: justify;"><span style="color: #000000;">Waznymi narzedziami, wykorzystywanymi w integrowanej ochronie roslin sa:</span></p>
<p style="text-align: justify;"><span style="color: #000000;">&#8211; metodyki integrowanej ochrony roslin poszczegolnych upraw,</span></p>
<p style="text-align: justify;"><span style="color: #000000;">&#8211; progi ekonomicznej szkodliwosci - progi te okreslaja, kiedy stosowanie chemicznej ochrony roslin staje sie ekonomicznie oplacalne, tzn. przy jakiej liczebnosci organizmu szkodliwego dla roslin straty, jakie moze on spowodowac, przewyzszaja koszty jego chemicznego zwalczania,</span></p>
<p style="text-align: justify;"><span style="color: #000000;">&#8211; systemy wspomagania decyzji w ochronie roslin - systemy te, bazujace na znajomosci biologii organizmow szkodliwych, wskazuja optymalny termin wykonania chemicznych zabiegow ochrony roslin.</span></p>
<p style="text-align: justify;"><span style="color: #000000;">Istnieje rowniez mozliwosc skorzystania z uslug doradczych (profesjonalnego doradztwa) dotyczacych metod ochrony roslin w zakresie realizacji wymagan integrowanej ochrony roslin.</span></p>
<p style="text-align: justify;"><span style="color: #000000;">Podstawowym elementem integrowanej ochrony roslin powinno byc swiadome zarzadzanie uprawami oparte na znajomosci warunkow klimatyczno-glebowych gospodarstwa. Decyzje o wykonaniu zabiegow ochrony roslin powinny byc podejmowane w oparciu o monitoring wystepowania organizmow szkodliwych, z uwzglednieniem progow ekonomicznej szkodliwosci. Dokonujac wyboru srodkow ochrony roslin nalezy brac pod uwage ich selektywnosc i klasyfikacje. Ponadto, stosowanie srodkow ochrony roslin powinno byc ograniczone do niezbednego minimum, w szczegolnosci poprzez zredukowanie dawek lub ograniczenie ilosci wykonywanych zabiegow.</span></p>
<p style="text-align: justify;"><span style="color: #000000;">Stosowanie ogolnych zasad integrowanej ochrony roslin przez profesjonalnych uzytkownikow srodkow ochrony roslin zostalo w Polsce uregulowane przepisami ustawy z dnia 8 marca 2013 r. o srodkach ochrony roslin (Dz. U. poz. 455) oraz rozporzadzenia Ministra Rolnictwa i Rozwoju Wsi z dnia 18 kwietnia 2013 r. w sprawie wymagan integrowanej ochrony roslin (Dz. U. poz. 505).</span></p>
<p style="text-align: justify;"><span style="color: #000000;">Gwarancja wypelnienia obowiazku stosowania ogolnych zasad integrowanej ochrony roslin jest przystapienie do dobrowolnego systemu certyfikacji jakosci zywnosci - Integrowana Produkcja Roslin (IP). Glowna roznica pomiedzy integrowana ochrona roslin, a Integrowana Produkcja polega zatem na koniecznosci stosowania integrowanej ochrony i mozliwosci przystapienia do IP. Mozna w zwiazku z tym stwierdzic, ze kazdy producent uczestniczacy w IP musi spelniac wymogi integrowanej ochrony roslin oraz dodatkowo realizowac zalozenia okreslone wylacznie dla systemu IP.</span></p>
