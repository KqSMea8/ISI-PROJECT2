<div class="entrybody">
<p style="text-align: justify;"><span style="font-size: 16px;"><span style="font-size: 16px;"><strong><em>Parafia p.w. Meczenstwa sw. Jana Chrzciciela w Zawisci </em></strong></span><span style="font-size: 16px;">Pierwotnie Zawisc nalezala do parafii Woszczyce, dopiero od 1931 r. do parafii w Orzeszu. Starania o utworzenie parafii rozpoczal jeszcze w latach 50. i 60. orzeski proboszcz ksiadz dziekan Wladyslaw Wojciech, ale decydujacy glos mialy tu wladze swieckie sprzeciwiajace sie takiemu zamyslowi i budowie kosciola. W 1979 r.. juz za czasow proboszczowania ksiedza Stanislawa Adamczyka, w Zawisci rozpoczal &#8211; na wyrazna prosbe mieszkancow &#8211; dzialalnosc punkt katechetyczny (salka) na terenie posesji panstwa Witek. Wiosna 1981 r. staraniem orzeskiego duszpasterza ks. Adamczyka zakupiono w Janowie kolo Katowic barak, ktory przewieziono na posesje Elfrydy Stachula, mieszkanki Zawisci, ofiarodawczyni terenu pod budowe przyszlej swiatyni. Po adaptacji baraku do potrzeb duszpasterskich dokonano 30 sierpnia 1981 roku poswiecenia powstalego w ten sposob obiektu sakralnego. Parafie w Zawisci erygowano 29 sierpnia 1982 r. Jej administratorem zostal ks. Jan Grzegorzek, orzeski wikary, przeniesiony w lipcu 2000 r. do innej parafii. W 1987 roku zalozono cmentarz parafialny, a na poczatku lat 90. zakupiono teren pod przyszla swiatynie. W latach 1994 &#8211; 97 wzniesiono tam dzwonnice. Natomiast od 1998 roku trwa realizacja projektu budowy nowego kosciola autorstwa inzyniera Grzegorza Ratajskiego. Wspolnota liczy 1600 wiernych. Na terenie Zawisci mieszka takze ok. 70 osob roznych wyznan chrzescijanskich (glownie ewangelikow). Zycie formacyjne skupione jest w grupach: Caritasu, Apostolstwa Dobrej Smierci, Rodziny Rozancowej, Akcji Katolickiej, Dzieci Maryi, ministrantow. Parafia zamierza dokonczyc budowe kosciola, a takze rozpoczac starania o wzniesienie probostwa i domu parafialnego. Odpust parafialny obchodzony jest w ostatnia niedziele sierpnia.</span></span></p>
<p>Proboszczowie:<br />
