<div class="col-lg-4 col-md-6 col-sm-6 wow fadeInUp" data-wow-delay="0.1s">
<p>Firma SPEC-BUD GROUP  jest obecna na polskim rynku budowlanym od poczatku 2008 roku. Na poczatku swojej dzialalnosci firma swiadczyla uslugi w zakresie uslug remontowo - budowlanych  na terenie Poznania i okolic.  </p>
<p>Od roku 2011 jestesmy firma, ktora specjalizuje sie w kompleksowej obsludze inwestycji w segmencie adaptacji wnetrz na terenie calej Polski.</p>
<p>Duze zapotrzebowanie na uslugi budowlane o najwyzszej jakosci pomoglo nam rozwinac dzialalnosc. W ciagu ostatnich kilku lat przeszlismy ewolucje i z kilku osobowego zespolu w roku 2016  powstala Spolka z o.o. zatrudniajaca kilkadziesiat osob.</p>
<p>W okresie tym caly czas, bez przerwy, pracujemy i rozwijamy sie chcemy byc coraz lepsi, bardziej zaangazowani i profesjonalni. Biorac pod uwage trudny zmienny rynek oraz kryzys budowlany w tym okresie, uwazamy to za nasze osiagniecie.</p>


</div>
<div class="col-lg-4 col-md-12 col-sm-12 wow fadeInUp" data-wow-delay="0.2s">
<p>Zaloge firmy SPEC-BUD stanowi wykwalifikowana i sprawdzona kadra pracownikow, ktorych fachowosc poparta jest wieloletnim doswiadczeniem i praktyka zdobytymi na budowach realizowanych w calym kraju i za granica.</p>
<p>Dzialalnosc firmy oparta jest o najnowsze technologie z dziedziny budownictwa.</p>
<p>Posiadamy niezbedne certyfikaty oraz uprawnienia do wykonania prac w systemie suchej zabudowy oraz liczne referencje, ktore dotycza obiektow: biurowych, przemyslowych, handlowych, hoteli, sportowych, domow, mieszkan i innych.</p>
