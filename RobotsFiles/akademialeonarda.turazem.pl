<h3><strong>O Akademii</strong></h3>
<strong>Akademia Leonarda da Vinci - Zloty Wiek</strong> to przestrzen edukacyjna inspirowana tworczoscia Leonarda da Vinci dla mieszkancow Wroclawia,<strong> powyzej 60 r.z.</strong>

W ramach Akademii w okresie od maja do grudnia 2015 uczestnicy skorzystaja <strong>nieodplatnie</strong> z innowacyjnych, wielowymiarowych zajec w trzech blokach tematycznych. Aby ukonczyc Akademie uczestnik programu musi wziac udzial w <strong>minimum 100 godzinach zajec</strong> z trzech filarow: <strong>CIALO, ZDROWIE i UMYSL</strong>. Uczestnik otrzymuje indeks, w ktorym, odznaczane jest ukonczenie wybranych przez niego warsztatow.

Akademia Leonarda da Vinci to pierwszy tego typu projekt w miescie Wroclawiu, dajacy szanse na wielowymiarowy rozwoj seniorow poprzez uczestnictwo w specjalnie dostosowanych do ich wieku i mozliwosci zajec.

Zapraszamy do udzialu!

