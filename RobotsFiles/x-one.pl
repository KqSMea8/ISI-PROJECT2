<h2 class="sectionTitle sectionTitle-first">Wybrane <span>realizacje</span>.</h2>
<p style="margin-bottom: 1em;">W realizacjach projektow nie ograniczamy sie biznesowo. Projektowalismy i realizowalismy rozwiazania informatyczne, mi. in. w branzy produkcyjnej, w branzy ubezpieczen, w branzy nieruchomosci, w branzy transportu oraz logistyki i wielu innych. Dzialamy rowniez aktywnie w obszarach ecommerce'owych (sprzedaz internetowa, sklepy internetowe, portale internetowe). </p>
<p style="margin-bottom: 1em;">Potrafimy z powodzeniem odnalezc sie w roznorodnych branzach i dostarczyc dla nich nowoczesne rozwiazania. Nasze realizacje sa kompleksowe. Dostarczamy nie tylko systemy, ale rowniez tworzymy koncepcje rozwiazan, zarzadzamy projektami, prowadzimy formalna analize, planujemy i koordynujemy testy, zapewniamy infrastrukture, wsparcie wdrozeniowe i utrzymanie powdrozeniowe, prowadzimy szkolenia i warsztaty.</p>
<p>Istotne jest dla nas, aby klient byl usatysfakcjonowany nie tylko efektem finalnym, ale rowniez procesem jego powstawania.</p>
