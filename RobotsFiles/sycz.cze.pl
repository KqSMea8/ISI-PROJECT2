<p><strong>Wasyl Sycz (Vasyl Sych)</strong> jest lekarzem medycyny, absolwentem Tarnopolskiego Panstwowego Instytutu Medycznego w Tarnopolu na Ukrainie. Studia ukonczyl w roku 1978 z tytulem lekarza medycyny ogolnej. Jego dyplom uznano przez Ministerstwo Zdrowia RP w roku 2001 ("kwalifikacje lekarza sa rownorzedne z dyplomem ukonczenia studiow wyzszych oraz tytulem zawodowym lekarza nadanym przez szkoly wyzsze RP").</p>
<p>Staz lekarski odbyl w Kijowskim Obwodowym Szpitalu Psychiatrycznym otrzymujac tytul lekarza - psychiatry.</p>
<p>W latach 1978 - 1984 pracowal jako lekarz psychiatra w Kijowie i obwodzie Kijowskim.  W roku 1984 przeprowadzil sie do Tarnopola, gdzie pracowal w Tarnopolskim Obwodowym Szpitalu Psychiatrycznym jako lekarz narkolog - psychiatra do roku 1998.</p>
<p>Przez kolejne lata uzupelnial swoja wiedze na studiach podyplomowych w Kijowie w zakresie psychiatrii (1979), narkologii (1985), mikrofalowej terapii biorezonansowej (1992), terapii manualnej, masazu klasycznego wschodniego (1992), w Winnice z zakresu psychoterapii (1990), w Charkowie z zakresu niekonwencjonalnych metod leczenia i refleksoterapii (1992), w Doniecku z zakresu hirudoterapii (2005). </p>
<p>W swojej pracy zawodowej zawsze laczyl wiedze lekarza z niekonwencjonalnymi metodami leczenia.</p>

