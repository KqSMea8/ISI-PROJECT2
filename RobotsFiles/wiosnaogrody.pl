<article id="O-Firmie" >
<div>
<p>
<b>Rozpoczecie dzialalnosci w 1995r. od samego poczatku urzadzanie terenow zielonych</b> i wspolpraca z najlepszymi w regionie producentami roslin. Jako cel nadrzedny promowanie naturalnych form ogrodowych z naciskiem na ekologie i jakosc proponowanych uslug. W kontakcie z klientem najwazniejsze dobre obopolne relacje,stala opieka nad wykonanymi ogrodami i otwartosc na wszelkie nowosci.<br/>
<b>Od 2000 roku rozpoczecie wspolpracy z niemiecka Firma GARDENA</b><br/>
w zakresie handlu sprzetem ogrodniczym oraz systemami nawadniajacymi.<br/>
<b>Od samego poczatku oferujemy poprawiajace strukture gleby</b><br/>
oraz zdrowotnosc roslin naturalno- mineralne nawozy do wszystkich grup roslin. Poczatki wspolpracy ze szwajcarska Firma GUNTHER a obecnie Firma EKONAW jako przedstawicielem niemieckiej Firmy NORDLAND GUANO, oferujacej w 100% naturalne nawozy na bazie guano peruwianskiego.
<b>Kolejnym atutem naszej firmy jest oferta kamieni i mineralow do ogrodu.</b><br/>

</p>

<p>
W ciaglej ofercie posiadamy ponad 30 rodzajow kamieni, zwirow, otoczakow jak
rowniez wykonanych z ich elementow architektury ogrodowej.
<b>Nawierzchnie trawiaste wykonujemy wylacznie z uzyciem mieszanek produkcji krajowej</b><br/>
co gwarantuje bardzo dobre efekty, biorac pod uwage warunki klimatyczne w naszym regionie. Na zyczenie rozkladamy rowniez trawniki rolowane.
Od 2010 roku posiadamy wlasna kompostownie, co pozwala nam na przygotowywanie wlasnych podlozy pod trawniki i rabaty roslinne z uzyciem kory i kompostu.
Oferujemy rowniez kore kompostowana luzem i pakowana w worki.
<br/><br/><b>Oferta</b><br/><br/>

<b>Podstawa naszej dzialalnosci jest urzadzanie terenow zielonych.</b>
Poniewaz nie mozna zostawiac klientow samych sobie z klopotem, jakim jest utrzymanie
ogrodu w dobrej kondycji, dlatego na zyczenie zajmujemy sie rowniez pielegnacja ogrodow, dbaloscia o zdrowie roslin
</p>

<p>
i zwalczaniu szkodnikow. ,,Budzimy" ogrody do zycia wiosna, jak
i przygotowujemy je do zimy. Zimowa pora pomagamy w usuwaniu
sniegu. Od 2011 roku we wspolpracy z firma JARMEX rozpoczelismy instalowanie ogrodzen i donic gabionowych.<br/>

Naszymi klientami sa wlasciciele, zarowno malych przydomowych ogrodow, jak i posiadacze rezydencjonalnych terenow zielonych o pow. nawet kilkunastu ha. Zielen przy obiektach firmowych, jak i uzytecznosci publicznej takze znajduje sie w naszej ofercie.
<br/>
<b>Druga czescia naszej dzialalnosci jest handel sprzetem ogrodniczym,</b><br/>
kamieniem, kora kompostowana, podlozami i nawozami.
Na zyczenie klienta dopasowujemy sprzet do ogrodu, opierajac sie na wieloletnim doswiadczeniu w branzy.
<br/><br/><b>Zespol</b><br/><br/>

dysponujemy zespolem ludzi z wieloletnim doswiadczeniem w branzy wyposazonym w najwyzszej jakosci sprzet do wykonywania nawet najtrudniejszych prac.
</p>
