Popularny w czasach PRL slogan brzmial ,,Wegiel tlenem polskiej gospodarki". Tlenem gospodarki panstw zachodniego wybrzeza Ameryki Poludniowej w XIX wieku bylo guano. Rowniez ono - jak przed nim zloto, a po nim ropa naftowa - stawalo sie tam przyczyna konfliktow zbrojnych i trwajacych po dzis dzien sporow terytorialnych.</p>
<p>Guano, czyli odchody ptakow morskich - glownie mew, gluptakow, pelikanow i kormoranow - zmieszane ze skorupkami jaj, piorami, piaskiem i martwymi ptakami, ze wzgledu na wysoka zawartosc azotu, fosforu, potasu, magnezu i wapnia bylo w XIX wieku nawozem doskonalym. </p>

<p>Jego zloza przez stulecia odkladaly sie na zachodnich wybrzezach krajow Ameryki Poludniowej: Peru, Boliwii i Chile, a zwlaszcza na nadbrzeznych wyspach (przede wszystkim peruwianski archipelag Chincha). Peru cenilo je jako nawoz w czasach inkaskich, Hiszpanie jednak nie przejeli od nich tej wiedzy, bo i nie uprawa roli byla tam ich priorytetem. Dopiero w roku 1804 slynny przyrodnik Alexander von Humboldt zebral tam probki guana, przewiozl je do Europy i przebadal. Okazaly sie swietnym nawozem, ale wiedza ta rozpowszechniala sie powoli. W Ameryce ,,goraczka guana" miala miejsce od lat 30-tych XIX wieku, gdy pojawily sie o nim pierwsze publikacje. W Europie zaczela sie w 1840 r. wraz z wydaniem pracy niemieckiego chemika Justusa von Liebiga o nawozeniu. Podsumowywala ona badania nad przydatnoscia guana w rolnictwie i wykazywala jego wyzszosc wobec tradycyjnych nawozow. Byl oczywiscie obornik, probowano takze uzywac odchodow golebich, te jednak trudno bylo pozyskiwac na masowa skale - a takowej wymagaly rosnace z kazdym rokiem uprawy ziemniakow i kukurydzy. Poza tym w guanie bylo duzo wiecej azotu, a to dlatego, ze tamtejsze ptaki zywily sie glownie bogatymi w ten pierwiastek rybami, w przeciwienstwie do europejskich krow i golebi.</p><p>
Zalety guana dosc szybko docenili konserwatywni rolnicy z Niemiec (von Liebig, profesor i baron, mial olbrzymi autorytet), w tym gospodarze z Wielkopolski, zwlaszcza odkad w 1853 r. otwarto sklad guana w Kwidzyniu. (W zaborach rosyjskim i austriackim guano stosowano znacznie rzadziej, bo i bylo tam drozsze) Najwiecej zuzywala jednak Wielka Brytania, po niej zas Francja, co wiazalo sie min. z latwoscia eksportu. Popyt (mimo sporych cen) byl tak znaczny, ze nieuczciwi sprzedawcy probowali falszowac guano wapnem, kreda, glina, popiolem czy po prostu ziemia. Juz w roku 1841, kiedy pierwsze statki z guanem wyplynely z peruwianskiego Iquique do Liverpoolu, pojawili sie tez pierwsi sprzedawcy ,,sztucznego guana", czesto bedacego zwyklym oszustwem. Nie zmniejszylo to popytu na oryginalny towar : przez nastepne 18 lat sprowadzono do Europy - nie tylko z Peru, ale i Boliwii, Chile oraz niektorych wysp pacyficznych i krajow afrykanskich - guano warte 25 milionow funtow. Do roku 1880 samo Peru wyeksportowalo blisko 2 miliony ton nowego nawozu.
Najwyzej ceniono guano peruwianskie, znacznie gorsza opinie mialo to z Chile.</p>
