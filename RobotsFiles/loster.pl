<h4>Prof. dr hab. Bartlomiej W. Loster</h4>
<p>Specjalista ortodoncji, protetyki stomatologicznej i stomatologii ogolnej. Prywatna praktyke stomatologiczna prowadzi od prawie trzydziestu lat. Od roku 2009 zajmuje sie wylacznie leczeniem ortodontycznym dzieci i osob doroslych. Wspolnie z prof. Stephenem Williamsem jest wspoltworca nowej krakowskiej szkoly ortodontycznej opartej glownie na systemie Kracovia oraz systemie Cannon Ultra (europejska i swiatowa ortodoncja oparta na faktach naukowych). Wspomaga tez planowanie leczenia protetycznego dr n. med. Jolanty Loster, specjalisty w dziedzinie protetyki stomatologicznej (a prywatnie zony) po zakonczonym leczeniu ortodontycznym. Prowadzona praktyka prywatna jest kontynuacja rodzinnej tradycji rozpoczetej przez lek. dent. Wande Krupinska-Loster w Rabce-Zdroj w roku 1957.</p><br/>
<p>Poza prywatna dzialalnoscia od roku 1987 jest pracownikiem Uniwersytetu Jagiellonskiego Collegium Medicum (wczesniej Akademii Medycznej w Krakowie). W Uniwersytecie Jagiellonskim pelni funkcje Prodziekana Wydzialu Lekarskiego, kierownika Katedry Ortodoncji ISWL UJCM i kierownika Poradni Ortodoncji Uniwersyteckiej Kliniki Stomatologicznej w Krakowie. Jest Prezydentem Polskiego Towarzystwa Stomatologicznego oraz czlonkiem Polskiego Towarzystwa Ortodontycznego, European Orthodontic Society, American Association of Orthodontists oraz World Federation of Orthodontists.</p>
