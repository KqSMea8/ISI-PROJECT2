<p>Zapraszamy do wspolpracy i dolaczenia do stowarzyszenia&nbsp;<em>Towarzystwem dla Natury i Czlowieka</em>!</p>
<div class="entry-content">
<p><i>Statut i inne dokumenty Towarzystwa dostepne sa na stronie internetowej <a href="http://ekolublin.pl/tdnicz">ekolublin.pl/tdnicz</a> oraz w Biuletynie Informacji Publicznej</i></p>
<p>Warunki konieczne do przyjecia w poczet czlonkow zwyczajnych TdNiCz:</p>
<ul>
<li>
wypelnienie formularza ,,Deklaracja czlonkowska" na stronie (lub wersji papierowej dostepnej w siedzibie stowarzyszenia)
</li>
<li>
oplacenie skladek za biezacy rok
</li>
<li>
uchwala zarzadu stowarzyszenia podejmowana po stwierdzeniu wplyniecia prawidlowo wypelnionej deklaracji czlonkowskiej oraz wplaty skladek czlonkowskich
</li>
</ul>
<p>W formularzu deklaracji on-line pola oznaczone (*) sa obowiazkowe. W przypadku pozostalych pol prosze wypelnic te, ktore uznaja Panstwo za stosowne.</p>
<p>Istnieje takze mozliwosc zostania&nbsp;<strong>czlonkiem wspierajacym</strong>&nbsp;TdNiCz. Czlonek wspierajacy to osoba lub instytucja (osoba prawna), ktora deklaruje sie wspierac Stowarzyszenie organizacyjnie lub finansowo. Sposob wspierania Stowarzyszenia nalezy okreslic na etapie skladania deklaracji. Czlonkostwo rozpoczyna sie z chwila podjecia uchwaly przez Zarzad o przyjeciu Czlonka. Czlonek wspierajacy nie ma praw wyborczych w Stowarzyszeniu (nie moze wybierac, ani byc wybieranym do wladz Stowarzyszenia). Deklaracja dla czlonkow wspierajacych znajduje sie&nbsp;<a href="?name=deklaracja_czlonka_wspierajacego" title="Deklaracja Czlonka Wspierajacego">tutaj</a>.</p>
<p>O przyjeciu do stowarzyszenia nowy czlonek informowany jest droga elektroniczna - na podany adres e-mail, a w przypadku stwierdzenia nieprawidlowego adresu e-mail SMSem na podany numer telefonu.</p>
<p><strong>Regulamin placenia skladek czlonkowskich TdNiCz</strong></p>
<ul>
<li>
Skladka czlonkowska czlonkow zwyczajnych TdNiCz wynosi 40 zl rocznie (za rok kalendarzowy)
</li>
</ul>
