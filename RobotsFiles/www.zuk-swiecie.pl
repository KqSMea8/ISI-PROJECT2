<br><div align="center"><h1 style="text-align: center;">Oczyszczanie miasta i odbior odpadow komunalnych: ZUK w Swieciu&nbsp;</h1><div align="center"><br></div><div align="justify">ZUK w Swieciu swiadczy kompleksowe uslugi w zakresie odbioru odpadow komunalnych, oczyszczania miasta i <a href="http://www.zuk-swiecie.pl/oferta/ziele-miejska">pielegnacji terenow zielonych</a> dla gmin ulokowanych na terenie wojewodztwa kujawsko-pomorskiego.</div><div align="center"><span style="color: rgb(0, 176, 80); font-family: calibri, sans-serif; font-size: 11pt;"><br></span></div><div align="center"><h2 style="text-align: center;">Ponad pol wieku doswiadczenia</h2></div><div align="justify">Jestesmy doswiadczonym przedsiebiorstwem z bogata historia. <b>Uslugi oczyszczania miasta</b> swiadczymy juz od ponad pol wieku. Nasz zaklad przeszedl w tym czasie liczne zamiany organizacyjne. Od momentu powolania do zycia w 1951 roku, bylismy przedsiebiorstwem panstwowym, gospodarstwem panstwowym podlegajacym urzedowi miejskiemu, az w koncu od wrzesnia 1995 roku spolka. Jedyne, co nie zmienilo sie przez tyle lat dzialalnosci, to wysoka jakosc uslug swiadczonych przez nasz zaklad. Czego dowodem moze byc liczne grono uslugobiorcow, ktorzy korzystaja z naszej oferty. Oprocz naszej rodzinnej miejscowosci naleza do nich nastepujace gminy Lniano, Bukowiec, Naklo, Swiekatowo, Osie, Jezewo i Sadki.&nbsp;</div><div align="center"><br></div>  <div align="center"><h2 style="text-align: center;">Profesjonalizm, starannosc, terminowosc</h2><div align="justify"><br></div></div>    <div align="justify">Wybierajac nasz zaklad <a href="http://www.zuk-swiecie.pl/o-nas">uslug komunalnych</a>, moga miec Panstwo pewnosc stabilnej i dlugoterminowej wspolpracy. <b>Niezaleznie do tego, czy chodzi o oczyszczanie poziome ulic, formowanie zywoplotow czy wywoz nieczystosci stalych, nasze uslugi swiadczymy zawsze profesjonalnie, terminowo i starannie.&nbsp;</b></div><div align="center"><b><br></b></div><div align="center"><h2 style="text-align: center;">Szeroki zakres uslug</h2><div><div align="justify">Nasza oferta, oprocz <a href="http://www.zuk-swiecie.pl/oferta/wywoz-odpadow-komunalnych">uslug odbioru odpadow komunalnych</a> oraz dbania o estetyke i czystosc miasta, obejmuje rowniez uslugi transportowe. Zajmujemy sie przewozem materialow sypkich i roznego rodzaju towarow. Dysponujemy duza flota, zlozona z wielu rozmaitych modeli samochodow ciezarowych o roznej ladownosci. Ponadto oferujemy szeroki zakres prac ziemnych wykonywanych ciezkim sprzetem, a takze <b>utrzymanie porzadku na terenach utwardzonych</b>. Zapraszamy do zapoznania sie nasza oferta.<br></div><br><br></div></div>  <br></div>

