<span class="title">STUDIA STACJONARNE TAKZE W SOBOTY I NIEDZIELE</span>

Senat WWSSE w Srodzie Wlkp., w dniu 10.01.2019 r. podjal uchwale o prowadzeniu studiow stacjonarnych takze w soboty i niedziele. Studia te beda odbywaly sie zgodnie z planem dostepnym na stronie WWSSE.
Studentow I i II roku studiow licencjackich i I roku studiow magisterskich zapraszamy na studia stacjonarne od drugiego semestru roku akademickiego 2018/2019. Oplata za te studia jest taka sama jak za studia niestacjonarne, a liczba godzin zajec zgodna z Uchwala Senatu i Ustawa Prawo o szkolnictwie wyzszym i nauce.
Zajecia na studiach stacjonarnych beda odbywaly sie w soboty i niedziele, ewentualnie w piatki.
Senat podjal takze uchwaly o utworzeniu studiow stacjonarnych i niestacjonarnych na kierunku filologia w specjalnosciach: filologia angielska, germanska i rosyjska takze w specjalnosciach nauczycielskich.

