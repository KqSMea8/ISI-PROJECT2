<span class="title">STYPENDIA DLA STUDENTOW - RODZIELONE</span>

Uczelnia przyznala na I semestr roku akademickiego 2018/2019 - 256 stypendiow.
W I semestrze roku akademickiego 2018/2019 WWSSE wyplaci swoim studentom okolo 705 tys zl.
Maksymalna kwota przyznanego stypendium socjalnego wynosi 750 zl miesiecznie, stypendium Rektora dla najlepszych studentow - 650 zl oraz stypendium specjalnego dla osob niepelnosprawnych 300 zl.
Najwyzsza kwota lacznego stypendium, jaka otrzymuja studenci WWSSE w Srodzie Wlkp. co miesiac to 1700 zl, a wiec jest to kwota znacznie przekraczajaca czesne, ktore w naszej Uczelni wynosi na studiach I stopnia na kierunku pedagogika, praca socjalna, ekonomia, dietetyka 300 zl; na kierunku informatyka 340 zl miesiecznie, a na studiach II stopnia na kierunku pedagogika i ekonomia - 340 zl miesiecznie.
