<h2></h2>
<p>&nbsp;</p>
<p>BIM jest jednym z najgoretszych tematow ostatnich lat w branzy budowlanej. Wykorzystanie cyfrowego modelu 3D budynku w projektowaniu i realizacji inwestycji spowodowalo istotne zwiekszenie efektywnosci tych procesow.</p>
<blockquote><p>BIM jako technologia, metodyka, zbior procedur i narzedzi jest wielobranzowy. Tytul &#8222;interdyscyplinarny BIM&#8221; to swiadomy wybor, aby podkreslic interdyscyplinarny charakter studiow, ktore powstaly na bazie doswiadczen dydaktycznych dwoch wiodacych wydzialow branzy budowlanej na Politechnice Warszawskiej.</p></blockquote>
<h3><a href="https://www.ibim.pw.edu.pl/?page_id=228">Korzysci dla uczestnikow miedzywydzialowych studiow podyplomowych interdyscyplinarny BIM &gt;&gt;</a></h3>
<p>Studia kierowane sa do absolwentow studiow pierwszego i drugiego stopnia, ktorzy rozpoczeli kariere zawodowa i chca poszerzyc kompetencje, zwiekszajac swoja atrakcyjnosc na rynku pracy. Osob zainteresowanych przekraczaniem granic swojej dziedziny &#8211; projektantow architektury, konstrukcji lub instalacji chcacych wykorzystac technologie BIM w wielobranzowych zespolach projektowych.</p>
<p>Zajecia prowadzone beda na Wydziale Inzynierii Ladowej 
i Wydziale Architektury Politechniki Warszawskiej 
w systemie zaocznym, w dwoch semestrach, 16 dwudniowych zjazdow w soboty i niedziele - lacznie 256 godzin dydaktycznych.</p>
