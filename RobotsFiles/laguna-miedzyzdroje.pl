<p>Zapraszamy do odwiedzenia naszego osrodka. To idealne miejsce na wymarzone wakacje. Osrodek polozony jest tylko 400 metrow od piaszczystej plazy, w cichej i spokojnej okolicy, a jednoczesnie w centrum Miedzyzdrojow. Zapewniamy naszym gosciom komfortowe i w pelni wyposazone pokoje, kazdy z nich posiada wlasna osobna lazienke. Dysponujemy prywatnymi kwaterami dla dwoch, trzech i czterech osob. Dla komfortu naszych gosci w osrodku panuje calkowity zakaz palenia. Zapewniamy profesjonalna obsluge, dbamy o to, by wszyscy nasi goscie byli zadowoleni ze swojego pobytu. Oferujemy korzystne ceny i najlepsze standardy noclegow. Zachecamy do zapoznania sie z nasza galeria fotografii oraz do kontaktu. Wystarczy zadzwonic lub wyslac e-mail i zapytac o wolne terminy. Zapraszamy serdecznie do Blekitnej Laguny! Z nami kazde wakacje beda udane!</p>
