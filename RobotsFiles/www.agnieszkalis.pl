<h1>Agnieszka Lis</h1>
<p>Agnieszka Lis - pisarka. Z wyksztalcenia pianistka i&nbsp;dziennikarka. Przez wiele lat handlowiec w&nbsp;wielkich korporacjach.
Autorka powiesci <i><b>Jutro bedzie normalnie</b></i> (2011), <i><b>Samotnosc we dwoje</b></i> (2011), <i><b>Pozytywka</b></i> (2016), <i><b>Karuzela</b></i> (2017) oraz cyklu bajek dla przedszkolakow <i><b>Przygody Pana Parasola</b></i> (2011 - wygraly "Konkurs na dobra powiesc" w kategorii bajek dla dzieci). Czlonek Stowarzyszenia Autorow Polskich oraz Stowarzyszenia Autorow i Wydawcow Copyright Polska.
</p>
<p>
Przez wiele lat publikowala w&nbsp;branzowych czasopismach muzycznych - przede wszystkim w&nbsp;<i><b>Hi-Fi i&nbsp;Muzyka</b></i>, gdzie byla takze redaktorem odpowiadajacym za dzial "Muzyka, plyty audiofilskie". Publikowala w&nbsp;magazynie <i><b>STUDIO</b></i> oraz <i><b>KLASYKA.</b></i>
</p>
<p>
Koszalinianka. Na studia przyjechala do&nbsp;Warszawy, i&nbsp;tutaj zostala, a&nbsp;dokladniej pod Warszawa, z&nbsp;dala od halasu i&nbsp;biegu.<br />
Uczy muzyki, gry na&nbsp;fortepianie, prowadzi warsztaty kreatywnego pisania i&nbsp;szkolenia pisarskie.<br>
Jest pelnoetatowa zona, wychowuje dzieci, dba o dom i zajmuje sie wszystkim tym, czego wymaga proza zycia.
</p>
<p>
I, oczywiscie, pisze.
</p>
