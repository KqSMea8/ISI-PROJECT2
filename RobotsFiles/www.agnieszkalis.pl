<div class="main_page_box">
<span class="title">"Gwiezdne marzenia"</span>
<span class="date">25.06.2017</span><br/>
<hr />
<span class="desc">
Wyjatkowa, pieknie zilustrowana ksiazka, ktora powstala z mysla o dzieciach chorychi opuszczonych.
Nad powstaniem ksiazki dla dzieci ,,Gwiezdne marzenia" przez dziewiec miesiecy pracowalo ponad sto osob. Co wazne: WSZYSCY pracowali za darmo. W koncu to ksiazka charytatywna!<br>
Uroczysta Premiera ksiazki charytatywnej dla dzieci ,,Gwiezdne marzenia" odbyla sie 25 czerwca 2017 w Studio Polskiego Radia im. Wladyslawa Szpilmana.
Ksiazka w pierwszej kolejnosci zostanie przekazana do szpitali i Rodzinnych Domow Dziecka, nastepnie do hospicjow i domow dziecka. W nastepnej kolejnosci otrzymaja ja darczyncy, ktorzy wplacili pieniadze na druk ksiazki (w akcji na portalu Wspieram.to).
Od wrzesnia ksiazka bedzie dostepna w sprzedazy - dochod wesprze akcje charytatywne na Siepomaga.pl.

Bo marzenia sie nie spelniaja - marzenia sie spelnia. Szczegolnie te gwiezdne!<br>
