<h3>
O Firmie
</h3>

<p>Architektura to sztuka projektowania budynkow oraz innych budowli przestrzennych, organizowania przestrzeni uzywanej przez czlowieka.<br>Wg Witruwiusza, dobry budynek powinien spelniac trzy reguly - trwalosci, uzytecznosci i piekna.<br><br>&nbsp;* Trwalosci - powinien stac niewzruszony,<br>&nbsp;* Uzytecznosci - powinien byc uzyteczny i funkcjonalny,<br>&nbsp;* Piekna - powinien zachwycac ludzi i podnosic ich na duchu.<br><br>Pracownia Autorska Architektoniczna Krzysztof Kulik to firma dzialajaca na rynku od dwudziestu lat.<br>W swojej pracy dazymy do jak najlepszego polaczenia wszystkich trzech atrybutow.<br>Obiekt, dzielo architektury, powinien odpowiadac zamierzonej funkcji, celowosci technicznej, wymaganiom ekonomicznym i estetycznym, a przede wszystkim, dazeniom i oczekiwaniom uzytkownikow jego przestrzeni.<br><br>Wspolczesne obiekty wymagaja w procesie projektowania powiazania ze wspolczesnymi wymaganiami technicznymi, uwzglednienia zdrowia uzytkownikow oraz rownowagi ekologicznej, znajomosci uwarunkowan artystycznych, kulturowych, intelektualnych, historycznych, spolecznych, ekonomicznych i srodowiskowych.<br>Dzialalnosc architekta obejmuje szeroki zakres - od projektowania detali i wnetrz, poprzez obiekty architektoniczne i ich zespoly, do wielkich form urbanistycznych i krajobrazowych.<br><br>Dzisiaj projektowanie i architektura wymaga zespolu profesjonalistow z roznych dziedzin, ale architekt pozostaje nadal najwazniejszy.<br><br>,,Zatrudnisz kamien, drewno i beton i za pomoca tych materialow budujesz domy i palace: to jest konstruowanie. Pomyslowosc jest w pracy. Ale nagle dotykasz mojego serca, sprawiasz mi przyjemnosc. Jestem szczesliwy i mowie: To jest piekne. To jest architektura." - Le Corbusier<br></p>

