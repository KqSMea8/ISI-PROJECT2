<h2>Wstep</h2>

<p>Zapraszamy na Europejski Tydzien Zrownowazonego Transportu, ktorego celem jest promocja alternatywnych dla samochodu srodkow transportu w miescie, szczegolnie komunikacji miejskiej, rowerow i poruszania sie pieszo. Chcemy zachecic mieszkancow i mieszkanki Lublina do zmiany nawykow transportowych (chocby na tydzien!)</p>

<p>W tym roku, w grupie kilkunastu roznorodnych inicjatyw, w ramach obchodow ETZT przygotowalismy dla Was szereg atrakcyjnych wydarzen <!--i konkursy z nagrodami (w tym piekny rower miejski)-->, oraz przestrzenie w centrum Lublina w nowej odslonie. Zarzad Transportu Miejskiego zacheca do darmowych przejazd komunikacja miejska w dniu 22 wrzesnia - <b>na podstawie waznego dowodu rejestracyjnego</b>.</p>

<p>Zachecamy do udzialu w spotkaniach i wydarzeniach, a takze do kontaktu z nami i do wspolpracy. W tym roku do obchodow ETZT po raz kolejny dolacza Swidnik. Witamy!</p>

<p>Europejski Tydzien Zrownowazonego Transportu oraz Europejski Dzien bez Samochodu jest odpowiedzia na negatywne konsekwencje wynikajace z nadmiernego ruchu samochodowego w miastach.</p>

<p>Aby obejrzec caly harmonogram zajrzyj do dzialu <a href="/?name=harmonogram">"co, gdzie, kiedy?"</a>;</p>
<p>Kampania skupia sie glownie na trzech zadaniach:<br/>
<ul>
<li>zacheceniu jak najwiekszej liczby osob do uzywania alternatywnych form transportu takich jak komunikacja publiczna, piesza lub rowerowa</li>
<li>zwiekszeniu swiadomosci oraz informowaniu o zwiazanych z masowa motoryzacja negatywnych konsekwencjach dla jakosci zycia</li>
<li>pokazaniu miasta bardziej przyjaznego mieszkancom</li>
</ul>
</p>
<p>Europejski Dzien bez Samochodu oraz Europejski Tydzien Zrownowazonego Transportu jest okazja dla wladz wszystkich uczestniczacych miast do pokazania swojej troski o sprawy jakosci zycia.</p>
<p>Dlatego tez ogolnym celem kampanii jest ksztaltowanie proekologicznych wzorcow zachowan oraz popularyzacja transportu miejskiego przyjaznego srodowisku.</p>
<p>Ponadto, kampania przyczynia sie do zmniejszenia zanieczyszczen powietrza oraz halasu, przez co wplywa na polepszenie jakosci zycia w miescie.</p>
<p>Daje rowniez mozliwosc zaangazowania mieszkancow w dzialania na rzecz wspolnego dobra jakim jest miejsce, w ktorym zyja na co dzien.</p>

