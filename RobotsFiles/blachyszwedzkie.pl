<p><a href="http://www.mluksus.pl/blachodachowka/" target="_blank"><b>Blachodachowka</b></a>
to obecnie jeden z tanszych sposobow na pokrycie dachu.<br />
Blachodachowki posiadaja estetyczne ksztalty, ktore do zludzenia przypominaja klasyczne rodzaje dachowek ceramicznych, zachowujac przy tym walory uzytkowe i kosztujac mniej. Produkcja
blachodachowek na calym swiecie polega na wprowadzeniu gotowej lakierowanej blachy stalowej w kregu (w formie tasmy) do maszyny. Procesy przetwarzania
obejmuja wzdluzne proflowanie blachy (poczawszy od srodka), natluszczanie, formowanie przetloczenia oraz ciecie na zamowiony wymiar.<br /><br />

Dzieki uzyciu blach szwedzkich czy blachodachowek konstrukcja dachu staje sie lekka, nie obciazajac scian i fundamentow.
Poza tym nie wymagaja one robienia dodatkowych odeskowan, ktore byly by podpora dla ciezkiego dachu.</p>
