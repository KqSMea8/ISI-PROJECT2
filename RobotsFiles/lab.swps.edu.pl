<h1 class="entry-title">
<a href="http://lab.swps.edu.pl/?p=159" rel="bookmark">Konta do Panelu Badawczego</a>
</h1>

<div class="entry-meta">
<span class="date"><a href="http://lab.swps.edu.pl/?p=159" title="Bezposredni odnosnik do &#8222;Konta do Panelu Badawczego&#8221;" rel="bookmark"><time class="entry-date" datetime="2014-11-12T08:01:54+00:00">12 listopada 2014</time></a></span><span class="categories-links"><a href="http://lab.swps.edu.pl/?cat=1" rel="category">Bez kategorii</a></span><span class="author vcard"><a class="url fn n" href="http://lab.swps.edu.pl/?author=1" title="Zobacz wszystkie wpisy, ktorych autorem jest blazej.mrozinski" rel="author">blazej.mrozinski</a></span>					</div><!-- .entry-meta -->
</header><!-- .entry-header -->

<div class="entry-content">
<p>Szanowni Panstwo,</p>
<p>trwaja prace nad automatycznym dostepem do Panelu Badawczego za pomoca Panstwa loginow SWPS. W tej chwili jest to jeszcze niemozliwe, ale powinno sie w ciagu kilku dni zmienic.</p>
<p>Wszystkich, ktorzy <a title="Panel Badan Internetowych" href="http://lab.swps.edu.pl/?page_id=48">zglaszali chec utworzenia konta</a>, bardzo prosze o cierpliwosc. Caly proces <strong>odbywa sie recznie</strong> i wymaga chwili czasu.</p>
<p>Zwykle czas oczekiwania nie przekraczal kilku godzin w ciagu dnia roboczego &#8211; teraz, gdy kolejka chetnych siega 100 osob potrzebuje na to 2-3 dni.</p>
<p>Konto zostanie wkrotce utworzone a dane potrzebne do logowania, system automatycznie przesle na podany e-mail.</p>
<p>Wszelkie informacje nt samego dzialania platformy badawczej mozna odnalezc w manualu pod adresem http://manual.limesurvey.org/LimeSurvey_Manual</p>
</div><!-- .entry-content -->

