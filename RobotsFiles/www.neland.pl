<h1>O firmie</h1>
<p>Domena Neland-u od poczatku jego istnienia jest koncentracja na zapewnieniu najlepszych rozwiazan klasy klient-serwer dla niewielkich przedsiebiorstw, jak rowniez duzych korporacji, ktore pragna w prosty i szybki sposob zaspokajac podstawowe potrzeby zwiazane z obsluga procesow biznesowych.</p>
<p>W oparciu o uslugi wlasne oraz o wspolprace z partnerami dostarczamy rozwiazania z zakresu nastepujacych dziedzin:</p>
<ul>
<li>projektowanie grafiki reklamowej i tworzenie stron internetowych,</li>
<li>budowa i aktualizacja serwisow WWW,</li>
<li>tworzenie sytemow informatycznych klasy CMS, CRM i innych, obslugujacych specyficzne potrzeby klienta,<br /></li>
<li><span>rozwiazania z zakresu bezpieczenstwa systemow informatycznych.</span></li>
</ul>
<p><span>Z profesjonalizmem przeprowadzimy Panstwa rowniez przez caly proces zaistnienia w internecie - poczawszy od wyboru miejsca utrzymania serwisu internetowego, poprzez konfiguracje kont pocztowych i serwera WWW, az do projektu witryny internetowej i zamieszczenia na serwerze.<br /><br />Kontynuujac opieke oferujemy wspracie informatyczne, zapewnienie bezpieczenstwa Panstwa danym az po szkolenia pracownikow z wybranego zakresu wiedzy informatycznej.<br /></span></p>
