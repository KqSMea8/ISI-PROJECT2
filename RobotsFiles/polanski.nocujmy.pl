<div class="title"><h1>Apartament Polanski</h1></div>
<div class="content">Komfortowy, nowoczesnie urzadzony, w pelni wyposazony apartament czteropokojowy, mieszczacy sie na drugim pietrze (poddasze) przedwojennej kamienicy w centrum Rabki-Zdr&oacute;j. <br /><br />W stumetrowym apartamencie moze zamieszkac od jednej do osmiu os&oacute;b. <br /><br />Lokal sklada sie z: <br />- sypialni 30<br />-metrowej (z rozkladanym podw&oacute;jnym l&oacute;zkiem) wraz z przylegla sypialnia 12 metrowa (z podw&oacute;jnym l&oacute;zkiem) - sypialni 12-metrowej (z dwoma pojedynczymi l&oacute;zkami) <br />- pokoju 30 metrowego ( z rozkladanym podw&oacute;jnym l&oacute;zkiem) oraz aneksem kuchennym <br />- lazienki 10 metrowej. <br /><br />Pomieszczenia laczy przestronny przedpok&oacute;j. Do apartamentu przylega piekny, widokowy taras z calorocznym grillem. Kuchnia wyposazona jest w plyte indukcyjna, lod&oacute;wko-zamrazarke, mikrofal&oacute;wke, czajnik, naczynia i garnki. W obiekcie dostepny jest bezprzewodowy internet. <br /><br />Dla Gosci apartamentu dostepny jest parking. <br /><br />Cena: wynajem apartamentu: 300 zl za dobe. Promocja: w miesiacach styczen-marzec 2013 r. cena za wynajem calego apartamentu wynosi: 250 zl za dobe.</div>	</div>
