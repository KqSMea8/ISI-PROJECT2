<div style="text-align:justify; padding-top:8px;" class="desc">Skwer Gyddanyzc to nowa nazwa placu przy kosciele sw. Brygidy w Gdansku noszacego do niedawna imie ks. Henryka Jankowskiego. Za przyjeciem uchwaly w tej sprawie podczas dzisiejszej sesji Rady Miasta zaglosowalo 19 radnych, 5 bylo przeciwko, a 7 wstrzymalo sie od glosu. Nazwa, choc trudna do wymowienia, nie powinna budzic kontrowersji, oznacza bowiem najstarsza znana forme zapisu nazwy Gdansk. Z wnioskiem o  jej nadanie wystapila prezydent miasta Gdanska. Pomyslodawca jest profesor Andrzej Januszajtis.<br>
