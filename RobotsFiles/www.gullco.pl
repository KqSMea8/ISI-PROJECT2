<h1>Samojezdny wozek spawalniczy Moggy(r) i Magnetic Moggy(r)</h1>


<p class="text">Precyzyjne automatyczne spawanie pachwinowe, na zakladke i doczolowe z zastosowaniem uniwersalnego samojezdnego wozka do roznych zastosowan.</p>
<p class="text">
Opcje modeli wozkow:
<ul>
<li>- modele z jednym lub dwoma palnikami</li>
<li>- baza magnetyczna lub niemagnetyczna</li>
<li>- modele do spawania ciaglego lub przerywanego</li>
</ul>
</p>
<p class="text">
Dostepne sa opcje dodatkowe:
<ul>
<li>- glowica do spawania oscylacyjnego</li>
<li>- kola prowadzace do spawania na zakladke</li>
<li>- przyrzad do promienia wewnetrznego</li>
<li>- przyrzad z palnikiem do ciecia</li>
<li>- tor z rowkiem klinowym</li>
</ul>
</p>
<p class="text">
Wozek MOGGY(r) przeznaczony jest do spawania ciaglego lub przerywanego przy wykorzystaniu sterowania realizowanego przez mikroprocesor Gullco. W zestawie znajduja sie rolki prowadzace o regulowanej wysokosci, przelacznik ograniczenia ruchu, pionowe i poziome moduly przesuwne (zapewniajace regulacje o 44,4 mm [1 3/4 cala]) oraz polautomatyczny uchwyt na palnik. Zostal on zaprojektowany tak, aby poruszac sie w pozycji poziomej lub pionowej (Magnetic MOGGY(r)) wzdluz wykonywanej spoiny pachwinowej, doczolowej lub na zakladke. Rolki prowadzace MOGGY(r) mozna skonfigurowac na kilka sposobow, co zapewnia niezrownana wszechstronnosc.
