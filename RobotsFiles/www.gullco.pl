<h1>Aktualnosci</h1>
<h1 class="entry-title">Pillars of Support</h1>
<div class="news">

<p><img src="img/news-kat_gullco_ascillator/Structure.jpg" alt="Structure" height="NaN" width="100%" /></p>
<p>Zr&oacute;dlo: Oculus train hub &ndash; photograph by duo Hufton + Crow</p>
<p>Oscylator spawalniczy Gullco&rsquo;s KAT&nbsp; zostal niedawno zastosowany przy odbudowie metra Oculus w Nowym Yorku - stacja, kt&oacute;ra zostala zniszczona podczas ataku terrorystycznego 11 wrzesnia. Urzadzenie zostalo wykorzystane do kluczowych operacji projektu konstrukcji. Czesc projektu hiszpanskiego architekta Santiago Calatrava&rsquo;s zawiera bombo odporne filary, kt&oacute;re&nbsp; sa rezultatem bardzo substancjonalnej spoiny niezbednej do produkcji tych wzmocnionych kolumn. Spoiny wytwarzane przez nasz w&oacute;zek spawalniczy KAT wyr&oacute;zniaja sie nie tylko wysoka jakoscia spoiny, a takze innymi wyjatkowymi cechami.&nbsp;</p>
<p>Glowica oscylatora spawalniczego pozwala na poruszanie sie w r&oacute;znych pozycjach oraz posiada wlasciwosc dotarcia do najglebszych rowk&oacute;w. Korzysc ta jest bardzo wazna dla projektu Oculus, z powodu ogromnej grubosci materialu oraz duzych stozkowych rowk&oacute;w niezbednych do zapewnienia odpornosci na eksplozje filar&oacute;w.</p>
<p>Niezalezna glowica byla niezbedna w tym przypadku, poniewaz projekt zostal op&oacute;zniony, przyjmujac 12 lat na zakonczenie. Prace skrajnie przekroczyly budzet. Urzadzania automatyzacji spawania sa czesto wybierane, kiedy projekt nie dotrzymuje termin&oacute;w lub spawy staja sie coraz bardziej kosztowne (nie wystepuja koszty skali) i w tym przypadku nie bylo inaczej.</p>
<p><img src="img/news-kat_gullco_ascillator/Welding.jpg" alt="Welding 768x1024" height="NaN" width="75%" /></p>
<p>Zr&oacute;dlo: KAT weld oscillator in operation at the Oculus project in New York; www.gullco.com</p>

<p>Spawanie jest czesto jednym z najwiekszych koszt&oacute;w og&oacute;lnych w kazdym projekcie budowlanym realizowanym na duza skale. Poprzez automatyzacje procesu nastepuje usuniecie wielu niepotrzebnym wad zwiazanych ze spawaniem recznym. Przestoje oraz zmeczenie spawacza zostaja wyeliminowane. Nastepuje poprawa osadzenia spoiny. Na takim grubym spawie nawet niewielka poprawa moze spowodowac dziesiatki tysiecy dolar&oacute;w oszczednosci. Dzieki wdrozeniu rozwiazan automatyzacji produkcji takich jak np. oscylator spawania uzyskuje sie bardzo precyzyjna kontrole nad spoina oraz eliminuje sie kosztowne przer&oacute;bki . Dzieki czemu inwestycja splaca sie w kr&oacute;tkim czasie produkcji.</p>
<p>Czescia strategii wdrazania Gullco jest zapewnienie kurs&oacute;w i szkolen w miejscu pracy dla kazdego urzadzenia, kt&oacute;re zostalo zakupione. Gwarantuje to jakosc wynik&oacute;w oraz nastawia zar&oacute;wno uzytkownika koncowego jak i sprzet na sukces. Przeszkoleni operatorzy w miejscu realizacji projektu Oculus&nbsp;byli zdolni do wykonania tych niezwykle waznych spoin konczac te czesc projektu zgodnie z harmonogramem.</p>

<p>Artykul autorstwa Nicka Drake: <a href="http://www.gullco.com/pillars-of-support/">http://www.gullco.com/pillars-of-support/</a> [EN]</p>

