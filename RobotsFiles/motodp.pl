<h1>O NAS</h1>
<hr>
<p>
Prowadzimy warsztat samochodowy oraz motocyklowy, swiadczac kompleksowe uslugi w zakresie diagnostyki, wulkanizacji oraz naprawy pojazdow. Z zamilowania do motoryzacji postanowilismy otworzyc warsztat oferujacy uslugi na najwyzszym poziomie, zapewniajac naszym Klientom satysfakcje z wykonanych uslug oraz bezpieczenstwo podczas dalszego uzytkowania pojazdu. Nasza pasja, poparta szeroka wiedza oraz wieloletnim doswiadczeniem w branzy motoryzacyjnej gwarantuje Panstwu sprawna obsluge i profesjonalna realizacje zlecen.
Nasz warsztat wyposazony jest w profesjonalny sprzet renomowanych firm oraz nowoczesne narzedzia. Posiadamy kilka stanowisk naprawczych, do obslugi opon i elektromechaniki oraz stanowiska do obslugi motocykli.
W swojej ofercie mamy oryginalne czesci zamienne, oleje oraz filtry znanych marek a takze ich tansze zamienniki. Mozemy sprostac kazdym oczekiwaniom cenowym dopasowujac oferte do okreslonego budzetu. Wspolpracujemy rowniez z firmami zajmujacymi sie regeneracja takich elementow jak przekladnie, turbiny, uklady wtryskowe. itp
