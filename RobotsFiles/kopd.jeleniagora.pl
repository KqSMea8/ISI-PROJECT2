</header><!-- .entry-header -->
<div class="entry-content">
<p style="text-align: justify">Droga Krystyno. Jeszcze raz dziekuje za wspolprace. Przybylas w tak duzej liczbie osob, to drogi prezentem. List ponizszy przekaz wszystkim polskim uczestnikom.</p>
<p style="text-align: justify"><strong>Drodzy uczestnicy 25. Miedzynarodowej Konferencji.</strong></p>
<p style="text-align: justify">Jubileuszowa czesko-polsko-niemiecka konferencja w Usti nad Labem, odbyta z powodzeniem, jest juz za nami. Wspolnie z partnerami niemieckimi i polskimi caly dzien spedzilismy w Congress Clarion na konferencji na temat ,,Kompetencje rodzicielskie&#8221;. Wyklady wskazaly na rozne spojrzenia i byly bardzo inspirujace &#8211; do przemyslenia. Referaty wszystkich 3 krajow wskazywaly na prace z dziecmi, mlodzieza i rodzinami. Wysylam materialy z tych referatow przygotowane na multimedia, do ktorych mozecie wracac. Dolaczam tez wspolne zdjecie (niestety, nie wszyscy jestesmy), ale mysle, ze jest bardzo dobre. Wierze, ze konferencja spelnila twoje oczekiwania i kazdy z Was wzial to, czego potrzebowal.</p>
<p style="text-align: center"><a href="http://kopd.jeleniagora.pl/wp-content/uploads/2018/11/Spolecna.jpg"><img class="alignnone size-full wp-image-1435" src="http://kopd.jeleniagora.pl/wp-content/uploads/2018/11/Spolecna.jpg" alt="Spolecna" width="2000" height="1335" /></a></p>
<p style="text-align: justify">Mam nadzieje, ze przeslane materialy i mozliwosc wejscia na strone <a href="http://www.silnirodice-silnedeti.cz">www.silnirodice-silnedeti.cz</a>, ktora zostala niedawno zaktualizowana, zaciekawia Was. A jesli jestescie zainteresowani, mozna rowniez wejsc na strone organizacji goszczacej &#8211; Centrum Wolontariatu Z.S. <a href="http://Www.dcul.cz">Www.dcul.cz</a> i FB: Centrum Wolontariatu, Z.S., gdzie prezentowane sa rowniez informacje o sprawach rodzicielskich i innych dzialaniach.</p>
<p style="text-align: justify">Raz jeszcze dziekuje wszystkim za godna postawe, za patronat nad Konferencja gubernatora Pana Bubenicek, wszystkim referentom, absolutnie wspanialej Pani mgr Michelle Advisor, niestrudzonym tlumaczom, Clarion Hotel za swietna obsluge, studiu Karim i fotografowi Vlada oraz wszystkim gosciom. Jestesmy wdzieczni tym, ktorzy z roznych powodow nie mogli byc z nami. Moze beda w nastepnym roku w Niemczech - gospodarza 26 Konferencji.</p>
<p style="text-align: right">Z powazaniem, Centrum Wolontariatu</p>
