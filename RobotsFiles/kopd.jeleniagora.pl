&nbsp; <span class="author vcard" >Przez <a class="url fn n" href="http://kopd.jeleniagora.pl/?author=4" title="View all posts by Jozef Pawlowski">Jozef Pawlowski</a> <span class="bl_sep">|</span></span>  <span class="onDate"> <a href="http://kopd.jeleniagora.pl/?p=1434" title="13:19" rel="bookmark"><span class="entry-date">12 listopada 2018</span> <span class="entry-time"> - 13:19</span></a> <span class="bl_sep">|</span> </span> <span class="bl_categ"> <a href="http://kopd.jeleniagora.pl/?cat=1" title="Zobacz wszystkie wpisy z kategorii &#8222;STATUT&#8221;" rel="category">STATUT</a> </span>  							<div class="comments-link">
