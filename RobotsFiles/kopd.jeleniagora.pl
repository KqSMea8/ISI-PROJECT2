<h2 class="entry-title"><a href="http://kopd.jeleniagora.pl/?p=1425" title="Permalink to Jak pomoc dziecku krzywdzonemu?" rel="bookmark">Jak pomoc dziecku krzywdzonemu?</a></h2>
</hgroup>
<div class="entry-meta">
&nbsp; <span class="author vcard" >Przez <a class="url fn n" href="http://kopd.jeleniagora.pl/?author=4" title="View all posts by Jozef Pawlowski">Jozef Pawlowski</a> <span class="bl_sep">|</span></span>  <span class="onDate"> <a href="http://kopd.jeleniagora.pl/?p=1425" title="10:48" rel="bookmark"><span class="entry-date">15 pazdziernika 2018</span> <span class="entry-time"> - 10:48</span></a> <span class="bl_sep">|</span> </span> <span class="bl_categ"> <a href="http://kopd.jeleniagora.pl/?cat=1" title="Zobacz wszystkie wpisy z kategorii &#8222;STATUT&#8221;" rel="category">STATUT</a> </span>  							<div class="comments-link">
<a href="http://kopd.jeleniagora.pl/?p=1425#respond" title="Skomentuj &#8222;Jak pomoc dziecku krzywdzonemu?&#8221;">Skomentuj</a>			</div>
</div><!-- .entry-meta -->


</header><!-- .entry-header -->
<div class="entry-content">
<p style="text-align: justify"><a href="http://kopd.jeleniagora.pl/wp-content/uploads/2015/03/asd1.jpg"><img class="alignnone size-full wp-image-36" src="http://kopd.jeleniagora.pl/wp-content/uploads/2015/03/asd1.jpg" alt="asd1" width="150" height="44" /></a> Jak pomoc dziecku krzywdzonemu w roznych sytuacjach i jak zgodnie z prawem doprowadzic do umieszczenia go z dala od oprawcy, gdy po badaniu lekarskim ma sie pewnosc, ze dziecko bylo naprawde krzywdzone, zarowno fizycznie, jak i psychiczne? To byly jedne z glownych tematow piatkowej konferencji, ktora odbyla sie 12.10.2018 w sali konferencyjnej Centrum Szpitalnego Kotliny Jeleniogorskiej na Zabobrzu.</p>
<p style="text-align: center"><a href="http://kopd.jeleniagora.pl/wp-content/uploads/2018/10/IMG_20181012_101205.jpg"><img class="alignnone size-full wp-image-1427" src="http://kopd.jeleniagora.pl/wp-content/uploads/2018/10/IMG_20181012_101205.jpg" alt="IMG_20181012_101205" width="1024" height="608" /></a></p>
<p style="text-align: justify">Wsrod zaproszonych gosci byli kuratorzy, policjanci, pedagodzy, psycholodzy, prawnicy, a nawet prezes sadu. Otwarcie rozmawiano na temat przemocy w stosunku do dzieci poruszajac rozne aspekty i problemy. Wyglaszane prelekcje dotykaly rowniez tak delikatnych tematow jak przemoc seksualna oraz ewidentne zaniedbania, gdy rodzice naduzywaja alkoholu i nie sa w stanie kontrolowac swoich emocji, a tym bardziej wychowywac wlasne dzieci.<em><br />
</em></p>
<p style="text-align: center"><a href="http://kopd.jeleniagora.pl/wp-content/uploads/2018/10/IMG_20181012_101312.jpg"><img class="alignnone size-full wp-image-1428" src="http://kopd.jeleniagora.pl/wp-content/uploads/2018/10/IMG_20181012_101312.jpg" alt="IMG_20181012_101312" width="1024" height="768" /></a> <a href="http://kopd.jeleniagora.pl/wp-content/uploads/2018/10/IMG_20181012_101542.jpg"><img class="alignnone size-full wp-image-1429" src="http://kopd.jeleniagora.pl/wp-content/uploads/2018/10/IMG_20181012_101542.jpg" alt="IMG_20181012_101542" width="1024" height="768" /></a></p>
<p style="text-align: justify"><em>- Niestety czesto otrzymujemy sygnaly, ze w przypadku dzieci krzywdzonych nauczyciele i wychowawcy w placowkach edukacyjnych nie bardzo wiedza jak im pomoc, jak interweniowac w takich sytuacjach i gdzie kierowac sie po fachowa pomoc, zeby jednoczesnie nikogo nie skrzywdzic. Dzis mamy tu przedstawicieli prawie wszystkich instytucji, ktore w konkretny sposob pomagaja dzieciom, wiec wymiana doswiadczen jest bardzo wazna, to takze okazja poszerzenia swojej wiedzy w tym temacie</em> - podkreslila Krystyna Demkowicz, przewodniczaca Terenowego Komitetu Ochrony Praw Dziecka w Jeleniej Gorze.</p>
<p style="text-align: justify">Takze bardzo istotny i zarazem klopotliwy problem pozostawiania zdrowych dzieci, ktore doznaly krzywdy, w szpitalu na oddzialach z powodu braku innych mozliwosci, poruszyla Anna Sinica Prezes Stowarzyszenia Pomocy Chorym Dzieciom przy Oddziale Psychiatrycznym.</p>
<p style="text-align: justify">- Kazdy przypadek jest inny. Pytan i watpliwosci powstaje wiec czesto sporo, ale wazne, ze sa ludzie, ktorym na sercu lezy dobro kazdego dziecka i potrafia o to walczyc - oswiadczyla z przekonaniem Pani prezes.</p>
<p style="text-align: justify">Organizatorem konferencji byly Terenowy Komitet Ochrony Praw Dziecka, Wojewodzkie Centrum Szpitalne Kotliny Jeleniogorskiej oraz Stowarzyszenie Pomocy Chorym Dzieciom przy Oddziale Pediatrycznym.</p>
