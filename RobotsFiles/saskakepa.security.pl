<div id="col2_middle"><div><em><strong>Szanowni Panstwo!</strong></em></div>
<div> </div>
<div>Jest nam niezmiernie milo powitac Panstwa w serwisie internetowym pierwszej, nie tylko w Warszawie, ale rowniez w Polsce, <strong>Lokalnej Inicjatywy Informatycznej</strong> - <strong>"Bezpieczna Saska Kepa"</strong>.</div>
<div> </div>
<div><span style="text-decoration: underline;"><strong>W jej ramach oferujemy Panstwu:</strong></span><br />
<ul>
<li>tworzenie i serwis <strong>oprogramowania dla biznesu</strong></li>
<li>serwis <strong>sprzetu komputerowego</strong> i peryferiow (w tym serwis pogwarancyjny)</li>
<li><strong>bezpieczenstwo i monitoring informatyczny</strong> (projekty/rozwiazania/wdrozenia/wsparcie)</li>
<li><strong>pomoc</strong> przy doborze rozwiazan informatycznych</li>
<li>pomoc w doborze i bezpiecznym korzystaniu z rozwiazan typu <strong>Cloud Computing</strong>,</li>
<li><strong>szkolenia, warsztaty i seminaria</strong> - kazdorazowo dostosowywane do Panstwa indywidualnych potrzeb!</li>
</ul>
<div><strong>Celem</strong> niniejszego przedsiewziecia jest dostarczenie Panstwu najwyzszej jakosci, kompleksowych uslug informatycznych przy jednoczesnym zapewnieniu jak najkrotszego czasu jaki uplywa od przyjecia przez nas zlecenia, do momentu jego realizacji. </div>
<div> </div>
<div>Mozemy otoczyc Panstwa tak szybka i profesjonalna opieka, poniewaz nie tylko jestesmy Panstwa sasiadami z Saskiej Kepy, ale rowniez dzialamy na rynku rozwiazan informatycznych od jesieni 1993 roku. Pozwolilo nam to na zgromadzenie rozleglej wiedzy oraz doswiadczen w rozwiazywaniu najrozniejszych problemow u naszych Klientow. Przez te blisko 20 lat, nauczylismy sie, iz najlepsza forma opieki jest stale wsparcie i profilaktyka.</div>
<div> </div>
<div>Zapraszamy Panstwa do wspolpracy!!</div>
