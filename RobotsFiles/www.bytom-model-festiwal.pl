<h2>Informacja UM Bytom</h2>
<p>
Juz w najblizsza sobote, 2 marca rozpoczyna sie w Bytomiu - Szombierkach /Hala na Skarpie/ jedna z najwiekszych imprez w poludniowej Polsce - Miedzynarodowy Festiwal Modelarski. W tym samym dniu odbedzie sie takze Targ Staroci. Obie imprezy przyciagaja wielu odwiedzajacych - spodziewamy sie utrudnien w ruchu, dlatego zachecamy do przyjazdu komunikacja publiczna.
<br />Do Hali na Skarpie w Bytomiu - Szombierkach mozna latwo dojechac z dworca autobusowego na pl. Wolskiego i z pl. Sikorskiego, gdzie lacza sie linie tramwajowe w Bytomiu.

<p>
<b>AUTOBUS:</b>
<br />Z dworca autobusowego w strone Szombierek dojedziemy liniami nr 118, 146, 183 i 227, gdzie musimy wysiasc na przystanku Szombierki Kosciol. Nastepnie kierujemy sie pieszo w strone ul. Frycza - Modrzewskiego - kierunek Hala na Skarpie i stadion miejski.

<p>
<b>TRAMWAJ:</b>

<br />Tramwajem, do przystanku Szombierki Kosciol mozna rowniez dojechac liniami nr 5 i 9 z pl. Sikorskiego. Korzystajac z linii nr 9 mozna wysiasc na przystanku Szombierki Frycza Modrzewskiego. Szczegolowy rozklad jazdy dla linii autobusowych i tramwajowych znajduje sie na stronie Zarzadu Transportu Metropolitalnego w zakladce Rozklad Jazdy

<p>
Dla osob, ktore maja zamiar przyjechac samochodem do Szombierek z uwagi na wzmozony ruch - w dniu 2 marca moga tworzyc sie korki juz na ul. Zabrzanskiej i Frycza - Modrzewskiego. W tym dniu prosimy o pozostawienie samochodow na parkingach:
<ul>
<li>po dawnym markecie TESCO /wjazd od ul. Karpackiej/,</li>
<li>bocznych ulicach od ul. Frycza - Modrzewskiego tj. ks. Biskupa Wlodarskiego (tylko na wysokosci od ul. Frycza - Modrzewskiego do Zabrzanskiej), Witosa, Piatka i Krupinskiego,</li>
<li>na parkingu Szkoly Muzycznej - wjazd od ul. Krupinskiego - mozliwosc parkowania do 15.30!</li>
</ul>

<br />Z kolei odcinek ul. ks. Biskupa Wlodarskiego od ul. Modrzewskiego do wjazdu na Hale na Skarpie bedzie zamkniety od godz. 6.00 do 13.00 w zwiazku z organizowanym Festiwalem Modelarskim.

<br />Apelujemy o nie wjezdzanie samochodami na trawniki wokol blokow i w parku przy Hali na Skarpie oraz nie utrudnianie przejscia pieszym. Prosimy o przestrzeganie przepisow zakazujacych postoju i wjazdu.
</p>
