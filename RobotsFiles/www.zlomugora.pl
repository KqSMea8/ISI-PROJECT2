uczciwe wazenie na <u>legalizowanych</u> wagach elektronicznych,</li>
<li style="text-align: justify;">
rzetelna klasyfikacje towaru,</li>
<li style="text-align: justify;">
terminowosc odbior&oacute;w i dostaw,</li>
<li style="text-align: justify;">
szybka platnosc.</li>
</ul>
<p>
&nbsp;</p>
<p style="text-align: justify;">
Skup surowc&oacute;w w ilosciach hurtowych prowadzimy przy uzyciu wlasnego transportu, ilosci detaliczne przyjmujemy na placach w Bielsku-Bialej<br />
