<h2>O nas</h2>
<h3 class="pacifico">
Historia pewnego uczucia</h3>
<p>
Zaczelo sie dosc banalnie, bo jak sie mozecie domyslac, od wakacji w Tajlandii. Dobre jedzenie i dalekie
podroze, to jedne z naszych najwiekszych milosci...
</p>
<p class="italic quotation">
W momencie wyprobowania lodow tajskich po raz pierwszy - zakochalam sie. Mialam frajde z tego, ze moglam
sama skomponowac smak, a swieze owoce byly ciete i mieszane na biezaco, tuz przed podaniem lodow.
<span>Charlotta - Wspolwlascicielka</span>
</p>
<p class="italic quotation">
W Warszawie nie bylo jeszcze takiego produktu. Mialem wowczas jedna,przewodnia mysl: po powrocie do domu
- nadal chce jesc takie lody! I chce sie tym smakiem podzielic z jak najwieksza iloscia ludzi. W
szczegolnosci z tymi, ktorzy nie byli w Tajlandii - chce przyblizyc magie smaku tego miejsca.
<span>Gerald - Wspolwlasciciel</span>
</p>
<h3 class="pacifico about-us--tmp">Od pomyslu do realizacji</h3>
<p>
Nie rzucamy slow na wiatr - padl pomysl, trzeba go zrealizowac. I tym sposobem, po kilku miesiacach
tworzenia maszyn, knowhow i receptury lodowej - urodzil sie <span class="chop">ChopChop</span>.
Wieloletnie doswiadczenie w biznesie Geralda polaczone z kreatywnoscia Charlotty, sprawily ze <span class="chop">ChopChop</span>
stal sie preznie rozwijajaca sie marka.
</p>
<h3 class="pacifico">Z milosci do piekna, do podrozy i do niebanalnego jedzenia...
</h3>
