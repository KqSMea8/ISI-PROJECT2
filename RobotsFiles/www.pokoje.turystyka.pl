<h1 style="text-align: center;"><em><span style="color: #800000;"><strong><span style="font-size: 18pt;"><span style="font-size: 24pt;">Noclegi Krak&oacute;w dla grup zorganizowanych z wyzywieniem</span></span></strong></span></em></h1>
<p style="text-align: center;">&nbsp;</p>
<p style="text-align: center;"><strong><em><span style="font-size: 12pt; color: #800000;"><a style="color: #800000;" tabindex="-1" href="http://meteor-turystyka.pl/noclegi%2ckrakow%2c0.html" target="_parent">Noclegi w Krakowie</a><!--EndFragment -->&nbsp;z wyzywieniem dla grup turystycznych, &nbsp;grup szkolnych, firmowych, klubowych. Noclegi dla grup organizowanych przez biura turystyczne, biura podr&oacute;zy. &nbsp;Noclegi dla grup os&oacute;b starszych i mlodszych (sportowcy , seniorzy). Noclegi w Krakowie na wyjazdy z zaklad&oacute;w pracy, integracyjne i wypoczynkowe. Noclegi dla &nbsp;wycieczek z Dom&oacute;w Pomocy Spolecznej. &nbsp;Noclegi dla grup os&oacute;b niepelnosprawnych.</span></em></strong></p>
<p style="text-align: left; padding-left: 30px;">&nbsp;</p>
<h2 style="text-align: left;"><strong><span style="font-size: 14pt;">Lokalizacja. Nocleg w Krakowie. <span style="font-size: 12pt;"><em>Dzielnica - slawne Lagiewniki.</em></span></span></strong></h2>
<h2 style="text-align: left; padding-left: 30px;"><br /><span style="font-size: 12pt;"><strong>Dojazd do <span style="color: #800000;">centrum</span> Krakowa to tylko 15-25 minut</strong>.</span></h2>
<p>&nbsp;</p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;">Swietna lokalizacja na nocleg dla grupy zorganizowanej z wyzywieniem. Nocujac u nas w 15-25 minut dojedziesz bez problemu autokarem i&nbsp;<strong>zwiedzisz</strong> Centrum Krakowa (<strong>Rynek, Barbakan ,Sukiennice, Wawel</strong> ) i okolice (<strong>Wieliczke, Ojc&oacute;w, Oswiecim</strong>). Masz <strong><span style="text-decoration: underline; color: #ff0000;">latwy dojazd</span></strong> do wszystkich atrakcji, nie stoisz w korkach i nie szukasz miejsca do parkowania dla autokaru w ciasnych krakowskich uliczkach. Kierowca zawiezie grupe do centrum, moze do nas wr&oacute;cic i za kilka godzin podjechc w um&oacute;wione miejsce po odbi&oacute;r grupy.</span></p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;">Wygodnie i <span style="text-decoration: underline;">tanszy nocleg niz w centrum</span> Krakowa, a parking jest u nas <strong>bezplatny</strong>&nbsp;przez caly okres pobytu. </span></p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;">Czas dojazdu od nas <span style="color: #800000;"><strong>do Wawelu autokarem ok. 15 -20 min.</strong></span> Czy warto meczyc sie w centrum i denerwowac ?. Czas dojazdu <strong><span style="color: #800000;">do</span></strong> <span style="color: #800000;"><strong>Wieliczki ok.15 min.</strong></span> autostrada A4. Zastan&oacute;w sie bo nasza lokalizacja, to naprawde korzystne rozwiazanie.</span></p>
<h2 style="text-align: left;"><br /><span style="font-size: 14pt;"><strong>Oferta noclegowa.</strong></span></h2>
<p style="text-align: left;">&nbsp;</p>
<h3 style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;"><strong>Pokoje z lazienkami.</strong></span></h3>
<p style="text-align: left; padding-left: 30px;"><br /><span style="font-size: 12pt;"><strong>Wszystkie</strong> nasze <span style="color: #800000;"><strong>pokoje sa z lazienkami</strong></span> lub w ukladzie " <strong>studio</strong>" ( 2 pokoje + 1 Lazienka). Kazda lazienka wyposazona jest w kabine prysznicowa, umywalke i WC.</span></p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;">Dla <span style="color: #800000;"><strong>grupy</strong></span>&nbsp;proponujemy pokoje nie wieksze niz <span style="color: #800000;"><strong>2, 3, 4, osobowe</strong> <strong>z</strong> </span><strong><span style="color: #800000;">lazienkami</span>. </strong>Jesli jest taka potrzeba to &nbsp;moze byc r&oacute;wniez pok&oacute;j rodzinny dla 5 os&oacute;b , lub w ukladzie 2 + 3 + lazienka gdzie rodzina moze czuc sie swobodnie a jednoczesnie razem. </span></p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;">Rodziny lub malzenstwa maja spakowane rzeczy zazwyczaj do jednego bagazu, wiec nie mozna ich rozdzielac. Z tego wlasnie powodu malzenstwa uczestniczace w wycieczce tez powinny zamieszkac w jednym pokoju razem.Jesli organizatorzy wszesniej zglaszaja nam takie potrzeby to wtedy tak rozkladamy gosci aby wszyscy byli zadowoleni.</span></p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;">Dla <strong>organizatora/pilota</strong> oraz dla kierowcy mamy <strong>pok&oacute;j jednoosobowy</strong> z lazienka aby mogli odpoczac w spokoju.</span></p>
<p style="text-align: left; padding-left: 30px;">&nbsp;</p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;">Od wielu lat przyjmujemy grupy na nocleg i mamy duze doswiadczenie w sprawnym rozlokowaniu wycieczki i razem z organizatorem staramy sie zawsze aby wszyscy uczestnicy czuli sie u nas dobrze.</span></p>
<p style="text-align: left; padding-left: 30px;">&nbsp;</p>
<p style="text-align: left;"><span style="font-size: 14pt;"><strong>Restauracja. Wyzywienie. Zamawianie posilk&oacute;w.</strong></span></p>
<p style="text-align: left;">&nbsp;</p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;"><strong>Oferta - nocleg z wyzywieniem dla grup.</strong></span></p>
<p style="text-align: left; padding-left: 30px;">&nbsp;</p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;">Restauracja z pelnym wyzywieniem znajduje sie <strong>obok</strong> naszego Domu " Promien" (za ogrodzeniem). Nowa restauracja o nazwie &nbsp;" Dom sw. Siostry Faustyny " jest czynna od <strong>8.00 - 20.00</strong> . Restauracja jest prowadzona przez siostry ze Zgromadzenia Si&oacute;str Matki Bozej Milosierdzia.</span></p>
<p style="text-align: left; padding-left: 30px;">&nbsp;</p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;">Rezerwacja telefoniczna posilk&oacute;w dla grup zorganizowanych zamawiajacych nocleg z wyzywieniem w naszym Domu " Promien " odbywa sie pod nr. <strong>tel. 720 955 431</strong>&nbsp;(biuro restauracji) lub mailowo <a href="mailto:rezerwacja@siostryfaustyny.pl">rezerwacja@siostryfaustyny.pl</a>&nbsp;.</span></p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;"> Siostry prowadza jeszcze inne punkty z wyzywieniem wiec prosimy o dokladna informacje o lokalizacji restauracji : prosimy dodac informacje o tym, ze posilek jest zamawiany <strong>dla grupy nocujacej w Domu " Promien"</strong> przy parkingu <span style="color: #ff0000;"><strong>autokarowym</strong></span>&nbsp; znajdujacym sie przy ul. Motarskiego. Pozwoli to na unikniecie pomylek.&nbsp;</span></p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;">Ceny posilk&oacute;w dla <strong>grup zorganizowanych </strong>zamawiajacych nocleg z wyzywieniem w naszym Domu:&nbsp;</span></p>
<p style="text-align: left; padding-left: 30px;">&nbsp;</p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;">sniadanie od - 15 zl</span></p>
<p style="text-align: left; padding-left: 30px;">&nbsp;</p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;">obiadokolacja - od 20 - do 25 zl&nbsp;</span></p>
<p style="text-align: left; padding-left: 30px;">&nbsp;</p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;">Zapraszamy do korzystania z restauracji i zyczymy smacznego.</span></p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;">Bezposrednie sasiedztwo restauracji, dogodne menu dostosowane do r&oacute;znych potrzeb organizator&oacute;w sprawia, &nbsp;ze nasza oferta niedrogiego noclegu z wyzywieniem (+ dobry dojazd i parking) jest konkurencyjna dla ofert nocleg&oacute;w w Centrum Krakowa.&nbsp;</span></p>
<p style="text-align: left; padding-left: 30px;">&nbsp;</p>
<h2 style="text-align: left;"><br /><span style="font-size: 14pt;"><strong>Na terenie obiektu.</strong></span></h2>
<p style="text-align: left;">&nbsp;</p>
<h3 style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;"><strong>Sala do spotkan / TV.</strong></span></h3>
<p style="text-align: left; padding-left: 30px;"><br /><span style="font-size: 12pt;">Do dyspozycji sala do spotkan towarzyskich, <strong>grupy</strong> lub indywidualnie, jest to r&oacute;wnoczesnie sala telewizyjna. Mozna zrobic tutaj spotkanie integracyjne pracownik&oacute;w firmy, male szkolenie (nie zapewniamy wyposazenia sali konferencyjnej). Mozna zrobic spotkanie grupy wycieczkowej, zabawy inegracyjne grupy szkolnej lub pr&oacute;be ch&oacute;ru. <span style="text-decoration: underline;">Wygodnie</span> siedziec przy stolach moze <strong>ok.25-30 os&oacute;b</strong>. Spotkanie w innej formie ( krzesla w rzedach lub w kole )pomiesci cala grupe.</span></p>
<p style="text-align: left; padding-left: 30px;">&nbsp;</p>
<h3 style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;"><strong>Internet WiFi.</strong></span></h3>
<p style="text-align: left; padding-left: 30px;"><br /><span style="font-size: 12pt;">W budynku do dyspozycji gosci bezprzewodowy <strong>bezplatny</strong> internet Wi-Fi.</span></p>
<p style="text-align: left; padding-left: 30px;">&nbsp;</p>
<h3 style="text-align: left; padding-left: 30px;"><strong><span style="font-size: 12pt;">Przechowalnia bagazu.</span></strong></h3>
<p>&nbsp;</p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;">Na terenie obiektu istnieje mozliwosc pozostawienia bagazu <strong>calej grupy</strong> w przypadku gdy zaistnieje taka potrzeba np. p&oacute;zniejsze godziny odjazdu pociagu z dworca, zmiana planu zwiedzania itp.</span></p>
<p style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;">Moze byc r&oacute;wniez sytuacja <strong>gdy grupa przyjedzie wczesniej</strong> niz podane godziny zameldowania i pokoje sa jeszcze zajete przez innych gosci, wtedy grupa moze zostawic bagaze oraz skorzystac ze swietlicy, kuchni oraz toalety.</span></p>
<p style="text-align: left; padding-left: 30px;">&nbsp;</p>
<h2 style="text-align: left;"><strong><span style="font-size: 14pt;">Parking.</span></strong></h2>
<p style="text-align: left;">&nbsp;</p>
<h3 style="text-align: left; padding-left: 30px;"><span style="font-size: 12pt;"><strong>Bezplatny parking dla autokaru.</strong></span></h3>
<p style="text-align: left; padding-left: 30px;"><br /><span style="font-size: 12pt;">Na ogrodzonym terenie naszego Domu znajduja sie <strong>bezplatne miejsca</strong> <strong>parkingowe</strong> dla samochod&oacute;w osobowych, bus&oacute;w i autokaru. Naprzeciw jest og&oacute;lnodostepny parking autokarowy dla pielgrzym&oacute;w sanktuarium gdzie w razie potrzeby <strong>bezplatnie</strong> parkuja nasi goscie ( parking ten jest zamykany na noc ). Lagiewniki to dzielnica spokojna, szanowana i bezpieczna, niekt&oacute;rzy m&oacute;wia " swieta "dzielnica. Zabudowa to tradycyjne domy jednorodzinne z dala od wielkich blokowisk.</span></p>
<p style="text-align: left; padding-left: 30px;">&nbsp;</p>
<h2 style="text-align: left;">&nbsp;</h2>
<h2 style="text-align: left;"><br /><span style="font-size: 14pt;"><strong>Atrakcje w najblizszej okolicy. Pieszo.</strong></span></h2>
<h3 style="text-align: left; padding-left: 30px;"><br /><strong><span style="font-size: 12pt;">Wieza widokowa najwyzsza w Krakowie.</span></strong></h3>
<p style="text-align: left; padding-left: 30px;"><br /><span style="font-size: 12pt;">W odleglosci zaledwie 400 m (pieszo 4 min.) od naszego Domu jest <strong>najwyzsza</strong> Wieza Widokowa (<strong>77m.</strong>) <strong>w Krakowie</strong> przy Bazylice Milosierdzia. Z wiezy widac fantastyczna panorame na caly Krak&oacute;w a przy dobrej pogodzie nawet widac Tatry. Na wiezy sa zrobione szczeg&oacute;lowe opisy widocznych zabytk&oacute;w i atrakcji, co w latwy spos&oacute;b przyblizy uczestnikom wycieczki poznanie Krakowa. W taki spos&oacute;b warto rozpoczac kazda wycieczke, od pieknego widoku z g&oacute;ry i lokalizacje obiekt&oacute;w z programu zwiedzania. Na wieze mozna wjechac winda.</span></p>
<h3 style="text-align: left; padding-left: 30px;"><br /><span style="font-size: 12pt;"><strong>Sanktuarium Bozego Milosierdzia. Obraz Pana Jezusa Milosiernego.</strong> <strong>Relikwie sw. Siostry Faustyny.</strong></span></h3>
<p style="text-align: left; padding-left: 30px;"><br /><span style="font-size: 12pt;">W odleglosci 400m. (<strong>4-5 min.pieszo</strong>)znajduje sie Sanktuarium Bozego Milosierdzia w Krakowie Lagiewnikach ze slynacymi na caly swiat obrazem Pana Jezusa Milosiernego i relikwiami sw. Siostry Faustyny. </span><br /><span style="font-size: 12pt;">W odleglosci 700m. (20- 30 min.pieszo) znajduje sie <strong>Sanktuarium sw.</strong> <strong>Jana Pawla II</strong> z relikwiami i plyta z grobu przywieziona z Watykanu. Od kwietnia 2015 do dyspozycji r&oacute;wniez wieza widokowa .</span></p>
