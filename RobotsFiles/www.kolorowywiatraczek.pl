<h1><strong>Zlobek Przedszkole KOLOROWY WIATRACZEK funkcjonuje od poniedzialku do piatku w godzinach od 7:00 do 17:00.</strong></h1>



<h1 style="text-align:left"><strong>Zapraszamy </strong></h1>



<hr class="wp-block-separator"/>



<p>Zlobek Przedszkole KOLOROWY WIATRACZEK jest placowka swiadczaca profesjonalne uslugi z zakresu opieki nad dziecmi w wieku od 20 tygodnia zycia do 6 lat.</p>



<p>Wykwalifikowana kadra pedagogiczna stwarza warunki umozliwiajace dziecku podejmowanie roznych form aktywnosci oraz angazuje sie w budowaniu pozytywnej relacji z dzieckiem i rodzicem. Niezbednym elementem bowiem jest przede wszystkim znajomosc i rozumienie potrzeb dziecka, wspomaganie procesu adaptacji czyli pomyslnego przechodzenia kolejnych progow zycia malego czlowieka.</p>



<p>Stwarzamy takie warunki w zlobku i w przedszkolu aby srodowisko dziecka z jednej strony bylo stabilne, aby moglo sie do niego przyzwyczaic i zapamietac, a z drugiej strony wystarczajaco roznorodne, aby pobudzac dziecko do rozwoju i poznawania nowych rzeczy i czynnosci.</p>



<p>Filozofia naszej placowki zostala zainspirowana idea edukacji i wychowania czlowieka, zgodna z zalozeniami Marii Montessori.</p>



<p><strong>Nasze motto brzmi :<em> ,,Pomoz mi</em> <em>to</em> <em>zrobic</em> <em>samemu</em><em>",, Daj mi czas"</em>.</strong></p>



<p>Wierzymy w fundamentalne prawo wszystkich dzieci do realizowania swojego potencjalu kreatywnosci. Z zaangazowaniem tworzymy pozytywne otocznie, w ktorym dziecko powierzone naszej opiece bedzie traktowane z szacunkiem i zostanie zachecone do tego, aby stac sie odpowiedzialna jednostka majaca poczucie celu, otwarta i rezolutna.</p>



<p>Uznajemy fakt, ze male dziecko zasluguje na to, aby byc traktowane z takim samym szacunkiem, jaki okazywalibysmy ich rodzicom i jaki oczekiwalibysmy z ich strony w stosunku do nas. Z oddaniem staramy sie byc przykladem pod wzgledem uprzejmosci, rozwagi i odpowiedniego zachowania w kazdej sytuacji.</p>



<p>Wierzymy, ze potencjal ludzki jest nieograniczony a inteligencja nie jest ustalona w momencie narodzin. Wiemy, ze dzieci ucza sie poprzez przyklad, dlatego stosujemy pozytywne wspieranie, zachecamy do robienia postepow oraz zwracamy uwage na indywidualny wysilek jak rowniez na poziom rozwoju. Kiedy dziecko wierzy, ze jest&nbsp; zdolne i samodzielne odnosi sukces i stara sie bardziej. My dokladamy wszelkich staran, aby dac dziecku poczucie odpowiedzialnosci i zachecic do bycia dumnym z robienia czegos samemu.</p>



<p>Dzieci od najmlodszych lat sa oswajane z jezykiem obcym (angielski, hiszpanski, francuski). Organizowane sa zajecia ruchowe, takie jak rytmika i gimnastyka korekcyjna oraz zabawy taneczne. Dla rodzicow w miare potrzeb organizowane sa spotkania ze specjalistami z dziedziny psychologii, logopedii, pediatrii.</p>



<p>Do dyspozycji dzieci sa trzy duze sale dydaktyczno-opiekuncze, a takze duzy ogrod, ktory jest doskonalym miejscem zabaw na swiezym powietrzu. Kazde dziecko ma dostep do swojego lezaczka, kocyka i poduszeczki aby moglo sie polozyc i odpoczac gdy poczuje sie zmeczone.</p>



<p>Nad realizacja czuwa wykwalifikowany dyrektor z wieloletnim stazem pedagogicznym w pracy z dziecmi oraz z doswiadczeniem w prowadzeniu placowki.</p>



<p>Lokal zlobka i przedszkola przygotowywany jest zgodnie z wymogami Panstwowej Inspekcji Sanitarnej oraz Komendanta Panstwowej Strazy Pozarnej.</p>



<p><strong>Wspolnie z rodzicami bedziemy przygotowac dzieci do zycia, ktore czeka na nie w przyszlosci.</strong></p>

