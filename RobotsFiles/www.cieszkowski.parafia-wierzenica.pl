Najblizsze wydarzenia
<HR>
<BR>
27 wrzesnia Kolo PTTK ,,Laziki" organizuje XXI rajd ,,Witamy jesien u Augusta Cieszkowskiego" (do Wierzenicy).
<BR><HR>
We wrzesniu w Luboniu odbedzie sie rajd pieszy po miejscach zwiazanych z Augustem hr. Cieszkowskim i bl. Edmundem Bojanowskim. W kwartalniku ,,Nauka" ukaze sie artykul Medrzec z Wierzenicy W dwusetna rocznice urodzin Augusta Cieszkowskiego autorstwa czlonka korespondenta PAN prof. Zbigniewa Kundzewicza.
<BR><HR>
2 pazdziernika Towarzystwo Milosnikow Ziemi Swarzedzkiej planuje wieczornice ,,Cieszkowski w pamietniku Klary Dembinskiej" (w Zespole Szkol nr 1 w Swarzedzu).
<BR><HR>
16 pazdziernika KUL, Wydzial Zamiejscowy Nauk o Spoleczenstwie w Stalowej Woli organizuje miedzynarodowa konferencje ,,Aktualnosc mysli Augusta Cieszkowskiego. W dwusetna rocznice urodzin".
<BR><HR>
Okolo polowy pazdziernika odbedzie sie walne zgromadzenie Poznanskiego Towarzystwa Przyjaciol Nauk ku czci Augusta Cieszkowskiego - krotki referat, odczytanie fragmentow tekstow, wystawa. Kolejna  ksiazka o Cieszkowskim wydana przez Uniwersytet Przyrodniczy powinna ukazac sie w pazdzierniku.
<BR><HR>
Na jesien Stowarzyszenie Kulturalno-Oswiatowe Forum Lubonskie im. A. Cieszkowskiego wyda publikacje Wybitni Wielkopolanie - bl. Edmund  Bojanowski i August hr. Cieszkowski. Wydanie ksiazki polaczone bedzie z sympozjami oraz spotkaniami i konkursami.
<BR><HR>
14 listopada Archiwum Panstwowe w Poznaniu organizuje spotkanie z cyklu ,,Popoludnie z dokumentem" zatytulowane August Cieszkowski (1814-1894) - w 200 rocznice urodzin. Obejmuje ono wystawe, wyklady, prezentacje archiwaliow i eksponatow muzealnych. Odbedzie sie ono w Archiwum Panstwowym w Poznaniu, ul. 23 Lutego 41/43 oraz w Muzeum Historii Miasta Poznania (Oddzial MNP) - ratusz.
<BR><HR>
21-23 listopada Dni Patrona Uniwersytetu Przyrodniczego w Poznaniu.
<BR><HR>
Na listopad i grudzien planowana jest w hallu Urzedu Miasta Lubon wystawa historyczna pod haslem ,,Architektura  szkoly oraz folwarku Augusta hr. Cieszkowskiego" polaczona z  wieczorem historycznym.
<BR><HR>
PTPN planuje (11 grudnia?) w swojej siedzibie konferencje naukowa poswiecona Augustowi Cieszkowskiemu.</div>
