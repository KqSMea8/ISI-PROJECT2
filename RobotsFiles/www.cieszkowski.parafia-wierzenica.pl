<h1><a href="http://cieszkowski.parafia-wierzenica.pl/?p=1039" rel="bookmark">Kapliczka w Stawiskach</a></h1>

<br>
<p>W niedziele, 21 czerwca, o godz. 15:00 w Stawiskach mialo miejsce poswiecenie kapliczki. Ten znak religijny stanal w miejscu, gdzie niegdys stal dwor rodziny Cieszkowskich &#8211; ludzi wierzacych, niezwykle ofiarnych dla naszej grebkowskiej parafii. Z rodziny tej wywodzi sie hrabia August Cieszkowski &#8211; filozof, ekonomista, dzialacz i mysliciel spoleczny i polityczny. Ubiegly 2014 rok byl poswiecony wlasnie jego osobie. W Stawiskach mieszkali jego przodkowie, m.in. jego dziadek Krzysztof Cieszkowski, ktorego akt urodzenia zachowal sie w parafialnych ksiegach. Dwor zostal spalony przez rosyjskich zolnierzy podczas II wojny swiatowej.<br />
Poswiecenia kapliczki dokonal ks. proboszcz Krzysztof Pawelec.<br />
<iframe width="350" height="197" src="https://www.youtube.com/embed/B18cbETDXh8?rel=0" frameborder="0" allowfullscreen></iframe></p>
