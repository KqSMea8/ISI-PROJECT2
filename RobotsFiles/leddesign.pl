<div style="clear:both; width:100%; height:60px"></div><h1 class="section-title">Pytania i odpowiedzi</h1><div style="clear:both; width:100%; height:60px"></div><h4 class="wpb_toggle">Jak ustawic wlasciwy poziom swiatla w polu pracy?</h4><div class="wpb_toggle_content"><p align="LEFT">Ustawienie wlasciwego poziomu swiatla w polu pracy jest bardzo proste. Ustawiamy fotel w pozycji lezacej, nastepnie kladziemy luksomierz na zaglowek. Pilotem regulujemy natezenie swiatla (+,-) tak, aby luksomierz wskazywal 1000 luksow.</p>
</div>
<h4 class="wpb_toggle">Kto zamontuje lampe w moim gabinecie?</h4><div class="wpb_toggle_content"><p align="LEFT">Montaz jest bardzo prosty i przemyslany. Lampe moze bez problemu zamontowac Twoj serwisant lub elektryk.</p>
</div>
<h4 class="wpb_toggle">Co to jest wspolczynnik Ra (CRI)?</h4><div class="wpb_toggle_content"><p align="LEFT">Jest to wskaznik oddawania barw, charakteryzujacy zrodlo swiatla. Wyrazony jest liczba z przedzialu od 0 (dla swiatla monochromatycznego) do 100 (dla swiatla bialego - widmo ciagle). Okresla, jak dobrze postrzegane sa barwy oswietlonych przedmiotow. Im ten wspolczynnik jest wyzszy, tym barwy sa lepiej oddawane, a oswietlane przedmioty wygladaja naturalniej. (<a href="http://pl.wikipedia.org/wiki/Wsp%C3%B3%C5%82czynnik_oddawania_barw">zrodlo</a>)</p>
</div>
<h4 class="wpb_toggle">Czy moge zamowic lampe o innej temperaturze barwowej niz 5000 K?</h4><div class="wpb_toggle_content"><p>Tak, dostepne temperatury barwowe to: 3000 K, 4000 K, 6000 K. Czas oczekiwania na niestandardowa temperature barwowa wynosi do 21 dni roboczych.</p>
</div>
<h4 class="wpb_toggle">Jak zamowic niestandardowy kolor lampy?</h4><div class="wpb_toggle_content"><p>W przypadku niestandardowego koloru lampy, prosimy w trakcie skladania zamowienia, dopisac kolor z <span style="color: #3366ff;"><strong><a href="http://leddesign.pl/Kolornik_RAL.pdf" target="_blank"><span style="color: #3366ff;">kolornika RAL</span></a></strong></span>, zaznaczajac mat lub polysk.</p>
<p>Czas zamowienia niestandardowego koloru wynosi 14 dni roboczych.</p>
