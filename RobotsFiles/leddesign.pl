<div style="clear:both; width:100%; height:60px"></div><h1 class="section-title">Cechy i zalety lamp LED-Design</h1><h2 class="section-tagline">2 lata pracy nad jednym produktem? Bylo warto!</h2><div style="clear:both; width:100%; height:60px"></div>
<div class="wpb_tabs wpb_content_element" data-interval="0">
<div class="wpb_wrapper wpb_tour_tabs_wrapper ui-tabs vc_clearfix">
<ul class="wpb_tabs_nav ui-tabs-nav vc_clearfix"><li><a href="#tab-1403072000-1-50">Cechy i zalety</a></li><li><a href="#tab-1403072000-2-69">Specyfikacja techniczna</a></li></ul>


<div id="tab-1403072000-1-50" class="wpb_tab ui-tabs-panel wpb_ui-tabs-hide vc_clearfix">

<div class="wpb_text_column wpb_content_element ">
<div class="wpb_wrapper">
<h1>Jakosc</h1>
<p>W lampie LED-Design zastosowano diody najwyzszej jakosci, koreanskiej firmy Liteon, ktore charakteryzuja sie duza odpornoscia i dluga zywotnoscia. Zywotnosc zastosowanych diod siega ponad 150 tysiecy godzin, co przy 12-godzinnym systemie pracy wystarczy na 40 lat. Profesjonalne podloze PCB, ma gruba warstwe miedzi, doskonale przewodzaca prad, zasilajac diody bez spadkow napiecia. Stabilnosc pradu zapewnia optymalna prace, co przedluza znacznie zywotnosc ukladu. W polaczeniu z aluminiowa obudowa, ktora dziala jak duzy radiator zyskujemy dobre odprowadzanie zbednego ciepla. Lampa LED-Design zostala tak zaprojektowana, aby diody jak i uklad elektroniczny nie byly w pelni obciazone, co oznacza dluzsza zywotnosc. Wszystkie elementy lampy montowane sa przy uzyciu wysoce profesjonalnych narzedzi przez doswiadczony zespol specjalistow w Polsce.</p>

</div>
</div>
<div class="wpb_text_column wpb_content_element ">
<div class="wpb_wrapper">
<h1 style="text-align: left;">Silny i lagodny strumien swiatla</h1>
<p style="text-align: left;">W lampie LED-Design panel swietlny swieci cala swoja powierzchnia, przez co strumien swietlny jest rownomiernie rozlozony. Zastosowalismy 648 mocno swiecace diody, ktore zapewniaja bardzo duza ilosc swiatla. Jednoczesnie dzieki specjalnej pryzmie o wysokiej przepuszczalnosci siegajacej 92%, swiatlo pozostaje mocne i idealnie rozproszone, dajac uczucie duzego komfortu i naturalnosci.</p>

</div>
</div>
<div class="wpb_text_column wpb_content_element ">
<div class="wpb_wrapper">
<h1>Odpowiednie natezenie swiatla</h1>
<p>Dolaczony pilot radiowy pozwala plynnie regulowac moc lampy w zakresie 1-200 watow, co umozliwia montaz lampy na dowolnej wysokosci. Lampe LED-Design mozna zamontowac nawet na wysokosci 350 cm, zachowujac przy tym natezenie swiatla w polu pracy na poziomie 1000 luksow. Jednoczesnie odporny na zaklocenia pilot pracuje na czestotliwosci 433 Mhz, zapewniajac sterowanie natezeniem swiatla z kazdego miejsca w gabinecie.</p>

</div>
</div>
<div class="wpb_text_column wpb_content_element ">
<div class="wpb_wrapper">
<h1 style="text-align: left;">Swiatlo bez promieniowania UV</h1>
<p style="text-align: left;">W naszej lampie wyeliminowalismy pasmo swiatla UV, co wplywa na wiekszy komfort pracy poprzez redukcje przyspieszonego procesu polimeryzacji wypelnien i materialow dentystycznych. Wolne od efektu stroboskopowego swiatlo zabezpiecza przed niebezpiecznym, pozornym bezruchem wiertel i innych narzedzi obrotowych. Dodatkowo stabilne swiatlo i brak pasma swiatla UV wplywa pozytywnie na zdrowie i dobre samopoczucie.</p>

</div>
</div>
<div class="wpb_text_column wpb_content_element ">
<div class="wpb_wrapper">
<h1 style="text-align: left;">Wlasciwe barwy w naturalnym swietle</h1>
<p style="text-align: left;">W lampie LED-Design uzylismy diod o temperaturze barwowej 5000 kelwinow, zblizonej do naturalnego swiatla slonecznego w bezchmurny dzien. Wysoki wspolczynnik oddawania barw Ra (CRI), wplywa na prawidlowe ich postrzeganie. Oba te parametry pomagaja wlasciwie dopasowac kolor wypelnienia lub pracy protetycznej.</p>

</div>
</div>
<div class="wpb_text_column wpb_content_element ">
<div class="wpb_wrapper">
<h1 style="text-align: left;">Energooszczednosc i ekologia</h1>
<p style="text-align: left;">Dzieki zastosowaniu nowoczesnych diod i modulu zasilajacego, oszczedzamy nawet do 60% zuzycia pradu w porownaniu z oswietleniem wykorzystujacym swiatlo jarzeniowe. koreanskie diody Liteon daja swiatlo na poziomie 120 lumenow z 1 wata! W odroznieniu od swietlowek, panele LED nie zawieraja rteci, olowiu, kadmu ani innych substancji bardzo szkodliwych i niebezpiecznych dla srodowiska i naszego zdrowia. Do produkcji naszych lamp uzyte zostaly komponenty (aluminium, miedz, zloto), ktore nadaja sie do ponownego przetworzenia. Panele LED produkowane sa w technologii bezolowiowej.</p>

</div>
</div>
<div class="wpb_text_column wpb_content_element ">
<div class="wpb_wrapper">
<h1 style="text-align: left;">Estetyczny wyglad</h1>
<p style="text-align: left;">Postawilismy na prostote. Nasza lampa jest bardzo cienka, jej grubosc to zaledwie 22 mm. Wykonana jest z lekkiego stopu aluminium, dzieki czemu idealnie wkomponuje sie w kazda istniejaca lub nowo powstala wizje architektoniczna. Istnieje mozliwosc wyboru dowolnego <span style="color: #3366ff;"><strong><a href="http://leddesign.pl/Kolornik_RAL.pdf" target="_blank"><span style="color: #3366ff;">koloru</span></a></strong></span> obudowy, co podniesie walory estetyczne Twojego gabinetu. W wersji podstawowej, lampa ma srebrny, naturalny kolor dla aluminium.</p>

</div>
</div>
<div class="wpb_text_column wpb_content_element ">
<div class="wpb_wrapper">
<h1 style="text-align: left;">Bezawaryjnosc i trwalosc</h1>
<p>Na dluga zywotnosc naszej lampy wplywa wiele czynnikow. Przede wszystkim profesjonalny zasilacz z duzym zapasem mocy, ktory nie przeciaza diod i gwarantuje duza stabilnosc pradu. Lampa dzieki bardzo sztywnej, aluminiowej konstrukcji jest odporna na drgania. Anodowana powierzchnia chroni przed zarysowaniami.</p>

</div>
</div>
<div class="wpb_text_column wpb_content_element ">
<div class="wpb_wrapper">
<h1 style="text-align: left;">Szczelna, odporna na wilgoc i kurz obudowa</h1>
<p style="text-align: left;">Obecnosc kurzu i zapylenia w praktyce dentystycznej wynika z uzywania piaskarek. Wplywa to negatywnie na jakosc swiatla i zywotnosc diod. W lampach LED-Design zastosowalismy specjalne uszczelki silikonowe i piankowe, ktore skutecznie chronia lampe przed kurzem, zapyleniem i wilgocia.</p>

</div>
</div>
<div class="wpb_text_column wpb_content_element ">
<div class="wpb_wrapper">
<h1 style="text-align: left;">Lekka i mocna</h1>
<p style="text-align: left;">Lampa LED-Design, ktora wazy tylko 5 kg, pobiera maksymalnie 200 watow, dajac 19 000 lumenow swiatla, co odpowiada 13 lampom halogenowym o mocy 100 watow.</p>

</div>
</div>
<div class="wpb_text_column wpb_content_element ">
<div class="wpb_wrapper">
<h1 style="text-align: left;">Modul zasilajacy nowej generacji</h1>
<p style="text-align: left;">Dzieki zastosowaniu zaawansowanego ukladu elektronicznego, napiecie wyjsciowe jest bardzo stabilne, przez co lampa bedzie swiecic ciaglym swiatlem nawet przy duzych spadkach napiecia. Modul zasilajacy charakteryzuje sie duza tolerancja napiecia wejsciowego od 90 do 305 woltow. Lampa moze byc stosowana nawet na obszarach oddalonych od elektrowni, gdzie napiecie w sieci jest bardzo niestabilne i niskie, szczegolnie dotyczy to daleko polozonych obszarow wiejskich. Lampe mozna rowniez uzywac we wszystkich krajach na swiecie, gdzie napiecie w sieci jest inne niz w Polsce. Modul elektroniczny zostal tak zaprojektowany, aby diody i uklad elektroniczny nie byly w pelni obciazone, co gwarantuje znacznie dluzsza zywotnosc i duzo mniejszy poziom nagrzewania sie. Temperatura modulu zasilajacego nie przekracza 40degC nawet przy pelnym obciazeniu!</p>

</div>
</div>
<div class="wpb_text_column wpb_content_element ">
<div class="wpb_wrapper">
<h1 style="text-align: left;">Praca bez szumow i zaklocen</h1>
<p style="text-align: left;">W naszych lampach zastosowano uklady zasilajaco-sterujace, ktore pracuja bezglosnie w kazdych warunkach. Zasilacz nie posiada wiatraka, a oprocz tego calosc jest zalana specjalna masa zywiczna, ktora doskonale odprowadza cieplo. Praca modulu zasilajaco-sterujacego LED-Design jest praktycznie nieslyszalna! Poprzez wyeliminowanie zaklocen radiowych, nasza lampa jest bezpieczna dla wszystkich urzadzen elektronicznych, ktore znajduja sie w gabinecie.</p>

</div>
</div>
<div class="wpb_text_column wpb_content_element ">
<div class="wpb_wrapper">
<h1 style="text-align: left;">Prosty, uniwersalny montaz</h1>
<p style="text-align: left;">Lampy LED-Design posiadaja uniwersalny sposob montazu. Mozna je montowac na suficie pelnym przy uzyciu zawiesi linkowych, na sufitach kartonowo-gipsowych oraz bezposrednio do sufitow typu Armstrong.</p>

</div>
</div>
<div class="wpb_text_column wpb_content_element ">
<div class="wpb_wrapper">
<h1 style="text-align: left;">Dluga gwarancja</h1>
<p style="text-align: left;">Jakosc zastosowanych komponentow i rozwiazan zapewnia wysoka trwalosc i bezawaryjnosc. Gwarancja na lampy LED-Design wynosi 36 miesiecy z mozliwoscia przedluzenia o kolejne 24 miesiace, czyli lacznie na okres 5 lat. Zdajemy sobie sprawe, jak wazne jest swiatlo w gabinecie. W przypadku wystapienia awarii ukladu zasilania, nowy modul wysylany jest do lekarza firma kurierska w ciagu 24 godzin na nasz koszt.</p>

