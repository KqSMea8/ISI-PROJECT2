<h1 class="naglowek-strony">O firmie</h1>

<div class="row">
<div class="col-md-12">
</div>
</div>

<div class="row">
<div class="col-md-6">
<h3><span class="orange">LUX</span> kiedys</h3>
<p>Firma LUX powstala w 1997 roku w Sokolowie Podlaskim. Istniejemy na rynku od poczatku ekspansji telefonii komorkowej w Polsce - zajmujemy sie obsluga klientow od czasow technologii NMT, nastepnie pod marka IDEA, a obecnie jako partner Orange.</p>
<p>Na poczatku istnienia siec sprzedazy skladala sie z pieciu salonow, na terenie obecnego wojewodztwa mazowieckiego. W nastepnych latach, rozwoj firmy zaowocowal powstaniem kilkudziesieciu salonow sprzedazy oraz salonow Franchisingowych na terenie Polski polnocno-wschodniej.</p>    </div>

<div class="col-md-6">
<h3><span class="orange">LUX</span> dzisiaj</h3>
<p>Obecnie siec sprzedazy obejmuje tereny calej Polski i sklada sie z 50 salonow sprzedazy. Firma utrzymuje na biezaco wysoki poziom obslugi klientow przez szkolenie swoich pracownikow z zakresu technik sprzedazy i pracy z klientem we wspolpracy z Orange Polska.</p>    </div>
</div>

<div class="row">
<div class="col-md-6">
<h3>Nasza <span class="orange">misja</span></h3>
<p>Naszym dazeniem jest zapewnic klientom Orange najwyzszy poziom obslugi, poprzez kompetentnych i zaangazowanych pracownikow firmy, ktorzy w pelni utozsamiaja sie z firma LUX oraz marka i wartosciami Orange. Rozwoj roznych form wspolpracy z Orange Polska, to podstawowy nurt naszej dzialalnosci, ktory ma zapewnic obustronne korzysci i zadowolenie.</p>    </div>

<div class="col-md-6">
<h3>Nasz <span class="orange">cel</span></h3>
<p>Chcemy obslugiwac klientow w calej Polsce, rozwijajac siec sprzedazy Salonow Orange. Nasza aktywnosc biznesowa ukierunkowana jest takze na kanal sprzedazy <i>Konsultantow Klienta Biznesowego</i>.</p>    </div>
</div>

</div>


