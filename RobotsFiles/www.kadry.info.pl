<h1>Zarzadzanie zasobami ludzkimi</h1>
<p>Wraz z rozpoczeciem sie transformacji systemowej nastapilo otwarcie polskiej gospodarki na miedzynarodowa konkurencje. Polskie firmy w szybkim tempie nadrobily dystans dzielacy je od podmiotow dzialajacych od wielu lat w gospodarce rynkowej. Jednym z obszarow, ktory podlega dynamicznemu rozwojowi jest zarzadzanie zasobami ludzkimi.</p>
<p>Zarzadzanie zasobami ludzkimi (inaczej zarzadzanie personelem, funkcja personalna lub Human Resource Management, w skrocie HCR) oznacza ogol dzialan zwiazanych z dysponowaniem posiadanymi zasobami ludzkimi przez dana organizacje, podejmowanych dla osiagniecia jej celow, ktorym nadaje sie strategiczne znaczenie. Inaczej jest to sposob kierowania najcenniejszym z kapitalow kazdej organizacji - ludzmi, ktorzy osobistym i zbiorowym wysilkiem urzeczywistniaja zaplanowane przez organizacje cele, a tym samym zabezpiecza jej przewage nad konkurencja.</p>

<p>Rozpatrujac zagadnienie zarzadzania zasobami ludzkimi, nalezaloby przyjac, ze ludzie nie sa zasobem, lecz nim dysponuja (ogolem cech i wlasciwosci w nich ucielesnionych, ktore umozliwiaja pelnienie roznych rol w organizacji.</p>
<p>Do najwazniejszych czynnikow zarzadzania zasobami ludzkimi (HCR) </p>
<ul>
<li>wiedza (wiedza teoretyczna)</li>
<li>zdolnosci</li>
<li>umiejetnosci (np. umiejetnosc obslugi programu komputerowego)</li>
<li>doswiadczenie</li>
<li>postawy (np. zaangazowanie)</li>
<li>wartosci</li>
<li>motywacja</li>
</ul>
<p>Zarzadzanie zasobami ludzkimi ma wymiar funkcjonalny, instytucjonalny i instrumentalny. W wymiarze funkcjonalnym obejmuje ono organizowanie poszczegolnych zadan i czynnosci w procesy tworzenia wartosci; wymiar instytucjonalny obejmuje okreslenie rol i kompetencji podmiotow-tej czesci zasobu ludzkiego, ktora jest uprawniona do podejmowania decyzji personalnych. W wymiarze instrumentalnym dotyczy doboru wlasciwych technik i metod rozwiazywania problemow personalnych.</p>

