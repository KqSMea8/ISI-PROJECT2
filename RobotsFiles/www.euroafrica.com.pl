function PLCreateCookie(name, value, days) {
var date = new Date();
date.setTime(date.getTime() + (days*24*60*60*1000));
var expires = "; expires=" + date.toGMTString();
document.cookie = name+"="+value+expires+"; path=/";
}

function PLReadCookie(name) {
var nameEQ = name + "=";
var ca = document.cookie.split(';');
for(var i=0; i < ca.length; i++) {
var c = ca[i];
while (c.charAt(0) == ' ') c = c.substring(1, c.length);
if (c.indexOf(nameEQ) == 0) return c.substring(nameEQ.length, c.length);
}
return null;
}

window.onload = PLCheckCookies;

function PLCheckCookies() {
if(PLReadCookie('cookies_accepted') != 'T') {
var message_container = document.createElement('div');
message_container.id = 'cookies-message-container';
var html_code = '<div id="cookies-message" style="padding: 10px 0px; font-size: 12px; line-height: 22px; border-bottom: 1px solid rgb(211, 208, 208); text-align: center; position: fixed; top: 0px; background-color: #f3f3f3; width: 100%; z-index: 999;">Nasza strona uzywa plikow cookie. Wiecej informacji o zastosowaniu i sposobie modyfikacji plikow cookie znajdziesz w naszej <a style="font-size:inherit; color: #F00" href="http://euroafrica.com.pl/index.php/polityka_cookie/">Polityce Cookie</a>.&nbsp;<a href="javascript:PLCloseCookiesWindow();" id="accept-cookies-checkbox" name="accept-cookies" style="display: inline-block;text-decoration:underline; cursor: pointer; color:inherit; font-size:inherit">Nie pokazuj wiecej tego komunikatu</a></div>';
message_container.innerHTML = html_code;
