<div id="body">
<p>Centrum Leczenia i Rehabilitacji Beskid polozony jest na slonecznym zboczu G&oacute;ry Krzyzowej w scislym centrum Krynicy Zdr&oacute;j jednego z najwiekszych i najbardziej znanych uzdrowisk polskich. W poblizu CLiR Beskid znajduja sie: slynny deptak, pijalnie w&oacute;d mineralnych, basen, lodowisko, wyciagi narciarskie korty tenisowe i tor saneczkowy.</p>
<p>CLiR Beskid oferuje Panstwu doskonale warunki do wypoczynku indywidualnego i grupowego oraz do leczenia i rehabilitacji. Czyste g&oacute;rskie powietrze i niepowtarzalne wody lecznicze uzdrowiska wspomagaja prowadzone przez nas zabiegi lecznicze.</p>
<p>Baza noclegowa CLiR Beskid sklada sie z 89 pokoi. Wszystkie pokoje posiadaja pelen wezel sanitarny, a czesc pokoi takze balkony.</p>
<p>Z uwagi na swoje polozenie CLiR Beskid jest idealnym obiektem noclegowym podczas pobyt&oacute;w dla os&oacute;b jezdzacych na nartach, a takze dla turyst&oacute;w uprawiajacych piesze i rowerowe wycieczki urokliwymi trasami w Krynicy-Zdr&oacute;j i okolicach.</p>
<p style="text-align: center;"><strong>Serdecznie Zapraszamy!</strong></p>			</div>
