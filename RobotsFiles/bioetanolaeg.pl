<h1 class="header">Strona glowna</h1>
<p>Szanowni Panstwo,</p>
<p> </p>
<p>Informujemy, ze z dniem 8 lutego 2018 nazwa spolki wraca do formy Bioetanol AEG Sp. z o.o., zapis o upadlosci zostal wykreslony w zwiazku z prawomocnym wyrokiem Sadu w tej kwestii. W <a href="../../../../../shared/data/uploads/tinymce/Informacja o zmianie nazwy na Bioetanol AEG sp. z o.o._14022018.pdf">zalaczeniu</a> pismo do wszystkich partnerow handlowych z prosba o zmiane nazwy Spolki w Panstwa systemach.</p>
<p> </p>
<p>Jeszcze raz chcielibysmy podziekowac za wsparcie w tym trudnym dla Spolki czasie. Zywimy gleboka nadzieje na dalsza, owocna wspolprace i zyczymy samych sukcesow.</p>
<p> </p>
<p>Dziekujemy,</p>
<p>Pracownicy i Zarzad</p>
<p>Bioetanol AEG</p>
<p> </p>
<br />
<p style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif;">Spolka Bioetanol AEG Sp. z o. o<strong>.</strong> korzysta z wieloletniego doswiadczenia Zarzadu Firmy, technologow oraz specjalistow branzy spirytusowej a historia glownego zakladu produkcyjnego, gdzie miesci sie takze siedziba spolki, siega 1947 roku.</span></p>
<p style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif;">Obecnie firma produkuje alkohol etylowy surowy, rektyfikowany i odwodniony oraz wyroby chemiczne powstale na bazie alkoholu etylowego.</span></p>
<p style="text-align: justify;"> </p>
<p style="text-align: right;"><strong><img  style="float: left; margin-left: 4px; margin-right: 4px;"  src="http://www.bioetanolaeg.pl/shared/data/uploads/tinymce/wytwornia alko.jpg" alt="" width="171" height="120" /><span style="font-family: arial,helvetica,sans-serif;">Wytwornia Alkoholii</span></strong><span style="font-family: arial,helvetica,sans-serif;"> mieszczaca sie w Chelmzy przy ul. Bydgoskiej 4, gdzie miesci sie takze siedziba Zarzadu Firmy. </span><span style="font-family: arial,helvetica,sans-serif;">Powstala w 1947 roku przy sasiadujacej cukrowni i byla jej czescia do roku 2003. Od tego czasu nieprzerwanie dziala jako oddzielny zaklad produkcyjny.</span></p>
