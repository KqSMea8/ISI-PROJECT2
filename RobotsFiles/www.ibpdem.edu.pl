<div id="main-text"><h2>Instytut Badan nad Podstawami Demokracji</h2>
Pomyslany zostal jako niezalezna instytucja badawcza. Niezalezna to znaczy bez powiazan i zobowiazan politycznych.
Badawcza to znaczy podejmujaca badania na poziomie akademickim w dziedzinie filozoficznych, historycznych,
spolecznych, politycznych i kulturowych warunkow powstania i funkcjonowania systemow demokratycznych we wspolczesnych
spoleczenstwach. Fundatorzy uznali - i nadal tak uwazaja - ze jest to problematyka kluczowa dla Polski (i, ogolniej, dla krajow postkomunistycznych),
jej rozwoju i szans. Instytucje demokratyczne - takze, a moze przede wszystkim na poziomie lokalnym - kreuja przestrzen wolnosci oraz ucza bycia wolnymi i odpowiedzialnymi.
Tylko demokratyczna przestrzen tworzy warunki dla dobrowolnego respektowania zasad, szanowania wartosci, dla inicjatyw indywidualnych i grupowych oraz swobodnego wspoldzialania.
Demokracja jest podstawa, ale i sluzy dynamice rozwoju, wspiera tradycje, ale i popycha ku nowoczesnosci.
</div>







