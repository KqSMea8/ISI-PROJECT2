<header class="entry-header"><h1 class="entry-title">MONITORING KANDYDATOW</h1></header><!-- .entry-header -->
<div class="entry-content">
<div class="pf-content"><div class="printfriendly pf-alignright"><a href="#" rel="nofollow" onclick="window.print(); return false;" class="noslimstat" title="Printer Friendly, PDF & Email"><img src="https://cdn.printfriendly.com/icons/printfriendly-icon-sm.png" alt="Print Friendly, PDF & Email" style="display: inline" />Print</a></div><p style="text-align: justify;">W roku 2016 Fundacja im. Stefana Batorego we wspolpracy z partnerami prowadzila obserwacje wyborow osob na najwyzsze urzedy panstwowe powolywanych przez Sejm, Senat oraz Prezydenta RP. Wraz z Polskim Towarzystwem Ekonomicznym przygladalismy sie wyborom do Rady Polityki Pienieznej; we wspolpracy z Osrodkiem Fundacja KARTA sledzilismy wybory czlonkow Kolegium Instytutu Pamieci  Narodowej i konkurs na stanowisko Prezesa Instytutu Pamieci Narodowej, natomiast z Ogolnopolska Federacja Organizacji Pozarzadowych obserwowalismy wybory do Krajowej Rady Radiofonii i Telewizji oraz nowo utworzonego gremium, jakim jest Rada Mediow Narodowych.</p>
<p>Zachecamy do lektury raportu koncowego z monitoringu &#8211; <a href="http://monitoringkandydatow.org.pl/wp-content/uploads/2017/10/Pod-lupa-obywateli.pdf" target="_blank" rel="noopener"><strong>Pod lupa obywateli &#8211; standardy, regulacje i praktyka wyborow na wysokie stanowiska publiczne</strong></a> [PDF]. Jest to opracowanie przygotowane przez Lukasza Bojarskiego i Grzegorza Wiaderka na podstawie monitoringu. Zawiera nie tylko ocene praktyki obsadzania najwyzszych stanowisk w wyzej wymienionych instytucjach, ale tez konkretne rekomendacje i postulaty jak owe procedury powinny wygladac, zeby spelnialy najwyzsze standardy przejrzystosci i wymogi merytoryczne.</p>
