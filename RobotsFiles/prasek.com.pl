jQuery(document).ready(function($){

/**
* Otwieranie modala i uzupelnienie go danymi w sekcji ze sklepem
*/
$('.shop').on('click', '.zoomButton', function(){

let $img = $(this).parents('div.photo').find('img').clone().css({ 'max-width': '100%' });

let data = {
'img': $img,
'imgTitle': $(this).parents('div.photo').find('.title').text(),
'src': $img.attr('src'),
'indexElement': $(this).closest('div.photo').index(),
'showArrows': true
};

if($('.shop').find('#modal').length == 0){
createModal(data).appendTo('.shop'); //tworzenie modala
} else {
setModalData(data);
}

hideLR(data.indexElement);

$('#modal').show();
});

/**
* Otwieranie modala i uzupelnienie go danymi w sekcji z referencjami
*/
$('.references').on('click', '.zoomButton', function(){

let $img = $(this).parents('div.photo').find('img.referes').clone().css({ 'max-width': '100%' });

let data = {
'img': $img,
'imgTitle': $(this).parents('div.photo').find('.title').text(),
'src': $img.attr('src'),
'indexElement': $(this).closest('div.photo').index()
};

if($('.references').find('#modal').length == 0){
createModal(data).appendTo('.references'); //tworzenie modala
} else {
setModalData(data);
}

$('#modal').show();
});

/**
* Zamykanie modala
*/
$('.allWrapper').on('click', '.hide-modal', function(){
$('#modal').hide();
});

/**
* Zmiana obrazkow w modalu
*/
$('.shop').on('click', '.carousel .carousel-control', function(){
var side = $(this).attr('data-side'),
indexElement = $('#modal').attr('data-index'),
photos = $('.shop .images-content').children();

var element = null;
if(side == 'prev'){
var position = parseInt(indexElement) - 1;
if(position >= 0){
element = photos.eq(position);
}
}

if(side == 'next'){
var position = parseInt(indexElement) + 1;
if(position < photos.length){
element = photos.eq(position);
}
}

var data = {
'src': element.find('img').attr('src'),
'indexElement': position,
'imgTitle': element.find('.title').text()
};

hideLR(data.indexElement);

setModalData(data);
});

/**
* Funkcja tworzaca modal
*/
function createModal(data){

let closeModal = $('<button />', {
'class': 'close hide-modal',
'type': 'button',
'data-dismiss': 'modal',
'text': 'x'
}),
title = $('<h3/>', {
'class': 'modal-title',
'text': data.imgTitle
}),
modalHead = $('<div/>', {
'id': 'modal-header',
'html': [closeModal, title]
});

let bodyCarousel = $('<div>',{
'class': 'carousel-inner',
'html': [data.img]
}),
left = $('<a>', {
'class': 'carousel-control left',
'href': '#modalCarousel',
'data-side': 'prev',
'html': '<i class="fa fa-arrow-left" aria-hidden="true"></i>'
}),
right = $('<a>', {
'class': 'carousel-control right',
'href': '#modalCarousel',
'data-side': 'next',
'html': '<i class="fa fa-arrow-right" aria-hidden="true"></i>'
});


let carouselContent = [bodyCarousel];

if(data.showArrows != undefined &&  data.showArrows == true){
carouselContent.push(left, right);
}

let carousel = $('<div>', {
'class': 'carousel',
'id': 'modalCarousel',
'html': carouselContent
}),
modalBody = $('<div/>', {
'id': 'modal-body',
'html': [carousel]
});

let modalContent = $('<div>', {
'class': 'modal-content',
'html': [modalHead, modalBody]
}),
modalDialog = $('<div>',{
'class': 'modal-dialog',
'html': [modalContent]
});

let modal = $('<div>', {
'id': 'modal',
'class': 'modal',
'role': 'dialog',
'data-index': data.indexElement,
'html': [modalDialog]
});

return modal;
}

/**
* Funkcja odpowiedzialna za ustawianie danych w modalu
* @param data
*/
function setModalData(data){
$('#modal').find('.modal-title').text(data.imgTitle);
$('#modal').find('.carousel .carousel-inner img').attr('src', data.src);
$('#modal').attr('data-index', data.indexElement);
}

/**
* Funkcja odpowiedzialna za ukrywanie i pokazywanie strzalek
* @param indexElement
*/
function hideLR(indexElement) {
var photosLength = $('.shop').find('div.photo').length;

if(indexElement == 0){
$('.carousel').find('.left').hide(); //ukrywanie strzalki w lewo gdy wyswietlany pierwszy element
} else {
$('.carousel').find('.left').show();
}

if((indexElement + 1) == photosLength){
$('.carousel').find('.right').hide();  //ukrywanie strzalki w prawo gdy wyswietlany ostatni element
} else {
$('.carousel').find('.right').show();
}
}

$('.footer').on('change', '.subject', function(){
$('.footer').find('.reply').val($(this).find(':selected').attr('send'));
});

});
