<p>Wszyscy wiemy, ze najtrudniejsza rzecza do pokonania jest nasze myslenie: To co myslimy o sobie i to co myslimy o innych &#8211; czy staramy sie poznac drugiego czlowieka czy oceniamy go przez pryzmat stereotypow i uprzedzen. Kazdy z nas ma pewne przekonania na temat osob z niepelnosprawnoscia. Te przekonania znaczaco wplywaja na to co, czujemy widzac osobe niepelnosprawna. Taka emocja, o ktorej czesto mowia mlodzi ludzie jest lek. Bardzo czesto jest on zwiazany z tym, ze nie wiemy jak sie zachowac w stosunku do osoby niepelnosprawnej, jak jej pomoc, zeby jej nie skrzywdzic, co powiedziec, zeby jej nie urazic. Celem szkolenia jest proba przezwyciezenia lub przynajmniej oslabienia sily tego leku. Pokazujac mlodym ludziom w jaki sposob mozna pomoc takiej osobie, jak sie zachowac &#8211; przelamujemy pierwsze bariery w kontakcie z osoba niepelnosprawna. Pokazujemy, ze te bariery da sie przekroczyc i ze czesto w naszych glowach je wyolbrzymiamy.</p>
