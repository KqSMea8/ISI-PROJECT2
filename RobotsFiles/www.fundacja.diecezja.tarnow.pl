<h1 class="entry-title"><a href="http://www.fundacja.diecezja.tarnow.pl/?p=143" rel="bookmark">Spotkanie stypendystow &#8211; galeria zdjec</a></h1>
<div class="entry-meta">
<span class="posted-on">Posted on <a href="http://www.fundacja.diecezja.tarnow.pl/?p=143" rel="bookmark"><time class="entry-date published" datetime="2016-06-06T13:16:53+00:00">6 czerwca 2016</time><time class="updated" datetime="2016-06-30T12:48:20+00:00">30 czerwca 2016</time></a></span><span class="byline"> by <span class="author vcard"><a class="url fn n" href="http://www.fundacja.diecezja.tarnow.pl/?author=1">ks. Tomasz</a></span></span>		</div><!-- .entry-meta -->

</header><!-- .entry-header -->

<div class="entry-content">
<p>4 czerwca 2016 roku w kosciele Milosierdzia Bozego w Tarnowie odbylo sie spotkanie stypendystow z dwoch fundacji: Fundacji Imienia Arcybiskupa Jerzego Ablewicza oraz Fundacji Dzielo Nowego Tysiaclecia. Ponizej kilka zdjec z tego wydarzenia &#8211; zapraszam do ogladniecia. Ks. Tomasz</p>
<p><a title="foto" href="http://fundacja.diecezja.tarnow.pl/menu/galeria/styp_czer_2016" target="_blank"><img class="size-medium wp-image-144 aligncenter" src="http://www.fundacja.diecezja.tarnow.pl/wp-content/uploads/2016/06/kosciol-300x198.jpg" alt="kosciol" width="300" height="198" /></a></p>
<p style="text-align: center;"><a href="http://fundacja.diecezja.tarnow.pl/menu/galeria/styp_czer_2016" target="_blank"><span style="color: #ff0000;">kliknij w zdjecie</span></a></p>
<p>Relacja ze spotkania &#8211; stypendystka Joanna Adamczyk:</p>
<blockquote>
<p style="text-align: justify;">Dnia 4 kwietnia 2016 w Kosciele Milosierdzia Bozego w Tarnowie odbylo sie spotkanie stypendystow Fundacji Imienia Arcybiskupa Jerzego Ablewicza i Fundacji Dzielo Nowego Tysiaclecia. Rozpoczelo sie ono uroczysta Msza swieta, w trakcie ktorej moglismy sie spotkac z Panem Jezusem, zjednoczyc z nim w komunii swietej i uslyszec Boze Slowo. Jezus mowil do naszych serc i otwieral nas na swiatlo Ducha Swietego. Eucharystia dala nam ogromna radosc, ktora emanowalismy podczas agapy, kiedy to moglismy porozmawiac z przybylymi ksiezmi, kolegami, a takze nawiazac nowe znajomosci.</p>
<p style="text-align: justify;">Kolejnym punktem programu bylo omowienie wszystkich spraw stypendialnych a takze pozegnanie uczniow konczacych nauke pod skrzydlami naszej Fundacji. Spotkanie zakonczylo sie pouczajacym wykladem ksiedza doktora Marka Podgorskiego na temat chrztu Polski. Przyblizone zostaly nam dzieje Polski, historia jej powstania a takze wazne postacie zwiazane z chrystianizacja naszej Ojczyzny. Ksiadz Marek mowil miedzy innymi o pochodzeniu ich imion, o ich osobistej wierze i powodach, dla ktorych zdecydowali sie wspierac i wprowadzac chrzescijanstwo na naszych ziemiach. Dowiedzielismy sie rowniez jak zyli ludzie w tamtym okresie, jak wygladal chrzest i jak to wydarzenie wplynelo na wladcow Polski i panstw sasiednich. Poruszyl rowniez temat powolnego i stopniowego wypleniania poganskich zwyczajow niewyksztalconej i prostej ludnosci, wyjasnil w jaki sposob wprowadzano w zycie chrzescijanskie tradycje i jak tworzono pierwsze w naszym kraju zgromadzenia chrzescijanskie - w tym biskupstwo misyjne. Przekazal nam wiele ciekawych informacji.</p>
<p style="text-align: justify;">Spotkanie stypendystow zaowocowalo nowymi znajomosciami a takze odnowieniem dawnych, moze nawet zapomnianych znajomosci. Duch Swiety sprawil, ze wyszlismy z niego uradowani, ulepszeni i umocnieni.</p>
</blockquote>
