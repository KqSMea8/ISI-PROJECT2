<p>Czy mozna przedstawic tworczosc Wiliama Szekspira wsrod niemilknacych smiechow i burz oklaskow? &nbsp;&nbsp;&nbsp; <strong>Tak! </strong> <br>
Udalo sie to <strong>Kabaretowi Nowaki i Krzysztofowi Jaroszynskiemu</strong>, czyli tworcom widowiska
<strong>"Krolestwo za usmiech</strong>" <br><br>
Bez sekundy szkolnej nudy widz dowiaduje sie wiele o sztukach:  Sen nocy letniej, Makbet, Wesole Kumoszki z Windsoru, Otello, Burza, Hamlet, Krol Lear.
Jakim czlowiekiem byl Szekspir i jak powstawaly jego dziela poznacie panstwo w skrzacych sie humorem skeczach i monologach. <br><br><br>

Ten wyjatkowy projekt, czerpiacy z tego, co najlepsze w kabarecie i teatrze tworzy nowa, zupelnie nowatorska jakosc.
Kabaret Nowaki i Krzysztof Jaroszynski chca w ten sposob uczcic rocznice smierci i narodzin Williama Szekspira.
Premiera spektaklu odbedzie sie w przeddzien 450 rocznicy urodzin Williama Szekspira. <br><br>
<strong>Od 22 kwietnia 2014 roku spektakl bedzie wystawiany przez 2 lata.</strong><br></p><p>							Kim byla zona Szekspira? Kto byl jego szefem? Jak wspominaja go rodzice? Co mysli sam o sobie? <br>Co by sie stalo gdyby pisal wspolczesnie? <br><br>
