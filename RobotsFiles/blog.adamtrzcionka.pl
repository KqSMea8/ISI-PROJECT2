<h2 class="entry-title"><a href="https://blog.adamtrzcionka.pl/?p=6438" title="Permalink to WARSZTATY Z PLENERU / 2019" rel="bookmark">WARSZTATY Z PLENERU / 2019</a></h2>

<div class="entry-meta entry-meta-top">

<p>	sroda, Kwiecien 17, 2019										<span class="posted-in">Posted in  <a href="https://blog.adamtrzcionka.pl/?cat=9" rel="category">BIEZACE INFO</a></span></p>

</div><!-- .entry-meta-top -->

<div class="entry-content self-clear">

<p>Czesc!</p>
<p>Chlopak, zainteresowany warsztatami z pleneru, napisal do mnie kiedys tak:<br />
,,a jaka bedzie pozowac para? zawodowcy czy brzydcy amatorzy? jesli ci drudzy, to jade!"</p>
<p>Ta rezolutna rozterka przywiodla mnie do powaznej refleksji dotyczacej warsztatow; tego, jak chcialbym, zeby wygladaly, oraz o tym, jak sam mysle o wlasnych sesjach.</p>
<p>I jednak chcialbym, zeby to nie byly zajecia o parach, szczegolnie o trudnych parach. Tez, ale nie glownie.<br />
Uwazam, ze przerzucenie ciezaru plenerowych niepowodzen badz sukcesow glownie na ,,fotogenicznosc" par i miejsc, jest jednym z najwiekszych mitow tej branzy. Internet peka w szwach od ladnych, lecz nudnych zdjec pieknych par.</p>
<p>To beda zajecia o nas, o fotografach - o tym co chcemy robic, co umiemy i jaki mamy pomysl na siebie w plenerze. Na siebie.</p>
<p>Chcialbym, zeby to byly zajecia o relacjach.<br />
O zdjeciach intrygujacych, intensywnych, ekspresyjnych.<br />
O tym co i jak mozemy wydobyc z ludzi, zeby ,,przytulanie" nie bylo glownym leitmotivem naszych dzialan.<br />
O tym jak chcemy fotografowac ludzi.<br />
O niepozowaniu w pozowaniu.<br />
O charyzmie.<br />
O tym, zeby na to niesmiertelne pytanie ,,co mamy teraz robic" nie miekly nam nogi, kiedy z mina fachowca odpowiemy ,,wezcie sie jakos przytulcie".</p>
<p><img class="aligncenter size-full wp-image-6439" src="https://blog.adamtrzcionka.pl/wp-content/uploads/2019/04/wzp2019.jpg" alt="" width="500" height="750" srcset="https://blog.adamtrzcionka.pl/wp-content/uploads/2019/04/wzp2019.jpg 500w, https://blog.adamtrzcionka.pl/wp-content/uploads/2019/04/wzp2019-200x300.jpg 200w" sizes="(max-width: 500px) 100vw, 500px" /></p>
<p>O czytaniu swiatla, o probie zrozumienia, ze zaprzeczeniem dobrego swiatla nie jest brzydkie swiatlo, ale jego brak - a wiec o tym, ze niemal kazde swiatlo mozna plastycznie wykorzystac.</p>
<p>A pozuja amatorzy. Wlasciwie nie tyle pozuja, co chca byc sfotografowani.<br />
Reszta jest w naszych rekach i glowach.<br />
Najserdeczniej zapraszam!<br />
A.</p>
