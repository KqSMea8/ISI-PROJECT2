<h1 id="o_nas" style="padding-left: 150px;"><strong>O nas</strong></h1>
<p style="padding-left: 90px; text-align: left;"><strong>Alliance Francaise</strong>, to jedyne w Bydgoszczy centrum jezykowe stanowiace filie francuskiej organizacji AF z siedziba w Paryzu.</p>
<p style="padding-left: 90px; text-align: left;">Osrodki <strong>Alliance</strong> <strong>Francaise</strong>, na calym swiecie sa powolane przez paryska Fundacje AF, ktora od <strong>1883 </strong>roku zajmuje sie szerzeniem kultury francuskiej, frankofonskiej i nauczaniem jezyka francuskiego.</p>
<p style="padding-left: 90px; text-align: left;">Alliance Francaise w Bydgoszczy to wyjatkowe miejsce spotkan z miedzynarodowa kultura,w przepieknych, przygotowanych do pracy wnetrzach.</p>
<p style="padding-left: 90px; text-align: left;">Stowarzyszenie Alliance Francaise oferuje swoim sluchaczom:</p>
<ul>
<li style="text-align: justify;"><strong>najwyzszy poziom nauczania</strong> weryfikowany przez strone francuska</li>
<li style="text-align: justify;">mozliwosc zdobywania <strong>miedzynarodowych certyfikatow</strong> znajomosci jezyka (egzaminy Francuskiego Ministerstwa Edukacji DELF i DALF respektowane na calym swiecie)</li>
<li style="text-align: justify;"><strong>zajecia na 12 poziomach nauczania</strong> - zgodne z Europejskim Systemem Opisu Ksztalcenia Jezykowego</li>
<li style="text-align: justify;">bogata biblioteke, czytelnie czasopism i mediateke (wszystkie zbiory w jezyku francuskim)</li>
<li style="text-align: justify;">bezplatne testy kwalifikacyjne i konsultacje z nauczycielem prowadzacym</li>
<li style="text-align: justify;">uczestnictwo w ciekawych wydarzeniach kulturalnych</li>
<li>Alliance Francaise w Bydgoszczy wdraza plan zachowan proekologicznych i respektuje Karte Duzej Rodziny 3+.</li>
<li>AF scisle wspolpracuje z Ambasada Francji, instytucjami kultury, z przedsiebiorcami i  7 stowarzyszeniami AF<br />
w Polsce oraz z wieloma organizacjami pozytku publicznego.</li>
</ul>
