<h1 class="entry-title">
<a href="http://www.wielojezycznosc.pl/?p=1391" rel="bookmark">Po co sa organizowane konkursy recytatorskie dla dzieci?</a>
</h1>

<div class="entry-meta">
<span class="date"><a href="http://www.wielojezycznosc.pl/?p=1391" title="Bezposredni odnosnik do &#8222;Po co sa organizowane konkursy recytatorskie dla dzieci?&#8221;" rel="bookmark"><time class="entry-date" datetime="2016-04-21T09:29:58+00:00">Kwiecien 21, 2016</time></a></span><span class="categories-links"><a href="http://www.wielojezycznosc.pl/?cat=1" title="Zobacz wszystkie wpisy z kategorii &#8222;Zeby sie tak chcialo, jak sie nie chce - MOTYWACJA&#8221;" rel="category">Zeby sie tak chcialo, jak sie nie chce - MOTYWACJA</a></span><span class="author vcard"><a class="url fn n" href="http://www.wielojezycznosc.pl/?author=1" title="Zobacz wszystkie wpisy, ktorych autorem jest admin" rel="author">admin</a></span>					</div><!-- .entry-meta -->
</header><!-- .entry-header -->

<div class="entry-content">
<p>Gdy przegladamy cele konkursow recytatorskich i porownujemy je z rzeczywistoscia&#8230; warto przypomniec, ze:</p>
<p>1. Konkurs recytatorski jest dla dzieci (nie dla rodzicow, nauczycieli ani jury) i to dzieci maja z niego wyniesc zachete do wystepowania, poznawania literatury, etc.</p>
<p>2. Nigdy pedagodzy nie pisza: &#8222;celem konkursu jest wylonienie zwyciezcow&#8221;. Zwyciezcy to jest efekt uboczny wynikajacy z niezrozumienia mechanizmow motywacyjnych, mechanizmow uczenia sie i plynacej  z uczenia radosci  (90-96% dzieci przed pojsciem do szkoly, uczy sie wszystkiego z wielka radoscia dzieki wewnetrzej motywacji, z ktora sie rodzimy).</p>
<p>3. Kazde dziecko wystepuje, by sie zaprezentowac. Dlatego tak wielkie podziekowania dla oragnizatorow ktorzy:</p>
<p>- podkreslaja wage kazdego wystepu ciekawymi zapowiedziami</p>
<p>- dziekuja dzieciom za przygotowanie wierszy</p>
<p>- stwarzaja mila atmosfere, dzieki, ktorej konkurs recytatorski jest przyjemnym przezyciem, bez skupiania uwagi dzieci na rywalizacji.</p>
<p>Pamietajmy, ze  &#8222;tradycyjne oceny budza lek, zabijaja motywacje i wzmagaja wychowawczo niepozadana rywalizacje miedzy uczniami&#8221;. Z ksiazki prof. Hanny Komorowskiej &#8222;Sprawdzanie umiejetnosci w nauce jezyka obcego&#8221; Wyd. Fraszka Edukacyjna 2007:155.</p>
<p>&nbsp;</p>
</div><!-- .entry-content -->

