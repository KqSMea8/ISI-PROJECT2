<h1 class="entry-title">
<a href="http://www.wielojezycznosc.pl/?p=1396" rel="bookmark">Konkurs szkolny &#8211; cala prawda.</a>
</h1>

<div class="entry-meta">
<span class="date"><a href="http://www.wielojezycznosc.pl/?p=1396" title="Bezposredni odnosnik do &#8222;Konkurs szkolny &#8211; cala prawda.&#8221;" rel="bookmark"><time class="entry-date" datetime="2016-05-06T03:58:28+00:00">Maj 6, 2016</time></a></span><span class="categories-links"><a href="http://www.wielojezycznosc.pl/?cat=1" title="Zobacz wszystkie wpisy z kategorii &#8222;Zeby sie tak chcialo, jak sie nie chce - MOTYWACJA&#8221;" rel="category">Zeby sie tak chcialo, jak sie nie chce - MOTYWACJA</a></span><span class="author vcard"><a class="url fn n" href="http://www.wielojezycznosc.pl/?author=1" title="Zobacz wszystkie wpisy, ktorych autorem jest admin" rel="author">admin</a></span>					</div><!-- .entry-meta -->
</header><!-- .entry-header -->

<div class="entry-content">
<p>Dorosli kochani, czy myslicie, ze juz bez konkursu, oceniania i rywalizacji dzieci niczego sie nie naucza?</p>
<p>Powtarzajac jak mantre cytat z ksiazki prof. Hanny Komorowskiej: &#8221; [...] <strong>oceny budza lek, zabijaja motywacje i wzmagaja wychowawczo niepozadana rywalizacje miedzy uczniami</strong>&#8221; pochylimy sie dzisiaj nad problemem, tak PROBLEMEM, tzw. &#8222;konkursow szkolnych&#8221;.</p>
<p>W szkole organizowany jest konkurs. Dzieci rywalizuja miedzy soba. Efekt? Faworyci boja sie zrzucenia z piedestalu, najslabsi nie widza nawet sensu w probowaniu swych sil, pokusa oszustwa (sciaganie, pomoc osob trzecich) jest duza. Porownywanie do innych jako kryterium samooceny, rywalizacja o nagrode, o dyplom i wyroznienie na tle spolecznosci szkolnej naleza do motywacji zewnetrznej, tej, ktora sprawdza sie tylko na krotka mete i co gorsza zabija motywacje wewnetrzna, o ktora przeciez nam chodzi (motywacja wewnetrzna to chec do dzialania).</p>
<p>Oprocz konkursow szkolnych typu TEST sa tez konkursy uznaniowe typu JURY Cie lubi albo nie. UWAGA: mowimy o konkursach szkolncyh, czyli rozgrywanych wewnatrz spolecznosci szkolnej. Z punktu widzenia prawidlowego motywowania, obnizania napiecia i tym samym agresji w szkole, sa pozbawione sensu, wrecz szkodliwe.</p>
<p>Nauczycielu, przyklad dla Ciebie. Organizujemy konkurs na najladniej ubranego nauczyciela. Konkurs szkolny wiec wewnetrzny, dla grona pedagogicznego. Codziennie rano uczniowie po zobaczeniu jak nauczyciel przyszedl ubrany do szkoly, do urn wrzucaja ankiety z punkatmi za szyk, fason, elegancje, fantazje etc. Po tygodniu punkty sa zliczane i tworzony jest ranking od najlepiej do najgorzej ubranego. Wreczenie dyplomow na apelu. Czy juz drogi nauczycielu czujesz jak szkodliwie na Twoje postrzeganie ubierania sie wplynalby taki konkurs? No tak wlasnie na postrzeganie czym jest uczenie i po co chodzi sie do szkoly wplywaja konkursy wewnatrzszkolne. Dajmy sobie  z tym spokoj.</p>
</div><!-- .entry-content -->

