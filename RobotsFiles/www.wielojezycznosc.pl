<a href="http://www.wielojezycznosc.pl/?p=1418" rel="bookmark">Sesja wybaczania (I): uraz z dziecinstwa</a>
</h1>

<div class="entry-meta">
<span class="date"><a href="http://www.wielojezycznosc.pl/?p=1418" title="Bezposredni odnosnik do &#8222;Sesja wybaczania (I): uraz z dziecinstwa&#8221;" rel="bookmark"><time class="entry-date" datetime="2017-03-05T09:39:41+00:00">Marzec 5, 2017</time></a></span><span class="categories-links"><a href="http://www.wielojezycznosc.pl/?cat=1" title="Zobacz wszystkie wpisy z kategorii &#8222;Zeby sie tak chcialo, jak sie nie chce - MOTYWACJA&#8221;" rel="category">Zeby sie tak chcialo, jak sie nie chce - MOTYWACJA</a></span><span class="author vcard"><a class="url fn n" href="http://www.wielojezycznosc.pl/?author=1" title="Zobacz wszystkie wpisy, ktorych autorem jest admin" rel="author">admin</a></span>					</div><!-- .entry-meta -->
</header><!-- .entry-header -->

<div class="entry-content">
<p><span style="text-decoration: underline;"><strong>I.  Po co mi sesja wybaczania?</strong></span></p>
<p>A jak dlugo samochod pojedzie na zaciagnietym hamulcu recznym? W jakim stanie bedzie po przejechaniu kilku kilometrow? Brak wybaczenia to jak jazda na zaciagnietym recznym.</p>
<p>Zwolnij hamulec, wybacz sobie, rodzicom, wrogom, jedz ku zdrowiu psychicznemu i fizycznemu. Po to jest wlasnie ta sesja, BY ZDROWO ZYC.</p>
<p>Nie chodzi o to, ze masz poprzec to co sie stalo, ze masz to zrozumiec</p>
<p>&#8222;Chowanie urazy to jak picie trucizny w nadziei, ze pozabija Twoich wrogow&#8221; Nelson Mandela</p>
<p><span style="text-decoration: underline;"><strong>Jak wykonac sesje wybaczania w domu?</strong></span></p>
<p>1. Wybierz osobe, na ktora chcesz zrobic sesje. Nazywac ja bedziemy TA OSOBA.</p>
<p>Na pierwsza  sesje nie wybieraj bardzo traumatycznego zdarzenia, szczegolnie jesli czujesz, ze Twoje wewnetrzne dziecko wciaz bardzo boi sie oprawcy. Wybierz kogos, kto tylko troche Cie uwiera i tylko troche zatruwa Twoje mysli. Raczej  nie wybieraj Twojego wroga numer jeden, robiac sesje wybaczania pierwszy raz, bo zadanie moze okazac sie zbyt trudne.</p>
<p>2. Wyobraz sobie miejsce, w ktorym sie znajduja</p>
<p>- TA OSOBA, czyli osoba, ktora Cie skrzywdzila (obrazila, dokuczyla, wysmiala, czyli ta, na ktora robisz sejse)</p>
<p>- TY z tamtego okresu (w wieku lat, ktore wtedy miales, gdy smutne zdarzenie  mialo miejsce)</p>
<p>- Ty TERAZ</p>
<p>2. Zabierz  cos do pisania (kartka, komputer etc.) usiadz, skup sie i pisz nastepujacy dialog</p>
<p>PUNKT 1.  Ty, dziecko z tamtego okresu, w obecnosci pozostalych dwoch osob (Ciebie z teraz i TEJ OSOBY, na ktora robisz sesje) wypisujesz wszystko co masz do zarzucenia TEJ OSOBIE. Piszesz jak bardzo Cie zranila, jak mogla tam postapic, wyrzucasz z siebie wprost w twarz TEJ OSOBY  cala swoja zlosc, wszystkie emocje.</p>
<p>PUNKT 2. Piszesz co odpowiada Ci TA OSOBA po uslyszeniu Twoich zarzutow. Wyobraz sobie co moglaby TA OSOBA na to odpowiedziec i pisz w jej imieniu.</p>
<p>PUNKT 3. Ty jako dziecko znowu odpowiadasz na to co uslyszales od  TEJ OSOBY.  Wyrzucasz kolejna porcje zlosci, zalow, goryczy i pretensji.</p>
<p>PUNKT 4. TA OSOBA odpowiada znowu dziecku ale juz z POZYCJI DUSZY. To bardzo wazne, napisz jej odpowiedzi, ktore plyna z glebi, z jej wnetrza.  Wyobraz co moze Ci na to odpowiedziec teraz TA OSOBA z pozycji duszy, jej wnetrza, jej przezyc etc.</p>
<p>PUNKT 5. Po uslyszeniu odpowiedzie z duszy, dziecko podaje reke TEJ OSOBIE. Rozumie juz wiecej. Dziekujecie sobie za spotkanie.</p>
<p>PUNKT 6. Ty, z teraz, przytulasz dziecko i mowisz, ze zawsze jestes przy nim, ze zawsze moze na Ciebie liczyc, ze zawsze pomozesz.</p>
<p>PUNKT 7. Zegnacie sie, kazde odchodzi.</p>
<p>Bardzo wazne, by moc isc po tej sesji spac. W glowie wtedy wszystko na nowo sie uklada.</p>
<p>Czy ta instrukcja jest czytelna? Zastanawiam sie co poprawic. Komentarze mile widziane.</p>
