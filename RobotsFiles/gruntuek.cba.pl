<div class="entry-content">
<p style="text-align: justify;"><strong><span lang="PL">W dniu 25 listopada 2016 roku czlonkowie Kola Naukowego Grunt uczestniczyli, w Palacu Krzysztofory, w konferencji organizowanej przez Polska Federacje Rynku Nieruchomosci wraz z Malopolskim Stowarzyszeniem Posrednikow w Obrocie Nieruchomosciami.</span></strong></p>
<p style="text-align: justify;">W sklad zaproszonych gosci wchodzili przedstawiciele Ministerstwa Infrastruktury i Budownictwa oraz prezesi branzowych stowarzyszen regionalnych z calej Polski. Pod koniec konferencji mialo miejsce wreczenie wyroznien dla zasluzonych czlonkow stowarzyszen oraz nagrody im. Jerzego Klaskaly za najlepsza prace magisterska z zakresu rynku nieruchomosci.</p>
<p style="text-align: justify;"><span lang="PL">Konferencja miala na celu przedstawic miasto Krakow, jego historie oraz przemiany i dynamiczny rozwoj. Oprocz tematyki scisle lokalnej bylo poruszanych wiele zagadnien zwiazanych z krajowym rynkiem nieruchomosci. Od jego finansowych aspektow, przez integracje uslug posrednikow i doradcow finansowych, po ekologiczne uwarunkowania branzy deweloperskiej. Duzy nacisk byl rowniez polozony na zastosowanie w sektorze nieruchomosci nowoczesnych technologii takich jak wirtualne spacery, czy porownywarki kredytow hipotecznych. Po czesci konferencyjnej uczestnicy udali sie na uroczysta kolacje.</span></p>
<p style="text-align: justify;">Autor: Maciej Galas</p>
