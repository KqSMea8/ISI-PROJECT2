<div class="entry-content">
<h1 style="text-align: justify;"><strong>Organizatorzy IX Sympozjum naukowo-dydaktycznego ,,Nieruchomosci i inwestycje" wygrali z natura. I to doslownie. Sroga kwietniowa zima nie przestraszyla uczestnikow ani nie zniechecila prelegentow. Co wiecej, mozna zaryzykowac stwierdzenie, ze zimowa aura byla bajkowym tlem dla przyswajania wiedzy w czasie konferencji.</strong></h1>
<p style="text-align: justify;">Sympozjum, ktore odbylo sie w dniach <strong>21-23 kwietnia w Koscielisku</strong>, przygotowywano z wielka starannoscia od kilku miesiecy. Dzieki staraniom komitetu organizacyjnego patronatem honorowym wydarzenie objal Jego Magnificencja Prof. UEK dr hab. Andrzej Chochol Rektor Uniwersytetu Ekonomicznego w Krakowie.</p>
<p style="text-align: justify;">Patronami merytorycznymi zostaly Polska Federacja Rynku Nieruchomosci oraz Malopolskie Stowarzyszenie Posrednikow w Obrocie Nieruchomosciami. Czasopismo naukowe ,,Swiat nieruchomosci" wspieralo organizatorow bedac patronem medialnym.</p>
<p style="text-align: justify;">W wydarzeniu wzieli udzial przedstawiciele Katedry Ekonomiki Nieruchomosci i Procesu Inwestycyjnego z naszej Alma Mater: dr Katarzyna Kania, dr Katarzyna Najbar oraz dr Bartlomiej Marona. W Sympozjum uczestniczyli takze reprezentanci zaprzyjaznionych kol naukowych Studenckiego Kola Naukowego Propertus z Uniwersytetu Ekonomicznego w Poznaniu oraz Studenckiego Kola Naukowego Inwestycji i Nieruchomosci z Szkoly Glownej Handlowej w Warszawie. Najwieksza czesc uczestnikow wydarzenia stanowili czlonkowie KN GRUNT oraz ich przyjaciele.</p>
<p style="text-align: justify;">Kolo Naukowe GRUNT juz od kilkunastu lat organizuje wyjazdowe sympozja naukowe. Inicjatorka tych dzialan byla dr Katarzyna Najbar, owczesny opiekun Kola. Tegoroczna IX juz edycja roznila sie jednak od poprzednich wyjazdow. Zdecydowano sie zmienic charakter wydarzenia z naukowego na naukowo-dydaktyczny. Dlatego oprocz wykladow i prelekcji zaproponowano sluchaczom inne pozycje programu w formie warsztatow, szkolen nawiazujacych do umiejetnosci lekkich czy wykladow o charakterze motywacyjnym.</p>
<p style="text-align: justify;">W piatek 21. kwietnia po oficjalnym rozpoczeciu IX Sympozjum KN GRUNT jako pierwszy prelegent wystapil pan <strong>Piotr Cieszewski</strong>. Ekonomista, byly dyrektor oddzialu firmy Skanska, profesjonalny mowca, taternik i zdobywca Mount Everest. Niesamowity zyciorys prelegenta zafascynowal sluchaczy, a jego cenne rady i spostrzezenia byly znakomitym prologiem do dalszych punktow programu. Tak prelekcje pana Cieszewskiego wspomina <strong>Marta Owczarek z SKN Propertus</strong> (UE w Poznaniu):</p>
<p style="text-align: justify;"><em>Najbardziej podobalo mi sie wystapienie Piotra Cieszewskiego pt. &#8222;Twoj Everest&#8221;. Bylo ono konkretna dawka motywacji do dzialania, a sam prelegent byl bardzo autentyczny w tym co mowil. Mysle, ze jego historia oraz ponadczasowe wskazowki zostana nam w pamieci na dlugo i wykorzystamy je w naszym zyciu.<br />
</em></p>
<p style="text-align: justify;">Drugie wystapienie stanowilo spora dawke praktycznej wiedzy. Pan <strong>Wojciech Orzechowski</strong>, bazujac na swoim doswiadczeniu, opowiadal o tym jak zarobic na nieruchomosciach. Nasz prelegent juz od 10 lat z sukcesami dziala w branzy nieruchomosci. Jest przedsiebiorca, wlascicielem kilku spolek, inwestorem i czlowiekiem, ktory uczestnikow swoich Warsztatow Inwestowania w Nieruchomosci ,,prowadzi za reke przez caly proces inwestowania". Nasz prelegent rozbudzil wyobraznie wszystkich obecnych, kiedy przedstawial sposoby inwestowania w nieruchomosci i mowil o wielu korzysciach plynacych z takiej dzialalnosci. Pan Orzechowski podarowal uczestnikom wydarzenia 20 egzemplarzy swojej ksiazki ,,Zarabiaj na nieruchomosci".</p>
<p style="text-align: justify;">W trakcie lektury wyzej wspomnianej pozycji jest juz<strong> Amal Al-Azazi z SKN Inwestycji i Nieruchomosci</strong> (SGH w Warszawie):</p>
<p style="text-align: justify;"><em>Wszystkie warsztaty byly bardzo interesujace i profesjonalnie przeprowadzone. Kazdy prelegent omowil inna kwestie oraz poruszyl inny obszar. Obecnie jestem w trakcie czytania ksiazki Pana Wojciecha Orzechowskiego na temat Inwestowania w nieruchomosci, tak wiec tez uwazam, ze powyzszy prelegent mial najwieksze doswiadczenie i wiedze do przekazania oraz to, co bardzo mnie do niego przekonalo, to sama chec uczenia i przekazywania tej wiedzy dalej.</em></p>
<p style="text-align: justify;">Pierwszy dzien Sympozjum zakonczylismy z najbardziej bliska organizatorom prelegentka. Wiekszosc czlonkow KN GRUNT uczestniczyla juz w zajeciach prowadzonych przez<strong> dr Katarzyne Kanie</strong>. Jej wyklad o land dewelopingu byl nie tylko bardzo interesujacy, ale mial tez wysoki poziom merytoryczny. Wyklad nawiazywal do zainteresowan naukowych dr Kani oraz do jej pracy doktorskiej.</p>
<p style="text-align: justify;">Ostatnim gosciem KN GRUNT, ktory wystapil podczas drugiego dnia Sympozjum byl pan <strong>Jozef Kajta</strong> - pomyslodawca cyklu szkoleniowego ,,Mistrz Wynajmu", wlasciciel firmy Promabi oraz lider Stowarzyszenia Mieszkan na Wynajem ,,Mieszkanicznik". Pan Kajta opowiadal o swoich doswiadczeniach na temat inwestowania w mieszkania na wynajem. Przekazal zebranym wiele praktycznych wskazowek.</p>
<p style="text-align: justify;">W zwiazku z Sympozjum zorganizowano konkurs na najlepszy referat naukowy. Z powodu braku jednego faworyta konkursu i wysokiego poziomu wszystkich nadeslanych prac, Jury pod kierownictwem dr. Bartlomieja Marony zdecydowalo sie zrezygnowac z przyznania nagrody glownej. Przyznano natomiast dwa rownorzedne wyroznienia. Laureatkami konkursu zostaly <strong>Magdalena Sikorska</strong> (Uniwersytet Ekonomiczny w Krakowie) autorka pracy &#8222;<em>Odwrocona hipoteka i renta dozywotnia jako sposob na otrzymywanie srodkow finansowych przez wlascicieli nieruchomosci w podeszlym wieku</em>&#8221; oraz <strong>Wiktoria Korbecka</strong> (University of Aberdeen) autorka artykulu pt. ,,<em>Investment in commercial property or the equity (share) market; comparison and contrast.</em>&#8222;</p>
