<p><strong>W dniach 5-6 grudnia 2016 roku odbyla sie konferencja REC 2016 organizowana przez zaprzyjaznione Kolo SKNIiN z Warszawy.</strong></p>
<p>Czlonkowie Studenckiego Kola Naukowego Inwestycji i Nieruchomosci przy SGH<br />
w Warszawie zaprosili KN Grunt na cykliczna konferencje naukowa pt: Real Estate<br />
Conference 2016 . Dzien pierwszy obejmowal cykl wykladow oraz debate, w ktorej bral udzial dr Michal Gluszak &#8211; reprezentujacy nasza Alma Mater. Drugiego<br />
dnia studenci uczestniczyli w <em>viewingu</em> trzech nieruchomosci komercyjnych.</p>
<p>Nasze Kolo reprezentowalo 13 czlonkow oraz przedstawiciele kadry naukowej Katedry Ekonomiki Nieruchomosci i Procesu Inwestycyjnego: dr Bartlomiej Marona i dr Michal Gluszak.</p>
<p>Warto zaznaczyc, ze kompleks biurowy Warsaw Spire, ktory mielismy okazje zwiedzic podczas REC 2016 znalazl sie w finalowej czworce biurowcow walczacych o tytul najlepszej inwestycji biznesowej na swiecie. Tym bardziej cieszymy sie, ze moglismy poznac blizej te wspaniala inwestycje. To dowod, ze warto uczestniczyc w konferencjach naukowych.</p>
<p>Jeszcze raz dziekujemy Organizatorom za zaproszenie i gratulujemy organizacji tak udanego wydarzenia!</p>
