<h1 class="entry-title"><a href="http://gruntuek.cba.pl/?p=690" title="Permalink to Z milosci do nieruchomosci, czyli IX Sympozjum KN GRUNT" rel="bookmark">Z milosci do nieruchomosci, czyli IX Sympozjum KN GRUNT</a></h1>

<div class="entry-meta">
<span class="sep">Posted on </span><a href="http://gruntuek.cba.pl/?p=690" title="15:47" rel="bookmark"><time class="entry-date" datetime="2017-04-23T15:47:31+00:00" pubdate>23 kwietnia 2017</time></a><span class="by-author"> <span class="sep"> by </span> <span class="author vcard"><a class="url fn n" href="http://gruntuek.cba.pl/?author=1" title="View all posts by admin" rel="author">admin</a></span></span>			</div><!-- .entry-meta -->

<div class="comments-link">
<a href="http://gruntuek.cba.pl/?p=690#respond"><span class="leave-reply">Reply</span></a>			</div>
