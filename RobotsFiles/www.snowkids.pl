<h3 class="title"><a href="http://www.snowkids.pl/?p=1340">Rolki jednosladowe</a></h3>

<p>Sprobujmy dac dzieciom mozliwosc szybkiego, latwiejszego i bezpieczniejszego wejscia w swiat narciarstwa alpejskiego dzieki sprawdzonemu przez wielu zawodnikow treningowi narciarskiemu na rolkach. Uczac sie podstaw jazdy, laczac z technikami narciarskimi ,dajemy dzieciom nowe spojrzenie i mozliwosci tkwiace w poruszaniu sie na rolkach. Urozmaicajac nauke jazdy, stosujac autorskie rozwiazania staram sie zaszczepic prawidlowe nawyki i standardy nowych technik jazdy na nartach - dziecko stosujac w praktyce na sniegu nabyte umiejetnosci ma ulatwiony start wsrod rowiesnikow i z wieksza przyjemnoscia pomysli o tej bardzo aktywnej formie spedzania czasu na sniegu.</p>
