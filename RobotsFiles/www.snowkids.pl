<h3 class="title"><a href="http://www.snowkids.pl/?p=1335">Zeglarstwo</a></h3>

<p>Wszystkie moje dzialania sportowe maja zmierzaja w jednym kierunku - by Panstwa dzieci wyrobily w sobie nawyk aktywnego, zdrowego spedzania wolnego czasu, przez 365 dni w roku uczyly sie nowych umiejetnosci sportowych &#8211; doskonalily poznane , utrzymywaly dobra kondycje i ksztaltowaly poprawna sylwetke.Zeglowanie , pierwsze kroki na wodzie wlasnorecznie otaklowana lodka to niezapomniane przezycie dla malego zeglarza, ktory uczy sie pokory wobec sil natury - wiatru i wody, a jednoczesnie wykorzystania ich fizycznych mozliwosci. Zeglowanie z rowiesnikami ulatwia trudne poczatki , doskonali relacje kolezenskie- stajemy sie odpowiedzialni wobec innych, pomagamy chetniej kolegom i kolezankom &#8211; tworzymy zgrane srodowisko zeglarskie.</p>
