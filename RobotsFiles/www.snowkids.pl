<h3 class="title"><a href="http://www.snowkids.pl/?p=2711">Narciarstwo alpejskie</a></h3>

<p>Zapraszam dzieci do nauki i doskonalenia jednego z piekniejszych moim zdaniem sportow. Narciarstwo zjazdowe raz poznane , sprobowane , posmakowane w mniejszym lub wiekszym gronie przyjaciol zostawia wrazenia ktore wracaja w glowie w ciagu roku i powoduja ze chcemy wrocic na osniezony stok jak najpredzej. Pomagaja nam w tym inne sporty uprawiane w ciagu roku a zawierajace te same ,,zrecznosci" co jazda na nartach - rolki jednosladowe , deskorolka, hulajnoga , wodniacki skim, windsurfing czy wake. Stad latwiej opanowac nam techniki narciarstwa alpejskiego, przygotowac sie do sezonu dzieciom (rolki w Snowkids)a po wpieciu nart , bezpieczniej i latwiej czerpac przyjemnosc z jazdy w dol stoku.</p>
