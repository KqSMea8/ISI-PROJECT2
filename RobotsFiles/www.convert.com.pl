w standardzie) oraz przekladniki pradowe (w tym przekladniki pradowe z otwieranym
rdzeniem, zabezpieczeniowe czy z wbudowanym przekaznikiem).
W ofercie jest tez szeroka gama przekaznikow roznorakich typow.
Dodatkowo oferujemy tez gotowe baterie kondensatorow i filtry aktywne.

