Firma <b>CONVERT Sp. z o.o.</b> juz od ponad 20 lat reprezentuje w Polsce firme
<b>CIRCUTOR S.A</b>, oferujaca szeroka game urzadzen dla elektro-energetyki jak:
przekladniki i przekazniki, mierniki panelowe i na szyne DIN (TS35), analizatory,
regulatory oraz kodensatory i filtry wyzszych harmonicznych po transformatory
i przekazniki do zabezpieczen roznicowo-pradowych. Szczegolnie interesujace
sa analizatory, tak panelowe (seria CVM) jak i przenosne (np. AR-6, CIRe3, WiBeee).
