<h2 class="entry-title"><a href="https://kminiciel.blog.deon.pl/?p=183" rel="bookmark">Nadchodza zmiany. 05.05.2103</a></h2>

<div class="entry-meta">
<span class="meta-prep meta-prep-author">Opublikowano</span> <a href="https://kminiciel.blog.deon.pl/?p=183" title="18:55" rel="bookmark"><span class="entry-date">5 maja 2013</span></a>, <span class="meta-sep">autor:</span> <span class="author vcard"><a class="url fn n" href="https://kminiciel.blog.deon.pl/?author=116" title="Zobacz wszystkie wpisy, ktorych autorem jest Kuba">Kuba</a></span>			</div><!-- .entry-meta -->

<div class="entry-content">
<p>Zmiany sa czesto dobrym krokiem&#8230; Zmiana otoczenia pozytywnie wplywa na czlowieka, moze zaczac od nowa albo&#8230; dzielic sie swoim doswiadczeniem gdzies indziej.<br />
Jezus tez dzisiaj w Ewangelii zapowiada zmiany, po raz kolejny zapowiada swoje odejscie do Ojca. Mowi: &#8222;Odchodze i przyjde znow do was. Gdybyscie Mnie milowali, rozradowalibyscie sie, ze ide do Ojca, bo Ojciec wiekszy jest ode Mnie. A teraz powiedzialem wam o tym, zanim to nastapi, abyscie uwierzyli, gdy sie to stanie&#8221;.</p>
<p>Nadchodzi zmiana, dosc istotna dla tych, ktorzy zostawili wszystko i poszli za nim.<br />
Ale mowi On im takze: &#8222;Niech sie nie trwozy serce wasze ani sie leka&#8221; i zapowiada zstapienie Ducha. Ducha, ktory nauczy ich wszystkiego i przypomni to, co On sam im powiedzial.<br />
Po raz kolejny mowi takze o tym, ze zostawia im swoj dar: POKOJ. Nie taki, jaki daje swiat. Jego pokoj.<br />
Pokoj w oczekiwaniu na Ducha, kiedy On odejdzie.</p>
<p>Nastapi to juz niedlugo. On wroci do Ojca, a pozniej Duch zawieje. Wiemy o tym, bo tak jest co rok.<br />
I co z tego? Czy to cos zmieni dla nas &#8222;tu i teraz&#8221;?<br />
Moze to czas, zeby ten Duch zawial i przyniosl zmiany takze dla mnie i dla Ciebie?<br />
Duch pocieszyciel, ktory przyjdzie i umocni. Duch radosci i Duch pokoju. Przyjdzie juz wkrotce.</p>
<p>Niech On dziala i zmienia. Bo wraz z Nim nadejdzie nowe. W pokoju. Niech przyjda zmiany!</p>
