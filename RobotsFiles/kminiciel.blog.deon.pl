<h2 class="entry-title"><a href="https://kminiciel.blog.deon.pl/?p=170" rel="bookmark">Matka. 03.05.2013</a></h2>

<div class="entry-meta">
<span class="meta-prep meta-prep-author">Opublikowano</span> <a href="https://kminiciel.blog.deon.pl/?p=170" title="20:54" rel="bookmark"><span class="entry-date">2 maja 2013</span></a>, <span class="meta-sep">autor:</span> <span class="author vcard"><a class="url fn n" href="https://kminiciel.blog.deon.pl/?author=116" title="Zobacz wszystkie wpisy, ktorych autorem jest Kuba">Kuba</a></span>			</div><!-- .entry-meta -->

<div class="entry-content">
<p>W dzisiejsza Uroczystosc Ewangelia przedstawia nam sytuacje spod krzyza.<br />
Sw. Jan &#8211; jako przedstawiciel nas wszystkich &#8211; staje sie &#8222;synem&#8221; Matki Bozej, a ona zostaje &#8222;formalnie&#8221; Jego matka. Jezus mowi do nich: &#8222;Niewiasto, oto syn Twoj&#8221;,  &#8222;Oto Matka twoja&#8221;. To samo Jezus mowi dzis do Ciebie: Oto Matka Twoja, a Ciebie przedstawia jej &#8211; jako jej kolejne dziecko.</p>
<p>Dzisiaj chcialbym sie z Wami podzielic krotka osobista refleksja.. Kiedy najbardziej odczuwam, ze Maryja jest moja Matka?<br />
Jest taki wspanialy czas &#8211; pielgrzymka. Wtedy, kiedy na wlasnych nogach ide do niej &#8211; do Jej domu, na Jasna Gore. I kiedy wchodze tam, do Sanktuarium Jasnogorskiego, czuje sie rzeczywiscie jak w domu.<br />
Jak u Mamy. Czuje to, jak ona wysluchuje tego, co chce jej powiedziec, czuje, jak wstawia sie za mna. Czuje, ze ona przytula mnie i mowi &#8222;Czekalam tu na Ciebie, wiedzialam, ze przyjdziesz!&#8221;. Moja Matka &#8211; Maryja.</p>
<p>Dzisiaj, w dzien rocznicy uchwalenia Konstytucji 3-ego Maja, nie sposob tez nie zauwazyc tego, jak ona przez wieki bronila naszej ojczyzny. Jako Waleczna Krolowa Polski. Niczym  matka chroniaca swoje dzieci i zaslaniajaca je wlasna piersia.<br />
I dzis trzeba nam ja prosic: Mamo ratuj, gdy giniemy! Mamo, pilnuj nas, zebysmy nie zbladzili! Mamo, badz z nami w kazdy czas!<br />
Kochamy Cie!</p>
