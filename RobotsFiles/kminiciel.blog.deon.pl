<h2 class="entry-title"><a href="https://kminiciel.blog.deon.pl/?p=174" rel="bookmark">Nie ma lekko! 04.05.2013</a></h2>

<div class="entry-meta">
<span class="meta-prep meta-prep-author">Opublikowano</span> <a href="https://kminiciel.blog.deon.pl/?p=174" title="20:52" rel="bookmark"><span class="entry-date">3 maja 2013</span></a>, <span class="meta-sep">autor:</span> <span class="author vcard"><a class="url fn n" href="https://kminiciel.blog.deon.pl/?author=116" title="Zobacz wszystkie wpisy, ktorych autorem jest Kuba">Kuba</a></span>			</div><!-- .entry-meta -->

<div class="entry-content">
<p>Jak niektorzy ludzie, szczegolnie starsi,  zwykli mawiac: &#8222;W zyciu nie ma lekko!&#8221;. I mysle, ze jest w tym sporo racji. W sumie, na wszystko trzeba zapracowac. Zeby miec pieniadze &#8211; trzeba miec prace. Zeby miec prace trzeba najpierw zdobyc doswiadczenie, a potem jej poszukac, co i tak nie gwarantuje tego, ze sie ja znajdzie. Mozna powiedziec &#8211; caly czas pod gore.</p>
<p>W dzisiejszej Ewangelii Jezus tez nie slodzi. Mowi: &#8222;Jezeli Mnie przesladowali, to i was beda przesladowac. Jezeli moje slowo zachowali, to i wasze beda zachowywac&#8221; albo &#8222;Jezeli was swiat nienawidzi, wiedzcie, ze Mnie pierwej znienawidzil&#8221;. Niezbyt optymistycznie. Czy wiec i pojscie z Nim ma byc kolejnym ciezarem na drodze? Kolejnym bagazem, ktory trzeba niesc przez zycie? Nie wydaje mi sie. Jezus mowil, ze Jego droga nie jest droga latwa. Ale jest droga, ktora ma cel.<br />
Mowil rowniez, ze kto chce isc za Nim musi sie zaprzec samego siebie i wziac swoj krzyz. Musi Jego nasladowac. A Jego zycie wcale nie bylo proste i przyjemne. Przeciez On tez nie mial lekko. Dzisiaj mowi nam o trudnosciach, ktore wynikaja z tego, ze sie idzie za Nim: &#8222;To wszystko wam beda czynic z powodu mego imienia, bo nie znaja Tego, ktory Mnie poslal&#8221;.<br />
Te wszystkie doswiadczenia moga spasc na Ciebie tylko dlatego, ze idziesz za Nim. To Twoj krzyz &#8211; bo &#8222;w zyciu nie ma lekko&#8221;. Ale jesli chcesz isc za Nim, wez Go. Bo On jest Twoja droga do zbawienia.</p>
<p>Warto tez dzis wspomniec krotko sw. Floriana, rzymskiego zolnierza, ktory stal sie potem meczennikiem. Dlaczego? Bo byl zolnierzem Chrystusa, wiernym do konca Jego nauce. Byl do konca wierny temu, w co wierzy. Sobie samemu i Chrystusowi. Wiara doprowadzila do Jego meczenstwa, przyplacil On zyciem za wyznawanie Chrystusa Zmartwychwstalego. A dzis moge Go postawic jako najlepszy przyklad, potwierdzajacy slowa, ktore czytamy w Aklamacji: &#8222;Jesli wiec z Chrystusem powstaliscie z martwych, szukajcie tego, co w gorze, gdzie przebywa Chrystus zasiadajac po prawicy Boga&#8221;.<br />
Sw. Florian wytrwal i znalazl. A dzis przebywa w Jego obecnosci.</p>
<p>Mimo, ze jest &#8222;pod gore&#8221;, mimo, ze w zyciu moze byc czasem ciezej, wytrwaj przy Chrystusie do konca.</p>
<p><strong>&#8222;Wez udzial w trudach i przeciwnosciach jako dobry zolnierz Chrystusa Jezusa!&#8221;</strong> 2 Tm 2,3</p>
<p>&nbsp;<br />
Badz wojownikiem!</p>
