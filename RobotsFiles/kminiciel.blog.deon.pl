<h2 class="entry-title"><a href="https://kminiciel.blog.deon.pl/?p=162" rel="bookmark">Wytrwaj w milosci! 02.05.2013</a></h2>

<div class="entry-meta">
<span class="meta-prep meta-prep-author">Opublikowano</span> <a href="https://kminiciel.blog.deon.pl/?p=162" title="20:17" rel="bookmark"><span class="entry-date">1 maja 2013</span></a>, <span class="meta-sep">autor:</span> <span class="author vcard"><a class="url fn n" href="https://kminiciel.blog.deon.pl/?author=116" title="Zobacz wszystkie wpisy, ktorych autorem jest Kuba">Kuba</a></span>			</div><!-- .entry-meta -->

<div class="entry-content">
<p>Piotr Szczepanik spiewal kiedys dosc znane slowa: &#8222;Kochac, jak to latwo powiedziec!&#8221;. I jak tak mysle, to w sumie jest prawda.. powiedziec rzeczywiscie jest latwo i prosto.<br />
A co z wykonaniem?</p>
<p>Jezus w dzisiejszej Ewangelii porusza temat wiernosci w milosci. Mowi krotko, ale bardzo tresciwie: &#8222;Jak Mnie umilowal Ojciec, tak i Ja was umilowalem. Wytrwajcie w milosci mojej! Jesli bedziecie zachowywac moje przykazania, bedziecie trwac w milosci mojej, tak jak Ja zachowalem przykazania Ojca mego i trwam w Jego milosci&#8221;.<br />
Ale czy prosto jest w dzisiejszym swiecie wypelniac Jego przykazania &#8211; a co za tym idzie trwac w Jego milosci? Okazywac zrozumienie drugiemu czlowiekowi, czasem mimo niecheci mu pomagac? Kilka dni temu pisalem, ze wystarczy kochac, ale to wcale nie jest takie proste. Dlaczego? Wezmy prosty przyklad: drogi ludzi czesto sie rozchodza. Ile w dzisiejszym swiecie jest rozwodow? Mimo, ze ludzie sie kochaja &#8211; odchodza od siebie, ida ku nowemu, nieznanemu.. Ciagle bladza, szukajac. A Jezus mowi dzis:<br />
&#8222;Wytrwajcie w milosci mojej&#8221; &#8211; to znaczy takze: wytrwajcie w milosci wobec siebie, wobec swiata, wobec drugiego czlowieka. Jakze proste, ale jakze trudne do wykonania.</p>
<p>Trwanie w Jego milosci oznaczac moze czasem pojscie pod prad. Pojscie pod prad temu, co oferuje swiat. Pojscie pod prad temu, co sobie sam zalozyles.<br />
Pojscie pod prad swojej ludzkiej milosci i zanurzenie sie w Nim, bo to ON jest prawdziwa Miloscia. Miloscia, ktorej ja sam chce zawsze pozostac wierny.</p>
<p>Jezus nie prosi Cie dzis o duzo. Tylko o to, zebys wytrwal w Jego milosci. Abys umial pojsc z Nim pod prad. Daje Ci do tego sile i zapewnienie: &#8222;To wam powiedzialem, aby radosc moja w was byla i aby radosc wasza byla pelna&#8221;.<br />
Mimo, ze pewnie bedzie pod gore, ta gora warta jest zdobycia. Wytrwaj w Jego milosci!<br />
Kochaj i pamietaj, ze milosc to wiernosc wyborowi.</p>
