<span class="meta-prep meta-prep-author">Opublikowano</span> <a href="https://kminiciel.blog.deon.pl/?p=131" title="23:59" rel="bookmark"><span class="entry-date">26 kwietnia 2013</span></a>, <span class="meta-sep">autor:</span> <span class="author vcard"><a class="url fn n" href="https://kminiciel.blog.deon.pl/?author=116" title="Zobacz wszystkie wpisy, ktorych autorem jest Kuba">Kuba</a></span>			</div><!-- .entry-meta -->

<div class="entry-content">
<p>W zyciu poznajemy wielu ludzi. Z jednymi nawiazujemy blizsze, z innymi dalsze, relacje. Jedne sa tylko chwilowe, zas inne mimo tego, ze zakonczyly sie &#8211; trwaja w pamieci i sa tak samo wazne, co wczesniej, wtedy kiedy trwaly&#8230; Dzisiaj chcialbym zwrocic uwage na dwie relacje, o ktorych jest mowa w dzisiejszej Ewangelii.</p>
<p>Zaczne od tej, ktora wymieniona jest jako druga. Bog Ojciec i Jezus &#8211; Jego Syn, ktorzy sa Jednym. Wedlug mnie, ta relacja swietnie opisana jest w ksiazce W.P. Younga &#8222;Chata&#8221; (polecam!). To, co wie Bog Ojciec &#8211; wie takze Jezus i Duch Swiety, poniewaz sa Oni jednym. Jezus mowi dzis: &#8222;Slow tych, ktore wam mowie, nie wypowiadam od siebie. Ojciec, ktory trwa we Mnie, On sam dokonuje tych dziel.&#8221;<br />
Wszystko rozgrywa sie w tej szczegolnej relacji w jakiej sa oni, w tej harmonii i jednosci. W calkowitej zgodzie i wspolgraniu. Jakze przedziwna i cudowna jest ta relacja. Bog w trojcy, szczegolnie ukazujacy sie w Jezusie, ktory jest Jego odbiciem.</p>
<p>Pokazana jest takze relacja Jezusa i Filipa. Moze nie tyle relacja, co jej fragment. Filip, jeden z Jego uczniow, ten, ktory przebywal z Nim caly czas, prosi Jezusa o to, aby ten pokazal Mu Ojca. Jezus odpowiada Mu: &#8222;Filipie, tak dlugo jestem z wami, a jeszcze Mnie nie poznales? Kto Mnie zobaczyl, zobaczyl takze i Ojca.&#8221;<br />
Filip byl w relacji z Jezusem, chodzil z Nim, sluchal Jego nauczania, a jednak nie dostrzegl w Nim Boga. A jak jest z Toba? Jaka jest Twoja relacja z Jezusem?<br />
Widzisz w Nim tylko czlowieka? Czy i najlepszego posrednika, ktory jest wspolistotny z Ojcem? Spojrz na Niego raz jeszcze! Wpatrz sie i sprobuj dostrzec, ze to On &#8211; Syn Bozy &#8211; Odbicie Ojca, Bog w ludzkiej postaci.</p>
<p>Jezus mowi dzis takze bardzo wazne dla nas zdania:<br />
&#8222;O cokolwiek prosic bedziecie w imie moje, to uczynie, aby Ojciec byl otoczony chwala w Synu. O cokolwiek prosic mnie bedziecie w imie moje, Ja to spelnie.&#8221;</p>
<p>Prosmy wiec dzis szczegolnie o otwarte oczy i serce. Abysmy umieli w Nim zobaczyc odbicie Ojca. Abysmy mogli prawdziwie wejsc z Nim w relacje i Go poznac. I wreszcie, abysmy mogli nie tyle zobaczyc Go oczami, co raczej poznac sercem.</p>
