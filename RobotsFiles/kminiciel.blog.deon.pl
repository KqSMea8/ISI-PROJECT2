<h2 class="entry-title"><a href="https://kminiciel.blog.deon.pl/?p=186" rel="bookmark">Wiary-godny. 02.06.2013</a></h2>

<div class="entry-meta">
<span class="meta-prep meta-prep-author">Opublikowano</span> <a href="https://kminiciel.blog.deon.pl/?p=186" title="21:02" rel="bookmark"><span class="entry-date">1 czerwca 2013</span></a>, <span class="meta-sep">autor:</span> <span class="author vcard"><a class="url fn n" href="https://kminiciel.blog.deon.pl/?author=116" title="Zobacz wszystkie wpisy, ktorych autorem jest Kuba">Kuba</a></span>			</div><!-- .entry-meta -->

<div class="entry-content">
<p>,,Panie, nie jestem godzien, abys przyszedl do mnie" &#8211; te Slowa chyba kazdy zna. Powtarzane tyle razy, w tylu jezykach, na calym swiecie&#8230; W dzisiejszej Ewangelii dotykaja mnie najbardziej. Dlaczego?<br />
Dlatego, ze mi dzis kojarza sie bardzo z ostatnimi kilkoma dniami. Moge to zestawic jasno z tekstem pewnej piosenki.</p>
<p>&#8222;Wiary-godni, co powstali z win,<br />
Narodzeni z laski, ktora wiecznie trwa!<br />
Wiary-godni, uzdrowieni z ran,<br />
Odrodzeni, bo zyja w Nim!&#8221;<br />
Dwa dni temu spiewalismy to na Placu Litewskim.</p>
<p>Z jednej strony Slowo, ktore pokazuje dzis czlowieka, ktory swiadom tego, jaki jest, mowi, ze nie jest godny, aby Jezus wszedl do Jego mieszkania. Z drugiej zas tekst piosenki.<br />
Jedno i drugie mega prawdziwe.</p>
<p>Bo jedno i drugie to odbicie mnie. Grzesznika, ktory ze skruszonym sercem przyznaje, ze nie jest godny tego, aby ON byl ze mna. Ale cale szczescie ON na to nie patrzy.<br />
Jestem jaki jestem, ale kocham Go i chce z Nim byc i to czyni mnie wiary-godnym.<br />
Powstaje z moich wlasnych win &#8211; jako ten, ktory jest odrodzony Jego laska.<br />
I moge zyc &#8211; bo zyje w Nim.</p>
<p>Pelnie Milosci, pelnie Wolnosci i pelnie Radosci mam w Tobie, Panie!</p>
