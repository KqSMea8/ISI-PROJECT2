<div class="col-md-6">
<p>Firma Telekom Media S.A. powstala w 1988 roku. Na poczatku swej dzialalnosci specjalizowalismy sie w tworzeniu unikalnych systemow telekomunikacyjnych na indywidualne zamowienia wymagajacych klientow. Jednak z uplywem czasu internet stal sie naszym glownym polem dzialania. Projektujemy i wykonujemy aplikacje webowe i desktopowe, rozproszone systemy bazodanowe, sklepy online. Podejmujemy sie realizacji nawet bardzo zlozonych zamowien od wymagajacych klientow.</p>

