<div class="page-content">

<span><img src="images/daradent-img.jpg" alt="Gabinet Stomatologiczny Dara-Dent" title="Gabinet Stomatologiczny Dara-Dent" class="img" align="left" /></span>
Jestesmy doswiadczonym i zgranym zespolem. Dzialamy na rynku juz od 20-tu lat. Cechuje nas profesjonalizm i perfekcyjnosc. Leczymy kompleksowo cale rodziny. Satysfakcja i piekny usmiech naszych pacjentow jest dla nas najwazniejsza.<br /><br />

<b>Wszystkie zabiegi wykonujemy calkowicie bezbolesnie.</b> Stosujemy zasade minimalnej ingerencji w twarde tkanki zeba dla uzyskania maksymalnie idealnego efektu.
<br /><br />

Udzielamy szybko i komfortowo pomocy. Pracujemy w milej i przyjaznej atmosferze. Jako nowoczesna praktyka stomatologiczna dysponujemy sprzetem i materialami najwyzszej jakosci.
<br /><br />
<b>Nasze produkty cechuje:</b>
<ul>
<li>Nowoczesnosc</li>
<li>Unikalnosc</li>
<li>Indywidualny charakter</li>
<li>Wysoka jakosc i estetyka</li>
<li>Atrakcyjnosc</li>
<li>Oryginalnosc</li>
<li>Funkcjonalnosc</li>
<li>Szeroka gama produktow</li>
<li>Zgodnosc z polskimi i europejskimi normami</li>
</ul>

