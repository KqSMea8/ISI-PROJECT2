<h2>Start</h2>
<p>Jestesmy hurtownia wyrobow hutniczych, istniejaca od 1994 roku. Specjalizujemy sie w dostawach pretow okraglych walcowanych, kutych i odkuwek swobodnie kutych. Oprocz tego oferujemy pozostale wyroby hutnicze ze stali i metali niezelaznych. Stale zwiekszamy oferowany przez nas asortyment. Posiadamy mozliwosc ciecia na pile tasmowej do srednicy 450 mm. Dysponujemy wlasnym transportem.
Dazymy do osiagniecia najkorzystniejszej oferty dla klienta. Szczegolny nacisk kladziemy na jakosc i terminowosc dostaw. Sprzedaz prowadzimy z naszego magazynu lub dostarczamy wyroby wlasnym transportem.
Oferowane przez nas wyroby posiadaja odpowiednie atesty i certyfikaty. </p>

<p>Wychodzac naprzeciw potrzebom i oczekiwaniom naszych odbiorcow oraz tendencjom na rynku stali, staramy sie elastycznie poszerzac zakres oferowanego asortymentu. Ostatnio znaczaco powiekszylismy oferte pretow ulepszanych w gatunku 42CrMo4 jak rowniez elementow ksztaltowych wypalanych z blach.</p>

<p>Oferujemy przede wszystkim prety okragle kute i walcowane w gatunkach: S355J2G3, C45, 41Cr4, 42CrMo4 i 42CrMo4+QT w zakresie srednic od fi 30 do fi 450.</p>

<p>Oprocz pretow walcowanych w naszej ofercie, znajduja sie rowniez:</p>
<ul>
<li> prety kute</li>
<li> prety ciagnione</li>
<li> blachy</li>
<li> rury</li>
<li>  ksztaltowniki</li>
<li>  wypalki z blach</li>
<li>  wyroby z metali niezelaznych</li>
</ul>


