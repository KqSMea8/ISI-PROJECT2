<h1>Witaj</h1>
<p>
Zapewniamy najwyzszej jakosci wyroby hutnicze, narzedza producentow cenionych w branzy budowlanej (Makita, Black&Decker, DeWalt), narzedza skrawajace firmy Dormer, dostarczamy takze wyroby zeliwne odlewane ciagle oraz kokilowo. Szczegolnie polecamy roznego rodzaju zawiesia, haki, szakle firmy Sangermetal oraz inne produkty sluzace zabezpieczaniu oraz mocowaniu ladunkow.</p>


