<p><br />
<br />
<br />
<br />
Biuro <strong>EUROKONTAKT</strong> wpisane jest do rejestru agencji zatrudnienia pod numerem 1107/1b. od grudnia 2004 roku, czyli od otwarcia rynku pracy w Szwajcarii dla obywateli Polskich.<br />
Wspolpracujemy ze szwajcarska firma &nbsp;<a href="http://www.agroimpuls.ch" target="_blank">Agrimpuls</a>&nbsp;, ktora ma wieloletnie doswiadczenie w branzy rolniczej.<br />
Kierujemy do pracy w rolnictwie na terenie calej Szwajcarii. <br />
Posiadamy oferty pracy dla kobiet i mezczyzn w nastepujacych obszarach zatrudnienia:</p>
<ul class="rul">
<li><span>konwencjonalne rolnictwo,</span></li>
<li><span>gospodarstwa uprawy warzyw,</span></li>
<li><span>specjalistyczne zaklady uprawy winorosli,</span></li>
<li><span>gospodarstwa tuczenia i hodowli swin,</span></li>
<li><span>hodowla bydla i krow mlecznych,</span></li>
<li><span>stadniny koni,</span></li>
<li><span>specjalistyczne gospodarstwa uprawy owocow / jagod,</span></li>
<li><span>serowarnie,</span></li>
<li><span>praca w domu i ogrodzie&nbsp;</span></li>
</ul>
<p><strong>Ogolne kryteria dla wszystkich obszarow zatrudnienia</strong></p>
<p><strong>Jezyk:</strong> minimum komunikatywna znajomosc jezyka niemieckiego, francuskiego lub wloskiego.<br />
<strong>Doswiadczenie:</strong> doswiadczenie w pracy w rolnictwie - kryterium podstawowe (referencje z poprzedniego miejsca pracy za granica, najlepiej w&nbsp;kraju niemieckojezycznym).<br />
<strong>Prawo jazdy:</strong> prawo jazdy kategorii B i/lub T.</p>
<p style="text-align: justify;"><strong>Zarobki:</strong> wynagrodzenie minimalne - 3235 frankow szwajcarskich Brutto (ok. 12 tys. PLN).<strong> </strong>Wynagrodzenie minimalne Netto wynosi ok 1500 CHF, wynika ono z odliczenia od wynagrodzenia brutto kosztow zakwaterowania (345), wyzywienia (645), ubezpieczenia zdrowotnego (ok. 220), skladek emerytalnych, podatkow i innych.</p>
<p style="text-align: justify;">Do wynagrodzenia netto nalezy doliczyc wynagrodzenie za nadgodziny, swiadczenia socjalne (ok.200 CHF na jedno dziecko i ok.100 CHF rozlakowe), 4 tygodnie&nbsp;platnego urlopu na rok. Bardzo czesto wynagrodzenie miesieczne netto wynosi ok. 2000 CHF za ok. 50-60 godzin pracy tygodniowo. Ilosc godzin pracy w tygodniu zalezy od kantonu.</p>
<p><strong>Okres zatrudnienia:</strong> od 2 miesiecy do max 364 dni (z mozliwoscia przedluzania).<br />
<strong>Zakwaterowanie zapewnia pracodawca.</strong><br />
<strong>Wyzywienie:</strong> przewaznie u pracodawcy. Jezeli wlasne to zapewniony jest dostep do kuchni.</p>
<strong>Wymagane dokumenty:</strong>
<ul style="margin-top: 0px; padding-top: 0px">
<li>1 zdjecie</li>
<li>formularz zgloszeniowy</li>
<li>mile widziane referencje z poprzedniego miejsca pracy</li>
</ul>
<p>Biuro EUROKONTAKT otrzymuje zlecenia&nbsp; z regionow niemiecko-, francusko- i wloskojezycznych. Srednia powierzchnia gospodarstwa w&nbsp; Szwajcarii wynosi 18 ha, dlatego przewaznie zgloszenia&nbsp;sa&nbsp;dla&nbsp;jednej osoby, rzadziej dla wiekszej ilosci osob lub pary/malzonkow. W zgloszeniach zawarte sa przede wszystkim: <br />
- <strong>Okres zatrudnienia</strong>- przewaznie na rok, oraz zgloszenia krotsze od 3 do 12 miesiecy <br />
- <strong>Rodzaj pracy</strong> - bardzo rozny, ale w zasadzie nie wychodzacy poza rolnictwo, praca w produkcji roslinnej lub zwierzecej, pomoc gospodyni domowej.<br />
- <strong>Dodatkowe wymaganie</strong> takie jak prawo jazdy kategorii B lub T czy znajomosc jezyka obcego. Prawo jazdy jest bardzo czesto wymagane.</p>
<p style="text-align: justify;">Nalezy pobrac&nbsp; z tej strony internetowej tutaj: &nbsp;<a href="/userfiles/file/Lebenslauf_AK_2016.pdf" target="_blank">formularz zgloszeniowy do&nbsp;pobrania w jez. niemieckim</a> oraz <a href="/userfiles/file/Lebenslauf_PL_2017.doc" target="_blank">formularz w jez. polskim</a>. Te dwa formularze aby wlasnorecznie podpisac nalezy KONIECZNIE WYDRUKOWAC, uzupelnic, przykleic zdjecie , ew. dolaczyc referencje&nbsp;i odeslac na adres <a href="mailto:info@pracawszwajcarii.pl">info@pracawszwajcarii.pl</a> lub poczta tradycyjna.&nbsp;Oferty pracy przedstawiamy po wypelnieniu formularzy. Jezeli Panstwo &nbsp;akceptujecie oferte pracy, kierujemy Panstwa dokumenty do Szwajcarii jako nasza propozycje. Gospodarz po akceptacji naszej propozycji przysyla&nbsp;umowe do podpisania. Panstwo podpisujecie umowe z gospodarzem i z biurem EUROKONTAKT&nbsp;z siedziba w Zamosciu. Otrzymujecie Panstwo tez&nbsp;bardzo dokladny&nbsp;opis gospodarstwa.&nbsp;Pracownicy powinni wyjezdzac autokarem linii miedzynarodowych&nbsp;z waznym biletem powrotnym. Pracownicy odbierani&nbsp;sa&nbsp;z&nbsp;autokaru w&nbsp;Zurichu, Bernie lub innym wiekszym miescie,&nbsp;lub z najblizszego dworca kolejowego od gospodarstwa. Wtedy nalezy wyjechac pociagiem z miasta w ktorym zatrzymuje sie autokar do dworca obok gospodarza. Otrzymujecie Panstwo od nas w tym celu plan jazdy pociagow.&nbsp;Gospodarz ma taki sam plan&nbsp;pociagow i odbiera&nbsp;Panstwa&nbsp;z dworca.</p>
<p>Pracodawcy zglaszaja zapotrzebowanie przewaznie z kilkumiesiecznym wyprzedzeniem, oferuja prace nie tylko w sezonie letnim.&nbsp;Majac Panstwa dokumenty staramy sie na biezaco czesto tego samego dnia realizowac zgloszenia ze Szwajcarii, dlatego ofert pracy nie zamieszczamy na tej stronie.<br />
<br />
Jezeli Panstwo chcieliby spotkac sie osobiscie prosimy o kontakt telefoniczny w celu umowienia spotkania w&nbsp;Zamosciu tel: <span style="font-size:9.5pt;font-family:&quot;Arial&quot;,&quot;sans-serif&quot;">501 935 359</span> w poniedzialki - srody - piatki w godzinach 9-13.&nbsp;<br />
