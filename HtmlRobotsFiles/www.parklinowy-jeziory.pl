<p><div style="height: 33px; line-height: 30px; margin-top: 8px; margin-bottom: 0px; font-family: Arial black; font-size: 24px; color: black"><marquee> Chcesz aktywnie wypoczac &nbsp;&nbsp;&nbsp; ??? &nbsp;&nbsp;&nbsp; Zapraszamy przez caly rok &nbsp;&nbsp;&nbsp; !!! &nbsp;&nbsp;&nbsp; Chcesz aktywnie wypoczac &nbsp;&nbsp;&nbsp; ??? &nbsp;&nbsp;&nbsp; Zapraszamy przez caly rok &nbsp;&nbsp;&nbsp; !!! </marquee></div>
<p>Zapraszamy do Rodzinnego Parku Linowego w Jeziorach Wysokich kolo Brodow. Do zaoferowania mamy trzy trasy w parku:</p>
<ul>
<li><strong><span style="color: #ffcc00;">Maly Tarzan</span></strong> na ok. 1,5-2 metrach wysokosci w pelnej kolorowej asekuracji siatkowej konczacej sie zjazdem na slizgawce; trasa liczy 10 przeszkod.</li>
<li><strong><span style="color: #339966;">Zielona</span></strong>, na wysokosci ok. 3 metrow, dla osob powyzej 110cm wzrostu,  liczaca 14 przeszkod.</li>
<li><strong><span style="color: #0000ff;">Niebieska</span></strong>, na wysokosci ok. 5 metrow, dla osob powyzej 150cm wzrostu liczaca 16 przeszkod.</li>
</ul>
<p>Trase zielona i niebieska dzieci do lat 12 pokonuja z osoba dorosla.</p>
<div>
<p><strong>Zapewniamy kompleksowa obsluge na najwyzszym poziomie w zakresie:</strong></p>
</div>
<div>
<ul>
<li>organizacja imprez integracyjnych dla szkol, przedszkoli i firm</li>
<li>nauka asekuracji i wspolnego dzialania na trasach</li>
<li>mile spedzenie czasu dla calych rodzin</li>
<li>pomoc na kazdym poziomie i w kazdej chwili przez wykwalifikowany personel</li>
</ul>
<p></p>
<h3>A Poza tym warto zobaczyc&#8230;</h3>
<p>
W najblizszej okolicy znajduja sie ruiny czesciowo odremontowanego <strong>Palacu </strong><strong>Bruhla</strong> , gdzie miesci sie niewielka <strong>stajnia koni</strong> . W okolo roztacza sie piekny widok na <strong>park przypalacowy</strong>, w jednej z naw znajduje sie  <strong>hotel</strong>, restauracja , jest<strong> </strong>rowniez miejsce na<strong> ognisko. </strong>Istnieje<strong> mozliwosc spacerowania po okolicy z przewodnikiem.</strong></p>
<p><strong>Kompleks Edukacyjno- Przyrodniczy</strong> w Jeziorach Wysokich. Do kompleksu nalezy  <strong>wieza widokowa</strong> z ktorej roztacza sie piekny widok na znaczna czesc okolicy w ktorej wyodrebniono sciezki do spacerow  i nord- walkingu.</p>
<p>Na dluzszy wypoczynek zapraszamy do licznych gospodarstw agroturystycznych, a milosnikow lowienia ryb nad jeziora ktorych nie brakuje w okolicy.</p>
<p>W odleglosci okolo 15 kilometrow od parku po stronie niemieckiej w Forst znajduje sie  <strong>park rozany</strong> i <strong>basen</strong> z wieloma atrakcjami, do ktorych prowadzi <strong>sciezka rowerowa</strong> .Jest to doskonale miejsce dla rodzin i wycieczek.</p>
