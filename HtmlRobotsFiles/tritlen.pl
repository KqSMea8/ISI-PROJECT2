<div class="col-12 col-md-5">
<p>Przemyslowy sposob wytwarzania ozonu polega na przepuszczeniu
powietrza lub czystego tlenu przez urzadzenia zwane generatorami ozonu.
Zachodzace w nich powierzchniowe wyladowania elektryczne, tak zwane
ujemne koronowe, powoduja powstawanie ozonu.</p>
<p>Zastosowanie wentylatora o duzym przeplywie powietrza, wbudowanego
w generator, powoduje ze ozon zostaje dokladnie rozprowadzony po
pomieszczeniach, co zwieksza skutecznosc przeprowadzonego zabiegu.</p>
<p>Ozon wchodzi w reakcje ze zwiazkami organicznymi (plesn, grzyby, wirusy,
bakterie, alergeny) i calkowicie je niszczy. Natomiast zwiazki pierscieniowe,
oraz ich pochodne (farby, lakiery, kleje) -  rozklada i utlenia.</p>
</div>

<div class="col-12 col-md-5">
<p>Dzialanie biobojcze ozonu jest piecdziesieciokrotnie skuteczniejsze i 3000
razy szybsze niz chloru. Juz niewielkie stezenie ozonu w ciagu kilku minut
zabija wszystkie patogeny.</p>
<p>Ponadto ozon nie maskuje przykrych woni, ale calkowicie je usuwa,
sprawiajac, ze ozonowane pomieszczenia dlugo pachna swiezoscia,
a przy tym sa czyste i wolne od szkodliwych czynnikow chorobotworczych.
Prawidlowo wykonany zabieg ozonowania jest w pelni bezpieczny dla otoczenia.</p>
</div>

