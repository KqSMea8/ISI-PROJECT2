<h1 class="section-title section-title--blue">Ozonowanie</h1>
<div class="ozon-container">
<strong>Ozon (tritlen)</strong> - alotropowa odmiana tlenu o czasteczce trojatomowej O3. Jest to niebieskawy gaz o charakterystycznym zapachu i silnym dzialaniu biobojczym. Jest nietrwaly, rozklada sie latwo na czasteczke tlenu O2, oraz tlen atomowy. Powstaje z tlenu atmosferycznego pod wplywem wyladowan elektrycznych (np. podczas burzy) lub promieniowania ultrafioletowego. Ozon jest jednym z najskuteczniejszych znanych srodkow dezynfekcyjnych.
</div>
</div>
