<p><span style="color: #000080;"><em>Kolejny rok juz dobiega konca. Fundacja, ktorej Dawid jest podopiecznym, zakonczyla rozliczanie 1%. Dzieki Waszym fantastycznym serduchom znowu mozemy z nadzieja patrzec na kolejny rok bez obawy, ze zabraknie srodkow na rehabilitacje, lekarstwa i potrzebne sprzety dla Dawida. 1% podatku z ubieglego roku pozwolil nam m.in. na oplacenie dwoch turnusow rehabilitacyjnych Dawida i zakup nowego lozka rehabilitacyjnego. Bardzo dziekujemy, ze w tym roku rowniez pamietaliscie o Dawidzie i przekazaliscie mu swoj 1%. Goraco dziekujemy takze tym, ktorzy wsparli Dawida w tym roku po raz pierwszy. </em></span></p>
