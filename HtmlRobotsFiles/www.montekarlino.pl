<h1>WITAMY na oficjalnej stronie rajdu</h1><p>
<img src=img/dyr45-1.jpg border=0 align=left style="padding: 0px 6px 2px 0px;">
Najwieksza impreza motoryzacyjna w naszym regionie odbedzie sie w tym roku w dniach 23-24 marca. Bedzie to juz 45. edycja Rajdu Monte Karlino, a 11 rok z rzedu runda ogolnopolskiego cyklu. Rajdowe Mistrzostwa Polski Zachodniej 2019 rozpoczna sie wlasnie
w Karlinie, Bialogardzie i Koszalinie. Jak co roku bedzie to niepowtarzalna okazja by zobaczyc w akcji zalogi rajdowe i pelen przekroj samochodow wyczynowych. Baza rajdu pozostaje Karlino i tutejsza Homanit Arena, wokol ktorej odbedzie sie takze pierwszy z dwoch
sobotnich prologow rajdu. Drugi rozegrany zostanie po zmroku na historycznej kostce wokol bialogardzkiego Placu Wolnosci. W niedziele rozegrane zostana najdluzsze odcinki specjalne rajdu. Na ulubionych przez kierowcow i pilotow z calej Polski
bocznych drogach lotniska w Zegrzu Pomorskim przygotowalismy trzy ponad 4-kilometrowe oesy. Kazdy z nich bedzie pokonywany trzykrotnie, z przerwami na wizyty w Parku Serwisowym dzialajacym takze na terenie lotniska. Na zakonczenie rajdu nie moze zabraknac
miejskiego oesu ulicami centrum Koszalina. W zeszlym roku zmienilismy koncepcje tego widowiskowego superoesu i rozegralismy go jako odcinek rownolegly, na ktorym rywalizuja po dwa auta na raz. Ten innowacyjny w skali kraju pomysl, dzieki ktoremu kibice moga sledzic bezposrednia walke
dwoch zalog rajdowych, okazal sie strzalem w dziesiatke, wiec w tym roku rowniez rozegrany zostanie w tej niecodziennej formule. Tylko na Monte Karlino mozna zobaczyc jak dwie rajdowki startuja naprzeciwko siebie, nastepnie kreca synchronicznie &quot;beczki&quot; i rozjezdzaja sie
w przeciwne strony, by po nawrocie powrocic na przeciwleglych torach i znow spotkac sie na &quot;beczkach&quot;. A to wszystko na glownym skrzyzowaniu Koszalina! Zyczac wszystkim bezpiecznego rajdu zapraszamy serdecznie na najstarszy rajd w regionie, ktory od lat dostarcza niezapomnianych wrazen i pozytywnych emocji.
Do zobaczenia na oesach!<br>
