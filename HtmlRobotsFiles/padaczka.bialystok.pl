<h1 style="text-align: center;"> <strong style="line-height: 22px; font-size: 15px; text-align: justify;">Cele i srodki dzialania. </strong></h1>
<p style="text-align: center;"><strong>SS 7 statutu. </strong></p>
<p style="line-height: 22.5px;"><strong>Celem Stowarzyszenia jest <em>udzielanie pomocy w roznych formach ludziom cierpiacym na padaczke i ich rodzinom we wszystkich sprawach zycia publicznego w celu ich spolecznej integracji, rehabilitacji, ochrony interesow zawodowych, ekonomicznych i spolecznych, przeciwdzialania ich dyskryminacji, przestrzegania wobec nich praw czlowieka, inicjowania i organizowania samopomocy wzajemnej w dazeniu do normalnego funkcjonowania w spoleczenstwie oraz zapewnienia im opieki i ochrony prawnej.</em></strong></p>
<hr style="width: 580px;" />
<h2 style="line-height: 23.3999996185303px;"> </h2>
<p style="line-height: 22.5px;" align="center"><strong>SS 8 statutu.</strong></p>
<p style="text-align: center;"><strong>Stowarzyszenie realizuje swoje cele poprzez:</strong> </p>
<p style="margin: 5pt 0cm 6pt 35.7pt; text-indent: -17.85pt; text-align: left;">1.<strong>    </strong>  Wspolprace z konstytucyjnymi organami wladzy, administracji rzadowej i samorzadowej, z instytucjami wykonujacymi zadania na rzecz osob niepelnosprawnych i z zakresu polityki spolecznej np. PFRON, ZUS, KRUS, ROPS, osrodki pomocy spolecznej itp., z Kosciolem Katolickim, z Kosciolem Prawoslawnym i innymi kosciolami i zwiazkami wyznaniowymi, placowkami publicznej i niepublicznej sluzby zdrowia, firmami farmaceutycznymi i sprzetu medycznego, bankami i firmami sektora ubezpieczen, z podmiotami-spolkami prawa handlowego i innymi jednostkami gospodarczymi, z fundacjami i innymi organizacjami pozarzadowymi.</p>
<p style="margin: 5pt 0cm 6pt 35.7pt; text-indent: -17.85pt; text-align: left;">2.<strong>     </strong> Wspolprace z organizacjami zagranicznymi i miedzynarodowymi, Unia Europejska, jej agendami i funduszami, Bankiem Swiatowym, Swiatowa Organizacja Zdrowia /WHO/ oraz spolecznosciami i instytucjami w roznych krajach europejskich, USA, Kanadzie, Australii itp..</p>
<p style="margin: 5pt 0cm 6pt 35.7pt; text-indent: -17.85pt; text-align: left;">3. <strong>     </strong>Wspolprace z organizacjami polonijnymi i Polakami za granica. </p>
<p style="margin: 5pt 0cm 6pt 35.7pt; text-indent: -17.85pt; text-align: left;">4.  <strong>   </strong> Udzielanie pomocy czlonkom i ich rodzinom w rozwiazywaniu spraw zycia codziennego oraz wspoldzialanie w organizowaniu dzialalnosci spolecznej, kulturalnej i sportowo-rekreacyjnej. </p>
<p style="margin: 5pt 0cm 6pt 35.7pt; text-indent: -17.85pt; text-align: left;">5.<strong>      </strong>Organizacje, prowadzenie oraz wspoldzialanie w prowadzeniu rehabilitacji leczniczej, zawodowej i spolecznej chorych na padaczke w roznych formach: turnusy rehabilitacyjne, imprezy integracyjne, warsztaty terapii zajeciowej, swietlice terapeutyczne, zaklady aktywnosci zawodowej, spoldzielnie socjalne, osrodki leczniczo -szkoleniowo - rehabilitacyjne, kolonie i obozy dla dzieci i mlodziezy i inne.</p>
<p style="margin: 5pt 0cm 6pt 35.7pt; text-indent: -17.85pt; text-align: left;">6.<strong>      </strong>Powolanie i prowadzenie agencji zatrudnienia, doradztwa zawodowego i posrednictwa pracy osob niepelnosprawnych oraz inne dzialania na rzecz promocji zatrudnienia chorych na padaczke i innych osob niepelnosprawnych, wplywanie na wlasciwy stosunek pracodawcow do zatrudnionych u nich chorych na padaczke i innych osob niepelnosprawnych na otwartym rynku pracy oraz w zatrudnieniu chronionym, w spoldzielniach socjalnych itp..</p>
<p style="margin: 5pt 0cm 6pt 35.7pt; text-indent: -17.85pt; text-align: left;">7.      Inicjowanie i wystepowanie z wnioskami o uregulowania prawne w zakresie niezbednych form pomocy dla chorych na padaczke i ich rodzin, w tym szczegolnie dla dzieci i mlodziezy oraz jej edukacji. </p>
<p style="margin: 5pt 0cm 6pt 35.7pt; text-indent: -17.85pt; text-align: left;">8.      Organizowanie samopomocy czlonkowskiej majacej na celu zespolenie srodowiska chorych na padaczke, przeciwdzialajac uczuciu rezygnacji, bezwartosciowosci, osamotnienia, bezradnosci, spolecznej izolacji itp.. </p>
<p style="margin: 5pt 0cm 6pt 35.7pt; text-indent: -17.85pt; text-align: left;">9.      Prowadzenie we wspolpracy ze srodkami masowej komunikacji / prasy, radia, telewizji, internetu / i wlasnych publikacji-poradnikow ciaglej pracy informacyjno-uswiadamiajacej cale spoleczenstwo, majacej na celu promowanie i ksztaltowanie prawidlowych postaw wobec osob chorych na padaczke. </p>
<p style="margin: 5pt 0cm 6pt 35.7pt; text-indent: -17.85pt; text-align: left;">10.  Wspieranie placowek leczenia padaczek / przychodnie, szpitale, kliniki / poprzez ogolnopolskie programy zakupu i darowizny specjalistycznego sprzetu medycznego - aparatow EEG, wideometrii i innego sprzetu niezbednego w diagnozowaniu i leczeniu padaczek u dzieci, mlodziezy i doroslych. Inicjowanie prac badawczych, naukowych i legislacyjnych dotyczacych opieki zdrowotnej oraz warunkow zycia chorych na padaczke i ich rodzin. </p>
<p style="margin: 5pt 0cm 6pt 35.7pt; text-indent: -17.85pt; text-align: left;">11.  Ujawnianie i dazenie do likwidacji barier spolecznych, psychologicznych i innych utrudniajacych prawidlowe funkcjonowanie ludzi z padaczka w tym szczegolnie dzieci i mlodziezy w polskim spoleczenstwie.</p>
<p style="margin: 5pt 0cm 6pt 35.7pt; text-indent: -17.85pt; text-align: left;">12.  Prowadzenie na zlecenie, w szczegolnosci organow rzadowych i samorzadowych oraz ze srodkow wlasnych, roznych form rehabilitacji, integracji, sluzacych realizacji celow statutowych Stowarzyszenia, w tym ze srodkow PFRON, funduszy publicznych oraz z funduszy Unii Europejskiej i innych funduszy krajowych i zagranicznych. </p>
<p style="margin: 5pt 0cm 6pt 35.7pt; text-indent: -17.85pt; text-align: left;">13.  Gromadzenie funduszy na dzialalnosc statutowa Stowarzyszenia.</p>
<p style="margin: 5pt 0cm 6pt 35.7pt; text-indent: -17.85pt; text-align: left;">14.  Inspirowanie i wspoldzialanie na rzecz rozwiazan prawnych w dostepie do roznego rodzaju stanowisk zgodnych z predyspozycjami chorego na padaczke poczynajac od roznych form ksztalcenia i wychowania do zatrudnienia wlacznie. </p>
<p style="margin: 5pt 0cm 6pt 35.7pt; text-indent: -17.85pt; text-align: left;">15.  Inna dzialalnosc korzystna dla chorych na padaczke i ich rodzin oraz na rzecz innych srodowisk osob niepelnosprawnych, pacjentow, rencistow i emerytow.</p>
