<h4 style="text-align: center;">Witamy na stronie Osrodka Terapii Uzaleznien "Nadzieja"</b></h4>

<!-- <p style="text-align: center;"><span style="color: #ff0000;"><br />Darmowa terapia, bez kolejki przyjecia na biezaco - wystarczy sie umowic!</span><br />&nbsp;<br />  -->
<!-- <span style="color: #709e0c;">Od stycznia 2019 r. terapia stacjonarna w Naszym osrodku finansowana bedzie w ramach umowy z NFZ!</span> </p> -->
<p style="text-align: center;"><img border="0" align="middle" src="/images/leczenie-uzaleznien-od-alkoholu-na-nfz.png" alt="Darmowa terapia uzaleznien na NFZ - Leczenie Uzaleznien NFZ" style="border: 0pt none;"></p>

<p style="text-align: left;">Terapia stacjonarna trwa 42 dni, zapewniamy zakwaterowanie w komfortowych pokojach z lazienkami oraz opieke najwyzszej klasy specjalistow uzaleznien, pacjenci objeci sa takze opieka lekarza psychiatry.</p>

<p><strong>Pacjentow zapraszamy takze do nowo otwartej Poradni Leczenia Uzaleznien przy Naszym osrodku - tu zarejestrujesz sie na wizyte <a target="_blank"href="https://www.twojnzoz.pl/wyszukiwarka/3/3798/17792?entityId=2143&localization=51.178438%2C17.1898239&noDoctorChoice=True">kliknij</a>. </strong></p>


<h4 style="text-align: left;">W poradni osoby uzaleznione oraz wspoluzaleznione moga skorzystac z pomocy:</h4>
<ul>
<li>Terapeuty uzaleznien</li>
<li>Psychologa</li>
<li>Psychiatry</li>
<li>Psychologa klinicznego</li>
</ul>

<p style="text-align: left;">Bezplatna terapie oferujemy osobom uzaleznionym od alkoholu, narkotykow, dopalaczy, lekow, hazardu, seksoholizmu i gier komputerowych oraz ich bliskim.<br />&nbsp;<br />

Zainteresowanych prosimy o kontakt <a href="tel:502136039">502 136 039</a> </p>

<h4>Co nas wyroznia:</h4>
<ul>
<li>Opieka wysokiej klasy specjalist&oacute;w, psychoterapeut&oacute;w i lekarzy</li>
<li>Komfortowe warunki pobytu - dwuosobowe pokoje z lazienkami</li>
<li>Praca z osoba uzalezniona na zasobach, mocnych stronach, wsparciu</li>
<li>Program terapii uzaleznien, kt&oacute;ry pomoze osobie uczestniczacej w terapii poradzic sobie z problemem uzaleznienia i funkcjonowac w zyciu codziennym po opuszczeniu osrodka</li>
<li>Program pracy z rodzina osoby uzaleznionej</li>
<li>Przystosowanie budynku dla os&oacute;b niepelnosprawnych</li>
<li>Oferujemy terapie poglebiona po zakonczeniu terapii na poziomie podstawowym</li>
</ul>

<p style="text-align: left;"><br>Poradzenie sobie z uzaleznieniem wymaga checi i pracy. Nasi specjalisci w profesjonalny sposob motywuja , a takze przekazuja odpowiednia wiedze na temat uzaleznienia oraz pokazuja praktyczne sposoby radzenia sobie z nim podczas zajec grupowych i podczas indywidualnej pracy z pacjentem.</p>
<p style="text-align: left;">Jednak w naszej terapii wychodzimy poza te ramy, zdajemy sobie sprawe, ze osoba po odbyciu terapii bedzie musiala radzic sobie w nowej rzeczywistosci bez uzaleznienia. Dlatego pokazujemy takze jak poradzic sobie ze stresem, jak zachowywac sie wobec bliskich i przyjaciol po opuszczeniu osrodka.</p>
<p style="text-align: left;">Pomagamy takze w odbudowie wiezi rodzinnych - rodziny naszych podopiecznych moga skorzystac z bezplatnych warsztatow i porad specjalistow.</p><br>

<h4>Podczas pracy nad problemem uzaleznienia oferujemy:</h4>
<ul>
<li><strong>Opieke </strong>wyspecjalizowanej kadry w dziedzinie Terapii Uzaleznien</li>
<li><strong>Wiedze </strong>na czym polega uzaleznienie i jak sobie poradzic z problemem</li>
<li><strong>Umiejetnosci i narzedzia niezbedne</strong> do pracy nad&nbsp; uzaleznieniem i funkcjonowaniem w codziennym zyciu po opuszczeniu osrodka, m.in. warsztaty radzenia sobie ze stresem, komunikacji interpersonalnej, czy tez wsparcie w poszukiwaniu pracy</li>
<li><strong>Wsparcie </strong>rowniez po zakonczeniu terapii, osoby opuszczajace Osrodek zawsze moga zwrocic sie po pomoc do nas, zarowno w obszarze rodzinnym, zawodowym, czy osobistym</li>
<li><strong>Pomoc w odbudowie wiezi rodzinnych</strong> - program terapeutyczny obejmuje rowniez opieke dla systemu rodzinnego, m.in. poprzez edukacje na temat problemu uzaleznienia, warsztatow dla czlonkow rodzin, czy organizacji wspolnego czasu w weekend</li>
</ul>
<p>&nbsp;</p>
