<h2>O firmie</h2>
<p><strong>Tutaj znajdziesz szereg ciekawych produkt&oacute;w do Twojego wymarzonego domu<br />w najlepszej cenie!</strong></p>
<p><br />W naszym sklepie internetowym mozesz kupic produkty najwyzszej jakosci w korzystny, latwy i wygodny spos&oacute;b. <br />Znajdziesz tutaj nie tylko ekologiczne <a href="/pl/produkty/k_1062.htm">domy drewniane</a> lecz r&oacute;wniez produkty kt&oacute;re pozwola Panstwu wyposazyc oraz upiekszyc tak wielki wysilek Waszego zycia jakim jest budowa wlasnego domu.<br />Firma nasza PPHU PIO-DO powstala w 1991 r. Podstawowym profilem dzialalnosci sa<br /> - produkcja ekologicznych dom&oacute;w z drewna; <br /> - produkcja i handel materialami budowlanymi oraz chemia, przemyslowa, gospodarcza, samochodowa;<br /> - produkty do obsadzen ogrod&oacute;w przydomowych, trawnik&oacute;w, p&oacute;l golfowych ;</p>
<p>Nasze doswiadczenia w zakresie produkcji opieraja sie na:</p>
<p><br /><strong>PRODUKCJA TARCICY DROBNO I WIELKOWYMIAROWEJ LISCIASTEJ I IGLASTEJ</strong><br />Dysponujemy trakiem pionowym, trakiem tasmowym i tarczowym oraz wielo-pilami do produkcji: <br />element&oacute;w dom&oacute;w drewnianych, <br />wiezb dachowych, <br />galanterii drewnianej, <br />element&oacute;w stolarskich, <br />kant&oacute;wek, <br />Dysponujemy wlasna stolarnia, wyposazona we wszystkie niezbedne wysokiej klasy maszyny. <br />Produkowane przez nas elementy sa absolutnie czyste ekologicznie.<br />Posiadaja wszelkie niezbedne certyfikaty Las&oacute;w Panstwowych.</p>
<p><br /><strong>CHEMIA SAMOCHODOWA</strong><br />Firma nasza produkuje oraz dystrybuuje produkty wiodacych firm produkujacych chemie przemyslowa,  samochodowa, gospodarcza.</p>
<p><br /><strong>PRODUKTY DO OBSADZEN OGROD&Oacute;W PRZYDOMOWYCH, TRAWNIK&Oacute;W, P&Oacute;L GOLFOWYCH</strong><br />Dysponujemy wszystkimi potrzebnymi instrumentami by zaopatrzyc Klienta w produkty do obsadzen ogrod&oacute;w przydomowych, trawnik&oacute;w, p&oacute;l golfowych. golfowych tym zakresie jestesmy przedstawicielem austriackiej firmy ALPINA POLYMERS oraz firmy GREEN.</p>
