<h1 class="title" >Witamy w CARO</h1>
<div class="article">
<p>
<img alt="" src="/img/artykuly/kowadlo.jpg" style="float: left; height: 200px; width: 300px; margin-left: 0pt; margin-top: 0pt;" /> <strong>Nasza Firma prowadzi dzialalnosc juz od ponad 15 lat w&nbsp;zakresie slusarstwa ze szczegolnym naciskiem na kowalstwo artystyczne.</strong> Prowadzimy uslugi i&nbsp;produkcje recznie kutych wyrobow. Stosujemy wzory wlasne i&nbsp;tradycyjne oparte na dawnym kowalstwie. Nasze wyroby moga byc ozdoba kazdego nowego budynku, pomieszczenia oraz doskonale sluzyc renowacji obiektow zabytkowych. Zatrudniamy doskonalych fachowcow specjalizujacych sie w&nbsp;tej branzy, z&nbsp;dlugoletnim stazem. <strong>Wykonujemy projekty dostosowane do potrzeb klienta i&nbsp;na specjalne zamowienie.</strong></p>
<p>
<strong>Dla nas nie ma rzeczy niemozliwych</strong>, tam gdzie konczy sie wyobraznia - tam wkracza nasza firma. Rzeczy niemozliwe dla nas nie istnieja, na specjalne zamowienie jestesmy w&nbsp;stanie wykonac w&nbsp;metalu wszystko i&nbsp;wszedzie.</p>
<p>
<strong>Nasze prace znajduja sie w&nbsp;calej Europie i&nbsp;znalazly uznanie u ponad 10000 klientow w&nbsp;kraju i&nbsp;za granica</strong>.</p>
<h5 style="text-align: center;">
Decydujac sie na nasza firme maja Panstwo zagwarantowana najwyzsza jakosc uslug.</h5>

