<p style="margin-bottom: 0cm">_ o mnie</p>

<p style="margin-bottom: 0cm; text-align: justify;">Projekty <b>powstaja z pasji</b> do projektowania, popartego kierunkowym wyksztalceniem i zdobytym doswiadczeniem praktycznym (ukonczone 5-letnie studia w Instytucie Wzornictwa Politechniki Koszalinskiej i dlugoletnia praca w studiach projektowych z zapleczem wykonawczym). W pracy za cel stawiam<b> tworzenie przestrzeni</b> ergonomicznych i estetycznych, scisle <b>dopasowanych do potrzeb</b> danej inwestycji z uwzglednieniem upodoban jej p&oacute;zniejszych uzytkownik&oacute;w. Pragne dzielic sie wciaz zdobywana wiedza, szukaniem <b>nietypowych i innowacyjnych rozwiazan</b>, <b>nadawaniu nowego znaczenia</b> przestrzeniom i przedmiotom juz sie w nich znajdujacych.<br />
<br />
Zapraszam do zapoznania sie z _oferta</p>

