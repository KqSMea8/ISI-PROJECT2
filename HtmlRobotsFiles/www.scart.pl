<h2>NOWE WERSJE PROGRAMOW ASSECO WAPRO</h2>
<p>===========================================================================</p>
<p>Szanowni Panstwo,<br />
Informujemy, ze 3 pazdziernika 2017 zostaly udostepnione nowe wersje programow ASSECO WAPRO.<br />
Zmiany zawieraja szereg nowych funkcjonalnosci. Wszystkie programy zostaly takze dostosowane do zmian przepisow obowiazujacych od stycznia 2018 roku. Szczegolna uwage poswiecono zmianom przepisow o JPK (Jednolity Plik Kontrolny). Obowiazek przesylania tego pliku do Ministerstwa Finansow obejmuje wszystkie podmioty prowadzace dzialalnosc gospodarcza od stycznia 2018 roku. </p>
<p>&nbsp;</p>
<h2>NOWY ADRES BIURA</h2>
<p>===========================================================================</p>
<p>Szanowni Panstwo,<br />
Od 21 wrzesnia 2015 przenieslismy nasze biuro do nowej siedziby. Obecnie miesci sie ono w Smolcu przy ulicy Chlopskiej 46.<br />
Niestety z powodu tymczasowego braku stacjonarnej linii telefonicznej, dotychczasowy numer telefonu 71 788-22-48 bedzie juz nieaktywny.</p>
<p>Prosimy o kontakt pod numerem tel. komorkowego <strong>509 493 570</strong>.</p>
<p>===========================================================================</p>
<h2>Aktualizacja programu Umowy-Zlecenia.</h2>
<p>18 pazdziernika 2016 roku opublikowana zostala nowa wersja programu Umowy-Zlecenia v2.2</p>
<p><strong>Najwazniejsze zmiany:</strong></p>
<p>- dodano obsluge deklaracji PIT-11 w wersji 23,<br />
- usprawniono dzialanie filtrow w przegladarkach,<br />
- poprawiono kilka drobnych bledow w programie.</p>
<p><strong>Przypominamy, ze ta wersja programu jest ostatnia wersja wspierana technicznie. Z koncem 2016 r. program zostanie wycofany ze sprzedazy.<br />
Deklaracje PIT-11(23) bedzie mozna obliczyc za rok 2016 oraz 2017. Rok 2017 bedzie ostatnim, za ktory bedzie mozna stworzyc te deklaracje.</strong></p>
<p>Aktualizacja do tej wersji jest platna.</p>
<p>===========================================================================<br />
&nbsp;</p>
<h2>Aktualizacja programu Delegacje SQL.</h2>
<p>5 i 7 stycznia 2016 roku opublikowana zostala nowa wersja programu Delegacje SQL v4.0.8.8</p>
<p><strong>Zmiany w wersji 4.0.8.8 zdn. 7.01.2016 r.:</strong><br />
- W zwiazku ze zmianami w NBP dotyczacymi udostepniania elektronicznej tabeli kursow, zmieniono algorytm importu tabeli tak, aby mozliwy byl odczyt tabel archiwalnych z lat ubieglych. Import tabeli z roku biezacego pozostal bez zmian.</p>
<p><strong>Zmiany w wersji 4.0.8.6 z dn. 5.01.2016 r:</strong><br />
- poprawiono blad ilosci miejsc po przecinku w oknie definicji stawek za 1 km.<br />
&nbsp;</p>
<h2>Aktualizacja programu Umowy-Zlecenia.</h2>
<p>10 grudnia 2015 roku opublikowana zostala nowa wersja programu Umowy-Zlecenia v2.1</p>
<p><strong>Najwazniejsze zmiany:</strong></p>
<p>- dodano obsluge deklaracji PIT-11 w wersji 22,<br />
- na wydruku zestawienia umow dodano pole PESEL,<br />
- zmodyfikowano wydruk zestawienia umow tak, aby byl bardziej czytelny,<br />
- dodano znacznik pozycji umowy &#8211; Nie uwzgledniaj w PIT-11 &#8211; zaznaczenie powoduje, ze taka umowa i jej rachunki nie beda wliczane do deklaracji PIT-11,<br />
- przebudowano kartoteke zleceniobiorcow oraz dodano informacje o rezydencie i zagranicznym identyfikatorze podatkowym,<br />
- poprawiono kilka drobnych bledow w programie.</p>
<p>Aktualizacja do tej wersji jest platna.</p>
<p>===========================================================================<br />
&nbsp;</p>
<h2>Aktualizacja programu Delegacje SQL.</h2>
<p>6 lutego 2015 roku opublikowana zostala nowa wersja programu Delegacje SQL v4.0.8.0</p>
<p><strong>Najwazniejsze zmiany:</strong></p>
<p>- wydluzono pole pelnej nazwy firmy, teraz nazwa firmy miesci cztery linie po 50 znakow,<br />
- wydluzono pole skroconej nazwy w danych firmy do 50 znakow,<br />
- wydluzono pole ulica w danych firmy do 100 znakow,<br />
- wydluzono pole miejscowosc w danych firmy do 100 znakow,<br />
- wydluzono pole nazwy waluty w slowniku walut do 100 znakow,<br />
- na wydruku rozliczenia delegacji krajowej wylaczono zbedne informacje o przekroczeniu granicy,<br />
- na wydruku ewidencji przebiegu zmieniono rozklad i dlugosc pol tak, aby miescily sie dlugie<br />
opisy tras przejazdu,<br />
- dostosowano wszystkie wydruki do dlugiego opisu firmy,<br />
- wyeliminowano blad podczas wprowadzania kwoty wydatku na poleceniu wyjazdu,<br />
- zmodyfikowano modul pobierania aktualizacji, w niektorych instalacjach nie informowal<br />
o pojawieniu sie nowej wersji programu.</p>
<p>===========================================================================<br />
&nbsp;</p>
<h2>Aktualizacja programu Umowy-Zlecenia dla Windows.</h2>
<p>6 stycznia 2015 roku zaktualizowalismy formularze deklaracji PIT-11 do wersji 21. Wersja ta obowiazuje w przypadku rozliczenia dochodu za rok 2014.<br />
Biezaca wersja programu to 2.0.0.8. Aktualizacja do tej wersji jest bezplatna dla posiadaczy wersji 2.0 i wyzszej.</p>
<p>===========================================================================<br />
&nbsp;</p>
<h2>Aktualizacja programu Delegacje SQL.</h2>
<p>8 wrzesnia 2014 roku opublikowana zostala nowa wersja programu Delegacje SQL.</p>
<p><strong>Najwazniejsze zmiany:</strong></p>
<p>- Dodano obsluge umow z pracownikami na uzytkowanie samochodu prywatnego do celow sluzbowych,<br />
