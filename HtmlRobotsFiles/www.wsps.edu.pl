<h3 class="widget-title">O stronie</h3>
<p>





Sa osoby, dla ktorych religia jest bardzo waznym elementem w ich zyciu. Dla wielu jednak z nas religia zajmuje jedno z ostatnich miejsc. Jest to czesto efektem tego, ze zostalismy wychowaniu w wierzeniu, poniewaz taka wiara kierowali sie nasi rodzice, a wczesniej ich rodzice. Wiec zazwyczaj wiara przekazywana jest z pokolenia na pokolenie. Rzadko jest to wiara z wyboru. Wiekszosc Polakow oczywiscie przyznaje sie do tego, ze sa katolikami. Jednak jest tak dlatego, ze od dziecka przyjmowali taki styl zycia. Niektorzy jednak odchodza od swojej wiary, poniewaz nie godza sie na to, aby ktos decydowal za nich.




</p>
