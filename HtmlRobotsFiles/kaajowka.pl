<div style="text-align: center;"><h3><b>O NAS</b></h3></div>
<div style="text-align: justify;"><font size="4px"><font face="Gill Sans">
KAAJowka Ludowa Chata w Bieszczadach jest wyjatkowa pod kazdym wzgledem i jedyna w swoim rodzaju.
Powstala z checi ocalenia od zapomnienia starej ponad 100-letniej wiejskiej chalupy, przeniesionej z okolic Lublina.
I tak z marzen powstala caloroczna ludowa chata polozona w samym sercu dziewiczej bieszczadzkiej krainy.
Wielkim walorem naszego domu jest jego malownicze polozenie na wzgorzu zwanym Wideta w miejscowosci Smerek (777 m.n.p.m.)
z przepieknym widokiem na panorame Poloniny Wetlinskiej.
To idealne miejsce dla rodzin lub grupy przyjaciol szukajacych wytchnienia od pedu zycia codziennego.
To takze doskonala baza wypadowa w gory, w ktorych od wielu wielu lat jestesmy bezgranicznie zakochani.
Dolozylismy wszelkich staran, aby wypoczynek w naszej chacie spelnil oczekiwania.
Caly dom jak i wiekszosc sprzetow to autentyczne meble majace swoja historie i dusze.
Zadbalismy o to, by atmosfera w chacie byla ciepla i rodzinna, ale rowniez aby przyblizyla chociaz odrobine zycie minionej epoki.
Do dyspozycji pozostawiamy cztery tematycznie urzadzone pokoje, a kazdy z nich posiada wlasna lazienke.
Ponadto mozna korzystac ze stylowo urzadzonej kuchni, wygodnej jadalni oraz saloniku z romantycznym kominkiem.
W sumie w domu komfortowo moze przebywac 12 osob. Sercem kazdego domu jest jego kuchnia.
Znajdziecie tam niezbedne wyposazenie do przygotowywania posilkow.
Tuz przy kuchni usytuowany jest kacik jadalniany z dwoma stolami oraz serwantka, w ktorej znajdziecie zastawe stolowa.
Po przeciwleglej stronie aneksu kuchennego znajduje sie salonik z wygodna sofa oraz kacikiem telewizyjnym.
W chlodne wieczory mozna ogrzac sie przy ogniu niezwykle klimatycznego kominka.
Aby dostac sie na pieterko nalezy przejsc do holu, w ktorym napotkacie na niezwykle schody.
Gdy juz pokonamy wszystkie drewniane stopnie schodow, bedziemy mogli udac sie do trzech pokoikow znajdujacych sie na pieterku chaty.
Tu znajduje sie takze mala biblioteczka z ksiazkami i grami.
Zejdzmy jednak na dol, aby obejrzec letni pokoj czyli taras, z ktorego roztacza sie cudowny widok na gory.
Cieple letnie wieczory zachecaja do dluzszego biesiadowania przy wspolnym ognisku.
Chata posiada dwa zadaszone tarasy z hustawka i hamakiem oraz klimatyczne miejsce do grillowania i kociolkowania.
