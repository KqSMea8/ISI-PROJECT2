<p>Przeprowadzam poszukiwania i selekcje kandydatow. Badam satysfakcje pracownikow, przyczyny fluktuacji, wykonuje analize potrzeb szkoleniowych i wiele innych. Moja misja jest profilowanie i zapelnianie stanowisk pracy, a takze przeprowadzanie badan majacych na celu maksymalne zwiekszenie efektywnosci pracy.<br />
Podstawa moich analiz jest wiedza socjologiczna, statystyczna, a takze praktyczna zdobyta w dotychczasowej karierze. Umiejetnosc analizy i kompulsywna potrzeba weryfikacji kazdej informacji sprawia, ze przegladajac CV zwracam uwage na kazdy detal. Poczatkowo w swojej karierze zajmowalem sie analiza materialow prasowych przy monitoringu prasy, co od kilku lat skutecznie przekladam na analize zyciorysow i listow motywacyjnych.<br />
Prywatnie cialo trenuje w maratonach biegowych, a umysl w grach logicznych i planszowych.</p>
