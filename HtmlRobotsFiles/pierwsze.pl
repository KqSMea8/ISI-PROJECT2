<div class="featured-title grid col-620">Nasze <span class="f1">wydawnictwo jest male</span> i&nbsp;ma zamiar takie pozostac.
<span class="f2">Dzialamy chaotycznie, intuicyjnie i&nbsp;bez planu.</span> Wydajemy, co chcemy i&nbsp;co nam sie spodoba. <span class="f3">Nie wiemy, co nam sie podoba, dopoki tego nie przeczytamy.</span>
Nie mamy czasu. Nie chce nam sie. Jestesmy balaganiarscy i&nbsp;nieodpowiedzialni. <span class="f2">Rzadko odpowiadamy na maile. Czasem nie odbieramy telefonu. Nie zachecamy do kontaktu z&nbsp;nami.</span>
Chyba ze ktos chce <span class="f1">kupic nasza ksiazke.</span> Wtedy robimy sie sumienni i&nbsp;uporzadkowani. Reagujemy natychmiast. Bo dzieki temu bedziemy mogli <span class="f4">wydac nastepna ksiazke</span>, ktora nam sie spodoba.
</br><span class="f2">Poza tym:</span></br>
Upieramy sie przy staroswieckim pogladzie, ze <span class="f3">czytanie ksiazek jest milym zajeciem.</span>
Nasze ksiazki mozna dostac w sieci ksiegarni Matras, a takze w niezaleznych ksiegarniach. Ale jesli ktos juz trafil na te strone, <span class="f4">proponujemy zamowic je w naszej Ksiegarence.</span>
Nalezymy do stowarzyszenia <span class="f1">ZMOWA</span>, czyli Zwiazku Malych Oficyn Wydawniczych z&nbsp;Ambicjami.
