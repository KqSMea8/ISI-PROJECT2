<p>Projekt <strong>,,Dzieje <strong>Bazaru Rozyckiego"</strong></strong> zostal zainicjowany w Laboratorium Reportazu Instytutu Dziennikarstwa Uniwersytetu Warszawskiego. Jego inspiratorem i pomyslodawca byl Marek Nowakowski, pisarz zajmujacy sie w swojej tworczosci oficjalnie przemilczanymi obszarami zycia, ludzmi z marginesu spolecznego, zafascynowany Bazarem.</p>
<p>Autorem i realizatorem projektu jest reportazysta Marek Miller.</p>
<h3>Marek Miller &#8211; O projekcie Dzieje Bazaru Rozyckiego</h3>
<p><iframe src="//www.youtube.com/embed/Z57vpxCP6Xs" width="853" height="480" frameborder="0"></iframe></p>
<p>&nbsp;</p>
<p>W latach 2001-2011, pod jego kierownictwem, zespol redakcyjny zlozony ze studentow Laboratorium Reportazu zebral bogata dokumentacje ktorej istota sa wywiady z "ludzmi bazaru" i jego bywalcami. Nawiazano tez wspolprace z: Muzeum Warszawskiej Pragi, Panstwowym Muzeum Etnograficznym, Towarzystwem Przyjaciol Pragi, Stowarzyszeniem Kupcow Warszawskich Bazaru Rozyckiego.</p>
<p>W 2012 r. kolejne materialy zostaly zebrane przez uczniow szkol licealnych z terenu dzielnicy Praga Polnoc i Praga Poludnie w ramach projektu <a title="Jezyk polski w relacjach o dziejach Bazaru Rozyckiego" href="http://bazar-rozyckiego.pl/?page_id=682">,,Jezyk polski w relacjach o dziejach Bazaru Rozyckiego. Warsztaty i konkurs Laboratorium Reportazu i liceow praskich"</a>.</p>
<p>Obecny etap projektu to stworzenie internetowej multimedialnej wersji polifonicznej powiesci dokumentalnej o dziejach Bazaru.</p>
<p>Jej polifoniczny styl polega na  prezentowaniu rownoleglosci i wielosci punktow widzenia. Jest to mozliwe dzieki  zastosowania unikalnej metody montazu i kompozycji jednolitego tekstu z dziesiatkow roznych relacji. Dzieje Bazary Rozyckiego sa tutaj panoramiczna rekonstrukcja wydarzen, odtworzeniem minionego czasu za pomoca relacji bezposrednich swiadkow i uczestnikow wydarzen, przywolaniem scen, ktore mialy miejsce i slow, ktore wowczas padly. Zastosowana specyficzna narracja i dramaturgia pozwala osiagnac efekt epickiej opowiesci pozostajacej nadal historycznym dokumentem.</p>
<p>Multimedialny charakter powiesci polega na wprowadzeniu rownoleglej narracji fotograficznej, filmowej i dzwiekowej.</p>
<p>Strukture fabularna powiesci stanowi podzial na okresy historyczne: Za Cara, Za Sanacji, Za Niemca, Za Stalina, Za Gomulki, Za Gierka, Za Solidarnosci, Za Unii.</p>
<p>Struktura ideowa powiesci jest ukazanie etosu Bazaru - stylu zycia przedmiescia, jego wielkosci i bogactwa, jego powolnego upadku i proby zmartwychwstania.</p>
<p>Szczegolny nacisk zostal polozony na uwypuklenie bogactwa i walorow polskiego jezyka potocznego, jezyka przedmiescia, jezyka subkultur, wyrazen gwarowych, obcojezycznych nalecialosci jezykowych bedacych rezultatem wielokulturowosci srodowisk zwiazanych z Bazarem.</p>
