<h1 style="text-align: center;"><strong>XLIV Plebiscyt&nbsp;&bdquo;Sportowiec Roku 2018 &ndash; Wyr&oacute;znienie Mieszkanc&oacute;w&rdquo;</strong></h1>

<p style="text-align: center;">&nbsp;</p>

<p style="text-align: center;">Burmistrz Miasta Dzialdowo oglasza 44 Plebiscyt na &bdquo;Najlepszego Sportowca&rdquo; i &bdquo;Sportowy Talent&rdquo; roku 2018. W biezacym roku, po raz drugi w historii Plebiscytu, o jego wynikach beda mogli zdecydowac takze mieszkancy Dzialdowa, dzieki mozliwosci glosowania w kategorii &bdquo;Sportowiec Roku &ndash; Wyr&oacute;znienie Mieszkanc&oacute;w&rdquo;.</p>

<p style="text-align: center;">Glosowanie na Sportowca Roku w kategorii &bdquo;Wyr&oacute;znienie Mieszkanc&oacute;w&rdquo; odbywa sie wylacznie za posrednictwem portalu konsultacji spolecznych miasta Dzialdowo &ndash; <strong>bo.dzialdowo.pl</strong>. W glosowaniu moze wziac udzial kazda osoba, kt&oacute;ra najp&oacute;zniej w roku przeprowadzenia konsultacji konczy 14 rok zycia i jest zameldowana na pobyt staly lub czasowy w Gminie-Miasto Dzialdowo. Kazdy mieszkaniec moze glosowac jeden raz, poprzez zaznaczenie jednej osoby z listy nominowanych do otrzymania tytulu &bdquo;Sportowiec Roku &ndash; Wyr&oacute;znienie Mieszkanc&oacute;w&rdquo;.</p>

<p style="text-align: center;">Glosowanie trwa od 14 do 31 stycznia 2019 r. Ogloszenie wynik&oacute;w oraz wreczenie tytul&oacute;w najlepszym sportowcom roku 2018 odbedzie sie podczas Balu Sportowca, kt&oacute;ry odbedzie sie 9 lutego 2019 r. o godz. 19.00 w Sali Bankietowej &bdquo;Na Wzg&oacute;rzu&rdquo; w miejscowosci Wysoka.</p>

