<p class="omnie">
<img src="img/seksuolog.jpg" align="right" hspace="10" vspace="5" alt="seksuolog wroclaw">
<b>Wieslaw Slosarz</b> - jest <b>seksuologiem</b> klinicznym, doktorem psychologii i bieglym sadowym. Od 1988 roku kieruje pierwsza na Dolnym Slasku Prywatna Praktyka Seksuologiczna i Psychologiczna.<br> Pracowal jako adiunkt w <b>Instytucie Psychologii Uniwersytetu Wroclawskiego</b>, byl stypendysta na University of Central Lancashire w Wielkiej Brytanii oraz na University of Tsukuba w Japonii.<br>
Zgodnie z klasyfikacja zaburzen DSM-IV <br>dr Wieslaw Slosarz zajmuje sie terapia <b>zaburzen seksualnych</b> i tozsamosci plciowej oraz terapia zaburzen osobowosci. Nalezy do wielu towarzystw naukowych krajowych i zagranicznych, w tym m.in. <b>Polskiego Towarzystwa Seksuologicznego</b>, New York Academy of Sciences. Opublikowal ponad 40 artykulow naukowych i ponad 600 popularnonaukowych dotyczacych m.in. genezy, diagnozy i <b>terapii zaburzen seksualnych</b>, ktore ukazaly sie w Polsce, Holandii, Wielkiej Brytanii, Stanach Zjednoczonych i Japonii. Wyniki swoich badan i doswiadczen klinicznych przedstawil na kilkudziesieciu konferencjach naukowych krajowych i zagranicznych. Dr Wieslaw Slosarz popularyzuje wiedze seksuologiczna i psychologiczna wspolpracujac m.in. z<b> TVP, Polsatem, TVN, Polskim Radiem, Radiem Zet, RMF FM, Gazeta Wyborcza, Rzeczpospolita, Dziennikiem, Wprostem i Newsweekiem</b>.
