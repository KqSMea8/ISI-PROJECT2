<p>
Szanowni Panstwo,<br><br>
Uprzejmie informujemy, ze dojazd do Kancelarii Notarialnej przy ulicy Bronowickiej nr 19, w zwiazku z
remontem torowiska przy ulicy Bronowickiej, aktualnie odbywa sie w nastepujacy sposob:<br><br>
1) samochodem osobowym mozna dojechac bezposrednio pod budynek kancelarii od strony ulicy
Balickiej i ulicy Armii Krajowej oraz od ulicy Rydla. Na skrzyzowaniu ulicy Rydla i ulicy Bronowickiej nalezy tak
jak zawsze wjechac w czesc ulicy Bronowickiej, przy ktorej znajduje sie kancelaria. Umieszczony na
skrzyzowaniu znak zakazu wjazdu nie dotyczy dojazdu do posesji, a wiec dojazdu pod budynek nr 19 (rysunek
ponizej),
</p>
<br>
<img src="././././././www/public/images/pics/Dojazd1.png">
<p>
2) srodkami komunikacji miejskiej - specjalnie uruchomionymi liniami autobusowymi 704 (ktory
kursuje z czestotliwoscia co 3 minuty na odcinku BRONOWICE MALE - TEATR BAGATELA) i 713 (ktory kursuje
z czestotliwoscia co 6 minut na odcinku TEATR BAGATELA - BRONOWICE MALE), nadto od 1 pazdziernika
przywrocony zostal ruch tramwajowy pomiedzy petla w BRONOWICACH MALYCH do OSIEDLA WIDOK,
<br>
</p>
<img src="././././././www/public/images/pics/Dojazd2.png">
<p>
3) dojazd samochodami osobowymi jest mozliwy do ulicy Glowackiego i ulicy Piastowskiej oraz ulicy
Lucjana Rydla i ulicy Staszczyka, gdzie nie obowiazuje strefa platnego parkowania (Ci z Panstwa, ktorzy
korzystaja z taksowki transportowej - najdalej mozna dojechac do ulicy Glowackiego nr 4, a nastepnie
przejsc do budynku nr 19 - ok. 50m), aktualnie mozliwy jest dojazd samochodem osobowym pod Kancelarie
od strony Osiedla Widok, znajdujacy sie na skrzyzowaniu znak zakazu wjazdu nie dotyczy osob
dojezdzajacych do posesji.<br><br>
Za utrudnienia przepraszamy i mamy nadzieje, ze trwajace niedogodnosci nie zniecheca Panstwa do
korzystania z uslug Naszej Kancelarii. Nadto informujemy, ze dojazd bezposrednio do budynku kancelarii jest
takze mozliwy od ulicy Glowackiego nr 4 (w szczegolnosci dla osob starszych i majacych trudnosci w
poruszaniu sie) przez szlaban zamontowany na terenie Wspolnoty Mieszkaniowej Budynku nr 4 przy ulicy
Glowackiego - po uprzednim kontakcie telefonicznym z Kancelaria w celu otwarcia szlabanu pilotem.<br><br>
W przypadku kolejnych zmian w organizacji ruchu bedziemy Panstwa na biezaco informowac, na dzien
dzisiejszy z informacji uzyskanej w Zarzadzie Drog Miasta Krakowa wynika iz w najblizszym czasie zostanie
ponownie uruchomione skrzyzowanie drog Piastowskiej - Glowackiego.<br><br>
Serdecznie pozdrawiamy,<br><br>
Zespol Kancelarii
