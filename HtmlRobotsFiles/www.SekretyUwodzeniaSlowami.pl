<h1><strong>"Jak ODPOWIEDNIMI Slowami<span class="style216"> Zainteresowac Dziewczyne, <u>Podniecic </u> i  Stac Sie Jej <u>Seksualna Fantazja</u></span><u>"</u></strong></h1>
<h1>&nbsp;</h1>
<h2 class="style9"><strong>Mowisz To Co Trzeba i Masz...
PIEKNA KOBIETE W LOZKU!</strong></h2>
<p><br />
Chcesz nauczyc sie tak lechtac slowami zmysly kobiet, by absolutnie  zawladnac ich wyobraznia? Aby nagle zobaczyly w Tobie upragnionego ksiecia ze  swojej najpiekniejszej bajki  - bez  wzgledu na to jak wygladasz i ile masz lat? <br />
Nie wiem, czy chcesz tylko zdobyc piekna dziewczyne, czy moze marzysz  o tym, by jedna po drugiej zaciagac do lozka. Sam zdecyduj. Ale w obu  przypadkach musisz najpierw zdobyc jej podziw i uwielbienie. I to okazuje sie  bardzo latwe.</p>
<p align="left" style="text-align:center; font-size:24px; font-weight:bold;">&nbsp;</p>
<p class="style12" style="text-align:center; font-size:24px; font-weight:bold;"><strong class="style1">Kazda  kobieta ma jedna glupia slabosc</strong><br />
</p>
<p align="left">Jesli tylko umiejetnie zawladniesz kobieca wyobraznia, nic nie  powstrzyma jej przed zaciagnieciem Ciebie do najblizszego lozka. Uwazaj wiec, z  kim i jak rozmawiasz! Tylko zaczniesz ja hipnotyzowac slowami, a sprawy zaczna  dziac sie tak szybko, ze sytuacja moze sie wymknac spod kontroli i nie  odczepisz sie od jej dzikiego pozadania. Bedziesz sie musial zgodzic na seks. <br />
<br />
Pewne slowa aktywizuja najbardziej wrazliwe obszary jej mozgu i wtedy  jak oszolomiona zaczyna Cie rozbierac wzrokiem. Przy ludziach! Kazde kolejne  zdanie pograza ja w coraz glebszej fascynacji Toba. </p>
<p align="center"><br />
<strong class="style1">Licza sie tylko slowa, ktore do niej mowisz!</strong></p>
<p align="left"><br />
Nie ma znaczenia, kim jestes, jak wygladasz i ile masz kasy. Kobiety  kochaja uszami. To, co mowisz i jakiego tonu uzywasz decyduje o tym, czy zobaczy w Tobie  fascynujacy obiekt pozadania, czy moze Ty zobaczysz tylko usmieszek politowania. <br />
<br />
Gdy zaczniesz uzywac wlasciwych slow w rozmowach z kobietami, zaskoczy  Cie, ze tak czesto beda mowily Ci, jaki to jestes INNY niz wiekszosc facetow,  choc same do konca beda wiedzialy dlaczego. <br />
<br />
Nawet, jesli uwazasz, ze nie masz w sobie niczego ciekawego, nie martw  sie, Twoje slowa zniewola jej wyobraznie i doprowadzisz ja do takiego stanu, ze  umowi sie z Toba na szybka randke i nie wypusci Cie z lozka przez tydzien. </p>
<p align="center"><br />
<strong class="style1">Wiec jak, chcesz zaczac  wreszcie<br />
fascynowac kobiety samymi slowami?</strong></p>
<p><br />
Co wolisz? </p>
<ul>
<li>Pustke w glowie i rozmowe, ktora sie ,,nie klei", czy rozbudzanie  slowami jej wyobrazni i <strong>wywolywanie oszalamiajacego pociagu seksualnego</strong>? </li>
<li>Narazac sie na smiesznosc wyswiechtanymi tekstami podrywaczy amatorow,  czy <strong>wprawiac ja w zachwyt</strong>, swiezymi gotowcami najbardziej popularnego  uwodziciela w Polsce? </li>
<li>Wibracje pelne flirtu, gdy <strong>seks az wisi w powietrzu</strong>, czy za  kazdym razem slyszec ,,zostanmy przyjaciolmi" lub przedstawianie innym  dziewczynom ,,to tylko kolega"? </li>
<li>Po kilku minutach <strong>otrzymac numer telefonu najpiekniejszej  dziewczyny</strong> w klubie, czy spedzic samotny wieczor, walczac z myslami  ,,podejsc - nie podejsc?"</li>
<li><strong>Miec wiecej propozycji randek niz czasu</strong>, czy jedna ulubiona  laske na RedTube?</li>
</ul>
<p><br />
Jesli trudno Ci uwierzyc w to, ze i Ty wkrotce zaczniesz zniewalac  piekne kobiety swoimi slowami, posluchaj mojej historii.</p>
<p align="center" class="style1"><br />
<strong>Jak pokonalem pustke w  glowie<br />
i zmienilem nudne teksty na intrygujace zdania, budzace </strong><strong>nieodparty  pociag seksualny u najbardziej atrakcyjnych kobiet?</strong><strong> </strong></p>
<p align="center" class="style1">&nbsp;</p>
<p style="text-align:center; font-size:24px; font-weight:bold;"><img src="img/9308f85510.jpg" style="float:left; margin-right:6px; margin-bottom:6px;" width="203" height="169" /></p>
<p>  Nazywam sie Marcin Szablewski i do 2007 roku mialem kazdy mozliwy  problem podczas podrywu kobiet. </p>
<p>&nbsp;</p>
<p>Jesli chodzi o rozmowe z dziewczyna bylem kiedys kompletna fujara. Nie  wiedzialem, co mowic kobiecie, co ona chce ode mnie uslyszec. I moje randki nie  konczyly sie dobrze.<br />
<br />
Niezaleznie jak bardzo sie staralem, to i tak nie moglem zainteresowac  soba kobiet, na ktorych mi zalezalo. W najlepszym razie zostawalem ich kolega.  W najgorszym... wole nie mowic.<br />
<br />
Mialem na przyklad &quot;kolezanke&quot;, ktora podchodzilem od  miesiecy. Jakim niewyobrazalnym cudem dala sie zaprosic na impreze u mnie w  domu. Na tej samej domowce, byl kumpel mojego kuzyna - sredni z wygladu, zaden  bogacz - wydawaloby sie, zwykly koles.<br />
<br />
I na tej domowce, podczas ktorej mialem w koncu przejsc do dzialania,  on ja poznal, pogadal chwile i... po godzinie przelecial u mnie na balkonie.<br />
Bylem wkurzony i zazdrosny. Jak to moze byc? Ona, taka porzadna i  ulozona. Probowalem z nia cos ugrac od kilku miesiecy, a jemu sie udalo w jedna  noc.<br />
Myslalem sobie &quot;Co sie k... dzieje do cholery ?&quot;.<br />
Ale zaraz potem zadalem sobie pytanie &quot;ale, chwila moment... JAK  ON TO ZROBIL?? I DLACZEGO MI SIE TO NIE UDALO??&quot;<br />
<br />
Meczylo mnie to tak bardzo, ze z trudem schowalem swoja dume i  zrozumialem, ze w tych sprawach trzeba prosic o pomoc nie kolezanki, nie  telewizje, ale kolesi, ktorzy kosza kobiety jak zboze.<br />
<br />
Wiec, kiedy nastepnego dnia dopadlem go i poprosilem o pomoc... co  zmienilo CALE MOJE ZYCIE. Odpowiedzial mi tak:</p>
<p align="center"><br />
<strong class="style1">&quot;Wiesz co, bo tak  naprawde to nie jest wazne czy jestes ladny, brzydki, lysy, grubszy, chudszy,  starszy czy mlodszy... wazne jest, zebys mial DOBRY BAJER i wiedzial, co  powiedziec dziewczynie...&quot;</strong></p>
<p><br />
Jakos nie moglem w to od razu uwierzyc, nawet po wspomnianej sytuacji  z balkonem. Ale wtedy przypomnialem sobie, ze innym razem bylem z moim kuzynem  i wspomnianym juz jego kumplem na imprezie. I na tej imprezie widzialem jak  dziewczyny IGNOROWALY przystojnego i umiesnionego tancerza erotycznego, aby  bawic sie z nimi.<br />
<br />
Na poczatku imprezy wszystkie spojrzenia zbieral ten tancerz, rozsiadl  sie jak krol i byl &quot;MACZO&quot;. Dziewczyny byl zainteresowane.<br />
Ale juz po godzinie do pokojow szly z moim kuzynem i jego kumplem  (obecnie jest on poslem, ciekawe prawda?). Oni byli prawdziwymi  &quot;naturalami&quot;.  Mieli w sobie  to cos, co magicznie przyciagalo do nich kobiety, wtedy tego nie rozumialem.  Dopiero polaczenie kilku faktow sprawilo, ze zaczalem uwaznie sluchac </p>
<p align="center"><br />
<span class="style1"><strong>Ja tez tak moge?</strong> </span></p>
<p><br />
Nie jestem najprzystojniejszy na swiecie. Nie mam kasy jak lodu ani  fury jak marzenie (tylko Mazde  z 2008 roku). Moj glos brzmi chropowato i  dziwacznie, poniewaz mialem wrodzona wade strun glosowych. Moje slowa brzmia  czasem jak bym mial scisniete gardlo. I jak tu uwodzic te piekne laski? <br />
<br />
Dzis wiem, ze to wszystko nie ma najmniejszego znaczenia. Po kilku  minutach rozmowy potrafie tak nakrecic dziewczyne, ze zaczyna myslec tylko o  seksie. <br />
Ale zanim to sie stalo, musialem sie wiele nauczyc. Mi zajelo to kilka  lat, a Ty...   za chwile otrzymasz calosc  na tacy, jak sniadanie do lozka w luksusowym apartamencie. </p>
<p align="center"><br />
<strong class="style1">Jak grzeczna dziewczynke  zmienic slowami<br />
w dzika i napalona kotke?</strong><br />
</p>
<p>Od 2005 Roku, kiedy poznalem metody uwodzenia, to zmienilem wszystko,  co mowilem kobietom na randkach, zastosowalem chyba wszystkie teksty i gotowce,  jakie uslyszalem lub wyciagnalem na szkoleniach od wielu &quot;Guru&quot;  uwodzenia z USA... a i tak rezultaty nie byly zachwycajace...<br />
<br />
Poszedlem na calosc i wydalem fortune na szkolenia guru uwodzenia z  USA i Anglii. Chcialem uczyc sie od najlepszych na swiecie, zdobyc ich wiedze i  doswiadczenie. <br />
<br />
<img width="601" height="151" src="indexp_clip_image004.jpg" /> <br />
I to byl strzal w dziesiatke.<br />
Nie znajdziesz wiekszosci takiego materialu w Internecie, ani na  zadnym polskim kursie i szkoleniu (poza jednym, o ktorym powiem Ci dalej), nie  znajdziesz tego w zadnej ksiazce (jeszcze!)...<br />
<br />
Pewnego dnia zrozumialem, gdzie popelnialem bledy. W tym, co mowilem  kiedys kobietom byl groch z kapusta. Wymieszane ze soba wszystkie techniki,  jakie przeczytalem. I ten balagan sprawial, ze nie moglem osiagnac takich  sukcesow z kobietami, jakie bym chcial.<br />
Dlatego:</p>
<ul>
<li>
<div align="center">Zmodyfikowalem gotowe teksty Guru do wlasnych potrzeb.</div>
</li>
<li>
<div align="center">Zaczalem tworzyc swoje wlasne teksty, historie i gotowce.</div>
</li>
</ul>
<p>Szlifowalem miesiacami zdobyta wiedze, na potege uzywalem rutynowych  tekstow i to czasem dzialalo, ale ciagle cos zgrzytalo. Czasem kobiety mowily  mi, ze jestem sztuczny, nienaturalny albo &quot;zebym byl soba&quot;. Wiec  ciagle bylem w kropce.<br />
<br />
Dopiero 2 lata temu, gdy skonsultowalem moje teksty i historie z  trenerami z USA (nizej Ci o nich opowiem). Zwrocili mi uwage na POTEZNE BLEDY,  ktorych sam w swoich tekstach nie dostrzegalem!!</p>
<p>To mnie oswiecilo. Zrozumialem swoje bledy polegajace na:<br />
<br />
- nieswiadomym obnizaniu wlasnej wartosci w tekstach (tego prawie nikt  nie wie, nie pisza o tym w ksiazkach, a kobiety to czuja i Cie skreslaja juz po  pierwszych kilku slowach)<br />
- przechwalaniu sie, bo bardzo latwo jest wyjsc na chwalipiete i  popsuc cale dobre wrazenie (pokaze Ci jak temu zapobiec)<br />
- nienaturalnym wprowadzaniu moich historii do rozmowy, np. kobieta  zadaje sobie pytanie, ,,ale po co mi to mowisz?&quot; i rozmowa sie psuje  (opowiem Ci jak latwo mozna temu przeciwdzialac)<br />
<br />
Teraz to jasne jak slonce. Jesli stosujesz caly czas przestarzale  metody uwodzenia to najwyzsza pora  by  to zmienic.
Gdy wprowadzilem te male poprawki do tego, co mowie kobietom,  rezultaty mnie zaskoczyly.</p>
<p align="center"><br />
<strong class="style1">W ten weekend zakrecisz  swoimi tekstami<br />
w glowie pierwszym dziewczynom</strong></p>
<p><br />
Piekne kobiety w koncu znowu zaczely sie ze mna umawiac i spac.<br />
Poznalem sekret, dzieki ktoremu umialem w rozmowie wzbudzic w  kobietach pociag do mnie. Zaraz zdradze Ci kilka szczegolow.<br />
Wiem, ze jesli to zastosujesz, mozesz byc spokojny o prace dla swojego  penisa :) Jeszcze bedziesz miec  tego dosc.
Proponuje zaczac od zaraz, po co czekac na seks?<br />
</p>
