var count=$('.news_box').find('.news_entry').length;
var req = loadPosts();
req.open("GET", "aplication/ajax/"+$(this).data('kind')+".php?from="+count, true);
req.onreadystatechange = function(){
if(req.readyState==4 && req.status==200){
$('.news_box').append(req.responseText);
}
}

req.send(null);
});


