<h1 class="title">
Maszyny rolnicze                     <span class="yellow">
John Deere Wanicki                    </span>
</h1>
<div class="content center">

<p style="text-align: center;"><strong>Firma Agro Wanicki, najmlodsza sp&oacute;lka Grupy Wanicki to autoryzowany partner marki John Deere na terenie wojew&oacute;dztwa lubelskiego. </strong></p>
<p style="text-align: center;">W ramach jej dzialalnosci oferujemy Panstwu kompleksowa obsluge w zakresie zakupu nowych oraz uzywanych maszyn rolniczych, sklep czesci i akcesori&oacute;w oraz autoryzowany serwis wyposazony w najnowoczesniejsze narzedzia diagnostyczne dzialajacy stacjonarnie oraz w terenie &ndash; na posiadlosciach klient&oacute;w.&nbsp; Nasze uslugi swiadczymy przez szesc dni w tygodniu, oferujemy kr&oacute;tkie terminy napraw oraz atrakcyjne rabaty dla stalych klient&oacute;w.</p>
<p style="text-align: center;">Oferte Agro Wanicki uzupelnia sklep internetowy <a href="http://czesci.wanicki.pl/" target="_blank">www.czesci.wanicki.pl</a>, w kt&oacute;rym prezentujemy czesci zamienne do maszyn takich marek jak John Deere, New Holland czy Manitou.</p>
<p style="text-align: center;">Nasz zesp&oacute;l tworza ludzie doskonale znajacy oczekiwania klient&oacute;w z branzy rolniczej, kt&oacute;rych wiedza jest nieustannie aktualizowana i rozszerzana poprzez system szkolen produktowych oferowanych przez Agro Wanicki w porozumieniu m.in. z marka John Deere.</p>
<p style="text-align: center;">Osmiu przedstawicieli handlowych oczekuje na Panstwa&nbsp; w terenie lub jednym z naszych oddzial&oacute;w w Pulankowicach, Sitancu, Kazimierz&oacute;wce i Jan&oacute;wku w wojew&oacute;dztwie Lubelskim. Zapraszamy!</p>
