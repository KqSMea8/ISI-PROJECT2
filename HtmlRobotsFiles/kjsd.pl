<h1>PONAD 20 LAT NA RYNKU USLUG PRAWNICZYCH</h1>
<div id="content">
<div class="columns" style="overflow: hidden;">
<div class="col_1" style="float: left; width: 48%; text-align: justify;"><br />Kornecki, Jaworska, Stankiewicz &amp; Dzierzanowski Kancelaria Adwokat&oacute;w i Radc&oacute;w Prawnych S.j. jest kontynuatorem dzialalnosci&nbsp; Kancelarii Kornecki &amp; Stankiewicz S.c., zalozonej w 1992 roku przez Roberta Korneckiego i Wojciecha Stankiewicza. W latach dziewiecdziesiatych obaj gl&oacute;wni wsp&oacute;lnicy laczyli praktyke prawnicza z praca dydaktyczno-naukowa, bedac nauczycielami akademickimi na Wydziale Prawa i Administracji Uniwersytetu Szczecinskiego. W 2010 roku do Kancelarii dolaczyli jako wsp&oacute;lnicy radca prawny dr Rita Jaworska-Stankiewicz&nbsp; i adwokat Michal Dzierzanowski. Dr Rita Jaworska-Stankiewicz do dzisiaj jest aktywnym nauczycielem akademickim na Wydziale Prawa i Administracji Uniwersytetu Szczecinskiego, gdzie pelnila m.in. funkcje Prodziekana. <br /><br />Siedziba Kancelarii miesci sie w prestizowej lokalizacji na szczecinskim Podzamczu, w kamienicy powstalej na murach szczecinskiego Starego Miasta. Kancelaria posiada filie w Swinoujsciu. <br /><br />Od poczatku istnienia Kancelarii przedmiotem jej dzialalnosci bylo i jest swiadczenie uslug prawnych dla podmiot&oacute;w gospodarczych i os&oacute;b fizycznych w zakresie przewidzianym ustawa o radcach prawnych, a po rozszerzeniu skladu osobowego, takze ustawa o adwokaturze. Kancelaria w szczeg&oacute;lnosci i najchetniej zajmuje sie obsluga prawna biznesu w r&oacute;znych branzach,&nbsp; kompleksowa obsluga inwestycji, ponadto podmiot&oacute;w publicznych, a takze chetnie podejmuje sie obrony w skomplikowanych, trudnych sprawach karnych. <br /><br />Kancelaria posiada duze doswiadczenie w obsludze klient&oacute;w miedzynarodowych, w tym firm pochodzacych z wielu kraj&oacute;w europejskich oraz z Azji, a takze ze Stan&oacute;w Zjednoczonych. <br /><br /></div>
<div class="col_sep" style="float: left; width: 3%; height: 16px;">&nbsp;</div>
<div class="col_2" style="float: left; width: 48%; text-align: justify;"><br />W 2005 roku Kancelaria podpisala umowe o wsp&oacute;lpracy z siecia niemieckich kancelarii prawniczych pod przewodnictwem Kancelarii Becker &amp; Partner GbR.&nbsp; Wsp&oacute;lpraca z siecia kancelarii niemieckich pozwala zintensyfikowac kontakty biznesowe pomiedzy polskimi i niemieckimi klientami Kancelarii. <br /><br />Kancelaria swiadczy uslugi w ramach podstawowego zespolu skladajacego sie z dw&oacute;ch adwokat&oacute;w oraz trojga radc&oacute;w prawnych, kt&oacute;rych wspomagaja aplikanci adwokaccy i&nbsp;radcowscy. Zesp&oacute;l wspierany jest w razie potrzeby oraz w ramach indywidualnych projekt&oacute;w przez zewnetrznych prawnik&oacute;w wsp&oacute;lpracujacych. Ponadto z Kancelaria wsp&oacute;lpracuje stale konsultant do spraw funduszy unijnych. Prawnik&oacute;w wspomaga czteroosobowy personel techniczny. Zesp&oacute;l prawnik&oacute;w Kancelarii to mieszanka doswiadczonych adwokat&oacute;w i radc&oacute;w prawnych (z pokolenia piecdziesieciolatk&oacute;w) i mlodych adwokat&oacute;w (z pokolenia trzydziestolatk&oacute;w) oraz starannie wybranych, szczeg&oacute;lnie prawniczo uzdolnionych&nbsp; aplikant&oacute;w, zar&oacute;wno adwokackich jak i&nbsp;radcowskich. <br /><br />Kancelaria posiada pelne wyposazenie techniczne i&nbsp;teleinformatyczne, stosujac w swojej praktyce najnowsze technologie w sprawny spos&oacute;b obsluguje klient&oacute;w. <br /><br />Obsluga swiadczona jest takze w jezykach obcych &ndash; angielskim i&nbsp;niemieckim. Obsluga w jezykach obcych obejmuje sporzadzanie projekt&oacute;w um&oacute;w i innych akt&oacute;w prawnych, a takze korespondencje i prowadzenie negocjacji w tychze jezykach.<br /><br /></div>
