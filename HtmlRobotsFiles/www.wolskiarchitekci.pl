<div class="hyphenate text" lang="pl"><p>Kameralny obiekt typu boutique hotel zostal zaprojektowany w przedwojennym pensjonacie o tej samej nazwie. Nastroj hotelu przenosi swoiich gosci w swiat lat trzydziestych i morskich podrozy. Domowa atmosfera przepelniona pamiatkami z okresu miedzywojennego czyni z tego obiektu najciekawszy boutique hotel w Trojmiescie. Nagrodzony przez prezydenta miasta Gdyni za najlepsza realizacje 2002 r. oraz przewodnik Pascal za obiekt o wyjatkowym klimacie w 2003 i 2004 r. Do ksiegi gosci wpisalo sie wielu znanych ludzi, a wsrod nich Grazyna Szapolowska, Cezary Pazura i Jan Nowak Jezioranski.</p></div>
