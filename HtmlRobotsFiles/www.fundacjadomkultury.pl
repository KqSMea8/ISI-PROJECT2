<h2 class="entry-title"><a href="http://www.fundacjadomkultury.pl/?p=3345" rel="bookmark">Warsztaty obywatelskiej refleksyjnosci w wiezieniu dla kobiet</a></h2>

<div class="entry-meta">
<span class="meta-prep meta-prep-author">Opublikowano</span> <a href="http://www.fundacjadomkultury.pl/?p=3345" title="7:43 am" rel="bookmark"><span class="entry-date">Listopad 26, 2018</span></a>, <span class="meta-sep">autor:</span> <span class="author vcard"><a class="url fn n" href="http://www.fundacjadomkultury.pl/?author=1" title="Zobacz wszystkie wpisy, ktorych autorem jest admin">admin</a></span>			</div><!-- .entry-meta -->

<div class="entry-content">
<p>&nbsp;</p>
<p style="text-align: justify;">,,Refleksyjnosc obywatelska w wiezieniu dla kobiet" to cykl zajec poswieconych demokracji, prawom czlowieka, odpowiedzialnosci i sprawczosci obywatelskiej prowadzonych dla grupy kobiet odbywajacych kare wiezienia w Areszcie Sledczym Warszawie Grochowie przez dziennikarzy, pedagogow, psychologow i prawnikow zaprzyjaznionych z nasza Fundacja. W sumie 7 zajec, po ktorych refleksje znajdziecie na blogu eWKratke.pl w postaci postow.</p>
<p class="Standard" style="text-align: justify;">Projekt ,,Warsztaty obywatelskiej refleksyjnosci w wiezieniu dla kobiet" wspierany jest przez Fundusz Obywatelski zarzadzany przez Fundacje dla Polski".</p>
<p class="Standard" style="text-align: justify;"><b>Rozmawialismy o wolnosci prasy.</b></p>
<p class="Standard" style="text-align: justify;">W dzisiejszej rzeczywistosci medialnej slowo ,,prasa" wydaje sie archaiczne. Wystepuje bowiem bardzo duzo form komunikacji spolecznej. W ostatnich latach coraz wieksza role informacyjna odgrywaja media spolecznosciowe. Tymczasem pojecie ,,prasa" jest nadal potrzebne. Chociazby dlatego, ze jest ono uzywane w aktach prawnych.</p>
<p class="Standard" style="text-align: justify;">Podczas spotkania wyjasnilismy sobie wlasnie co rozumiemy pod pojeciem prasy i jakie funkcje ona spelnia. Przyjrzelismy sie miedzynarodowym i polskim aktom prawnym gwarantujacym wolnosc prasy. Omowilismy tez rozne formy cenzury wystepujace przed upadkiem bloku wschodniego. Rozmawialismy o roznych formach nacisku na media wywierane przez kosciol czy przez rzadzacych. Obejrzelismy i przedyskutowalismy fragmenty programow informacyjnych zaklamujacych rzeczywistosc.</p>
<p class="Standard" style="text-align: justify;">Na koniec rozwazalismy kwestie potrzeby dalszego istnienia mediow w znanej nam obecnie formie. Zastanawialismy sie czy moze media spolecznosciowe sa w stanie zastapic zespoly dziennikarzy pracujacych w redakcjach.</p>
<p><img class="aligncenter wp-image-3425 size-full" title="fot. Malgorzata Brus" src="http://www.fundacjadomkultury.pl/wp-content/uploads/2018/11/03-02-DSC_0009.jpg" alt="" width="1200" height="797" srcset="http://www.fundacjadomkultury.pl/wp-content/uploads/2018/11/03-02-DSC_0009.jpg 1200w, http://www.fundacjadomkultury.pl/wp-content/uploads/2018/11/03-02-DSC_0009-300x199.jpg 300w, http://www.fundacjadomkultury.pl/wp-content/uploads/2018/11/03-02-DSC_0009-768x510.jpg 768w, http://www.fundacjadomkultury.pl/wp-content/uploads/2018/11/03-02-DSC_0009-1024x680.jpg 1024w" sizes="(max-width: 1200px) 100vw, 1200px" /></p>
<p class="Standard" style="text-align: justify;"><strong>Wolnosc prasy - fakt czy fikcja?</strong></p>
<p class="Standard" style="text-align: justify;">W dobie internetu tradycyjne papierowe gazety powoli odchodza w zapomnienie. Ale czy na pewno? Kazda wieksza gazeta ma swoja strone internetowa.</p>
<p class="Standard" style="text-align: justify;">Prasowka, to w moim rozumieniu, wyselekcjonowanie faktow okraszonych komentarzami redaktorow, zwiazanych z roznymi opcjami politycznymi. Ogladanie jednej stacji, ograniczenie sie do jednego periodyku, to poddanie sie indoktrynacji, to przejmowanie cudzych pogladow i plyniecie z wiekszoscia. Zatracanie zdolnosci samodzielnego myslenia.</p>
<p class="Standard" style="text-align: justify;"><strong>Bella</strong></p>
<p class="Standard" style="text-align: justify;">Uwazam, ze media sa potrzebne i przydatne, bo gdyby nie dziennikarze, to nie byloby wiadomo, co sie dzieje w naszym kraju i na swiecie. Bardzo tez przydatne sa informacje internetowe. Tam rowniez mozna sie sporo madrych rzeczy dowiedziec, takich ktorych nie ma gdzie indziej.</p>
<p class="Standard" style="text-align: justify;">Ja osobiscie rzadko ogladam dzienniki, czy tez czytam gazety. Informacje czerpie z internetu. Gdyby nie to, to bym zostala bez wiedzy.</p>
<p class="Standard" style="text-align: justify;"><strong>Evenement</strong></p>
<p class="Standard" style="text-align: justify;">Interesuja mnie glownie suche, konkretne fakty, wydarzenia. Oczywiscie slucham i czytam komentarze, lecz staram sie zawsze obiektywnie patrzec na rzeczywistosc. Autorzy komentarzy maja rozne rzeczy na mysli. Czesto sa one zalezne od tego, co wypili i w jakich ilosciach, badz, co wzieli oraz na jakim etapie rozwoju emocjonalnego sie znajduja.</p>
<p class="Standard" style="text-align: justify;">Duzo czytam publicystyki i uwielbiam Waldemara Lysiaka. Jest on dla mnie autorytetem majacym zdolnosci plastyczne. Jego wyobraznia jest nieograniczona i czerpie swe spostrzezenia z trojwymiaru. Rzadko, kiedy cos interpretuje. Nie ocenia, nie krytykuje, nie wybiela. Jest obserwatorem i dzieli sie tym ze mna, co zobaczy, a widzi wiele. Ma lekkie pioro. Informacje sa poparte konkretnymi faktami, wydarzeniami. Dlatego taka powinna byc prasa i telewizja.</p>
<p class="Standard" style="text-align: justify;">Nie wyobrazam sobie publikacji i informacji bez gazet. Papier jest czyms wyjatkowym</p>
<p class="Standard" style="text-align: justify;">Jestem nieprzychylnie nastawiona do serwisow spolecznosciowych typu Facebook. Dla mnie zagladanie tam to strata czasu. Nie mam tam, czego szukac.</p>
<p class="Standard" style="text-align: justify;">Jestem bardzo za prawdziwymi redakcjami z zespolem dziennikarzy. Praca w zespole jest bardziej owocna.</p>
<p class="Standard" style="text-align: justify;"><strong>Charli</strong></p>
