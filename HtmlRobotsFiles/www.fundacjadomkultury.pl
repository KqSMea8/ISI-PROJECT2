<p><img class="aligncenter wp-image-2929 size-large" src="http://www.fundacjadomkultury.pl/wp-content/uploads/2018/09/Logo-sztuki-2-proste-e1537444134135-1024x355.jpg" alt="" width="640" height="222" srcset="http://www.fundacjadomkultury.pl/wp-content/uploads/2018/09/Logo-sztuki-2-proste-e1537444134135-1024x355.jpg 1024w, http://www.fundacjadomkultury.pl/wp-content/uploads/2018/09/Logo-sztuki-2-proste-e1537444134135-300x104.jpg 300w, http://www.fundacjadomkultury.pl/wp-content/uploads/2018/09/Logo-sztuki-2-proste-e1537444134135-768x266.jpg 768w" sizes="(max-width: 640px) 100vw, 640px" /></p>
<p style="text-align: justify;">Zapraszamy na warsztaty tworcze i wystawe prac dziewczat i chlopcow z warszawskich Mlodziezowych Osrodkow Wychowawczych wspoltworzonych razem z artystami: , Paulina Jedrzejewska, Malgorzata Karkosz, Jankiem Mioduchowskim, Anna Siekierska, Zosia Szulc.</p>
<p style="text-align: justify;">Piec technik artystycznych, pietnascie warsztatow, trzy mlodziezowe grupy.</p>
<p style="text-align: justify;">Rozne sposoby ekspresji i wiele mozliwosci powiedzenia czegos o sobie. Taka konfiguracja to przede wszystkim proces, ktory wystawa pragnie podsumowac. Spotkania mlodziezy z artystkami i artystami to momenty szczegolnej wymiany. Profesjonalni tworcy dostarczali pewna rame, prezentujac zasady technik, a uczestnicy warsztatow wypelniali rame trescia - swoja osobowoscia, codziennoscia, zainteresowaniami czy poczuciem humoru. W tym sensie sztuka przestaje byc domena natchnionych i wybitnych jednostek. Sztuka jest narzedziem dla kazdego - by mowic o sobie i o tym kim sie jest. Sztuki sa proste - jesli tylko zechcemy sie z nimi spotkac.</p>
<p>,,Sztuki proste" warsztaty, wystawa</p>
<p>13 grudnia 2018 r.</p>
<p>godz. 11.00</p>
<p>Muzeum Warszawskiej Pragi  Targowa 50/52</p>
<p>sala: Penthouse</p>
<p>wstep wolny</p>
<p>INFO: 608 852 830</p>
