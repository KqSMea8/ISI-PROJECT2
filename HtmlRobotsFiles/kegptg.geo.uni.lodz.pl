<p class="intro">Szanowni Panstwo, Nasi Drodzy Goscie,</p>
<p>Witamy na stronie Komisji Edukacji Geograficznej Polskiego Towarzystwa Geograficznego, towarzystwa naukowego, ktore w roku 2018 bedzie obchodzic swoje 100-lecie.</p>
<p>Zapraszamy do sledzenia rozmaitych dzialan naszej Komisji, jej roznorodnych prac majacych na celu doskonalenie ksztalcenia geograficznego realizowanego na roznych etapach edukacji. Prosimy zapoznac sie z tematyka organizowanych przez nas konferencji naukowych oraz publikacjami z zakresu dydaktyki geografii. Zachecamy rowniez do zapoznania sie z historia Polskiego Towarzystwa Geograficznego
<a href="http://www.ptgeo.org.pl/towarzystwo/historia/">www.ptgeo.org.pl/towarzystwo/historia/</a></li> oraz historia Komisji Edukacji Geograficznej PTG: z ubogacajacym nas dziedzictwem, o ktorym nie zapominamy oraz z postaciami, ktore tworzyly i nadal tworza polska dydaktyke geografii.</p>
<p>Komisja Edukacji Geograficznej PTG ma za zadanie inicjowanie dzialan oraz koordynowanie prac w zakresie szeroko rozumianej edukacji geograficznej oraz dydaktyki geografii. Jednym z jej glownych celow jest dbanie o dobra jakosc i stosowna range szkolnej edukacji geograficznej, od ktorej zalezy dobre wyksztalcenie geograficzne mlodego pokolenia.</p>
<p>Naszym obecnym wyzwaniem i zadaniem jest dbalosc o to, by geografia jako przedmiot szkolny wrocila po latach na nalezna jej wysoka pozycje w ksztalceniu ogolnym z racji jej wielu roznorodnych wartosci edukacyjnych, w tym wychowawczych.</p>
<p>Zapraszamy nie tylko do odwiedzin naszej strony, ale do kontaktu z nami. Liczymy na to, ze wraz z Panstwem - z szerokim gronem naukowcow - dydaktykow geografii, z nauczycielami geografii, doradcami metodycznymi, z przyjaciolmi i sympatykami, uda nam sie zrealizowac postawione cele, a nastepnie wyznaczyc cele nowe i isc dalej, i siegac wyzej...</p>				<p class="signature">Elzbieta Szkurlat<br><small>Przewodniczaca Komisji Edukacji Geograficznej PTG</small></p>			</article>
