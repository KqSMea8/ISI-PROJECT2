<h3>o pizzerii <img src="/assets/img/logo_black.png"></h3>
</header>
<article>
<p>
Pizzeria Adwokacka powstala w czerwcu 1996 roku jako kawiarnio-restauracja. Sama geneza nazwy Adwokacka wywodzi sie z faktu, iz w bezposrednim sasiedztwie naszego lokalu znajdowaly sie dwie kancelarie adwokackie.<br><br>
Po roku dzialalnosci rozszerzylismy zakres uslug gastronomicznych o przepyszna Pizze. Z biegiem czasu i zapotrzebowaniem gosci Adwokackiej, jako pierwsi w Chodziezy, poszerzylismy menu serwowanych potraw o kebab i szybko podawane potrawy, ktore pozwalaja naszym gosciom na szybkie przekaszenie smakowitych dan - bez koniecznosci dlugiego oczekiwania na ich przygotowanie.<br><br>
Na przestrzeni ostatnich 20 lat Adwokacka rozwinela swoje skrzydla jako najbardziej popularny lokal gastronomiczny w Chodziezy i okolicach.<br><br>
Z dniem 1 stycznia 2013 roku Adwokacka zmienila swoja lokalizacje na ul. Wiosny Ludow 9A. Nowy lokal, to zupelnie nowe mozliwosci rozwoju, dzieki ktoremu mozemy dac Wam wiecej.<br><br>
Organizujemy male imprezy okolicznosciowe dla firm i osob prywatnych. Z powodzeniem jestesmy w stanie przygotowac dla Panstwa impreze zasiadana dla 30 osob. Duzym powodzeniem ciesza sie uroczyste obiady komunijne i kameralne przyjecia weselne.<br><br>
Dzieki zdobytemu lat doswiadczeniu obslugujemy naszych klientow rowniez w formie cateringow wyjazdowych. Jednorazowo mozemy przygotowac potrawy dla 100 osob, co w polaczeniu z zawsze swiezymi produktami i profesjonalna obsluga pozwala na stworzenie zawsze milej i smacznej atmosfery na organizowanej przez Ciebie imprezie.<br><br>
Zapraszamy do Adwokackiej - miejsca w ktorym zjesz przepyszne dania w oddali od zgielku codziennosci.
</p>
