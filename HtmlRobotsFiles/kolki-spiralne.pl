<h1>Rodzaje srub</h2>
<p>Obecnie w przemysle budowniczym stosuje sie wiele roznych rodzajow srub.<img src='images/0.jpg'  class='al' /> Patrzac na ksztalt, dokladnosc wykonania i przeznaczenie rozroznia sie takie typy: sruby zgrubne, sruby klasy srednio dokladnej, sruby pasowane, sruby do polaczen sprezonych. W budownictwie uzywa sie takze sruby o specjalnym przeznaczeniu, takie jak np.: sruby fundamentowe, sruby rzymskie, sruby hakowe i mlotkowe, sruby dwustronne i sruby rozporowe czyli kolki. Sruby podzielono rowniez na klasy w zaleznosci na wlasciwosci mechaniczne stali, z ktorych sa wykonane. W zaleznosci od wymaganej wytrzymalosci na rozciaganie i od wymaganej granicy plastycznosci w stalowych konstrukcjach budowlanych stosuje sie az 10 klas srub. Klasy srub oznacza sie symbolem skladajacym sie z dwoch liczb przedzielonych kropka. Ostatnio w modzie sa stosowane sruby zabezpieczajace, uzywane np. do przymocowania kol samochodu. Istnieja takze nietypowe sruby, jak chocby te z tzw. lewym gwintem, ktore wkreca sie i wykreca w odwrotnym kierunku niz wiekszosc innych.</p>
