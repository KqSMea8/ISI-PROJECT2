<header class="entry-header"><h1 class="entry-title">Firma</h1></header><!-- .entry-header -->
<div class="entry-content">
<p>Od 2006 roku rozpoczelismy wspolprace z firmami na wschodzie Europy w zakresie obrotu czesciami dla przemyslu lotniczego. Sprowadzamy podzespoly na rynek Polski jak rowniez dla kontrahentow z Afryki, Ameryki Poludniowej, Dalekiego Wschodu. Oferujemy czesci i podzespoly do samolotow Antonov, Tupolev, Iliuszyn, helikopterow transportowych MI-2, MI-6, MI-8, Mi-17, Mi-14, MI-171.    Wspolpracujac z nami macie Panstwo pewnosc otrzymania wyrobow spelniajacych Wasze wymagania, fachowe porady i zawsze mila obsluge. Wieloletnie zaufanie i zadowolenie naszych dotychczasowych klientow pozwala nam zaoferowac wspolprace na najwyzszym poziomie.                                                       Rowniez prowadzimy sprzedaz maszyn do przetworstwa tworzyw sztucznych takich jak wtryskarki, extrudery, mlyny, linie do granulacji.  Prowadzimy serwis i sprzedaz czesci dla tej branzy.</p>
<p style="text-align: center;"><span style="text-decoration: underline;"><em>Posiadamy koncesje na obrot wyrobami o przeznaczeniu wojskowym i policyjnym</em></span></p>
