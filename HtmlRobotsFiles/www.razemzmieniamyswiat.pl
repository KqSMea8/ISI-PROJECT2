<p><span class="bold">Nazwa fundacji TZMO - Razem Zmieniamy Swiat</span> - jest ilustracja naszego spojrzenia na dzialalnosc spoleczna.
Wszystko, co osiagnelismy, zawdzieczamy scislej i wieloletniej wspolpracy ze srodowiskiem profesjonalistow.
To ludzie, ktorzy kazdego dnia, z pasja i zaangazowaniem ksztaltuja jakosc opieki nad przewlekle chorymi.</p>
<p>Jestesmy przekonani, ze <span class="bold">swiat da sie zmienic</span>, jesli tylko zechcemy otworzyc sie na wspolne poszukiwanie rozwiazan. Dlatego tak wazna dla naszej dzialalnosci jest <span class="bold">edukacja</span> i jeden z kluczowych jej wymiarow
-<span class="bold"> budowanie wiedzy poprzez wymiane doswiadczen.</span></p>

