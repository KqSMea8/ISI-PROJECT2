<h2 class="text-center top-space">Czesto zadawane pytania</h2>
<br>

<div class="row">
<div class="col-sm-6">
<h3>Co to jest numer IBAN?</h3>
<p>IBAN (ang. International Bank Account Number - Miedzynarodowy Numer Rachunku Bankowego) to miedzynarodowy standard numeracji kont bankowych. Zostal stworzony aby wyeliminowac problemy zwiazane z wystepowaniem roznych standardow bankowosci w krajach Unii Europejskiej. Wiecej informacji mozesz znalezc w Bazie wiedzy.</p>
</div>
<div class="col-sm-6">
<h3>Co to jest kod SWIFT?</h3>
<p>
Kod SWIFT to zwyczajowo przyjeta w Polsce nazwa nadanego przez organizacje SWIFT kodu BIC (ang. Bank Identifier Code) sluzacego do identyfikacji bankow i innych instytucji finansowych podczas realizacji przelewow miedzynarodowych. Sprawdz takze informacje na temat kodu SWIFT w naszej bazie wiedzy. </p>
</div>
</div> <!-- /row -->

<div class="row">
<div class="col-sm-6">
<h3>Jak sprawdzic do kogo nalezy konto bankowe?</h3>
<p>
Niestety, ale nie ma technicznej mozliwosci aby uzyskac na 100% pewna informacje do kogo nalezy rachunek. Banki nie udostepniaja danych wlascicieli kont, a takze nie weryfikuja poprawnosci tych danych podczas wykonywania przelewow. Jedyna mozliwosc to weryfikacja numerow kont oraz uczciwosci wlascicieli dzieki komentarzom innych uzytkownikow. Jednak nalezy pamietac ze taka weryfikacja nie daje 100% pewnosci.
</p>
</div>
<div class="col-sm-6">
<h3>Co to jest suma kontrolna numeru IBAN i jak sie ja liczy?</h3>
<p>Suma kontrolna sluzy do sprawdzenia poprawnosci numeru IBAN. Znajduje sie ona na poczatku numeru IBAN, zaraz po dwuliterowym kodzie kraju. Jezeli chcesz poznac wiecej szczegolow zapraszamy do Bazy Wiedzy.</p>
</div>
</div> <!-- /row -->

