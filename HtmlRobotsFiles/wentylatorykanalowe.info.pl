<div class="entry-content">
<p>Wentylatory kanalowe przemyslowe osiowe</p>
<p><a title="Blog o wentylatorach" href="http://wentylatoryinfo.wordpress.com/">Wentylatory</a> kanalowe do warunkow przemyslowych powinny charakteryzowac wytrzymaloscia przy ciaglej pracy .Powinny trze byc odporne na warunki przemyslowe (zapylenie , wilgoc itp.) , czyli silniki montowane w tych wentylatorach powinny miec odpowiednie IP co najmniej IP54 . Ponadto wentylatory kanalowe przemyslowe powinny miec mozliwosc regulacji wydajnosci (obrotow silnika ) .W przypadku wentylatorow trojfazowych powinny byc one przystosowane do pracy z falownikami natomiast w przypadku wentylatorow  jednofazowych powinny byc przygotowane do wspolpracy z regulatorami silnikow jednofazowych .   Jezeli nie maja takich mozliwosci to oznacza ze neizalenie od warunkow panujacych w pomieszczeniu zawsze beda pracowaly z najwieksza wydajnoscia . Przy wyborze takiego wentylatora kanalowego nalezy zwrocic uwage na jego srednica oraz parametry wydajnosciowe  i cisnieniowe . Czesto bowiem kupujacy patrza tylko na wydajnosc i kupuja <a href="http://www.wentylatory.info.pl">wentylator</a> ktory ma faktycznie duza wydajnosc ale przy stosunkowo malym sprezu .Oznacza to ze wentylator przezuci  przez okno lub sciane  okreslona ilosc powietrza  ale przy kanale lub filtrze nie da sobie rady bo bedzie mial za maly sprez . W przypadku wentylatorow kanalowych osiowych wentylatory z wiekszym sprezem maja zazwyczaj wieksze obroty 2800 obr/min lub  sa robione jako tak zwane przeciwbiezne z dwoma silnika i z dwoma wirnikami w jednej obudowie .</p>
