<p>Godne uwagi wentylatory kanalowe FKO firmy FERONO oraz CF firmy ELPLAST.</p>
<p>Dzisiejszy wpis poswiecimy wentylatorom, ktore naszym zdanie wyrozniaja sie na tle konkurencji i oferuja bardzo dobre paramtetry uzytkowania. Beda to wentylatory kanalowe osiowe firmy FERONO seria FKO oraz wentylatory promieniowe firmy ELPLAST seria CF.</p>
<p>Pierwszymi z nich sa wentylatory kanalowe serii FKO. Seria FKO charakteryzuje sie przed wszystkim aerodynamicznym wyprofilowaniem lopatek wirnika w taki sposob, ze wentylatory te maja wysoka skutecznosc przeplywu powietrza przy jednoczesnie minimalnym halasie, nawet przy maksymalnej predkosci obrotowej. Sercem wentylatora jest bezszczotkowy, nowoczesny silnik przystosowany do regulacji napieciowej.Posiada wbudowane termiczne zabezpieczenie, realizowane poprzez czujnik temperatury uzwojen. Bardzo wysoka klasa izolacji uzwojen silnika F oraz klasa szczelnosci IP55 gwarantuja bezproblemowa prace. Silnik wykonany zostal z metalu. Warto dodac tutaj, ze silnik jest oparty na podwojnych lozyskach zapewniajacych wydluzona zywotnosc. Temperatura pracy ma bardzo wysoki zakres od -20 do 80 stopni Celsjusza, natomiast zakres wilgotnosci wynosi od 20 do 95 %. Wentylator posiada klase ochrony IP 55.</p>
<p>Drugim godnym uwagi produktem jest wentylator promieniowy firmy EL-PLAST seria CF. Wentylatory te w przeciwienstwie do wentylatorow FKO sa wykonane z bardzo trwalego kompozytu i charakteryzuja sie niskim poborem mocy przy bardzo dobrej wydajnosci. Predkosc obrotowa wynoszaca 2600 obr/min pozwala na bardzo sprawne zasysanie</p>
