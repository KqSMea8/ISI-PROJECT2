<div class="entry-content">
<p>Wentylatory kominowe - co to jest ? </p>
<p>Wentylatory kominowe (montowane do kominow wentylacyjnych nie spalinowych ) mozna takze podciagnac pod wentylatory kanalowe . Wentylatory tego typu najczesciej sa  instalowane w kominach wentylacyjnych obiektow inwentarskich , obiektow przemyslowych , magazynow itp. .<br />
Ze wzgledu na specyficzne warunki pracy (zazwyczaj wilgoc , zapylenie , opary )  wentylatory tego typu musza charakteryzowac sie silnikami z dobra izolacja odpornymi na te warunki pracy i mycie .<br />
Konstrukcja takiego wentylatora jest bardzo prosta zazwyczaj sklada sie tego typu wentylator z silnika , wirnika z tworzywa sztucznego i trzymakow ktorymi przykreca sie wentylator wewnatrz komina . Silniki w tego typu wentylatorach powinny byc przystosowane do regulacji obrotow i wspolpracy ze sterownikami mikroklimatu . Wentylatory kominowe zazwyczaj wykonywane sa w srednicach typ[owych dla kanalow wentylacyjnych czyli 315 mm , 355 mm , 400 mm , 500 mm ,560 mm oraz 630 mm . Zasilanie jest z sieci 230 V lub 400 V w zaleznosci od potrzeb i warunkow w obiekcie . Ceny tego typu wentylatorow oscyluja w granicach 500 -1000 zl netto w zaleznosci od srednicy i wydajnosci . Wentylatory kominowe do nabycia w sklepie z wentylatorami www.wentylatorysklep.pl </p>
