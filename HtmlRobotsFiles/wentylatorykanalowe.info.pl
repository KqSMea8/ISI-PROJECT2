<h2 class="entry-title"><a href="https://wentylatorykanalowe.info.pl/?p=77" rel="bookmark">Czujnik cisnienia IFM PN2094</a></h2>

<div class="entry-meta">
<span class="meta-prep meta-prep-author">Opublikowano</span> <a href="https://wentylatorykanalowe.info.pl/?p=77" title="12:49" rel="bookmark"><span class="entry-date">23 stycznia 2018</span></a>, <span class="meta-sep">autor:</span> <span class="author vcard"><a class="url fn n" href="https://wentylatorykanalowe.info.pl/?author=1" title="Zobacz wszystkie wpisy, ktorych autorem jest admin">admin</a></span>			</div><!-- .entry-meta -->

<div class="entry-content">
<p>Czujnik cisnienia IFM PN2094</p>
<p>W ukladach wentylacji  automatyki hydrauliki badz pneumatyki, czesto zachodzi koniecznosc pomiaru i monitorowania cisnienia cieczy roboczej. Przetworniki cisnienia firmy IFM oferuja dokladny pomiar cisnienia. Zbudowane sa z mysla o trudnych warunkach przemyslowych przez co mozemy cieszyc sie ich niezawodnoscia. Wbudowany dwukolorowy wyswietlacz pozwala na biezaco monitorowac mierzone cisnienie. Istnieje mozliwosc ustawienia innego koloru wyswietlania dla roznych zakresow cisnien, dzieki czemu mozemy zauwazyc ze cos nie tak dzieje sie z ukladem hydraulicznym badz pneumatycznym.</p>
<p>&nbsp;</p>
<p>Przetwornik posiada przyciski na obudowie dzieki ktorym istnieje mozliwosc zmiany parametrow badz ustawienie rodzaju wyjscia. Przetwornik oferuje 2 rodzaje wyjsc: przelaczajace (normalnie otwarty i normalnie zamkniety) i analogowy ( wyjscie pradowe 4-20 mA lub napieciowe 0-10 V) Czujnik pracuje w temperaturze od -25 do +80 *C przez co znajdzie zastosowanie w wiekszosci ukladach. Opisywany model oferuje pomiar medium do wartosci cisnienia 75 bar, a cisnienie niszczace wynosi 150 bar.</p>
<p>&nbsp;</p>
<p>Przetwornik cisnienia IFM PN2094 oferuje :</p>
<ul>
<li>Programowalne wyjscie przelaczajace z IO-Link i skalowalne wyjscie analogowe</li>
<li>Czerwono/zielony wyswietlacz do jasnego wskazywania dopuszczalnego zakresu</li>
<li>Mozliwosc obracania przylacza procesowego w celu optymalnego wyosiowania</li>
<li>Dlugoterminowa stabilnosc dzieki skutecznemu zabezpieczeniu przed przeciazeniem</li>
<li>Wytrzymala konstrukcja do zastosowan w trudnych warunkach przemyslowych</li>
</ul>
