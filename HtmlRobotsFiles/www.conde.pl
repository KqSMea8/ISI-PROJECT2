<h2>pracownia</h2>
<h4>misja</h4>
<p>Nasza pasja jest projektowanie prostych, funkcjonalnych i atrakcyjnych budynkow doskonale<br />wpisujacych sie w otaczajaca ich przestrzen.<br /> Skupiamy sie nie tylko na architekturze zewnetrznej samego obiektu lecz takze na planowaniu<br /> wnetrz oraz terenu wokol.<br />Dbamy o kazdy detal zarowno na etapie projektowym jak i wykonawczym w ramach nadzorow<br /> autorskich. Celem jest perfekcyjna realizacja zgodna z zamierzeniem inwestora i zamyslem<br />architekta</p>
<h4>biuro</h4>
<p>Pracownia projektowa powstala w 2001r. Firma zapewnia kompleksowa obsluge inwestycji<br /> komercyjnych oraz klientow indywidualnych.<br />Nasze biuro specjalizuje sie w architekturze budynkow biurowo-uslugowych, mieszkalnych<br />wielorodzinnych, jednorodzinnych oraz w projektach wnetrz.</p>
<h4>zespol</h4>
<p>W sklad zespolu projektowego wchodza specjalisci branzy architektonicznej, konstrukcyjnej,<br />instalacji sanitarnych i wentylacji mechanicznej, instalacji elektrycznych, branzy drogowej oraz<br /> kosztorysanci. Dlugie doswiadczenie zawodowe oraz wszechstronna wiedza zdobyta w trakcie<br /> realizacji inwestycji na terenie calego kraju gwarantuje najwyzsza jakosc<br />swiadczonych uslug.<br />Niezaleznie w zakresie spraw formalno-prawnych Pracownia wspolpracuje z renomowana<br /> Kancelaria Prawna.</p>
<h4>architekt prowadzacy</h4>
<p>Konrad Idaszewski, mgr. inz.<br />Absolwent Wydzialu Architektury Politechniki Poznanskiej, czlonek Izby Architektow.<br />Doswiadczenie zdobyl w trakcie pracy na stanowisku asystenta architekta w poznanskich<br />pracowniach projektowych oraz w ramach kontraktu w Irlandzkim biurze projektowym.<br />Laureat konkursow architektonicznych. Od 2001r. prowadzi wlasna pracownie<br />projektowa.</p>
