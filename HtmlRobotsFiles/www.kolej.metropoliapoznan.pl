<b>Docieraja do nas sygnaly, najczesciej od mieszkancow gminy Czerwonak polozonej wzdluz linii kolejowej Poznan - Wagrowiec, ze jadac rano do Poznania, maja problem aby wsiasc do pociagu, taki panuje tam scisk. Jak Pan mysli, kiedy ci pasazerowie moga liczyc na poprawe sytuacji?</b><br />
