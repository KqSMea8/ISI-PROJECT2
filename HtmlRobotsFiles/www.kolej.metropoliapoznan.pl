<div id="w2027d" style=" display: none;padding: 10px;">
<b>W ubieglym roku pociagi nalezace do spolki Koleje Wielkopolskie przewiozly ok. 11 mln pasazerow, czyli o ponad 2 mln osob wiecej niz w roku 2017. Bezposredni wplyw na wzrastajaca popularnosc pasazerskiego transportu kolejowego mialo uruchomienie polaczen w ramach systemu Poznanskiej Kolei Metropolitalnej. </b><br />
<br />
Kolejnego wzrostu liczby pasazerow mozna sie spodziewac w tym roku, poniewaz na wrzesien planowane jest wlaczenie do systemu PKM polaczen kolejowych na linii Poznan - Gniezno i na przedluzeniu linii, ktora obecnie konczy sie w Swarzedzu, do Kostrzyna.<br />
16 kwietnia w siedzibie Urzedu Marszalkowskiego Wojewodztwa Wielkopolskiego podpisane zostaly porozumienia ,,o podjeciu wspolnych dzialan zmierzajacych do uruchomienia Poznanskiej Kolei Metropolitalnej". Dokumenty podpisali przedstawiciele Wojewodztwa Wielkopolskiego oraz gmin i powiatow lezacych na trasie linii kolejowej <br />
z Poznania do Gniezna i do Kostrzyna. Porozumienia zawieraja deklaracje udzialu samorzadow we wspolfinansowaniu PKM. - Dzieki wspolnemu, solidarnemu podejsciu do finansowania dodatkowych polaczen mozliwe jest zwiekszenie czestotliwosci kursowania pociagow ku zadowoleniu pasazerow. To zadowolenie jest tak duze, ze na wielu odcinkach mamy klopoty, aby obsluzyc zwiekszajace sie potoki podroznych. Samorzad wojewodztwa czeka na dostarczenie 15 sztuk nowoczesnych pociagow elektrycznych, a Koleje Wielkopolskie koncza zamowienie na zakup 4. nowych pociagow spalinowych - informowal Wojciech Jankowiak, Wicemarszalek Wojewodztwa Wielkopolskiego.<br />
Podpisanie dokumentow nie rodzi zadnych konsekwencji finansowych. Teraz porozumienia beda glosowane przez rady poszczegolnych samorzadow. Po ich akceptacji podpisane zostana umowy. Nastepnym etapem bedzie juz uruchomienie polaczen kolejowych w systemie PKM na wymienionych liniach. <br />
W czerwcu ubieglego roku rozpoczela funkcjonowanie Poznanska Kolej Metropolitalna. Pociagi z logo PKM wyruszyly na cztery linie. Z Poznania w kierunku Grodziska Wielkopolskiego, Jarocina, Nowego Tomysla i Wagrowca. We wrzesniu w system wlaczono polaczenia do Swarzedza. Podstawa funkcjonowania polaczen sa umowy podpisane pomiedzy Wojewodztwem Wielkopolskim a samorzadami (gminy i powiaty) polozonymi na trasach przebiegu PKM. Przed umowami podpisane zostaly porozumienia. Porozumienia zaakceptowaly wszystkie rady samorzadow. <br />
Poznanska Kolej Metropolitalna to wspolne przedsiewziecie Marszalka Wojewodztwa Wielkopolskiego, Stowarzyszenia Metropolia Poznan, Miasta Poznan, Powiatu Poznanskiego oraz 39 gmin i 10 powiatow polozonych wzdluz 9 linii kolejowych Poznanskiego Wezla Kolejowego, oparte na zasadzie solidaryzmu. <br />
Funkcjonowanie PKM obejmie obszar w promieniu okolo 50 km od Poznania, wyznaczany przez stacje koncowe PKM na poszczegolnych liniach: Nowy Tomysl, Wagrowiec, Grodzisk Wlkp., Jarocin, Wronki, Rogozno, Koscian, Gniezno i Wrzesnia.<br />
Cele PKM to przede wszystkim zachecenie mieszkancow Metropolii do korzystania ze zbiorowego transportu publicznego oraz zmniejszenie natezenia indywidualnego ruchu samochodowego w obrebie Miasta Poznania. - Tworzac strategie dla powiatu poznanskiego zapytalismy mieszkancow, co jest dla nich najwazniejsze. Zdecydowanie  odpowiedzieli, ze komunikacja publiczna. Staramy sie zaspokoic ich oczekiwania. Zdajemy sobie sprawe z tego, jak wazne dla mieszkancow sa sprawne polaczenia kolejowe. Wiemy ile samochodow wjezdza do powiatu, to sa bardzo duze liczby. Projekt pod nazwa Poznanska Kolej Metropolitalna ma szanse na zmiane tej sytuacji - powiedzial Tomasz Lubinski, Wicestarosta Powiatu Poznanskiego.<br />

