<br><br>
<div id="kolej" style="border: 1px solid #CCCCCC; background-color: #F8F8F8; padding: 10px; line-height: 26px;">
Poznanska Kolej Metropolitalna (PKM) to jedno z najwiekszych tego typu przedsiewziec w Polsce. <b>Pierwsze cztery linie polacza Grodzisk Wielkopolski, Jarocin, Nowy Tomysl i Wagrowiec z Poznaniem.</b> Docelowo w stolicy Wielkopolski pociagi PKM beda zatrzymywac sie na 13 stacjach i przystankach. Odetchna miejskie i gminne ulice. <b>Kursy w godzinach szczytu beda odbywac sie nawet co 30 minut</b>, ulatwiajac droge do szkoly, pracy lub w innych celach.
<br><br>
To pierwsze wspolne tak duze przedsiewziecie samorzadow. Zasieg Poznanskiej Kolei Metropolitalnej bedzie coraz wiekszy, aby usprawniac ruch nie tylko w samej aglomeracji. <b>Do 2021 roku aktywowane beda polaczenia z Gniezna, Kosciana, Rogozna, Wronek i Wrzesni</b>.
</div>
<div id="inwestycja" style="display:none;border: 1px solid #CCCCCC; background-color: #F8F8F8; padding: 10px; line-height: 26px;">
Dziesiatki stacji w pierwszym etapie projektu w ciagu kilku lat podwoja swoja liczbe. Mozliwa stanie sie takze reaktywacja zapomnianych polaczen. Rozpoczely sie prace nad przywroceniem linii Srem-Czempin w wariancie z jej elektryfikacja. Podroz z Poznania do Sremu zajmie polowe czasu, jaki na pokonanie tego dystansu potrzebuje samochod.
<br><br>
Porownanie uruchamianego polaczenia PKM na trasie Pobiedziska-Poznan z jej pokonaniem samochodem wypadlo na korzysc transportu szynowego. W ciagu miesiaca mozna oszczedzic 22 godziny i ponad 100 zlotych. Prosta kalkulacja porownuje wydatek na bilet Bus-Tramwaj-Kolej z zakupem paliwa. A to przeciez nie jedyne koszty utrzymania wlasnego pojazdu.
<br><br>
<b>Zintegrowany bilet Bus-Tramwaj-Kolej</b> funkcjonuje od 2012 roku. Od wrzesnia 2017 roku strefe jego dzialania rozszerzono na obszar ok. 50 kilometrow. Pokrywa sie on z siatka linii kolejowych PKM. Dodatkowe informacje o bilecie dostepne sa na stronie <a href="http://bustramwajkolej.pl" target="_blank" class="l">bustramwajkolej.pl</a>.
