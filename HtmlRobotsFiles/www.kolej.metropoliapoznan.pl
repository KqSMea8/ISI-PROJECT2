<i class="fa fa-long-arrow-right fa-2x" aria-hidden="true"></i>&nbsp;<i class="fa fa-star-o fa-2x" aria-hidden="true"></i>&nbsp;<i class="fa fa-long-arrow-left fa-2x" aria-hidden="true"></i>
