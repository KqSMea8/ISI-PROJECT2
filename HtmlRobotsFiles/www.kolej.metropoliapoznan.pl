Z przeprowadzonych na zlecenie Stowarzyszenia Metropolia Poznan ekspertyz wynika, ze pod kazdym wzgledem - tak spolecznym, jak gospodarczym rewitalizacja linii ma silne uzasadnienie. - Z wielokierunkowych analiz wynika, ze modernizacja tej linii jest jak najbardziej wskazana. Potencjal pasazerski po przywroceniu polaczenia Srem - Czempin to ok. 7 tys. osob w ciagu doby, ktore przewozilyby pociagi w 24 kursach, po 12 kursow w kazda ze stron. Uwazam, ze prace nalezy kontynuowac. Pierwszy etap mamy za soba, postawmy kolejny krok. Projekt, mimo roznych trudnosci infrastrukturalnych i technicznych jest mozliwy do zrealizowania - twierdzi Wojciech Jankowiak, Wicemarszalek Zarzadu Wojewodztwa Wielkopolskiego.   <br />
