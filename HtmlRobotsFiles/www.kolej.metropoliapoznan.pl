14 lutego 2019 r. w siedzibie Urzedu Marszalkowskiego Wojewodztwa Wielkopolskiego w Poznaniu Wicemarszalek Wojciech Jankowiak podpisal umowe pomiedzy Wojewodztwem Wielkopolskim a konsorcjum Pojazdy Szynowe PESA Bydgoszcz SA i Zaklady Naprawcze Taboru Kolejowego ZNTK Minsk Mazowiecki. Przedmiotem umowy jest dostawa 5 nowych, piecioczlonowych elektrycznych zespolow trakcyjnych (ezt) serii ,,Elf 2" do wykonywania aglomeracyjnych kolejowych przewozow pasazerskich na obszarze Poznanskiej Kolei Metropolitarnej. Oferte PESY uznano za najkorzystniejsza sposrod trzech zlozonych ofert (oferty zlozyli takze NEWAG Nowy Sacz i Stadler Polska). Inwestycja o wartosci 127,45 mln zl (w tym dofinansowanie z UE w wysokosci 48,75 mln zl) zrealizowana zostanie w okresie czerwiec-grudzien 2020 r. W ramach umowy przewidziano takze przeprowadzenie instruktazy technicznych, udzielenie 60. miesiecznej gwarancji, opracowanie dokumentacji technicznej i udzielenie licencji na dokumentacje techniczna i oprogramowanie.<br />
Tabor bedzie dostosowany do obslugi ruchu aglomeracyjnego (2 pary drzwi na czlon) z predkoscia 160 km/h. Dla kazdego pojazdu przewidziano m.in. min. 240 miejsc siedzacych, 5 automatow biletowych, dwie toalety w ukladzie zamknietym, klimatyzacje przestrzeni pasazerskiej, system Wi-Fi, system dynamicznej informacji pasazerskiej, monitoring, miejsca dla niepelnosprawnych i do przewozu rowerow, petle indukcyjne dla osob slaboslyszacych, automatyczne odswiezacze powietrza, defibrylatory. Ponadto pojazdy beda przystosowane do zainstalowania systemu Poznanskiej Karty Aglomeracyjnej (PEKA) na pokladzie.<br />
Nowe pojazdy beda przeznaczone do obslugi polaczen w ramach Poznanskiej Kolei Metropolitalnej na linii Opalenica-Poznan-Wrzesnia, lecz beda mogly byc wykorzystywane rowniez na innych liniach kolejowych - do Gniezna, Czempinia, Wronek i do Rogozna.<br />
Wojewodztwo Wielkopolskie po zrealizowaniu przedmiotu umowy bedzie dysponowalo 71 jednostkami taborowymi, w tym 49 elektrycznymi zespolami trakcyjnymi. Wojewodztwo Wielkopolskie bedzie moglo skorzystac takze z prawa opcji obejmujacej zakup dodatkowych dwoch ezt.<br />
Zakup pojazdow wspolfinansowany jest ze srodkow Unii Europejskich w ramach Programu Operacyjnego Infrastruktura i Srodowisko na lata 2014-2020 (dzialanie 5.2 Rozwoj transportu kolejowego poza TEN-T).<br />

