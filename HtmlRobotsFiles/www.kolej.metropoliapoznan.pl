<div style="color: #FFFFFF; background-color: #035890;line-height: 20px; padding: 10px;">16 kwietnia 2019</div>
<div style="color: #000000; background-color: #FFFFFF; font-weight: bold;line-height: 28px; padding: 10px;">Kolejne polaczenia w systemie PKM</div>
<a href="#/"  onclick="openModal('data/aktualnosci/2027/b/DSC02789.JPG', 'Kolejne polaczenia w systemie PKM<br>Foto: materialy wlasne');"><img src="data/aktualnosci/2027/b/DSC02789.JPG" alt="" class="img-responsive pointer"></a>
<div style="color: #999999; padding: 10px;">Foto: materialy wlasne</div>
<div id="w2027m" style="padding: 10px;">
<b>W ubieglym roku pociagi nalezace do spolki Koleje Wielkopolskie przewiozly ok. 11 mln pasazerow, czyli o ponad 2 mln osob wiecej niz w roku 2017. Bezposredni wplyw na wzrastajaca popularnosc pasazerskiego transportu kolejowego mialo uruchomienie polaczen w ramach systemu Poznanskiej Kolei Metropolitalnej. </b><br />
