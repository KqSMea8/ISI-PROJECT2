Wojciech Jankowiak: Rzeczywiscie wydarzenie bylo bardzo wazne, wrecz przelomowe. Dzieki PKM zdecydowanie wzrosly komfort i tempo podrozowania mieszkancow metropolii. Kolej Metropolitalna jest projektem, ktory bedziemy w dalszym ciagu rozwijac, aby objela linie odchodzace z Poznania takze w innych kierunkach. Przy okazji sukcesu zwiazanego z uruchomieniem PKM nie zabraklo tez pewnych zaskoczen. Zwiazane sa one z tym, ze u niektorych kolegow samorzadowcow wystapily objawy nieufnosci co do decyzji o wspolfinansowaniu projektu. Uwazam, ze przyczyna takiego podejscia mogla byc zbyt mala liczba spotkan, dyskusji. W trakcie przygotowywania projektu mielismy rowniez watpliwosci zwiazane z naszymi mozliwosciami taborowymi, gdyz oczywiste jest, ze zwiekszenie liczby polaczen pociaga za soba wieksze potrzeby taborowe i naklady organizacyjne. O ile na pierwszym etapie kolej wystartowala bez perturbacji, z duzym zaangazowaniem przewoznika - Kolei Wielkopolskich, o tyle dzis braki w zakresie taboru kolejowego zaczynaja dawac o sobie znac. A wiec z jednej strony osiagnelismy cele, do ktorych dazylismy, czyli przyczynilismy sie do znacznego zwiekszenia liczby przewozonych pasazerow i wzrostu zainteresowania koleja metropolitalna jako forma transportu zbiorowego, ale z drugiej strony zaczyna brakowac nam taboru, aby te potrzeby zaspokoic. Jest to w tej chwili problem, ktory wymaga pilnego rozwiazania i rozwiazany zostanie. Jednak pomijajac te kwestie i podsumowujac pierwszy okres funkcjonowania PKM, mozna stwierdzic, ze projekt jest wielkim sukcesem samorzadow w nim uczestniczacych. A co najwazniejsze, z rozwiazania zadowoleni sa mieszkancy. To daje najwieksza satysfakcje.<br />
