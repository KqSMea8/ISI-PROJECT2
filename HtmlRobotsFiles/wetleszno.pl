Wyznaczenia 2019 uzupelnienie            </a>
</h2>

<ul class="post-meta">
<li class="byline">
By <span class="author"><a href="http://wetleszno.pl/?author=1" rel="author">admin</a></span> in <span class="terms"><a class="term term-category term-23" href="http://wetleszno.pl/?cat=23">Wyznaczenia 2019</a></span>	        </li>
</ul>

<div class="entry-content clearfix">



<p>Powiatowy Lekarz Weterynarii w Lesznie informuje o przystapieniu do wyznaczenia, na podstawie art. 16 ust. 1 i 1a ustawy o Inspekcji Weterynaryjnej - lekarzy weterynarii, technikow weterynarii oraz innych osob  do wykonywania czynnosci okreslonych w tych przepisach w okresie od 20 luty 2019 r. do 31 grudnia 2019 r.  ubiegajacych sie o wyznaczenie do <strong><em>nadzoru nad rozbiorem, przetworstwem lub przechowywaniem miesa i wystawiania wymaganych swiadectw zdrowia oraz nad ubojem i ocena zwierzat rzeznych i miesa.</em></strong></p>
<p><strong> </strong></p>
<p>Zgloszen nalezy dokonywac w Powiatowym Inspektoracie Weterynarii w Lesznie, ul. Swieciechowska 150  Wzory wnioskow o wyznaczenie oraz oswiadczen o spelnianiu wymogow kwalifikacyjnych do wykonywania okreslonych czynnosci sa dostepne w zakladce <em><strong>Wyznaczenia 201 .</strong></em>oraz w Powiatowym Inspektoracie Weterynarii w Lesznie. Wyznaczenie lekarza weterynarii swiadczacego uslugi w ramach zakladu leczniczego dla zwierzat wymaga zgody kierownika tego zakladu.</p>
<p>Wyznaczeniem moga byc objete wylacznie osoby, ktore:</p>
<p>1)      nie sa pracownikami Inspekcji Weterynaryjnej,</p>
<p>2)      spelniaja wymagania kwalifikacyjne, okreslone szczegolowo w rozporzadzeniu Ministra Rolnictwa i Rozwoju Wsi z dnia 22.04.2004 r. w sprawie zakresu czynnosci wykonywanych przez osoby niebedace pracownikami Inspekcji Weterynaryjnej oraz kwalifikacji tych osob (Dz. U. 2004.89.860 z pozniejszymi zmianami),</p>
<p>3)      sa objete ubezpieczeniem spolecznym z tytulow, ktore nie generuja obowiazku  oplacania skladek pobieranych przez ZUS ze srodkow Powiatowego Lekarza Weterynarii.</p>
<p>&nbsp;</p>
<p>W odniesieniu do osob objetych wyznaczeniem zostana wydane decyzje administracyjne, po uprawomocnieniu ktorych mozliwe bedzie zawarcie umow w sprawie wykonywania czynnosci  w granicach decyzji o wyznaczeniu.</p>
<p>&nbsp;</p>
<p><a href="http://wetleszno.pl/pliki/2019/2019_02_19_oswiadczenie_tylko_do_sprawowania_nadzoru_nad_rozbiorem_przetworstwem.docx">oswiadczenie tylko do sprawowania nadzoru nad rozbiorem, przetworstwem</a></p>




