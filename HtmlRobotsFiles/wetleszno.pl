<li>c) obrotu <em>zwierzetami</em>, z wyjatkiem obrotu prowadzonego w ramach dzialalnosci rolniczej w rozumieniu przepisow prawa dzialalnosci gospodarczej, posrednictwa w tym obrocie lub skupu <em>zwierzat</em>,</li>
</ol>
<p>sa obowiazane zawiadomic powiatowego lekarza weterynarii wlasciwego ze wzgledu na miejsce organizowania targow, wystaw, pokazow lub konkursow oraz zaladunku i wyladunku <em>zwierzat</em> o:</p>
<p>1) czasie i miejscu organizowania:</p>
<ol>
<li>a) targow, wystaw lub konkursow &#8211; co najmniej na 30 dni,</li>
<li>b) pokazow &#8211; co najmniej na 7 dni</li>
</ol>
<p>&#8211; przed planowanym terminem ich zorganizowania;</p>
<p>2) zaladunku i wyladunku <em>zwierzat</em> co najmniej na 24 godziny przed planowanym terminem ich zaladunku i wyladunku.</p>
<p>Powyzszy obowiazek wynika z art.5 ust. 8 pkt 2 ustawy z dnia 11 marca 2014r. o ochronie zdrowia zwierzat i zwalczaniu chorob zakaznych zwierzat.( Dz.U. 2018 poz. 1967)</p>




