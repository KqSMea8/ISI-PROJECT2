<div class="contentHeader"><h1>O firmie</h1></div>
<div class="text"><p>Firma <strong>HARDER Sp. z o.o.</strong> istnieje na polskim rynku od 2004 r. i jest obecnie jednym z czolowych dostawc&oacute;w urzadzen, material&oacute;w eksploatacyjnych i akcesori&oacute;w w segmencie domowej i warsztatowej praktyki spawalniczej.</p>
<p>Nieustannie monitorujemy oczekiwania klient&oacute;w i sukcesywnie rozwijamy oferte pod katem kompleksowosci gamy i jakosci proponowanych artykul&oacute;w. Wysoki poziom sp&oacute;jnej marki <strong>PIROTEC</strong>, wynikajacy zar&oacute;wno z niezawodnosci i funkcjonalnosci, jak i nowoczesnego wzornictwa i wykorzystywanej technologii, pozwala nam na stosowanie rozwiazan wykraczajacych poza og&oacute;lnie przyjete standardy rynkowe, a pozytywnie ocenianych przez naszych partner&oacute;w handlowych i ostatecznych uzytkownik&oacute;w. Urzadzenia <strong>PIROTEC</strong> objete sa pelna, dwuletnia gwarancja w dogodnym systemie <strong>D2D</strong> - &bdquo;od drzwi do drzwi&rdquo;.</p>
<p>Dzialalnosc firmy <strong>HARDER</strong> obejmuje r&oacute;wniez inne obszary aktywnosci handlowej i uslugowej: oferujemy kompletne&nbsp;systemy&nbsp;odkurzaczy centralnych <strong>DUSTOFF</strong>, prowadzimy poznanski Salon oraz krajowa dystrybucje&nbsp;komfortowych saun i wanien spa - jaccuzi&nbsp;wlasnej marki&nbsp;<a target="_blank" href="http://www.sparden.pl"><strong>SPARDEN</strong></a>,&nbsp;swiadczymy uslugi w zakresie&nbsp;gospodarki magazynowej, transportu&nbsp;i eksportu. Dzieki sprawdzonemu zaufaniu jakim obdarzaja nas kontrahenci i scislej wsp&oacute;lpracy z wiodacymi sieciami market&oacute;w o profilu technicznym oraz hurtowniami i sklepami specjalistycznymi nasze produkty dostepne sa na terenie calego kraju a nasze marki zyskuja ciagle wzrastajaca rzesze usatysfakcjonowanych klient&oacute;w.</p>
<p>Wykorzystujac ugruntowane doswiadczenie wspieramy partner&oacute;w handlowych w zakresie doradztwa i optymalnego doboru asortymentu, szkolen produktowych, projektowaniu i realizowaniu ekspozycji a takze serwisu marketingowego, logistycznego i posprzedazowego. Wszystkie dzialania naszej firmy prowadzone sa z poszanowaniem dla otoczenia i srodowiska naturalnego oraz dbaloscia o zdrowie i bezpieczenstwo osobiste.</p></div>





