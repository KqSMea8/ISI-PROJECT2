<h2 class="text-center">To juz XVIII edycja programu. Dlaczego menedzerowie z calej Polski co roku wybieraja Management<sup>TM</sup>? Co nas wyroznia?</h2>
<div class="visible-lg">
<div class="row">
<div class="col-md-4">
<h3 class="container-fluid-abs width">Konkretne odpowiedzi na najbardziej palace wyzwania liderow </h3>
<img src="images/box/zaawansowana-diagnoza-indywidualna-uczestnikow.jpg" class="img-responsive container-fluid-rv" alt="Zaawansowana diagnoza indywidualna uczestnikow">
<p><strong>W odroznieniu od wielu programow akademickich (w tym MBA) Management<sup>TM</sup> jest scisle zintegrowanym i praktycznym programem rozwoju</strong>. W kazdym z blokow tematycznych uczestnicy wykonuja konkretny projekt dotyczacy aktualnych zagadnien i problemow ich firm. W kazdej chwili moga takze skorzystac ze wsparcia i doswiadczenia ekspertow oraz bogatych zasobow wiedzy ICAN Institute i ,,Harvard Business Review Polska". <strong>Dodatkowym atutem jest takze roczne wsparcie po ukonczeniu programu</strong> w ramach interaktywnego portalu.  </p>
</div>
<div class="col-md-4">
<h3 class="container-fluid-abs width">Natychmiastowe wdrazanie nowych rozwiazan w firmie</h3>
<img src="images/box/2.png" class="img-responsive container-fluid-rv" alt="Kompleksowe, praktyczne podejscie">
<p>W trakcie rocznego programu skupiamy sie na rozwoju kluczowych kompetencji z obszarow strategii, przywodztwa, marketingu, sprzedazy, finansow oraz efektywnosci menedzerskiej. Podczas intensywnych sesji warsztatowych otrzymasz <strong>wiele konkretnych narzedzi i wskazowek, ktore pozwola Ci zwiekszyc swoja skutecznosc w roli menedzera jeszcze w trakcie trwania programu</strong>. Z racji tego, ze program nie wiaze sie z koniecznoscia kilkumiesiecznego wyjazdu, mozesz sie rozwijac, pozostajac jednoczesnie blisko biezacych wyzwan firmy.  </p>
</div>
<div class="col-md-4">
<h3 class="container-fluid-abs width">Metoda ICAN Smart Learning<sup>TM</sup></h3>
<img src="images/box/unikalna-metoda-nauczania.jpg" class="img-responsive container-fluid-rv" alt="Unikalna metoda nauczania">
<p>Podobnie jak wszystkie programy rozwojowe ICAN Institute, Management<sup>TM</sup> wykorzystuje metode  <a href="http://ican.pl/ican-smart-learning/" target="_blank"><strong>ICAN Smart Learning<sup>TM</sup></strong></a>, zapewniajaca maksymalne korzysci edukacji liderow. Skladaja sie na nia m.in. metoda <em>blended learning</em> (laczaca tradycyjne formy ksztalcenia oraz prace poprzez portal wiedzy online) oraz harwardzka metoda studiow przypadkow (dostarczamy ich najwiecej na polskim rynku). <strong>Naczelnym wyroznikiem metody jest podejscie evidence-based management: przekazujemy uczestnikom wylacznie sprawdzone rozwiazania poparte badaniami skutecznosci</strong>. </p>
</div>
</div>
<div class="row">
<div class="col-md-4">
<h3 class="container-fluid-abs width">Intensywna formula, opracowana z mysla o zabieganych menedzerach</h3>
<img src="images/box/wygodna-formula.jpg" class="img-responsive container-fluid-rv" alt="Wygodna formula">
<p>Wiemy, jak napiety jest Twoj harmonogram, dlatego Management<sup>TM</sup> to <strong>12 dwudniowych sesji rozlozonych w ciagu roku</strong>, odbywajacych sie w cyklach sroda-czwartek lub piatek-sobota w wygodnych lokalizacjach (organizujemy szkolenia w Warszawie, Krakowie, Wroclawiu, Gdansku i Poznaniu). Z wyprzedzeniem mozesz zaplanowac udzial w zajeciach, a w razie potrzeby skorzystac z opcji odrobienia danej sesji w innym terminie. </p>
