<img src="images/box/wsparcie-miedzy-zajeciami.jpg" class="img-responsive container-fluid-rv" alt="Wsparcie miedzy zajeciami">
<p>W trakcie programu bedziesz mogl w nieograniczony sposob korzystac z zasobow Harvard Business Review Digital Learning Center. Do Twojej dyspozycji 24 godziny przez 7 dni w tygodniu beda m.in. nagrania konferencji ze swiatowymi autorytetami zarzadzania, moduly szkoleniowe ICAN Business Advisor<sup>TM</sup> oraz biblioteka wiedzy z materialami uzupelniajacymi sesje programu.</p>
</div>
<div class="col-md-4">
<h3 class="container-fluid-abs width">Pewnosc korzysci + zero ryzyka </h3>
<img src="images/box/unikalna-gwarancja-korzysci.jpg" class="img-responsive container-fluid-rv" alt="Unikalna gwarancja korzysci">
