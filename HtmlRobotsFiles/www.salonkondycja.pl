<div style="text-align: center;">&nbsp;</div>

<div>Wydaje Ci sie, ze to nie Twoja bajka ? Od tygodni, miesiecy, lat siedzisz przykuty/ta do biurka, stoisz za lada, a moze godzinami siedzisz za kierownica ? Moze jestes emerytem, kt&oacute;remu powiedziano, ze na PESEL jeszcze nie znaleziono lekarstwa i ograniczenia ruchu to naturalny cykl zycia ?<br />
