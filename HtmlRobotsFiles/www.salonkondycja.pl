<br />
Chcesz zaczac biegac, wspinac sie, spokojnie pokonywac schody, a moze marza Ci sie bardziej ekstremalne sporty ? <img alt="smiley" src="http://salonkondycja.pl/_admin/skrypty/ckeditor/plugins/smiley/images/regular_smile.gif" style="height:20px; width:20px" title="smiley" /><br />
<br />
Zapraszamy do naszego salonu. U nas popracujesz nad podstawa ciala, czyli nad miesniami posturalnymi. W odciazeniu usprawnisz prace staw&oacute;w. Zadbasz o uklad krazenia i limfatyczny. Pozbedziesz sie nadmiaru wody i porzadnie dotlenisz cale cialo, za co m&oacute;zg Ci bardzo podziekuje <img alt="wink" src="http://salonkondycja.pl/_admin/skrypty/ckeditor/plugins/smiley/images/wink_smile.gif" style="height:20px; width:20px" title="wink" /><br />
&nbsp;</div>

<div style="text-align: justify;">Z klientem pracujemy kompleksowo. Nie skupiamy sie na wybranej czesci ciala. Aby nasz organizm dobrze funkcjonowal prace trzeba wykonac calosciowo. Do kazdego klienta podchodzimy indywidualnie. Reagujemy na biezaco na to co sie dzieje z cialem. Nasz salon to nie tasma produkcyjna.W salonie gwarantujemy super atmosfere.<br />
