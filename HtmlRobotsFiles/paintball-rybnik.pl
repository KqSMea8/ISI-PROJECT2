<ul class="bxslider clearfix">
<li>
<div class="title">ZASADZKA</div>
Jedna z ekip przygotowuje zasadzke podczas gdy druga poszukuje ukrytej flagi...
</li>

<li>
<div class="title">OCHRONA PREZYDENTA</div>
Scenariusz stary jak swiat. Jedna osoba musi poswiecic sie i odlozyc pistolet (Prezydent). Pozostali gracze dziela sie na ochroniarzy i terrorystow. Ochrona powinna byc liczniejsza...
</li>
<li>
<div class="title">POJEDYNEK</div>
Druzyny na poczatku rozgrywki stoja za metrowymi murkami oddalonymi od siebie o 20 metrow. W srodku znajduje sie flaga. Celem gry jest zabranie flagi i wrzucenie jej za murek przeciwnikow....
</li>
<li>
<div class="title">EKSTERMINACJA</div>
W grze bierze udzial kilka (3-4 osobowych )druzyn. Gra polega na wyeliminowaniu przeciwnych druzyn.  Przepisy sa podobne jak w przypadku innych gier: gra zaczyna sie po sygnale startu; trafiony-schodzi, zakaz komunikowania sie po trafieniu. Wygrywa druzyna ktora dotrwa do konca.
</li>
<li>
<div class="title">ZWIAD</div>
Kazda druzyna musi sie odnotowac w punktach kontrolnych, problem w tym, ze w tych samych. Kilka 3 osobowych zespolow. Punkty kontrolne (Tyle ile druzyn) oznakowane i porozrzucane na polu. W kazdym punkcie kontrolnym znajduje sie pisak (kazdy w innym kolorze).Gracze maja przymocowane do bluzy lub giwerki kartki na ktorych odnotowuja pisakami obecnosc w punktach kontrolnych...
</li>
</ul>
