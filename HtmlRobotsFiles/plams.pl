<h1>Historia firmy</h1>
<p>Firma istnieje od 1990 roku. Od samego poczatku zajmowala sie sprzedaza hurtowa artykulow spozywczych, sokow, napojow oraz piwa i wina. W kolejnych latach zostala wybrana jako wylaczny dystrybutor Browaru Dojlidy Bialystok, bardzo silnego regionalnego browaru, ktory 30 kwietnia 2003 r. wszedl w sklad ogolnoswiatowego koncernu SABMiller.</p>
<p>W zwiazku z ciagle rosnaca sprzedaza napojow alkoholowych wyspecjalizowala sie w tej branzy. Firma przez wszystkie lata swojej dzialalnosci byla wielokrotnie nagradzana wieloma nagrodami za wspolprace, solidnosc i efektywnosc.</p>
<p>Obecnie dzialamy na terenie wojewodztwa podlaskiego, posiadamy dwa oddzialy w Suchowoli oraz w Bialymstoku, zajmujemy sie dystrybucja piw browarow regionalnych, jak rowniez ogolnopolskich oraz napojow, sokow i win. Firma nieustannie sie rozwija <strong>podwyzszajac funkcjonalnosc, wydajnosc</strong> oraz <strong>jakosc</strong> swiadczonych uslug.</p>
<p>PGK DYSTRYBUCJA stanowi 100% kapital polski.</p>
