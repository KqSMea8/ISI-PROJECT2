<h2>Petrus - zespol ekspertow od badan geologicznych</h2>
</header>
<div class="left">
<p>PETRUS to rodzinna firma swiadczaca uslugi z zakresu geologii inzynierskiej, geotechniki i geologii surowcowej. W ciagu 12 lat naszej dzialalnosci przygotowalismy blisko 3 000 opracowan, zyskalismy grono stalych klientow i zbudowalismy zaufany zespol ekspertow. Wspolpracujemy z pracownikami Zakladu Geotechniki na Wydziale Budownictwa Zachodniopomorskiego Uniwersytetu Technologicznego oraz ADIUMENTUM Diagnostyka Laboratoryjna w Szczecinie.</p>
</div>
<div class="right">
<p>Na polskim rynku zaistnielismy w 2000 roku, ale nasz debiut mial miejsce juz piec lat wczesniej. W 1995 roku <br />dr Andrzej Piotrkowski zalozyl pracownie geologiczna EKO-GEO, w rozwoj ktorej od poczatku zaangazowany byl Maciej Piotrowski. Pod okiem ojca zdobyl wiedze praktyczna i doswiadczenie zawodowe, a po uzyskaniu dyplomu otworzyl wlasny zaklad -<br /> PETRUS Maciej Piotrowski.</p>
<p>Po zakonczeniu dzialalnosci EKO-GEO w grudniu 2010 roku, PETRUS kontynuuje rodzinny dorobek, czerpiac z prawie 40-letniego doswiadczenia <br />dr Andrzeja Piotrowskiego.</p>
</div>
