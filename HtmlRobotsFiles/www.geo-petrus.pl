<header>
<h2>Co robimy oprocz badania gruntu</h2>
</header>
<div class="left">
<h3>Geologia Inzynierska i Geotechnika</h3>
<p>wykonujemy wiercenia badawcze gruntu, dokumentacje, opinie, nadzory pod inwestycje budowlane (m.in.: domy jednorodzinne, budownictwo mieszkaniowe, hale magazynowe i produkcyjne, biurowce, drogi, sieci wodno-kanalizacyjne)
sporzadzamy dokumentacje geologiczno-inzynierskie dla miejscowych planow zagospodarowania przestrzennego</p>
<h3>Geologia</h3>
<p>poszukujemy i dokumentujemy zloza kopalin pospolitych; pomagamy w zdobyciu koncesji na wydobycie zloz</p>
<h3>Hydrogeologia </h3>
<p>oferujemy kompleksowe wykonanie studni i piezometrow oraz opracowujemy dokumentacje hydrogeologiczne zasobow wod podziemnych</p>
<h3>Ochrona srodowiska</h3>
<p>wykonujemy badania gruntu i wod podziemnych dla przedsiewziec mogacych oddzialywac negatywnie na srodowisko okreslamy naturalne tlo hydrogeochemiczne
dokonujemy oceny stopnia zanieczyszczenia i kierunku migracji wod zanieczyszczonych, a takze przydatnosci gruntu do rozsaczania sciekow z przydomowych oczyszczalni sciekow</p>
</div>
<div class="left">
<h3>Badania gruntu pod budowe domow jednorodzinnych</h3>
<p> Przed budowa zalecamy wykonanie badan geotechnicznych okreslajacych parametry podloza gruntowego, ktore sa pomocne przy projektowaniu fundamentow dla danej inwestycji. Pekanie scian budynku, nierownomierne osiadanie, zalewanie piwnic czy katastrofy budowlane sa skutkiem niekorzystnych warunkow gruntowo-wodnych. Nasza ekspertyza pozwoli uniknac tych wszystkich nieprzyjemnych niespodzianek.</p>
<h3>Pomoc przy zakupie dzialki budowlanej</h3>
<p>Przy kupnie dzialki warto zasiegnac opinii geologicznej, gdyz walory krajobrazowe, czy dogodny dojazd nie powinny byc jedynymi atrybutami przyszlej inwestycji. <br />Koszt wykonania badan jest relatywnie niewielki w stosunku do korzysci jakie niesie.</p>
</div>
