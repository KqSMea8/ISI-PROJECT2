<article id="faq">
<header>
<h2>Co powinienes wiedziec na temat badan gruntu</h2>
</header>
<div id="st-accordion" class="st-accordion">
<ul>
<li> <a href="#">Czy geolog zajmuje sie dzieleniem dzialek i projektowaniem map?<span class="st-arrow">Rozwin lub Zwin</span></a>
<div class="st-content">
<p>Nie, geolodzy zajmuja sie badaniami ziemi. To geodeci zajmuja sie  tworzeniem map oraz podzialem i scalaniem gruntow na dzialki.</p>
</div>
</li>
<li> <a href="#">Czy wiercac poszukujecie ropy?<span class="st-arrow">Rozwin lub Zwin</span></a>
<div class="st-content">
<p>Nie, no chyba, ze wiercimy na terenie stacji benzynowej ;-) Ropa w  Polsce znajduje sie na glebokosci ponad 3 km. Nasze wiercenia maja glebokosc  paru, parunastu metrow, a w przypadku studni dochodza do ok. 100 metrow pod  powierzchnia ziemi. </p>
</div>
</li>
<li> <a href="#">To po co wiercicie?<span class="st-arrow">Rozwin lub Zwin</span></a>
<div class="st-content">
<p> Najczesciej badamy warunki gruntowo-wodne pod inwestycje, czyli sprawdzamy jakie grunty wystepuja pod projektowanym obiektem i czy wytrzymaja nacisk budowli. Sprawdzamy rowniez czy, i na jakiej glebokosci, jest woda gruntowa. </p>
</div>
</li>
<li> <a href="#">Czy pod zwyczajny domek jednorodzinny trzeba robic wiercenia geotechniczne?<span class="st-arrow">Rozwin lub Zwin</span></a>
<div class="st-content">
<p>Jak najbardziej! Proces projektowania jest najwazniejszym i jednym z najtanszych etapow budowy. Z pewnoscia taniej jest zrobic badania, niz inwestowac w przewymiarowane fundamenty, albo patrzec jak peka dom marzen niewlasciwie postawiony na slabym gruncie. Jednak pod wzgledem prawnym proste domy jednorodzinne jako budynki geotechnicznej kategorii pierwszej nie maja takiego obowiazku. Chyba, ze w Miejscowym Planie Zagospodarowania Przestrzennego znajduje sie zapis o koniecznosci okreslenia warunkow gruntowo-wodnych ze wzgledu na slabe warunki wystepujace w okolicy. </p>
</div>
</li>
<li> <a href="#">Czy warto skorzystac z uslug rozdzkarza, aby odnalazl ,,zyle wodna"?<span class="st-arrow">Rozwin lub Zwin</span></a>
<div class="st-content">
<p>Pomimo wielu staran oraz licznych badan i eksperymentow, nigdy nie stwierdzono jakiejkolwiek skutecznosci dzialan rozdzkarzy. Najczesciej to hochsztaplerzy zerujacy na ludzkiej niewiedzy, dlatego przestrzegamy przed ich uslugami i wyjasniamy. Woda podziemna w naszych warunkach wystepuje w warstwach piaszczystych nasaczonych woda. Sa to warstwy roznej miazszosci, o duzej, czasami wielokilometrowej rozciaglosci. Sytuacja bardziej przypomina tort, gdzie pomiedzy warstwami nieprzepuszczalnego kremu znajduje sie biszkopt, nasaczony np. sokiem. Nie ma zyl z woda, sa warstwy z woda. Tak wiec zazwyczaj nie ma znaczenia w ktorym miejscu na dzialce bedzie wiercony otwor. Jesli pod powierzchnia terenu znajduje sie warstwa wodonosna, woda bedzie wszedzie.</p>
</div>
</li>
</ul>
