<h3><span style="font-size:16px"><strong><span style="font-family:times new roman,serif">Lekcje muzyki na nowo! Startuje Og&oacute;lnopolski Program Pracownia Muzyczna Casio</span></strong><em><span style="font-family:times new roman,serif">&nbsp;</span></em></span></h3>

<p style="text-align:justify"><span style="font-size:12px"><span style="font-family:arial,helvetica,sans-serif">Milo nam poinformowac, ze Stowarzyszenie Nauczycieli Muzyki wraz z Casio Music Polska uruchamia Og&oacute;lnopolski Program Pracownia Muzyczna Casio. Przeznaczony jest on dla publicznych szk&oacute;l podstawowych a jego celem jest upowszechnianie nauki gry na instrumentach klawiszowych wsr&oacute;d polskich dzieci. Mecenasem projektu jest Bank Zachodni WBK &minus; nie po raz pierwszy wspierajacy uzdolniona muzycznie polska mlodziez i dzieci. Zainteresowane osoby zapraszamy do udzialu!</span></span></p>

<p style="text-align:justify"><span style="font-size:12px"><span style="font-family:arial,helvetica,sans-serif">W realizacje nowego programu edukacyjnego Pracownia Muzyczna Casio zaangazowal sie i opieke merytoryczna nad nim sprawuje zesp&oacute;l ekspert&oacute;w Stowarzyszenia Nauczycieli Muzyki pod przewodnictwem dr hab. Miroslawa Grusiewicza. Dzieki mecenatowi Banku Zachodniego WBK wprowadzanie nauki gry na instrumentach klawiszowych w polskich szkolach bedzie mozliwe i bedzie moglo byc prowadzone na odpowiednim poziomie. W ramach projektu odbywac sie beda szkolenia i warsztaty, a nauczyciele wyposazeni zostana w pomocne materialy dydaktyczne, w tym materialy video z pokazowych zajec. W kazdym roku szkolnym odbywac sie bedzie takze konkurs z nagrodami dla najbardziej aktywnej i zaangazowanej pracowni.</span></span></p>

<p style="text-align:justify">&nbsp;</p>

<p style="text-align:justify"><span style="font-size:12px"><span style="font-family:arial,helvetica,sans-serif">Arkadiusz Bialek, nauczyciel muzyki&nbsp; z wieloletnim doswiadczeniem a zarazem wiceprezes Stowarzyszenia Nauczycieli Muzyki tak sie wyrazil o projekcie: </span></span></p>

<p style="text-align:justify">&nbsp;</p>

<p style="text-align:justify"><span style="font-size:12px"><span style="font-family:arial,helvetica,sans-serif">"Mala rewolucja muzyczna juz sie rozpoczela. Wzorcowa Pracownia Muzyczna Casio powstala w Publicznej Szkole Podstawowej nr 17 im. Przyjaci&oacute;l Dzieci w Radomiu. Od listopada 2017 roku uczniowie z klasy 3b na lekcjach muzyki ucza sie tam pod moim przewodnictwem gry na klawiszach. Tworza duza, dziewietnastoosobowa, elektroniczna orkiestre. Takie zajecia to dla nich wspaniala muzyczna przygoda. Wsp&oacute;lnie pokazujemy, ze lekcje muzyki, gdzie kazde dziecko ma przed soba mini-keyboard sa absolutnie mozliwe w publicznej szkole!"</span></span></p>

<p style="text-align:justify">&nbsp;</p>

<p style="text-align:justify"><span style="font-size:12px"><span style="font-family:arial,helvetica,sans-serif">Swiatowe badania pokazuja, ze wplyw gry na instrumentach klawiszowych jest nie do przecenienia zar&oacute;wno jesli chodzi o kompetencje muzyczne, jak tez wzrost IQ i rozw&oacute;j kory m&oacute;zgowej dzieci. Og&oacute;lnopolski Program Pracowania Muzyczna Casio to szansa, by uczestniczace w nim szkoly staly sie liderami nowoczesnej nauki muzyki a dzieci wszechstronnie sie rozwijaly. </span></span></p>

