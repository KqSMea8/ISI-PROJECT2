<div class="current_event edytorek"><h1>
Witamy na naszej stronie</h1>
<p style="text-align: right;">
<a href="http://www.wychmuz.pl/konferencje-metodyczne_z_4.html" target="_blank"><br />
</a></p>
<p style="text-align: justify;">
&nbsp;</p>
<p style="text-align: justify;">
<strong>&bdquo;Wychowanie Muzyczne w Szkole&rdquo; - od 2011 roku "Wychowanie Muzyczne" - jest jedynym og&oacute;lnopolskim periodykiem o problematyce powszechnej edukacji muzycznej. Jest przede wszystkim poradnikiem dla nauczycieli i instruktor&oacute;w muzyki, ale i dla wszystkich zainteresowanych edukacja muzyczna i amatorskim uprawianiem muzyki. Nadrzednym celem periodyku jest inspirowanie do podejmowania kreatywnych dzialan w zakresie edukacji muzycznej. </strong></p>
<p style="text-align: right;">
<a href="http://www.wychmuz.pl/witamy_z_33.html">wiecej...<br />
</a></p>
<div class="clearfloat"></div></div><div class="current_event edytorek"><h2 style="text-align:center"><span style="color:#800000">Zapraszamy na konferencje&nbsp;</span></h2>

<p>&nbsp;</p>

<p><img alt="" src="/userfiles/2019/Plakat - Gramy z nut.jpeg" style="height:679px; margin-left:40px; margin-right:40px; width:480px" /></p>

<p style="text-align:center">&nbsp;</p>

<p style="text-align: justify;">&nbsp;</p>

<p style="text-align: justify;">W imieniu Zarzadu Stowarzyszenia Nauczycieli Muzyki i Wladz Dziekanskich Wydzialu Pedagogiki i Psychologii UMCS zapraszamy do wziecia udzialu w naszej konferencji. W tym roku jest ona jednodniowa i sila rzeczy posiada mniej rozbudowany program niz dotychczasowe edycje, ale mamy nadzieje, iz spotka sie ona z Panstwa przychylnoscia i zainteresowaniem.</p>

<p style="text-align: justify;">Zapraszamy nauczycieli muzyki wszystkich szczebli, metodyk&oacute;w i dydaktyk&oacute;w, nauczycieli akademickich, praktyk&oacute;w i teoretyk&oacute;w edukacji muzycznej. W programie przewidujemy wystapienia plenarne na temat wsp&oacute;lczesnej kultury i edukacji w Polsce i na swiecie. Beda warsztaty w grupach: znamienitych specjalist&oacute;w od edukacji muzycznej dla dzieci &ndash; Agnieszki Sienkiewicz i&nbsp; Jacka Tarczynskiego, przedstawimy realizowany obecnie wsp&oacute;lnie z Casio program szkolnej nauki gry na instrumentach klawiszowych pt &bdquo;Pracownia Muzyczna Casio&rdquo;, planujemy warsztat praktycznej muzykoterapii, przedstawimy nowe przestrzenie i narzedzia technologii informacyjnych do realizacji w szkole. W ramach konferencji wsp&oacute;lnie z dziecmi z lubelskich przedszkoli wezmiemy udzial w audycji muzycznej przygotowanej przez Ewe Kuszewska. Bedzie tez poranny koncert.</p>

<p>&nbsp;</p>

