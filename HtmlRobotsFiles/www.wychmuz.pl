<p style="text-align:center"><span style="font-size:16px"><span style="font-family:arial,helvetica,sans-serif">W wieku 88 lat <strong>zmarla Ewa Hoffman-Lipska</strong> &ndash; wieloletnia (1977&minus;2000) redaktor naczelna naszego periodyku.</span></span></p>

<p style="text-align:center"><span style="font-size:16px"><span style="font-family:arial,helvetica,sans-serif">Czesc Jej pamieci!</span></span></p>

<p>&nbsp;</p>

<p>&nbsp;</p>

<p>&nbsp;</p>

<p><span style="font-size:16px"><strong>Od redakcji 1. 2019</strong></span></p>

<p>&nbsp;</p>

<p>&nbsp;</p>

<p style="text-align:justify">Ostatnimi znaczacymi rocznicami muzycznymi byly bogato i hucznie obchodzone dwusetne rocznice urodzin Fryderyka Chopina (Rok Chopinowski 2010) oraz Oskara Kolberga (Rok Kolberga 2014). W biezacym roku przypada dwusetna rocznica urodzin Stanislawa Moniuszki i rok 2019 zostal ochrzczony jego imieniem.</p>

<p style="text-align:justify">&nbsp;</p>

<p style="text-align:justify">Instytucje kultury do swoich zadan statutowych wpisaly spuscizne kompozytora, opery beda wystawiac jego dziela, w filharmoniach uslyszymy utwory instrumentalne. Szereg imprez edukacyjnych i kulturalnych bedzie zrealizowanych w ramach program&oacute;w dotacyjnych MKiDN i Instytutu Muzyki i Tanca &minus; Muzyczny Slad i Moniuszko 2019. Wprawdzie na oba przeznaczono niespelna 6 mln zl (program Fryderyk Chopin 2010 &ndash; Promesa dysponowal budzetem 50 mln zl), ale miejmy nadzieje, ze dzieki aktywnosci i gospodarnosci spolecznej pozostawia one trwaly slad i przyczynia sie do glebszego poznania patrona 2019 roku i jego tw&oacute;rczosci, obecnie dosc jednostronnie kojarzonej z kilkoma operami i piesniami.</p>

<p style="text-align:justify">&nbsp;</p>

<p style="text-align:justify">O wydarzeniach Roku Moniuszkowskiego bedziemy informowac i o nich pisac. Tu, korzystajac z okazji, namawiamy do zainteresowania sie nowym cyklem warsztat&oacute;w muzyczno-teatralnych dla dzieci &bdquo;Strasznie Fajny Dw&oacute;r&rdquo; organizowanych w Muzeum Fryderyka Chopina w Warszawie, czy tez nowym, miedzynarodowym konkursem wykonawczym muzyki polskiej im. Stanislawa Moniuszki, jaki zostanie zainaugurowany we wrzesniu biezacego roku w Rzeszowie.</p>

<p style="text-align:justify">&nbsp;</p>

<p style="text-align:justify">Goraco polecamy i namawiamy do zagladania na strony organizator&oacute;w Roku Moniuszki &minus; <a href="https://moniuszko200.pl/pl/">https://moniuszko200.pl/pl/</a>. Tam mozna znalezc pelne zestawienia wydarzen, publikacji, przystepnie zredagowane informacje o naszym narodowym kompozytorze, bazy jego dziel oraz materialy edukacyjne.</p>

