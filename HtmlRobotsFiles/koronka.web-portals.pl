<h1>Koronka do Milosierdzia Bozego</h1>
<a id="godzina_milosierdzia" name="godzina_milosierdzia"></a>
<h2>Godzina milosierdzia</h2>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Pan Jezus do Sw. Faustyny: "O trzeciej godzinie blagaj Mojego Milosierdzia,
szczegolnie dla grzesznikow, i choc przez krotki moment zaglebiaj sie w Mojej Mece, szczegolnie w Moim opuszczeniu w
chwili konania. Jest to godzina Wielkiego Milosiedzia dla swiata calego. W tej godzinie nie odmowie duszy niczego,
ktora Mnie prosi przez Meke Moja (Dz. IV. s. 59).. zanurz sie w Milosierdziu Moim uwielbiajac Je wzywaj Jego
Wszechmocy dla swiata calego, a szczegolnie dla biednych grzesznikow... w godzinie tej uprosisz wszysttko dla
siebie i dla innych. Staraj sie w tej godzinie odprawiac droge krzyzowa... uczcij Moje Serce, ktore  jest pelne
Milosierdzia w Najswietszym Sakramencie, a jak nie mozesz wstapic do kaplicy, pograz sie w modlitwie tam, gdzie
jestes, chociaz przez krociutka chwile. Zadam czci dla Milosierdzia Mojego, od wszelkiego stworzenia... (Dz. V. s. 144)
</p>
<div class="right source">zrodlo: Armia Najdrozszej Krwi Jezusa Chrystusa - Rudolf Szaffer str. 190-191</div>
<a id="godzina_milosierdzia" name="niedziela_milosierdzia_bozego"></a>
<h2>Niedziela Milosierdzia Bozego</h2>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Niedziela Milosierdzia Bozego zostala wprowadzona przez Jana Pawla II w 1995r. na prosbe Episkopatu Polski.
Zostala ona wprowadzona dla wszystkich diecezji w polsce, dopiero 30 kwietnia 2000r. w dniu kanonizacji sw.
Faustyny Kowalskiej zostala ustanowiona dla calego kosciola. Aktualnie niedziela milosierdzia Bozego wypada
zawsze w pierwsza niedziele po swietach Wielkanocnych. Wczesniej obchodzono Niedziele Milosierdzia Bozego w
pazdzierniku.
</p>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Wybor tej niedzieli, jak i rowniez wyrazne zyczenie Jezusa, aby kaplani glosili
kazania o milosierdziu Bozym, wskazuje jasno, ze w planach Bozych istnieje scisly zwiazek miedzy tajemnica
Odkupienia a tym swietem. Przystepujacym tego dnia do Komunii sw., Chrystus obiecuje:
"dostapienia zupelnego odpuszczenia win i kar" (Dzienniczek nr 300 i 699), czyli takiej laski, jaka otrzymujemy
tylko w sakramencie chrztu swietego.
</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Slowa Pana Jezusa dotyczace niedzieli Milosierdzia Bozego:
"Pragne, azeby pierwsza niedziela po Wielkanocy byla swietem Milosierdzia (Dz. 299). Pragne, aby swieto Milosierdzia,
bylo ucieczka i schronieniem dla wszystkich dusz, a szczegolnie dla biednych grzesznikow. W dniu tym otwarte sa
wnetrznosci milosierdzia Mego, wylewam cale morze lask na dusze, ktore sie zbliza do zrodla  milosierdzia  Mojego.
<b>Ktora dusza  przystapi  do  spowiedzi i Komunii swietej, dostapi zupelnego odpuszczenia win i kar. W dniu tym otwarte sa
wszystkie upusty Boze, przez ktore plyna laski (Dz. 699)." </b>
W wielu objawieniach Pan Jezus okreslil nie tylko miejsce swieta w kalendarzu liturgicznym Kosciola, ale takze motyw i
cel jego ustanowienia, sposob przygotowania i obchodzenia oraz wielkie obietnice. Najwieksza z nich jest laska
"zupelnego odpuszczenia win i kar" zwiazana z Komunia swieta przyjeta w tym dniu po dobrze odprawionej spowiedzi
(bez przywiazania do najmniejszego grzechu), w duchu nabozenstwa do Milosierdzia Bozego, czyli w postawie ufnosci
wobec Boga i czynnej milosci blizniego. Jest to - jak tlumaczy ks. prof. Ignacy Rozycki - laska wieksza od odpustu
zupelnego. Ten polega bowiem tylko na darowaniu kar doczesnych naleznych za popelnione grzechy, ale nie jest nigdy
odpuszczeniem samychze win. Najszczegolniejsza laska jest zasadniczo rowniez wieksza niz laski 6-ciu sakramentow z wyjatkiem
sakramentu chrztu: albowiem odpuszczenie wszystkich win i kar jest tylko sakramentalna laska chrztu swietego. W przytoczonych
zas obietnicach Chrystus zwiazal odpuszczenie win i kar z Komunia swieta przyjeta w swieto Milosierdzia, czyli pod tym
wzgledem podniosl ja do rzedu "drugiego chrztu". Przygotowaniem do tego swieta ma byc <b>nowenna polegajaca na odmawianiu przez 9 dni,
poczynajac od Wielkiego Piatku, Koronki do Milosierdzia Bozego.</b><br/>
Swieto Milosierdzia Mojego wyszlo z wnetrznosci [Moich] dla pociechy swiata calego (Dz. 1517) - powiedzial Pan Jezus do
Siostry Faustyny.
</p>
<div class="right source">zrodlo: http://www.faustyna.pl</div>
<a id="fragmenty_z_dzienniczka" name="fragmenty_z_dzienniczka"></a>
<h2>Fragmenty z dzienniczka o Koronce do Bozego Milosierdzia</h2>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; W piatek, 13 IX 1935. Wieczorem, kiedy bylam w swojej celi, ujrzalam Aniola, wykonawce
gniewu Bozego. Byl w szacie jasnej z promiennym obliczem, oblok pod jego stopami, z obloku wychodzily pioruny i blyskawice
do rak jego, z reki jego wychodzily i dopiero dotykaly ziemi. Kiedy ujrzalam ten znak gniewu Bozego, ktory mial dotknac
ziemie, a szczegolnie pewne miejsce, ktorego wymienic nie moge dla slusznych przyczyn, zaczelam prosic aniola, aby sie
wstrzymal kilka chwil, a swiat bedzie czynil pokute. Jednak niczym prosba moja byla wobec gniewu Bozego. W tej chwili
ujrzalam Trojce Przenajswietsza. Wielkosc Majestatu Jej przeniknela mnie do glebi i nie smialam powtorzyc blagania mojego.
W tej samej chwili uczulam w duszy swojej moc laski Jezusa, ktora mieszka w duszy mojej; kiedy mi przyszla swiadomosc tej
laski, w tej samej chwili zostalam porwana przed stolice Boza. O, jak wielki jest Pan i Bog nasz i niepojeta jest swietosc
Jego. Nie bede sie kusic opisywac tej wielkosci, bo niedlugo ujrzymy Go wszyscy jakim jest. Zaczelam blagac Boga za swiatem
slowami wewnetrznie slyszanymi. <b>Kiedy tak sie modlilam, ujrzalam bezsilnosc aniola i nie mogl wypelnic sprawiedliwej kary,
ktora sie slusznie nalezala za grzechy. Z taka moca wewnetrzna jeszcze sie nigdy nie modlilam jako wtenczas. Slowa, ktorymi
blagalam Boga sa nastepujace: Ojcze Przedwieczny, ofiaruje Ci Cialo i Krew, Dusze i Bostwo najmilszego Syna Twojego, a Pana
naszego Jezusa Chrystusa za grzechy nasze i swiata calego; dla Jego bolesnej meki miej milosierdzie dla nas. </b>
(Dz.474, Dz.475)
</p>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Na drugi dzien rano, kiedy weszlam do naszej kaplicy, uslyszalam te slowa wewnetrznie: Ile
razy wejdziesz do kaplicy, odmow zaraz te modlitwe, ktorej Cie nauczylem wczoraj. Kiedy odmowilam te modlitwe, uslyszalam
w duszy te slowa: <b>Modlitwa ta jest na usmierzenie  gniewu Mojego.</b> Odmawiac ja bedziesz przez dziewiec dni na zwyklej czastce
rozanca w  sposob nastepujacy: najpierw odmowisz jedno "Ojcze nasz" i "Zdrowas Maryjo", i "Wierze w Boga", nastepnie na
paciorkach "Ojcze nasz" mowic bedziesz nastepujace: slowa: "Ojcze Przedwieczny, ofiaruje Ci Cialo i Krew, Dusze i Bostwo
Najmilszego Syna Twojego, a Pana naszego Jezusa Chrystusa na przeblaganie za grzechy nasze i swiata calego"; na
paciorkach "Zdrowas Maryjo" bedziesz odmawiac nastepujace slowa: "Dla Jego bolesnej meki miej milosierdzie dla nas i
swiata calego". Na zakonczenie odmowisz trzykrotnie te slowa: "Swiety Boze, Swiety Mocny, Swiety Niesmiertelny,
zmiluj sie nad nami i nad calym swiatem".(Dz. 476)
</p>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; W pewnej chwili, kiedy przechodzilam korytarzem do kuchni, uslyszalam w duszy te slowa:
Odmawiaj nieustannie te koronke, ktorej cie nauczylem. Ktokolwiek bedzie ja odmawial, dostapi wielkiego milosierdzia w
godzine smierci. <b>Kaplani beda podawac grzesznikom jako ostatnia deske ratunku; chociazby byl grzesznik najzatwardzialszy,
jezeli raz tylko odmowi te koronke, dostapi laski z nieskonczonego milosierdzia mojego.</b> Pragne, aby poznal swiat caly
milosierdzie moje; niepojetych lask pragne udzielac duszom, ktore ufaja mojemu milosierdziu". (Dz. 687)
</p>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <b>Obietnica Pana: Dusze, ktore odmawiac beda te koronke, milosierdzie moje ogarnie w zyciu,
a szczegolnie w smierci godzinie. (Dz. 754)</b>
</p>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Kiedy weszlam do swej samotni, uslyszalam te slowa: <b>Kazda dusze bronie w godzinie smierci,
jako swej chwaly, ktora odmawiac bedzie te koronke albo przy konajacym inni odmowia - jednak odpustu tego samego dostepuja.</b>
Kiedy przy konajacym odmawiaja te koronke, usmierza sie gniew Bozy, a milosierdzie niezglebione ogarnia dusze i poruszaja
sie wnetrznosci milosierdzia mojego - dla bolesnej meki Syna mojego. (Dz. 811)
</p>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Wtem uslyszalam glos podczas odmawiania tej koronki: O, jak wielkich lask udziele duszom,
ktore odmawiac beda te koronke, wnetrznosci milosierdzia mego poruszone sa dla odmawiajacych te koronke. Zapisz te slowa,
corko moja, mow swiatu o moim milosierdziu, niech pozna cala ludzkosc niezglebione milosierdzie moje. <b>Jest to znak na
czasy ostateczne, po nim nadejdzie dzien sprawiedliwy. Poki czas, niech uciekaja [sie] do zrodla milosierdzia mojego,
niech korzystaja z krwi i wody, ktora dla nich wytrysla.</b> (Dz. 848)
</p>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Corko moja, mila mi jest mowa serca twojego, a przez odmawianie tej koronki zblizasz
ludzkosc do mnie (Dz. 929)
</p>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <b>Corko Moja, zachecaj dusze do odmawiania tej koronki, ktora ci podalem. Przez odmawianie
tej koronki, podoba mi sie dac wszystko, o co mnie prosic beda. Zatwardziali grzesznicy, gdy ja odmawiac beda, napelnie
ich dusze spokojem, a godzina smierci ich bedzie szczesliwa (...)</b>. Napisz: Gdy te koronke przy konajacych odmawiac beda,
stane pomiedzy Ojcem, a dusza konajaca nie jako Sedzia sprawiedliwy ale jako Zbawiciel milosierny. (Dz. 1541)
</p>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Kiedy weszlam na chwile do kaplicy, powiedzial mi Pan: Corko moja, pomoz mi zbawic pewnego
grzesznika konajacego; odmow za niego te koronke, ktorej cie nauczylem. Kiedy zaczelam odmawiac te koronke, ujrzalam tego
konajacego w strasznych mekach i walkach. Bronil go Aniol Stroz, ale byl jakby bezsilny, wobec wielkosci nedzy tej duszy;
cale mnostwo szatanow czekalo na te dusze. Jednak podczas odmawiania tej koronki ujrzalam Jezusa [w] takiej postaci,
jak jest namalowany na tym obrazie. Te promienie, ktore wyszly z Serca Jezusa ogarnely chorego, a moce ciemnosci uciekly w
poplochu. Chory oddal ostatnie tchnienie spokojnie. Kiedy przyszlam do siebie, zrozumialam, jak ta koronka wazna jest przy
konajacych, ona usmierza gniew Bozy. (Dz. 1565)
</p>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Dzis obudzila mnie wielka burza, wicher szalal i deszcz, jakoby chmura byla oberwana, co
chwila uderzaly pioruny. Zaczelam sie modlic, aby burza nie wyrzadzila zadnej szkody. Wtem uslyszalam te slowa: Odmow te
koronke, ktorej cie nauczylem, a burza ustanie. Zaraz zaczelam odmawiac te koroneczke i nawet jej nie skonczylam, a burza
nagle ustala i uslyszalam slowa: Przez nia uprosisz wszystko, jezeli to, o co prosisz, bedzie zgodne z wola moja.
(Dz. 1731)
</p>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Gdy sie zblizala wielka burza, zaczelam odmawiac te koroneczke. Wtem uslyszalam glos
Aniola: Nie moge sie zblizac w burzy, gdyz jasnosc wychodzaca z ust jej odtraca mnie i burze - zalil sie aniol Bogu.
Wtem poznalam, jak wielkiego spustoszenia mial dokonac przez te burze, lecz rowniez poznalam, ze mila byla Bogu modlitwa
ta i jak wielka jest moc tej koroneczki.  (Dz. 1791)
</p>
