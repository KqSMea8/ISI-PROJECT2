<h2>Modlitwy uzywane podczas odmawiania koronki</h2>
Ponizej lista modlitw ktore sa wypowiadane przy kazdym odmawianiu rozanca:
<ul>
<li><strong>Wierze w Boga:</strong> Wierze w Jednego Boga, Ojca Wszechmogacego, Stworzyciela nieba i ziemi. I w Jezusa Chrystusa, Syna Jego Jedynego, Pana Naszego, ktory sie poczal z Ducha Swietego, narodzil sie z Maryi Panny. Umeczon pod Ponckim Pilatem ukrzyzowan, umarl i pogrzebion. Zstapil do piekiel. Trzeciego dnia zmartwychwstal; wstapil na niebiosa, siedzi po prawicy Boga, Ojca wszechmogacego. Stamtad przyjdzie sadzic zywych i umarlych. Wierze w Ducha Swietego, swiety Kosciol powszechny, swietych obcowanie, grzechow odpuszczenie, ciala zmartwychwstanie, zywot wieczny. Amen.<br/><br/></li>
<li><strong>Ojcze nasz:</strong> Ojcze nasz, ktorys jest w niebie, swiec sie imie Twoje; przyjdz krolestwo Twoje; badz wola Twoja jako w niebie, tak i na ziemi. Chleba naszego powszedniego daj nam dzisiaj; i odpusc nam nasze winy, jako i my odpuszczamy naszym winowajcom; i nie wodz nas na pokuszeni, ale nas zbaw ode Zlego. Amen.<br/><br/></li>
<li><strong>Zdrowas Maryjo: </strong> Zdrowas Maryjo, laski pelna, Pan z Toba, blogoslawionas Ty miedzy niewiastami i blogoslawiony owoc zywota Twojego, Jezus. Swieta Maryjo, Matko Boza, modl sie za nami grzesznymi teraz i w godzine smierci naszej. Amen.<br/><br/></li>
<li><strong>Ojcze przedwieczny:</strong> Ojcze Przedwieczny, ofiaruje Ci Cialo i Krew, Dusze i Bostwo najmilszego Syna Twojego, a Pana naszego Jezusa Chrystusa za grzechy nasze i swiata calego<br/><br/></li>
</ul>
