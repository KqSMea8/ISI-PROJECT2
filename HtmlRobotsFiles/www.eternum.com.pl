<h3>Biura</h3>

<p>Projekt przewiduje budowe budynku biurowo-uslugowego z jednokondygnacyjnym garazem podziemnym. Usytuowanie obiektu wzgledem otaczajacej zabudowy uwzglednia wolna przestrzen na osi widokowej - otwarcie na Plac Solidarnosci oraz historyczna zabudowe
Gdanska zgodnie z zapisem MPZP. Przewiduje sie zielen na poziomie terenu jak i na stropodachach.</p>

