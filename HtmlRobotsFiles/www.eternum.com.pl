<h3>Dostep</h3>

<p>Glowne wejscie do budynku znajduje sie od strony poludniowo-wschodniej. Pozostale wejscia i wyjscia ewakuacyjne zostaly zlokalizowane odpowiednio od strony polnocno-zachodniej oraz poludniowo-wschodniej i poludniowo-zachodniej. Parter wraz z poziomem
+1 stanowi jedna strefe pozarowa. Od strony polnocno-zachodniej zaprojektowano korytarz ewakuacyjny prowadzacy na zewnatrz wydzielajacy pomieszczenia stanowiace odrebna strefe pozarowa. Parkowanie w budynku rozwiazane jest przy pomocy garazu podziemnego,
jednokondygnacyjnego do ktorego wjazd i wyjazd poprowadzony jest projektowana rampa od polnocno-zachodniej strony inwestycji z ul. Stefana Jaracza.</p>

