<h3>Opis Inwestycji</h3>

<p>Eternum to budynek biurowy zlokalizowany u zbiegu ulic Nowomiejskiej i Jaracza  w jednym z najbardziej charakterystycznych punktow w Gdansku tuz obok Europejskiego Centrum Solidarnosci na terenie Mlodego Miasta, w bardzo dobrze skomunikowanej czesci miasta.
Nowoczesny, przeszkolony budynek bedzie wznosil sie na 8 kondygnacji. Na parterze budynku zaprojektowane zostaly lokale uslugowe, natomiast pietra od 1 do 7 przewidziane sa na powierzchnie biurowe.
Istnieje mozliwosc elastycznej aranzacji powierzchni biurowej.
Powierzchnia najmu na pietrze wynosi od 561 m2 do 659 m<sup>2</sup>.</p>

