<span class="common-title">Witamy</span>
<div class="cke_wraper">
<p>
Firma Izoterm-Glass zajmuje sie obrobka szkla i luster - potocznie wszyscy mowia na nas szklarz lub zaklad szklarski. Zaklad powstal w roku 2009, w wyniku likwidacji firmy Izoterm-Glass Sp. z o.o. Na chwile obecna mamy ponad 15 letnie doswiadczenie w zakresie obrobki szkla i kontaktu ze szklem. Obecna firma od roku 2010 zajmuje sie produkcja szkla meblowego dla producentow mebli sklepowych. Dla producentow mebli sklepowych obrabiamy szklo float 4,5,6 mm oraz lustra srebrne 4mm. Do grupy naszych klientow naleza rowniez firmy remontowo-budowlane, salony lazienkowe, producenci mebli kuchennych, projektanci wnetrz oraz klienci indywidualni. Uslugi szklarskie dla klientow indywidualnych swiadczymy na terenie Belchatowa i calego powiatu Belchatowskiego. Naszym klientom dostarczamy szkla lacobel, szkla bezpieczne, lustra, oraz gabloty caloszklane zrobione ze szkla float, hartowanego lub bezpiecznego.</p>
<p>
Posiadamy wlasny park maszynowy tj. szlifierke, fazowarke, wiertarke oraz szlifierke do lukow. Dzieki posiadanemu sprzetowi wszystkie uslugi wykonujemy na miejscu, nie musimy korzystac z uslug innych zakladow szklarskich swiadczacych tego typu uslugi.</p>
<p>
Zapraszamy do odwiedzenia naszego zakladu, w ktorym mamy ekspozycje luster - szlifowanych, fazowanych i luster oprawionych w szklo artystyczne; ekspozycje szkla artystycznego. Wystawke uzupelniaja zdjecia wydrukowane na szkle tzw. Grafika na szkle; polki szklane - przyklady wykonania i kinkiety do oswietlenia do luster.</p>
