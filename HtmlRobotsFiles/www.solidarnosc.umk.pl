<h4><a class="index" href="http://www.solidarnosc.umk.pl/?p=8687" rel="bookmark" title="Permanent Link to "></a></h4>
<div class="entry">
<p><a onclick="javascript:pageTracker._trackPageview('/downloads/wp-content/uploads/500_500_productgfx_8602107a5280baa7538ddb48c2ed7e79.jpg');"  href="http://www.solidarnosc.umk.pl/wp-content/uploads/500_500_productgfx_8602107a5280baa7538ddb48c2ed7e79.jpg"><img class="aligncenter size-medium wp-image-8688" title="500_500_productgfx_8602107a5280baa7538ddb48c2ed7e79" src="http://www.solidarnosc.umk.pl/wp-content/uploads/500_500_productgfx_8602107a5280baa7538ddb48c2ed7e79-239x300.jpg" alt="" width="239" height="300" /></a></p>
<p style="text-align: justify;">Przyklad niezwyklego mestwa i poswiecenia. Obrona Lwowa to historia bohaterstwa najmlodszych. Nikt nie kazal im walczyc, to byl potezny zryw serc. Broni prawie nie mieli, musieli ja dopiero sukcesywnie zdobywac. Odwage Orlat Lwowskich, jak nazwano tych mlodych bohaterow, wspomagal spryt i swietna znajomosc terenu.</p>
<p style="text-align: justify;">Nie tylko w dziejach Rzeczypospolitej trudno o przyklad wiekszego mestwa i poswiecenia niz obrona Lwowa w okresie 1918-1919. Kiedy Wielka Wojna dobiegla praktycznie konca, mieszkancy tego pieknego miasta w dzien Wszystkich Swietych, 1 listopada 1918 r., wyszli rankiem na ulice. Nie mogli jednak udac sie na groby swych bliskich. Po Lwowie krazyly bowiem uzbrojone i strzelajace w roznych kierunkach oddzialy Ukraincow, ktorzy korzystajac z wojennej zawieruchy postanowili z zaskoczenia zawladnac owczesna stolica Galicji. Tu, gdzie od tylu wiekow byla Polska, teraz miala byc tylko Ukraina. Lwow byl oczywiscie miastem kilku kultur, narodowosci i kilku religii, ale zywiol polski byl caly czas dominujacy, nawet pod zaborami. Mieszkali tam Zydzi, Ormianie, w nieduzym procencie rowniez Ukraincy - do tej pory wszyscy zyli tu w zgodzie. Kwitnace w tym miescie kultura, sztuka i nauka, byly w olbrzymim stopniu dzielem Polakow. Przez pare wiekow rowniez obrona przed zalewem muzulmanskim spoczywala na barkach Rzeczypospolitej. Jednak 1 listopada 1918 r. nad miastem zalopotaly zolto-niebieskie flagi.</p>
<p style="text-align: justify;">Zaskoczenie Polakow bylo olbrzymie, ale jeszcze wieksza okazala sie chec walki, obrony ukochanego miasta. Kto jednak mial to uczynic, skoro tyle polskiej krwi polalo sie wczesniej na wszystkich frontach Wielkiej Wojny? Do obrony spontanicznie przystapila wiec mlodziez, dzieci w zasadzie; byli za mlodzi, by wczesniej zostac wcielonymi do zaborczych armii. Nikt nie kazal im walczyc, to byl potezny zryw serc. Broni prawie nie mieli, musieli ja dopiero sukcesywnie zdobywac. Odwage Orlat Lwowskich, jak nazwano tych mlodych bohaterow, wspomagal spryt i swietna znajomosc terenu. Do walki stanelo niewielu zawodowych oficerow oraz 1374 uczniow szkol powszechnych i srednich oraz studentow. Najmlodszy z nich mial zaledwie 9 lat... 13-letni Antos Petrykiewicz, polegly od zadanych mu ran, zostal najmlodszym kawalerem Orderu Virtuti Militari... Smiercionosna kula dopadla 14-letniego harcerza Jurka Bitschana&#8230; Nieraz ginely cale rodzenstwa, jak Tadeusz (21 lat), Jan (18) i Helena (15) Grabscy...</p>
<p style="text-align: justify;">Na tragiczny krzyk wydobywajacy sie z okrazonego przez Ukrainska Armie Halicka miasta zareagowala Ojczyzna, ktora wlasnie zaczynala sie odradzac. W pore nadeszla pomoc, najpierw ochotnicza, a potem  regularna, wojskowa. Wybitny historyk prof. Wojciech Roszkowski slawi w tej ksiazce mestwo Orlat Lwowskich. W Krakowie ma stanac ich pomnik. Ta publikacja sluzy nie tylko propagowaniu prawdy historycznej, ale tez wyrazeniu wdziecznosci wobec Orlat i ma wspomoc finansowo budowe pomnika ku ich czci.</p>
<p style="text-align: justify;">Ksiazke w promocyjnej cenie 59 zl mozna zamawiac w biurze NSZZ ,,Solidarnosc" - Rektorat, pok. 607</p>

