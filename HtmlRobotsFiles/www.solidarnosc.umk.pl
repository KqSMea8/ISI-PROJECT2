<div class="entry">
<p class="MsoNormal" style="text-align: left;"><strong>Szanowne Kolezanki i Szanowni Koledzy - Pracownicy Uniwersytetu Mikolaja Kopernika w Toruniu!</strong></p>
<p class="MsoNormal" style="text-align: left;"><strong> </strong></p>
<p class="MsoNormal" style="text-align: left;">Informujemy wszystkich Pracownikow UMK, ze jako Komisja Uczelniana NSZZ ,,Solidarnosc" przy Uniwersytecie Mikolaja Kopernika w Toruniu zdecydowalismy o wyrazeniu, w drodze uchwaly nr 8 z dnia 18 marca 2019 r., naszej NEGATYWNEJ OPINII wobec projektu Statutu Uniwersytetu Mikolaja Kopernika (w wersji datowanej na dzien 4 marca 2019 r.).</p>
<p class="MsoNormal" style="text-align: left;">Projekt nowego Statutu UMK, przekazany nam do zaopiniowania przez JM Rektora UMK w trybie przewidzianym w art. 34 ust. 2 ustawy Prawo o szkolnictwie wyzszym i nauce, zawiera z punktu widzenia praw pracowniczych i zwiazkowych cztery glowne wady prawne:</p>
<p class="MsoNormal" style="text-align: left;"><strong>1) naruszenie uprawnienia zwiazkow zawodowych do uczestniczenia przedstawicieli kazdego z nich z glosem doradczym w posiedzeniach kazdego organu kolegialnego Uniwersytetu;</strong></p>
<p class="MsoNormal" style="text-align: left;"><strong>2) brak koniecznego okresu przejsciowego dla przepisow Statutu wprowadzajacych nagla i daleko idaca zmiane podleglosci sluzbowej pracownikow (stanowiaca konsekwencje afiliacji do dyscypliny naukowej), rozpoczynajacego bieg od dnia wejscia w zycie nowego Statutu i trwajacego do dnia 1 stycznia 2021 r.;</strong></p>
<p class="MsoNormal" style="text-align: left;"><strong>3) brak nalezytej precyzji przy ksztaltowaniu tresci paragrafu 100 Statutu dotyczacego umow o prace nauczycieli akademickich oraz zwiazanych z nimi konkursow na zatrudnienie;</strong></p>
<p class="MsoNormal" style="text-align: left;"><strong>4) brak uprawnienia zwiazkow zawodowych do opiniowania uchwal Senatu UMK przyjmowanych w sprawie ,,Wewnetrznych systemow zapewniania jakosci" w Uniwersytecie.</strong></p>
