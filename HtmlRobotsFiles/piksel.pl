<article id="serwis">
<header><h2>Serwis</h2></header>
<p>
<img src="resources/images/articles/service-b.jpg" alt="Piksel - serwis." class="right">
</p>
<p>
W biznesie jak i w zyciu prywatnym ciaglosc dzialania urzadzen informatycznych stala sie obecnie kluczowa i niezbedna cecha sprawnego funkcjonowania w danym srodowisku. Problemy z oprogramowaniem, siecia internet, czy ze sprzetem informatycznym, narazaja nas na niepotrzebny stres a czesto rowniez na niepowetowane straty finansowe. Niemozliwosc odebrania, przetworzenia i przekazania informacji odcina nas od glownego nurtu wspolczesnej cywilizacji a co za tym idzie naraza nas na tzw. wykluczenie informacyjne.
</p>
<p>
Sprawny serwis i regularna pielegnacja naszego otoczenia informatycznego pozwala nam skoncentrowac sie na kluczowych dla nas sprawach i zadaniach a nie na przyslowiowej walce z komputerami, co w znacznym stopniu przeklada sie na zwiekszenie naszej pewnosci siebie. A efektywnosc naszej pracy wzrasta.
</p>
<p>
<a href="#kontakt">Skontaktuj sie ze mna</a> i dowiedz sie w jaki sposob mozna zaradzic twoim biezacym jak i przyszlym problemom z oprogramowaniem, siecia informatyczna, czy sprzetem komputerowym.
</p>
</article>
<article id="doradztwo">
<header><h2>Doradztwo</h2></header>
<p>
<img src="resources/images/articles/consultancy-b.jpg" alt="Piksel - doradztwo." class="left">
</p>
<p>
Dzisiejsza roznorodnosc rozwiazan informatycznych, ich zakres, funkcjonalnosc, mnogosc opcji,  moga oniesmielic kazdego, nawet doswiadczonego pracownika branzy IT. Czy nie mieli nigdy Panstwo wrazenia, ze zakupiony przez Panstwa produkt wykorzystywany jest jedynie w niewielkim zakresie swoich mozliwosci i ze nigdy tak naprawde w pelni nie wykorzystamy tego, za co zaplacilismy ? Marketing zrobil swoje! A nas trapi przekonanie, ze wydalismy pieniadze, ktore moglismy o wiele bardziej racjonalnie wykorzystac.
</p>
<p>
Odpowiedni dobor oprogramowania i parametrow sprzetu informatycznego jest sztuka, wymagajaca dokladnej analizy biezacych jak i przyszlych potrzeb, kosztow i funkcjonalnosci rozwiazan informatycznych ktore planujemy wdrozyc.
</p>
<p>
<a href="#kontakt">Skontaktuj sie ze mna</a> i dowiedz sie w jaki sposob mozna zaplanowac rozwoj informatyczny firmy i wdrozyc rozwiazania informatyczne scisle dopasowane do potrzeb i dostepnych funduszy.
</p>
</article>
<article id="outsourcing">
<header><h2>Outsourcing</h2></header>
<p>
Rozwiazania informatyczne nie sa tanie, a w szczegolnosci te, ktore charakteryzuja sie wysokim zaawansowaniem technicznym. Utrzymywanie we wlasnym zakresie skomplikowanych struktur informatycznych wiaze sie z kosztami, ktore moga przytloczyc mala lub srednia firme. Z drugiej strony, w dzisiejszych czasach, brak w firmie zaawansowanych rozwiazan informatycznych skazuje ja na drugi obieg, a co za tym idzie, prowadzi takze do coraz wiekszej jej marginalizacji na rynku.
</p>
<p>
Outsourcing jest rozwiazaniem biznesowym, ktory rozwiazuje powyzsze problemy. Takie rozwiazania informatyczne jak: serwery poczty elektronicznej czy stron internetowych, hosting czy   cloud computing moga byc w wiekszosci przypadkow powierzone obsludze firmom zewnetrznym. A przy odpowiednim i racjonalnym doborze uslug, koszy zwiazane z korzystaniem z ich dobrodziejstw, mozna zminimalizowac do kwot akceptowalnych przez kazda firme, ktora chce sie rozwijac i byc nowoczesna.
</p>
<p>
<img src="resources/images/articles/outsorcing-c.jpg" alt="Piksel - outsourcing." class="left right">
</p>
<p>
<a href="#kontakt">Skontaktuj sie ze mna</a> i dowiedz sie w jaki sposob mozna powierzyc obsluge potrzeb informatycznych Panstwa Firmy, jakie sa koszty takiego przedsiewziecia i jakie korzysci.
</p>
</article>
<article id="strony-www">
<header><h2>Strony WWW</h2></header>
<p>
<img src="resources/images/articles/webdesign-a.jpg" alt="Piksel - strony www." class="right">
</p>
<p>
Dzisiejszy marketing przeniosl sie z ulicy do sieci internet. Zaistnienie i  zareklamowanie sie w internecie staje sie wazniejsze od tradycyjnych metod dotarcia do potencjalnych klientow. Wszyscy juz poslugujemy sie smartfonami, tabletami, czy laptopami , tak w domu, jak i w pracy, czy na ulicy. Internet  stal sie podstawowym i niezbednym zrodlem informacji o otaczajacej nas rzeczywistosci a publikowane w nim tresci wyprzedzaja juz na samym starcie o rzad dlugosci tradycyjne nosniki informacji.
</p>
<p>
Dobrze zaprojektowana strona, o przemyslanej tresci i strukturze, zgodna z regulami sztuki, standardami internetowymi i typograficznymi, a takze dostosowana do roznorodnosci uzywanych urzadzen, jest nie tylko wizytowka naszej firmy, ale stanowi rowniez punkt wyjscia dla przyszlych klientow. Od niej to zalezy, czy nastepnym krokiem potencjalnego klienta bedzie chec skorzystania z naszych uslug czy zakup oferowanych przez nas produktow.
</p>
<p>
<a href="#kontakt">Skontaktuj sie ze mna</a> i dowiedz sie w jaki sposob twoja przyszla strona internetowa moze efektywnie zaistniec w internecie i dotrzec do jak najwiekszej liczby uzytkownikow w sieci.
</p>
</article>
<article id="renowacja-i-recykling">
<header><h2>Renowacja i recykling</h2></header>
<p>
<img src="resources/images/articles/renovation-and-recycling-a.jpg" alt="Piksel - renowacja i recykling." class="left">
</p>
<p>
Dzisiejszy, agresywny marketing sklania nas do pogoni za coraz to nowszym sprzetem, szybszym i wyposazonym w niezliczona ilosc nowych, topowych, zachwalanych  i niezbednych funkcji, przez co czesto nie zdajemy sobie sprawy z faktu, ze statystycznie mozliwosci naszego najnowszego sprzetu w przyszlosci wykorzystamy zaledwie w kilku procentach a nasze stare chociaz jeszcze sprawne urzadzenia trafia w kat a potem na smietnik.
</p>
<p>
<a href="#kontakt">Skontaktuj sie ze mna</a> i dowiedz sie w jaki sposob mozna przywrocic do zycia twoje zapomniane komputery i w jaki sposob mozna je efektywnie wykorzystywac tak w pracy jak i w domu. A jezeli faktycznie ich juz nie potrzebujesz, dowiedz sie, w jaki sposob zgodnie z prawem poddac je recyklingowi.
</p>
</article>
<article id="odzyskiwanie-danych">
<header><h2>Odzyskiwanie danych</h2></header>
<p>
Kazdy informatyk, czy zaawansowany uzytkownik komputerow wie, ze podstawa  pracy z komputerami jest regularne robienie kopii bezpieczenstwa danych, tzw. ,,backup'u". Jednakze przez pospiech i ped dzisiejszego zycia czesto zapominamy o tym oczywistym fakcie a nasze nosniki danych, takie jak: pamieci USB, plyt CD czy DVD nie sa w nalezyty sposob przechowywane i pielegnowane, przez co narazone sa na uszkodzenia i utrate danych.
</p>
<p>
<img src="resources/images/articles/data-recovery-a.jpg" alt="Piksel - odzyskiwanie danych." class="left right">
</p>
<p>
<a href="#kontakt">Skontaktuj sie ze mna</a> i dowiedz sie w jaki sposob moge pomoc, kiedy Twoje nosniki danych zawioda, a dane przechowywane na Twoich plytach CD, DVD, dyskach twardych czy pamieciach USB byly dla Ciebie wazne.
</p>
</article>
