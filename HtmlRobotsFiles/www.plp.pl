<div align="center"><h2>Witamy na stronie PLP !<br><br></h2><br>W 2009 roku zostala reaktywowana Polska Liga Paintballowa. Nowa PLP jest wynikiem rozmow i konsultacji z Europejska Federacja Paintballowa (EPBF) i bedzie dziala w ramach jej struktur. Niewatpliwa zaleta ligi jest funkcjonowanie w ramach zestandaryzowanych zasad i struktur na jakich oparte sa pozostale narodowe ligi europejskie (np. liga niemiecka, francuska, itd.). Idea przewodnia EPBF jest standaryzacja formatu rozgrywek i ujednolicenie zasad tak, zeby w kazdym europejskim kraju rywalizacja sportowa wygladala tak samo, a docelowo mozliwe byloby prowadzenie Europejskich Finalow w oparciu o klasyfikacje i ranking druzyn narodowych (na ksztalt Chamipons League w pilce noznej). W 2012 roku majac na uwadze dobro graczy oraz calego srodowaiska paintballowego, Polska Liga Paintballowa oraz Narodowa Liga Paintballowa podjely decyzje o polaczeniu lig. Obie ligi wyrazily chec integracji sportowego srodowiska pantballowego i stworzenia preznie dzialajacej ligi osadzonej w strukturach EPBF.</div>

