<h2>Nasza oferta</h2>
<h3>,,Notariusz na podstawie art. 79 ustawy z dnia 14 lutego 1991 r. Prawo o notariacie dokonuje nastepujacych czynnosci:</h3>
<ul>
<li>sporzadza wszelkiego rodzaju  akty notarialne w tym umowy w formie aktu notarialnego ,</li>
<li>sporzadza akty poswiadczenia dziedziczenia,</li>
<li>sporzadza poswiadczenia podpisu , poswiadczenia zgodnosci z okazanym dokumentem, poswiadczenia daty pewnej , poswiadczenia pozostawania przy zyciu ,</li>
<li>dorecza oswiadczenia ,</li>
<li>spisuje protokoly w tym protokoly zawierajace uchwaly zgromadzen wspolnikow spolek prawa handlowego i uchwaly organow spoldzielni ,</li>
<li>sporzadza protesty weksli i czekow,</li>
<li>przyjmuje na przechowanie pieniadze, papiery wartosciowe, dokumenty, dane na informatycznym nosniku danych, o ktorym mowa w przepisach o informatyzacji dzialalnosci podmiotow realizujacych zadania publiczne,</li>
<li>sporzadza wypisy, odpisy i wyciagi dokumentow,</li>
<li>sporzadza, na zadanie stron, projekty aktow, oswiadczen i innych dokumentow." </li>
</ul>
