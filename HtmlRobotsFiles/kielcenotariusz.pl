<p>,, Kancelaria Notarialna notariusza Jacka Frelicha swiadczy uslugi juz od 1992 roku . Poczatkowo siedziba kancelarii miescila sie w Kielcach przy ul. Sienkiewicza nr 4A. W 2005 roku siedziba zostala przeniesiona do obecnego  przestronnego i latwo dostepnego lokalu mieszacego sie w Kielcach przy al. IX Wiekow Kielc nr 2b/2. Dwudziestopiecioletnie doswiadczenie zawodowe notariusza Jacka Frelicha jest rekojmia profesjonalnego zalatwienia spraw urzedowych. W kontaktach z interesantami notariusz sluzy rowniez znajomoscia jezyka francuskiego."</p>

