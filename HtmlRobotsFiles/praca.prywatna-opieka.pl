<strong>O firmie</strong><br>
<br>
Firma istnieje na rynku od stycznia 2007 roku. Obecnie oferujemy prace za granica dla pielegniarek, opiekunek osob starszych oraz chorych. Nasza oferta dotyczaca tego obszaru jest bardzo szeroka i atrakcyjna. Mamy tam mozliwosc dokladnego sprawdzenia i analizy kazdej pracy na miejscu przez co jestesmy w stanie zagwarantowac pewnosc i co najwazniejsze legalnosc takiej pracy. Zalezy nam na jakosciowym i ilosciowym rozwoju proponowanej przez nas oferty, tak aby jak najlepiej sluzyc naszym klientom. Totez staramy sie nawiazac kontakty bezposrednio z pracodawcami w Niemczech oraz Austrii. Nasza firma ma swoj oddzial w Lublinie oraz przedstawicieli handlowych na terenie calego kraju.
<br>
<br>
<strong>OPIEKA NAD OSOBAMI STARSZYMI ORAZ CHORYMI</strong><br>
<br>
Oferujemy Panstwu mozliwosc pracy w charakterze opiekunki/opiekuna osob starszych na terenie Niemiec oraz Austrii. Nasza oferta skierowana jest do osob, ktore ukonczyly 25 rok zycia, opanowaly jezyk niemiecki w stopniu komunikatywnym. W sposob szczegolny zapraszamy pielegniarki i pielegniarzy, sanitariuszy oraz osoby z pokrewnym wyksztalceniem (fizjoterapeuta, masazysta, rehabilitantka itp.).<br>
