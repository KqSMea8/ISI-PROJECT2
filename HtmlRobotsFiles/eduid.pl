<h2 class="czym_jest_portal"><span class="invisible">Czym jest portal eduID.pl?</span></h2>
<p>Portal <span class="eduid">edu<b>ID</b>.pl</span> daje Ci mozliwosc utworzenia Twojego <b>Prywatnego Identyfikatora</b>, za pomoca ktorego bedziesz mogl bezpiecznie logowac sie do swoich ulubionych serwisow internetowych, bez uciazliwego korzystania z roznych loginow i hasel.</p>
<h2 class="czy_portal_eduid"><span class="invisible">Czy portal eduID.pl umozliwi mi cos jeszcze?</span></h2>
<p>Tak! W portalu <span class="eduid">edu<b>ID</b>.pl</span> mozesz utworzyc Twoja <b>Prywatna Wizytowke</b>. Dzieki niej pola w formularzach internetowych, jakie czasem masz potrzebe wypelniac, beda uzupelnialy sie same, automatycznie. </p>
<p>Portal <span class="eduid">edu<b>ID</b>.pl</span> zapewnia Ci maksymalna wygode korzystania z serwisow internetowych.</p>
