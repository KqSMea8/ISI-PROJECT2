<div class="column_left">
<h2>Dlaczego My?</h2>
<p><b>Specjalizujemy sie w pourazowej pomocy i ocenie
medycznej</b>, <b>pracujemy dla Szpitali, Sadow, Prokuratur, Policji,
firm ubezpieczeniowych i osob poszkodowanych</b> na terenie calego kraju. <br />
<br />Wydajemy rocznie ponad 1500 opini i <b>ekspertyz
specjalistycznych</b> z niemal wszystkich dziedzin medycznych.
Zatrudniamy 30 lekarzy specjalistow, kadre profesorska,
a ich liczba stale rosnie. Zapraszamy do
korzystania z naszej rozleglej wiedzy i wieloletniego
doswiadczenia. <br />
<br /><b>Miales wypadek, uraz i nie otrzymales zadnej
rekompensaty?</b>
Nie przyznano ci <b>odszkodowania za uszczerbek na zdrowiu</b> lub
<b>odszkodowanie to jest nieadekwatne</b> do Twoich dolegliwosci po
wypadku?
Jezeli chcesz to zweryfikowac, sprawdzic czy nalezy ci sie
odszkodowanie czy doznales dlugotrwalego lub trwalego
uszczerbku na zdrowiu i jaka moze byc jego wysokosc.</p>
</div>
<div class="column_right">
<h2>Miales wypadek?</h2>
<p>
Czy warto starac sie o odszkodowanie, tego mozesz dowiedziec
sie tutaj, wybierz opcje ktora cie interesuje, i rozpocznij
wymiane informacji z IWO.
</p>
<ul class="dedicated_ul">
<li>Jezeli chcesz dowiedziec sie czy urazy jakich
donazles spowodowaly u Ciebie <b>powstanie uszczerbku na zdrowiu</b>, wybierz te opcje.
System zada ci serie pytan i okresli <b>jakiego rodzaju
uszczerbku na zdrowiu doznales</b> i czy masz szanse na uzyskanie
odszkodowania.</li>
<li>System umozliwi ci rozniez wybor
jednej z wspolpracujacych z nami kancelarii prawniczych ktora
BEZPLATNIE pomoze Ci uzyskac odszkodowanie w najwygodniejszy i jak
najbardziej zgodny z Twoimi oczekiwaniami sposob.</li>
<li><b>Wirtualny Orzecznik</b> umozliwi Ci rowniez <b>uzyskanie pisemnej
opini sadowo-lekarskiej wydanej
przez lekarzy specjalistow</b>, ktora moze byc podstawa do
wystapienia z roszczeniem o odszkodowanie do likwidatora szkody
lub wstapienia na droge sadowa.</li>
</ul>
