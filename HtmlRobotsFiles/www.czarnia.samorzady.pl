<h1 class="art_title"><a href="/art/id/2090">Oglaszenie o naborze na wolne stanowisko w Gminnej Bibliotece Publicznej w Czarni</a></h1>
<br class="clear" />
<p class="art_head"></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Czarnia 01 kwietnia 2019r.</p>
<p><strong>&nbsp;</strong></p>
<p><strong>Dyrektor Gminnej Biblioteki Publicznej w Czarni</strong></p>
<p><strong>oglasza nab&oacute;r na stanowisko- </strong><strong>Mlodszy bibliotekarz</strong></p>
<p><strong>&nbsp;</strong></p>
<p><strong>&nbsp;</strong></p>
<p>Miejsce wykonywania pracy: Gminna Biblioteka Publiczna w Czarni&nbsp; Filia w Surowem.</p>
<p>&nbsp;</p>
<p>Forma zatrudnienia: umowa o prace, od 06 maja 2019r.</p>
<p>Wymiar czasu pracy: pelen etat</p>
<p>&nbsp;</p>
<ol>
<li>Kandydat musi spelniac nastepujace wymogi:</li>
</ol>
<ul>
<li>Wyksztalcenie min. studium bibliotekarskie, srednie bibliotekarskie lub srednie oraz praktyki albo szkolenia w zakresie bibliotekarskim ( humanistyczne, artystyczne)</li>
<li>og&oacute;lna wiedza z zakresu literatury polskiej i swiatowej.</li>
<li>znajomosc rynku wydawniczego,</li>
<li>umiejetnosc wykorzystywania narzedzi cyfrowych w pracy edukacyjnej,</li>
<li>umiejetnosc publikacji artykul&oacute;w w mediach spolecznosciowych,</li>
<li>umiejetnosc pracy z dziecmi i doroslym czytelnikiem,</li>
<li>zdolnosci animacyjne, manualne,</li>
<li>umiejetnosc dobrej organizacji pracy samodzielnej i w zespole,</li>
</ul>
<p>&nbsp;</p>
<ol start="2">
<li>Wymagania dodatkowe:</li>
</ol>
<ul>
<li>biegla znajomosc obslugi komputera i podstawowych program&oacute;w pakietu MS Office oraz program&oacute;w graficznych (podstawowych),</li>
<li>mile widziana znajomosc i praca w elektronicznym systemie bibliotecznym MATEUSZ</li>
<li>mile widziane uzdolnienia artystyczne,</li>
<li>umiejetnosc pracy z czytelnikami, udostepnianie uzytkownikom komputer&oacute;w i pomoc w korzystaniu z nich.</li>
<li>aktywne realizowanie powierzonych dzialan oraz wlasnych pomysl&oacute;w,</li>
<li>zaangazowanie w rozw&oacute;j biblioteki oraz godne jej reprezentowanie,</li>
<li>komunikatywnosc, wysoka kultura osobista, punktualnosc, skrupulatnosc, sumiennosc, zaangazowanie w pracy,</li>
<li>terminowosc i odpowiedzialnosc za powierzone zadania, kreatywnosc, dobra organizacja pracy,</li>
<li>gotowosc do stalego podnoszenia kwalifikacji,</li>
</ul>
<p>&nbsp;</p>
<ol start="3">
<li>Od kandydata wymaga sie umiejetnosci w zakresie:</li>
</ol>
<p>Prowadzenia czynnosci bibliotecznych - sprawne pelnienie uslug w zakresie informowania i udostepniania zbior&oacute;w biblioteki, skontrum ksiegozbioru. Doswiadczenie na polu organizacji imprez integrujacych spolecznosc lokalna, prowadzenia warsztat&oacute;w dla dzieci i mlodziezy, wystaw itp. udzielanie informacji bibliograficznej i rzeczowej, upowszechnianie czytelnictwa, doskonalenie form pracy bibliotecznej, dbalosc o stan powierzonego ksiegozbioru i mienia biblioteki, wsp&oacute;ludzial w organizowaniu dzialalnosci kulturalnej na terenie gminy.</p>
<p>&nbsp;</p>
<ol start="4">
<li>Wymagane dokumenty:</li>
</ol>
<ul>
<li>List motywacyjny, zyciorys CV.</li>
<li>Kserokopie dokument&oacute;w potwierdzajacych zatrudnienie oraz wyksztalcenie.</li>
<li>Oswiadczenie kandydata o pelnej zdolnosci do czynnosci prawnych oraz o korzystaniu z pelni praw publicznych.</li>
<li>Oswiadczenie kandydata o niekaralnosci za przestepstwa popelnione umyslnie oraz, ze nie toczy sie przeciwko niemu postepowanie karne.</li>
<li>Oswiadczenie kandydata o wyrazeniu zgody na przetwarzanie danych osobowych do cel&oacute;w rekrutacji.</li>
</ul>
<p>&nbsp;</p>
<ol start="5">
<li>Forma i termin skladania ofert:</li>
</ol>
<p>Oferty nalezy skladac w zamknietej kopercie w siedzibie GBP w Czarni, Czarnia 43A, 07-431 Czarnia, w terminie <strong>do dnia 12 kwietnia 2019 r. do godziny 15.00,</strong> z dopiskiem <strong><em>&bdquo;Dotyczy naboru na stanowisko Mlodszego bibliotekarza&rdquo;.</em></strong></p>
<p><strong><em>&nbsp;</em></strong></p>
<p>Dodatkowych informacji udzielamy telefonicznie pod nr 29 7727048</p>
<p>&nbsp;</p>
<ol start="6">
<li>Uwagi koncowe:</li>
</ol>
<ul>
<li>Dostarczone dokumenty zostana poddane analizie przez komisje rekrutacyjna,</li>
<li>W wyniku w/w analizy, zostanie wytypowana lista kandydat&oacute;w, kt&oacute;rzy spelniaja wymagania formalne okreslone w ogloszeniu, ostateczna decyzje podejmuje Dyrektor GBP,</li>
<li>Kontaktowac sie bedziemy telefonicznie lub e-mailowo z wybranymi kandydatami,</li>
<li>Wytypowani kandydaci zostana powiadomieni telefonicznie lub e-mailowo o terminie i miejscu rozmowy kwalifikacyjnej,</li>
<li>Oferty odrzucone, kt&oacute;re nie zostana odebrane w terminie 30 dni od zakonczenia naboru, zostana zniszczone.</li>
</ul>
