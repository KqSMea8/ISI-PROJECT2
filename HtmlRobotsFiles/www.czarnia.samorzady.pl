<li>jednorazowe srodki na podjecie dzialalnosci gospodarczej &ndash; 124 os&oacute;b (42 K/ 82 M).</li>
</ol>
<p style="text-align: justify;">Budzet projektu na lata 2017-2018 wynosi 5.574.455,84 zl. W 2018 r. zaplanowano wydatki w wysokosci 2.993.177,29 zl, gdzie 2.394.541,83 zl to srodki Europejskiego Funduszu Spolecznego.</p>
