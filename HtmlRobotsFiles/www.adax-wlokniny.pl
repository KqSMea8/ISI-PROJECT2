<h1>WITAMY NA STRONACH FIRMY ADAX</h1>

<h2>Historia firmy</h2>

<img src="img/photo03.png" width="120" height="120" alt="" class="unitPng" style="float:left; margin: 10px"/>

<p>Firma PPHU ADAX zostala zalozona w 1985 roku jako spolka jednoosobowa przez mgr Dariusza Adamiaka i od poczatku
firma nastawila sie na produkcje wloknin odziezowych i meblowych.</p>

<p>Dazac do  uzyskania wyrobow o najwyzszej mozliwej swiatowej
jakosci dokonywano zakupow parku maszynowego uznanych firm
niemieckich ktore sa na tym rynku wyznacznikiem najnowszych trendow najwyzszej jakosci.</p>

<h2>Technologia</h2>

<img src="img/photo01.png" width="120" height="120" alt="" class="unitPng" style="float:right; margin:10px"/>

<p>Na potrzeby dynamicznie rozwijajacego sie rynku mebli tapicerskich
zostaly uruchomione linie spelniajace swiatowe standardy, ktore zostaly uzupelnione o najnowsza automatyke.</p>

<p>Nasza produkcja jest proekologiczna - praktycznie bez odpadow. </p>

<p>Zakupiona w 1999 roku lina do pneumatycznego formownia wloknin
w systemie High-Loft jest (najwieksza w kraju) zapewnia uzyskanie
wloknin tapicerskich o wyjatkowych parametrach odpreznosci.</p>

<p>Dzieki wlasnemu transportowi i zapleczu logistycznemu zapewniamy
blyskawiczna obsluge.</p>

<h2>Wspolpraca</h2>

<p>Poszukujemy partnerow do stalej trwalej rzetelnej i uczciwej wspolpracy ktorzy doceniaja takze te cechy w naszym dzialaniu.</p>

