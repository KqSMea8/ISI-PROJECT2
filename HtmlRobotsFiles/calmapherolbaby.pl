<p style="text-align: justify;">Naskorek noworodka jest tylko&nbsp;troche cienszy i&nbsp;bardziej delikatny, ma mniej kwasny odczyn, a&nbsp;wartosc typowa dla doroslego (pH 5,4--5,9) osiaga po&nbsp;ukonczeniu kilku tygodni zycia. Ponadto jest mniej elastyczna i&nbsp;wyraznie jasniejsza, a&nbsp;tym samym jest bardziej wrazliwa na&nbsp;swiatlo sloneczne.<br />
