<p style="text-align: justify;">Lato to&nbsp;taki czas, kiedy nie&nbsp;tylko&nbsp;dorosli, <img class=" size-Wlasny wp-image-361 alignright" src="https://calmapherolbaby.pl/wp-content/uploads/2015/04/komar-437x264.jpg" alt="Close-up of a&nbsp;mosquito sucking blood" width="437" height="264" srcset="https://calmapherolbaby.pl/wp-content/uploads/2015/04/komar-437x264.jpg 437w, https://calmapherolbaby.pl/wp-content/uploads/2015/04/komar-300x181.jpg 300w, https://calmapherolbaby.pl/wp-content/uploads/2015/04/komar-1024x619.jpg 1024w, https://calmapherolbaby.pl/wp-content/uploads/2015/04/komar-630x381.jpg 630w, https://calmapherolbaby.pl/wp-content/uploads/2015/04/komar-157x95.jpg 157w, https://calmapherolbaby.pl/wp-content/uploads/2015/04/komar-900x544.jpg 900w" sizes="(max-width: 437px) 100vw, 437px" />ale&nbsp;przede wszystkim niemowleta i&nbsp;male dzieci sa bardziej narazone na&nbsp;ukaszenia komarow. Powoduja one ciagle drapanie i&nbsp;podraznianie, w wyniku ktorych&nbsp;pojawiaja sie zmiany skorne, niejednokrotnie dajace odczyn alergiczny. W&nbsp;takich sytuacjach trzeba szybko zastosowac preparaty zawierajace mikronizowany tlenek cynku i&nbsp;witamine E, ktore dzialaja kojaco i&nbsp;przeciwswiadowo.</p>
<p>&nbsp;</p>
<p style="text-align: justify;">Rumien wywolany promieniowaniem slonecznym</p>
<p style="text-align: justify;">Latem skora jest szczegolnie narazona na&nbsp;promieniowanie sloneczne UVA i&nbsp;UVB, ktore powoduja rumien posloneczny, czyli zaczerwienienie skory. Nasilenie takiego zaczerwienienia zalezy od&nbsp;zawartosci barwnika skory (melaniny), grubosci skory, pory roku oraz&nbsp;szerokosci geograficznej. Moze on powodowac stale uszkodzenia skory, jak np.&nbsp;przebarwienia. Powoduje tez wytwarzanie wolnych rodnikow, ktore przyspieszaja proces starzenia sie skory. Na&nbsp;rynku pojawil sie nowy preparat, Calmapherol S.C. ,<img class=" wp-image-362 size-Wlasny alignleft" src="https://calmapherolbaby.pl/wp-content/uploads/2015/04/slonce-437x291.jpg" alt="" width="437" height="291" srcset="https://calmapherolbaby.pl/wp-content/uploads/2015/04/slonce-437x291.jpg 437w, https://calmapherolbaby.pl/wp-content/uploads/2015/04/slonce-300x200.jpg 300w, https://calmapherolbaby.pl/wp-content/uploads/2015/04/slonce-1024x683.jpg 1024w, https://calmapherolbaby.pl/wp-content/uploads/2015/04/slonce-615x410.jpg 615w, https://calmapherolbaby.pl/wp-content/uploads/2015/04/slonce-140x94.jpg 140w, https://calmapherolbaby.pl/wp-content/uploads/2015/04/slonce-157x105.jpg 157w, https://calmapherolbaby.pl/wp-content/uploads/2015/04/slonce-900x600.jpg 900w" sizes="(max-width: 437px) 100vw, 437px" /> ktory&nbsp;dziala kojaco na&nbsp;rumien posloneczny, gdyz&nbsp;zawiera innowacyjny skladnik, czyli sol glicerofosfoinozytolu (GPI). Preparat jest alternatywa dla miejscowego leczenia kortykosteroidami. Zawarta w&nbsp;nim witamina E jest silnym reduktorem wolnych rodnikow, opoznia takze proces starzenia sie skory.<br />
Rumien wywolany promieniami UVB powoduje uszkodzenia nagle i&nbsp;chroniczne na&nbsp;skorze, jak np.&nbsp;(photoanging, nowotworzenie), ktore sa spowodowane dluga i&nbsp;czesta ekspozycja skory na&nbsp;sloncu. Rumien i&nbsp;stan zapalny maja obnizona bariere hydrolipidowa w&nbsp;warstwie rogowej skory, co moze wspomagac wchlanianie GPI do&nbsp;warstw glebokich naskorka.</p>


