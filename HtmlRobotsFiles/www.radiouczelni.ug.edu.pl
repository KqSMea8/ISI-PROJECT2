<h3 style="text-align: center;"><strong>Nowa edycja, nowe miejsce, nowe zasady konkursu! </strong></h3>
<p style="text-align: justify;">Studenckie rozglosnie radiowe od lat stanowia wazne centrum rozwoju kultury studenckiej w Polsce. Obecnie w miastach akademickich dziala ponad 30 rozglosni studenckich, w ktorych mlodzi ludzie stawiaja pierwsze kroki w zawodzie dziennikarza i poznaja niezwykla atmosfere radia. Rozsiani po calej Polsce maja niewiele okazji, by spotkac sie ze soba i wymienic doswiadczeniami. Dlatego tez - jako kierunek Dziennikarstwo i komunikacja spoleczna UG, Radio MORS &#8211; Mega Otwarte Radio Studenckie, Naukowe Kolo Radiowcow wraz z Radiem Gdansk - zapraszamy redakcje i badaczy z calej Polski do Gdanska na <strong>II Ogolnopolska Konferencje Naukowa Rozglosni Studenckich ,,Radioucze(L)ni"</strong>.</p>
<p style="text-align: justify;">Celem Konferencji jest prezentacja dzialalnosci studenckich rozglosni radiowych, integracja srodowiska mediow studenckich oraz dyskusja wokol roznorodnych zjawisk medioznawczych, zwiazanych z radioznawstwem, m.in.:</p>
<ul style="text-align: justify;">
<li>historia radiofonii studenckiej w Polsce i na swiecie,</li>
<li>misja, cele, funkcje radia studenckiego,</li>
<li>popularnosc mediow studenckich,</li>
<li>radioznawstwo praktyczne i teoretyczne,</li>
<li>edukacja radiowa,</li>
<li>przyszlosc dziennikarstwa radiowego.</li>
</ul>
<p style="text-align: justify;">W ramach Konferencji przyznawana jest takze nagroda ,,Zlotej Foki"  za najlepsza audycje radiowa ,,Zapytaj o Polske", poswiecona 100-leciu odzyskania niepodleglosci.</p>
<p>Studencie, studentko - zapytaj o Polske. Przedstaw nam jej oblicza. Pokaz zarowno jej codziennosc, jak i chwile dla niej wyjatkowe. Wysluchaj jej opowiesci i zaprezentuj ja w dowolnej formie dzwiekowej.</p>
<p>Czekamy na zgloszenia rozglosni studenckich. Wszystkie zgloszenia zostana zaprezentowane podczas II Ogolnopolskiej Konferencji Naukowej Rozglosni Studenckich ,,Radioucze(L)ni". Jury wyloni zwycieska rozglosnie radiowa, ktora otrzyma nagrode Zlotej Foki -  za najlepsza audycje radiowa ,,Zapytaj o Polske", poswiecona 100-leciu odzyskania niepodleglosci.</p>
<h3>Trzy najlepsze audycje beda emitowane na antenie Radia Gdansk!</h3>
<p style="text-align: justify;">Wiecej informacji o Konkursie: <a href="http://www.radiouczelni.ug.edu.pl/?page_id=26">tutaj</a>.</p>
<p style="text-align: justify;">Po Konferencji planowane jest wydanie numeru specjalnego czasopisma &#8222;<a href="http://www.ejournals.eu/MBK/">Media Biznes Kultura</a>&#8221; (czasopismo w trakcie parametryzacji, wkrotce uzyska punkty ministerialne). Artykuly zostana takze opublikowane na stronie Konferencji.</p>
