<p class="western" align="justify"><span lang="en-GB">The European Mycological Association and the Polish Mycological Society are very pleased to invite you to the XVIII Congress of European Mycologists (XVIIICEM), to be held from 16 to 21 September 2019, in Warsaw and Bialowieza, Poland. </span></p>
<p class="western" align="justify"><span lang="en-GB">The Congress keynote lectures, session talks and poster presentations will focus on fungal diversity and interactions, genomics, metabolomics, evolution and fungal conservation. We will discuss different strategies for bioremediation and biocontrol. An emphasis will be put on applying basic science and molecular techniques from diverse areas of mycology towards solving biomedical problems. We will pay special attention to fungi in primaeval forests and natural habitats, as we will have an exceptional opportunity to visit the Bialowieza Forest, one of the very last remaining natural lowland forests in Europe. Please, visit our website <a href="https://xviiicem.pl">https://xviiicem.pl</a> and see the programme.</span></p>
