<div class="entry-content">
<div>Polskie Towarzystwo Mykologiczne wspolorganizuje <b>w dniach 12-13 kwietnia 2019 roku konferencje skierowana do studentow i doktorantow pod nazwa <i>MycoRise Up! Mlodzi w mykologii</i>. </b>Mamy nadzieje, ze spotkanie takie pozwoli naszym mlodym kolegom sie poznac i nawiazac wspolprace oraz bedzie stanowic dobra motywacje do dalszej pracy w mykologii i rozwoju osobistego. Konferencja ta bedzie tez dobra alternatywa dla osob, dla ktorych za drogie bedzie uczestnictwo w Congress of European Mycologists Warsaw-Bialowieza 2019.</div>
<div></div>
<div>Jedna z zalet konferencji jest mozliwosc prezentacji swoich badan <b>w jezyku angielskim</b>. Wystapienia (referaty, multimedialne postery) w tym jezyku beda preferowane w ramach konferencji, choc nieobowiazkowe. Przewidujemy wyroznienia i motywujace nagrody materialne za najlepsze wystapienia ustne i postery.</div>
<div></div>
<div>Prosze rowniez zwrocic uwage na <b>pokrycie kosztow noclegu i czesciowo wyzywienia w ramach niskiej oplaty wpisowej</b> (110 zl).</div>
