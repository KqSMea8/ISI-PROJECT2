<p align="left">
<font size="2">Stowarzyszenie powstalo w czerwcu
1994 roku.<br>
Grupa zalozycielska opracowala wczesniej statut,
a nastepnie dokonala rejestracji w sadzie.<br>
Stowarzyszenie jest dobrowolnym zrzeszeniem osob
dotknietych choroba alkoholowa oraz ich rodzin,
a takze osob fizycznych ktore propaguja trzezwy
styl zycia.<br>
<br>
Do 1998 roku Stowarzyszenie miescilo sie przy
ulicy Kolomurnej 17a w budynku Zespolu Opieki
Zdrowotnej.<br>
W 1998 roku Urzad Miejski przekazal nam lokal
przy ulicy Rolnej 5. Budynek zostal odnowiony i
do dzis jest siedziba Stowarzyszenia.<br>
<br>
Jestesmy czlonkiem:<br>
Polskiej Rady Ruchu Abstynenckiego w
Skierniewicach<br>
Zwiazku Stowarzyszen Abstynenckich Wojewodztwa
Lodzkiego.<br>
<br>
Zadaniem Stowarzyszenia jest motywowanie do
zaprzestania picia poprzez spotkania otwarte w
klubie. Czlonkowie klubu dzialaja na zasadzie
wolontariatu, swoim zasiegiem obejmuja caly
powiat opoczynski.<br>
<br>
W siedzibie Stowarzyszenia organizowane sa
spotkania integracyjne, grupy wsparcia dla osob
z problemem alkoholowym, wspoluzaleznionych,
oraz dla dzieci z rodzin dysfunkcyjnych,
prowadzone przez wykwalifikowanych terapeutow.<br>
Istnieje punkt konsultacyjny dla osob
uzaleznionych od alkoholu i narkotykow, i dla
osob dotknietych przemoca domowa. Prowadzimy
zajecia zapobiegania nawrotom picia, treningi
radzenia ze zloscia, nauke asertywnosci oraz
warsztaty dla rodzicow.<br>
Przy Stowarzyszeniu istnieja grupy samopomocowe
Anonimowych Alkoholikow (AA), Anonimowych
Narkomanow (AN), Doroslych Dzieci Alkoholikow (DDA).
W grupach tych organizowane sa mitingi,
spotkania robocze. Na co dzien pracuja grupy
wstepne dla osob wspoluzaleznionych i
uzaleznionych, prowadzone przez
wyspecjalizowanych i doswiadczonych
wolontariuszy.<br>
Nasi czlonkowie biora czynny udzial w
szkoleniach Polskiej Rady Ruchu Abstynenckiego,
Zwiazku Lodzkiego oraz biezacych spotkaniach
Zarzadow innych Stowarzyszen.<br>
<br>
Stowarzyszenie &quot;OPOKA&quot; organizuje corocznie dla
swoich beneficjentow szereg imprezy jak: Dzien
Kobiet, Dzien Dziecka, Forum Trzezwosciowe,
Ogolnopolski Dzien Trzezwosci, Spotkania
Wielkanocne i Wigilijne oraz inne spotkania
integracyjne typu wycieczki i pielgrzymki, np.
do Czestochowy, Lichenia. Jestesmy rowniez
organizatorami w Opocznie Sylwestra
Bezalkoholowego.<br>
<br>
Przy Klubie Abstynenta istnieje sekcja pilki
noznej. Bierzemy udzial w rozgrywkach Ligi Pilki
Noznej Klubow Abstynenckich woj. lodzkiego, w
ramach ktorej organizujemy dwa turnieje w naszym
miescie.<br>
<br>
Jestesmy partnerami Urzedow Gmin i Komisji
Rozwiazywania Problemow Alkoholowych w
realizowaniu Gminnych Programow Profilaktyki i
Uzaleznien.</font><p align="center">
