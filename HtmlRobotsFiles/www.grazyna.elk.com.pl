<div class="PostContent">
<p style="text-align: justify;">Serdecznie zapraszamy do Pensjonaciku Grazyna na Mazurach. Pensjonacik polozony jest nad Jeziorem Elckim (tylko 30m od linii brzegowej) i jednoczesnie w scislym centrum miasta Elk.</p>
<p style="text-align: justify;">Do Panstwa dyspozycji sa pokoje jedno i dwuosobowe oraz apartament (pokoj z kuchnia) z tarasem i widokiem na jezioro.</p>
<p style="text-align: justify;">Zapraszamy rowniez do oddalonego o 150m od Pensjonaciku mieszkania. Na powierzchni 50m<sup>2</sup> znajda Panstwo w pelni wyposazona kuchnie z jadalnia, salon, sypialnie oraz lazienke (prysznic, pralka). Mieszkanie to idealne rozwiazanie dla rodzin, ktore cenia sobie komfort i niezaleznosc.</p>
<p style="text-align: justify;">Pensjonacik GRAZYNA oferuje w cenie pokoju wysmienite sniadania w formie bufetu szwedzkiego, ktore wprawia Panstwa w dobry nastroj na caly dzien</p>
<p style="text-align: justify;">Na terenie calego obiektu dostepny jest bezprzewodowy INTERNET.</p>
<p style="text-align: center;"><strong>Zapraszamy do zapoznania sie z cala oferta pensjonaciku.</strong></p>
<p style="text-align: center;">Gwarantujemy udany i przyjemny wypoczynek wsrod mazurskiej przyrody.</p>
<p style="text-align: center;">Pensjonacik Grazyna to idealne miejsce na wczasy rodzinne i pobyty weekendowe.</p>
