<h1>Strona glowna</h1>

<a href="/demo" title="Demo">
<img src="/images/main/nowe_demo.jpg" alt="Nowe demo" style="margin: 10px 0 5px 0"/>
</a>

<p>
Slyszac slowa: dobre wesele, zabawa, festyn, czy studniowka od razu myslimy "to temat dla nas". Dzialamy na rynku muzycznym
od blisko 20 lat, co jest gwarantem wysokiej jakosci uslug. Zespol Artigo sprawdza sie w roznych gatunkach muzycznych:
przypominamy numery od Hanki Ordonowny poczynajac, poprzez wszystkie dekady swingu, bluesa, rockandrolla, soulu, funki do
wspolczesnych nurtow muzycznych (Jennifer Lopez, Katy Perry, Enej, Maroon 5 i wiele innych).
</p>

<p>
Nasz repertuar staramy sie dopasowac do Panstwa wymagan, dlatego mile widziane sa sugestie imprezowiczow. Oprocz dobrej
muzyki nasza kapela oferuje niezly show i ciekawe zabawy weselne, kulturalnie prowadzona konferansjerke, profesjonalne
naglosnienie i oswietlenie o duzej mocy i skutecznosci, odpowiednie dopasowane do charakteru i rozmiarow pomieszczenia oraz
ewentualna ekipe techniczna potrzebna do jego obslugi. Sprzet moze byc uzywany takze przez innych wykonawcow,
konferansjerow oraz inne osoby zwiazane z organizacja przebiegu imprezy - kwestie te podlegaja wczesniejszym ustaleniom.
</p>

<p>
Dbamy o odpowiednie zaplanowanie badz stworzenie scenariusza imprezy, ktory uwzgledni wszystkie jej punkty, takze te
pozaartystyczne.
</p>

<p>
Oferujemy pomoc w zaangazowaniu dodatkowych wykonawcow, wynajeciu dodatkowych elementow techniki scenicznej itp.
</p>

<p>
Wszystko to nie byloby mozliwe na slabej jakosci sprzecie, dlatego stawiamy na takie firmy jak: JBL, Fender, Yamaha, Boss,
Selmer, Shure. Posiadamy rowniez wlasny transport.
</p>

