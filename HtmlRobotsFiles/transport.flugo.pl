<h1>Dlaczego Flugo</h1>

<p>
To juz 15 lat odkad Biuro Podroznikow Flugo zajmuje sie organizowaniem podrozy, wyjazdow, sprzedaza
biletow na wszelkie srodki transportu. To Panstwa coraz bardziej rosnace wymagania sprawily, iz zebralismy
tak ogromne doswiadczenie, dzieki ktoremu jestesmy w stanie sprostac kazdemu wyzwaniu. Dzieki
zrzeszeniu w Polskiej Izbie Turystyki, Polskiej Sieci Biur Podrozy i IATA dajemy gwarancje nalezytej obslugi
imprez i zabezpieczenia finansowego.
</p>

<p>
Jest nam niezmiernie milo poinformowac, ze do tej pory spelnilismy marzenia o podrozy wielu tysiecy
klientow, dlatego wlasnie zapraszamy do skorzystania z naszych uslug.
</p>

