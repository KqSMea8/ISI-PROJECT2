<b>Pensjonat Dr.Demeter - Rimetea</b><br><br>

Dysponujemy dwoma domami, ktore oddalone sa od siebie o 120 metrow. W mniejszym budynku mieszcza sie jedynie pokoje
goscinne, posilki serwowane sa w wiekszym. <br><br>

<b><span style="color: #FF0000">Mniejszy dom</span></b><br><br>
Mniejszy dom posiada trzy pokoje.
Pierwszy wyposazony jest w dwa lozka, drugi w trzy, natomiast ostatni w cztery. Znajduja sie tam trzy oddzielne
lazienki oraz wspolny dla wszystkich taras.


<br><br>

<b><span style="color: #FF0000">Wiekszy dom</span></b><br><br>
Wiekszy dom posiada osiem pokoi.
Cztery pokoje sa wyposazone w trzy lozka, cztery w dwa. We wszystkich sa dostepne lazienki.
W sumie dom pomiesci dwadziescie osob. Centralne ogrzewanie, sat-tv(telewizja satelitarna) oraz wi-fi jest
w kazdym pokoju.
<br><br>
Dysponujemy restauracja, ktora jest w stanie pomiescic maksymalnie 60 osob.<br>
Do (bezplatnej)dyspozycji naszych gosci posiadamy rowniez rowery. <br>
<br>
W sytuacji, kiedy goscimy duzy autokar(50-60 osob) mozemy skorzystac z dodatkowych pieciu pensjonatow, z ktorymi
wspolpracujemy. W zwiazku z tym mozemy zakwaterowac w sumie 50-60 osob u naszych sasiadow. Jednak oferuja oni
jedynie miejsce noclegowe, sniadania i obiady serwowane sa w naszym pensjonacie. Od najbardziej oddalonego pensjonatu
dzieli nas 120 metrow.
<br><br>
Menu jest bardzo bogate, szwedzki stol, w ktorego sklad wchodza zazwyczaj: 4 rodzaje kielbasek, salami, szynka,
2-3 rodzaje sera, 2-3 rodzaje dzemu, jajka, smazone kielbaski, mleko, kawa, 3 rodzaje herbaty, 2 rodzaje platkow
sniadaniowych, warzywa, miow, swieze pieczywo.
<br><br>

