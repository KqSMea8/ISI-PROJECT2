<h2 class="text-uppercase">Hosting</h2>
<p class="item-intro">Hosting to usluga polegajaca na udostepnieniu zasobow naszych serwerow na potrzeby uslug informatycznych Klienta.</p>
<img class="img-fluid d-block mx-auto" src="img/uslugi/ttcomp_hosting_big.png" alt="">
<p>

Uruchomienie poczty elektronicznej, strony, sklepu lub innej aplikacji internetowej wymaga zasobow serwera umieszczonego w internecie.
Serwer taki musi charakteryzowac sie niezawodnoscia, gdyz od tego uzaleznione jest poprawne dzialanie uslug, ktore sa przez ten serwer obslugiwane.

Hosting to usluga polegajaca na udostepnieniu zasobow naszych serwerow na potrzeby uslug informatycznych Klienta.
Klient moze wykorzystac nasze zasoby serwerowe na uruchomienie na swoje potrzeby poczty elektroniczne, stron internetowych, sklepow i innych aplikacji.
Nasze zasoby serwerowe cechuje wysoka niezawodnosc.
W ramach hostingu oferujemy:
- wynajem powierzchni dyskowej serwera
- wynajem serwerow wirtualnych

Wynajem powierzchni dyskowej serwera.
Na potrzeby uruchomienia stron internetowych, sklepow lub innych aplikacji czesto Klientowi wystarczy miejsce na dysku twardym serwera, dostep do bazy danych i przekierowanie adresu domenowego.
W takich przypadkach nie ma potrzeby wynajmowac calego serwera, a wystarczy jedynie jego czesc wspoldzielona z innymi uslugami pracujacymi na serwerze.
W ramach stalej wspolpracy nasi Klienci otrzymuja bezplatna mozliwosc skorzystania z powierzchni dyskowych naszych serwerow oraz administracji.

Wynajem serwerow wirtualnych.
Dla Klientow ktorzy wymagaja niezaleznych i dedykowanych serwerow swiadczymy uslugi wynajmu serwerow wirtualnych.
Na serwerach wirtualnych mozliwe jest uruchomienie uslug poczty elektronicznej, stron internetowych, sklepow, uslug FTP i inne.
Na potrzeby serwerow udostepniamy okreslona powierzchnie dyskowa i zasoby fizyczne serwerow.
Serwery wirtualne sa skonfigurowane zgodnie z wymaganiami Klienta.
Mozemy uruchomic systemy operacyjne Linux, Windows (zgodnie z posiadana przez Klienta licencja).
W ramach stalej wspolpracy nasi Klienci otrzymuja bezplatna mozliwosc skorzystania z powierzchni dyskowych naszych serwerow oraz administracji tymi serwerami.

Nasze serwery umieszczone sa w kolokacji spelniajacej najwyzsze wymagania stawiane profesjonalnym kolokacjom.
</p>

