<h1 class="entry-title"><a href="http://www.captaincook.pl/?p=5" title="Permalink to Grissini z prosciutto" rel="bookmark">Grissini z prosciutto</a></h1>
<div class="entry-meta">
Posted on <a href="http://www.captaincook.pl/?p=5" title="21:03" rel="bookmark"><time class="entry-date" datetime="2012-09-30T21:03:44+00:00" pubdate>30 wrzesnia 2012</time></a><span class="byline"> by <span class="author vcard"><a class="url fn n" href="http://www.captaincook.pl/?author=1" title="View all posts by admin" rel="author">admin</a></span></span>		</div><!-- .entry-meta -->
</header><!-- .entry-header -->

<div class="entry-content">
<p><a href="http://www.captaincook.pl/wp-content/uploads/2012/09/2420_grissini_luz.jpg"><img class="alignleft size-medium wp-image-6" title="grissini" src="http://www.captaincook.pl/wp-content/uploads/2012/09/2420_grissini_luz-300x145.jpg" alt="" width="300" height="145" /></a></p>
<p>Grissini to rodzaj wloskiego chrupkiego pieczywa, wyrabianego z drozdzowego ciasta, ktore produkowane jest w postaci cienkich paluszkow.</p>
<p>Historia powstania grissini datowana jest na XVII wiek. Jak glosi legenda, chorowity ksiaze Wiktor Amadeusz II z rodu Savoiow juz jako dziecko mial ogromne problemy zoladkowe. Nadworny lekarz Don Teobaldo Pecchio zdiagnozowal, ze przyczyna problemow trawiennych jest zle pieczony chleb. Lekarz wezwal do wspolpracy znajomego piekarza, Antonia Bruneto, ktory zaczal wypiekac dla chlopca pieczywo bez miekiszu, dobrze wyrosniete, i bardzo chrupkie. Ciasto w postaci slupkow musialo byc rozciagane (stirato) do dlugosci okolo pol metra.</p>
<p>Warto dodac, ze slawa tego pysznego pieczywa zaczela szybko rozprzestrzeniac sie rowniez za granica. Znanym smakoszem grissini byl m.in. Napoleon Bonaparte, ktory kazal przywozic je z Turynu do Paryza.</p>
<p>Chrupiace paluszki grissini mozna obecnie spotkac na stolach calego swiata. Piekarze z Piemontu, skad wywodzi sie pieczywo, produkuja je w dwoch rodzajach:</p>
<p>- tradycyjne, zwane stirati &#8211; dlugie i cienkie o regularnym ksztalcie<br />
- mniej znane, zwane rubata &#8211; krotsze, grubsze, o nieregularnym ksztalcie.</p>
<p>Grissini mozna podawac jako dodatek do zup oraz dipow, idealnie sprawdzaja sie rowniez jako przekaska dla dzieci i doroslych.  Ja zawsze trzymam w swojej spizarni kilka paczek tego przysmaku, ktory ratuje mnie w czasie niespodziewanych wizyt znajomych. Podaje je wtedy zawiniete w suszona szynke serano, prosciutto, parmenska lub nawet szynke staropolska wedzona pocieta w przezroczyste wrecz plasterki. Ponizej prezentuje Wam przepis. Smacznego!!!</p>
<p>&nbsp;</p>
<div id="attachment_16" class="wp-caption aligncenter" style="width: 630px"><a href="http://www.captaincook.pl/wp-content/uploads/2012/09/ok2.jpg"><img class="size-full wp-image-16" title="grissini z prosciutto" src="http://www.captaincook.pl/wp-content/uploads/2012/09/ok2.jpg" alt="" width="620" height="415" /></a><p class="wp-caption-text">grissini z prosciutto</p></div>
<p>&nbsp;</p>
<p><strong>Skladniki:</strong></p>
<ul id="internal-source-marker_0.879449548158264">
<li>12 paluszkow grissini</li>
<li>12 plasterkow szynki prosciutto</li>
<li>1/3 szklanki swiezo startego parmezanu</li>
<li>garsc rukoli</li>
<li>2 lyzki masla lub majonezu</li>
</ul>
<p>Na plasterkach szynki rozloz po 2-3 oplukane i osuszone listki rukoli. Maslo lub majonez wymieszaj z parmezanem i w przygotowanej pascie obtocz paluszki do polowy dlugosci. Nastepnie czesc pokryta serem owin szynka z rukola.</p>
<p>Paste mozna przygotowac rowniez na inne sposoby, np.  do masla i parmezanu dodac ser gorgonzola i twarozek.</p>
<p>Porada: jesli uzyjesz masla, pamietaj aby wczesniej je wyjac z lodowki. Schlodzone, zle rozprowadza sie na paluszkach.</p>
<p>&nbsp;</p>
