<h1 class="entry-title"><a href="http://www.captaincook.pl/?p=31" title="Permalink to Placki ziemniaczano-cukiniowe z lososiem i salsa z mango" rel="bookmark">Placki ziemniaczano-cukiniowe z lososiem i salsa z mango</a></h1>
<div class="entry-meta">
Posted on <a href="http://www.captaincook.pl/?p=31" title="17:40" rel="bookmark"><time class="entry-date" datetime="2012-10-13T17:40:08+00:00" pubdate>13 pazdziernika 2012</time></a><span class="byline"> by <span class="author vcard"><a class="url fn n" href="http://www.captaincook.pl/?author=1" title="View all posts by admin" rel="author">admin</a></span></span>		</div><!-- .entry-meta -->
</header><!-- .entry-header -->

<div class="entry-content">
<p>Gdy bylam dzieckiem, czesto z bratem prosilismy mame o przygotowanie na obiad plackow ziemniaczanych. Nie mielismy maszynki do tarcia warzyw wiec niezmordowanie, na zmiane tarlismy na tarce ziemniaki, kuszeni wizja chrupiacych placuszkow, polanych swieza smietana i posypanych cukrem. Bo takie najbardziej nam smakowaly.<br />
Wariacji na temat plackow jest wiele bo przeciez do ziemniaka tak naprawde mozna dodac wszystko. Amatorzy miesnej wersji na pewno beda zachwyceni plackami z pikantnym gulaszem wolowym, czyli tzw. plackiem po wegiersku, jaroszom z kolei mozna je podac z sosem grzybowym.<br />
Ja proponuje cos calkowicie odmiennego, ale wartego sprobowania &#8211; placki z ziemniakow i cukinii podane z lososiem i salsa z mango.</p>
<p>&nbsp;</p>
<p><a href="http://www.captaincook.pl/wp-content/uploads/2012/10/placki-z-salsa_11.jpg"><img class="aligncenter size-full wp-image-37" title="placki ziemniaczano-cukiniowe z lososiem i salsa z mango" src="http://www.captaincook.pl/wp-content/uploads/2012/10/placki-z-salsa_11.jpg" alt="" width="800" height="536" /></a></p>
<p>&nbsp;</p>
<p><strong>Skladniki:</strong></p>
<p>placki:</p>
<ul>
<li>3 ziemniaki</li>
<li>1 cukinia</li>
<li>2 zabki czosnku</li>
<li>2 lyzki maki</li>
<li>pieprz i sol</li>
<li>olej do smazenia</li>
</ul>
<p>salsa:</p>
<ul>
<li>2 male mango</li>
<li>1 czerwona cebula</li>
<li>1 limonka</li>
<li>po garsci swiezej miety i pietruszki</li>
<li>pieprz</li>
</ul>
<ul>
<li>120 g lososia</li>
</ul>
<p>Obrane ziemniaki wraz z cukinia zetrzec na tarce o grubych oczkach. Starte warzywa odsaczyc z powstalej wody, wymieszac z jajkiem, maka i wycisnietym przez praske czosnkiem oraz pieprzem i sola. Na patelni rozgrzac olej i smazyc placki na wolnym ogniu z dwoch stron.<br />
W trakcie smazenia mozna przygotowac salse. Obrane mango i cebule pokroic w kostke. Wrzucic do miseczki, zalac sokiem z limonki, posypac posiekana pietruszka i mieta oraz pieprzem. Wymieszac i wstawic do lodowki.<br />
Gotowe placki ulozyc na talerzu, na kazdym polozyc plasterek lososia, a na nim salse z mango.</p>
<p>Smacznego!!!</p>
<p>&nbsp;</p>
