<h1>Kancelaria notarialna Krak&#243;w | Notariusz Krak&#243;w Malgorzata Hedrzak-Slota, Notariusz Dariusz Klimonda</h1>
<p>
<div style="text-align: justify;"><strong>Kancelaria Notarialna Krak&oacute;w </strong>- Malgorzata Hedrzak-Slota, Dariusz Klimonda S.C. -&nbsp;<strong>Notariusze Krak&oacute;w</strong>&nbsp;oferuje obsluge prawna w zakresie dokonywania czynnosci notarialnych, w oparciu o przepisy ustawy Prawo o notariacie.&nbsp;Wszystkie czynnosci notarialne wykonujemy ze szczeg&oacute;lna starannoscia. Analizujac dostepne dokumenty i przepisy wskazujemy najlepsze rozwiazania, zgodne z prawem, aby skutek prawny czynnosci byl zgodny z oczekiwaniami Klienta.<br />
<br />
Kancelaria zlokalizowana jest&nbsp;w centrum starych Debnik - na Rynku Debnickim pomiedzy ulica Baluckiego oraz Tyniecka - <strong><span style="font-size: 13px; text-align: justify;">Krak&oacute;w</span></strong>, 5 minut pieszo od alei Marii Konopnickiej i 10 minut od ulicy Monte Cassino.<br />
Przed budynkiem znajduje sie kilkanascie miejsc parkingowych og&oacute;lnie dostepnych.<br />
Kancelaria &nbsp;posiada dla swoich Klient wydzielony parking, kt&oacute;ry miesci sie z tylu budynku - wjazd od ulicy Tynieckiej.<br />
<br />
<strong>Kancelaria </strong>- miesci sie na pierwszym pietrze budynku i czynna jest w godzinach od 9-18, przy czym istnieje mozliwosc ustalenia innych termin&oacute;w po wczesniejszym uzgodnieniu z notariuszem, r&oacute;wniez w soboty.&nbsp;Wszystkich informacji&nbsp;udziela&nbsp;nasi pracownicy w siedzibie Kancelarii, jak r&oacute;wniez telefonicznie oraz w formie elektronicznej. Zachecamy do skorzystania z ponizszego formularza kontaktowego.<br />
<br />
Malgorzata Hedrzak-Slota oraz Dariusz Klimonda&nbsp;<strong>Notariusze&nbsp;w Krakowie</strong> gwarantuja Panstwu profesjonalne podejscie do klienta. Gl&oacute;wna cecha <strong>kancelarii notarialnej</strong>&nbsp;dzialajacych na terenie <strong>Krakowa</strong> jest nie tylko rzetelna obsluga i fachowe wsparcie, ale r&oacute;wniez maksymalnie nowoczesne podejscie do powierzonych nam przez Panstwa spraw. Dokladamy wszelkich staran, by obsluga klient&oacute;w odbywala sie sprawnie.<br />
