<header class="entry-header">	<h2 class="entry-title" itemprop="headline"><a href="http://waldowo.salezjanie.pl/?p=1629" rel="bookmark">NIEDZIELA ZMARTWYCHWSTANIA PANSKIEGO  21.04.2019</a></h2>
<div class="entry-meta">
<time class="entry-time" datetime="2019-04-20T18:25:35+02:00" itemprop="datePublished" title="sobota, Kwiecien 20, 2019, 6:25 pm">20 kwietnia 2019</time>
<span class="entry-author" itemprop="author" itemscope="itemscope" itemtype="http://schema.org/Person">przez <a href="http://waldowo.salezjanie.pl/?author=2" title="Wpisy, ktorych autorem jest Proboszcz" rel="author" class="url fn n" itemprop="url"><span itemprop="name">Proboszcz</span></a></span>
</div><!-- .entry-meta --></header><!-- .entry-header -->
<div class="entry-summary" itemprop="description">
<ol>
<strong> Wszystkim Parafianom<br />
i sympatykom naszego dziela salezjanskiego,<br />
zycze aby milosc, radosc i pokoj<br />
plynace ze Zmartwychwstania Pana<br />
wypelnialy nasze serca<br />
i zapalaly do wielkodusznej odpowiedzi milosci<br />
w naszej zwyklej, szarej codziennosci.</strong>
<ol></ol>
<p>1. Pragne podziekowac wszystkim, ktorzy przyczynili sie do uswietnienia tych Uroczystosci Paschalnych: ekipie dekoracyjnej, ktora zrobila Ciemnice i Grob, Paniom sprzatajacym kosciol, wszystkim czytajacym i spiewajacym, orszakowi procesyjnemu, Ministrantom oraz wszystkim za modlitwe, za ofiary na kwiaty i na kosciol a przede wszystkim za obecnosc - Bog zaplac!</p>
<p>2<strong>. Dzis i przez caly tydzien, o godz. 15.00 bedzie wspolnie odmawiana Nowenna do Bozego Milosierdzia polaczona z Koronka.<br />
</strong><br />
3. Jutro, Poniedzialek Wielkanocny &#8211; II dzien swiat Msze swiete jak w kazda niedziele.</p>
<p>4. We wtorek o godz. 18.30 proba przed Bierzmowaniem w kosciele i egzamin Kurialny.</p>
<p>5.<strong> W piatek 26 kwietnia w czasie Mszy sw. o godz. 18.00, mlodziez z naszej parafii przyjmie Sakrament Bierzmowania </strong>przez posluge Bp. Janusza Ostrowskiego. Otoczmy tych mlodych ludzi nasza modlitwa. Zapraszamy na te wazna dla zycia Parafii uroczystosc</p>
<p>6. <strong>W przyszla niedziele Swieto Milosierdzia Bozego. Pragne, azeby pierwsza niedziela po Wielkanocy byla swietem Milosierdzia (Dz. 299).Pragne, aby swieto Milosierdzia, bylo ucieczka i schronieniem dla wszystkich dusz, a szczegolnie dla biednych grzesznikow. W dniu tym otwarte sa wnetrznosci milosierdzia Mego, wylewam cale morze lask na dusze, ktore sie zbliza do zrodla  milosierdzia  Mojego.  Ktora dusza  wyspowiadana przystapi  do  Komunii swietej, dostapi zupelnego odpuszczenia win i kar. W dniu tym otwarte sa wszystkie upusty Boze, przez ktore plyna laski. <br />
&#8211;  godz. 15.00 uroczysta koronka do Bozego Milosierdzia.  </strong></p>
<p>7. Niech radosc Zmartwychwstania, towarzyszy nam wszystkim i pozostanie jak najdluzej w naszych sercach. Szczesc Boze!</p>

