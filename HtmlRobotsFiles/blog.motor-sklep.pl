<h2 class="entry-title"><a href="http://blog.motor-sklep.pl/?p=20" rel="bookmark">Mocowania kufra centralnego GIVI MONOKEY i MONOLOCK &#8211; co to takiego?</a></h2>	</header><!-- .entry-header -->

<div class="entry-content">
<p>Rodzaje i zastosowanie centralnych mocowan kufrow motocyklowych <a href="http://www.motor-sklep.pl/kufry-centralne-c-456_457.html">GIVI</a></p>
<p>Rodzaje mocowan kufrow centralnych <a href="http://www.motor-sklep.pl/kufry-centralne-c-456_457.html">GIVI</a><br />Na przestrzeni lat firma <a href="http://www.motor-sklep.pl/kufry-centralne-c-456_457.html">GIVI</a> wypracowala sobie wygodny i niezawodny system mocowania kufra do plyty. Aktualnie jest to najpopularniejsze na swiecie rozwiazanie, dzieki ktoremu mozna swobodnie sciagac i zakladac wybrany produkt bez koniecznosci jazdy z nim na stale. Za pomoca jednego klucza mozna kufer zablokowac na plycie jak rowniez zamknac go.</p>


<p>Plyty mocujace kufry centralne dziela sie na dwie grupy:<br><a href="http://www.motor-sklep.pl/kufry-centralne-c-456_457.html">MONOKEY</a> - System docelowo wymyslony dla wszystkich motocykli i duzych skuterow. Plyta wykonana jest z bardzo wytrzymalego, sztywnego i lekkiego tworzywa sztucznego, ktory jest odporny na warunki atmosferyczne i uszkodzenia mechaniczne. Trzy punkty mocujace sa rozlozone na powierzchni plyty w taki sposob aby uzyskac mozliwie najwieksza powierzchnie styku. Tworcy zwrocili szczegolna uwage na wytlumienie wszystkich drgan przenoszonych z motocykla na kufer. Producent przewiduje ladownosc do 10kg, jednak w rzeczywistosci mozemy zapakowac troche wiecej. Bardzo duza sztywnosc calej konstrukcji umozliwia dynamiczna jazde szosowa ale rowniez odcinki szutrowe nie stanowia problemu dla kufrow <a href="http://www.motor-sklep.pl/kufry-centralne-c-456_457.html">GIVI MONOKEY</a>.<br>Dostepne plyty MONOKEY w ofercie GIVI <a href="http://www.motor-sklep.pl/plyta-givi-m3-pod-kufer-centralny-monokey-p-13.html">M3</a>, <a href="http://www.motor-sklep.pl/plyta-givi-m5-pod-kufer-centralny-monokey-p-79.html">M5</a>, <a href="http://www.motor-sklep.pl/plyta-givi-m7-pod-kufer-centralny-monokey-p-80.html">M7</a>, <a href="http://www.motor-sklep.pl/plyta-aluminiowa-givi-m8a-pod-kufer-centralny-monokey-p-684.html">M8A</a>, <a href="http://www.motor-sklep.pl/plyta-aluminiowa-givi-m8b-pod-kufer-centralny-monokey-czarna-p-685.html">M8B</a>, <a href="http://www.motor-sklep.pl/plyta-uniwersalna-givi-e251-pod-kufer-centralny-monokey-p-327.html">E251</a></p>



<div class="wp-block-image"><figure class="aligncenter is-resized"><img src="http://blog.motor-sklep.pl/wp-content/uploads/2019/01/M5.jpg" alt="" class="wp-image-34" width="340" height="340" srcset="http://blog.motor-sklep.pl/wp-content/uploads/2019/01/M5.jpg 700w, http://blog.motor-sklep.pl/wp-content/uploads/2019/01/M5-150x150.jpg 150w, http://blog.motor-sklep.pl/wp-content/uploads/2019/01/M5-300x300.jpg 300w" sizes="(max-width: 340px) 100vw, 340px" /><figcaption>Jedna z plyt w systemie MONOKEY <a href="http://www.motor-sklep.pl/plyta-givi-m5-pod-kufer-centralny-monokey-p-79.html">GIVI M5 </a></figcaption></figure></div>



<p>Bardzo popularne i sprawdzone kufry MONOKEY to seria <a href="http://www.motor-sklep.pl/szukaj.html/szukaj=kufer%20GIVI%20v47/opis=tak/nrkat=tak/kodprod=tak">V47</a> i <a href="http://www.motor-sklep.pl/szukaj.html/szukaj=kufer%20GIVI%20V56/opis=tak/nrkat=tak/kodprod=tak">V56</a> dla motocykli sportowo turystycznych, jesli jednak masz motocykl turystyczny to warto zwrocic uwage na serie <a href="http://www.motor-sklep.pl/szukaj.html/szukaj=kufer%20givi%20TRK52/opis=tak/nrkat=tak/kodprod=tak">TRK52</a> lub <a href="http://www.motor-sklep.pl/szukaj.html/szukaj=kufer%20givi%20outback%2058/opis=tak/nrkat=tak/kodprod=tak">OUTBACK 58</a>.</p>



<div class="wp-block-image"><figure class="aligncenter is-resized"><img src="http://blog.motor-sklep.pl/wp-content/uploads/2019/01/V47NNT_moto.jpg" alt="" class="wp-image-37" width="386" height="386" srcset="http://blog.motor-sklep.pl/wp-content/uploads/2019/01/V47NNT_moto.jpg 500w, http://blog.motor-sklep.pl/wp-content/uploads/2019/01/V47NNT_moto-150x150.jpg 150w, http://blog.motor-sklep.pl/wp-content/uploads/2019/01/V47NNT_moto-300x300.jpg 300w" sizes="(max-width: 386px) 100vw, 386px" /><figcaption>kufer <a href="http://www.motor-sklep.pl/kufer-givi-v47nnt-tech-monokey-czarny-mat-wstawka-wzor-carbon-p-193.html">GIVI V47NNT</a> w roli glownej</figcaption></figure></div>



<p>MONOLOCK - System przewidziany dla skuterow i malych motocykli miejskich. Producent zaleca ladownosc do 3kg, ale podobnie jak w przypadku poprzedniego rozwiazania spokojnie mozna przyjac na to poprawke. Jest to delikatniejsza i czesto tansza wersja mocowan <a href="http://www.motor-sklep.pl/kufry-centralne-c-456_457.html">GIVI</a>. Do kufrow <a href="http://www.motor-sklep.pl/kufry-centralne-c-456_457.html">MONOLOCK</a> plyta uniwersalna dokladana jest gratis. Zaleca sie jednak aby zakupic plyte dedykowana do stelaza dla konkretnego motocykla. Jesli posiadamy miejski jednoslad i mamy notorycznie problem z zapakowaniem zakupow lub spakowaniem sie do pracy/szkoly to jest to bardzo dobre wyjscie. Zazwyczaj kufry <a href="http://www.motor-sklep.pl/kufry-centralne-c-456_457.html">MONOLOCK</a> sa mniejsze i bardziej podreczne.</p>



<p>Dostepne plyty <a href="http://www.motor-sklep.pl/kufry-centralne-c-456_457.html">MONOLOCK</a> w ofercie GIVI <a href="http://www.motor-sklep.pl/plyta-givi-mm-pod-kufer-centralny-monolock-p-245.html">MM</a>, <a href="http://www.motor-sklep.pl/plyta-givi-m5m-pod-kufer-centralny-monolock-p-81.html">M5M</a>, <a href="http://www.motor-sklep.pl/plyta-givi-m6m-pod-kufer-centralny-monolock-u-lock-p-82.html">M6M</a></p>



<p>Z serii MONOLOCK polecam kufry <a href="http://www.motor-sklep.pl/szukaj.html/szukaj=givi%20b47%20kufer/opis=tak/nrkat=tak/kodprod=tak">B47</a> i analogicznie mniejszy <a href="http://www.motor-sklep.pl/szukaj.html/szukaj=givi%20kufer%20b37/opis=tak/nrkat=tak/kodprod=tak">B37</a>.</p>
