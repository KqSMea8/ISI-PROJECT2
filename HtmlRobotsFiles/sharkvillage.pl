<div class="content">

<div class="show">
<article class="slide current">
<header><h5>Shark Place</h5></header>
<p><img src="resources/images/info/surfer-v01.svg" alt="Shark Surfer.">Shark Village to centrum zycia rozrywkowego i czynnego wypoczynku. Na maksa zaladowane wszelimi atrakcjami, wsrod ktorych znajduja sie: scene z naglosnieniem, telebim, bar w stylu hawajskim, kino letnie, plac zabaw dla dzieci, boisko do siatkowki plazowej i wiele, wiele wiecej.</p>
<p>Shark Village to duze wydarzenie! Rozciagniete w czasie. 60 dni i 60 nocy niekonczacej sie imprezy: wystepy, koncerty, pokazy oraz zabawy dla dzieci i doroslych. Zapewnia to niepowtarzalna, jedyna w swoim rodzaju atmosfere przyciagajaca rzesze fanow.</p>
</article>
<article class="slide next">
<header><h5>Shark Food</h5></header>
<p><img src="resources/images/info/surfer-v01.svg" alt="Shark Surfer.">Wyzywienie i napojenie wyglodnialego rekina nie jest rzecza latwa! Dlatego, przygotowalismy meni, ktore zaspokoi rowniez wybredne rekiny. Na szybko polecamy shark-burgery, shark-dogi i kebab. Na dluzsze posiedzenie stek lub kotlet schabowy.</p>
<p>Na zakonczenie posilku, proponujemy wysmienita wloska kawe parzona w profesjonalnym ekspresie, lody i owocowe desery. Spragnionych zas, zapraszamy do niezle zaopatrzonego baru w stylu hawajskim na zimne piwo lub wymyslnego drinka. </p>
</article>
<article class="slide">
<header><h5>Shark Kids</h5></header>
<p><img src="resources/images/info/surfer-v01.svg" alt="Shark Surfer.">Specjalnie dla najmlodszego pokolenia rekinow przygotowalismy specjalna Strefe Kids. Jest to codzienny zestaw atrakcji, gier i zabaw dla dzieci w godzinach od 10:00 do 22:00. Strefa opiekuja sie dwie studentki majace doswiadczenie w pracy z dziecmi a w godzinach od 17:00 do 19:00 profesjonalni animatorzy.</p>
<p> W strefie znajdziecie Panstwo dzungle ze zjezdzalnie i zamkiem do skakania, trampoliny, piaskownice i plac zabaw a takze stoisko z popcornem i wata cukrowa. Przewidujemy rowniez czesta organizacje konkursow plastyczny i karaoke dla Panstwa pociech. </p>
</article>
<article class="slide previous">
<header><h5>Shark Show</h5></header>
<p><img src="resources/images/info/surfer-v01.svg" alt="Shark Surfer.">Po leniwie spedzonym dniu na plazy rekiny zadne nowych wrazen zapraszamy do naszej imprezowni. W tym sezonie przewidujemy szereg atrakcyjnych wystepow znanych zespolow, zabaw tanecznych prowadzonych przez najlepszych DJ-ow okraszonych zaskakujacymi niespodziankami.</p>
<p>W tym roku powroci rowniez hit poprzednich sezonow, czyli Silent Disco i Piana. </p>
</article>
