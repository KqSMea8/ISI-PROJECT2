<h1 style="text-align:center; font-size:13px;">Dywany, wykladziny, firanki, firany, karnisze tapety Krakow</h1>
<p>Witamy na stronie firmy A.Witek. Od 1998 roku zajmujemy sie sprzedaza artykulow wyposazenia wnetrz: dywanow, wykladzin, firanek i firan, tapet i karniszy. Nasz sklep zlokalizowany jest w kompleksie Centrum Witek Krakow. W ofercie naszego sklepu w Krakowie znajda Panstwo w szerokim wyborze: dywany, wykladziny, firanki, firany, tapety, karnisze i rolety. Czesc naszej oferty dostepna jest w naszym sklepie internetowym <a href="http://sklep24.dywanywitek.pl">sklep24.dywanywitek.pl</a>.</p>
<p><strong>Dywany</strong>: W szerokim wyborze oferujemy dywany, takze jedwabne, welniane, wiskozowe, chodniki oraz wykladziny dywanowe i elastyczne PCV. W zakresie naszych uslug swiadczymy takze obszywanie dywanow i wykladzin.</p>
<p><strong>Firanki i firany</strong>: W dziale firan i firanek znajda Panstwo imponujaca ilosc tkanin o bogatej strukturze i kolorystyce. Poprzez organtyny, woale, firanki z kreszu, tiule, batysty, markizety, tergale, siatki, gipiury, zakardy az do firanek sznurkowych. Dysponujemy takze bogactwem zaslon z poliestru, lnu, jedwabiu, taftu, szenilu, kreszu i satyny. W zakresie naszych uslug wykonujemy wszelkie i kompleksowe aranzacje okienne, szycie firanek i firan oraz zaslon oraz fachowe doradztwo w domu klienta (pomiary okien, dobor karniszy, firan i zaslon, rolet i przygotowanie projektow).</p>
<p><strong>Karnisze</strong>:W naszym sklepie znajda Panstwo najwiekszy w Krakowie wybor karniszy i zabezpieczen przeciwslonecznych. W ofercie mamy karnisze: metalowe, kute, linkowe, miedziane, mosiezne, ze stali nierdzewnej, z plexi, z krysztalowymi koncowkami, drewniane, szyny plastikowe oraz szyny aluminiowe KS-system z mozliwoscia giecia.</p>
<p><strong>Rolety</strong>: Posiadamy Szeroki wybor rolet tekstylnych, winylowych, w kasetach, w prowadnicach, przyklejanych, o roznym stopniu przepuszczalnosci swiatla i ciepla, czyszczacych powietrze, rzymskich, austriackich, bambusowych, z ozdobnymi falbankami lub obciaznikami, do wszystkich okien dachowych, niepalnych i odpornych na wilgoc.</p>
<p><strong>Tapety</strong>: Harmonijnie wkomponowane we wnetrze naszego otoczenia kreuja pozytywne nastawienie do zycia. Dzieki tapetom mozna w kazdym pokoju stworzyc inna atmosfere, inny swiat. Bogata paleta wzornictwa w roznorodnej kolorystyce i fakturze z programu tapet naszej firmy to spelnienie kazdego zyczenia.
W naszym sklepie w Krakowie znajda Panstwo tapety winylowe, papierowe, flizelinowe, tekstylne, jedwabne, flokowane, bambusowe, sizalowe, z wlokna Agawy, na flizelinie i do malowania, z krysztalkami Svarowskiego.</p>
<p>Zapraszamy goraco do zapoznania sie z nasza oferta na terenie sklepu A.Witek w Centrum Witek Krakow oraz z oferta online w naszym sklepie internetowym <a href="http://sklep24.dywanywitek.pl">sklep24.dywanywitek.pl</a>.</p>
