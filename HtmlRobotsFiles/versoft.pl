<h2>Audyt IT</h2>
<img src="images/pic/audyt.png" alt="Audyt IT" class="img-responsive">
<p>Dlaczego jest tak wazny? Bo audyt IT pozwala zdiagnozowac, czy system informatyczny i powiazany z nim sprzet wlasciwie chronia majatek Twojej firmy, utrzymuja spojnosc danych i czy dostarczaja rzetelnych informacji. Taka wiedza jest bezcenna, bo dzieki niej latwiej mozesz chronic sie przed niepozadanymi zdarzeniami lub bardzo szybko je wykryc. Audyt systemu informatycznego pozwala zapewnic bezpieczenstwo infrastruktury, do ktorej nalezy sprzet wraz z zainstalowanym oprogramowaniem. Dzieki audytom bedziesz mogl tez lepiej zaplanowac wydatki na zakup sprzetu i oprogramowania.</p>
