<h2>Bezpieczenstwo informacji</h2>
<img src="images/pic/security.png" alt="bezpieczenstwo systemow informatycznych" class="img-responsive">
<p>Nikt nie chce, zeby dane z systemu informatycznego jego firmy dostaly sie w niepowolane rece. Warto miec wiec w zanadrzu procedury postepowania i plan ciaglosci dzialania w sytuacjach, kiedy bezpieczenstwo zostalo naruszone. Tym bardziej, ze cyberprzestrzen pelna jest rozmaitych zagrozen. Dlatego na bazie naszego kilkunastoletniego doswiadczenia oferujemy m.in. przeprowadzenie analiz ryzyka pozwalajacych okreslic najlepsze rozwiazania dla polityki bezpieczenstwa, zbadanie, ktore zasoby systemu powinny byc chronione i w jaki sposob, poprowadzimy tez szkolenia dla pracownikow. Zapewnione bezpieczenstwo systemow informatycznych jest nieodlacznym elementem ochrony systemu przed zagrozeniem atakami hakerskimi. W zwiazku z tym przygotowana polityka bezpieczenstwa systemu informatycznego jest kluczem do bezpiecznego funkcjonowania systemu i zwiazanej z nim infrastruktury.</p>
