<h2 class="section-header" style="text-align: left;">OFERTA</h2>
<p style="text-align: left;">Firma Rockheroes oferuje kursy wspinaczki sportowej, wspinaczki na sztucznej scianie a takze treningi majace na celu podniesienie umiejetnosci poruszania sie po skale.</p>
<p style="text-align: left;">Zajecia prowadzone sa na Jurze krakowsko-czestochowskiej oraz na sciankach wspinaczkowych: KS Korona, Avatar i Renisport.</p>
<p style="text-align: left;">W ofercie mamy takze szkolenia dla firm, wyjazdy weekendowe  i obozy wspinaczkowe w Grecji, Wloszech i Slowenii.</p>
<div>
<ul class="nav nav-tabs" style="text-align: left;">
<li><a href="#k1" data-toggle="tab">Kurs wspinaczki na sztucznej scianie</a></li>
<li><a href="#k2" data-toggle="tab">Kurs wspinaczki po drogach ubezpieczonych</a></li>
</ul>
<p style="text-align: left;"><!-- Tab panes --></p>
<div class="tab-content">
<div id="k0" class="tab-pane active" style="text-align: left;">
<p class="text-center">Prowadze kursy i treningi wspinaczkowe. Kurs wspinaczkowy trwa kilka dni, podczas ktorych przyszly przyszly adept dowiaduje sie jak bezpiecznie uprawiac wspinaczke w skalkach.</p>
</div>
<div id="k1" class="tab-pane" style="text-align: left;">
<p class="text-center">Ten kurs przygotowuje do samodzielnego korzystania ze sztucznych obiektow wspinaczkowych.</p>
<p class="text-center">Sprzet omawiany jest pod katem optymalnych zakupow wlasnego wyposazenia. Kurs trwa minimum dwa a najlepiej 3 dni (chetnie 3, niekoniecznie nastepujace po sobie, popoludnia) i odbywa sie na sztucznej scianie <a title="Centrum Wspinaczkowe MyAvatar" href="http://myavatar.pl/pl/">Avatar</a> lub <a title="Centrum Wspinaczkowe ReniSport" href="http://renisport.pl/">ReniSport</a>.</p>
<p class="text-center">Podczas zajec zapoznacie sie ze sztuczna sciana, jej budowa, punktami asekuracyjnymi, stanowiskami do wedki oraz zasadami bezpieczenstwa. Wytlumacze i opisze uzywany sprzet i zasady jego uzytkowania:</p>
<ul class="text-center">
<li>liny</li>
<li>uprzeze biodrowe</li>
<li>karabinki i ich rodzaje</li>
<li>ekspresy</li>
<li>przyrzad asekuracyjny Reverso</li>
<li>buty i akcesoria</li>
</ul>
<p class="text-center">Zasady asekuracji:</p>
<ul class="text-center list-group">
<li>zapiecie uprzezy, przywiazanie do niej liny</li>
<li>asekuracja na wedke korzystajac z reverso</li>
<li>asekuracja dolna ( technika wpinek, prowadzenie liny, podawanie liny do wpinki, nauka odpadniec )</li>
<li>wlasciwa postawa asekurujacego</li>
<li>spotting (asekuracja) boulderujacego</li>
</ul>
<p class="text-center">Wezly i ich zastosowanie:</p>
<ul class="text-center list-group">
<li>osemka</li>
<li>kluczka</li>
<li>flagowy</li>
<li>zwijanie liny</li>
</ul>
<p class="text-center">Nauczanie techniki wspinania:</p>
<ul class="text-center list-group">
<li>wykorzystanie chwytow i stopni</li>
<li>ustawienia ciala: pozycja frontalna i boczna</li>
<li>wspinaczka statyczna i dynamiczna</li>
<li>poruszanie sie w terenie przewieszonym</li>
</ul>
</div>
<div id="k2" class="tab-pane" style="text-align: left;">
<p class="text-center">Ten kurs przygotowuje do samodzielnego wspinania na wedke i z dolna asekuracja po drogach wyposazonych w punkty asekuracyjne (obitych) w terenie naturalnym. Daje rowniez podstawy do korzystania z obiektow sztucznych.<br />
Sprzet omawiany jest pod katem optymalnych zakupow wlasnego wyposazenia.</p>
<p class="text-center">Kurs trwa minimum cztery dni (optymalnie dwa kolejne weekendy) i ma miejsce w podkrakowskich dolinach. Podczas zajec zapoznacie sie z zagrozeniami wynikajacymi z przebywania w terenie skalnym a takze zasadami bezpieczenstwa.</p>
<p class="text-center">Opisze uzywany sprzet i wytlumacze zasady jego uzytkowania:</p>
<ul class="text-center">
<li>liny</li>
<li>uprzeze biodrowe</li>
<li>karabinki i ich rodzaje</li>
<li>ekspresy</li>
<li>przyrzad asekuracyjny Reverso</li>
<li>buty i akcesoria</li>
<li>stale punkty asekuracyjne zainstalowane w skale</li>
</ul>
<p class="text-center">Zasady asekuracji:</p>
<ul class="text-center">
<li>zapiecie uprzezy, przywiazanie do niej liny</li>
<li>asekuracja na wedke korzystajac z reverso ( wlasciwa postawa asekurujacego )</li>
<li>asekuracja dolna (technika wpinek, prowadzenie liny, podawanie liny do wpinki nauka odpadniec ( wlasciwa postawa asekurujacego )</li>
<li>spotting (asekuracja) bulderujacego</li>
<li>nauka asekuracji na zrzutni</li>
<li>nauka budowania wlasnych stanowisk oraz wykorzystywanie juz istniejacych stanowisk zamontowanych na stale</li>
<li>przewiazywanie liny przez ring zjazdowy; zdejmowanie ekspresow podczas opuszczania</li>
<li>zjazdy na linie w wysokim przyrzadzie &#8211; gotowe stanowiska zjazdowe; zjazd z posrednim stanowiskiem.</li>
