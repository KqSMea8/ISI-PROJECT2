<h3>Krok I</h3>
<div>
Przed przystapieniem do testowania predkosci lacza <b>prosimy wylaczyc</b> wszystkie aplikacje korzystajace z uslug internetowych m.in. aplikacje P2P, menadzery pobierania plikow itp. Na czas przeprowadzenia testow wskazanym jest wylaczyc rowniez wszelkie zapory sieciowe oraz oprogramowanie antywirusowe.
</div>

<h3 style="margin-top: 20px;">Krok II</h3>
<div>
Przetestuj predkosc pobierania oraz wysylania za pomoca lokalnego serwera speedtest. Klikajac w przycisk (START) po chwili otrzymaja Panstwo wyniki pomiaru szybkosci pobierania (POBIERZ) oraz wysylania (WCZYTAJ).
</div>
