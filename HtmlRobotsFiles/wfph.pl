<h1>O funduszu WFPH</h1>
<h2>Warszawski Fundusz Pozyczek Hipotecznych specjalizuje sie w udzielaniu prywatnych pozyczek zabezpieczonych na nieruchomosciach. Oferte kierujemy zar&oacute;wno do os&oacute;b fizycznych jak i os&oacute;b prawnych.</h2>
<p>Nasze produkty to polaczenie wygody i bezpieczenstwa, tak aby szybko i komfortowo odpowiedziec na potrzeby Klient&oacute;w.</p>
<p>Dzialamy nieprzerwanie od 2001 roku. Nasz zesp&oacute;l tworza wysokiej klasy specjalisci, kt&oacute;rzy zdobywali doswiadczenie w polskich i miedzynarodowych instytucjach finansowych. Tym samym jestesmy w stanie zapewnic ekspercka obsluge i efektywnie spelnic oczekiwania Klient&oacute;w.</p>

