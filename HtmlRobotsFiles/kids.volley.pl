<h2 class="right_col_title">Fair Play</h2>
<p><em>Rywalizacja jest wspanialym przezyciem jesli przestrzega sie okreslonych regul. O wyniku sportowym powinny decydowac wylacznie umiejetnosci prezentowane w sportowej walce.<br />Kazda proba nieuczciwych zachowan niszczy sens wspolzawodnictwa.<br />Sportowcom, ktorzy potrafia w chwilach zacietej rywalizacji pamietac o szacunku dla rywala, ktorzy potrafia czasami tracac szanse na swoj sukces przestrzegac zasad uczciwej gry, przyznaje sie corocznie jedna z najcenniejszych sportowych nagrod FAIR PLAY ("czysta gra").<br />Od pierwszych chwil sportowej przygody warto o tym pamietac, a wtedy sport zawsze bedzie piekny i emocjonujacy. Tego zyczymy wszystkim mlodym siatkarkom i siatkarzom.<br /><br />Organizatorzy KIDS VOLLEY<br /></em></p>
