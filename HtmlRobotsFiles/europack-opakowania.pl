<p class="title_site">Strona gl&#243;wna</p>
<p>Euro-Pack S.C. producent opakowan z folii sztywnej oferuje klientom: opakowania termoformowalne, opakowania z folii sztywnej, opakowania sztancowane, opakowania wykrawane oraz wiele innych produkt&oacute;w z folii twardej</p>

<p>&nbsp;</p>

<p>Z posr&oacute;d szerokiej gamy opakowan z folii sztywnej oferujemy m.in.:</p>

<p>- blistry zgrzewane, blistry zaginane, blistry dwustronne, blistry stojace</p>

<p>- wytloczki &ndash;wkladki do pudelek kartonowych, wklady do ksiazek i czasopism, wkladki foliowe na ciasteczka i czekoladki, pojemniki termoformowalne, opakowania na zywnosc, wytloczki z grubych folii</p>

<p>- wytloczki w plyt &ndash; wytloczki z tworzyw sztucznych, tace transportowe, obudowy, wklady</p>

<p>- pudelka wykrawane &ndash; skladane (pudelka z wykrojnika) &ndash; opakowania prostopadloscienne, wklady do skrzynek i pudelek kartonowych</p>

<p>- tuby i tunele z folii sztywnej</p>

<p>- displaye - wieczka z folii - pudelka klejone i pudelka ozdobne.</p>

<p>&nbsp;</p>

<p>Oferujemy r&oacute;wniez uslugi z zakresu tloczenia reklamowego - woblery reklamowe, jak r&oacute;wniez arkuszowanie folii sztywnej.</p>

