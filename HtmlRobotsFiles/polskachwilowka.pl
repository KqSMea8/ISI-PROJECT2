<div class="content-wrapper container"><h1>Kompendium wiedzy o Filarum</h1>

<figure class="amp-featured-image"> <amp-img src='https://polskachwilowka.pl/wp-content/uploads/2018/08/logo-filarum-1.png' width='602' height='200' layout=responsive alt='Filarum'></amp-img>			</figure>
<p>Firma pozyczkowa Filarum to uznana marka, nalezaca do miedzynarodowej grupy Rapid Finance Sp. z.o.o. Na polskim rynku pojawila sie w 2013 roku - szybko zyskujac zaufanie oraz sympatie odbiorcow.</p>
<p>Specjalizacja Filarum jest przede wszystkim udzielanie pozyczek w kwotach o sredniej wysokosci, z uwzglednieniem systemu jednorazowej splaty. Marka oferuje swoim klientom atrakcyjne warunki zadluzenia, czego przykladem moze byc calkowicie darmowa, nieoprocentowana i pozbawiona prowizji pierwsza pozyczka. Za bezpieczenstwem wspolpracy z Filarum odpowiada bez watpienia wysoki kapital zakladowy oraz niemale, zdobyte przez lata doswiadczenie - przedsiebiorstwo Rapid Finance Sp.z.o.o zarzadza bowiem takze inna uznana firma pozyczkowa, Soho Credit.</p>
