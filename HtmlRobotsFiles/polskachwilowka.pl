<!-- Warunki pozyczki-->

<div class="dzial" id="warunki-pozyczki">
<h2>Warunki pozyczki</h2>
<p>Pozyczka w Filarum &#8211; szczegolnie ta pierwsza &#8211; jest bardzo atrakcyjna pozycja na rynku, bo nie ponosimy przy niej zadnych kosztow. Dodatkowo, wszystkie formalnosci sprowadzaja sie do przelewu weryfikujacego (o tym piszemy ponizej).</p>
</div>


<!-- Maksymalna kwota pozyczki-->

<div class="dzial" id="maksymalna-kwota">
<h2>Maksymalna kwota pozyczki</h2>
<p>Maksymalna kwota pozyczki przy pierwszej pozyczce wynosi 1000 zl. Po jej splaceniu kolejne pozyczki moga opiewac na kwoty do 5000 zl.</p>

Filarum to oczywiscie nie jedyne miejsce z taka oferta. Sprawdz, gdzie jeszcze <a href="/kwota-pierwszej-pozyczki/1000/">kwota pierwszej pozyczki to 1000 zl</a>.

Z kolei na tej liscie znajdziesz wszystkie firmy, w ktorych <a href="/kwota-pozyczki/5000/">maksymalna kwota pozyczki to 5000 zl</a>.

</div>


<!-- RRSO (dla pierwszej, dla kolejnych)-->


<div class="dzial" id="rrso">
<h2>RRSO dla pierwszej i dla kolejnych pozyczek w Filarum</h2>
<p>RRSO przy pierwszej pozyczce wynosi 0%, poniewaz pierwsza pozyczka w Filarum jest darmowa. Przy kolejnych pozyczkach wskaznik ten oczywiscie znaczaco rosnie; przykladowo przy kwocie 2000 zl pozyczonej na 30 dni, RRSO wyniesie 1936.3%.</p>
Przykladowa pierwsza pozyczka:<br />
<ul style="margin-top: 7px;">
<li>pozyczka: <b>pierwsza</b></li>
<li>kwota: <b>1000 zl</b></li>
<li>okres: <b>30 dni</b></li>
</ul>
= <b>RRSO: 0%</b>


<br /><br />Przykladowa druga pozyczka:<br />
<ul style="margin-top: 7px;">
<li>pozyczka: <b>druga</b></li>
<li>kwota: <b>2000 zl</b></li>
<li>okres: <b>30 dni</b></li>
</ul>
= <b>RRSO: 1936.3%</b>

</div>


<!-- Pierwsza pozyczka za darmo -->

<div class="dzial" id="pierwsza-za-darmo"><h2>Pierwsza pozyczka w za darmo - jak wyglada to w Filarum?</h2>
<p>Filarum oferuje pierwsza pozyczke za darmo, bez zadnych kosztow. Jedyne ograniczenie to kwota i okres splaty &#8211; jest to maksymalnie 1000 zl do splaty w max. 30 dni. Po splaceniu darmowej pozyczki mozna starac sie o kolejna, o wiekszej wartosci.</p>

Nie jest to oczywiscie jedyny pozyczkodawca, ktory oferuje darmowe pozyczki. Na tej stronie znajdziecie <a href="/koszt-pierwszej-pozyczki/tak/">liste firm udzielajacych pozyczek za darmo</a>.<br /><br />

</div>


