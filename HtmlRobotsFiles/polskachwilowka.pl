<div class="dzial" id="wniosek-o-pozyczke">
<h2>Wniosek o pozyczke w Filarum - jak wypelnic i jaki jest proces rozpatrywania?</h2>

Proces wnioskowania o pozyczke w Filarum jest bardzo prosty. Wystarczy przejsc do <a href="https://www.filarum.pl/rejestracja" rel="nofollow" target="_blank">tego formularza</a>.
<ol>
<li>Na stronie wniosku wypelnij wszystkie dane.</li>
<li>Zaakceptuj wymagane zgody i zakoncz rejestracje.</li>
<li>Przelej 1 grosz w celu weryfikacji na wybrane konto bankowe. Jesli na liscie kont znajduje sie Twoj bank, wybierz go. Jesli Twoj bank nie widnieje na liscie, wybierz dowolny (sugerowany PKO BP).</li>
<li>Jesli posiadasz konto z listy, pozyczke otrzymasz bardzo szybko. W innym razie musisz zaczekac, az przelew z pozyczka zostanie zaksiegowany pomiedzy bankami (max. 1 dzien roboczy). Jesli zlozysz wniosek przed popoludniem, pozyczka powinna dotrzec tego samego dnia.</li>
</ol>
