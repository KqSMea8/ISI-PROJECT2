<!-- Bez BIK-->
<div class="dzial" id="pozyczka-bez-bik">
<h2>Pozyczka bez BIK?</h2>

Filarum, jak kazda instytucja udzielajaca pozyczek, zastrzega sobie prawo do weryfikowania zdolnosci kredytowej w wybranych zrodlach.

<ul style="margin-top: 7px;"><li><a href="https://polskachwilowka.pl/weryfikacja/big-infomonitor/">BIG InfoMonitor</a> - System BIG InfoMonitor pozwala na sprawdzenie stanu zadluzenia osob i firm. Jest platforma wymiany danych miedzy instytuacjami finansowymi i innymi podmiotami gospodarczymi; umozliwia sprawdzenie potencjalnego kontrahenta w Biurze Informacji Kredytowej i Zwiazku Bankow Polskich.</li><li><a href="https://polskachwilowka.pl/weryfikacja/krajowe-biuro-informacji-gospodarczej/">Krajowe Biuro Informacji Gospodarczej</a> - KBIG to calodobowa platforma pozwalajaca sprawdzic zdolnosc kredytowa przedsiebiorstw i prywatnych konsumentow na podstawie numeru NIP w przypadku firm lub numeru PESEL w przypadku osob fizycznych.</li><li><a href="https://polskachwilowka.pl/weryfikacja/krd-krajowy-rejestr-dlugow/">KRD - Krajowy Rejestr Dlugow</a> - Krajowy Rejestr Dlugow umozliwia szybkie stworzenie raportu dotyczacego zadluzenia konsumenta lub firmy, pobierajac dane z wlasnego rejestru, a takze KRS, KNF, CEIDG i innych.</li><li><a href="https://polskachwilowka.pl/weryfikacja/rejestr-dluznikow-erif/">Rejestr Dluznikow ERIF</a> - Biuro informacji Gospodarczej, dzialajace od 2003 roku, nalezace do Grupy Kapitalowej KRUK. Przechowuje i udostepnia dane o dluznikach oraz o konsumentach regularnie splacajacych swoje zobowiazania.</li></ul></div>

