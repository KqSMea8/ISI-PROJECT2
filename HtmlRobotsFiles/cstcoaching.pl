<p style="text-align: justify;">Firma <strong>CST Coaching</strong> Anita Orzechowska to firma coachingowo &#8211; szkoleniowa, zajmujaca sie rozwojem osobistym, firm i organizacji. Usluga rozwojowa wykonywana jest przez prowadzenie szkolen zamknietych i otwartych, proces&oacute;w coachingowych, trening&oacute;w of the job, doradztwa, mentoringu oraz superwizji. Oferowane uslugi swiadczone sa na najwyzszym poziomie, a zakres oraz poziom dostosowany jest do oczekiwan klient&oacute;w poprzez wczesniejsza analize potrzeb.</p>
<p style="text-align: justify;">Jestesmy firma, kt&oacute;ra tak jak rynek, ciagle sie zmienia. Podnosimy kwalifikacje wlasne, aby profesjonalnie uczyc i wspierac innych.&nbsp;Jezeli jest to mozliwe, pracujemy na terenie calej Polski. Dysponujemy kadra, kt&oacute;ra sprosta najtrudniejszym zadaniom, niemozliwemu i zawsze na najwyzszym poziomie.</p>
<p style="text-align: center;"><strong>Znajdziecie Panstwo u Nas</strong>:</p>
<p style="text-align: center;">&#8211; <strong>coacha:</strong> executive, sprzedazy,&nbsp;edukacji, kariery, biznesu, life,</p>
<p style="text-align: center;">&#8211; <strong>trenera</strong>: biznesu, sprzedazy, edukacji, kompetencji coachingowych, komunikacji interpersonalnej, kreatywnosci,</p>
<p style="text-align: center;">&#8211; mentora i doradce biznesowego,</p>
<p style="text-align: center;">&#8211; superwizora pracy coacha, pracownik&oacute;w na ich stanowiskach pracy.</p>
<p><strong>Nasza Misja to</strong>: Nowa jakosc Twojego zycia, biznesu, kariery. Szkolimy i wzmacniamy. Nowa jakosc polskiego przedsiebiorstwa &ndash; poprzez rozw&oacute;j osobisty pracownik&oacute;w.</p>
<p><strong>Wartosci jakimi sie kierujemy: </strong></p>
<p><strong>Wolnosc</strong> &ndash; zdobywajac nowa wiedze, umiejetnosci, rozwiazania &#8211;&nbsp; czlowiek, firma, organizacja moze dokonywac wyboru, przez co staje sie wolny.</p>
<p><strong>Uczciwosc</strong> &ndash; nie obiecujemy gruszek na wierzbie, prowadzimy klient&oacute;w do wyznaczonych przez nich cel&oacute;w prywatnych, biznesowych, kariery.</p>
<p><strong>Rozw&oacute;j </strong>&ndash; jedyne co jest pewne to zmiana, ciagly rozw&oacute;j i doskonalenie naszych uslug sprawia, ze Nasi klienci przechodza zmiany lagodnie w spos&oacute;b dla nich najbardziej odpowiedni,</p>
<p><strong>Ciekawosc</strong> &ndash; czlowiek ciekawy znajduje to, czego szuka, pracujemy z biznesem, edukacja, osobami prywatnymi, jestesmy ciekawi nowych rozwiazan.</p>
<p><strong>Wiara</strong> &ndash; wierzymy, ze kazdy moze osiagnac sukces, my w tym pomagamy.</p>
<p><strong>Nasza Wizja:</strong> Jestesmy firma otwarta na nowe wyzwania, stale rozwijajaca sie, dostosowujaca do potrzeb rynku i ludzi.</p>
<p><strong>Gl&oacute;wnym celem firmy</strong>: jest dostarczanie uslug na najwyzszym poziomie w ramach obejmujacym dana tematyke z zakresu zarzadzania, sprzedazy, biznesu, umiejetnosci interpersonalnych, rozwoju osobistego, rozwoju firmy.</p>
<p>Cel realizujemy poprzez:</p>
<p>&#8211; rzetelne przygotowanie uslugi w oparciu o potrzeby klienta oraz wytyczne zawarte w Polskiej Ramie Kwalifikacyjnej dotyczace wiedzy, umiejetnosci i kompetencji spolecznych, dostosowane do poziomu edukacyjnego.</p>
<p>&#8211; profesjonalne wykonywanie uslug,</p>
<p>&#8211; ewaluacje wykonywanych uslug,</p>
<p>&#8211; analize osiaganych efekt&oacute;w ksztalcenia,</p>
<p>&#8211; doskonalenie program&oacute;w edukacyjnych, dostosowywanych do zmieniajacego sie rynku,</p>
<p>&#8211; stale podnoszenie kwalifikacji kadry wykonujacej uslugi,</p>
<p>&#8211; zdobywanie certyfikat&oacute;w poswiadczajacych umiejetnosci oraz prawo do ich wykonywania,</p>
<p>&#8211; zdobywanie akredytacji swiadczacych o kontroli i stosowania odpowiednich procedur przy wykonywaniu uslug.</p>
<p>Nasze standardy potwierdzil audyt, nadajac certyfikat Standard Uslugi Szkoleniowo-Rozwojowej Polskiej Izby Firm Szkoleniowych SUS 2.0 .</p>
<p style="text-align: justify;"><strong>Dzieki standaryzacji uslug i akredytacji SUS 2.0 CST Coaching moze swiadczyc Uslugi Rozwojowo &ndash; Szkoleniowe dla MMS przedsiebiorstw ich wlascicieli i pracownik&oacute;w z dofinansowaniem od 50% &#8211; 80 % wartosci uslugi, na terenie calego kraju.</strong></p>
<p style="text-align: justify;">Sa dwie sciezki:</p>
<ol>
<li style="text-align: justify;">Mozna korzystac z uslugi otwartej dostepnej w BUR &ndash; przed zapisaniem, nalezy sprawdzic warunki dofinansowania w danym wojew&oacute;dztwie!</li>
<li style="text-align: justify;">Mozna skorzystac z uslugi zamknietej &#8211; w tym przypadku nalezy najpierw skontaktowac sie z CST Coaching, aby om&oacute;wic obszary do rozwoju, ustalic zakres Uslug, efekt&oacute;w szkolenia.</li>
</ol>
<p><a title="uslugirozwojowe.parp.gov.pl/podmioty" href="https://uslugirozwojowe.parp.gov.pl/podmioty/view?id=9689" target="_blank" rel="noopener">uslugirozwojowe.parp.gov.pl/podmioty&nbsp;&nbsp;</a></p>
<p style="margin: 0cm 0cm 12.0pt 0cm;"><b><span style="font-size: 10.5pt; font-family: 'Arial',sans-serif; color: #1c1e21; border: none windowtext 1.0pt; mso-border-alt: none windowtext 0cm; padding: 0cm;">Dofinansowania dla Mikro, Malych i Srednich Przedsiebiorstw do 2020roku!<u1:p style="box-sizing: border-box; cursor: text;"></u1:p></span></b><span style="font-size: 10.5pt; font-family: 'Open Sans',sans-serif; color: #666666;"><o:p></o:p></span></p>
<p style="margin: 0cm 0cm 12.0pt 0cm;"><span style="font-size: 10.5pt; font-family: inherit, serif; color: #1c1e21;">Na co warto zwr&oacute;cic uwage przy wyborze uslugi?<br /> Na to, czy opr&oacute;cz uslug zamieszczonych na platformie, firma szkoleniowa przygotowuje usluge pod potrzeby klienta, na podstawie przeprowadzonego wywiadu badania potrzeb.<o:p></o:p></span></p>
