<div class="title">O firmie</div>
<div class="text"><p><strong>Centrum Poszanowania Energii BUDMAR</strong><span> oferuje profesjonalne uslugi zwiazane z obsluga inwestycji budowlanych. Zapewniamy rzetelna i kompetentna prace oraz profesjonalne doradztwo. Nasz zespol sklada sie z grupy specjalistow z branzy budowlanej oraz energetycznej, dzieki czemu </span><strong>jakosc swiadczonych przez nasza firme uslug jest bardzo wysoka</strong><span>.</span><br /><br /><span>Potwierdzeniem tego jest fakt, ze wszystkie sporzadzone przez nas opracowania, takie jak m.in. </span><strong>przeglady okresowe budynkow, Ksiazki Obiektow Budowlanych, projekty dociepelen, inwentaryzacje budowlane itp. zostaly pozytywnie zweryfikowane przez odpowiednie Inspektoraty</strong><span>.</span><br /><br /><span>Z mysla o wygodzie Klientow, stworzylismy zespol mobilnych i dyspozycyjnych pracownikow, ktorzy w krotkim czasie zrealizuja Panstwa zlecenia. Oferujemy wysoka jakosc uslug w rynkowych cenach. Glowne obszary dzialalnosci BUDMARu to Lodz i Warszawa oraz caly obszar wojewodztw lodzkiego i mazowieckiego. </span></p>
