<p><strong>Szanowni Panstwo.</strong> Serdecznie witamy na naszej stronie internetowej.
<strong>KALEX</strong> jest jednym z najpiekniejszych zakatkow w tych okolicach.
Lezy u ujscia Slupi do jeziora Zukowskiego. Jest 20 hektarowym gospodarstwem agroturystycznym. Wczasy, urlopy czy wakacje u nas, to prawie raj dla ludzi kochajacych nature. Jezioro, rzeka las, pola i laki... <strong>czegoz mozna wiecej pragnac.</strong></p>
<h1>Wynajmujemy 2 obiekty.</h1>
<a href="images/img/domek/9.jpg" rel="lightbox[domek]"><img class="img" src="images/thumbs/domek/9.jpg" alt="domek" /></a>
<h2>1) Domek wczasowy, ktory posiada:</h2>
<ul>
<li>5 sypialni po 2 lozka, a wiec dla grupy do 10 osob.</li>
<li>Salonik z jadalnia i kuchnia.</li>
<li>2 lazienki z prysznicami i pralka</li>
<li>Kuchnia jest wyposazona w kuchenke elektryczna z piekarnikiem, zmywarke (wazne dla pan!)</li>
<li>Lodowke z zamrazalnikiem i mikrofalowke.</li>
<li>Domek ma duzy taras i balkon z pieknymi widokami.</li>
</ul><br />
<a href="images/img/mieszkanie/1.jpg" rel="lightbox[domek]"><img class="img" src="images/thumbs/mieszkanie/1.jpg" alt="mieszkanie 1" /></a>
<h2>2) Mieszkanie ktore wynajmujemy to:</h2>
<ul>
<li>Salonik</li>
<li>2 sypialnie z 5 miejscami do spania</li>
<li>Kuchnia i lazienka.</li>
<li>Kuchnia wyposazona jest w kuchenke gazowa z piekarnikiem, lodowke z zamrazalnikiem i mikrofalowke.</li>
<li>Mieszkanie ma osobne, niekrepujace wejscie.</li>
<li>Jest rowniez balkon z pieknym widokiem na jezioro.</li>
<li>Gosciom wynajmujacym mieszkanie udostepniamy pralnie</li>
</ul>
<br />
<p>
Naszym gosciom proponujemy kapiele w jeziorze czy w rzece, spacer i zbieranie grzybow, plywanie lodka, kajakami i rowerem wodnym, lowienie ryb i grillowanie w duzym zadaszonym grillu, w ktorym miesci sie 12 osob. Te wszystkie zajecia sprawiaja, ze dni dla naszych gosci sa po prostu za krotkie, a dla dzieci KALEX to ogromny plac zabaw.</p>
<p>
Jest u nas wiele miejsca dla ludzi lubiacych spedzac wczasy, urlopy czy wakacje w kampingach, przyczepach campingowych, lub w namiotach.<br />
Zapewniamy zaplecze socjalne, tzn. WC i prysznice.<br />
Zapraszamy i gwarantujemy, ze przyjezdzajac do nas nie bedziecie Panstwo zalowac, a KALEX oczaruje Was tak samo jak swego czasu ten piekny zakatek oczarowal nas.<br /><br />
<strong>Z kaszubskim pozdrowieniem pozostajac do Panstwa uslug. Jolanta i Franz Becker</strong><br />
</p>
