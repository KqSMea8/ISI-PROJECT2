<h5><strong>Przywodztwo w procesie zmian: nowe realia i nowe wyzwania dla liderow</strong></h5>
<p class="">Poznaj najnowsze wyzwania zwiazanie z laczeniem wielu rol lidera i dowiedz sie, skad bierze sie zrodlo dlugofalowego sukcesu na najwyzszych szczeblach zarzadzania.  Zweryfikuj dzieki narzedziu Life Styles Inventory<sup>TM</sup> (360deg), na ktorym z osobistych wymiarow przywodztwa powinienes skupic sie juz teraz i jak zaplanowac dalszy rozwoj w najblizszej przyszlosci. </p>
</div>
<div id="sesja2" class="tab-pane fade">
<img src="images/sesje/2.jpg" class="img-responsive padd-t10">
<h5><strong>Lider w sieci relacji: budowanie inteligencji emocjonalnej i spolecznej</strong></h5>
<p class="">Dowiedz sie, kiedy i z jakich technik korzystac, aby budowac efektywna siec relacji. Od razu korzystaj z narzedzi rozwoju kompetencji interpersonalnych, samoswiadomosci i automotywacji. Przekonaj sie, jak radzisz sobie na tzw. ,,drabinie wnioskowania" i sprawdz, jak wyeliminowac nieporozumienia, ktore zaklocaja komunikacje menedzerow z ich otoczeniem. </p>
</div>
<div id="sesja3" class="tab-pane fade">
<img src="images/sesje/3.jpg" class="img-responsive padd-t10">
<h5><strong>Komunikacja lidera: od eliminacji bledow do mistrzostwa</strong></h5>
<p class="">Podczas tej sesji skupimy sie na diagnozie Twoich umiejetnosci prezentacyjnych. Dowiedz sie, ktore techniki musisz opanowac, aby komunikowac sie w przejrzysty i perswazyjny sposob, zarowno z podwladnymi, jak i czlonkami zarzadu. Poznaj tajniki inspirujacych wystapien publicznych: zdobadz serca i umysly swojej publicznosci!</p>
</div>
<div id="sesja4" class="tab-pane fade">
<img src="images/sesje/4.jpg" class="img-responsive padd-t10">
<h5><strong>Budowanie silnej marki osobistej jako narzedzia reputacji i perswazji</strong></h5>
<p class="">Podczas tej sesji zweryfikujesz swoj wizerunek i dowiesz sie, jak nim zarzadzac. Naucz sie w pelni swiadomie kreowac swoja marke i mierzyc zasieg jej wplywu, aby w efekcie byc gotowym do opracowania spojnej strategii wizerunkowej pod okiem naszych ekspertow.</p>
</div>
<div id="sesja5" class="tab-pane fade">
<img src="images/sesje/5.jpg" class="img-responsive padd-t10">
<h5><strong>Lider negocjator: jak realizowac zlozone cele w trudnych warunkach</strong></h5>
<p class="">Zdobadz umiejetnosci negocjacyjne niezbedne w roli lidera. Poznaj i od razu przetestuj podczas warsztatu narzedzia umozliwiajace opracowanie skutecznej strategii negocjacyjnej. Dowiedz sie takze, jakie dzialania podejmowac, gdy negocjacje nie ida zgodnie z poczatkowymi zalozeniami. </p>
</div>
<div id="sesja6" class="tab-pane fade">
<img src="images/sesje/6.jpg" class="img-responsive padd-t10">
<h5><strong>Wladza: kluczowy element sukcesu lidera - miedzy skutecznoscia a etyka</strong></h5>
<p class="">Zdobadz nowe sprawdzone narzedzia budowania wlasnego autorytetu i wladzy. Przeanalizuj studia przypadkow, ktore pomoga Ci dostrzec glowny kapital lidera w zdobywaniu zwolennikow i budowaniu swojej pozycji. Dowiedz sie, jak utrzymac wladze i jak korzystac z niej w etyczny sposob - rowniez w najtrudniejszych, nieprzewidywalnych sytuacjach. </p>
</div>
<div id="sesja7" class="tab-pane fade">
<img src="images/sesje/7.jpg" class="img-responsive padd-t10">
<h5><strong>Innowacyjny lider: jak przewodzic innowacji w firmie</strong></h5>
<p class="">Sprawdz, czy jestes liderem innowacji w swojej firmie oraz co mozesz zrobic, by skutecznie przejac te role. Zyskaj gotowe do wdrozenia rozwiazana, ktore pozwola Ci rozwijac nowe kompetencje i wplywac na kulture innowacji w firmie.</p>
</div>
<div id="sesja8" class="tab-pane fade">
<img src="images/sesje/8.jpg" class="img-responsive padd-t10">
<h3 class="">Lider jako agent zmiany: wymiar psychologiczny i organizacyjny</strong></h5>
<p class="">Dowiedz sie, jak radzic sobie z oporem wobec zmiany organizacyjnej. Poznaj poszczegolne fazy tego zjawiska i kluczowe role, jakie przyjmuja w nim pracownicy, kadra zarzadzajaca oraz osoby spoza firmy. Wez udzial w symulacji biznesowej, ktora pozwoli Ci zrozumiec mechanizmy psychologiczne zwiazane ze zmiana oraz przecwiczyc zdobyte wczesniej umiejetnosci w praktyce. </p>
</div>
<div id="sesja9" class="tab-pane fade">
<img src="images/sesje/9.jpg" class="img-responsive padd-t10">
<h5><strong>Zarzadzanie wyzwaniami zycia zawodowego i osobistego: miedzy stresem a osiagnieciami</strong></h5>
<p class="">Ta sesja pozwoli Ci odkryc, w ktorych obszarach swojego zycia prywatnego i zawodowego powinienes wprowadzic zmiany, aby osiagnac harmonie, ktora byc moze do tej pory wydawala Ci sie niemozliwa. Zdobadz wskazowki i narzedzia, ktore od razu pomoga Ci usprawnic zarzadzanie soba, a w kolejnym kroku opracuj plan, ktory pozwoli Ci utrzymac kierunek pozytywnych zmian.</p>
</div>
<div id="sesja10" class="tab-pane fade">
<img src="images/sesje/10.jpg" class="img-responsive padd-t10">
<h5><strong>Globalny lider przyszlosci: jak wygrywac w zmieniajacym sie swiecie biznesu</strong></h5>
<p class="">Zdobadz narzedzia podejmowania skuteczniejszych decyzji biznesowych i technik przelamywanie barier rozwojowych. Na koniec zdiagnozuj swoje dalsze kroki w drodze do osiagniecia tytulu globalnego lidera przyszlosci (kwestionariusz Global Leader of the Future). </p>
