<h2>Zapomniany element przywodztwa</h2>
<h3>Tylko nielicznym menedzerom udaje sie wspiac na sam szczyt organizacji i&nbsp;utrzymac te pozycje. Co zatem wyroznia najskuteczniejszych liderow?</h3>

