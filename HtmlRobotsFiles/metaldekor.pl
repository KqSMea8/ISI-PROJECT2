<div class="tekst">Szanowni Panstwo.
<p>
Pracownia METAL-ART Rafal Kotlarski, to nowa linia wzorow dekoracji o charakterze ozdobno - uzytkowym. Produkty nadaja sie jako: dekoracje wnetrz, dekoracje ogrodowe, dekoracje domu.
Nasza idea jest tworzenie przedmiotow oryginalnych i ciekawych. Plynne ksztalty i lagodna forma np. swiecznikow i kwietnikow z pewnoscia uatrakcyjnia Panstwa mieszkania oraz miejsca pracy: biura, restauracje. Doskonale zaprojektowane i dopracowane szczegolowo nasze dekoracje moga byc milym i docenionym prezentem na kazda okazje.<p>

Katalog "Kotlarski Styl" jest stale rozbudowywany i poszerzany, dlatego zachecamy do stalej obserwacji naszej strony. Wspolpraca Pracowni METAL-ART z klientem polega na sprzedazy gotowych wyrobow, dopasowywaniu ich do potrzeb klienta (kolor, wymiar, ilosc punktow swietlnych, sposobu zamocowania).<p>

Nasza zaleta jest przede wszystkim dbalosc o szczegoly i jakosc wykonania, dlatego mysla przewodnia firmy jest zdanie "Twoje dekoracje - moja pasja".<br><br>

