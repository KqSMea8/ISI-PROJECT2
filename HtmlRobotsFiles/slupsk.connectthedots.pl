<h3>Gminny Program Rewitalizacji Miasta Slupska na lata 2017-2025+<br/>- podstawowe informacje</h3>
<img class='lines' src='img/lines.svg'>
<div class='tresc' ss-container>
<div class="tekst">
<strong>Wizja rewitalizacji</strong>
Obszar rewitalizacji jest miejscem atrakcyjnym do zamieszkania, samorealizacji i prowadzenia dzialalnosci, zintegrowanym spolecznie i przestrzennie z dobrze rozwinieta infrastruktura spoleczna i czystym srodowiskiem, tetniacym zyciem, bedacym swiadectwem ambicji i gospodarnosci jego mieszkancow.
<br/><br/>
<strong>Rewitalizacja</strong>
Rewitalizacja to proces wyprowadzania ze stanu kryzysowego obszarow o szczegolnej koncentracji problemow spolecznych, technicznych, gospodarczych, przestrzenno - funkcjonalnych i srodowiskowych, prowadzony w sposob kompleksowy, poprzez zintegrowane dzialania na rzecz lokalnej spolecznosci, przestrzeni i gospodarki, skoncentrowany terytorialnie, prowadzony na podstawie gminnego programu rewitalizacji.
<br/><br/>
<strong>Rewitalizacja w Slupsku obejmuje:</strong>
Srodmiescie, Podgrodzie oraz Stare Miasto.<br/>
Zamieszkuje ja ok. 24 % mieszkancow miasta. <br/>
Powierzchnia to ok. 272 ha, czyli ok. 6,30% terenu Slupska.<br/><br/>
Szacunkowa wartosc inwestycji i programow nastawionych na poprawe zycia mieszkancow i mieszkanek obszaru rewitalizowanego w latach 2017-2025 + wynosi blisko 400 mln zl. Polowe z tej kwoty maja stanowic srodki UE. Beda to nie tylko przedsiewziecia realizowane przez Miasto Slupsk, ale takze inne podmioty publiczne. Okolo 70 mln zl maja kosztowac przedsiewziecia podmiotow prywatnych.
