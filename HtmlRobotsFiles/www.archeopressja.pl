<p><strong><em>KU PRZYDROZNYM LEKCJOM HISTORII </em></strong></p>
<p><strong>serdecznie zapraszamy na warsztaty pt.</strong></p>
<p style="text-align:center;"> <strong><em>PRZYWRACANIE PAMIECI NA WSPOLNYM CMENTARZU   </em></strong><strong><em>POLAKOW, NIEMCOW I ROSJAN</em></strong></p>
<p style="text-align:center;"><strong><em>  (w setna rocznice zamachu w Sarajewie).</em></strong></p>
<p>kiedy:</p>
<p>28<strong> czerwca 2014 (sobota)  godz. 12:00-14:00</strong></p>
<p>gdzie: <strong> </strong></p>
<p><strong>Cmentarz w Bolimowskiej Wsi</strong></p>
<p>Na Cmentarzu Wojennym w Bolimowskiej Wsi pochowani zostali obok siebie (!) zolnierze obydwu stron konfliktu, ktorzy poniesli smierc w walkach nad Rawka w latach 1914-1915. Dzis cmentarz ten, pomimo, ze relatywnie zadbany ulega ciaglemu procesowi niszczenia, zarowno z przyczyn naturalnych jak i antropogenicznych.</p>
<p>Warsztaty stanowiace integralny element projektu ARCHEOLOGIA JAKO ANTIDOTUM NA ZAPOMNIENIE i WANDALIZM. PIERWSZA WOJNA SWIATOWA NAD RAWKA I BZURA (1914 - 2014). KU PRZYDROZNYM LEKCJOM HISTORII, maja na celu oczyszczenie i zadokumentowanie  materialnych pozostalosci z ktorymi mamy do czynienia w obrebie tego cmentarza niemal 100 lat od jego zalozenia.</p>
<p>Postrzegamy to jako przejaw czynnej troski o cmentarz, ktory zaswiadcza o zlozonosci Wielkiej Wojny, w tym o zlozonosci naszej historii.</p>
<p>Upamietnianie poprzez dokumentowanie i refleksje, moze przyczynic sie do przedluzenia trwania materialnych reliktow przeszlosci, zarowno w sensie konceptualnym (w tym pamieciowym, historycznym) jak i fizycznym (w tym konserwatorskim, archeologicznym).</p>
<p><strong>Miejsce zbiorki: kapliczka przy cmentarzu niemiecko -rosyjskim Bolimowskiej Wsi</strong><br/> <strong>godzina: 12:00 -14:00</strong></p>
<p><strong>Zapraszamy  do uczestniczenia w WARSZTATACH</strong></p>
<p>PROSIMY wolontariuszy  zainteresowanych uczestniczeniem w warsztatach o uprzednie mailowe zgloszenie sie do A. Zalewskiej (kontakt: azalew@op.pl).<br/> Z uwagi na charakter prac, potrzebujemy miec wglad w ilosc osob zainteresowanych czynnym w nich uczestnictwem - dlatego bardzo prosze  o wczesniejsze zgloszenie checi uczestniczenia w warsztatach pod adresem azalew@op.pl). <br/> <strong>Prosimy uczestnikow o przyj</strong><strong>s</strong><strong>cie w stroju roboczym, tj. sprzyjaj</strong><strong>a</strong><strong>cym pracom porz</strong><strong>a</strong><strong>dkowym i dokumentacyjnym.</strong></p>
