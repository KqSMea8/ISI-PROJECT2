<h2>O nas</h2>
<p>
Jestesmy firma rodzinna, ktora powstala w roku 2006.
Specjalizujemy sie w swiadczeniu uslug, z zakresu obrobki mechanicznej detali, obrobki przewodow elektrycznych i kabli, modernizacji silnikow elektrycznych oraz wykonawstwa paneli sterowniczych, wg wymagan klienta.
Swiadczymy rowniez uslugi z zakresu projektowania nietypowych rozwiazan konstrukcyjno-technologicznych w obszarze obrobki jak rowniez narzedzi i oprzyrzadowania technologicznego oraz wykonawstwa dokumentacji technicznej.
</p>
<p>
Posiadamy licencjonowane oprogramowanie SolidEdge ST i NX CAM, pozwalajace na szybkie projektowanie i wdrazanie procesu obrobki skomplikowanych detali na obrabiarkach CNC oraz ich wysokiej jakosci wykonawstwo.
</p>
<p>
Zatrudniamy kadre techniczna z duzym doswiadczeniem zarowno konstrukcyjnym jak i technologicznym, ktora sluzy fachowym doradztwem na kazdym etapie ofertowania i realizacji zamowienia.
</p>
