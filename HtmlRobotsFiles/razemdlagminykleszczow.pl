<h1 class="entry-title">Razem mozemy wiecej!</h1>	</header><!-- .entry-header -->

<div class="entry-content">
<p><span style="color: #000000;"><span style="font-family: Arial;"><span style="font-size: small;">Witamy na oficjalnej stronie Stowarzyszenia ,,Razem dla Gminy Kleszczow"</span></span></span></p>
<p><span style="color: #000000;"><span style="font-family: Arial;"><span style="font-size: small;">W ramach Stowarzyszenia podejmujemy inicjatywy majace na celu staly wzrost atrakcyjnosci Gminy Kleszczow, co scisle wiaze sie z ciagla poprawa warunkow bytowych wszystkich mieszkancow.</span></span></span></p>
<p><span style="color: #000000;"><span style="font-family: Arial;"><span style="font-size: small;">Zachecamy do wspolpracy wszystkich tych, ktorym na sercu lezy szeroko rozumiane dobro naszej gminy. Otwieramy sie zarowno na osoby prywatne majace ciekawe pomysly i chcace aktywnie uczestniczyc w zyciu gminy, a takze na przedsiebiorcow, ktorych doswiadczenie i znajomosc problemow rynku lokalnego powinny byc wykorzystywane w prowadzeniu polityki gospodarczej gminy.</span></span></span></p>
<p><span style="color: #000000;"><span style="font-family: Arial;"><span style="font-size: small;">Jezeli choc troche identyfikujesz sie z powyzszym nie zwlekaj, przylacz sie do nas juz dzis!</span></span></span></p>
<p><span style="color: #000000;"><span style="font-family: Arial;"><span style="font-size: small;">Zapoznaj sie z naszym statutem zamieszczonym w zakladce ,,Dokumenty", wypelnij znajdujaca sie tam deklaracje, skontaktuj sie z nami mailowo badz telefonicznie i zacznij dzialac, bo RAZEM mozemy wiecej!</span></span></span></p>
