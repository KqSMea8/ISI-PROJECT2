<h6 class="entry-title"><a href="/poradnik-akwarystyczny/oczko-wodne-8211-rosliny-strefy-bagiennej-i-nadbrzeznej-id241.html">Oczko wodne &#8211; rosliny strefy bagiennej i nadbrzeznej</a></h6>

<p>Rosliny do oczka wodnego mozemy podzielic na kilka glownych grup. Podzial ten powstal ze wzgledu na miejsce ich wystepowania w stawie. Sadzac rosliny musimy wziac pod uwage ich preferencje do zycia w danej strefie. Strefa bagienna jest to strefa przejsciowa pomiedzy woda, a ladem. Rosliny rosnace w tej czesci bujnie rozwijaja sie w naprawde mokrej ziemi. Rosna na brzegach, a wiec pomagaja wkomponowac oczko w ogolny wyglad ogrodu. Ponadto sa doskonalym schronieniem dla zab i innych zwierzat. Sadzac rosliny nie nalezy wybierac gatunkow zbyt wrazliwych. W naturze poziom wody rowniez czesto obniza sie, a rosliny prawie na to nie reaguja. Strefa ta powinna byc dostatecznie szeroka.</p>
