<div class="title">Specjalizacje - egzaminy</div>
<div class="text"><p><span style="font-size: medium;">Szanowwni Panstwo,&nbsp;</span></p>
<p>Egzamin specjalizacyjny z kardiologii (test) w sesji jesiennej 2017 odbedzie sie 20 pazdziernika 2017 roku o godz. 11.00 w Warszawie.</p>
<p>Do egzaminu testowego w danym roku kalendarzowym (wiosna i jesien) obowiazuja zalecenia ESC z roku poprzedniego. Zatem w sesji jesiennej 2017 obowiazuja zalecenia ESC z 2016 roku. Egzamin nie bedzie obejmowal nowych czterech zalecen opublikowanych w j. angielskim w czasie kongresu ESC w Barcelonie w koncu sierpnia 2017 roku. </p>
