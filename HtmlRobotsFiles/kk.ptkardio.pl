<div class="title">Zadania Konsultanta Krajowego</div>
<div class="text"><p><b>Konsultant&oacute;w krajowych powoluje Minister Zdrowia</b> sposr&oacute;d specjalist&oacute;w z poszczeg&oacute;lnych dziedzin medycyny, farmacji oraz innych dziedzin majacych zastosowanie w ochronie zdrowia. W tym celu wystepuje o przedstawienie kandydat&oacute;w do towarzystw naukowych <br />o zasiegu krajowym, kt&oacute;re zrzeszaja specjalist&oacute;w w danej dziedzinie. Wszystkie zgloszone kandydatury przedstawiane sa do zaopiniowania przez wlasciwe samorzady zawod&oacute;w medycznych.</p>
<p><b>Gl&oacute;wne zadania Konsultanta krajowego</b></p>
<ul>
<li>inicjowanie badan epidemiologicznych na obszarze kraju oraz ocena metod i wynik&oacute;w tych badan;</li>
<li>prognozowanie potrzeb zdrowotnych w reprezentowanej przez konsultanta krajowego dziedzinie;</li>
<li>prowadzenie doradztwa w zakresie realizacji zadan wynikajacych z Narodowego Programu Zdrowia i innych program&oacute;w zdrowotnych;</li>
<li>opiniowanie wniosk&oacute;w o skierowanie pacjenta do przeprowadzenia leczenia lub badan diagnostycznych poza granicami Rzeczypospolitej Polskiej;</li>
<li>wydawanie opinii i doradztwo w zakresie zadan realizowanych przez Centrum Egzamin&oacute;w Medycznych, Centrum Ksztalcenia podyplomowego Pielegniarek i Poloznych oraz Centrum Medycznego Ksztalcenia Podyplomowego;</li>
<li>opiniowanie program&oacute;w lekowych Ministerstwa Zdrowia;</li>
<li>nadz&oacute;r nad programem specjalizacji z kardiologii i przeprowadzeniem egzaminu panstwowego.</li>
</ul></div>
