<h2>Circa</h2>
<hr>
<div class="description">
Pierwsze buty C1rca powstaly w 1999 r. w Kalifornii. Firma C1rca stworzona zostala w wyniku wsp&oacute;lpracy Four Star Distribution i profesjonalnego skateboardera Chada Muski, kt&oacute;ry opuscil wtedy szeregi &eacute;S Footwear. Od samego poczatku swojego istnienia, czyli od 2000 r., C1rca zwiazana byla z muzyka, sztuka i szeroko pojetym stylem zycia ale zawsze jej najwazniejszym kierunkiem byla deskorolka. Zmiany, kt&oacute;re zaszly w ostatnich latach w miedzynarodowej spolecznosci zwiazanej z muzyka, odcisnely na marce trwale pietno, co tez dobrze widac w jej obecnych kolekcjach. Dzis C1rca skupia sie gl&oacute;wnie na skateboardingu, oferujac klientom klasyczne &nbsp;deskorolkowe buty inspirowane punkiem i latami 90tymi. Dzieki nowemu wlascicielowi marka C1rca nabrala nowego, swiezego wiatru w zagle tworzac charyzmatyczny, pelen ciekawych osobowosci team!
