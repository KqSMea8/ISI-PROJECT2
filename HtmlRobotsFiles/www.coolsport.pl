<h2>O nas</h2>
<hr>
<div class="clearfix aboutus-wrapper"><div class="text-col">
<p>
Firma Cool Sport zrodzila sie z pasji do sport&oacute;w ekstremalnych. Zalozycielami firmy jest dw&oacute;jka braci kt&oacute;rzy byli jednymi z pierwszych os&oacute;b w Polsce jezdzacych na deskach snowboardowych. Na poczatku lat 90 snowboard i skateboard byly sportami egzotycznymi a sprzet do ich uprawiania byl zupelnie niedostepny w Polsce.</p>
<p>
Widzac te trudnosci a z drugiej strony rosnace zainteresowanie nowymi dyscyplinami sportu bracia postanowili przeniesc swoja pasje do sportu na grunt biznesowy.</p>
<p>
W 1995 roku powstal w Krakowie pierwszy w Polsce specjalistyczny sklep snowboardowo-skateboardowy pod nazwa COOL SPORT. Pomysl okazal sie strzalem w 10. W konc&oacute;wce lat 90 snowboard zaczal zyskiwac rzesze nowych fan&oacute;w stajac sie niemalze sportem masowym. W dwa lata od otwarcia sklepu firma Cool Sport postanowila rozszerzyc swoja dzialalnosc otwierajac kilka kolejnych sklep&oacute;w detalicznych oraz rozpoczela sprzedaz hurtowa produkt&oacute;w sprowadzanych na potrzeby swoich sklep&oacute;w. Zaowocowalo to podpisaniem w 1997 roku pierwszej umowy dystrybucyjnej na import i sprzedaz hurtowa desek snowboardowych Generics oraz wiazan i but&oacute;w snowboardowych Blax. Rok p&oacute;zniej oferta dystrybucyjna poszerzona zostala o buty snowboardowe Northwave i wiazania Drake. Kolejne lata przyniosly nowe umowy dajace firmie Cool Sport prawo reprezentowania i wylacznej sprzedazy produkt&oacute;w Lamar, Sims i Head Snowboards.</p>
</div>
<div class="text-col">
<p>
Przelomowym okazal sie rok 2003 kiedy to Cool Sport zostaje wylacznym dystrybutorem w Polsce firmy Sole Technology majacej w swojej palecie takie marki jak Etnies, Es, Emerica, Altamont czy Thirty Two. Byl to wazny moment dajacy firmie r&oacute;wnowage sprzedazy przez caly rok poniewaz do tej pory wiekszosc swoich obrot&oacute;w hurtowych firma realizowala zima placac za koszty utrzymywania magazyn&oacute;w caly rok. W tym samym roku firma postanowila r&oacute;wniez rozpoczac sprzedaz detaliczna on line.</p>
<p>
Doskonala wsp&oacute;lpraca z firma Head w dziedzinie dystrybucji snowboard&oacute;w zaowocowala propozycja przejecia do dystrybucji calej palety produkt&oacute;w marki Head. W 2008 roku Cool Sport zostaje oficjalnym dystrybutorem Head w Polsce poszerzajac swoja hurtowa oferte o narty, tenis, squosh i badminton. Niestety swiatowy kryzys spowodowal drastyczny wzrost ryzyka i fale upadlosci wielu sklep&oacute;w sportowych. Chcac uniknac powiekszajacego sie ryzyka i bardzo trudnej sytuacji w jakiej znalazla sie branza narciarska w Polsce firma Cool Sport, w porozumieniu z firma Head, doprowadzila do transformacji modelu dystrybucyjnego i utworzenia oddzialu firmy Head w Polsce.</p>
<p>
Przez lata swojej dzialalnosci Cool Sport zdobyla szereg nagr&oacute;d i wyr&oacute;znien zostajac kilkakrotnie &quot;Dystrybutorem Roku&quot;. W latach 2010 i 2011 Cool Sport dwukrotnie zdobywa prestizowy tytul &quot;Gazele Biznesu&quot; dostajac sie tym samym do elitarnego grona najbardziej dynamicznie rozwijajacych sie firm w Polsce.</p>
</div>
<div class="text-col">
<p>
Obecnie Cool Sport to profesjonalna firma dystrybucyjna, zatrudniajaca 46 pracownik&oacute;w, dysponujaca magazynem o powierzchni ponad 2500 m2 i biurami o powierzchni ponad 500 m2. W ofercie dystrybucyjnej Cool Sport znajduja sie takie marki jak Brixton, Etnies, Es, Emerica, Altamont, Thirty Two, Volcom, Element, Supra, Krew, C1rca, Dirty Velvet, Northwave, Drake, Dragon, Von Zipper i Level.</p>
<p>
Nasza firma wywodzi sie z pasji do sportu i to wlasnie z tej pasji przeniosla na grunt biznesowy cechy, kt&oacute;re umozliwily jej dzisiejszy sukces: optymizm, zaangazowanie, wytrwalosc i konsekwencja. Kierujemy sie zasadami uczciwosci i fair play. Firme Cool Sport tworza ludzie gotowi sprostac kazdemu wyzwaniu.</p>
</div>
<p>
