<h2>Dragon</h2>
<hr>
<div class="description">
Marka Dragon powstala w 1993 roku w Californii , poczatek jej istnienia zwiazany byl mocno z surfingiem , dlatego to pierwsze produkty jakie znalazly sie w ofercie to okulary przeciw sloneczne. Zalozycielem i gl&oacute;wnym kreatorem marki jest Will Howard , kt&oacute;ry to do tej pory jest bardzo mocno zaangazowany w dzialania rozwojowe marki Dragon. Misja brandu zawsze skupiona byla na mlodziezy i aktywnych sportowo ludziach , plywajacych na surfie , jezdzacych na deskorolce czy snowboardzie. Dzis Dragon jest jednym z lider&oacute;w na rynku optycznym w swiecie sportu , mocna pozycja pozwolila firmie na ekspansje i pojawienie sie praktycznie na kazdym kontynencie. W 2013 marka Dragon stala sie czescia globalnego potentata w dziedzinie optyki &ndash; firmy Marchon, kt&oacute;ra skupia w sobie kilkanascie ekskluzywnych i sportowych marek okular&oacute;w takich jak &nbsp;Calvin Klein, Jil Sander, Michael Kors czy Nike Optics. W chwili obecnej Dragon jest jednym z wiodacych producent&oacute;w gogli snowboardowych, oferujacych gogle stworzone na najlepszych technologiach.
