<div>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 28-29 maja 2019 r. w Inowroclawiu  odbedzie sie XXIV Ogolnopolskie Forum Ratownictwa. Temat konferencji: ,,Bezpieczenstwo  zdrowotne 4.0. Nowe technologie monitorowania, obrazowania, modelowania,  prognozowania stanu zdrowia i srodowiska". Celem Forum jest dokonanie  przegladu&nbsp; projektow i dzialan  wdrozeniowych technologii cyfrowych w  zakresie: teleopieki; telemedycyny;    robotyzacji; wirtualnej opieki medycznej; inzynierii systemow opieki zdrowotnej; zarzadzania danymi,  przechowywania i bezpieczenstwa w opiece zdrowotnej; diagnostyki, prognoz i  klasyfikacji chorob; technologii uczenia  maszynowego, medycyny obliczeniowej (big data); systemow powiadamiania w naglych i nadzwyczajnych zagrozeniach; monitorowania i obrazowania srodowiska; wykorzystania dronow w opiece medycznej i bezpieczenstwie  ekologicznym. Organizatorami sa:</div><div>Prezydent  Miasta Inowroclawia,     Starosta  Inowroclawski,    Szpital  Wielospecjalistyczny im. dr. Ludwika Blazka w Inowroclawiu,    Komenda  Powiatowa Panstwowej Strazy Pozarnej w Inowroclawiu,    Komenda  Powiatowa Policji w Inowroclawiu,    Zaklad  Studiow nad Bezpieczenstwem Uniwersytetu im. Adama Mickiewicza w Poznaniu,     Katedra  Medycyny Ratunkowej Uniwersytetu Medycznego im. Karola Marcinkowskiego w  Poznaniu,    Katedra  i Zaklad Medycyny Sadowej Uniwersytetu Medycznego im. Karola Marcinkowskiego w  Poznaniu,    Katedra  i Klinika Neurochirurgii i Neurotraumatologii Uniwersytetu Medycznego im.  Karola Marcinkowskiego w Poznaniu,    Instytut  Logistyki Wojskowej Akademii Technicznej im. Jaroslawa Dabrowskiego w Warszawie,    Wojewodzka  Stacja Pogotowia Ratunkowego w Bydgoszczy oraz    Polskie  Towarzystwo Ratunkowe.</div><div>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Istnieje mozliwosc przygotowania referatu, ktory zostanie zaprezentowany podczas sesji naukowej i opublikowany w recenzowanej monografii przygotowanej przez wydawnictwo znajdujace sie na liscie Ministerstwa Nauki i Szkolnictwa Wyzszego. Tematy wystapien i streszczenia prosimy przesylac do 30 kwietnia 2019 r., natomiast referaty do publikacji do 15 czerwca 2019 r. Szczegoly zamieszczono w warunkach publikacji.</div>

