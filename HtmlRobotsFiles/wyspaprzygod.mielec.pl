<h1>Witamy!</h1>
<p style="text-align: justify;"><span style="font-size: 16px;"><strong>Sala Zabaw Wyspa Przygod to caloroczny, klimatyzowany i kameralny plac zabaw stworzony by dawac dzieciom frajde, a rodzicom chwile dla siebie.</strong></span></p>
<p style="text-align: justify;"><span style="font-size: 16px;"><strong>Kazde dziecko znajdzie tu cos dla siebie</strong></span></p>
<ul style="list-style-type: circle;">
<li style="text-align: justify;"><span style="font-size: 16px;"><strong>tory przeszkod</strong></span></li>
<li style="text-align: justify;"><span style="font-size: 16px;"><strong>baseny z pileczkami</strong></span></li>
<li style="text-align: justify;"><span style="font-size: 16px;"><strong>zjezdzalnie</strong></span></li>
<li style="text-align: justify;"><span style="font-size: 16px;"><strong>urzadzenia do gry zrecznosciowej </strong></span></li>
<li style="text-align: justify;"><span style="font-size: 16px;"><strong>gry multimedialne m.in. playstation 3 </strong></span></li>
<li style="text-align: justify;"><span style="font-size: 16px;"><strong>atrakcyjne zabawki.</strong></span></li>
</ul>
<p style="text-align: justify;"><span style="color: #000000; font-size: 16px;"><strong>Dzieci milo spedzaja czas i swietnie sie bawia. Jednoczesnie maja okazje aktywnie spozytkowac swoja energie, cwiczyc koordynacje wzrokowo - ruchowa, umiejetnosci manipulacyjne oraz integrowac sie z rowiesnikami.</strong></span></p>
<p style="text-align: justify;"><span style="color: #000000; font-size: 16px;"><strong>Drodzy Rodzice!</strong></span></p>
<p style="text-align: justify;"><span style="color: #000000; font-size: 16px;"><strong>Zapraszamy Was do naszej kawiarni na pyszna kawke i ciastko. Tylko u nas mozecie sie zrelaksowac, czytajac aktualna prase i nie biegac za swoja pociecha, gdyz jest w zasiegu Waszego wzroku :)</strong></span></p>
<p style="text-align: justify;"><span style="color: #000000; font-size: 16px;"><strong>Wyspa Przygod to doskonale miejsce na niezapomniane Urodziny dla Waszego dziecka!</strong></span></p>
<p style="text-align: justify;"><span style="color: #000000; font-size: 16px;"><strong>Oferujemy rowniez:</strong></span></p>
<ul style="list-style-type: circle;">
<li style="text-align: justify;"><span style="color: #000000; font-size: 16px;"><strong>organizacje urodzin tematycznych w naszej sali jak i w Twoim domu lub ogrodzie</strong></span></li>
<li style="text-align: justify;"><span style="color: #000000; font-size: 16px;"><strong>organizacje imprez okolicznosciowych i tematycznych dla dzieci;</strong></span></li>
<li style="text-align: justify;"><span style="color: #000000; font-size: 16px;"><strong>warsztaty kreatywne dla rodzicow i dzieci</strong></span></li>
<li style="text-align: justify;"><span style="color: #000000; font-size: 16px;"><strong>opieke dzienna dla dzieci</strong></span></li>
<li style="text-align: justify;"><span style="color: #000000; font-size: 16px;"><strong>animacje zabaw dla dzieci</strong></span></li>
<li style="text-align: justify;"><span style="color: #000000; font-size: 16px;"><strong>wejscia grup zorganizowanych </strong></span></li>
<li style="text-align: justify;"><span style="color: #000000; font-size: 16px;"><strong>wypozyczalnia 800 strojow karnawalowych dla dzieci i doroslych;</strong></span></li>
<li style="text-align: justify;"><span style="color: #000000; font-size: 16px;"><strong>Piniata Party na rozne okazje: urodziny, wieczory panienskie, kawalerskie, Wesela</strong></span></li>
<li style="text-align: justify;"><span style="color: #000000; font-size: 16px;"><strong>Bajkowe Maskotki, ktore uswietnia Wasze wydarzenie np Urodziny, Wesela i wszelkiego rodzaju imprezy.</strong></span></li>
</ul>
<p style="text-align: left;"><span style="color: #000000; font-size: 16px;"><strong>Zapraszamy codziennie do <span style="font-size: 16px;"><strong>Sala Zabaw Wyspa Przygod</strong></span> w Mielcu!<br /></strong></span></p>
