<h2><a id="o-projekcie" data-ps2id-target=""></a>Informacje o projekcie</h2>
<p>Projekt pn. ,,System Ewidencji i Zarzadzania Drogami Wojewodzkimi Wojewodztwa Kujawsko Pomorskiego" jest wspolfinansowany z Europejskiego Funduszu Rozwoju Regionalnego w ramach Programu Operacyjnego Wojewodztwa Kujawsko - Pomorskiego na lata 2007 - 2013 w zakresie Rozwoju Infrastruktury Spoleczenstwa Informacyjnego dla Dzialania 4.2 Rozwoj Uslug i Aplikacji dla Ludnosci. Wartosc realizacji Projektu to 10 100 066,65 zl.Celem bezposrednim Projektu jest stworzenie systemu ewidencji drog oraz systemu zarzadzania drogami wojewodzkimi w wojewodztwie Kujawsko - Pomorskim poprzez wdrozenie nowoczesnych systemow teleinformatycznych, wykorzystujacych najnowsze technologie pozyskiwania, przetwarzania i przesylania danych. Jako cel glowny Projektu zalozono usprawnienie zarzadzania drogami wojewodzkimi Zarzadowi Drog Wojewodzkich w Bydgoszczy oraz dostep do informacji w zakresie infrastruktury drogowej oraz warunkow drogowo - ruchowych mieszkancom, przedsiebiorcom oraz uzytkownikom drog.W zakresie prac objetych Projektem przewidziano dostawe oprogramowania dziedzinowego i narzedziowego do wykonywania prac statutowych przez pracownikow Zarzadu Drog Wojewodzkich, dostawe sprzetu komputerowego i akcesoriow, min. stacje robocze, laptopy, tablety, smartfony, moduly i anteny GPS, monitory do Centrum Zarzadzania Ruchem Drogowym oraz urzadzenia pomiarowe, analizujace oraz informujace o aktualnej sytuacji na drogach wojewodzkich - stacje meteorologiczne, stacje monitoringu, stacje rejestrujace ANPR (analizujace tablice rejestracyjne pojazdow), znaki i tablice zmiennej tresci VMS.</p>
