<h1 class="title"><a href="/firma">FIRMA</a></h1>   <h1 style="margin-top: 0cm">
<font color="#000000">G+P IZOMONT SP. Z&nbsp;O.O.</font></h1>
<p align="JUSTIFY" style="margin-bottom: 0cm; line-height: 0.42cm">
&nbsp;</p>
<p align="JUSTIFY" style="margin-bottom: 0cm; line-height: 0.42cm">
<span style="color:#000;">
Specjalizujemy sie w&nbsp;dalece zaawansowanej ochronie przed halasem malych i&nbsp;duzych obiekt&oacute;w przemyslu energetycznego, dr&oacute;g szybkiego ruchu, autostrad, jak tez w&nbsp;budowie uklad&oacute;w wentylacji oraz izolacji cieplo- i&nbsp;zimnochronnej.</p>
<p align="JUSTIFY" style="margin-top: 0.49cm; margin-bottom: 0.49cm">
Od&nbsp;1991 roku dzialamy w&nbsp;szeroko pojetej branzy metalowej oraz budowlanej i&nbsp;mamy ugruntowana pozycje na&nbsp;rynku poparta stosownymi rekomendacjami. Posiadamy doswiadczenie w&nbsp;projektowaniu, a&nbsp;na&nbsp;naszym koncie znajduje sie wiele realizacji projekt&oacute;w obud&oacute;w akustycznych, system&oacute;w wentylacyjno &ndash; klimatyzacyjnych, i&nbsp;konstrukcji stalowych. Nasze wyroby, dostarczone i&nbsp;montowane w&nbsp;kraju i&nbsp;za&nbsp;granica, moga takze posiadac wykonanie iskrobezpieczne, przeciwpozarowe lub przeciwwybuchowe.</p>
<p align="JUSTIFY" style="margin-top: 0.49cm; margin-bottom: 0.49cm">
Dzieki wysoko wykwalifikowanej kadrze technicznej oraz rozbudowanym i&nbsp;zaawansowanym technologicznie parkiem maszynowym, jestesmy w&nbsp;stanie wykonac nawet najbardziej zlozone zlecenia, zar&oacute;wno poprzez centrale we Wloclawku, jak i&nbsp;w&nbsp; biurze technicznym w&nbsp;Niemczech.</p>
