<header>
<h2>O <strong>GRZE</strong></h2>
<h3>CZYM JEST BUBBLE SOCCER?</h3>
</header>
<p>

Czy wiesz czym jest Bubble football? Sport ten regulami zblizony jest do klasycznej pilki noznej,
gdzie priorytetowym celem jest zdobycie najwiekszej ilosci goli.
Jedynym wyjatkiem odstajacym od podstawowych zasad gry jest fakt,
iz zawodnicy kazdej z druzyn rywalizuja w duzych , przezroczystych kulach.
<br><br>
Nie ma mowy o powaznych kontuzjach poniewaz chronia one zawodnikow od glowy az po kolana.
Tak wiec mozna bezpiecznie przewracac oraz przesuwac przeciwnikow,
co powoduje ogromna dawke dodatkowych emocji.

Skontaktuj sie z Bubblebum!! Gwarantujemy, ze kule spelnia
swoja role w kazdych warunkach i o kazdej porze roku! Liczy sie tylko
mobilizacja zespolu, chec spalenia ogromnej dawki kalorii
i odpornosc na duza porcje smiechu. Jezeli wlasnie w tym momencie
pojawil sie u Ciebie cien niepewnosci, iz  BubbleBum jest zarezerwowane
tylko dla pilkarzy i sportowcow to jestes w bledzie.

Sport ten dostepny
jest dla wszystkich oraz nie wymaga szczegolnych umiejetnosci.
Bubble football na Slasku jeszcze nigdy nie byl tak blisko Ciebie.
<br><br>
Pozwol zorganizowac  nam swoj wieczor kawalerski/panienski, firmowa impreze integracyjna,
festyn  lub spotkaj sie ze znajomymi i spedz aktywnie wspolne chwile.
Zagraj z nami na boisku, plazy, hali czy tez na wlasnym podworku, bowiem
dysponujemy przenosnymi bramkami. Do zobaczenia!


</p>
