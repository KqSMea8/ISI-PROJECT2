<h5>Dziekujemy!</h5>
<p>
Tworzac w 2013 roku marke ERP Ekspert nie zdawalismy sobie nawet sprawy, jak wielu firmom
i na jaka skale pomozemy w automatyzacji ich codziennej dzialalnosci, a takze, jak wiele sami sie nauczymy.
Mijajace z koncem lutego 5 lat to okres pozytywnej wspolpracy i bez watpienia nasze najcenniejsze dotychczas
doswiadczenie zawodowe. Mamy nadzieje, ze rowniez Wy, jako nasi Klienci, dobrze oceniacie wspolprace z nami.
</p>
<p>
Nic jednak nie trwa wiecznie. Z uwagi na nadchodzace w maju 2018 zmiany w polskim prawie, zmuszeni jestesmy
zakonczyc dzialalnosc w dotychczasowej formie, w szczegolnosci w sektorze medycznym.
</p>
<p>
W zwiazku z tym, z poczatkiem 2018 wylaczylismy dotychczasowa strone i zaprzestalismy pozyskiwac nowych
klientow, a w najblizszych dniach bedziemy wysylac wypowiedzenia uslug dla tych sposrod Was, ktorym z
uwagi na nadchodzace zmiany nie bedziemy juz w stanie swiadczyc uslug po 24. maja 2018. Chcielibysmy jednak,
abyscie jak najdelikatniej odczuli zakonczenie naszej dzialalnosci, dlatego tez z kazdym z Was po kolei
bedziemy sie indywidualnie kontaktowac i podpowiadac mozliwe rozwiazania, tak aby pomoc zachowac ciaglosc
Waszego dzialania.
</p>
<p>
Zas juz teraz chcielibysmy raz jeszcze podziekowac Wam za zaufanie, ktorym nas obdarzyliscie decydujac sie
na skorzystanie z naszych uslug i podziekowac za te kilka lat wspolnej, pozytywnej przygody.
</p>
<p>
Zespol ERP Ekspert wraz z Zarzadem Klim Baron Business Solutions Sp. z o.o.
</p>
