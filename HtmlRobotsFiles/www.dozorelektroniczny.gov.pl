<h2 class="entry-title">Strona glowna</h2>
<div class="entry-content">
<h4>Szanowni Panstwo,</h4>
<p>Niniejsza strona informacyjna adresowana jest do osob, ktore zlamaly prawo i zostaly skazane na kare pozbawienia wolnosci do jednego roku.</p>
<p>Pobyt w zamknietym zakladzie karnym jest najsurowszym rodzajem odbywania kary. Jednak w przypadku osob, ktore zostaly skazane na kare pozbawienia wolnosci do jednego roku, sad penitencjarny moze wyrazic zgode na wykonanie tej kary w mniej rygorystyczny sposob. Aby bylo to mozliwe, do polskiego systemu prawnego wprowadzono System Dozoru Elektronicznego. Koniecznosc odbywania kary w bezwzglednej izolacji wieziennej i wszystkie dolegliwosci z niej wynikajace zastepuje on mozliwoscia odbycia jej w domu lub innym miejscu na wolnosci wskazanym przez sad (np. hostel, schronisko). Dzieki temu mozna bedzie prowadzic normalne zycie osobiste, zawodowe i rodzinne z zachowaniem rygorow pelnej kontroli sprawowanej przez sad, kuratora, pracownikow Centrali Monitorowania i Upowaznionego Podmiotu Dozorujacego.</p>
<p>System Dozoru Elektronicznego jest szansa na zastapienie obowiazku odbywania kary w wiezieniu -- wykonywaniem jej na wolnosci. Umozliwia naprawienie swoich bledow w normalnych warunkach zycia spolecznego oraz przyjaznym otoczeniu rodziny i osob bliskich.</p>
