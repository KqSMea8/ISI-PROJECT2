<h2>Spraw, aby zespol sprzedazy stal sie zrodlem Twojej przewagi konkurencyjnej dzieki nowym kompetencjom menedzerow</h2>
<div class="row border-line"><div class="col-md-2 border-red"></div><div class="col-md-10 border-greey"></div></div>
</div>
<div class="container container-fluid tlo-1 top-15">
<div class="container padd-tb-20 mp-0-3">
<div class="row">
<div class="col-md-12">
<p>Dobrze jest miec efektywnych sprzedawcow, ale <strong>kluczowe dla firmy znaczenie maja skuteczni kierownicy sprzedazy</strong>. Najlepsi handlowcy nie chca pracowac dla slabych menedzerow i odchodza, a ci, ktorzy zostaja, sa slabo zarzadzani, bez wsparcia i coachingu. Dlatego coraz wiecej ekspertow mowi: <strong>za sukces najlepszych sil sprzedazowych odpowiadaja menedzerowie liniowi, tj. osoby bezposrednio wspolpracujace z handlowcami</strong>.</p>
<p>Kierownicy sprzedazy zmagaja sie jednak z problemami z wielu obszarow: <strong>zarzadzania ludzmi, zarzadzania klientami, zarzadzania biznesem z perspektywy wewnetrznej oraz z perspektywy zewnetrznej</strong>.  </p>
<ul class="img-ul-li-white1">
<li>Jak nie stracic najlepszych handlowcow, ktorych chce podkupic konkurencja? </li>
<li>W jaki sposob planowac i realizowac cele, aby nie byly fikcja? </li>
<li>Jak efektywnie rekrutowac, wdrazac i zatrzymywac wlasciwych ludzi? </li>
</ul>
<p>To tylko niektore z wyzwan, przed jakimi stoja menedzerowie liniowi. Niestety czesto nie otrzymuja oni wsparcia ani dostepu do sprawdzonych narzedzi i technik, dzieki ktorym mogliby sobie poradzic z ich rosnacym znaczeniem dla koncowego wyniku calej ekipy handlowej. <strong>W efekcie sprzedaz maleje, handlowcy sa niezadowoleni, a klienci odchodza do konkurencji.</strong> </p>
