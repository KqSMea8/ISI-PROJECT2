<article class="hentry">

<p style="text-align: center;"><span style="font-size:22px"><span style="font-family:arial,helvetica,sans-serif"><span style="color:#0000FF"><strong>5 najwiekszych monitoring&oacute;w FPS</strong></span></span></span></p>

<p><span style="font-size:12px"><span style="font-family:arial,helvetica,sans-serif"><a href="http://fps.polska.edu.pl/">Fundacja FPS</a> (Fundacja &quot;Fundusz Pomocy Studentom&quot;,&nbsp;<span style="color:rgb(0, 0, 0); line-height:20px">Polish Student Support Fund</span>), jest jedyna organizacja pozarzadowa, kt&oacute;ra koordynuje duze og&oacute;lnopolskie monitoringi w obszarze szkolnictwa wyzszego.&nbsp;Od 2010 roku FPS realizuje je w ramach programu Watchdog.edu.pl.</span></span></p>

<p style="text-align: center;"><span style="font-family:arial,helvetica,sans-serif"><span style="font-size:22px"><span style="color:#0000FF"><strong><img alt="" src="/content/200watchdog_01.jpg" style="height:240px; width:200px" /></strong></span></span></span></p>

<p><strong>Pierwsza edycja</strong> programu Watchdog.edu.pl (lata 2010-2011) dotyczyla systemu stypendialnego. Badaniem objeto 54 uczelnie publiczne ze wszystkich wojew&oacute;dztw, doprowadzajac w efekcie do wprowadzenia ponad 150 zmian w regulaminach przyznawania stypendi&oacute;w.<br />
<strong><a href="http://archiwum.watchdog.edu.pl/files/raport-watchdogedupl-2011.pdf">W pierwszym raporcie</a></strong> watchdogowym FPS skatalogowano typowe bledy wystepujace w przepisach wewnetrznych uczelni dotyczacych obslugi funduszu stypendialnego. Raport w wersji ksiazkowej trafil do ponad 3 tys. odbiorc&oacute;w. Dla wielu komisji stypendialnych stal sie poradnikiem dzialania.</p>

<p style="text-align: center;"><span style="font-family:arial,helvetica,sans-serif"><span style="font-size:22px"><span style="color:#0000FF"><strong><img alt="" src="/content/200watchdog_02.jpg" style="height:240px; width:200px" /></strong></span></span></span></p>

<p><strong>Druga edycja</strong> programu Watchdog.edu.pl (lata 2011-2012) przyczynila sie do tego, ze niepubliczne szkolnictwo wyzsze zostalo w szeregu orzeczen ostatecznie uznane za sprawe publiczna. Badanie objelo 111 uczelni korzystajacych z dotacji&nbsp;publicznych, przeprowadzono je&nbsp;przy wykorzystaniu tej samej metodologii, kt&oacute;ra zastosowano do&nbsp;kontroli&nbsp;uczelnii panstwowych. Efekty badania opisano <strong><a href="http://archiwum.watchdog.edu.pl/files/raport-watchdogedupl-2012.pdf">w drugim raporcie</a></strong>, kt&oacute;ry r&oacute;wniez wydano jako publikacje ksiazkowa.<br />
Sady administracyjne&nbsp;w 14 sprawach prowadzonych z inicjatywy zespolu monitorujacego potwierdzily, ze informacje o systemie stypendialnym uczelni niepublicznych sa&nbsp;informacja publiczna. W ponad 40 uczelnianych regulaminach doszlo do istotnych zmian przepis&oacute;w.</p>

<p style="line-height: 20.8px; text-align: center;"><span style="font-family:arial,helvetica,sans-serif"><span style="font-size:22px"><span style="color:rgb(0, 0, 255)"><strong><img alt="" src="/content/200watchdog_03.jpg" style="height:240px; width:200px" /></strong></span></span></span></p>

<p style="line-height: 20.8px;"><strong>W trzeciej edycji</strong> monitoringu (lata 2014-2015) skupiono sie na sytuacji doktorant&oacute;w. Opr&oacute;cz uczelni badano plac&oacute;wki PAN oraz samodzielne instytuty naukowo-badawcze. Sprawdzono umowy o swiadczenie uslug edukacyjnych dla doktorant&oacute;w. Okolo 100 podmiot&oacute;w otrzymalo rekomendacje dotyczace pozadanych kierunk&oacute;w nowelizacji przepis&oacute;w wewnetrznych. Jeszcze w trakcie realizacji monitoringu 30 z nich zadeklarowalo wprowadzenie zmian. Sady administracyjne&nbsp;potwierdzily, ze organizatorzy studi&oacute;w niemajacy statusu uczelni r&oacute;wniez podlegaja rygorom transparentnosci. Badanie opisano <strong><a href="http://issuu.com/tlewin/docs/watchdog2015_internet">w trzecim raporcie</a></strong> watchdogowym FPS.</p>

<p style="line-height: 20.8px; text-align: center;"><img alt="" src="/content/203Watchdog.jpg" style="height:223px; width:180px" /></p>

<p style="line-height: 20.8px;">W marcu 2016 roku zakonczono<strong>&nbsp;czwarta&nbsp;edycje</strong> programu Watchdog.edu.pl (byl realizowany&nbsp;od 2014 roku). Dotyczyl barier w dostepie do bezplatnych studi&oacute;w magisterskich i doktoranckich.&nbsp;Badano&nbsp;m.in. uchwaly rekrutacyjne, protokoly z rozm&oacute;w kwalifikacyjnych, kryteria naboru, dane statystyczne dotyczace&nbsp;kandydat&oacute;w i os&oacute;b przyjetych, decyzje administracyjne o zakonczeniu postepowania.<br />
Uczelnie objete monitoringiem otrzymaly rekomendacje dotyczace postulowanych zmian w systemie naboru uczestnik&oacute;w studi&oacute;w drugiego i trzeciego stopnia. Do&nbsp;wszystkich&nbsp;uczelni publicznych rezeslano raport koncowy<u>&nbsp;</u>z monitoringu. Szerzej o tym monitoringu piszemy na stronie&nbsp;<strong><a href="http://rekrutacje.polska.edu.pl/">rekrutacje.polska.edu.pl</a></strong>.</p>

<p style="line-height: 20.8px; text-align: center;"><span style="font-family:arial,helvetica,sans-serif"><span style="font-size:22px"><span style="color:rgb(0, 0, 255)"><strong><img alt="" src="/content/200watchdog_05.jpg" style="height:240px; width:200px" /></strong></span></span></span></p>

<p style="line-height: 20.8px;">W roku 2015 rozpoczela sie <strong>piata edycja </strong>programu Watchdog.edu.pl, kt&oacute;ra dotyczy konkurs&oacute;w na stanowiska naukowo-dydaktycznych na uczelniach publicznych sa dla wielu uczonych zr&oacute;dlem frustracji. Media oraz instytucje skupiajace nauczycieli&nbsp;akademickich przytaczaja przyklady konkurs&oacute;w, w kt&oacute;rych, niczym w przedstawieniu teatralnym, wszystkie role byly z g&oacute;ry rozpisane, a nazwisko zwyciezcy od poczatku znajdowalo sie w scenariuszu. Monitoring bada skale &bdquo;ustawionych&rdquo; konkurs&oacute;w, spos&oacute;b ich organizacji i promowania&nbsp;wsr&oacute;d potencjalnych kandydat&oacute;w, wplyw na ograniczenie mobilnosci pracownik&oacute;w naukowych oraz zubozenie oferty uczelni. Wiecej o monitoringu na stronie&nbsp;<a href="http://konkursy.polska.edu.pl/">konkursy.polska.edu.pl</a>.</p>


