<p style="text-align: center;"><strong><span style="font-size: 18pt;">WIELKANOC - REZUREKCJA&nbsp;</span></strong></p>
<p style="text-align: center;">&nbsp;</p>
<p><a href="https://photos.app.goo.gl/XUxJs5cmJh2MQFFG7"><img style="display: block; margin-left: auto; margin-right: auto;" src="../../_resources/19_0_20190421093558.jpg" alt="" width="642" height="426" /></a></p>
<p style="text-align: justify;">&nbsp;&nbsp;</p>
<p style="text-align: justify;"><span style="font-size: 12pt;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;<a href="https://photos.app.goo.gl/XUxJs5cmJh2MQFFG7">Wiecej &gt;&gt;&gt;</a></span></p>
<p style="text-align: justify;"><span style="font-size: 14pt;">&nbsp;&nbsp;&nbsp; Wielkanoc to dzien pelen radosci, u kt&oacute;rej podstaw odnajdujemy cud zmartwychwstania. Poranna Msze rezurekcyjna poprzedza uroczysta procesja z Najswietszym Sakramentem wyniesionym z grobu Pana Jezusa dla podkreslenia tajemnicy, ze ten sam Zmartwychwstaly Chrystus jest obecny pod postaciami eucharystycznymi. Triumfalne bicie dzwon&oacute;w ma przypomniec trzesienie ziemi towarzyszace zmartwychwstaniu. Wszyscy spiewaja piesn "Wesoly nam dzien dzis nastal", pochodzaca z XVII w. Tradycyjna procesja trzykrotnie okraza kosci&oacute;l.</span></p>
<p style="text-align: justify;"><span style="font-size: 14pt;">&nbsp;&nbsp;&nbsp;&nbsp; Wielkanocna Msze sw. kaplani celebruja w bialych szatach. Teksty liturgiczne sa pelne radosci ze zwyciestwa Pana Jezusa odniesionego nad smiercia. Czytania biblijne przedstawiaja swiadectwo sw. Piotra o zmartwychwstaniu Chrystusa (Dz 10, 34; 37-43), wezwanie do wiernosci "temu, co w g&oacute;rze" (Kol 3, 1-4) oraz relacje o przybyciu kobiet i Apostol&oacute;w do pustego grobu (J 20, 1-9). Symbolem zwycieskiego Jezusa jest figura Zmartwychwstalego z choragwia w reku, wystawiana az do uroczystosci Wniebowstapienia. O wielkanocnej radosci przypominaja coraz rzadziej juz spotykane ( a szkoda) pozdrowienia: Chrystus zmartwychwstal - Prawdziwie zmartwychwstal.</span></p>
<p style="text-align: justify;"><span style="font-size: 14pt;">&nbsp;</span></p>
<p style="text-align: justify;"><span style="font-size: 14pt;"><strong>Alleluja! Jezus Zyje!</strong></span></p>
<p style="text-align: justify;"><span style="font-size: 14pt;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;Ta radosna nowina dobiega dzisiaj z kazdej swiatyni. Wesoly nam dzien dzis nastal - spiewamy wszyscy, bo Pan zmartwychwstal. Najwazniejsza uroczystosc Kosciola nazywamy Wielkanoca, poniewaz cud zmartwychwstania Jezusa Chrystusa dokonal sie nad ranem, o swicie. Uroczystosc ta jest pierwszym i najdawniejszym swietem w Kosciele. Obchodzona byla juz przez Apostol&oacute;w, kiedy Zydzi obchodzili swoja Pasche na pamiatke wybawienia z niewoli egipskiej. W tym samym czasie przypadla smierc Pana Jezusa i Jego zmartwychwstanie jako tajemnica naszego odkupienia, wybawienia z niewoli szatana. Zydzi obchodzili Pasche dnia 14 Nizan, bo tak przykazal im Mojzesz. Przez wyraz Nizan okreslano miesiac (30 dni) od nowiu marca do nowiu kwietnia. Poniewaz Pan Jezus zmartwychwstal w niedziele, dlatego chrzescijanie juz od II wieku pierwsza niedziele po pelni wiosennej zaczeli obchodzic jako Wielkanoc. Tak r&oacute;wniez uchwalil sob&oacute;r w Nicei w 325 r., dodajac, ze za pelnie wiosenna uwaza sie te, kt&oacute;ra wypada po zr&oacute;wnaniu dnia z noca, czyli po 21 marca. Dlatego Wielkanoc moze byc obchodzona pomiedzy 22 marca a 25 kwietnia.</span></p>
<p style="text-align: justify;"><span style="font-size: 14pt;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Uroczystosc Zmartwychwstania Panskiego jest najwazniejszym swietem w Kosciele. Mogloby sie wydawac, ze Boze Narodzenie - przyjscie na ziemie Syna Bozego, ma wieksza range. Jest jednak inaczej, gdyz dopiero swoim zmartwychwstaniem Chrystus potwierdzil, ze byl prawdziwie Mesjaszem i Synem Boga. Nawet kiedy Pan Jezus czynil cuda, mozna bylo powolac sie na dzialalnosc Mojzesza i prorok&oacute;w. Jednak cud zmartwychwstania przekonuje wszystkich, kt&oacute;rzy maja dobra wole i chca poznac prawde. Dlatego Wielkanoc jest sloncem roku koscielnego, centrum wszystkich tajemnic, zwiazanych z zyciem i poslannictwem na ziemi Jezusa Chrystusa.&nbsp;</span></p>
<p style="text-align: justify;"><span style="font-size: 14pt;">&nbsp;&nbsp;&nbsp;&nbsp; W Katechizmie Kosciola Katolickiego czytamy: "Zmartwychwstanie stanowi przede wszystkim potwierdzenie tego wszystkiego, co sam Chrystus czynil i czego nauczal. Wszystkie prawdy, nawet najbardziej niedostepne dla umyslu ludzkiego, znajduja swoje uzasadnienie, gdyz Chrystus dal ich ostateczne, obiecane przez siebie potwierdzenie swoim Boskim autorytetem" (nr 651). "Jesli Chrystus nie zmartwychwstal, daremne jest nasze nauczanie, pr&oacute;zna jest takze wasza wiara" (1 Kor 15, 14) .&nbsp;</span></p>
<p style="text-align: justify;"><span style="font-size: 14pt;">&nbsp;&nbsp;&nbsp;&nbsp; Misterium Zmartwychwstania Chrystusa jest rzeczywistym wydarzeniem, potwierdzonym znakami historycznymi, jak swiadczy o tym Nowy Testament. Okolo 56 r. sw. Pawel napisal do Koryntian: " Przekazalem wam na poczatku to, co przejalem: ze Chrystus umarl - zgodnie z Pismem - za nasze grzechy, ze zostal pogrzebany, ze zmartwychwstal trzeciego dnia, zgodnie z Pismem; i ze ukazal sie Kefasowi, a potem Dwunastu" (1 Kor 15, 3-4). Cale to wydarzenie pozostaje jednak tajemnica wiary.&nbsp;</span></p>
<p style="text-align: justify;"><br /><span style="font-size: 14pt;"> Sam wyraz "zmartwychwstac" oznacza powstac z martwych, przejsc ze smierci do zycia. Smierc polega na odlaczeniu duszy od ciala, natomiast zmartwychwstanie - na ponownym polaczeniu duszy z cialem. Jedynie B&oacute;g, kt&oacute;ry jest Panem zycia i smierci, moze tego cudu dokonac, gdyz jako Stworzyciel jest wszechmocnym Panem stworzen i praw, jakie nimi rzadza. &nbsp;&nbsp;&nbsp;</span></p>
<p style="text-align: justify;"><span style="font-size: 14pt;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Pan Jezus przez zmartwychwstanie dal podstawe do wiary, ze i nasze ciala r&oacute;wniez kiedys ozywi, tak jak obiecal. Po smierci Chrystusa Zydzi nakazali Pilatowi, aby zabezpieczyl gr&oacute;b az do trzeciego dnia, by przypadkiem nie przyszli Jego uczniowie, nie wykradli Go i nie powiedzieli ludowi, iz powstal z martwych. Pieczolowitosc, z jaka Sanhedryn to uczynil, wskazywala na wykluczenie wszelkiej nadziei. A jednak wlasnie ta straz miala byc koronnym swiadkiem wydarzenia. Pusty gr&oacute;b i lezace pl&oacute;tna oznaczaja, ze cialo Chrystusa dzieki mocy Bozej uniknelo zniszczenia smierci. Potem, przez czterdziesci dni Chrystus zjawial sie wsr&oacute;d swoich uczni&oacute;w i wyznawc&oacute;w, jadl z nimi i rozmawial, wydajac ostatnie polecenia</span></p>
