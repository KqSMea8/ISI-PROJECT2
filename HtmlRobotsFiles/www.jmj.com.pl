<h2>JMJ - Automotive Catalytic Converters</h2>
We are significant and one of the first independent manufacturer of aftermarket emission control systems in Europe.
<br><br>
We produce <a href="/en/catalytic-converters">catalytic converters</a>, <a href="/en/dpf-filters">diesel particulate filters</a>, and <a href="/en/elastic-joints">flexible pipes</a>.
<br><br>
From the very beginning of our activity, we have been focusing on quality and dynamic increase of product range which resulted much more higher share in the European automotive parts and components sector. Our team consist more then 180 highly professional employees operate from a 6000m2 factory equipped in state of the art machinery.
<br><br>
Our reliability, well organized production and customer service were credibility by granting management system ISO 9001-2000 in 2003 year. Our products quality is approved by obtained homologations according to R-103R directive.
<br><br>
We export our wide range of products to all European Union countries as well as to USA, Russia, Israel, Turkey, Australia, and more.
