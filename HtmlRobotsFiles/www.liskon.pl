<header>Koncert zespolu Livermorium!!!</header>
<section><div class="date-by">Dodano : 15.01.2018 20:17:29, przez: Aoi</div><p style="text-align: justify;">Grali dla nas na pierwszym i drugim Liskonie, po rocznej przerwie powracaja!</p><br />
<p style="text-align: justify;">Zesp&oacute;l folk metalowy zalozony w 2012 r. w Kedzierzynie-Kozlu, reaktywowany po kr&oacute;tkiej przerwie w 2013r pod nazwa Livermorium, aktualnie w szescioosobowym skladzie. Nazwa Livermorium nawiazuje bezposrednio do wszechobecnej branzy chemicznej w miescie zalozenia zespolu - Kedzierzyine-Kozlu oraz do kierunku ksztalcenia poczatkowych czlonk&oacute;w zespolu - nowoodkryty pierwiastek zabrzmial dla nich idealnie. Obecnie dzialalnosc zespolu skupia sie na komponowaniu nowych utwor&oacute;w bedacych polaczeniem ostrego brzmienia z delikatnymi folkowymi motywami oraz koncertach. Mozna uslyszec u nich sredniowieczne dudy, flety, piszczalki a nawet mongolski topshur. Grali przy takich zespolach jak Percival Schuttenbach, Runika, Radogost, Netherfell czy Morhana, a niebawem ma sie pojawic plyta.</p><br />
