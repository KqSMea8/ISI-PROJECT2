<div class="box"><h1>Strona Glowna</h1>
<div class="white-box"><p>Na tej stronie odnajda Panstwo wielka baze wiadomosci dotyczacych posiedzen Sejmu RP , w ktorych obok biezacych informacji znajda Panstwo rowniez dane historyczne. Informacje zebrane sa szczegolowo i opublikowane w latwo dostepnej formie ulatwiajacej wyszukiwanie.</p>

<p>Wymieniamy rozne opinie, ktore padly w ramach dyskusji w czasie posiedzen Sejmu, dodajemy przedmiot tych rozmow jak rowniez okreslamy osoby biorace w nich udzial. Dla poszerzenia Panstwa wiedzy z zakresu informacji dotyczacych sejmu przygotowalismy takze artykuly ogolne na temat wspolnych posiedzen sejmu i senatu, przebiegu glosowania sejmu, historii sejmu czy liczb zwiazanych z sejmem.</p>

<p>Wszelkie publikowane w naszym serwisie dane moga Panstwo przeszukiwac wedlug kategorii takich jak posiedzenie oraz konkretne dni, w ktorych to posiedzenie sie odbywalo, klub parlamentarny, z ktorego pochodza wypowiadajacy sie poslowie, osoby zabierajace glos w trakcie dyskusji oraz zagadnienia poruszane podczas posiedzen Sejmu. Wyszukiwanie po dacie posiedzenia umozliwia rowniez archiwum dyskusji znajdujace sie pod menu kategorii.</p>

<p>Intuicyjny sposob poruszania sie po witrynie, podglad toku wczesniejszych i kolejnych przemowien w trakcie danego posiedzenia, jak rowniez stale uzupelniane informacje pozwola na bycie na biezaco w omawianych tematach.</p>
</div>
