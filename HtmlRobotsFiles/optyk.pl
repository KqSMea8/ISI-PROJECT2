<h1>O nas</h1>
</div>
<p>z nami <strong style="font-size: 120%;">zobaczysz swiat wyrazniej</strong></p>
<p>Jestesmy firma rodzinna powstala w poprzednim tysiacleciu w Lublinie. W naszej pracy przestrzegamy zasad etyki zawodowej, jako <strong>priorytet traktujac dobro</strong><strong> pacjenta</strong>.</p>
<p>Specjalizujemy sie w wykonywaniu i naprawie okularow korekcyjnych, w tym: progresywnych, pryzmatycznych oraz ochronnych. Jako jedna z nielicznych firm w regionie zajmujemy sie doborem i wykonywaniem pomocy optycznych dla osob slabowidzacych.</p>
<p>Realizujemy zlecenia na wszystkie typy soczewek korekcyjnych. Nasze doswiadczenie zaowocowalo przyznaniem nam <strong>Certyfikatu Specjalisty Varilux</strong>, dzieki ktoremu mozemy oferowac naszym klientom najbardziej zaawansowane i innowacyjne soczewki okularowe.</p>
<p>Okulary progresywne wykonane w naszej pracowni posiadaja <strong>gwarancje latwej</strong><strong> adaptacji</strong>, dzieki ktorej nasi klienci maja zapewniona pelna satysfakcje z zamowionych okularow.</p>
<p>Zapraszamy Panstwa na badanie wzroku, w trakcie ktorego okreslimy wade wzroku, dobierzemy okulary oraz soczewki kontaktowe.</p>
<p>Jestesmy partnerem<strong> <a href="fundacja-one-sight.html">Fundacji One Sight</a></strong>, zbierajacej uzywane okulary korekcyjne, ktore po oczyszczeniu i naprawie sa przekazywane osobom zyjacym w biednych rejonach swiata.</p>
<p>Realizujemy <strong>recepty refundowana przez NFZ</strong>.</p>
<p>Korzystajac z naszych uslug, spotkacie sie Panstwo z mila i fachowa obsluga. Chetnie odpowiemy na pytania i postaramy sie rozwiac dreczace Panstwa watpliwosci.</p>				</div>
