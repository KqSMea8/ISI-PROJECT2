<p><a name="okancelarii"></a></p>
<h2 style="text-align: center;">O kancelarii</h2>
<p>Kancelaria swiadczy kompleksowe uslugi prawne na rzecz osob prawnych oraz osob fizycznych z zakresu prawa cywilnego, rodzinnego, karnego, gospodarczego oraz administracyjnego. Oferujemy zarowno uslugi dlugoterminowe w ramach stalych umow o wspolprace, jak i wykonywanie jednorazowych i indywidualnych zlecen. Podejmujemy sie reprezentacji Klientow w toku calego procesu, a takze wykonywania jednorazowych czynnosci (na przyklad sporzadzenia pozwu, opinii, apelacji i innych pism procesowych, reprezentacji na poszczegolnych etapach postepowania). Cechuje nas doswiadczenie, zdobyte podczas wspolpracy z krakowskimi i czestochowskimi kancelariami prawnymi, zaangazowanie oraz indywidualne podejscie do kazdej zleconej sprawy. Proponujemy najkorzystniejsze i najefektywniejsze rozwiazania, biorac pod uwage specyfike konkretnej sprawy. Poslugujemy sie jezykami obcymi &#8211; angielskim, francuskim i wloskim.</p>
<p>Kancelaria ma siedzibe w centrum Czestochowy. Po uprzednim ustaleniu, istnieje mozliwosc indywidualnego umowienia spotkania poza siedziba i godzinami pracy Kancelarii. Proponujemy takze zdalne konsultacje spraw, m.in. za posrednictwem poczty elektronicznej.<br />
<a name="zakresuslug"></a></p>
<h2 style="text-align: center;">Zakres uslug</h2>
<p>Przedsiebiorcom oferujemy kompleksowa obsluge prawna dostosowana do potrzeb oraz specyfiki prowadzonej dzialalnosci - udzielamy porad prawnych z zakresu prawa gospodarczego i handlowego, sporzadzamy projekty umow w obrocie gospodarczym oraz projekty wszelkich pism procesowych i przedprocesowych, opiniujemy umowy, zapewniamy reprezentacje podczas negocjacji handlowych, prowadzimy spory sadowe (miedzy innymi sprawy o zaplate, sprawy z zakresu prawa autorskiego, prawa budowlanego, prawa wlasnosci przemyslowej) oraz zapewniamy reprezentacje w postepowaniach ugodowych. Prowadzimy sadowe i pozasadowe czynnosci prowadzace do egzekucji przyslugujacych wierzytelnosci. Reprezentujemy dluznikow w postepowaniu egzekucyjnym.</p>
<p>&nbsp;</p>
<p>Oferujemy rowniez pomoc w zakladaniu dzialalnosci gospodarczej, rejestracji spolek, tworzeniu statutow i umow spolek oraz reprezentacje przed sadami rejestrowymi.</p>
<p>&nbsp;</p>
<p>Klientom indywidualnym oferujemy szeroki zakres uslug, miedzy innymi prowadzenie spraw:</p>
<p>&#8211; o rozwod i separacje</p>
<p>&#8211; o zniesienie malzenskiej wspolnosci majatkowej i podzial majatku wspolnego,<br />
&#8211; o zasadzenie, podwyzszenie, obnizenie, uchylenie alimentow</p>
<p>&#8211;  dotyczacych wladzy rodzicielskiej, opieki i ustalenia kontaktow z dzieckiem</p>
<p>&#8211;  dotyczacych ustalenia i zaprzeczenia ojcostwa</p>
<p>&#8211;  o ubezwlasnowolnienie</p>
<p>&nbsp;</p>
<p>&#8211; postepowania przeciwko bankom</p>
<p>&#8211; o zaplate</p>
<p>&#8211; o odszkodowanie i zadoscuczynienie przeciwko sprawcom i ubezpieczycielom</p>
<p>&#8211; dotyczacych ustanowienia i zniesienia sluzebnosci</p>
<p>&#8211;  o zasiedzenie</p>
<p>&#8211; dotyczacych ochrony wlasnosci i posiadania</p>
<p>&#8211; z zakresu prawa spadkowego (miedzy innymi stwierdzenie nabycia spadku, sprawy o zachowek, dzial spadku)</p>
<p>&nbsp;</p>
<p>W postepowaniu karnym zapewniamy:</p>
<p>&#8211; repreprezentacje osob pokrzywdzonych na etapie postepowania przygotowawczego i sadowego,</p>
<p>&#8211; obrone podejrzanych i oskarzonych na kazdym etapie postepowania i we wszystkich instancjach,</p>
<p>&#8211; obrone w sprawach o wykroczenia,</p>
<p>&#8211; reprezentacje w sprawach zwiazanych z zatrzymaniem oraz tymczasowym aresztowaniem</p>
<p>&#8211; reprezentacje w sprawach zwiazanych z odroczeniem wykonania kary, uzsykaniem przerwy w odbywaniu kary, przedterminowym zwolnieniem z odbycia reszty kary pozbawienia wolnosci, zarzadzeniem wykonania kary warunkowo zawieszonej</p>
<p>&nbsp;</p>
<p>Oferujemy rowniez reprezentacje przed organami i sadami administracyjnym.<br />
<a name="wynagrodzenie"></a></p>
<h2 style="text-align: center;">wynagrodzenie</h2>
<p>Wynagrodzenie ustalamy indywidualnie podczas pierwszego spotkania z Klientem, a jego wysokosc uzalezniona od rodzaju sprawy, stopnia jej skomplikowania oraz koniecznego nakladu pracy.</p>
<p>Proponujemy nastepujace formy rozliczen:<br />
&#8211; wynagrodzenie ryczaltowe - stale wynagrodzenie, ktorego wysokosc okreslona jest w umowie o wspolpracy<br />
&#8211; wynagrodzenie godzinowe - wysokosc uzalezniona jest od liczby godzin poswieconych na prace przy zleconej sprawie<br />
&#8211; wynagrodzenie jednorazowe - ustalane indywidualnie, zalezne jest od rodzaju sprawy i nakladu pracy.<br />
