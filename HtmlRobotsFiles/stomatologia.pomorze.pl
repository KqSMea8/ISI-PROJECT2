<h1 class="start"><span>O firmie</span></h1>


<div class="opis">
<p><p style="text-align: justify;">
Firma powstala w 2000 r. w wyniku restrukturyzacji SP ZOZ w Pucku. Od stycznia 2001 roku swiadczy uslugi zdrowotne w&nbsp; zakresie stomatologii og&oacute;lnej&nbsp; w formie Praktyki Lekarskiej.</p>
<p style="text-align: justify;">
&nbsp;</p>
<hr />
<p style="text-align: justify;">
Siedziba firmy obecnie zostala przeniesiona z ul. I Armii Wojska Polskiego na ul Abrahama 10A w Pucku.</p>
<p style="text-align: justify;">
W w/w lokalizacji znajduje sie gabinet stomatologiczny oraz pracownia&nbsp; rentgenowska.</p>
<p style="text-align: justify;">
&nbsp;</p>
<p style="text-align: justify;">
Drugi zarejestrowany w ramach Praktyki Lekarskiej gabinet stomatologiczny umiejscowiony jest&nbsp; w Pucku przy ulicy Meczennik&oacute;w Piasnicy 4A&nbsp; i jest przystosowany dla przyjec doraznych&nbsp; pacjent&oacute;w.</p>
<p style="text-align: justify;">
&nbsp;</p>
<hr />
<p style="text-align: justify;">
Uslugi stomatologiczne wykonywane sa zgodnie z systemem ISO 9001:2008, wymogami prawnymi oraz wymogami Narodowego Funduszu Zdrowia.</p>
<p style="text-align: justify;">
