<div class="ofirmie_title">o firmie</div>
<div class="ofirmie_text"><p>Jestesmy firma zajmujaca sie obsluga audio-wizualna i multimedialna wszelkiego rodzaj&oacute;w zawod&oacute;w sportowych, imprez kulturalnych, festiwali, event&oacute;w, targ&oacute;w, konferencji i szkolen. Od wynajmu sprzetu po jego konfiguracje i zarzadzaniem praca. W naszej ofercie wynajmu znajda Panstwo m.in. telebimy, sciany wizyjne, kamery, rezyserki obrazu, monitory dotykowe, systemy kontroli dostepu, rzutniki multimedialne, systemy pomiaru czasu oraz wiele inny rozwiazan niezbednych przy organizacji r&oacute;znego rodzaju imprez.</p>
<p>Nieustannie zmieniajacy sie rynek stawia przed nami coraz to nowe wyzwania, na kt&oacute;re odpowiadamy zgodnie z aktualnymi potrzebami naszych klient&oacute;w. A wszystko to mozliwe jest dzieki profesjonalnemu zespolowi pracownik&oacute;w, kt&oacute;ry pomoze Panstwu dobrac najefektywniejsze rozwiazania dla organizowanego przez Panstwa wydarzenia oraz zapleczu sprzetowemu, kt&oacute;re jest stale rozwijane, tak aby odpowiadalo najaktualniejszym trendom na rynku.</p>
<p>Obslugujemy imprezy w calej Polsce i poza granicami kraju. Dodatkowo naszym atutem sa scisle wsp&oacute;lpracujacy dzialy: multimedi&oacute;w oraz it, kt&oacute;re wsp&oacute;lnie tworza rozwiazania webowe wspomagajace realizacje imprez oraz sluza doradztwem przy tworzeniu tresci prezentacyjnych.&nbsp;</p></div>

