<p id="title">Dzialali Lokalnie</p>
<p id="data">26 marca 2019</p><br>
<img src="/img/1procent.png" style="float: left; margin-right: 10px" width="65">
<p>
W lutym Fundacja Lezajskiego Stowarzyszenia Rozwoju zakonczyla realizacje kolejnej edycji programu grantowego Dzialaj Lokalnie Polsko Amerykanskiej Fundacji Wolnosci. Program ten pozwala mieszkancom malych miejscowosci pozyskiwac fundusze na dzialania sluzace wspolnemu dobru. Do dnia dzisiejszego w ramach kilkunastu edycji programu Dzialaj Lokalnie przekazalismy lokalnej spolecznosci ponad 0,5 mln zlotych wspierajac realizacje ponad 155 przedsiewziec. W ramach ostatniej edycji programu dotacje na realizacje projektow trafily do 4 organizacji.
</p>
<p>
Stowarzyszenie Rozwoju Wsi Sarzyna zatroszczylo sie o bezpieczenstwo mieszkancow. W ramach projektu "Bezpieczni w swiecie - bezpieczni w necie" powstalo Miasteczko Ruchu Drogowego, odbyly sie warsztaty z zakresu udzielania pierwszej pomocy, spotkania ze strazakami dotyczace bezpieczenstwa pozarowego, spotkania z policjantami poswiecone bezpiecznemu zachowaniu sie w ruchu drogowym, a takze zajecia z pedagogiem z zakresu bezpieczenstwa w Internecie. Dzieci i mlodziez mogly sprawdzic swoja wiedze w Turnieju Wiedzy o Ruchu Drogowym.
</p>
<p>
Stowarzyszenie Rozwoju Wsi Wolka Grodziska zrealizowalo projekt "Turki ida". Glownym celem tego przedsiewziecia bylo przygotowanie tradycyjnych nakryc glowy dla czlonkow Wielkanocnej Strazy Grobowej. W ramach projektu odbyly sie m.in. warsztaty wyrobu czapek dla Turkow i bukietow konwalii zdobiacych te czapki. W zajeciach uczestniczyli zarowno dorosli jak i mlodzi mieszkancy miejscowosci. Zebrano zdjecia oraz informacje na temat grup Turkow ich dowodcow, ktore umieszczono w "Kronice Turki z Wolki Grodziskiej" a takze na stronie internetowej szkoly podstawowej prowadzonej przez Stowarzyszenie.
</p>
<p>
Biblioteka Publiczna Miasta i Gminy Kanczuga zrealizowala projekt "Dziennikarskie wariacje w bibliotece" skierowany do uczniow lokalnych szkol. Jego uczestnicy wzieli udzial w profesjonalnych warsztatach dziennikarskich oraz fotograficznych. Efekty pracy mlodych ludzi mozna bylo obejrzec podczas wystawy zorganizowanej na zakonczenie projektu przez Biblioteke Publiczna.
</p>
<p>
Szkola Podstawowa im. Sw. Jana Kantego w Lopuszce Wielkiej w ramach projektu ,,Dobre wychowanie niech z nami zostanie" przeprowadzila dzialania majace na celu przyblizenie mlodziezy podstawowych zasad savoir - vivre oraz wyposazenie w umiejetnosc kulturalnego zachowania oraz wychowanie w duchu szacunku dla drugiego czlowieka. Uczniowie szkoly uczestniczyli w zajeciach edukacyjnych, warsztatach z zakresu zachowania sie przy stole. Biblioteka wzbogacila sie o pozycje z zakresu savoir - vivre dla dzieci i mlodziezy. Cala lokalna spolecznosc spotkala sie na miedzypokoleniowej biesiadzie.
</p>
<p>
<strong>Realizacja programu Dzialaj Lokalnie nie bylaby mozliwa bez zaangazowania mieszkancow naszego regionu. Dzialania Fundacji mozna wesprzec m.in. przekazujac na jej rzecz 1% podatku dochodowego. Wystarczy wpisac w odpowiednia rubryke rocznego zeznania podatkowego KRS 0000328376.</strong>
</p>
<p>
<strong>PPrzekazujac 1% podatku dochodowego naszej organizacji przekazujesz go tym samym ludziom i organizacjom z Twojego najblizszego otoczenia. Twoje pieniadze zostana w miejscu, w ktorym zyjesz, z ktorego pochodzisz lub do ktorego czujesz sentyment.</strong>
</p>

