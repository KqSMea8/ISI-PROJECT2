Do 3 czerwca 2016 r. Fundacja Fundusz Lokalny w Lezajsku przyjmuje wnioski o dotacje w ramach IX edycji programu "Dzialaj Lokalnie" Polsko-Amerykanskiej Fundacji Wolnosci, prowadzonego we wspolpracy z Akademia Rozwoju Filantropii w Polsce.<br>
W tegorocznej edycji Programu "Dzialaj Lokalnie" wspierane beda projekty, ktore inicjuja wspolprace mieszkancow na rzecz dobra wspolnego. Program prowadzony jest z mysla o organizacjach pozarzadowych oraz grupach nieformalnych, ktore podejmuja wspolny wysilek, aby w ich spolecznosciach zylo sie lepiej.<br>
Maksymalna kwota dotacji - 6 000 zl.

