<p><strong>Koncepcja &bdquo;Miasta w miescie&rdquo;</strong><br />
Bazantowo to przestrzen tworzona z pasja. Na terenie blisko 25 ha, w oparciu o przemyslany plan urbanistyczny Millenium Inwestycje Sp. z o.o. od 17 lat konsekwentnie realizuje&nbsp;projekt rozbudowy osiedla o charakterze &bdquo;miasta w miescie&rdquo;. Bazantowo&nbsp;laczy w sobie funkcje: <em>mieszkalne, handlowo-uslugowe, sportowe,&nbsp;edukacyjne i rekreacyjne.</em></p>

<p><strong>Zadbalismy o wszystko!</strong><br />
W obrebie osiedla znajduja sie<strong>: Centrum Sportowe, Centrum&nbsp;Handlowe, Przedszkole, Szkola podstawowa i Gimnazjum, a takze Senior Residence.</strong></p>

<p><strong>Bazantowo Sport</strong> to tetniacy zyciem kompleks&nbsp;sportowo-rekreacyjny. Aktywni znajda tu&nbsp;miedzy innymi: hale tenisowa, squash, basen, saunarium, silownie i spa. Uzupelnieniem oferty jest: trzygwiazdkowy hotel, restauracja, pub i bowling. W centrum sportu funkcjonuje r&oacute;wniez osrodek rehabilitacji.</p>

<p><strong>Bazantowo Centrum Handlowe </strong>to: market premium Carrefour, centrum medyczne, apteka, banki, biuro podr&oacute;zy, salon kosmetyczny i fryzjerski, salon RTV i AGD, piekarnia, garmazeria, Rossmann, pralnia, ekodelikatesy,butik dzieciecy, salon Alkohole i Wina Swiata, restauracje: tajska, wloska oraz hiszpanska, salon masazu tajskiego, przychodnia weterynaryjna, a wszystko w przestronnym, nowoczesnym wnetrzu galerii.</p>

<p>W trosce o najmlodszych,&nbsp; zadbalismy nie tylko o place zabaw&nbsp;ale r&oacute;wniez o <strong>edukacje</strong>. Zesp&oacute;l budynk&oacute;w dydaktyczno-sportowych obejmuje miedzy innymi szkole podstawowa i gimnazjum prowadzone przez Zakon Pijar&oacute;w, przedszkole oraz szkole jezykowa.</p>

<p><strong>Lokalizacja i Komunikacja</strong><br />
Osiedle usytuowane jest na granicy poludniowych dzielnic Katowic: Piotrowic i&nbsp;Kostuchny, jednych z najbardziej zielonych&nbsp;dzielnic Katowic.<br />
Lokalizacja osiedla umozliwia wygodny dojazd do trasy S-81 w kierunku&nbsp;Wisly, S-86 w kierunku Bielska-Bialej, jak r&oacute;wniez w strone Krakowa,&nbsp;Wroclawia czy Warszawy. W poblizu znajduja sie tez przystanki autobusowe zapewniajace bezposrednia komunikacje z Centrum miasta oraz w kierunku Tych&oacute;w czy Mikolowa.</p>

<p><strong>Nowoczesna architektura&nbsp; i zielone otoczenie</strong></p>

<p>Zorganizowana struktura osiedla tworzy wewnetrzne Enklawy o zr&oacute;znicowanej skali i proporcjach architektonicznych. Kazda z nich wyr&oacute;znia sie wlasnym charakterem i stylem. Jasna kolorystyka elewacji z dodatkami element&oacute;w drewna i kamienia nadaje budynkom ciepla i lekkosci.<strong> </strong>Przestrzen osiedla uzupelniaja starannie opracowane elementy malej architektury miedzy innymi oczka wodne, strumyki i fontanny. Uporzadkowane skwery z oryginalna zielenia, alejki spacerowe oraz kolorowe place zabaw tworza przyjazne miejsce sprzyjajace budowaniu sasiedzkich wiezi.</p>

<p><br />
<strong>Komfort i Bezpieczenstwo</strong><br />
Osiedle posiada wszelkie rozwiazania niezbedne do wygodnego zycia.<br />
Dla zapewnienia bezpieczenstwa kompleks jest ogrodzony i monitorowany.&nbsp;System bram wjazdowych z siecia domofon&oacute;w oraz podzial na&nbsp;wewnetrzne&nbsp;enklawy zmniejsza ruch pojazd&oacute;w po calym osiedlu. Dzieki&nbsp;wykwalifikowanym Zarzadcom na osiedlu panuje spok&oacute;j i porzadek.</p>

