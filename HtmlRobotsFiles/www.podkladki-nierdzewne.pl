<h1>Rodzaje srub</h1>
<p>Kazdy mezczyzna podczas roznych prac domowych lub w warsztacie, z pewnoscia mial do czynienia ze srubami i innymi elementami laczeniowymi.<img src='images/0.JPG'  class='ac' /> Do wyboru jest ogromny wybor, w zaleznosci od zastosowania, rodzaju oraz wytrzymalosci polaczenia. Najpopularniejsze sa sruby z lbem w ksztalcie szesciokata, ktore wkreca sie przy pomocy specjalnych kluczy plaskich. Maja one najmocniejszy docisk, a takze wytrzymalosc na rozne przeciazenia. Stosowane glownie w przemysle budowlanym, maszynowym, wlasciwie tam gdzie pracuje sie z duzymi obciazeniami. Jak wiadomo dostepnych jest wiele wymiarow takich srub, moga miec od kilku do kilkudziesieciu centymetrow dlugosci i srednicy. Bardziej znormalizowanymi,  stosowane na co dzien sa sruby tzw. imbusowe. Oznacza to, iz w lebku maja otwor w ksztalcie szesciokata. Aby moc wkrecic tego typu srube rowniez potrzebny jest specjalny klucz imbusowy. Oprocz tego istnieje wiele innych typow, np. z lebkiem w ksztalcie motylka lub hakowe, ktore mozna wkrecic recznie. Oczywiscie wszystkie sruby posiadaja wyzlobione rowki, czyli gwinty, po to by moc docisnac skrecany material nakretka z drugiej strony.</p>
