<td valign="top" width="600"  style="padding:20px;">      <p><font size="5" face="Times New Roman">S</font>zanowni Klienci !<br />
Serdecznie zapraszamy do naszej internetowej galerii sztuki.&nbsp;</p>
<p>W naszej ofercie znajda panstwo obrazy olejne, akwarele, pastele, grafiki.&nbsp;Posiadane przez nas obrazy olejne to zar&oacute;wno klasyczne tematy takie jak martwa natura, kwiaty, ale i takze abstrakcje. Wykonujemy tez obrazy olejne na zam&oacute;wnie.&nbsp;Realizaujemy r&oacute;wniez profesjonalne portrety na zam&oacute;wnie zar&oacute;wno portrety olejne jak i portrety pastelowe. Wykonujemy portrety slubne i portrety stylizowane.&nbsp;Wykonujemy oprawe obraz&oacute;w zar&oacute;wno obraz&oacute;w olejnych jak i grafik czy pasteli. &nbsp;</p>
<p>Swiadczymy takze&nbsp; szeroki wachlarz uslug&nbsp; zwiazanych&nbsp; z nasza branza , a mianowicie:<br />
&nbsp;&nbsp;&nbsp;&nbsp; &bull; profesjonalne doradztwo w zakresie doboru odpowiedniej ramy, kolorystyki i rodzaju passe-partout. Oprawiamy kazda rzecz od obraz&oacute;w olejnych poprzez &nbsp;grafiki,pastele, zdjecia a nawet wyszywanki !<br />
&nbsp;&nbsp;&nbsp;&nbsp; &bull; werniksowanie i odswiezanie obraz&oacute;w olejnych.<br />
