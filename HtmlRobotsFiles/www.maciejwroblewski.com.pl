<h3 class="blueLine animated fadeInUp">Przechowywanie i przetwarzanie danych osobowych w Specjalistyczny Gabinet Pediatryczny Maciej Wroblewski</h3>
<div class="space_30"></div>
<p>1. Administratorem danych osobowych, zbieranych i przetwarzanych w zwiazku z prowadzona dzialalnoscia gospodarcza jest Specjalistyczny Gabinet Pediatryczny Maciej Wroblewski ul. Zmudzka 11, 60-604 Poznan, NIP:  781-120-15-17, kontakt: 601953674.</p>
<p>2. Administrator zbiera dane osobowe od opiekunow prawnych pacjentow takie jak: imie i nazwisko, numer telefonu, w celu rejestracji wizyt w gabinecie oraz kontaktu w sprawie wizyt; a w przypadku umowienia wizyty zbiera dodatkowo dane pacjentow - dzieci takie jak: imie, nazwisko, adres oraz numer PESEL w celu wystawiania recept i zaswiadczen, rozliczen z tytulu swiadczonych uslug medycznych, w zakresie niezbednym do realizacji tych celow.</p>
<p>3. Administrator zbiera i przetwarza dane szczegolnie chronione od pacjentow w postaci danych medycznych do celow profilaktyki zdrowotnej i leczenia, w zwiazku z wykonywaniem uslug opieki zdrowotnej, zgodnie z art. 9 RODO, ust. 2.</p>
<p>4. Kazdemu, kto udostepnil swoje dane Administratorowi przysluguje prawo dostepu do tresci danych oraz ich sprostowania, usuniecia lub ograniczenia przetwarzania. Ponadto przysluguje rowniez prawo wniesienia sprzeciwu, zazadania zaprzestania przetwarzania danych, prawo do cofniecia zgody na przetwarzanie w dowolnym momencie oraz prawo do wniesienia skargi do organu nadzorczego Prezesa Urzedu Ochrony Danych Osobowych.</p>
<p>5. Podanie danych jest dobrowolne, lecz niezbedne do realizacji celow wymienionych w punkcie 2. W przypadku niepodania danych nie bedzie mozliwa rejestracja na wizyty i skorzystanie ze swiadczonych uslug medycznych.</p>
<p>6. Odbiorcami udostepnionych danych beda instytucje upowaznione z mocy prawa, ktore sa uprawnione do ich otrzymania na mocy obowiazujacych przepisow (sa to organy wymiaru sprawiedliwosci, scigania i inne uprawnione w przepisach prawa).</p>
<p>7. Udostepnione dane osobowe, z wylaczeniem danych szczegolnie chronionych, moga byc powierzone wspolpracujacym z Administratorem podmiotom trzecim, zwanym Procesorami tych danych w celu ich przetwarzania w imieniu Administratora wylacznie w przypadku zawarcia przez Administratora z takimi podmiotami odpowiednich umow powierzenia danych. Procesorami danych osobowych sa wspolpracujace z Administratorem biuro rachunkowe oraz operatorzy innych uslug, niezbednych do wykonywania czynnosci wskazanych w punkcie 2.</p>
<p>8. Administrator danych nie bedzie przekazywac danych osobowych do panstwa trzeciego lub organizacji miedzynarodowej.</p>
<p>9. Dane pacjentow nie beda podlegaly profilowaniu w celach marketingowych.</p>
<p>10. Dane osobowe beda zbierane i przechowywane z zachowaniem nalezytej starannosci.</p>
<p>11. Dane w formie elektronicznej beda przechowywane na zabezpieczonych haslami urzadzeniach i nosnikach.</p>
<p>12. Dane w formie papierowej beda przechowywane w zamknietych szafach, w pomieszczeniach zabezpieczonych zamknieciem na klucz, do ktorego dostep beda mialy jedynie osoby upowaznione do przetwarzania danych osobowych.</p>
<p>13. Dane beda przechowywane od momentu ich przekazania i wyrazenia zgody na przetwarzanie danych osobowych do czasu zakonczenia realizacji czynnosci wymienionych w punkcie 2, maksymalnie do czasu zakonczenia prowadzenia dzialalnosci gospodarczej przez Administratora lub dluzej, jesli wymagaja tego odrebne przepisy, w szczegolnosci: dane ksiegowe - do 5 lat od daty wystawienia dokumentow.</p>
<p>14. Dane szczegolnie chronione w postaci kartotek pacjentow przechowywane beda przez okres 20 lat, zgodnie z art. 29 ustawy o prawach pacjenta i Rzeczniku Praw Pacjenta.</p>
