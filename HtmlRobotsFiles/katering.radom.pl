Nasza firma oferuje w milych kameralnych wnetrzach przyjecia rodzinne i roznego rodzaju spotkania okolicznosciowe, rowniez firmowe oraz obsluge konferencji.                    Zapewniamy tez catering domowy, firmowy i dla indywidualnych odbiorcow.<br />
Do dyspozycji Panstwa oddajemy duza sale restauracyjna/50 osob/, sale konferencyjna/150 osob/,obszerny hol umozliwiajacy odpoczynek lub bedacy miejscem zabaw dla dzieci.Hol wyposazony jest w stol do tenisa stolowego.                      Specjalnoscia naszej kuchni jest tradycyjna kuchnia polska,urozmaicona smakami kuchni europejskiej. Posilki sa smaczne, zdrowe i swieze.                                                    Gwarantujemy indywidualne podejscie do kazdego klienta, zapewniajac jednoczesnie fachowa pomoc.Uzgadniajac menu bierzemy pod uwage gusty klientow.                Staramy sie by kazda uroczystosc u nas byla piekna, udana i stanowila niezapomniane przezycie.                                                                                                                    <strong>Na BOZE NARODZENIE I WIELKANOC<span style="font-size: 15.2015px;">&#8211;</span></strong><span style="font-size: 0.95em;"><strong> dania wigilijne.   </strong>                                        Istniejemy na rynku od poczatku lat 90-tych ubieglego wieku i wyrobilismy sobie dobra marke.  Gwarantujemy wspaniale menu, fachowa obsluge, dobra zabawe w przytulnych wnetrzach.                                                                                                                                                           Organizujemy przyjecia plenerowe do 300 osob w urokliwym osrodku &#8222;Pod Brzozami&#8221;, ktory jest wyposazony w stanowiska grillowe i wiate/deszcz i wiatr nie sa wtedy przeszkoda/. Mozemy tez zorganizowac impreze na terenie Muzeum Wsi Radomskiej,np. w Dworku lub plenerze.</span></p>
