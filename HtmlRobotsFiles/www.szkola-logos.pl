<h2 class="title"><a href="http://www.szkola-logos.pl/?p=1229" title="Permalink to RODO" rel="bookmark">RODO</a></h2>

<div class="postmeta-primary">

<span class="meta_date">16/06/2018</span>
&nbsp;  <span class="meta_categories"><a href="http://www.szkola-logos.pl/?cat=49" rel="category">RODO</a></span>
</div>

<div class="entry clearfix">

<p style="text-align: left;"><strong>Klauzula informacyjna </strong></p>
<p style="text-align: left;"><strong>          o przetwarzaniu danych osobowych w Zespole Szkol Policealnych i Ogolnoksztalcacych LOGOS</strong></p>
<p style="text-align: left;">Zgodnie z art. 13 rozporzadzenia Parlamentu Europejskiego i Rady (UE) 2016/679 z dnia<br />
27 kwietnia 2016 r. w sprawie ochrony osob fizycznych w zwiazku z przetwarzaniem danych osobowych i w sprawie swobodnego przeplywu takich danych oraz uchylenia dyrektywy 95/46/WE (ogolnego rozporzadzenia o ochronie danych), Dz.U.UE.L.2016.119.1 (dalej: RODO) uprzejmie informuje, ze:</p>
<ol style="text-align: left;">
<li>Administratorem Pani/Pana/sluchacza danych osobowych jest Organ Prowadzacy Zespolu Szkol Policealnych i Ogolnoksztalcacych LOGOS, ktorej przedstawicielem jest Dyrektor Zespolu Szkol Policealnych<br />
i Ogolnoksztalcacych LOGOS w Piotrkowie Trybunalskim.</li>
<li>Kontakt z Inspektorem Ochrony Danych mozliwy jest pod adresem email: k.izydorczyklogos@gmail.com, tel. 44 649-39-97</li>
<li>Dane osobowe beda przetwarzane na podstawie art. 6 ust. 1 lit. c Rozporzadzenia o Ochronie Danych Osobowych, Ustawy - Prawo Oswiatowe z dn. 14 grudnia 2016 r. (Dz. U. z 2017 r., poz. 59 oraz Ustawy o Systemie Oswiaty z dnia 7 wrzesnia 1991 r. (Dz. U. z 2017 r., poz. 2198) w celu realizacji statutowych zadan dydaktycznych, opiekunczych i wychowawczych w placowce.</li>
<li>Na podstawie art. 6 ust. 1 lit. c oraz art. 9 ust.2 lit. b i c &#8211; ogolnego rozporzadzenia o ochronie danych osobowych z dnia 27 kwietnia 2016 r Pani/Pana/Sluchacza dane osobowe przetwarzane beda w celu:</li>
</ol>
<p style="text-align: left;">&#8211; rekrutacji sluchaczy do szkoly (art. 6 ust. 1 lit. c oraz art. 9 ust. 2 lit. b RODO),</p>
<p style="text-align: left;">&#8211; przetwarzanie jest niezbedne do wypelnienia obowiazku prawnego ciazacego na Administratorze,</p>
<p style="text-align: left;">&#8211; w innych przypadkach Pani/Pana/sluchacza dane osobowe przetwarzane beda wylacznie na podstawie wczesniej udzielonej zgody w zakresie i celu okreslonym w tresci zgody.</p>
<ol style="text-align: left;" start="5">
<li>Odbiorcami Pana/Pani/sluchacza danych osobowych beda:</li>
</ol>
<p style="text-align: left;">-podmioty przetwarzajace dane w naszym imieniu, uczestniczace w wykonywaniu czynnosci na nasza rzecz tj.: podmioty obslugujace i utrzymujace nasza siec informatyczna, podmioty swiadczace nam uslugi doradcze, audytowe, pomoc prawna, rachunkowa;</p>
<p style="text-align: left;">-inne podmioty uprawnione na podstawie przepisow prawa tj<strong>.: </strong>Ministerstwo Edukacji Narodowej, Kuratorium Oswiaty w Lodzi - delegatura w Piotrkowie Trybunalskim, Biuro Edukacji m.st. Warszawa, Okregowa Komisja Egzaminacyjna w Lodzi, Urzad Miasta w Piotrkowie Trybunalskim-Referat Edukacji.  W przypadku kontaktu<br />
w formie tradycyjnej odbiorca bedzie Poczta Polska. W przypadku komunikacji droga elektroniczna odbiorca Panstwa danych osobowych bedzie operator poczty elektronicznej.</p>
<p style="text-align: left;">&#8211; wylacznie podmioty uprawnione do uzyskania danych osobowych na podstawie przepisow prawa.</p>
<ol style="text-align: left;" start="6">
<li>Pana/Pani/sluchacza dane osobowe przechowywane beda przez okres niezbedny do realizacji wskazanych</li>
</ol>
<p style="text-align: left;"> w pkt. 3 celow, a po tym czasie przez okres wymagany przez przepisy prawa<em>. </em></p>
<ol style="text-align: left;" start="7">
<li>Posiada Pani/Pan prawo do: zadania od Administratora dostepu do swoich danych osobowych, ich sprostowania, usuniecia lub ograniczenia przetwarzania lub o prawie do wniesienia sprzeciwu wobec takiego przetwarzania, a takze prawo do przenoszenia danych.</li>
<li>Ma Pani/Pan prawo wniesienia skargi do organu nadzorczego, ktorym jest Prezes Urzedu Ochrony Danych Osobowych.</li>
<li>Podanie Pani/Pana/sluchacza danych jest wymogiem ustawowym i wynika z realizacji obowiazkow wynikajacych z przepisow prawa.</li>
<li>W trakcie przetwarzania Pani/Pana danych osobowych nie dochodzi do wylacznie zautomatyzowanego podejmowania decyzji ani do profilowania, o ktorych mowa w art. 22 ust. 1 i 4 RODO, co oznacza, ze zadne decyzje dotyczace osoby, ktorej dane dotycza, nie beda zapadac automatycznie oraz ze nie beda tworzone zadne profile dotyczacej Pani/Pana osoby.</li>
</ol>
<p style="text-align: left;"><strong>KLAUZULA INFORMACYJNA RODO</strong></p>
<p style="text-align: left;">(dla nauczycieli, pracownikow pedagogicznych i pracownikow obslugi administracyjnej szkoly, zwanych dalej pracownikami szkoly, w zwiazku                                 z pozyskiwaniem danych osobowych).</p>
<p style="text-align: left;">Zgodnie z rozporzadzeniem Parlamentu Europejskiego i Rady (UE) 2016/679 z dnia 27 kwietnia 2016 r. w sprawie ochrony osob fizycznych w zwiazku z przetwarzaniem danych osobowych i w sprawie swobodnego przeplywu takich danych oraz uchylenia dyrektywy 95/46/WE (ogolne rozporzadzenie  o ochronie danych) Informuje, ze:</p>
<p style="text-align: left;">
<ol style="text-align: left;">
<li>Administratorem danych osobowych pracownikow szkoly jest Organ Prowadzacy Zespol Szkol Policealnych I ogolnoksztalcacych LOGOS P, ktorej przedstawicielem jest Dyrektor Zespolu Szkol Policealnych i Ogolnoksztalcacych LOGOS w Piotrkowie Trybunalskim, z siedziba w Piotrkowie Trybunalskim ul. Slowackiego 114/118 zwany dalej administrujacym. Administrator prowadzi operacje przetwarzania nastepujacych kategorii danych osobowych pracownikow: imie            i nazwisko, data i miejsce urodzenia, adres zamieszkania, PESEL, imiona rodzicow, nazwisko rodowe matki, NIP, numer i seria dowodu osobistego, adresy poczty elektronicznej i numer telefonu, wyksztalcenie, zawod, stopien awansu zawodowego w przypadku nauczycieli, imiona i daty urodzenia wspolmalzonka  i dzieci.</li>
</ol>
<p style="text-align: left;">Telefon kontaktowy do administrujacego  44 649-39-97</p>
<p style="text-align: left;"> e- mail <a href="mailto:h.gornalogos@gmail.com">h.gornalogos@gmail.com</a></p>
<p style="text-align: left;">
<ol style="text-align: left;">
<li>Kontakt z Inspektorem Ochrony Danych mozliwy jest pod adresem</li>
</ol>
<p style="text-align: left;"> email: k.izydorczyklogos@gmail.com , tel. 44 649-39-97</p>
<ol style="text-align: left;">
<li>Dane osobowe pracownikow przetwarzane beda w zwiazku z realizacja zawartej</li>
</ol>
<p style="text-align: left;">z pracownikiem umowy o prace, zlecenia, dzielo. Odbiorca danych pracownikow beda podmioty wskazane w przepisach prawa oraz biuro rachunkowe.</p>
<ol style="text-align: left;">
<li>Podstawa przetwarzania danych osobowych pracownikow jest, ustawa o systemie informacji oswiatowej, ustawa Kodeks pracy, ustawa o pracownikach samorzadowych, rozporzadzenie Ministra Pracy i Polityki Socjalnej w sprawie zakresu prowadzenia przez pracodawcow dokumentacji w sprawach zwiazanych ze stosunkiem pracy oraz sposobu prowadzenia akt osobowych pracownika.</li>
<li>Pracownicy szkoly posiadaja prawo do:</li>
</ol>
<ol style="text-align: left;">
<li>zadania od administratora dostepu do danych osobowych i ich sprostowania,</li>
<li>wniesienia skargi do organu nadzorczego - Prezesa Urzedu Ochrony Danych Osobowych w zwiazku z niezgodnym z obowiazujacymi przepisami przetwarzaniem danych,</li>
<li>cofniecia zgody na przetwarzanie danych osobowych - dotyczy to tylko zgody na przetwarzanie danych wrazliwych, jezeli wczesniej taka zgoda zostala wyrazona. Pozostale dane osobowe zwykle pracownikow sa przetwarzane w wyniku spelniania obowiazku wynikajacego z przepisow prawa.</li>
<li>Dane osobowe pracownikow beda przechowywane przez okres wykonywania pracy w  Zespole Szkol Policealnych i Ogolnoksztalcacych LOGOS oraz po tym czasie przez okres wskazany w przepisach szczegolnych.</li>
<li>Dane pracownikow moga byc przetwarzane w sposob zautomatyzowany.</li>
</ol>
<p style="text-align: left;">
<p style="text-align: left;">Nie wykorzystujemy Panstwa danych osobowych w celach innych niz przewidziane przepisami prawa oraz przetwarzamy Panstwa dane tylko w zakresie w jakim jest to niezbedne i dbamy o ich bezpieczenstwo.</p>
