<p class="header">O nas</p>
<p class="content">
<p style=\"\\\"><span style=\"\\\"><span style=\"\\\"><span style=\"\\\">Witamy na stronie agencji fotografii artystycznej aVaStudio. Nasza firma istnieje od 2005 roku, stale rozwijajac sie i poszerzajac zakres uslug. Siedziba firmy miesci sie we Wroclawiu, ale pracujmy na terenie calej Polski oraz za granica.</span></span></span></p>
<p><span style=\"\\\"><span style=\"\\\">Specjalizujemy sie w fotografii slubnej, reportazu oraz sesjach zdjeciowych glamour, fashion i portrecie. Wykonujemy takze wszelkie inne zdjecia okolicznosciowe: komunie, chrzest, studni&oacute;wki, bale. Od 2006 roku wsp&oacute;lpracujemy z agencja prasowa, przez co nasze zdjecia sportowe ukazuja sie w prasie lokalnej, og&oacute;lnopolskiej a takze za granica. Zajmujemy sie r&oacute;wniez retuszem zdjec oraz projektami fotoksiazek. </span></span></p>
<p style=\"\\\"><span style=\"\\\"><span style=\"\\\"><span style=\"\\\"><span style=\"\\\"><span style=\"\\\">Pracujemy na najwyzej klasy cyfrowym sprzecie fotograficznym marki Canon. Do dyspozycji mamy kilka aparat&oacute;w oraz kilkanascie obiektyw&oacute;w. Do dyspozycji klient&oacute;w jest trzech fotograf&oacute;w i kamerzysta, z kt&oacute;rym na stale wsp&oacute;lpracujemy.</span></span></span></span></span></p>
<p style=\"\\\"><span style=\"\\\"><span style=\"\\\"><span style=\"\\\"><span style=\"\\\"><span style=\"\\\">Zapraszamy do odpowiednich dzial&oacute;w, aby zapoznac sie ze szczeg&oacute;lami naszej oferty i obejrzec obszerna galerie zdjec. W dziale kontakt do Panstwa dyspozycji jest formularz kontaktowy oraz podane sa inne formy komunikacji z nami.</span></span></span></span></span></p>
