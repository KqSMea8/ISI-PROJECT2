<h1>
O nas    </h1>

<div class="figure alignright"><img class="figure__img" src="http://re-konstrukcja.pl/wp-content/uploads/2013/07/dom_bigger.jpg" alt="Konstrukcje budowlane Re-Konstrukcja">
<div class="figure__number"><a href="tel:660946727"><i class="icon36 telephone"></i><span>+48 660 94 67 27</span></a></div>
</div>
<p>Jestesmy grupa wspolpracujacych ze soba specjalistow z zakresu projektowania wszelkiego rodzaju </span><span class="tx" style="color: #000000;"><strong>konstrukcji budowlanych i inzynierskich</strong>.</p>
<p>Specjalizujemy sie w opracowywaniu dokumentacji konstrukcyjnej do indywidualnych projektow </span><span class="tx" style="color: #000000;">budynkow jedno- i wielorodzinnych, ale zgodnie z potrzebami Naszych Klientow swiadczymy rowniez </span><span class="tx" style="color: #000000;">uslugi <strong>z zakresu nadzoru budowlanego i obslugi prawnej inwestycji</strong>.</p>
<p>Uwazamy, ze mozemy byc bardzo konkurencyjni cenowo w odniesieniu do wiekszosci biur </span><span class="tx" style="color: #000000;">projektowych, gdyz Nasz Klient placi tylko za prace osob bezposrednio zaangazowanych w wykonanie </span><span class="tx" style="color: #000000;">projektu, nie ponoszac przy tym kosztu administrowania swiadczona usluga.</p>
<p>Nasza oferte kierujemy zarowno do biur architektonicznych wykonujacych wlasne i adaptujacych </span><span class="tx" style="color: #000000;">gotowe projekty budowlane, jak rowniez bezposrednio do Inwestorow z sektora budownictwa </span><span class="tx" style="color: #000000;">jednorodzinnego, wielorodzinnych budynkow mieszkalnych, budynkow biurowych, hal </span><span class="tx" style="color: #000000;">produkcyjnych, magazynowych i sportowych.</p>
<p>Jestesmy otwarci na wszelkie propozycje wspolpracy!</p>
