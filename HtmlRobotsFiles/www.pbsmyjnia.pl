<p style="text-align: left;">
Zapewniamy dla kierowc&oacute;w pomieszczenie socjalne z WC, prysznicem, kuchnia oraz TV. Oferujemy bezplatny parking i mozliwosc korzystania z telefonu i internetu.&nbsp;</p>
<p style="text-align: left;">
Zapraszamy do korzystania z naszych uslug.</p>
