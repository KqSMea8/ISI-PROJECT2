<h2 style="text-align: left;">
Witamy na stronie Myjni Cystern PBS</h2>
<p style="text-align: left;">
Jako myjnia dzialamy od 1989 r., posiadamy w tej branzy bardzo duze doswiadczenie. Od 1998 roku specjalizujemy sie w w myciu wnetrz cystern i kontener&oacute;w. Myjemy mechanicznie wykorzystujac urzadzenia KARCHER, oraz wiodacych w tej branzy firm wloskich. Od maja 2004 roku przenieslismy sie do nowo wybudowanego kompleksu uslugowego, skladajacego sie z dwustanowiskowej myjni cystern i dodatkowego stanowiska do mycia zewnetrznego.</p>
<hr />
<p style="text-align: left;">
Myjnia jest czynna cala dobe, <strong>myjemy cysterny po srodkach spozywczych i chemicznych</strong> (osobne stanowiska) oraz silosy i 1000 - litrowe zbiorniki na europaletach i podgrzewamy kontenery goraca woda i para. Kazde wewnetrzne mycie cysterny i kontenera jest potwierdzane certyfikatem czystosci ECD.&nbsp;</p>
<div>
<strong>Chec umycia miedzy godzinami 24 - 6 rano musi byc zglaszana najp&oacute;zniej do godz. 23. Kontakt na numery kom&oacute;rkowe podane w zakladce <a href="/kontakt">kontakt.</a></strong></div>
