<p style="text-align: center; margin-left: 160px;">
<strong>PROGRAM LOJALNOSCIOWY</strong></p>
<p>
&nbsp;</p>
<p style="margin-left: 40px;">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Nasza firma stosuje prosty i przejrzysty program lojalnosciowy dla naszych uslugobiorc&oacute;w i ich kierowc&oacute;w oparty na miesiecznym okresie rozliczeniowym. Ten, gl&oacute;wnie przeznaczony dla firm program, polega na przyznawaniu rabat&oacute;w firmom, kt&oacute;re z nami stale wsp&oacute;lpracuja i korzystaja z naszych uslug wielokrotnie w ciagu miesiaca. Naliczanie rabatu nastepuje po zakonczeniu danego miesiaca rozliczeniowego (kalendarzowego) przy wystawianiu faktury za nasze uslugi.</p>
<p style="margin-left: 40px;">
&nbsp;</p>
<p style="margin-left: 40px;">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Rabaty dla firm i nagrody rzeczowe (gratyfikacje) dla kierowc&oacute;w przysluguja po spelnieniu nastepujacych warunk&oacute;w:</p>
<p style="margin-left: 40px;">
- od 10 do 30 myc w ciagu miesiaca udzielimy 2,5% rabatu,</p>
<p style="margin-left: 40px;">
- powyzej 30 myc w ciagu miesiaca udzielimy 5% rabatu.</p>
<p style="margin-left: 40px;">
