<h2>O nas</h2>

<div class="clear height10"></div>
<div class="col-md-8 col-md-offset-2">
<p>Historia firmy&nbsp;<strong>TOMEX</strong>&nbsp;rozpoczela sie w&nbsp;1990 roku kiedy to przy Rondzie Kocmyrzowskim powstal niewielki plac handlowy. W kr&oacute;tkim czasie nasz bazar stal sie&nbsp;<strong>najbardziej rozpoznawalna marka tej&nbsp;branzy w&nbsp;Malopolsce.</strong>&nbsp;W&nbsp;slad za tym sukcesem poszly dalsze dzialania, kt&oacute;re zaskutkowaly rozwinieciem sie bardzo wielu branz zwiazanych z tradycyjnym handlem i uslugami.</p>
<p>&nbsp;</p>
<p>Poczatki byly trudne, ale ogromna pasja i&nbsp;zapal zalozycieli polaczone z&nbsp;wielkim zaangazowaniem wszystkich kupc&oacute;w przynosily rezultaty. Kontynuujac tradycje najstarszego prywatnego placu handlowego Polski Poludniowej Firma rozwija sie nieustannie, poszerzajac powierzchnie handlowo &ndash; uslugowa i podnoszac standard. By sprostac rosnacym wymaganiom naszych klient&oacute;w stawiamy na niskie ceny i jakosc sprzedawanych produkt&oacute;w oraz swiadczonych uslug.</p>
<p>&nbsp;</p>
<p>W rezultacie, na niemal 7 hektarowym terenie powstala funkcjonalna i przestronna przestrzen handlowa zrzeszajaca&nbsp;<strong>ponad 500&nbsp; rzetelnych kupc&oacute;w</strong>, gdzie w&nbsp;milej atmosferze Klienci moga dokonywac zakup&oacute;w.</p>
<p>&nbsp;</p>
<p>Dzis firma TOMEX plasuje sie w scislej czol&oacute;wce najchetniej odwiedzanych miejsc zakupowych w Malopolsce. &nbsp;Pomimo silnej konkurencji, dzieki systematycznej pracy i wysokiej jakosci oferowanych produkt&oacute;w w niewyg&oacute;rowanych cenach, TOMEX stal sie&nbsp;<strong>ulubionym</strong>&nbsp;<strong>miejscem TRADYCYJNEGO HANDLU</strong>&nbsp;na mapie Krakowa.</p>
<p>&nbsp;</p>
<p>Zapraszamy serdecznie nowych klient&oacute;w oraz kontrahent&oacute;w aby razem z nami budowali przyszlosc naszej wsp&oacute;lnej, codziennej przestrzeni zakup&oacute;w.</p>
