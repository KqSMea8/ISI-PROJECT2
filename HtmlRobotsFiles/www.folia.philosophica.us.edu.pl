<h3>
<a href="https://www.journals.us.edu.pl/index.php/CHOWANNA" rel="bookmark">
Chowanna
</a>
</h3>
<div class="description">
<p>Czasopismo "<strong>Chowanna</strong>" stanowi interdyscyplinarne forum wymiany mysli i prezentacji wnioskow badan naukowych w zakresie wieloaspektowego aktualizowania procesow edukacji, wychowania i innych rodzajow uczestnictwa spolecznego w zmieniajacych sie warunkach spoleczno-kulturowych i cywilizacyjnych.<br>Pismo zostalo powolane do zycia w roku&nbsp;<strong>1929&nbsp;</strong>przez zalozycieli i pracownikow&nbsp;<strong>Instytutu Pedagogicznego w Katowicach</strong>&nbsp;jako biuletyn naukowy. Jego tytul nawiazuje do imienia slowianskiej bogini opiekujacej sie dziecmi, rownoczesnie jednak wskazujac na inspirujaca dla pierwotnej wizji czasopisma role monumentalnego dziela&nbsp;<strong>Bronislawa Ferdynanda Trentowskiego</strong>&nbsp;-&nbsp;<em><strong>Chowanna czyli system pedagogiki narodowej, jako umiejetnosci wychowania, nauki i oswiaty, slowem wyksztalcenia naszej mlodziezy</strong></em>. Rozwazania Trentowskiego mialy charakter interdyscyplinarny, uwzglednialy problematyke filozoficzna, psychologiczna, spoleczna i historyczna, pedagogiczna i kulturowa - profil czasopisma podaza tropem tego dziedzictwa.</p>
