<h3>
<a href="https://www.journals.us.edu.pl/index.php/FL" rel="bookmark">
Forum Lingwistyczne
</a>
</h3>
<div class="description">
<div style="text-align: justify; line-height: 150%;">Pismo koncentruje sie na problemach wspolczesnej lingwistyki. Nadrzednym celem Redakcji jest polaczenie najnowszych trenow wystepujacych w badaniach lingwistycznych z tradycja, dlatego pismo jest tak skonstruowane, aby prezentowac biezace analizy na tle tekstow nieco starszych, istotnych w rozwoju polskiej mysli jezykoznawczej. Teksty te sa tlumaczone na jezyk angielski w celu udostepnienia ich szerszej, miedzynarodowej rzeszy czytelnikow.</div><br />
<div style="text-align: justify; line-height: 150%;">W pierwszych numerach czasopisma byly publikowane glownie teksty polskich lingwistow, prezentujace autorskie spojrzenie na problemy jezykowe lub jezykoznawcze, analizy faktow jezykowych badz kulturowych dokonywane z perspektywy lingwistycznej oraz syntezy dokonan w roznych subdyscyplinach jezykoznawstwa. Material badawczy stanowily najczesciej zjawiska wystepujace w polskiej kulturze, chociaz niektore analizy odnosza sie tez do innych kregow kulturowych i jezykowych (np. j. hebrajskiego). Pismo publikuje rowniez teksty autorow zagranicznych (m. in. z Wloch, Korei Poludniowej, Chorwacji, Niemiec, Wegier, Malty i Stanow Zjednoczonych).<br>Kolejne numery periodyku maja charakter monograficzny.</div>
