</h3>
<div class="description">
<p>Czasopismo naukowe - rocznik internetowy - ,,Zoophilologica. Polish Journal of Animal Studies" ma na celu interdyscyplinarne badania nad znaczeniami, jakie przypisuje sie zwierzetom. Sposob uzgadniania tej wiedzy oraz rekonstrukcja nowoczesnych obszarow wezlowych dla myslenia o swiecie zwierzat i ludzi wiaza sie z potrzeba wytworzenia srodowiska wymiany tresci niemieszczacych sie w ramach jednej dziedziny. Czasopismo to stanowi forum wypowiedzi zarowno dla teoretykow i historykow kultury, jak i specjalistow z zakresu zoologii i historii nauki. Jedna z hipotez zalozycielskich czasopisma bylo przekonanie, ze perspektywa studiow nad zwierzetami (animal studies) pozwala na bezposrednie powiazanie dyskusji swiatopogladowych ze zmianami kulturowymi w relacji ludzie - zwierzeta. Poswiadczaja to juz opracowane badania anglojezyczne, a niniejsze czasopismo postawilo sobie za cel rekonstrukcje relacji ludzi i zwierzat w krajach Europy Srodkowo-Wschodniej oraz innych obszarach kulturowych. Zaproponowana perspektywa ma w kolejnych publikacjach odslonic niedostrzegane dotad przemiany wzorow kultury i moralnosci, ktore sa obecne w najnowszych dyskusjach ekologicznie zorientowanej humanistyki i jej wersji ,,post".</p><br />
<p>E-czasopismo o specjalistycznym charakterze jest poswiecone nie tylko publikowaniu artykulow z zakresu nowej dyscypliny wiedzy - zoofilologii. Powstanie czasopisma wychodzi naprzeciw wspolczesnym oczekiwaniom i wymaganiom, jakie stawia przed przedstawicielami animal studies ,,trzecia kultura", a co za tym idzie - biohumanistyka i ekoposthumanizm. Rocznik wpisuje sie w nurt rozwijajacych sie mlodych interdyscyplinarnych dziedzin spod znaku ,,zoo", a wiec zoopoetyki, zoosemiotyki, zooantropologii, zooteologii, zoomuzykologii, zooetnologii, a nawet zoofarmakognozji oraz wszelkich innych nowych i pokrewnych kierunkow badawczych takich, jak ekokrytyka, ekolingwistyka czy geopoetyka.</p><br />
