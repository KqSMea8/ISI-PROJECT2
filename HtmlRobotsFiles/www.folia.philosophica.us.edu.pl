<h3>
<a href="https://www.journals.us.edu.pl/index.php/FOLIA" rel="bookmark">
Folia Philosophica
</a>
</h3>
<div class="description">
<p>,,Folia Philosophica" jest wydawnictwem ciaglym Instytutu Filozofii Uniwersytetu Slaskiego. Pismo ukazuje sie dwa razy w roku. W ,,Folia Philosophica" zamieszczane sa nie tylko artykuly polskich autorow, ale rowniez przeklady tekstow filozoficznych, a jezeli chodzi o same przeklady, to w pismie publikowane sa zarowno rozprawy klasykow filozofii, jak i wybitnych przedstawicieli filozofii wspolczesnej. Publikujac rozprawy autorow zagranicznych, postawilismy sobie za cel, aby nasze pismo przekroczylo wymiar krajowy i wlaczylo sie do angazujacej rozne srodowiska miedzynarodowej dyskusji nad aktualnymi problemami filozoficznymi. Pismo ma przy tym rowniez dzial recenzji. Od 24. tomu ,,Folia Philosophica" jest w calosci dostepna w formie internetowej i od tego tomu ma Rade Redakcyjna oraz miedzynarodowa Rade Programowa. Od 30. tomu czlonkowie Rady Redakcyjnej sa zarazem redaktorami tematycznymi. Czasopismo posiada redaktorow tematycznych w zakresie ontologii, epistemologii, etyki, estetyki, logiki, filozofii polityki oraz historii filozofii.</p>
