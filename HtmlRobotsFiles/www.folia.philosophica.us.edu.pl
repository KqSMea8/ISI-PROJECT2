Przeklady Literatur Slowianskich
</a>
</h3>
<div class="description">
<p style="text-align: justify;">Czasopismo ,,Przeklady Literatur Slowianskich" zostalo zalozone przez profesor Bozene Tokarzowa w 2009 roku w Zakladzie Teorii Literatury i Translacji Instytutu Filologii Slowianskiej na Wydziale Filologicznym Uniwersytetu Slaskiego (poczatkowo ukazywalo sie jako wydawnictwo ciagle, a w 2012 roku zostalo przeksztalcone w czasopismo). Od poczatku wydawca czasopisma jest Wydawnictwo Uniwersytetu Slaskiego. ,,Przeklady Literatur Slowianskich" poswiecone sa zagadnieniom przekladu artystycznego w ramach jezykow poludniowo- i zachodnioslowianskich, widzianym w perspektywie teoretycznej, historycznoliterackiej i historycznokulturowej. Obejmuje zarowno rejestracje wydan i recepcji przekladow, jak rowniez studia dotyczace:<br>1. specyfiki przekladu w granicach jezykow blisko spokrewnionych,<br>2. przekladu jednej kultury na druga za posrednictwem medium literatury,<br>3. roli przekladu w badaniach komparatystycznych i w studiach nad recepcja literatur slowianskich w swiecie,<br>4. zagadnien podejmowanych w najnowszych badaniach translatologicznych.</p><br />
