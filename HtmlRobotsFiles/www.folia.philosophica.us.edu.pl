Studia Etnologiczne i Antropologiczne
</a>
</h3>
<div class="description">
<p>Studia Etnologiczne i Antropologiczne ukazuja sie od 1997 roku. Czasopismo - wydawane przez Instytut Etnologii i Antropologii Kulturowej w Uniwersytecie Slaskim w Katowicach - jest rocznikiem. Od wrzesnia 2012 roku ,,SEiA" znajduja sie na liscie czasopism punktowanych Ministerstwa Nauki i Szkolnictwa Wyzszego (tzw. <em>czesc B</em> wykazu czasopism naukowych nieposiadajacych wspolczynnika wplywu <em>Impact Faktor (IF))</em>. W marcu 2014 roku ,,Studia Etnologiczne i Antropologiczne" zostaly wpisane do <em>rejestru dziennikow i czasopism </em>Sadu Okregowego w Katowicach.</p><br />
<p>Informacje o wszystkich numerach ,,Studiow Etnologicznych i Antropologicznych"&nbsp; sa&nbsp; dostepne na stronie internetowej czasopisma: <a href="http://www.seia.us.edu.pl">www.seia.us.edu.pl</a>. W przypadku elektronicznej wersji pierwszych dziewieciu tomow mozliwe jest pobranie spisow tresci, wstepu oraz abstraktow opublikowanych w danym numerze artykulow. Poczawszy do tomu 10 mozliwy jest bezplatny dostep do wersji pelnotekstowych kazdego numeru.</p><br />
<p>Celem czasopisma jest publikowanie artykulow i rozpraw naukowych, doniesien z badan oraz recenzji i sprawozdan. Przyjeta formula ,,Studiow Etnologicznych i Antropologicznych" daje etnologom, antropologom kulturowym, folklorystom i wszystkim badaczom kultury mozliwosc dyskusji, wymiany doswiadczen i zapoznawania sie z wynikami prowadzonych badan. Zapraszamy do nadsylania tekstow - zarowno w jezyku polskim, jak rowniez w jezykach kongresowych (angielski, niemiecki, francuski, hiszpanski, rosyjski i wloski).</p>
