<h3>
<a href="https://www.journals.us.edu.pl/index.php/LOGOPEDIASILESIANA" rel="bookmark">
Logopedia Silesiana
</a>
</h3>
<div class="description">
<p>,,Logopedia Silesiana" wlacza sie w obszar periodykow podejmujacych problematyke zwiazana z praktycznymi i teoretycznymi aspektami postepowania logopedycznego z osobami z zaburzeniami mowy i zaburzeniami jezyka. Czasopismo zostalo powolane w 2010r. i w swoich zalozeniach ma byc naukowym forum, ktore bedzie sprzyjalo zarowno rozwijaniu logopedycznej nauki, jak i upowszechnianiu logopedycznej kultury. ,,Logopedia Silesiana" otwiera mozliwosc prezentacji dokonan i przemyslen wszystkim osobom zainteresowanym logopedia jako nauka o biologicznych uwarunkowaniach jezyka i zachowaniach jezykowych oraz opisem relacji miedzy mowa, mysleniem a rzeczywistoscia wyrazana jezykiem. Artykuly ukazuja bowiem interdyscyplinarny charakter&nbsp; postepowania logopedycznego i obejmuja swym obszarem perspektywe medycznego, lingwistycznego, psychologicznego oraz pedagogicznego opisu mowy w rozwoju i w zaburzeniach. W ,,Logopedii Silesianie", od 2012r. prezentowane sa dokonania i przemyslenia wspolczesnych terapeutow mowy i jezyka z kraju i zagranicy. Mamy nadzieje, ze rozwazania teoretyczno-kazuistyczne prezentowane w czasopismie przyczynia sie do doskonalenia terapeutycznego warsztatu i przyczynia sie do rozwoju badan nad poznawczymi mozliwosciami czlowieka oraz jego zdolnosciami interakcyjnymi. Lektura pisma ma bowiem sklaniac do teoretycznych poszukiwan i dociekan wokol zagadnien dotyczacych teorii logopedii oraz prowadzic do odkrywania coraz to doskonalszych praktycznych rozwiazan w zakresie wspierania osob z roznymi rodzajami zaburzen zdolnosci komunikacyjnej.&nbsp;</p>
