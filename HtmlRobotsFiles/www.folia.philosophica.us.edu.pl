Studia Politicae Universitatis Silesiensis
</a>
</h3>
<div class="description">
<p><strong>"Studia Politicae Universitatis Silesiensis"</strong> ISSN 2353-9747 (wersja elektroniczna) to czasopismo naukowe Instytutu Nauk Politycznych i Dziennikarstwa Uniwersytetu Slaskiego w Katowicach. Jest to periodyk o profilu politologicznym, w ktorym szczegolny nacisk polozony jest na polityki publiczne (Evidence Based Policy). Interesuja nas formy i mechanizmy systemowego rozwiazywania kluczowych problemow spolecznych przez panstwo w porozumieniu z reprezentantami roznych grup interesow, szczegolnie na poziomie lokalnym i regionalnym. Redakcja czasopisma przede wszystkim preferowac bedzie artykuly majace wymiar empiryczny. W pierwszym i drugim kwartale roku ukazuja sie tomy w jezyku polskim, a w trzecim i czwartym w pozostalych jezykach. Czasopismo zapewnia natychmiastowy otwarty dostep do jego zawartosci na zasadzie swobodnego dostepu publicznego wspierajacego globalna wymiane wiedzy. Obecnie nalezy do grupy czasopism punktowanych przez Ministerstwo Nauki i Szkolnictwa Wyzszego. Kazdy Autor opublikowanego na lamach ,,Studia Politicae Universitatis Silesiensis" artykulu otrzymuje <strong>20</strong> <strong>punktow</strong>.</p>
