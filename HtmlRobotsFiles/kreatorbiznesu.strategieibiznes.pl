<h3>Odbiorcy plebiscytu</h3>
<div>
<p>Redakcja Strategii i Biznes zamierza skupic wokol plebiscytu "Kreatora Biznesu" srodowisko wyzszej i sredniej kadry zarzadzajacej w firmach tak aby w jak najprostszy i czytelny sposob mogli oni zapoznac sie z nowymi trendami oraz swoimi dzialaniami poprowadzili do szybkiego sukcesu firmy. Otwarta formula plebiscytu stanowi jego glowna zalete.</p>
<h3>Co wyroznia nasz projekt?</h3>
<ul>
<li>Szerokie, przekrojowe dzialania edukacyjne</li>
<li>Nowatorska i interesujaca formula programu</li>
<li>Otwarty, ogolnopolski, plebiscytowy charakter</li>
<li>Wielomilionowy zasieg oddzialywania</li>
<li>Prestizowi i wiarygodni Partnerzy</li>
