<h1 class="entry-title">
<a href="http://fitnesspark.brzozow.pl/?p=81" rel="bookmark">Fitnesspark</a>
</h1>

<div class="entry-meta">
<span class="date"><a href="http://fitnesspark.brzozow.pl/?p=81" title="Bezposredni odnosnik do &#8222;Fitnesspark&#8221;" rel="bookmark"><time class="entry-date" datetime="2014-10-15T12:19:17+00:00">15 pazdziernika 2014</time></a></span><span class="categories-links"><a href="http://fitnesspark.brzozow.pl/?cat=1" rel="category">Bez kategorii</a></span><span class="author vcard"><a class="url fn n" href="http://fitnesspark.brzozow.pl/?author=1" title="Zobacz wszystkie wpisy, ktorych autorem jest Administrator" rel="author">Administrator</a></span>					</div><!-- .entry-meta -->
</header><!-- .entry-header -->

<div class="entry-content">
<p><a href="http://fitnesspark.brzozow.pl/wp-content/uploads/2014/10/logoLGD.jpg"><img class="alignnone size-large wp-image-123" alt="logoLGD" src="http://fitnesspark.brzozow.pl/wp-content/uploads/2014/10/logoLGD-1024x151.jpg" width="604" height="89" srcset="http://fitnesspark.brzozow.pl/wp-content/uploads/2014/10/logoLGD-1024x151.jpg 1024w, http://fitnesspark.brzozow.pl/wp-content/uploads/2014/10/logoLGD-300x44.jpg 300w, http://fitnesspark.brzozow.pl/wp-content/uploads/2014/10/logoLGD.jpg 1346w" sizes="(max-width: 604px) 100vw, 604px" /></a></p>
<p style="text-align: justify;">W ramach dzialania 413 Wdrazanie lokalnych strategii rozwoju dla malych projektow, tj. operacji, ktore nie odpowiadaja warunkom przyznania pomocy w ramach Osi 3 ale przyczyniaja sie do osiagniecia celow tej osi Gmina Brzozow zrealizowala projekt pn.: <i>,,Poprawa oferty spedzania wolnego czasu w Brzozowie poprzez utworzenie fitness parku<br />
i doposazenie placu zabaw"</i>. Miejsce realizacji projektu: Brzozow, ul. Parkowa.</p>
<p>Gmina Brzozow zrealizowala wszystkie zalozone cele projektu tj.</p>
<p>1.  Doposazenie placu zabaw wraz z budowa ogrodzenia</p>
<p style="text-align: justify;">&#8211; zakup i montaz nastepujacych elementow placu zabaw: hustawka wagowa, bujaki sprezynowe - 2 szt., lawka piknikowa, piaskownica szesciokatna, kosz na odpadki - 2 szt., lawki bez oparcia 6 szt., karuzela metalowa, hustawka podwojna (wiszaca),</p>
<p>&#8211; budowa ogrodzenia placu zabaw z furtka i brama wejsciowa</p>
<p>2. Dostawa i montaz lawek parkowych - 12 szt</p>
<p>3. Utworzenie fitness parku (silownia zewnetrzna)</p>
<p style="text-align: justify;">&#8211; zakup i montaz nastepujacych elementow silowni zewnetrznej: wyciskanie siedzac podwojne, wyciag gorny podwojny, wahadlo podwojne, biegacz podwojny, prasa nozna podwojna, porecze podwojne, narciarz, rowerek rehabilitacyjny, steper i wahadlo</p>
<p style="text-align: justify;">4. Zorganizowanie imprezy polaczonej ze szkoleniem instruktazowym z korzystania z fitness parku oraz pokazu sztuk walki i samoobrony</p>
<p style="text-align: justify;">Wartosc calkowita projektu wyniosla 79 167,72 zl, koszty kwalifikowalne wyniosly 64 364,00 zl, dofinansowanie 50 000,00 zl.</p>
<p style="text-align: justify;"> Inicjatywa stworzenia w Brzozowie Outdoor Fitness powstala na stronie internetowej <a href="http://www.petycjeonline.com/siownia_outdoorowa_dla_miasta_brzozow">http://www.petycjeonline.com/siownia_outdoorowa_dla_miasta_brzozow</a></p>
<p style="text-align: justify;">W ramach projektu utworzono obecna strone internetowa, ktora prezentuje walory nowopowstalej silowni, promujac w ten sposob projekt wsrod potencjalnych uzytkownikow nie tylko z obszaru Lokalnej Grupy Dzialania ,,Ziemia Brzozowska", ale takze terenow sasiednich, ktore sa pozbawione tego typu atrakcji.<strong></strong></p>
<p><iframe src="//www.youtube.com/embed/eIns-kXNgh4" height="315" width="600" allowfullscreen="" frameborder="0"></iframe></p>
<p style="text-align: justify;"><b>Silownia zewnetrzna</b> - wydzielone, ogolnodostepne miejsce (czesto ogrodzone), wyposazone w zespol urzadzen rekreacyjnych zainstalowanych na stale na lub za pomoca ktorych uzytkownicy bez nadzoru i bez pomocy z zewnatrz, moga cwiczyc w celu utrzymania lub poprawy ich zdolnosci fizycznych i intelektualnych.</p>
<p><b>Zalety zewnetrznych silowni:</b></p>
<ol>
<li>Jest dostepna dla kazdego (dzieci i ich rodzicow, mlodziezy, osob<br />
starszych, osob niepelnosprawnych).</li>
<li>Wzmacnia kondycje fizyczna i koordynacje.</li>
<li>Poprawia nastroj.</li>
<li>Wycisza wewnetrznie poprzez aktywnosc na swiezym powietrzu.</li>
<li>Ulatwia nawiazywanie i utrwala nie kontaktow miedzyludzkich.</li>
<li>Wplywa pozytywnie na poprawe zdrowia.</li>
<li> Przy odpowiednim doborze cwiczen dziala rehabilitacyjne.</li>
<li>Darmowa rozrywka</li>
</ol>
<p>&nbsp;</p>
