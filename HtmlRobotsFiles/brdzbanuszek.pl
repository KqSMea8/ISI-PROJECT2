<div class="col-md-8 col-xs-12">
<h1 class="main-text" aos="fade-left"><br>O nas</h1>
<div class="row">
<div class="col-sm-6 col-xs-12" aos="fade-left">
<p class="text-justify margTop">
Biuro Rachunkowe Boguslawy Dzbanuszek jest firma dzialajaca na rynku od 01.02.1994 roku.
Od poczatku dzialalnosci kladziemy nacisk na poszerzanie wiedzy w zakresie ekonomii, finansow, rachunkowosci i zarzadzania. Zdobywane doswiadczenie popieramy szkoleniami, seminariami i wykladami, co pozwala stale podnosic poziom swiadczonych uslug. Pracownicy na biezaco sledza nowosci z zakresu prawa pracowniczego, finansow i rachunkowosci dzieki czemu mozemy natychmiast informowac swych klientow o postepujacych zmianach i sluzyc fachowa porada. Wyreczamy tez ich w kontaktach z urzedami: Skarbowym, ZUS i GUS.
Przechowywane przez nas dane klientow, zarowno te w formie elektronicznej jak i tradycyjnej, papierowej sa chronione przed utrata i dostepem osob trzecich poprzez odpowiednie procedury archiwizacyjne oraz szyfrowanie polaczen sieciowych i inne zabezpieczenia elektroniczne.
Lata doswiadczen nauczyly nas odpowiedzialnosci i rzetelnosci w dbaniu o interesy naszych klientow.
</p>
</div>
<div class="col-sm-6 col-xs-12" aos="fade-left">
<p class="text-justify margTop">
Firma zatrudnia ludzi mlodych, ambitnych, ale stawia rowniez na profesjonalizm poparty doswiadczeniem. Posiadanie w pelni wyposazonego zaplecza technicznego i informatycznego, pozwala na terminowe i profesjonalne wykonywanie uslug. Biuro Rachunkowe Boguslawy Dzbanuszek uprawnione jest do wykonywania uslug doradztwa podatkowego oraz uslugowego prowadzenia ksiag handlowych.
Wlascicielka biura Boguslawa Dzbanuszek posiada wydane przez Ministerstwo Finansow dnia 03.12.1997 swiadectwo kwalifikacji do uslugowego prowadzenia ksiag handlowych nr 680 oraz certyfikat doradcy podatkowego z dnia 27.11.2002r. Nr 04176.
Ksiegowosc prowadzimy komputerowo za pomoca nowoczesnego oprogramowania Symfonia Finanse i Ksiegowosc, Symfonia Kadry i Place, Symfonia Handel, Symfonia Mala Ksiegowosc. Oprogramowanie to jest zgodne z zaleceniami Ministerstwa Finansow.
Posiadamy rowniez polise ubezpieczenia odpowiedzialnosci cywilnej. Misja naszej firmy jest uwolnienie naszych klientow od przykrego obowiazku sledzenia przepisow podatkowych oraz uciazliwego ewidencjonowania zdarzen gospodarczych zachodzacych w ich firmie.
</p>
