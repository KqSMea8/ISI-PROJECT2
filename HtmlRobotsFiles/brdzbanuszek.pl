<section class="section-more">
<div class="container">
<div class="row" aos="fade-right">
<div class="col-xs-12">
<p>
<h4 class="text-uppercase"><b>Szczegoly oferty:</b></h4>
</p>
</div>
</div>
<div class="row margTop">
<div class="col-md-6 col-sm-6 col-xs-12" aos="fade-up">
<p>
<b>Jak wyglada procedura rozpoczecia wspolpracy?</b><br>
Jezeli jestes zainteresowany podjeciem wspolpracy z naszym biurem rachunkowym prosimy o kontakt telefoniczny lub mailowy w celu podania danych niezbednych do podpisania umowy na prowadzenie uslug ksiegowych. Podpisanie umowy oraz upowaznien moze nastapic w siedzibie Biura Rachunkowego  lub w siedzibie zleceniodawcy. Biuro Rachunkowe sklada wszystkie niezbedne dokumenty w odpowiednim dla Panstwa firmy Urzedzie Skarbowym oraz Zakladzie Ubezpieczen Spolecznych.
Jezeli zamierzaja Panstwo dopiero zalozyc dzialalnosc gospodarcza prosimy o kontakt telefoniczny lub mailowy w celu umowienia sie w siedzibie biura rachunkowego na wypelnienie niezbednych formularzy podatkowych oraz wyboru najlepszej formy opodatkowania dla Panstwa nowej firmy.

</p>
</div>

<div class="col-md-6 col-sm-6 col-xs-12" aos="fade-up" aos-delay="200">
<p>
<b>Jak wyglada comiesieczna wspolpraca?</b><br>
Po podpisaniu umowy o swiadczenie uslug ksiegowych zglaszaja Panstwo telefonicznie lub mailowo gotowosc do przekazania miesiecznych dowodow ksiegowych (do 15 dnia kazdego miesiaca rozliczeniowego).
Dokumenty do biura rachunkowego mozna dostarczyc osobiscie w godzinach 8.00 - 16.00 lub skorzystac z mozliwosci odbioru dokumentow przez naszego pracownika w siedzibie Panstwa firmy.
Praktykujemy takze dostarczanie dokumentow za posrednictwem poczty lub firmy kurierskiej, a bezposrednie rozmowy prowadzimy za pomoca telefonu lub komunikatorow internetowych.
Pozwala to na znakomite obnizenie kosztow przejazdow i oszczednosc czasu. Po sporzadzeniu deklaracji rozliczeniowych zostaja Panstwo poinformowani o wyniku finansowym, oraz o
mozliwosci odebrania przelewow do US i ZUS lub zostana one Panstwu przeslane w formie papierowej lub elektronicznej. Oszczedzamy Wasz czas!

</p>

</div>

<div class="col-md-6 col-sm-6 col-xs-12" aos="fade-up" aos-delay="400">
<b>Gdzie przechowywana jest dokumentacja ksiegowa?</b><br>
Dokumenty ksiegowe podatnika przechowywane i archiwizowane sa w siedzibie biura rachunkowego.
Jezeli zachodzi potrzeba przechowywania dokumentacji w siedzibie platnika, dokumenty ksiegowe wydawane sa do biura rachunkowego tylko na czas sporzadzania dokumentacji i deklaracji podatkowej.
Po sporzadzeniu deklaracji dokumenty ksiegowe sa przekazywane do klienta.Po zakonczeniu roku podatkowego i sporzadzeniu rozliczenia rocznego, dokumentacja zostaje zwrocona podatnikowi.
</div>

<div class="col-md-6 col-sm-6 col-xs-12" aos="fade-up" aos-delay="600">
<b>Inne korzysci wspolpracy</b><br>
W razie kontroli skarbowej podmiotu obslugiwanego przez biuro rachunkowe, nasz przedstawiciel reprezentuje platnika przed Urzedem Skarbowym oraz Zakladem Ubezpieczen Spolecznych.
Zleceniodawca informowany jest o zakonczeniu kontroli i postanowieniach Urzedu Kontroli Skarbowej.
</div>

