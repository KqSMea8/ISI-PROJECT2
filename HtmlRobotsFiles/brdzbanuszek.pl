</div>
</Div>
<div class="row margTop2">
<div class="col-md-12" aos="fade-up">
<p class="text-justify">
Szybkosc zmian prawa podatkowego oraz prawa pracy zmusza do coraz uwazniejszego zaglebiania sie w tresci ustaw, a co za tym idzie jest to zajecie bardzo czasochlonne. Przekazujac prowadzenie ksiegowosci do Biura Rachunkowego minimalizuje sie czas poswiecony na prowadzenie rozliczen z Urzedem Skarbowym oraz Zakladem Ubezpieczen Spolecznych. Wyklucza sie rowniez ryzyko zwiazane z blednym prowadzeniem ksiegowosci oraz zwiazane z tym faktem nieprzewidziane koszty. Jako biuro rachunkowe bedziemy na biezaco informowac o stanie finansowym Panstwa firmy oraz przekazywac niezbedne informacje o zmianach w prawie podatkowym i prawie pracy. Wszystkie uslugi oferowane przez nasza firme sa objete ubezpieczeniem od Odpowiedzialnosci Cywilnej.
