<blockquote>
<p>Gdy bowiem traktatem dodatkowym w Wiedniu z d. 3. Maja 1815 r. zawarowanym zostal byt Wolnego Miasta Krakowa,
grono owczesnych profesorow uznalo potrzebe zawiazania Towarzystwa naukowego, na wzor istniejacego juz dawniej Towarzystwa
przyjaciol nauk w Warszawie. Projekt w tej mysli wniesiony na Rade rektorska w d. 24 Lipca 1815 r. stal sie pierwsza
ustawa Towarzystwa, scisle wowczas zlaczonego z Uniwersytetem. Otwarcie czynnosci nastapilo w d. 25 Lutego 1816 r. przez
odbyte w tym celu posiedzenie publiczne.</p>

