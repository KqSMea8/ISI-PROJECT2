<div id="naglowek"></div><p><strong>Sprezone powietrze jest jednym z kluczowych nosnik&oacute;w energii w wielu galeziach przemyslu. Dlatego urzadzenia do wytwarzania i uzdatniania powietrza sa niezwykle waznym elementem w gospodarce energetycznej kazdego przedsiebiorstwa niezaleznie od jego wielkosci i stopnia automatyzacji.</strong></p>
<p>Dominacja sprezarek tlokowych nalezy do przeszlosci. Sprezarki srubowe przejely wiodaca role w<br />aplikacjach od 5,5 kW do 250 kW mocy silnika. Sprezarki srubowe pomimo stosunkowo mlodego wieku przechodza stale przeobrazenia i ulegaja procesom modernizacji. Jedna z nowelizacji, kt&oacute;re zadomowily sie na dobre na rynku jest plynna regulacja obrot&oacute;w stosowana w wypadku kiedy zapotzebowanie na sprezone powietrze waha sie w znacznych granicach, a sterowniki nadrzedne polepszaja stopien wykorzystania wiekszej ilosci sprezarek pracujacych w jednym systemie.</p>
<p>Powietrze atmosferyczne zasysane przez sprezarke zawiera zanieczyszczenia stale oraz wode. Sama sprezarka r&oacute;wniez stanowi zr&oacute;dlo zanieczyszczen (olej, produkty zuzycia). Zatem po sprezeniu do<br />instalacji przeplywa powietrze zawierajace pare wodna, pyly, pary oleju i niewielkie ilosci agresywnych gaz&oacute;w. Dlatego tez bardzo wazne jest stosowanie urzadzen uzdatniania powietrza po jego sprezeniu.</p>		</div>

