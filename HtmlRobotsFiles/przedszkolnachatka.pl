</b></div>
<div align="center"><span style="color: rgb(255, 0, 0);"><span style="font-size: medium;"><b>DRODZY RODZICE!</b></span></span><b><br />
<br />
</b></div>
<div style="text-align: justify;"><span style="color: rgb(255, 0, 0);"><span style="font-size: larger;">Chcemy poinformowac, ze prowadzimy nab&oacute;r na rok szkolny 2018/2019, jak r&oacute;wniez na rok 2019/2020. Przyjmujemy dzieci juz od 2,5 roku do 6 lat. Wszystkich zainteresowanych rodzic&oacute;w zapraszamy do pobierania oraz skladania</span></span></div>
<div align="center">&nbsp;</div>
<div align="center"><a href="http://www.przedszkolnachatka.pl/rekrutacja_dla-rodzicow_przedszkole" target="_self"><span style="color: rgb(255, 0, 0);"><span style="font-size: medium;">KART ZGLOSZENIOWYCH</span></span></a></div>
<div align="center">&nbsp;&nbsp;</div>
<div style="text-align: justify;"><span style="color: rgb(0, 0, 0);"><span style="font-size: larger;">Przedszkole Niepubliczne &bdquo;Przedszkolna Chatka&rdquo; o profilu teatralno-muzycznym powstalo w 2008 roku. Poczatkowo byl to punkt przedszkolny, jednak w zwiazku z duzym zainteresowaniem Rodzic&oacute;w, w styczniu 2011 roku przeksztalcil sie on w przedszkole niepubliczne wpisane do ewidencji szk&oacute;l i plac&oacute;wek niepublicznych Miasta Bydgoszczy pod numerem 31/2010.</span></span></div>
<div style="text-align: justify;"><span style="color: rgb(0, 0, 0);"><span style="font-size: larger;">Organem prowadzacym przedszkole jest Fundacja &bdquo;Nowe Pokolenie&rdquo;, kt&oacute;ra jest organizacja pozytku publicznego czynnie wspierajaca dzieci i mlodziez w zakresie rozwijania i promocji talent&oacute;w artystycznych. Swoja dzialalnosc realizuje nie tylko poprzez prowadzenie przedszkola, ale przede wszystkim przez szeroko realizowana dzialalnosc charytatywna. Fundacja wsp&oacute;lpracuje z wiekszoscia plac&oacute;wek &ndash; opiekunczo wychowawczych w calej Polsce, w tym przede wszystkim z panstwowymi i rodzinnymi domami dziecka. Wsp&oacute;lpraca ta polega na realizacji projekt&oacute;w dla mlodych ludzi ze srodowisk dysfunkcyjnych, kt&oacute;re maja na celu wyr&oacute;wnanie szans w dostepie do edukacji kulturalnej oraz wskazywania alternatywnych sposob&oacute;w spedzania czasu wolnego poprzez rozw&oacute;j umiejetnosci i talent&oacute;w artystycznych. Przy realizacji og&oacute;lnopolskich projekt&oacute;w Fundacja wsp&oacute;lpracuje, ze wszystkimi instytucjami kulturalnymi z naszego miasta i nie tylko a takze z uczelniami wyzszymi, organizacjami pozarzadowymi i firmami.</span></span></div>
<div style="text-align: justify;"><span style="color: rgb(0, 0, 0);"><span style="font-size: larger;">&bdquo;Przedszkolna Chatka&rdquo; jest przedszkolem, kt&oacute;re intensywnie sie rozwija. W 2013 funkcjonowaly dwie grupy przedszkolne, do roku 2015/2016 - cztery, obecnie wychowanie przedszkolne obejmuje 5 grup.</span></span></div>
<div style="text-align: justify;"><span style="color: rgb(0, 0, 0);"><span style="font-size: larger;">Nasze przedszkole jest miejscem przyjaznym malemu czlowiekowi. Przygoda z &bdquo;Przedszkolna Chatka&rdquo; rozpoczyna sie od zajec adaptacyjnych oraz letnich p&oacute;lkolonii, kt&oacute;re wprowadzaja dziecko w nasz muzyczno-teatralny swiat.</span></span></div>
<div style="text-align: justify;"><span style="color: rgb(0, 0, 0);"><span style="font-size: larger;">&nbsp;&bdquo;Przedszkolna Chatka&rdquo; to nie tylko muzyka i teatr, to r&oacute;wniez taniec, plastyka, eksperymenty, rytmika i wiele innych. Nasze dzieci sa otwarte na nowe doswiadczenia, chca odkrywac swiat wszystkimi zmyslami. We wszystkim, co proponujemy nie brakuje zabawy, kt&oacute;ra jest przeciez podstawowa forma aktywnosci malego dziecka.</span></span></div>
<div style="text-align: justify;"><span style="color: rgb(0, 0, 0);"><span style="font-size: larger;">&bdquo;Przedszkolna Chatka&rdquo; nie jest miejscem tylko dla dzieci, jest to takze miejsce zawsze otwarte dla Rodzic&oacute;w. Dazymy do sp&oacute;jnej i harmonijnej wsp&oacute;lpracy. Oferujemy szeroki wachlarz aktywnosci dla dzieci z rodzicami&hellip; sa to warsztaty, uroczystosci, bale, wycieczki, imprezy... Sa to niezwykle i niezapomniane formy laczace pokolenia poprzez edukacje i zabawe. </span></span></div>
<div style="text-align: justify;"><span style="color: rgb(0, 0, 0);"><span style="font-size: larger;">W roku szkolnym 2016/2017 nasze przedszkolaki wziely udzial w wielu wydarzeniach r&oacute;znego typu: imprezy organizowane w przedszkolu, w tym: uroczystosci i swieta okolicznosciowe, przedstawienia teatralne w przedszkolu i na zewnatrz, warsztaty dla dzieci z rodzicami, koncerty muzyczne w przedszkolu i Filharmonii Pomorskiej,&nbsp; zajecia otwarte dla rodzic&oacute;w i zajecia otwarte w szkolach podstawowych, wycieczki, wystepy publiczne, zajecia i spotkania tematyczne realizowane w ramach roznych akcji...</span></span></div>
<div style="text-align: justify;"><span style="color: rgb(0, 0, 0);"><span style="font-size: larger;">W roku szkolnym 2016/2017 nasze przedszkole &nbsp;i przedszkolaki zdobyli w sumie 39 wyr&oacute;znien, nagr&oacute;d, certyfikat&oacute;w i podziekowan.</span></span></div>
<div style="text-align: justify;"><span style="color: rgb(0, 0, 0);"><span style="font-size: larger;">Dziecko przebywajac w naszym przedszkolu jest&hellip; szczesliwe, radosne, optymistycznie patrzace na swiat, otwarte na otaczajaca je rzeczywistosc, kreatywne, tw&oacute;rcze, kulturalne, tolerancyjne&hellip; Usmiechy naszych przedszkolak&oacute;w swiadcza o tym, ze &bdquo;Przedszkolna Chatka&rdquo; spelnia ich dzieciece potrzeby, marzenia i oczekiwania&hellip;</span></span></div>
<div align="center">&nbsp;</div>
<div align="center">&nbsp;</div>
<div align="center"><span style="color: rgb(0, 0, 0);"><span style="font-size: medium;"><b>NASZE PRIORYTETOWE DZIALANIE&hellip;</b></span></span><b><br />
<br />
</b></div>
<div style="text-align: justify;"><span style="color: rgb(0, 0, 0);"><span style="font-size: larger;">Stwarzanie dzieciom warunk&oacute;w do wszechstronnego rozwoju talent&oacute;w i zdolnosci poprzez rozbudzanie zainteresowan, ciekawosci oraz zaspokajanie naturalnej potrzeby poznawania swiata.</span></span></div>
