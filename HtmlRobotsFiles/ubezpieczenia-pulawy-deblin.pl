<h2><b>O nas</b></h2>
<div class="main">
<div>
<img alt="Agencja Ubezpieczeniowa Kazimierz Brzuska" src="images/img2.jpg" />
<div class="text">
<p>
Dzialalnosc prowadzimy od 1991 roku. Jestesmy tzw. multiagencja ubezpieczeniowa co oznacza, ze wspol-<br />
pracujemy z kilkoma towarzystwami ubezpieczen.</p>
<p>
Od roku 2000 reprezentujemy niezmiennie 7 tych samych ubezpieczycieli co swiadczy o&nbsp;stabilnosci naszej dzialalnosci.<br />
Dzieki specjalizacji zawezonej wylacznie do problematyki ubezpieczeniowej zapewniamy profesjonalna obsluge.</p>
<p>
Posiadamy 2 biura: w Pulawach<br />
i w Deblinie, w ktorych pracownicy z&nbsp;wieloletnim stazem staraja sie dopasowac ochrone ubezpieczeniowa do potrzeb kazdego klienta.</p>
