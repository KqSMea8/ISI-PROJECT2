<p><strong><span style="color: #ff0000;"><em>&#8222;&#8230; </em><em>Nie odchodz za daleko, potrzebuje Twojej pomocy&#8230;&#8221;</em></span></strong></p>
<p>Macius jest slicznym i pelnym energii chlopczykiem. Niestety <span style="text-decoration: underline;">urodzil sie glebokim obustronnym niedosluchem czuciowo-nerwowym, ma zdiagnozowane maloglowie, niskoroslosc, wade wymowy, jaskre mlodziencza obu oczna, migrene, zaburzenia SI,  autyzm, cechy dysformiczne twarzoczaszki, cierpi na mieszane bole glowy ze wspolistnieniem komponentow naczynioruchowych i napieciowych, skolioze, asymetrie lopatek, napiecie miesni prawego boku, zaburzenia aktywnosci i uwagi, zaburzenia zachowania, klopoty z dostosowaniem spolecznym, szmery nad sercem </span><strong>. </strong>Forsowna, wieloletnia rehabilitacja, w ktorej mama Maciusia bierze czynny udzial niesie coraz wieksza nadzieje na mowe werbalna dziecka i na jego prawidlowy rozwoj. Wciaz sa potrzebne pomoce edukacyjno-dydaktyczne, materialy logopedyczne i glottodydaktyczne, gry komputerowe, zabawki interaktywne, ksiazki wiele innych drogich materialow, ktore nie sa dostepne w powszechnej sprzedazy. Samotnej mamy Maciusia nie stac na duze wydatki poniewaz poswieca wszystkie dostepne pieniadze na koszty leczenia. Stad apel o pomoc. W walce o zdrowie Macka kazdy grosz sie liczy. Linki do przykladowych pomocy edukacyjno &#8211; dydaktycznych sa podane w dziale Bardzo Pilne. Poza tym trwa nieustajaca aukcja zbierania pieniedzy na turnusy rehabilitacyjne, aparaty sluchowe,  roznego typu urzadzenia wzmacniajace sluch dziecka oraz implanty, jak tez na dokladna diagnostyke dziecka . Lista wydatkow jest dluga ale leczenie zaczyna przynosic wymierne efekty. Pomozcie Mackowi uslyszec swiat &#8211; swiergot ptakow, padajacy deszcz, szum drzew&#8230;Pomozcie mu cieszyc sie zyciem&#8230;</p>
