<p>Kazdemu kierowcy moze zdarzyc sie sytuacja, kiedy zatrzymuje go na drodze samochod policyjny. Kiedy mamy sobie cos do zarzucenia - wiemy, dlaczego jestesmy zatrzymywani. Jednak jesli jestesmy pewni, ze nie naruszylismy przepisow i w zasadzie nie mamy zadnego pomyslu, czego policja moze od nas chciec, warto przyjrzec sie uwaznie zatrzymujacemu nas pojazdowi, a szczegolnie jego numerom ,,taktycznym" tj. widniejacym na bokach i na dachu pojazdu. Warto wiedziec, ze numer taktyczny jest uzalezniony od obszaru dzialania jednostek policyjnych, a wiec, jezeli np. jestesmy akurat w okolicach Gdanska i zatrzymuje nas samochod policyjny oznaczony literami HPP (Wojewodztwo Slaskie), nalezy zachowac szczegolna ostroznosc, a najlepiej nie zatrzymywac sie, tylko udac sie bezposrednio na najblizsza Komende Policji w celu wyjasnienia zaistnialej sytuacji, poniewaz taki pojazd policyjny moze okazac sie pulapka i proba kradziezy naszego ladunku (lub samochodu).
</p>
<p>
Tabela Numerow Taktycznych Pojazdow Policyjnych dostepna w dziale <a href="http://www.bezpiecznytir.pl/kierowcy.php">Kierowcy</a>
</p>

