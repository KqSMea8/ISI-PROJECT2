<h2>O FIRMIE</h2>



<p style="text-align: center">&nbsp;</p>
<p style="text-align: justify">Dzialalnosc biznesowa rozpoczelismy w 1990 roku w Niemczech. Obecnie, w Polsce, dzialamy poprzez siedzibe zlokalizowana w Belchatowie.</p>
<p style="text-align: justify">Nasza dzialalnosc opiera sie o trzy gl&oacute;wne elementy: dystrybucje tapet o bogatym wzornictwie i zr&oacute;znicowanym przeznaczeniu, poszukiwanie nowych trend&oacute;w dla autorskich kolekcji tapet oraz produkcje profesjonalnej jakosci klej&oacute;w oraz srodk&oacute;w chemicznych przeznaczonych do przygotowania powierzchni przed tapetowaniem lub malowaniem. O wysokim standardzie naszych wyrob&oacute;w swiadczy fakt, ze czesc z nich jest sprzedawana pod marka wlasna najbardziej liczacych sie fabryk tapet. Szeroki zakres tej oferty jest dowodem naszego profesjonalizmu i kompleksowego podejscia do tematu.</p>
