<h2>O firmie</h2>
<p><p>Firma DECODACH powstala juz w roku 1997r. Na polskim rynku zarejestrowana zostala w maju 2000 roku jako sp&oacute;lka cywilna a od roku 2002 juz jako sp&oacute;lka jawna. <br /><br /><strong>Doswiadczenie</strong> zdobywalismy przez 8 lat poczatkowo na rynku zachodnim<br />przy wsp&oacute;lpracy niemieckich firm. W oparciu o posiadana wiedze i umiejetnosci naszym zamiarem bylo stworzenie firmy budowlanej, kt&oacute;ra bedzie sie wyr&oacute;zniala sposr&oacute;d podobnych firm w Polsce. <br /><br />Technologie i procesy, kt&oacute;re stosujemy okazaly sie trafne i spowodowaly,<br />ze podnieslismy dzieki temu poprzeczke dla innych firm, a tym samym stalismy sie konkurencyjni pod wzgledem jakosci wykonywanych prac a takze cen. <br /><br />Uslugi, kt&oacute;re oferujemy, poczatkowo odbierane jako nowosci <br />z niedowierzaniem, spelnily oczekiwania najbardziej wymagajacych klient&oacute;w. Zadowolenie inwestora daje nam duzo nieukrywanej satysfakcji, a jednoczenie mozliwosc rozwoju i rozbudowy firmy. <br /><br />Firma oferuje zakres uslug og&oacute;lnobudowlanych: <br />- murarstwo &nbsp;<br />- ciesielstwo <br />- mury pruskie <br />- dekarstwo <br />- blacharstwo <br />- adaptacja poddaszy</p>
