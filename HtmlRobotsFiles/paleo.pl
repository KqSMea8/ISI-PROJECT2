<h1>Paleo.pl - wczasy nad polskim morzem</h1>
<p>
Witamy Panstwa na stronach osrodka wypoczynkowego Paleo we Wladyslawowie.
</p>
<p>
Nadmorska lokalizacja - u nasady polwyspu helskiego, 34 km od Helu i tyle samo od Gdyni stanowi wyjatkowa lokalizacje, a piekne i urozmaicone krajobrazy stanowia klimat dobrego wypoczynku.
</p>
<p>
Piasczyste i szerokie plaze nad Baltykiem, rzesze turystow latem nadaja niepowtarzalny charakter Wladyslawowa.
</p>
<p>
"Paleo" znajduje sie po srodku tego miasteczka w czesci pensjonatowo - mieszkalnej.
</p>

