<p>W budynku CUBE na nasze&nbsp;zwierzaki, czeka pani doktor w &nbsp;przychodni weterynaryjnej iVET. Znajduja sie tu takze: drogeria Rossmann, salon RTV, AGD, IT Kotlowski Max Elektro Prime Store, pralnia 5asec, optyk Optyk Okulista Signum, salon odziezy dla dzieci i mlodziezy Guapo-Guapa, piekarnia&ndash;cukiernia Wygorzele, garmazernia Art Menu, ekodelikatesy Natura i Zdrowie i salon alkoholi M&amp;P Alkohole i Wina Swiata.</p>

