<h1>Rockstar</h1>
<span class="bottomBar_1">Wprowadzilismy Rockstar na&nbsp;polski rynek </span>

<div class="colIn_50 paddingRight_20"><p>

<strong>Wyzwanie:</strong> Wprowadzenie marki Rockstar na&nbsp;polski rynek, na&nbsp;ktorym do&nbsp;2013 roku byla zupelnie nieznana. Znalezienie platformy komunikacji i&nbsp;zaistnienie w&nbsp;swiadomosci mlodych konsumentow na&nbsp;bardzo konkurencyjnym rynku energetykow.<br>
<br>
<strong>Realizacja:</strong> Od momentu pojawienia sie marki Rockstar na&nbsp;polskim rynku bralismy udzial w&nbsp;procesie jej pozycjonowania. Stworzylismy unikalna platforme, ktorej celem bylo dotarcie do&nbsp;mlodych ludzi. W&nbsp;pierwszej kolejnosci zapytalismy konsumentow, co&nbsp;ich interesuje i&nbsp;co&nbsp;chcieliby robic. W&nbsp;5 wybranych w&nbsp;glosowaniu miastach zrealizowalismy eventy pod haslem <strong>,,Zmaluj cos"</strong>. Zorganizowalismy wydarzenia skladajace sie z&nbsp;elementow wybranych przez ludzi, dajac im&nbsp;poczucie przynaleznosci i&nbsp;zaangazowalismy ich w&nbsp;tworzenie kampanii. Razem z&nbsp;nimi zmalowalismy niezla impreze! <br>
<br>
Idac za&nbsp;ciosem, w&nbsp;kolejnym roku wspolpracy postanowilismy <strong>,,Poruszyc miasto"</strong> i&nbsp;zachecic mlodych ludzi do&nbsp;ciekawego spedzenia wolnego czasu. Tym razem bylismy tam, gdzie odbiorcy naturalnie spedzaja swoj czas. Zorganizowalismy serie eventow, z&nbsp;ktorych najbardziej nietypowym byl ten w&nbsp;warszawskim Centrum Handlowym Arkadia, na&nbsp;te&nbsp;okazje zmienionym w&nbsp;wielki tor przeszkod dla&nbsp;zawodnikow BMX i&nbsp;MTB. Rozegraly sie tam widowiskowe zawody downhill po alejkach i&nbsp;schodach ruchomych oraz best trick contest. Dodatkowo, w&nbsp;galerii ustawilismy specjalna maszyne podlaczona do&nbsp;stacjonarnych rowerow i&nbsp;ekranow z&nbsp;aplikacja
</p>

</div>

<div class="colIn_50  paddingLeft_20"><p>
symulujaca wyscig na&nbsp;rowerze, ktora wydawala napoje Rockstar za&nbsp;pokonanie interaktywnego rajdu.<br/>
<br/>
W ostatnim roku dzialan rozszerzylismy zasieg naszej platformy komunikacji o&nbsp;4&nbsp;kolejne europejskie rynki. Zamiast inwestowac w kampanie TV, zorganizowalismy emocjonujaca miedzynarodowa impreze ,,Flying Rockstar Energy Tour". Tym razem udalo nam sie zrobic jeszcze wiecej halasu niz poprzednio, dzieki zaangazowaniu w&nbsp;eventy najlepszych zawodnikow freestyle motocross z&nbsp;calego swiata, ktorzy wykonali spektakularne akrobacje na&nbsp;motocyklach do&nbsp;muzyki rockowej granej na&nbsp;zywo. Eventy zostaly zaplanowane w&nbsp;najbardziej uczeszczanych miejscach kazdego z&nbsp;miast, np.&nbsp;przy Palacu Kultury i&nbsp;Nauki w Warszawie czy pod&nbsp;Palacem Parlamentu w&nbsp;Bukareszcie.<br>
<br>
<strong>Efekt:</strong> Wprowadzilismy marke Rockstar do&nbsp;Polski i&nbsp;stworzylismy zintegrowana platforme na&nbsp;kilku rynkach europejskich.<br><br>
- W niecale 3&nbsp;lata osiagnelismy ponad <strong>85%</strong> wzrost spontanicznej swiadomosci marki w&nbsp;Polsce.</strong><br><br>
- Wskaznik ,,rozwazylbym zakup tej marki" wzrosl o&nbsp;<strong>73%</strong>. <br><br>
- Na nasz event w Warszawie pod&nbsp;Palacem Kultury przyszlo <strong>10 000 osob</strong>, a&nbsp;viral z&nbsp;wydarzenia obejrzalo az&nbsp;<strong>1 000 000 osob</strong>.    <br><br>
- 78 000 osob obejrzalo teaser eventu w Arkadii, a&nbsp;<strong>100 000</strong> osob wzielo w&nbsp;nim udzial. <br><br>

</p></div>

