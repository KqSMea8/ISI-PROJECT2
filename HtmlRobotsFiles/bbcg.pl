<br>
<strong>Rozwiazanie:</strong> Wyszlismy z&nbsp;zalozenia, ze&nbsp;tradycyjne dzialania marketingowe nie&nbsp;wystarcza, by&nbsp;dotrzec do&nbsp;mieszkancow miasta i&nbsp;pokazac im&nbsp;wszystkie dobre strony IKEA. Zamiast inwestowac w&nbsp;tradycyjna kampanie w&nbsp;TV, zaprojektowalismy wiec dzialania niestandardowe, ktorym przyswiecala idea wprowadzania sie&nbsp;nowego sasiada. Byla nim oczywiscie IKEA, a&nbsp;jej przyszlym domem - Lublin. Nasza kampanie podzielilismy na&nbsp;etapy, a&nbsp;kazdy realizowalismy w&nbsp;duchu dobrych, sasiedzkich zwyczajow. <br>
<br>
Wspolnie z&nbsp;IKEA zdecydowalismy sie tez na&nbsp;stworzenie innowacyjnego kanalu komunikacji - miejsca doswiadczenia marki, ktore stalo sie jednoczesnie elementem spajajacym wszystkie inne dzialania w&nbsp;ramach kampanii. Tak powstala ,,IKEA Po Sasiedzku". Domowa przestrzen w&nbsp;centrum Lublina, w&nbsp;ktorej IKEA dzieli sie z&nbsp;mieszkancami tym, co&nbsp;najlepsze - swoim sposobem na&nbsp;szczescie, ,,mysa". Oczywiscie wszystko w&nbsp;charakterystycznym, skandynawskim stylu. Oprocz przestrzeni, w ktorej ludzie moga odpoczac, poczytac, pograc w&nbsp;gry planszowe czy&nbsp;pobawic sie z&nbsp;dziecmi,
</p></div>

<div class="colIn_50  paddingLeft_20"><p>
zaoferowalismy mieszkancom kawe, herbate i&nbsp;szwedzkie slodkosci wypiekane przez lokalnych cukiernikow.
Na&nbsp;miejscu mozna bylo rowniez zapisac sie&nbsp;do&nbsp;bazy IKEA FAMILY. Na gosci czekaly tez&nbsp;ciekawe warsztaty, ktore przyblizaly zarowno kulture i&nbsp;zwyczaje Szwecji, jak i&nbsp;wartosci IKEA. Mieszkancy Lublina mieli szanse poznac skandynawskie kino, charakterystyczne zasady aranzacji domowych przestrzeni i&nbsp;wlasnorecznego tworzenia ozdob. Pelen kalendarz wydarzen udostepnilismy w specjalnie zaprojektowanym przez nas&nbsp;bocie w&nbsp;aplikacji ,,Messenger", ktory pozwalal zapisywac sie&nbsp;na&nbsp;wybrane wydarzenia.  <br>
<br>
Wiekszosc komunikacji projektu ,,IKEA Po Sasiedzku" prowadzilismy on-line. Nie zapomnielismy przy tym o&nbsp;dostosowaniu wszystkich tresci do&nbsp;formatu mobile friendly.<br>
<br>
<strong>Efekt:</strong> Miejsce od&nbsp;pierwszych dni cieszylo sie&nbsp;ogromna popularnoscia wsrod mieszkancow.<br><br>
