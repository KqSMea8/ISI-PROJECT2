- Po dwoch miesiacach dzialalnosci w&nbsp;sieci dotarlismy do&nbsp;ponad <strong>700 000 osob</strong> na&nbsp;Lubelszczyznie, czyli <strong>71%</strong> calej grupy docelowej. <br><br>
- W ,,IKEA Po Sasiedzku" w&nbsp;ciagu 3 pierwszych miesiecy pojawilo sie ponad 18 000 gosci, z&nbsp;czego prawie  <strong>1 000 osob</strong> poszerzylo baze IKEA FAMILY w&nbsp;Lublinie.
