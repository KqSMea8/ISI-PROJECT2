<h1><a href="http://serywizajny.org.pl/?p=1897" rel="bookmark">Identyfikacja producentow w ramach Podlaskiego Centrum Produktu Lokalnego</a></h1></div>
<p><strong>Szanowni Panstwo,</strong></p>
<p>Samorzad wojewodztwa podlaskiego zaprasza do udzialu w wojewodzkiej akcji identyfikacji producentow w ramach&nbsp;<em>Podlaskiego Centrum Produktu Lokalnego</em>.&nbsp;Identyfikacja prowadzona jest za posrednictwem&nbsp;<a href="https://www.wrotapodlasia.pl/admin/auth/login/ref.aHR0cDovL3d3dy53cm90YXBvZGxhc2lhLnBsL2Ntcy9hZG1pblBhZ2UvZWRpdC9pZGVudHlmaWthY2phLndyb3RhcG9kbGFzaWEucGw=">ankiety on-line</a>, ktora wypelniaja bezposrednio zainteresowani producenci.</p>
<p><strong>Kto powinien wypelnic ankiete?</strong></p>
<p>Ankieta skierowana jest do&nbsp;<strong>rolnikow</strong>,&nbsp;<strong>producentow</strong>,&nbsp;<strong>wytworcow</strong>&nbsp;- a takze wiekszych i mniejszych firm, ktore oferuja zywnosc wysokiej jakosci oparta o surowce pochodzace z terenu wojewodztwa podlaskiego. Z ankieta chcemy dotrzec rowniez do wszystkich, ktorzy chca uruchomic lub rozwijac swa dzialalnosc w rolnictwie lub przetworstwie.</p>
<p><u>A wiec jesli jestes:</u></p>
<p>&#8211; rolnikiem i juz prowadzisz dystrybucje ,,prosto do klienta"<br />
&#8211; a moze zarzadzasz mala lub duza firma w ktorej dba sie o jakosc produktu, lokalne pochodzenie surowca oraz kultywuje sie tradycje. Moze to byc np. przetwornia, tlocznia, masarnia, olejarnia, piekarnia, kiszarnia warzyw, kaszarnia itp.<br />
&#8211; prowadzisz restauracje lub agroturystyke, w ktorej mozna zjesc kartacza lub babke ziemniaczana z&nbsp;lokalnych ziemniakow<br />
&#8211; moze dopiero planujesz uruchomic ktoras ze wskazanych powyzej dzialalnosci i nie wiesz jakie sprawy nalezy zalatwic celem dopelnienia formalnosci?</p>
<p><strong>W kazdym z tych przypadkow koniecznie wypelnij ankiete!</strong></p>
<p><strong>&nbsp;</strong></p>
<p><strong>Podlaskie Centrum Produktu Lokalnego - jakie korzysci dla producentow?</strong></p>
<p>To przede wszystkim udzial w roznorodnych inicjatywach majacych na celu zwiekszenie oplacalnosci produkcji m.in. promocja w Internecie, broszurach, imprezach sprzedazowych. To rowniez uzyskiwanie informacji o obowiazujacych przepisach, a takze wspolne dostawy np. do grup konsumenckich, stolowek, restauracji.</p>
<p><strong>&nbsp;</strong></p>
<p><strong>Bezposrednie korzysci dla producenta:</strong></p>
<p>&#8211; mozliwosc uzyskiwania informacji o wymogach wynikajacych z przepisow dot. produkcji zywnosci,<br />
&#8211; udzial w organizowaniu wzajemnej wspolpracy pomiedzy producentami celem poprawy oplacalnosci produkcji (organizacja szkolen, warsztatow zgodnie z potrzebami zglaszanymi przez producentow, tworzenie wspolnej oferty dla firm i instytucji),<br />
&#8211;&nbsp;budowanie sieci dostawcow, ktorzy mogliby docelowo dostarczac swe produkty np. do stolowek szkolnych, restauracji itp.,<br />
&#8211; ukazanie sie w dobrym swietle nabywcom produktow rolnych i srodkow spozywczych, aby zwiekszac mozliwosci zbytu,<br />
&#8211; propagowanie konkretnych i dostepnych lokalnie produktow spolecznym sieciom konsumenckim, ktore moglyby zaopatrywac sie bezposrednio u lokalnych producentow,<br />
&#8211; skracanie lancucha dystrybucji, aby produkty mogly trafiac do odbiorcow przy udziale jak najmniejszej liczby posrednikow (lub nawet przy zupelnym braku posrednikow!),<br />
&#8211; propagowanie produktow w Internecie za posrednictwem serwisow zarzadzanych przez Urzad Marszalkowski Wojewodztwa Podlaskiego, jak tez partnerow,<br />
