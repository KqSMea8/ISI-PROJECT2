<h3>O firmie</h3>
<hr />
<p style="text-align: justify;">Firma MK Dach System Sp. z o.o. z siedziba w Gliwicach przy ul. Plebiscytowej 1, rozpoczela dzialalnosc jako firma zajmujaca sie wykonawstwem pokryc dachowych z tworzyw sztucznych PVC, PE, ECB. Bylismy prekursorami wdrazania technologii membran PVC na dachach w Polsce. Nasze doswiadczenia siegaja 1994r.</p>
<p style="text-align: justify;">W latach 2004-2010 wykonalismy <strong>5,5 mln m2 pokryc dachowych</strong> na 220 obiektach dla wielu znaczacych inwestorow, m.in.: <strong>PROLOGIS, PANATTONI, PARKRIDGE, LG, REAL, TESCO, CARREFOUR, VOLKSWAGEN, AHOLD</strong>. </p>
<p style="text-align: justify;">Kazdorazowo budowy wykonywane sa przez zespol wysoko wykwalifikowanych pracownikow, na czele z kadra inzynierska przygotowana do pracy w zakladach <strong>BAUDER, KOSTER, FATRA I GAMRAT</strong>.   </p>
<p style="text-align: justify;">Najwiekszym atutem firmy jest szybkosc i dokladnosc w ukladaniu membran, odpowiedni sprzet i srodki transportu. Jestesmy w stanie - majac na uwadze dotychczasowe doswiadczenie - wykonywac na terenie calej Polski pokrycia dachowe na obiektach o roznym przeznaczeniu: hale magazynowe, przemyslowe, handlowe, obiekty sportowe i rekreacyjne, stacje paliw i inne.</p>
<p style="text-align: justify;">Zapewniamy fachowe doradztwo i pomoc przy wyborze materialu, organizacji budowy. Uwzgledniamy indywidualne potrzeby Klienta, oferujac dla kazdego przypadku rozwiazania systemowe wlasciwej technologii membran PVC.</p>
<p style="text-align: justify;"><strong>Polecamy sie jako solidny i godny zaufania wykonawca Panstwa obiektow. </strong></p>
