<div id="content">

<p style="text-align: justify;">Katedra Lingwistyki Komputerowej prowadzi badania nad automatyczna analiza tekstu polskiego, kladac glowny nacisk na analize slownictwa i budowe slownikow komputerowych.  Podstawowym narzedziem przetwarzania tekstu jest slownik fleksyjny (<a href="/sfjp">SFJP</a>), tj.  program sprowadzajacy forme tekstowa wyrazu, np. psa do postaci haslowej pies i przypisujacy wyrazowi charakterystyke gramatyczna, czyli rzeczownik meski zywotny. Zidentyfikowany za pomoca slownika fleksyjnego wyraz moze uzyskac interpretacje semantyczna, gdy odwolamy sie do przechowywanego w slowniku semantycznym (<a href="/ssjp">SSJP</a>) relacyjnego opisu znaczenia, gdzie relacje okreslaja laczliwosc definiowanego znaczenia z innym znaczeniami, np. <em>pies <span style="font-size: 12px; font-style: normal;">JEST</span> zwierzeciem, pies <span style="font-size: 12px; font-style: normal;">TO</span> spaniel, jamnik, owczarek, ...,  pies <span style="font-size: 12px; font-style: normal;">AKCJA</span> szczekac, merdac, ..., pies <span style="font-size: 12px; font-style: normal;">STAN_POZYTWNY</span> wierny, czujny, ...</em> </p>
<p style="text-align: justify;">Opracowywane w KLK slowniki sa narzedziem niezbednym do przeprowadzenia automatycznej analizy semantycznej tekstu. Budowe efektywnych algorytmow interpretujacych semantyczna warstwe tekstu chcemy oprzec na obserwacji naturalnych mechanizmow i zaleznosci pomiedzy znaczeniami, ujawniajacych sie w sieci skojarzeniowej (<a href="/sssjp">SSSJP</a>), uzyskanej eksperymentalnie za pomoca testu swobodnych skojarzen slownych.</p>
<p style="text-align: justify;">Indywidualne zainteresowania badawcze czlonkow zespolu obejmuja: filozofie, informatyke, jezykoznawstwo, psycholingwistyke, poetyke i retoryke.</p>
<h1>Zastosowania</h1>
<p style="text-align: justify;">Doswiadczenia zdobyte w pracach nad slownikiem semantycznym pozwolily zbudowac relacyjny system zarzadzania wielojezycznym korpusem tekstow - projekt <a href="/enchiridion">ENCHIRIDION</a>.</p>
<p style="text-align: justify;">W Katedrze Lingwistyki Komputerowej opracowano kognitywno-semantyczny model stanowiacy podstawe  projektowania i implementacji interfejsu uzytkownika dla osob z dysfunkcja wzroku, ktore korzystaja z  tzw. czytnika ekranowego, tj. programu konwertujacego tekst na mowe, a wiec sa silnie zalezne od poprawnosci interfejsu tekstowego. Model kognitywno-semantyczny pozwala programistom zrozumiec mechanizmy stojace u podstaw praktycznych zalecen  <i>Web Accessaibility Content Guidelines (WACG2)</i>, ktore okreslaja zasady budowy interfejsu uzytkownika dla osob z roznymi dysfunkcjami. Opis modelu i zasad jego implementacji z przykladami kodu zrodlowego zawiera zlozona do druku ksiazka: <i>Interfejs dla osob z dysfunkcja wzroku. Model kognitywny i przyklad dobrej praktyki</i>, red. I. Gatkowska, W. Lubaszewski, Krakow,  Wydawnictwo Uniwersytetu Jagiellonskiego, czerwiec 2013.</p>
<p style="text-align: justify;">Katedra byla gospodarzem konferencji NLPCS 2015 <a href="http://nlpcs2015.epi.uj.edu.pl">http://nlpcs2015.epi.uj.edu.pl</a>.</p>
<p style="text-align: justify;">Katedra bedzie gospodarzem konferencji NLPCS 2018 <a href="http://nlpcs2018.epi.uj.edu.pl">http://nlpcs2018.epi.uj.edu.pl</a>.</p>
