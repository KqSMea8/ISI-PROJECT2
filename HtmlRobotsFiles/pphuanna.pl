<h2>Witamy</h2>

<p>Zaklad <strong>PPHU "ANNA"</strong>  dziala na rynku krajowym i miedzynarodowym od roku 1995 zapewniajac wysoka jakosc swoich wyrobow i uslug.
</p>

<p>Firma bazuje na dlugoletnim doswiadczeniu w produkcji regranulatow i przemialow z tworzyw sztucznych z wykorzystaniem  nowoczesnego parku maszyn i technologii. Naszym  atutem jest bezposrednia i fachowa obsluga klienta. Celem firmy jest ciagle dazenie do zaoferowania produktow o najwyzszej klasie. Dlatego chetnie nawiazujemy stala wspolprace.
</p>

<p>Posiadamy uregulowany stan formalno-prawny w zakresie prowadzonej dzialalnosci tzn. pozwolenie na zbieranie, przetwarzanie i odzysk odpadow tworzyw sztucznych, ( zgodnie z art. 28 ust.2 i sut.5 ustawy z dnia 27.IV.2001 o odpadach - DZ.U.Nr.62 poz.628)
</p>

<p><img src="http://www.pphuanna.pl/media/img/unia_dla_przedsiebiorczych.jpg" alt="" class="img-right" />W roku 2006 zakupilismy nowa linie do regranulacji odpadow tworzyw sztucznych dla ulepszenia jakosci regranulatu, firmy EREMA o wydajnosci 300kg/h. Zainstalowana linia do granulacji wsparta jest uniwersalna kruszarka produkcji Weima 500kg/h. Firma zakupila linie do recyklingu odpadow tworzyw sztucznych z funduszu "UNIA DLA PRZEDSIEBIORCZYCH"
</p>
<p>W 2010 roku Przedsiebiorstwo Produkcyjno Handlowo Uslugowe ANNA zakupililo nowa maszyne do regranulacji tworzyw sztucznych firmy EREMA o wydajnosci do 1000 kg/h. Maszyna zostala zakupiona w ramach projektu " Wzrost konkurencyjnosci Firmy ANNA poprzez zakup linii do recyklingu odpadow" wspolfinansowanego z Europejskiego Funduszu Rozwoju Regionalnego. Wartosc projektu: 2 334 004,94 zl, wartosc dofinansowania: 1 147 871,28 zl. </p>
<p><img src="http://www.pphuanna.pl/media/img/rpo.jpg" alt="rpo wm" class="img-right"/>Wydatek wspolfinansowany przez Unie Europejska ze srodkow Europejskiego Funduszu Rozwoju Regionalnego w ramach Regionalnego Programu Operacyjnego Wojewodztwa Mazowieckiego 2007-2013 oraz ze srodkow budzetu panstwa.
</p>
<p style="padding-top:10px; padding-bottom:10px; padding-left:10px; color:red; background-color:#FFFDBE">ZAPYTANIE OFERTOWE z dnia 20.06.2013r. <a href="http://www.pphuanna.pl/zapytanie.pdf">6.1 PO IG PASZPORT  DO EKSPORTU</a></p>

<p style="font-size:1em; font-style:italic;">Informacje zrodlowe na temat Regionalnego Programu Operacyjnego Wojewodztwa Mazowieckiego 2007-2013 znajduja sie na stronie: <a href="http://www.mazowia.eu">www.mazowia.eu</a>.</p>
