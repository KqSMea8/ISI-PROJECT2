<h2>Strona glowna</h2>



























<div id="pure_content">
<p>
Spoldzielnia Mieszkaniowa w Olkuszu powstala 17 sierpnia 1959 roku, 25-ciu mieszkancow Olkusza podjelo Uchwale Zalozenia Spoldzielni,&nbsp;przyjelo Statut oraz dokonalo wyboru jej wladz, to jest Rady Nadzorczej i Zarzadu.&nbsp;Obecnie Spoldzielnia dziala na terenie miast: Olkusz, Wolbrom oraz gminy Klucze.&nbsp;Siedziba Spoldzielni jest miasto Olkusz.</p>
<p>
Spoldzielnia administruje 152 budynki, w ktorych jest 6568 mieszkan, o powierzchni 330905 m<sup>2</sup>, 82 lokalami uzytkowymi o&nbsp;powierzchni 10199 m<sup>2</sup> oraz 562 garazami o powierzchni&nbsp; 9200 m<sup>2</sup>.</p>
<p>
Spoldzielnia podlega corocznemu badaniu przez bieglego rewidenta oraz co 3 lata lustracji.&nbsp;Ksiegi rachunkowe prowadzone sa w siedzibie zarzadu przy uzyciu systemu komputerowego.</p>      </div>
