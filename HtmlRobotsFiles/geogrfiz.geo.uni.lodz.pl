<p style="text-align: center;">***</p>
<p style="text-align: left;"><strong>Katedra Geografii Fizycznej </strong>(KGF, Department of Physical Geography) stanowi jednostke organizacyjna <a href="http://www.geo.uni.lodz.pl/">Wydzialu Nauk Geograficznych Uniwersytetu Lodzkiego</a>.</p>
<p style="text-align: left;">Sklad osobowy Katedry tworzy obecnie zespol  dziewieciu pracownikow  naukowo-dydaktycznych, dydaktycznych  i naukowo-technicznych: dr hab. <strong>Jan Degirmendzic</strong> prof. UL, mgr <strong>Malgorzata Frydrych</strong>, dr <strong>Stanislaw Krysiak</strong>, dr <strong>Anna Majchrowska</strong>, dr <strong>Elzbieta Papinska</strong>, <br />dr hab. <strong>Zbigniew Rdzany</strong> prof. UL, mgr <strong>Aleksandra Retkiewicz</strong>, dr <strong>Aleksander Szmidt </strong>i dr <strong>Wojciech Toloczko</strong>.<strong><br /></strong><strong> </strong></p>
<p style="text-align: left;">W strukturze wewnetrznej katedry dzialaja: <strong>Zaklad Dynamiki Srodowiska i Gleboznawstwa </strong>(Sub-Department of Environmental Dynamics and Soil Science) oraz <strong>Pracownia Geoekologii</strong> (Labolatory of Geoecology). <strong> </strong></p>
<p style="text-align: left;">Dzialalnosc dydaktyczna Katedry Geografii Fizycznej obejmuje obecnie wszystkie kierunki Wydzialu Nauk Geograficznych oraz kierunki: ochrona srodowiska, biologia i biomonitoring Wydzialu Biologii i Ochrony Srodowiska. Katedra prowadzi specjalnosc magisterska <strong>geoekologia z ekofizjografia</strong>. <strong><br /></strong></p>
