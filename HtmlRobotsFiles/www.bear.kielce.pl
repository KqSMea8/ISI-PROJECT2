<p>&nbsp;</p>
<p style="text-align: justify;">Zaopatrujac duze hurtownie motoryzacyjne, sklepy, warsztaty ugruntowalismy sobie silna pozycje na rynku czesci zamiennych.<br />
Okres ten oraz zdobyte doswiadczenie i wiedza pozwolila nam na wyselekcjonowanie z rynku czesci samochodowych jakosciowo nie odbiegajacych od oryginalu lecz o znacznie nizszej cenie ,gwarantujac swoim klienta odpowiednia jakosc sprzedawanych przez nas produktow.</p>
<p style="text-align: justify;">Jako jedyni sprzedawcy mozemy pochwalic sie profesjonalnym dzialem do obslugi gwarancyjnej sprzedawanych przez nas czesci.<br />
W swojej ofercie posiadamy: elektryczne pompy paliwa, sondy lambda, cewki zaplonowe, silniki krokowe, czujniki halla, czujniki ABS, przeplywomierze oraz inne czesci elektroniki samochodowej renomowanych producentow.</p>
