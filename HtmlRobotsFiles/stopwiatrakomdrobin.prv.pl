Zamieszczajac informacje na stronie, mamy na celu uswiadomic spoleczenstwu, ze niezgoda na zainstalowanie obok swoich siedlisk wiatrakow w zbyt malej odleglosci, nie wiaze sie z zazdroscia i niechecia do rozwoju. Kieruje nami wylacznie nasze dobro. Stawiamy czola niedomowieniom i wszelkim niepozadanym dzialaniom.
