<div id="mybody"><p style="font-family:Calibri"><justify>
Neuhauser-Polska sp.zo.o. jest firma corka preznie dzialajacej firmy Neuhauser GmbH z siedziba w Lunen kolo Dortmundu. Zostala stworzona z pierwotnym zamyslem rozpowszechnienia i dystrybucji szyn do kolejek podwieszanych na polskich kopalniach. Przez dlugi czas elementy tras kolejek podwieszanych jak i szyny stanowily glowny filar dzialalnosci firmy.
<br>
<br>
Neuhauser-Polska prowadzi dzialalnosc i buduje swoje struktury na polskim rynku od 2007 r. W tym czasie ewaluowala od przedstawicielstwa handlowego do malego zakladu produkcyjnego specjalizujacego sie glownie w spawalnictwie. Dzialalnosc zostala wzbogacona o produkcje kontenerow dolowych do kopaln. Nasze kwalifikacje produkcyjno-spawalnicze zostaly poswiadczone swiadectwem Instytutu Spawalnictwa w Gliwicach, kwalifikujacym Spolke do I Grupy Zakladow Malych wg normy PN-M-690009.
<br>
<br>
Na dzien dzisiejszy staramy sie poszerzyc i rozwinac nasza dzialalnosc o produkcje konstrukcji przenosnikow magnetycznych oraz specjalnych wahaczy na potrzeby rynku niemieckiego. Firma zatrudnia dwadziescia osob na hali produkcyjnej oraz grupe pracownikow biurowych lacznie z kontrola jakosci pod zwierzchnictwem Instytutu Spawalnictwa w Gliwicach.
<br>
<br>
Jestesmy dostawcami do kopaln Poludniowego Koncernu Weglowego SA oraz Kompanii Weglowej SA. Wykonujemy uslugi spawalnicze dla firmy matki oraz polskich kontrahentow. Konkurencje okresla sie na bardzo silna jednak to nasza firma zdobywa rynek w szynach wysokoobciazalnych, przez co zostaje zwiekszana sprzedaz. Konkurencja produkuje tanie, zwykle szyny, ktorych zywotnosc wynosi nie wiecej niz 30% zywotnosci naszych szyn.
</justify></p>

