<li>Ewa Wachowicz - Dyrektor Szkoly Podstawowej im. Marii Kownackiej w&nbsp;Sokolowie z&nbsp;przedstawicielami Rady Pedagogicznej i pocztem sztandarowym;</li>
<li>Ewa Chrzaszczewska - Dyrektor Szkoly Podstawowej im. Marii Kownackiej w&nbsp;Nowym Kamieniu z przedstawicielami Rady Pedagogicznej i pocztem sztandarowym;</li>
