<h3><a href="/article/id/578">NAJMLODSI MIESZKANCY KAMIIENCA SPRZATAJA SWOJA MIEJSCOWOSC</a></h3>
<p><span style="font-family: times new roman,times; font-size: 12px;">Szanowni Mieszkancy,</span></p>
<p style="text-align: justify;"><span style="font-family: times new roman,times; font-size: 12px;">30 marca 2019 roku, z inicjatywy nowo wybranej Pani soltys, dzieci i mlodziez sprzatali najblizsza sobie okolice. Okazalo sie, ze w przydroznych rowach znajduje sie mn&oacute;stwo plastikowych i szklanych butelek oraz wiele innych smieci. Warto nadmienic, ze podczas zbierania odpad&oacute;w dokonywano od razu odpowiedniej segregacji. Na szczescie pogoda dopisala i najmlodsi spedzili milo, a przede wszystkim pozytecznie sobotni czas. Swoim postepowaniem z pewnoscia zwr&oacute;cili uwage innych ludzi na problem zasmiecania otoczenia. &nbsp;&nbsp;</span><br /><span style="font-family: times new roman,times; font-size: 12px;"> Pani Agata Rymarkiewicz zapewnia, ze wspomniane dzialania beda cykliczne, w koncu wszystkim powinno zalezec na czystosci i porzadku. </span></p>
