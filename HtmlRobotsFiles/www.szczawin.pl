<h3><a href="/article/id/571">Nadanie Imienia i Sztandaru Szkole Podstawowej w Trebkach</a></h3>
<p><strong>Drodzy Mieszkancy,</strong></p>
<p style="text-align: justify;">21 marca&nbsp; w Szkole Podstawowej w Trebkach odbyla sie uroczystosc nadania imienia Marii Kownackiej i sztandaru. Przygotowania do tej historycznej dla calej spolecznosci szkolnej chwili trwaly od 2016 r. W tym czasie podjeto szereg dzialan majacych na celu zapoznanie uczni&oacute;w, rodzic&oacute;w &nbsp;z wybrana w drodze glosowania Patronka.</p>
<p style="text-align: justify;">Tego dnia wszyscy uczniowie, nauczyciele, pracownicy, rodzice oraz zaproszeni goscie udali sie do miejscowego kosciola pod wezwaniem Sw. Stanislawa Kostki na uroczysta Msze sw. inaugurujaca obchody nadania imienia szkole i poswiecenia sztandaru. Mszy sw. przewodniczyl i homilie wyglosil oraz poswiecil sztandar Ksiadz Biskup Andrzej Franciszek Dziuba &ndash; Ordynariusz Diecezji Lowickiej. W liturgii czynny udzial brali nauczyciele, rodzice i&nbsp;uczniowie.</p>
<p style="text-align: justify;">Po Mszy sw., uczestnicy uroczystosci przemaszerowali do szkoly. Na czele kolumny szly&nbsp; poczty sztandarowe, nauczyciele, uczniowie, rodzice i zaproszeni goscie.<br /> Gl&oacute;wna czesc oficjalnej uroczystosci miala miejsce w specjalnie na te okolicznosc udekorowanej sali gimnastycznej. Po odspiewaniu hymnu<br /> panstwowego nastapilo powitanie przybylych gosci. Swa obecnoscia zaszczycili miedzy innymi:</p>
<ul>
<li>Ksiadz Biskup Andrzej Franciszek Dziuba - Ordynariusz Diecezji Lowickiej;</li>
<li>Ksiadz Dziekan Boleslaw Kusmirka;</li>
