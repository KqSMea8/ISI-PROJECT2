<p>&nbsp;</p>
<p>Uroczystosc prowadzila nauczycielka Pani Beata Gajdecka.</p>
<p>Pani Teresa Jurczyk zaprezentowala postac Marii Kownackiej, a dyrektor szkoly Urszula Koniarek przedstawila uzasadnienie wyboru Patronki.</p>
<p><strong>Przewodniczacy Rady Gminy w Szczawinie Koscielnym Pan Roman Wojciechowski odczytal Akt Nadania&nbsp; Szkole Podstawowej w Trebkach Imienia Marii Kownackiej.</strong> <br /> Potem nastapilo symboliczne wbicie gwozdzi honorowych i fundacyjnych w drzewiec sztandaru.</p>
<p>Kolejnym punktem byla ceremonia przekazania Sztandaru. Przewodniczaca Rady Rodzic&oacute;w Pani Ewa Chmielecka odczytala Akt Fundacyjny Sztandaru. Poczet Sztandarowy reprezentowany przez rodzic&oacute;w przekazal Sztandar dyrektorowi szkoly, kt&oacute;ry podziekowal za ten cenny dar i zapewnil, ze stanie sie on symbolem wartosci bedacych fundamentem pracy szkoly. Dyrektor dokonal &nbsp;prezentacji Sztandaru wszystkim zgromadzonym. Po prezentacji dyrektor szkoly przekazala Sztandar &nbsp;Pocztowi Uczni&oacute;w, Ci zapewnili, iz otocza go powszechnym szacunkiem i czcia. Nastepnie &nbsp;odbylo sie slubowanie uczni&oacute;w na Sztandar Szkoly.</p>
<p>W dalszej kolejnosci glos zabrali zaproszeni goscie.</p>
<p>Przedstawiciele Starostwa Gostyninskiego: Pan Arkadiusz Boruszewski &ndash; Starosta i Pani Katarzyna Osowicz - Szewczyk &ndash; Przewodniczaca Rady Powiatu na rece dyrektora szkoly zlozyli gratulacje i zyczenia, aby uczniowie i nauczyciele w nauce i pracy czerpali inspiracje z&nbsp;tw&oacute;rczosci Marii Kownackiej.</p>
<p>Pani Barbara Stepniak W&oacute;jt Gminy Szczawin Koscielny zyczyla nauczycielom, rodzicom i uczniom, aby to czym zyla Nasza Patronka, bylo dla nas wazne i stanowilo inspiracje wszelkich dzialan, dostarczalo radosci zycia.</p>
<p>Pani wizytator Anna Olobry - Szarow odczytala&nbsp;&nbsp; list gratulacyjny w imieniu Dyrektora Delegatury w Plocku Kuratorium Oswiaty w Warszawie.</p>
<p>Kolejni m&oacute;wcy r&oacute;wniez wyjatkowo cieplo i zyczliwie wypowiadali sie pod adresem szkoly, skladajac a rece dyrektora kwiaty i &nbsp;skromne upominki.</p>
<p>Po przem&oacute;wieniach poczty sztandarowe zostaly wyprowadzone, a zebranych zaproszono na&nbsp;przedstawienie, w kt&oacute;rym wystapili uczniowie szkoly.</p>
<p>Na zakonczenie dyrektor szkoly &nbsp;podziekowala wszystkim przybylym za uswietnienie swa obecnoscia tej uroczystosci. Wsp&oacute;lnie z przewodniczaca Rady Rodzic&oacute;w wreczyla podziekowania fundatorom i darczyncom.</p>
<p>Nadanie imienia szkole bylo swietem, kt&oacute;re zapamietamy na dlugo. Teraz nadszedl czas, by udowodnic, ze jestesmy godni nosic imie naszej Patronki.</p>
