<h3>
O Firmie
</h3>

<p>Pracownia Geodezyjno-Kartograficzna w Chrzanowie istnieje od 1995r.
Wlascicielem Naszej firmy jest inz. Stanislaw Ciereszko. PGK
specjalizuje sie w obsludze inwestycji. Prowadzimy prace w calym cyklu
inwestycyjnym, badz w pracach doraznych - od przygotowania podkladow
mapowych, zalozenia i wyrownania osnow, prac ziemnych, wznoszenia
obiektow, inwentaryzacji, montazu i regulacji urzadzen (suwnic, piecow,
dzwigow, etc.), tyczenia i inwentaryzacji ustrojow podziemnych, kontrola
gromadzonych mas ziemnych, prac powykonawczych.</p>

