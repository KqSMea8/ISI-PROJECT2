<p style="text-align: center;"><strong>Komunikat Zarzadu</strong></p>
<p style="text-align: center;"><em>07.07.17</em></p>
<p> </p>
<p style="text-align: left;">Zarzad Miraculum S.A. z siedziba w Warszawie informuje, ze zgodnie z decyzja wydana przez Komisje Nadzoru Finansowego (Komisja) w dniu 07 lipca 2017 roku Komisja zatwierdzila Prospekt Emisyjny Spolki.</p>
<p style="text-align: left;">Prospekt emisyjny zostal sporzadzony w zwiazku z oferta publiczna akcji serii W oraz zamiarem ubiegania sie o dopuszczenie do obrotu na rynku regulowanym akcji serii S, U, R1, R2 oraz akcji serii W, praw do akcji serii W, praw poboru akcji serii W Emitenta.</p>
<p style="text-align: left;">Prospekt emisyjny jest dostepny w formie elektronicznej na stronie internetowej Spolki, w zakladce Relacje Inwestorskie oraz na stronie internetowej Oferujacego - Polskiego Domu Maklerskiego S.A.<strong> </strong></p>
