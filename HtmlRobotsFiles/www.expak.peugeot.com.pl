function displayDealersCallBack(map)
{

var dealers = htmlDecode("&lt;Dealers /&gt;");

bounds = new google.maps.LatLngBounds();
gdealers	= [];
numDealer = 0;

$("<div>" + dealers + "</div>").find('Dealer').each(function() {

