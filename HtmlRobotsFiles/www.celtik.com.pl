<h1>CELTIK - Biuro tlumaczen - Obsluga Celna Pila</h1>
<p>Oferujemy uslugi dla firm i osob fizycznych dotyczace formalnosci zwiazanych z przywiezieniem samochodu z zagranicy.</p>
<p>
Wieloletnie doswiadczenie i wspolpraca z administracja celna, skarbowa i wydzialem komunikacji, a takze biezace sledzenie zmian w przepisach prawnych, sprawia, ze nasze uslugi sa w pelni profesjonalne i na wysokim poziomie.</p>


<h2>Tlumaczenia samochodowe - Pila i okolice</h2>
<p>Wspolpracujemy z tlumaczem jezyka niemieckiego, ktory zazwczaj w ciagu 1,5 do 2 godzin tlmaczy dokumenty niemieckie na miejscu w czasie, gdy Ty zalatwiasz formalnosci akcyzowe w urzedzie celnym.</p>
<p>Dokumenty samochodowe z innych jezykow przyjmujemy, a nastepnie przekazujemy do wlasciwego tlumacza. Na odbior z reguly trzeba czekac 1 dzien, az dotrze do nas poczta priorytetowa. Mozemy zlecic tlumaczowi, aby wyslal tlumaczenie poczta bezposrednio do klienta. Nie bedzie trzeba przyjezdzac ponownie po odbior tlumaczen.</p>
<p>Oferujemy tlumaczenia przysiegle i zwykle, jezyk niemiecki, francuski wloski, angielski, szwedzki, dunski, norweski i inne europejskie.</p>
<p>Nasi tlumacze przetlumacza takze inne dokumenty urzedowe, zaswiadczenia, pisma itp.</p>
<p>Wszyscy tlumacze w jednym miejscu!!!</p>

