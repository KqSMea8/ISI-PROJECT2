<h1>Co nas wyroznia:</h1>

<p class="light">
Biuro Podroznikow FLUGO zostalo zalozone w 1998 roku przez pilotow wycieczek. Pasja zalozycieli, sprawdzeni
kontrahenci, ogromne doswiadczenie pilotow oraz profesjonalizm w zorganizowaniu kazdej wyprawy,
gwarantuja Panstwu satysfakcje i zadowolenie. Realizowane programy swoim zasiegiem geograficznym
obejmuja <strong>wszystkie kontynenty i ponad 100 krajow</strong> z calego swiata, a ich <strong>roznorodnosc i pomyslowosc</strong> nie ma
zadnych ograniczen. Naszym glownym atutem przyciagajacym klientow szukajacych inspiracji przy organizacji
wydarzen typu MICE jest <strong>nieszablonowosc i niepowtarzalnosc</strong>.
</p>

<p>
<strong>FLUGO MICE</strong> dzieki efektowi synergii pomiedzy dzialami jest w stanie zapewnic powodzenie kazdego wyjazdu
firmowego, motywacyjnego, konferencji czy tez wyprawy integracyjnej.
</p>
<h2>Przykladowe wyjazdy motywacyjne, incentive organizowane przez FLUGO MICE:</h2>

<ul><li><a href="http://www.flugomice.pl/aktualnosci/16/szusowanie-w-val-di-fassa">wyjazdy narciarskie w Alpy</a>, Pireneje, do USA, Kanady, Chile
</li><li>zwiedzanie i udzial w zyciu kulturalnym miast (<a href="http://www.flugomice.pl/aktualnosci/15/farmacja-w-berlinie">Berlin</a>, Madryt, Barcelona, Praga, Budapeszt, Singapur, Sydney, Nowy Jork, Las Vegas etc.)
</li><li>pobyty na wyspach na wielu morzach (<a href="http://www.flugomice.pl/aktualnosci/12/sycylia-perla-morza-srodziemnego">Morze Srodziemne</a>, Karaibskie, <a href="http://www.flugomice.pl/aktualnosci/14/barcelona-i-teneryfa">Ocean Atlantycki</a>, Indyjski, Spokojny)
</li><li>luksusowe rejsy wycieczkowymi statkami - Europa, Australia, Ameryka, Azja
</li><li>rejsy jachtem po archipelagu Galapagos i Polinezji Francuskiej
</li><li>safari w Afryce Wschodniej i Poludniowej
</li><li>wspinaczki na Kilimandzaro, trekingi w Himalaje i Andy
</li><li>pakiety dla nurkow w Afryce, Azji, Australii
</li><li>wydarzenia sportowe, kulturalne (np. La Scala, <a href="http://www.flugomice.pl/aktualnosci/19/zawody-w-wiedniu">wyscigi samochodowe</a>, Formula 1, karnawal w Rio)
</li><li>i wiele innych...</li></ul>

<p>
<b>FLUGO to TOUROPERATOR</b> czyli bezposredni <a href="http://wyprawy.flugo.pl">organizator DALEKICH WYPRAW</a>. Nasze grupy docieraja do
najdalszych zakatkow swiata. jednoczesnie sprawdzajac mozliwosci organizacji wydarzen z zakresu MICE w
miejscach docelowych. Dzieki temu jestesmy przygotowani na zorganizowanie wyjazdow motywacyjnych (incentive), konferencji w wielu rejonach swiata.
Nasi piloci monitoruja stan bezpieczenstwa, przepisow oraz jakosci swiadczonych uslug przez naszych dostawcow.
</p>

<p>
<b>AKREDYTACJA IATA</b> (Miedzynarodowe Stowarzyszenie Przewoznikow Lotniczych) - pozwala na bezposrednie
zakladanie, modyfikowanie rezerwacji oraz <a href="https://transport.flugo.pl/">wystawianie biletow lotniczych</a> bez dodatkowych marz
posrednikow. <br />
<a href="http://flugosun.pl">CENTRUM PODROZY - flugosun.pl</a> - turystyka w oparciu o czartery renomowanych biur podrozy, standardowe
pakiety wakacyjne urozmaicone o zindywidualizowana obsluge pozwalaja na wyroznienie i zaspokojenie
specjalistycznych potrzeb zleceniodawcy.
</p>

<p>
<b>FIT (For Individual Travellers)</b> - od wielu lat FLUGO spelnia marzenia ponad 200 osob rocznie podrozujacych
indywidualnie po calym swiecie, bez opieki pilota ale pod opieka naszych lokalnych dostawcow. Sa to wyprawy
,,szyte na miare" wedlug specyficznych zamowien naszych Klientow.
</p>
<p><a href="http://www.flugo.pl">Siedziba firmy FLUGO</a> miesci sie w Bydgoszczy, w wojewodztwie kujawsko-pomorskim. Obslugujemy klientow <b>z calej Polski</b> -
zachecamy do kontaktu i sprawdzenia naszych uslug przy <a href="http://www.flugobts.pl">organizacji wyjazdow biznesowych</a>.</p>
