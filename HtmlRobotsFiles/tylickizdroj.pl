<p><strong>&ldquo;Tylicki Zdr&oacute;j&rdquo;</strong> to maly rodzinny pensjonat w Tyliczu &ndash; malowniczej miejscowosci polozonej w <strong>Popradzkim Parku Krajobrazowym</strong>, oddalonej zaledwie 6 km od jednego z najpiekniejszych uzdrowisk w Polsce: <strong>Krynicy-Zdroju</strong>. Swoim Gosciom oferujemy pobyt w przytulnie urzadzonych i komfortowych pokojach 1, 2, 3-osobowych, a takze w dwupokojowym apartamencie dla wiekszych rodzin. Wszystkie pokoje sa wyposazone w lazienke z prysznicem, TV oraz internet bezprzewodowy.&nbsp;Dodatkowym wyposazeniem kazdego pokoju jest czajnik elektryczny, naczynia oraz sztucce. Kazdy z pokoi posiada balkon. Zapewniamy takze bezpieczne miejsce do zaparkowania samochod&oacute;w (bezplatnie).</p>
<p>W sezonie narciarskim pobliskie stacje narciarskie TYLICZ SKI i MASTER SKI organizuja dojazd po narciarzy chcacych korzystac z ich uslug. W&oacute;wczas samoch&oacute;d mozna zostawic na parkingu przy pensjonacie i o nic sie nie martwic. &nbsp;W praktyce wyglada to tak, ze SKIBUSY przyjezdzaja po kazdego narciarza - <strong>BEZPLATNIE</strong>. Po zakonczeniu jazdy, mozna wr&oacute;cic do miejsca zakwaterowania - r&oacute;wniez <strong>BEZPLATNIE</strong>.</p>
<p><strong>&bdquo;Tylicki Zdr&oacute;j&rdquo;</strong> oferuje Panstwu bogate i smaczne sniadania oraz domowe obiadokolacje, serwowane w przytulnej jadalni. W trosce o Panstwa zadowolenie, wszystkie posilki przygotowujemy w&nbsp;tradycyjny, domowy spos&oacute;b dbajac o smak i swiezosc produkt&oacute;w. Mamy nadzieje, ze&nbsp;nasza kuchnia zadowoli najbardziej wymagajacych Gosci. Nasz pensjonat jest idealnym miejscem dla rodzin z dziecmi, kt&oacute;re w otoczeniu natury i specyficznego g&oacute;rskiego mikroklimatu moga korzystac z urok&oacute;w okolicy.</p>
<p>Do dyspozycji Gosci w okresie letnim jest <strong>duzy ogr&oacute;d</strong> sprzyjajacy wypoczynkowi i lezakowaniu oraz wieczornemu <strong>grillowaniu</strong>, a dla maluch&oacute;w przygotowalismy maly plac zabaw: hustawki, trampoline oraz piaskownice.</p>
<p>Ok. 300 metr&oacute;w od naszego osrodka znajduje sie <strong>basen</strong> z podgrzewana woda, <strong>boisko</strong> do siatk&oacute;wki plazowej, <strong>wypozyczalnia rower&oacute;w</strong> g&oacute;rskich (<strong>Osada Turystyczna &bdquo;Domki w lesie&rdquo;</strong>).</p>
<p>Po aktywnie spedzonych dniach moga Panstwo zrelaksowac sie korzystajac z uslug profesjonalnego i wykwalifikowanego magistra fizjoterapii oferujacego r&oacute;znego rodzaje <strong>masaze</strong> od klasycznego, poprzez <strong>masaz goracymi kamieniami</strong>.</p>
<p><strong>Zapraszamy ludzi chcacych wypoczac w milej domowej atmosferze przez caly rok.</strong></p>     </div>
