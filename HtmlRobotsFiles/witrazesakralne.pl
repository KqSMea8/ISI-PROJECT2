<section>
<p>Firma Vitro istnieje od 1990 roku. W tym czasie, oprocz zaprezentowanych powyzej, wykonalismy wiele mniejszych
i wiekszych zlecen. Wykonywalismy prace konserwatorskie, renowacje i calkowite odtworzenia historycznych witrazy
w wiekowych kosciolach i innych obiektach o wartosci historycznej. Zrealizowalismy rowniez sporo prac autorskich,
opartych o przekazane nam koncepcje lub stworzonych przez nas od zera, oraz niemozliwa do oszacowania liczbe
mniejszych zlecen. Pracowalismy na terenie calego kraju i za granica, w metropoliach i malych wioskach.
</p>
<p> Poniewaz obraz, szczegolnie malowany witrazowym swiatlem przemawia o wiele lepiej niz slowa, zapraszamy
do przejrzenia listy realizacji firmy Vitro, zbudowanej glownie w oparciu o zdjecia z przebiegu prac, a przede wszystkim
- ich efektow.
</p>
