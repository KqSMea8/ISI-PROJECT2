<h2 class="entry-title">Strona glowna</h2>

<div class="entry-content">
<p>&nbsp;<br />
&nbsp;</p>
<p>Nasze Stowarzyszenie powstalo na przelomie 2007 i 2008 roku z inicjatywy grupy studentow Kolegium Miedzywydzialowych Indywidualnych Studiow Humanistycznych Uniwersytetu Warszawskiego. Od poczatku naszym celem bylo pielegnowanie wiezi miedzyludzkich, ale i wykorzystywanie dla potrzeb ogolnospolecznych potencjalu ludzi o specyficznym wyksztalceniu i umiejetnosciach, jakimi jestesmy. Wiekszosc z nas studiuje lub ukonczyla kilka kierunkow studiow. Znaczna czesc przygotowuje prace doktorskie, a niektorzy nawet juz je z powodzeniem przygotowali.</p>
<p>Od poczatku Stowarzyszenie wspolorganizuje studenckie wyjazdy integracyjne (rozliczylismy juz okolo 10 takich imprez). Samorzad Studentow Kolegium MISH nie posiada osobowosci prawnej, a wynikajaca z tej okolicznosci luke wypelnia nasze Stowarzyszenie.</p>
<p>W 2009 zorganizowalismy na dziedzincu Uniwersytetu Warszawskiego koncert upamietniajacy 65 rocznice wybuchu Powstania Warszawskiego.<br />
Obecnie, dzieki srodkom przekazanym przez Fundacje im. Stefana Batorego, prowadzimy monitoring sadownictwa dyscyplinarnego</p>
