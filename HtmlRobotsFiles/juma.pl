<script type="text/javascript">

var slajdy = ["Images/Slajder/1.jpg","Images/Slajder/2.jpg","Images/Slajder/3.jpg","Images/Slajder/4.jpg","Images/Slajder/5.jpg","Images/Slajder/6.jpg","Images/Slajder/7.jpg"];
//////////////////////////////////////////////////////////////////////////////////

var bAutoZmien = 1;

var IndexSLA = 0;
var iLastSLA_Show = 6;

var myTimer = null;

var opacityCurent = 0;
var mleft = 0;

var IndexSLAStop = -1;

function Przeladuj(index)
{
if (IndexSLA != index)
{
ZwiekszIndex();

IndexSLAStop = index;
PokazAktualny();
}
}

function AutoZmien()
{
if (bAutoZmien == 0) return;

ZwiekszIndex();

PokazAktualny();
}

function ZwiekszIndex()
{
iLastSLA_Show = IndexSLA;

IndexSLA = IndexSLA + 1;
if (IndexSLA > 6) IndexSLA = 0;
}

function AutoOpasityShow()
{
window.clearInterval(myTimer);

if (IndexSLAStop == -1)
{
opacityCurent += 0.05;
}
else
{
opacityCurent += 0.2;
}

if (opacityCurent > 1) opacityCurent = 1;

document.getElementById('ID_IMAGE_SL_' + IndexSLA).style.opacity = opacityCurent;
document.getElementById('ID_IMAGE_SL_' + iLastSLA_Show).style.opacity = 1 - opacityCurent;

for(var i = 0; i <= 7; i++)
{
document.getElementById('ID_IMAGE_SU_' + i).style.left = mleft + 'px';
}

var division = 4;
if (IndexSLAStop == -1) division = 19;

var m_step = document.getElementById('ID_IMAGE_SU_0').offsetWidth / division;

mleft = mleft - m_step;

if (opacityCurent < 1)
{
if (IndexSLAStop == -1)
{
myTimer = window.setInterval( 'AutoOpasityShow()', 150);
}
else
{
myTimer = window.setInterval( 'AutoOpasityShow()', 10);
}
}
else
{
if (IndexSLA == IndexSLAStop) IndexSLAStop = -1;

if (IndexSLAStop == -1)
{
myTimer = window.setInterval( 'AutoZmien()', 8000);
}
else
{
myTimer = window.setInterval( 'AutoZmien()', 1);
}
}
}

function SetSlajder()
{
IndexSLA = 0;
bAutoZmien = 1;

PokazAktualny();
}

function PokazAktualny()
{
window.clearInterval(myTimer);

bAutoZmien = 1;
opacityCurent = 0;

mleft = 0;

var lp_t = (IndexSLA + 4 - 1) % 7 + 1;
for(var i = 0; i <= 7; i++)
{
var index_i = lp_t - 1;

document.getElementById('ID_IMAGE_SU_' + i).src = '' + slajdy[index_i];
document.getElementById('ID_IMAGE_SU_' + i).style.left = mleft + 'px';
document.getElementById('ID_IMAGE_SU_' + i).zd_i = index_i;

++lp_t;
if (lp_t > 7) lp_t = 1;
}

if (IndexSLAStop == -1)
{
myTimer = window.setInterval( 'AutoOpasityShow()', 150);
}
else
{
myTimer = window.setInterval( 'AutoOpasityShow()', 1);
}
}

function PRZE_IMAGE_SU(i)
{
Przeladuj(document.getElementById('ID_IMAGE_SU_' + i).zd_i);
}

