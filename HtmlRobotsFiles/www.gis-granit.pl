<br />
Wysokiej jakosci <strong>kostka granitowa</strong> do r&oacute;znych zastosowan.<br />
<br />
U nas znajdziecie Panstwo kamien naturalny w r&oacute;znych formach: <strong>kostka granitowa</strong>, <strong>plyty kamienne</strong>, <strong>plyty granitowe</strong>, <strong>krawezniki granitowe</strong>.<br />
<br />
Nasza firma swiadczy takze uslugi w zakresie:<br />
<strong><a href="http://www.brukarstwo.gis-granit.pl">brukarstwo</a></strong>, <strong><a href="http://www.brukarstwo.gis-granit.pl">roboty brukarskie</a></strong>, <strong><a href="http://www.brukarstwo.gis-granit.pl">ukladanie kostki</a></strong>.<br />
Wszelkie&nbsp;nasze prace brukarskie&nbsp; wykonywane sa z najwyzsza precyzja i dbaloscia&nbsp; o szczeg&oacute;ly.</div>
<div align="justify">&nbsp;<br />
Sp&oacute;lka Gran-Inter-Service zawiazala dzialalnosc w roku 2001. Poczatkowo jako firma handlowa prowadzila relacje handlowe w zakresie wyrob&oacute;w granitowych na terenie Polski oraz Niemiec. Nabierajac doswiadczenia w sektorze budowlanym w 2004 roku rozpoczela wlasna produkcje rozszerzajac sw&oacute;j zasieg sprzedazy o kraje Europy zachodniej, cieszac sie coraz to wiekszym uznaniem. Obecnie jako jeden z liczacych sie producent&oacute;w wytwarza wyroby z granitu szarego i szaro-rudego srednioziarnistego (Strzegomskiego), czarnego (Sjenit, Szwed) i czerwonego (Impala, Vanga).&nbsp;<br />
<br />
Jestesmy jedna z najszybciej rozwijajacych sie firm z branzy kamienia naturalnego. W naszym zakladzie produkujemy wyroby granitowe wykorzystywane w budownictwie drogowym oraz indywidualnym.<br />
<br />
W naszej ofercie znajduje sie:</div>
<div align="justify">
<ul type="disc">
<li><strong><a href="http://www.gis-granit.pl/kostka-granitowa/3">kostka granitowa</a></strong></li>
<li>kamien murowy</li>
<li>plyty chodnikowe</li>
<li><strong><a href="http://www.gis-granit.pl/plyty-granitowe/5">plyty granitowe</a></strong></li>
