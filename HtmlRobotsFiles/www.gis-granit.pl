<li>parapety</li>
<li>kraweznik drogowy, kraweznik uliczny, <strong><a href="http://www.gis-granit.pl/krawezniki/6">kraweznik mostowy</a></strong>(<strong><a href="http://www.gis-granit.pl/krawezniki/6">kraweznik granitowy</a></strong>)</li>
<li><strong><a href="http://www.gis-granit.pl/krawezniki/6">kraweznik kamienny</a></strong></li>
<li>architektura ogrodowa</li>
<li>krawezniki granitowe</li>
</ul>
</div>
<p align="justify">Dzieki ciaglym inwestycjom w sprzet i rozw&oacute;j zakladu jestesmy w stanie sprostac wymaganiom naszych klient&oacute;w pod wzgledem asortymentowym, ilosciowym oraz jakosciowym.<br />
Ciagle zmieniajacy sie rynek zmusza nas do poszukiwania nowych inspiracji. Poszerzajac charakter dzialalnosci o wykonastwo w zakresie rob&oacute;t brukarskich wyszlismy ku oczekiwaniom odbiorc&oacute;w zapewniajac im wysoka jakosc uslug.<br />
<br />
Wychodzac naprzeciw logistycznym potrzebom, w szczeg&oacute;lnosci terminowosci w dostawach sp&oacute;lka zainwestowala w park transportowy, przystosowany do przewozenia material&oacute;w sypkich oraz na paletach.<br />
<br />
Znaczaca data w historii firmy jest rok 2006. Rozszerzajac swoje horyzonty biznesowe sp&oacute;lka podjela wsp&oacute;lprace z koncernem Stiebel Getriebebau GmbH (Niemcy), wiodacym i cenionym na calym swiecie producentem zespol&oacute;w napedowych, stosowanych w przenosnikach tasmowych. Szybka ekspansja produkt&oacute;w Stiebel na polskim rynku spowodowala koniecznosc stworzenia biura handlowego w Jaworznie oraz zatrudnienia os&oacute;b o wysokich kwalifikacjach celem stawienia czola wyzwaniu. Obecnie jako wylaczny przedstawiciel produkt&oacute;w Stiebel znalezlismy zaufanie w najwiekszych grupach kapitalowych skupiajacych producent&oacute;w kruszyw, betonu, czy cementu. Jako dostawcy niezawodnych rozwiazan cieszymy sie ogromnym autorytetem. Posiadamy wlasny pion serwisowy.<br />
<br />
Szybki rozw&oacute;j sektora przekladni spowodowal zainteresowanie innych producent&oacute;w zespol&oacute;w napedowych. I tak w roku 2007 Gran-Inter-Service podpisal umowe o wylacznosci z innym producentem przekladni firma Bockwoldt Getriebenmotorenwerk GmbH (Niemcy) jako suplement programu dostaw.<br />
<br />
Uzupelnieniem powyzszej konstelacji bylo zawarcie umowy o partnerstwie z firma Precismeca Montan nalezacej do grupy Rulmeca Group, swiatowego lidera w produkcji rolek do przenosnik&oacute;w tasmowych.<br />
<br />
W niedalekiej perspektywie planuje sie dalsze inwestycje w tym segmencie dzialalnosci, miedzy innymi zwiekszenie magazynu. Ponadto trwaja rozmowy z zarzadem Stiebel celem finalizacji stworzenia przez Gran-Inter-Service skladu logistycznego dla kraj&oacute;w Europy wschodniej, takie jak Slowacja, Czechy, Rumunia, Bulgaria, Litwa, Estonia Ukraina, co rozszerzy kompetencje GIS do rangi centrum zaopatrzenia w zespoly napedowe dla Europy wschodniej produkt&oacute;w Stiebel. Projekt ma wejsc w zycie w 2009 roku.</p>
