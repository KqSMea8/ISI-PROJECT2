<h1 class="entry-title">Oferta</h1>	</header><!-- .entry-header -->

<div class="entry-content">
<p style="text-align: center;"><a href="http://www.bliskiekadry.pl/wp-content/uploads/2018/09/Bez-nazwy-2.jpg"><img class="alignnone size-medium wp-image-85" src="http://www.bliskiekadry.pl/wp-content/uploads/2018/09/Bez-nazwy-2-300x300.jpg" alt="" width="300" height="300" srcset="http://www.bliskiekadry.pl/wp-content/uploads/2018/09/Bez-nazwy-2-300x300.jpg 300w, http://www.bliskiekadry.pl/wp-content/uploads/2018/09/Bez-nazwy-2-150x150.jpg 150w, http://www.bliskiekadry.pl/wp-content/uploads/2018/09/Bez-nazwy-2-768x768.jpg 768w, http://www.bliskiekadry.pl/wp-content/uploads/2018/09/Bez-nazwy-2-1024x1024.jpg 1024w" sizes="(max-width: 300px) 100vw, 300px" /></a></p>
<p style="text-align: center;">Witaj!</p>
<p style="text-align: center;">Bardzo sie ciesze, ze tu jestes.</p>
<p style="text-align: center;">Mam nadzieje, ze chwilke zostaniesz, by moc obejrzec moje kadry.</p>
<p style="text-align: center;">Wiedz, ze tworze je z prawdziwa pasja, bez pospiechu,</p>
<p style="text-align: center;">tak byscie czuli sie komfortowo w moim i swoim towarzystwie,</p>
<p style="text-align: center;">by kazdy kto pozniej obejrzy gotowe fotografie widzial na nich uczucie,</p>
<p style="text-align: center;">bliskosc, radosc wspolnych chwil, przyjazn, milosc i szczescie.</p>
<p style="text-align: center;">Zlapmy razem bliski kadr, taki ktory zapiszemy na zawsze,</p>
<p style="text-align: center;">do ktorego z usmiechem na twarzy bedziecie wracac, ktory kiedys obejrzycie z lezka wzruszenia.</p>
<p style="text-align: center;">Fotografia zajmuje sie od 10 lat.</p>
<p style="text-align: center;">Wykonuje wylacznie sesje rodzinne, narzeczenskie, noworodkowe, dzieci, ciazowe oraz sesje dla par.</p>
<p style="text-align: center;">Moga one odbyc sie w Waszym domu albo w plenerze.</p>
<p style="text-align: center;">Bez problemu wylapie Wasza bliskosc, bez pozowania.</p>
<p style="text-align: center;">Zapraszam Was serdecznie!</p>
<p style="text-align: center;">Zdjecia oddaje w formie elektronicznej,</p>
<p style="text-align: center;">a jesli bedziecie zainteresowani to takze w formie odbitek i albumow.</p>
