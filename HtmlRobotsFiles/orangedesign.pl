<div class="ksiazki-readMore-tytul"><h1>Co wyroznia ksiazki skladane przeze mnie?</h1></div>
<div class="slide-ksiazki-readMore-content">
<div class="slide-ksiazki-readMore-teksty-lewe">
<div class="centrowanie-pion-parent">
<p>Ksiazki skladam zgodnie z&nbsp;zasadami polskiej typografii obejmujacej szeroki wachlarz zagadnien z&nbsp;zakresu operowania liternictwem, projektowania stronic i&nbsp;wszelkich elementow, z&nbsp;ktorych sklada sie ksiazka.</p><br/>
<p>Wiem, jak traktowac przypisy, tabele, zywe paginy, strony tytulowe. Rowniez bibliografie, cytaty, indeksy i&nbsp;spisy tresci nie maja przede mna tajemnic.</p><br/>
<p>Publikacje ksiazkowe wykonane przeze mnie odznaczaja sie estetycznym i&nbsp;przejrzystym wygladem, a&nbsp;sklad tekstu jest precyzyjny i&nbsp;jednolity.</p>
