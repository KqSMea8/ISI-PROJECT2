<p>Wiem, jak wazna przy pracy z&nbsp;tekstem jest redakcja i&nbsp;korekta. Dlatego podchodze do nich z&nbsp;wyjatkowa uwaga i&nbsp;starannoscia.</p><br/><br/>
<p>Znajomosc procesow poligraficznych pozwala mi w&nbsp;prawidlowy sposob przygotowac ksiazke do druku - pod katem wiernosci kolorow, oprawy, formatu, zastosowanego papieru.</p>
