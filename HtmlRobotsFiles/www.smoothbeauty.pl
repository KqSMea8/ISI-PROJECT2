<h4>O Smooth Beauty Institute</h4>
<br />
<h5>Piekno jest w kazdym z nas, trzeba tylko je odkryc.</h5>
<br />
<p style="text-align: center;">Smooth Beauty Institute to miejsce, kt&oacute;re powstalo z pasji i zaangazowania do kosmetologii. Naszym celem jest dbanie o najwyzsza jakosc uslug i indywidualne podejscie do klienta. Poniewaz to czlowiek i jego potrzeby sa dla nas najwazniejsze.</p>
<p style="text-align: center;">W swojej pracy cenie perfekcjonizm i jakosc wykonywanych uslug. W depilacji specjalizuje sie od <strong><em>2003 roku</em></strong>, stale podnoszac swoje kwalifikacje. Dzieki wieloletniemu doswiadczeniu w branzy kosmetycznej gladka i zadbana sk&oacute;ra nie ma przede mna zadnych tajemnic.</p>
<p style="text-align: center;">W trosce o <em><strong>komfort moich Klient&oacute;w</strong></em> i jakosc wykonywanych zabieg&oacute;w kosmetycznych pracuje wylacznie z wykorzystaniem sprawdzonych i renomowanych marek kosmetyk&oacute;w profesjonalnych. Dzieki temu z moich uslug moga korzystac takze osoby z alergiami i sk&oacute;ra wrazliwa, z problemami naczyniowymi konczyn dolnych oraz kobiety w ciazy.</p>
<p style="text-align: center;">Specjalizuje sie w <em><strong>depilacji pasta cukrowa</strong></em>, czyli najbardziej naturalnej i skutecznej metodzie usuwania owlosienia, zapewniajac swoim Klientom komfort, jakim jest piekna i gladka sk&oacute;ra, bez podraznien i zaczerwienienia. Jest to takze najdelikatniejsza i najmniej bolesna forma depilacji. Naturalne skladniki pasty cukrowej nie tylko pozostawiaja sk&oacute;re gladka, ale takze odzywiona i zdrowa.</p>
<p style="text-align: center;">W swojej ofercie mam r&oacute;wniez najpopularniejsza metode usuwania wlosk&oacute;w, czyli <strong><em>depilacje woskiem</em></strong>, kt&oacute;ra polecana jest do wiekszych obszar&oacute;w ciala, jak nogi czy plecy. Do jej najwiekszych zalet nalezy bez watpienia cena i szybkosc wykonania zabiegu, a takze jego dokladnosc i efektywnosc &ndash; jedna wizyta zapewnia gladka sk&oacute;re nawet na cztery tygodnie.</p>
<p style="text-align: center;">Stawiajac na rozw&oacute;j i podnoszenie kwalifikacji zawodowych, dokladam wszelkich staran, aby zapewnic Panstwu najlepsza obsluge i zabiegi wykonane na najwyzszym poziomie. R&oacute;wnie wazna jest dla mnie przyjazna atmosfera i zadowolenie Klient&oacute;w.</p>
