<p style="text-align: justify;">Sk&oacute;ra jest mocna, ochronna powloka, kt&oacute;ra zabezpiecza cialo czlowieka przed dzialaniem niekorzystnych czynnik&oacute;w zewnetrznych. Chroni przed nadmiernym wychlodzeniem i przegrzaniem, zabezpiecza przed czynnikami chorobotw&oacute;rczymi, szczelnie izoluje wnetrze organizmu przed zagrozeniami plynacymi ze swiata zewnetrznego. Jest elastyczna, latwo sie rozciaga, kiedy przybywamy na wadze, ale r&oacute;wnie szybko powraca do pierwotnego stanu. Jej wyglad wskazuje na og&oacute;lna kondycje organizmu. Zdrowa jest gladka, dobrze nawilzona i napieta, ale niezwykle latwo to zmienic. <strong>Przesuszona sk&oacute;ra to najczesciej pojawiajacy sie problem.</strong> Zwykle jest to wynik nieprawidlowej pielegnacji, uzywania niewlasciwych kosmetyk&oacute;w, wysuszajacych mydel i innych, niekorzystnych czynnik&oacute;w. Sk&oacute;ra traci w&oacute;wczas swoja ochronna warstwe lipidowa, staje sie zaczerwieniona i wrazliwa. <strong>Sucha sk&oacute;ra ma tendencje do infekcji</strong> i szybciej zaczynaja w niej zachodzic procesy starzenia. Brak bowiem wlasciwego jej nawilzenia znaczaco zmniejsza zdolnosc do wiazania wody w tkankach, a co za tym idzie stopniowej utraty elastycznosci i jedrnosci. Nadmiernie przesuszona sk&oacute;ra staje sie cienka i malo sprezysta. Tylko szybko podjete dzialania i zmiana nawyk&oacute;w pielegnacyjnych sa w stanie poprawic jej kondycje. Wysuszona sk&oacute;ra to czesto efekt stosowania niekt&oacute;rych lek&oacute;w czy toczacych sie w organizmie proces&oacute;w chorobowych. Choroby sk&oacute;rne, alergie czy inne przewlekle schorzenia czesto manifestuja zmianami sk&oacute;rnymi. Pojawic sie moze szorstka sk&oacute;ra, swiad czy nieefektowne przebarwienia, kt&oacute;re jednoznacznie pokazuja, ze w organizmie dzieje sie cos niepokojacego.</p>
