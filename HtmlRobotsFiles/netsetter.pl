<a href="http://netsetter.pl/?p=31" title="Zapalenie dziasel &#8211; przyczyny, objawy i leczenie">
<img width="520" height="270" src="http://netsetter.pl/wp-content/uploads/2017/05/listerine-na-bakterie-w-jamie-ustnej-1.jpg" class="attachment-post-thumbnail size-post-thumbnail wp-post-image" alt="" srcset="http://netsetter.pl/wp-content/uploads/2017/05/listerine-na-bakterie-w-jamie-ustnej-1.jpg 520w, http://netsetter.pl/wp-content/uploads/2017/05/listerine-na-bakterie-w-jamie-ustnej-1-300x156.jpg 300w" sizes="(max-width: 520px) 100vw, 520px" />								</a>

<p>Zapalenie dziasel jest jedna z powazniejszych dolegliwosci jamy ustnej, wymieniana obok nadwrazliwosci zebow i chorob prochniczych. Choroby dziasel tego typu moga dotknac pacjentow w kazdym wieku, ze wzgledu na brak utrzymania dostatecznej codziennej higieny jamy ustnej, przyjmowane leki badz kompleksowe schorzenia calego organizmu.<br />
XXX<br />
Jak wyglada zapalenie dziasel?<br />
Na niedokladnie wymytych zebach moga odkladac sie bakterie, ktore razem ze slina i resztkami jedzenia moga tworzyc mineralizujaca sie plytke nazebna, a nastepnie porowaty kamien wnikajacy pod dziasla i niszczacy konstrukcje utrzymujace zeby w szczece i zuchwie. Tym samym zeby zaczynaja sie chwiac i wypadac. Zapalenie dziasel moze pojawiac sie nawet u dzieci w okresie ich dojrzewania, choc choroba ta jest powszechniejsza u osob po 35 roku zycia. Moze tez czesto pojawiac sie u kobiet w czasie zmian hormonalnych zwiazanych z ciaza lub w okresie menopauzy.<br />
XXX<br />
Objawy zapalenia dziasel<br />
Objawy wskazujace na zapalenie dziasel widoczne sa juz po okolo 3-4 tygodniach od pojawienia sie tej choroby. Niepokojacym objawem moze byc uczucie rozchwiania sie zebow oraz krwawienie pojawiajace sie podczas szczotkowania zebow, nadwrazliwosc szyjek zebowych na zmiany temperatury, a takze nieprzyjemny zapach w ustach i uczucie suchosci.<br />
XXX<br />
Zapalenie dziasel - sposoby leczenia<br />
Aby zachowac zdrowe zeby i zdrowe dziasla, nalezy od razu po zauwazeniu niepokojacych objawow udac sie do dentysty, ktory zaleci wybor dalszej drogi leczenia. Pierwszym jej etapem jest zazwyczaj usuniecie kamienia nazebnego i osadu. Dlatego tez tak wazne do niedopuszczenia zapalenia dziasel jest regularne odwiedzanie dentysty, ktory moze usunac kamien nazebny i ocenic stan naszych dziasel. Jesli ich stan ogolny nie jest dramatyczny, leczenie moze zakonczyc sie na jednej wizycie. Jesli mamy do czynienia z sytuacja powazniejsza - konieczne moze byc dalsze badanie, np. krwi, wykluczajace cukrzyce, niedobor witamin czy mineralow. W ekstremalnych przypadkach lekarz moze zalecic podawanie antybiotykow i preparatow podnoszacych odpornosc organizmu lub zapisac zel do samodzielnego nakladania na zeby.</p>
