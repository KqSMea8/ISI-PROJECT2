<img width="907" height="190" src="http://netsetter.pl/wp-content/uploads/2017/05/listerine.png" class="attachment-post-thumbnail size-post-thumbnail wp-post-image" alt="" srcset="http://netsetter.pl/wp-content/uploads/2017/05/listerine.png 907w, http://netsetter.pl/wp-content/uploads/2017/05/listerine-300x63.png 300w, http://netsetter.pl/wp-content/uploads/2017/05/listerine-768x161.png 768w" sizes="(max-width: 907px) 100vw, 907px" />								</a>

<p>Pierwsze zeby mleczne pojawiaja sie okolo szostego miesiaca zycia. Zeby stale zadomawiaja sie w naszych jamach ustnych okolo 7 roku zycia. Pojawienie sie zebow stalych oznacza, ze beda one nam towarzyszyly przez wiekszosc zycia. W obecnym spoleczenstwie zeby oprocz ich pierwotnego zadania, a mianowicie rozdrabniania pozywienia zaczely pelnic dodatkowa spoleczna role. Zdrowe zeby swiadcza o ich posiadaczu, piekny usmiech stal sie obecnie wizytowka czlowieka dlatego tak wazne jest otoczenie ich nalezyta opieka. Najprostsza, a jednak najbardziej efektywna forma dbania o zeby jest systematyczne dokladne szczotkowanie. Nic nie moze rownac sie z tym mechanicznym sposobem oczyszczania zebow z resztek pozywienia. Nieodzowna w walce o piekny usmiech okaze sie takze nic dentystyczna pozwalajaca dotrzec w trudno dostepne miejsca. Wazne jest, aby wyrobic nawyk mycia zebow i uzywania nici dentystycznej u naszych pociech, takie przyzwyczajenie bedzie im towarzyszylo przez cale zycie, a my zaoszczedzimy niepotrzebnych wydatkow na dentyste. Pomimo powszechnej swiadomosci tego jak wazne jest dbanie o zeby wielu doroslych zapomina o swoich zebach. Tutaj sojusznikiem okazuja sie byc elektryczne szczoteczki do zebow. Okazuje sie, ze wiele osob chetniej myje zeby posiadajac ten stosunkowo niedrogi gadzet. Jesli mycie zebow jest dla Ciebie uciazliwe, moze warto byloby sie zastanowic czy nie kupic takiej szczoteczki. Zaawansowane, ale i kosztowne techniki leczenia pozwalaja nam na ratowanie wiekszosci zebow. Nie znajdziemy jednak nieprawidlowosci w jamie ustnej bez wizyt kontrolnych. Powinnismy sie na nie wybierac raz na pol roku. Moge zagwarantowac, ze szybciej zdiagnozowana choroba uzebienia jest latwiejsza w leczeniu, wymaga mniejszego nakladu pieniedzy, a przede wszystkim pozwala w wiekszosci przypadkow zachowac wlasnego zeba nie narazajac go na ryzyko koniecznosci usuniecia.<br />
Niektorych z nas moze dotykac nadwrazliwosc zebow. Czym jest i jak z nia walczyc? Jest to objawiajaca sie bolem dolegliwosc w wyniku dzialania na zeby niskiej lub wysokiej temperatury, substancji o kwasnym pH badz tez duzego stezenia np. cukru lub soli w jamie ustnej. Przyczyna powstawania bolu sa odsloniete kanaliki zebinowe. W naturalny sposob sa one chronione przez szkliwo i dziasla. Jesli ta naturalna ochrona zostanie w jakis sposob zaburzona pojawia sie bol. W wypadku pojawienia sie takich dolegliwosci nalezy zastosowac odpowiednie produkty lecznicze w postaci fluorkowego zelu do nakladania na zeby. Sa dostepne takze specjalne pasty o zmniejszonej ilosci skladnikow sciernych, aby dodatkowo nie niszczyc szkliwa. Mowilismy przed chwila, ze dziasla rowniez chronia kanaliki zebinowe przed wplywem czynnikow drazniacych.  Brak higieny jamy ustnej prowadzi do nadmiernego nagromadzenia sie plytki nazebnej, ktora w wyniku mineralizacji zmienia sie w kamien nazebny. Bytujace w tym obszarze bakterie wywoluja zapalenie dziasel. To z kolei, gdy nadmiernie sie rozwinie prowadzi do stanu zapalnego przyzebia, a jednym z jego objawow jest cofanie sie dziasel i odsloniecie szyjki zebow. Leczenie lagodnych odmian tej przypadlosci polega na systematycznym usuwaniu kamienia nazebnego i dbaniu o higiene jamy ustnej. W ten oto sposob wrocilismy do poczatku, a mianowicie codziennej systematycznej higieny jamy ustnej. W przypadku zapalenia przyzebia zwanego potocznie parodontoza doskonalym sprzymierzencem moga okazac sie specjalne pasty.<br />
Nawet w XXI wieku wiele ludzi pomimo rozwoju techniki i medycyny nadal zmaga sie z chorobami zebow. Najczestsza ich przyczyna jest nieprzykladanie wagi do higieny jamy ustnej. Po co leczyc skoro w tak prosty sposob mozemy zapobiegac chorobom zebow.</p>
