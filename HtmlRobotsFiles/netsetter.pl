<a href="http://netsetter.pl/?p=17" title="Czuj sie swietnie w kazdy dzien miesiaca!">
<img width="98" height="60" src="http://netsetter.pl/wp-content/uploads/2016/12/dojrzewanie-z-ob.png" class="attachment-post-thumbnail size-post-thumbnail wp-post-image" alt="" />								</a>

<p>Bycie kobieta w te dni wcale nie musi byc trudne i uciazliwe. Wszystko zalezy wylacznie od Twojego nastawienia i przygotowania. Ponizej prezentujemy dla Ciebie kr&oacute;tka instrukcje, co zrobic, by okres nie byl czyms nieprzyjemnym.</p>
<p><strong>Zaplecze techniczne</strong></p>
<p>Przede wszystkim, aby m&oacute;c niczym sie nie przejmowac, musisz zadbac o kilka aspekt&oacute;w technicznych. Skompletuj kilka zestaw&oacute;w &#8222;Przygotowanej Na Wszystko Kobiety&#8221; i wsadz je do kazdej swojej torebki. Na taki zestaw powinny sie skladac: tampony O.B., sprawdzone tabletki przeciwb&oacute;lowe oraz wkladka higieniczna. Dzieki temu nie dasz sie zaskoczyc naglym niespodziankom, a moze takze uratujesz kolezanke w potrzebie.</p>
<p>Tampony powinny byc w r&oacute;znych rozmiarach &#8211; takich, jak uzywasz. Pamietaj, ze O.B. doskonale dopasowuja sie do ksztalt&oacute;w kobiecego ciala i sa przystosowane do r&oacute;znej obfitosci krwawienia. Nie stosuj tampon&oacute;w wiekszych, niz potrzebujesz w konkretnym dniu okresu.</p>
<p><strong>Pysznosci!</strong></p>
<p>Nic nie poprawia humoru tak, jak cos dobrego do jedzenia. W czasie okresu pamietaj, aby dbac o swoja diete. Nie odchudzaj sie, nie drecz potrawami, na kt&oacute;re nie masz ochoty. Tw&oacute;j organizm dokladnie Ci m&oacute;wi, czego potrzebuje. Posluchaj go. Wiele kobiet m&oacute;wi, ze w czasie okresu pomocna okazuje sie czekolada &#8211; nic dziwnego! Naturalnie pobudza produkcje endorfin, a te poprawiaja humor.</p>
<p><strong> Rozpieszczaj sie</strong></p>
<p>To naturalne, ze w trakcie okresu mozesz sie czuc smutna, zdenerwowana i zmeczona. Mozesz miec tez zmienne nastroje. Pozw&oacute;l sobie na nie &#8211; jesli masz ochote sobie poplakac, to po prostu to zr&oacute;b. Porozmawiaj o tym z przyjaci&oacute;lka czy inna bliska kobieta &#8211; zapewniamy, ze odczuwaja podobne dolegliwosci w czasie okresu.</p>
<p><strong>R&oacute;b rzeczy mile i przyjemne</strong></p>
<p>Niezaleznie od tego, jakie jest Twoje hobby, nie ma przeciwwskazan, abys realizowala je takze w trakcie okresu. Dzieki tamponom o.b. mozesz plywac, jezdzic konno i na rowerze, skakac, tanczyc i absolutnie niczym sie nie przejmowac.</p>
<p><strong>Wysypiaj sie</strong></p>
<p>Okres to nie choroba, ale wplywa na Twoje samopoczucie. Dlatego waznym jest, abys pozwalala organizmowi wypoczac. Dzieki tamponom o.b. mozesz spokojnie przespac noc, nie martwiac sie o wstydliwe plamy.</p>
