<h1 class="entry-title">O NAS</h1>	</header><!-- .entry-header -->

<div class="entry-content">
<p>Sklad osobowy Zarzadu Kola KSS KOMUNALNI w Ozorkowie od 2015 r.:<br />
Prezes Kola &#8211; Krzysztof Gawronski<br />
