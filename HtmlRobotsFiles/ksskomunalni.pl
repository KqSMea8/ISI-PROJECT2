<p>&nbsp;</p>
<p><span style="font-size: medium;">Sklad osobowy Zarzadu Kola KSS KOMUNALNI w Ozorkowie od 2011 r.:<br />
Prezes Kola &#8211; Krzysztof Gawronski<br />
Sekretarz &#8211; Tomasz Komorowski<br />
Skarbnik &#8211; Dariusz Stanalowski<br />
</span></p>
<hr />
<div>
<p><strong>Kolo Strzelectwa Sportowego ,,Komunalni"</strong></p>
<p><strong>Krotki rys historyczny.</strong></p>
<p>Kolo Strzelectwa Sportowego w Ozorkowie zostalo powolane do zycia Zarzadzeniem Nr 10 Dyrektora Biura Zarzadu Wojewodzkiego Ligi Obrony Kraju w Lodzi z dnia 9 czerwca 2004 roku.</p>
<p>Na podstawie powyzszego Zarzadzenia, nowy podmiot statutowy na swym pierwszym zebraniu zalozycielskim przyjal nazwe KSS ,,Komunalni" z siedziba w Ozorkowie przy ul. Zwirki 30.</p>
<p>Lista czlonkow zalozycieli:</p>
<ol type="1">
<li>Krzysztof Gawronski,</li>
<li>Piotr Gizinski,</li>
<li>Marek Sobor,</li>
<li>Dariusz Piotr Stanalowski,</li>
<li>Zbigniew Garczynski,</li>
<li>Bohdan Korzycki,</li>
<li>Jerzy Oleski,</li>
<li>Tomasz Komorowski,</li>
<li>Andrzej Jasinski.</li>
</ol>
<p>Na pierwszym zebraniu ukonstytuowal sie Zarzad Kola w nast. skladzie:</p>
<ol type="1">
<li>Krzysztof Gawronski-Prezes,</li>
<li>Marek Sobor-Sekretarz,</li>
<li>Dariusz Stanalowski-Skarbnik.</li>
</ol>
<p>W dniu 28 czerwca 2004 roku  zostalo zatwierdzone pomieszczenie przeznaczone na magazyn broni oraz amunicji dla potrzeb naszego Kola.</p>
<p>Wlasciwa dzialalnosc treningowa i zawodnicza Kolo Strzelectwa Sportowego rozpoczelo wiosna 2004 roku w momencie oddania do uzytku strzelnicy polozonej w Lasku Miejskim w Ozorkowie.</p>
<p>Na podstawie umowy zawartej z wlascicielem obiektu, ktorym jest Komenda Wojewodzka Policji w Lodzi, Kolo nasze moze prowadzic swoja dzialalnosc we wszystkie weekendy w roku. W pozostale dni, strzelnica pozostaje w dyspozycji KWP w Lodzi.</p>
