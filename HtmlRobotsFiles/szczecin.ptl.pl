<h1>Witamy na stronie glownej</h1>


</section>
<section class="article-text">
<div class="row">
<div class="col-sm-8 col-md-8">
<div class="cushycms"><div style="text-align: justify;"> <div style="text-align: justify;">Poczatki Polskiego Towarzystwa Lesnego siegaja roku <strong>1882</strong>, kiedy to we Lwowie odbyl sie I Walny Zjazd Galicyjskiego Towarzystwa Lesnego. Pod nazwa obecna Towarzystwo istnieje od <strong>1925</strong> roku. II Wojna Swiatowa przerwala jego dzialalnosc. Wznowiono ja w roku 1946, a w rok pozniej ukazal sie kolejny numer <strong>Sylwana- wychodzacego od 1820 roku, najstarszego pisma lesnego w Polsce i jednego z najstarszych tego typu na swiecie</strong>.<br /> <br /> Dzialalnosc Polskiego Towarzystwa Lesnego nalezy do chlubnych kart historii naszego lesnictwa. Towarzystwo bylo obecne we wszystkich momentach waznych dla rozwoju i ksztaltu gospodarki lesnej, wspieralo ja wiedza i doswiadczeniem swoich czlonkow. Tak jest rowniez i teraz. W szeregach Towarzystwa dzialaja lesnicy- naukowcy i praktycy. Czlonkami PTL moga byc rowniez, i sa, ludzie zainteresowani lasem, lecz nie zwiazani z nim zawodowo. Do Polskiego Towarzystwa Lesnego naleza oprocz lesnikow reprezentanci innych zawodow. Szeregi Towarzystwa otwarte sa dla kazdego, komu nie jest obojetny los i stan polskich lasow.<br /> <br /> Jednym z  podstawowych zadan Towarzystwa jest<strong> inicjowanie badan naukowych na potrzeby lesnictwa oraz powiazanie nauki z praktyka</strong> czyli efektywne wdrazanie najcenniejszych rozwiazan do praktyki. Polskie Towarzystwo Lesne bierze czynny udzial w toczacych sie dyskusjach nad przyszlym ksztaltem gospodarki lesnej, m.in. W opracowywaniu zalozen strategii rozwoju lasow, zalozen polityki lesnej i sposobow ochrony zasobow lesnych.<br /> <br /> Innym priorytetowym zadaniem jest <strong>popularyzacja wiedzy lesnej, zarowno wsrod samych lesnikow, jak i w calym spoleczenstwie</strong>. Tradycyjna forma tej dzialalnosci sa wyklady, odczyty, zebrania dyskusyjne, konferencje i wydawnictwa poswiecone roznorodnym problemom lasow i lesnictwa. <strong>Centralnym punktem dzialalnosci wydawniczej Towarzystwa jest czasopismo ,,Sylwan"</strong>. Staraniem PTL wydawane sa rowniez inne opracowania i materialy zwiazane z lesnictwem.<br /> <br /> Polskie Towarzystwo Lesne rozwija wspolprace z podobnymi organizacjami za granica, uczestniczy w wymianie mysli i doswiadczen miedzy osrodkami naukowymi oraz reprezentuje polskie lesnictwo za granica. PTL od wielu lat jest <strong>czlonkiem Miedzynarodowej Unii Ochrony Przyrody (IUCN)</strong>.<br /> Towarzystwo stoi na strazy kultury i tradycji polskiego lesnictwa oraz jednoczy srodowisko lesnikow  w calym kraju.</div>
<p><br /> Zachecamy wszystkich lesnikow i przyjaciol lasu do wstepowania w szeregi czlonkow Polskiego Towarzystwa Lesnego.</p></div>
