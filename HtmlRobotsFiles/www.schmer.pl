<h3><a href="/konfigurator-felg" >Felgi</a>
</h3>
<p>
<img alt="Felgi aluminiowe" src="/schmer/media/image/wire_wheels.jpg" /><br />
<br />
Ekskluzywne felgi aluminiowe przeznaczone do samochod&oacute;w osobowych w rozmiarach od 14 do 24 cali. Alufelgi kt&oacute;re oferujemy, to produkty jakosciowo najlepsze sposr&oacute;d felg dostepnych na calym swiecie, wyprodukowane przez renowoane firmy specjalizujace sie w produkcji felg aluminiowych i chromowanych. Jakos i precyzja wykonania to charakterystyczne cechy towar&oacute;w sprzedawanych w naszym sklepie. Dokonujac zakupu na naszej stronie masz pewnosc, ze Tw&oacute;j samoch&oacute;d otrzyma bezpieczne felgi wysokiej jakosci, kt&oacute;re swoim wygladem beda cieszyly wzrok przez dlugie lata. W ofercie dostepne sa <a href="http://www.gtr.szczecin.pl">felgi</a> producent&oacute;w takich jak: ATS, AEZ, AZEV, BREYTON, CMS, DOTZ, DEZENT, ENZO, KESKIN, MAM, PROLINE, RH, RIAL, RONAL. Wiekszosc felg z naszej oferty posiada certfikat TUV, kt&oacute;re potwierdza wysoka jakosci wykonania.<br />
<br />
&nbsp;</p>
<h3><a href="/promocje?typ=akcesoria" >Akcesoria</a>
</h3>
<p>
<img alt="Alufelgi - akcesoria" src="/schmer/media/image/akcesoria_main.jpg" /><br />
<br />
Oferujemy r&oacute;wniez r&oacute;zne akcesoria samochodowe zwiazane z felgami i oponami. Mamy tutaj na mysli dystanse, kt&oacute;re sluza do zwiekszenia rozstawu k&oacute;l w samochodzie, dzieki czemu auto lepiej zachowuje sie podczas jazdy w tak zwanych szybkich zakretach. Pierscienie centrujace kt&oacute;re sa pomocne przy dopasowaniu i wycentrowaniu felgi na piascie. Wentyle chromowane i ukryte, kt&oacute;re opr&oacute;cz swojej wlasciwej funkcji pelnia r&oacute;wniez ozdobna. Sruby do felg aluminiowych, ich wlasciwe dobranie odpowiada za odpowiednie przymocowanie felgi do piasty, sruby takie maja r&oacute;zne kolnierze, odpowiednie do otworu, jaki wystepuje w feldze.</p>
