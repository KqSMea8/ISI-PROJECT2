<h3><a href="/tanie-opony" >Opony</a>
</h3>
<p>
<img alt="Opony letnie i zimowe" src="/schmer/media/image/opony.jpg" /><br />
<br />
W ofercie posiadamy r&oacute;wniez opony przeznaczone do samochod&oacute;w osobowych, terenowych i dostawczych. Wkr&oacute;tce nasza oferte rozszerzymy o opony do samochod&oacute;w ciezarowych. Szeroki wyb&oacute;r opon zimowych i letnich w r&oacute;znych rozmiarach z rantem ochronnym na felge lub bez. Ogumienie pochodzace od znanych producent&oacute;w takich jak Brigstone, Continental, Dunlop, Goodyear, Michelin, Nokian, Pirelli, Toyo czy Vredestein. Nabywajac opony u nas masz pewnosc, ze dostajesz towar nowy, kt&oacute;ry nie lezal w magazynie kilka lat. Pamietaj, ze na oponach nie warto oszczedzac, to od nich zalezy bezpieczenstwo Twoje i podr&oacute;zujacych z Toba os&oacute;b a wiec zaufaj profesjonalistom i rozwaz mozliwosc zakupu opon u nas.<br />
&nbsp;</p>
