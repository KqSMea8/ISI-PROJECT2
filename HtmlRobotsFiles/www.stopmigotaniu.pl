<h2>O FUNDACJI</h2>
<br/>
Fundacja Udaru Mozgu powstala w 2009 roku, aby zwrocic<br/>
uwage na wszystkie problemy zwiazane z udarem mozgu,<br/>
oraz osob po udarze znajdujacych sie w trudnej sytuacji.<br/>
<br/>
Posiadamy statut Organizacji Pozytku Publicznego. <br/>
Pomagamy osobom po udarach, wypadkach komunikacyjnych,<br/>
osobom chorujacym na stwardnienie rozsiane,<br/>
mozgowe porazenie dzieciece i wiele innnych schorzen<br/>
o charakterze neurologicznym. <br/>
<br/>
Dzialalnosc Fundacji to takze profilaktyka.<br/>
W tym celu organizujemy kampanie spoleczne, <br/>
wspolorganizujemy liczne konferencje i szkolenia<br/>
na terenie calego kraju. Nasze publikacje na temat udaru,<br/>
jego skutkow i profilaktyki sa cenym zrodlem wiedzy <br/>
dla ogolu spoleczenstwa. <br/>

