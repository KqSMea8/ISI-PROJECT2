<h2>O KAMPANII</h2>
<br/>
Kampania Stop Migotaniu jest poswiecona podniesieniu <br/>
wiedzy na temat migotania przedsionkow i jego konsekwencji. <br/>
Stop Migotaniu to polska wersja miedzynarodowej akcji <br/>
Sign Against Stroke .<br/>
<br/>
Pragniemy zapoznac polskie spoleczenstwo z ogromnym ryzykiem, <br/>
jakie niesie za soba nie leczone migotanie przedsionkow, <br/>
ktore moze prowadzic do udaru mozgu. W tym celu stworzylismy <br/>
strone internetowa www.stopmigotaniu.pl, broszury informacyjne<br/>
oraz spot informacvjny.<br/>
<br/>
Pacjenci z rozpoznanym migotaniem przedsionkow, <br/>
a takze z podejrzeniem choroby, dowiedza sie z nich miedzy innymi <br/>
o co warto pytac lekarza i jak sie z nim komunikowac, <br/>
aby byc zrozumianym.<br/>
<br/><br/>
<img alt="Fundacja udar mozgu" src="http://www.stopmigotaniu.pl/css/grafika/fundacja.png" />

