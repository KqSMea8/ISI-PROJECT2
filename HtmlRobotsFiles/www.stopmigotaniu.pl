<h2 style="color: #fb5050; margin-top: 5px;display: block;">OBJAWY MIGOTANIA <br/>PRZEDSIONKOW</h2>
<div class="clear"></div>

MIGOTANIE PRZEDSIONKOW MOZE PRZEBIEGAC<br/>
BEZOBJAWOWO, NATOMIAST DO CZESTO<br/>
WYSTEPUJACYCH SYMPTOMOW MOZEMY ZALICZYC:<br/>
<br/>
<span style="color: #fb5050">
- Uczucie kolatania, lub lomotania w sercu.<br/>
- Przyspieszone lub galopujace bicie serca<br/>
- Zmeczenie, brak energii<br/>
- Dusznosci<br/>
- Zawroty glowy<br/>
- Trudnosci w wykonywaniu cwiczen fizycznych
</span>
<br/><br/>
<br/>
Warto zwrocic uwage, na to ze objawy nie sa identyczne<br/>
u wszystkich osob. Badanie pulsu moze pomoc wykryc MP.<br/>
PAMIETAJ: Jesli zauwazysz u siebie jeden z powyzszych<br/>
objawow powiedz o tym swojemu lekarzowi.<br/>




