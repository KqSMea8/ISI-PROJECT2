<h2 style="margin-top: 5px;display: block;">
MIGOTANIE <br/>PRZEDSIONKOW
</h2>
<div class="clear"></div>
MIGOTANIE PRZEDSIONKOW  (MP) <br/>
JEST RODZAJEM ZABURZENIA RYTMU SERCA.<br/>
<br/>
W trakcie migotania przedsionkow serce nie bije <br/>
regularnie i we wlasciwym rytmie. Moze kolatac <br/>
i nie kurczy sie  prawidlowo. <br/>
<br/>
Kiedy serce nie pompuje prawidlowo zalega w nim krew<br/>
ktora moze tworzyc zakrzepy. Zakrzepy moga sie odrywac<br/>
i przedostawac wraz z wypompowywana krwia az do mozgu.<br/>
<br/>
W mozgu skrzeplina moze utknac w naczyniu krwionosnym<br/>
i spowodowac niedokrwienny udar mozgu.
<br/>
