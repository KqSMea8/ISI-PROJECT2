<h2 class="text-center">To jedyny program w Polsce przekazujacy najnowsze narzedzia i idee zarzadzania strategicznego. Co nas wyroznia?</h2>
<div class="visible-lg">
<div class="row">
<div class="col-md-4">
<h3 class="container-fluid-abs width">Konkretne odpowiedzi na najbardziej palace wyzwania liderow  </h3>
<img src="/images/box/zaawansowana-diagnoza-indywidualna-uczestnikow.jpg" class="img-responsive container-fluid-rv" alt="Zaawansowana diagnoza indywidualna uczestnikow">
<p><strong>Strategic Leadership Academy<sup>TM</sup> to scisle zintegrowany i praktycznym program rozwoju zaprojektowany zgodnie z najwyzszymi swiatowymi standardami <em>executive education</em></strong>. W kazdym z blokow tematycznych uczestnicy wykonuja konkretny projekt dotyczacy aktualnych zagadnien i problemow ich firm. W kazdej chwili moga takze skorzystac ze wsparcia i doswiadczenia ekspertow oraz bogatych zasobow wiedzy ICAN Institute i ,,Harvard Business Review Polska". <strong>Dodatkowym atutem jest takze roczne wsparcie po ukonczeniu programu</strong> w ramach interaktywnego portalu.  </p>
</div>
<div class="col-md-4">
<h3 class="container-fluid-abs width">Kompleksowe, praktyczne podejscie</h3>
<img src="/images/box/2.png" class="img-responsive container-fluid-rv" alt="Kompleksowe, praktyczne podejscie">
<p>Podczas programu skupiamy sie na rozwoju wszystkich niezbednych umiejetnosci lidera wplywajacych na zdobywanie przewagi konkurencyjnej i prowadzacych do stalego wzrostu biznesu. Podczas intensywnych sesji warsztatowych otrzymasz <strong>wiele konkretnych narzedzi i wskazowek, ktore pozwola Ci zwiekszyc swoje kompetencje jeszcze w trakcie trwania programu</strong>. Forma zajec pozwala ponadto na efektywna wymiane doswiadczen w gronie uczestnikow oraz na nawiazywanie nowych owocnych relacji biznesowych.  </p>
</div>
<div class="col-md-4">
<h3 class="container-fluid-abs width">Metoda ICAN Smart Learning<sup>TM</sup></h3>
<img src="/images/box/unikalna-metoda-nauczania.jpg" class="img-responsive container-fluid-rv" alt="Unikalna metoda nauczania">
<p>Podobnie jak wszystkie programy rozwojowe ICAN Institute, Strategic Leadership Academy<sup>TM</sup> wykorzystuje metode   <a href="http://ican.pl/ican-smart-learning/" target="_blank"><strong>ICAN Smart Learning<sup>TM</sup></strong></a>, zapewniajaca maksymalne korzysci edukacji liderow. Skladaja sie na nia m.in. metoda blended learning (laczaca tradycyjne formy ksztalcenia oraz prace poprzez portal wiedzy online) oraz harwardzka metoda studiow przypadkow (dostarczamy ich najwiecej na polskim rynku). <strong>Naczelnym wyroznikiem metody jest podejscie evidence-based management: przekazujemy uczestnikom wylacznie sprawdzone rozwiazania poparte badaniami skutecznosci</strong>. </p>
</div>
</div>
<div class="row">
<div class="col-md-4">
<h3 class="container-fluid-abs width">Intensywna formula, opracowana z mysla o zabieganych menedzerach</h3>
<img src="/images/box/wygodna-formula.jpg" class="img-responsive container-fluid-rv" alt="Wygodna formula">
<p>Wiemy, jak napiety jest Twoj harmonogram, dlatego Strategic Leadership Academy<sup>TM</sup> to <strong>10 dwudniowych sesji rozlozonych w ciagu dziesieciu miesiecy</strong>, odbywajacych sie w cyklach sroda-czwartek, piatek-sobota. Z wyprzedzeniem mozesz zaplanowac udzial w zajeciach, a w razie potrzeby skorzystac z opcji odrobienia danej sesji w innym terminie. Wystarczy, ze zglosisz sie do koordynatora programu, ktory juz teraz z przyjemnoscia  <a href="#">odpowie na Twoje pytania</a>. </p>
</div>
<div class="col-md-4">
<h3 class="container-fluid-abs width">Nielimitowany dostep do kompendium wiedzy i praktycznych narzedzi online</h3>
<img src="/images/box/wsparcie-miedzy-zajeciami.jpg" class="img-responsive container-fluid-rv" alt="Wsparcie miedzy zajeciami">
<p>W trakcie programu bedziesz mogl w nieograniczony sposob korzystac z zasobow <strong>Harvard Business Review Digital Learning Center</strong>. Do Twojej dyspozycji 24 godziny przez 7 dni w tygodniu beda m.in. nagrania konferencji ze swiatowymi autorytetami zarzadzania, moduly szkoleniowe ICAN Business Advisor<sup>TM</sup> oraz biblioteka wiedzy z materialami uzupelniajacymi sesje programu. </p>
</div>
<div class="col-md-4">
<h3 class="container-fluid-abs width">Pewnosc korzysci + zero ryzyka  </h3>
<img src="/images/box/unikalna-gwarancja-korzysci.jpg" class="img-responsive container-fluid-rv" alt="Unikalna gwarancja korzysci">
<p>Jesli po dwoch sesjach tematycznych (4 dni szkoleniowe) uznasz, ze Strategic Leadership Academy<sup>TM</sup> nie spelnia Twoich oczekiwan, <strong>oferujemy Ci calkowity zwrot kosztow szkolenia</strong>. Z uczestnictwem w programie nie wiaze sie wiec zadne ryzyko, a tylko mozliwosc osiagniecia wymiernych korzysci przekladajacych sie na wzrost wartosci Twojej firmy.</p>
</div>
