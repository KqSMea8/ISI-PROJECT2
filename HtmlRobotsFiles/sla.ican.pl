<div class="row">
<div class="col-sm-8"><p>Dzieki postepowi technologicznemu mozliwosci wzrostu, zwiekszania zasiegu dotarcia do klientow i zarabiania na innowacjach sa dzis nieporownywalnie wieksze, niz jeszcze kilka lat temu. <strong>Postep generuje nowe szanse, ale i wzmaga rywalizacje na rynku.</strong> Teraz na jeden nowy pomysl przypadaja setki pomyslow konkurencji, a ich realizacja bywa czesto niewiele bardziej skomplikowana, niz kilka klikniec.  </p>

