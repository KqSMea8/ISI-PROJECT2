<h2><p>O klubie</p></h2>
<p>Celem klubu jest zrzeszenie pracownikow Ratownictwa Medycznego wokol rozwoju motoryzacji, popularyzacji sportu motorowego, turystyki motorowej, propagowanie kultury motoryzacyjnej, podnoszenie umiejetnosci Czlonkow klubu w zakresie prowadzenia pojazdow motorowych oraz wspieranie bezpieczenstwa ruchu drogowego.</p>
<p>Czlonkiem klubu motorowego moze byc kazda osoba ktora pracowala/pracuje w Ratownictwie Medycznym lub Stacjach Pogotowia Ratunkowego po uzyskaniu rekomendacji dwoch czlonkow klubu. Ostateczna decyzje o przyjeciu nowego czlonka podejmuje Zarzad klubu.</p>
</article>
