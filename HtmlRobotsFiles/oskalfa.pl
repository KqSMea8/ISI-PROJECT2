</div>
<div class="panel panel-default">
<div class="panel-heading">
<h4 class="panel-title"><a data-toggle="collapse" aria-expanded="false" data-parent="#faq" href="#faq-2">Jakie dokumenty trzeba miec, aby zapisac sie na Kurs?</a></h4>
</div>
<div id="faq-2" class="panel-collapse collapse">
<div class="panel-body">
<p>Profil Kandydata na Kierowce (PKK). Wedlug nowej ustawy o kierujacych osoba, chcaca zapisac sie na kurs prawa jazdy musi zglosic sie do Wydzialu Komunikacji lub Starostwa Powiatowego odpowiedniego dla miejsca zameldowania
(u nas Zespol Obslugi Mieszkancow Gdansk-Chelm ul. Wilanowska 2 i Wrzeszcz ul. Partyzantow 74) gdzie zostanie on utworzony. Profil kierowcy stworzono w celu rejestracji kursanta w bazie danych. My jako osrodek
szkolenia po otrzymaniu od Was numeru PKK pobieramy profil elektronicznie i po zakonczeniu kursu odsylamy ta sama droga do urzedu. Wowczas mozecie zapisac sie na egzamin panstwowy.! Dzieki temu odbywa sie to
bez jakichkolwiek dokumentow ukonczenia kursu. Czas wydawania takiego dokumenty trwa od 20 minut (w Gdansku) do nawet 2 dni w zaleznosci od Miasta.</p>
<p>Zeby uzyskac PKK nalezy zlozyc 1 zdjecie do prawa jazdy, aktualne badania lekarskie, wniosek do pobrania w naszym OSK lub w Wydziale Komunikacji oraz dowod osobisty. Osoby niepelnoletnie powinny udac sie do urzedu
z rodzicem lub opiekunem.</p>
</div>
</div>
