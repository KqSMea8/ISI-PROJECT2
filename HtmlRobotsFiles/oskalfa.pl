</div>
<div id="faq-4" class="panel-collapse collapse">
<div class="panel-body">
<p>To zalezy od wielu czynnikow. Od rodzaju wybranego kursu, ale rowniez od dyspozycyjnosci Kursantow. Kursant moze miec tez wlasne tempo przyswajania wiedzy. Srednio mozna powiedziec, ze kurs prawa jazdy kat. B trwa
okolo 2 miesiace.</p>
