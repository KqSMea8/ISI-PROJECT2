<h1 class="art_title"><a href="/art/id/13">Szanowni Panstwo!</a></h1>
<br class="clear" />
<p class="art_head"></p>
<p style="text-align: justify;"><strong><span style="font-family: &#039;times new roman&#039;, times; font-size: 14px;">Przed skorzystaniem z naszej strony internetowej prosze zapoznac sie z Klauzula informacyjna stosowana w SKO w Ciechanowie dostepna w zakladce Informacje og&oacute;lne/Klauzula informacyjna.</span></strong></p>
<p style="text-align: justify;"><span style="font-family: &#039;times new roman&#039;, times; font-size: 14px;">Realizujac obowiazek wynikajacy z ustawy z dnia 06 wrzesnia 2001r. o dostepie do informacji publicznej (Dz. U. 2016.1764 j.t) zapraszamy do skorzystania z <strong>Biuletynu Informacji Publicznej SKO w Ciechanowie</strong>. Strona BIP spelnia wymagania jakie naklada wyzej wymieniona Ustawa oraz Rozporzadzenie Ministra MSWiA z dnia 18 stycznia 2007r. w sprawie Biuletynu Informacji Publicznej (Dz. U. 2007.10.68).&nbsp;</span></p>

