<p><strong>Plan budzetu domowego</strong></p>
<p>Nie kazda osoba ma stale dochody, ale kazda osoba ma stale wydatki. Dlatego zawsze warto prowadzic plan domowego budzetu. Taki plan mozna zrobic w arkuszu kalkulacyjnym na komputerze, ale tez mozna wykorzystac kartke papieru. Wazne jest wypisanie najpierw stalych zobowiazan, nawet jesli kwoty sa zmienne jak na przyklad w rachunkach za energie elektryczna. Wypisujemy wszystkie zobowiazania wraz z kwotami i datami oplaty.</p>
<p>Nastepnie warto wypisac inne stale wydatki, ktore przeznaczamy na zakup zywnosci, odziezy, bilety miesieczne lub paliwo do samochodu czy na srodki czystosci i regularne przyjemnosci jak np. wyjscie do kina raz w miesiacu, jesli taki mamy zwyczaj.</p>
<p>Jesli nie jestesmy w stanie dokladnie podac kwot to warto przez kolejny miesiac zbierac wszystkie paragony za zakupy, aby moc w kolejnym miesiacu bardziej dokladnie wyliczyc budzet.</p>
<p>W takim planie oczywiscie trzeba tez uwzglednic dochod. Dobrze jest jesli dochod calego gospodarstwa domowego, czyli wszystkich zarobkujacych osob, przewyzsza wydatki. Ale co zrobic, gdy jest na odwrot?</p>
<p><strong>Jak zmniejszyc wydatki i zwiekszyc oszczednosci?</strong></p>
<p>Dla wielu osob bardzo duzym obciazeniem sa rachunki domowe i splata zobowiazan takich jak pozyczki gotowkowe: <a href="https://www.wonga.pl/karta-kredytowa-czy-pozyczka-gotowkowa">https://www.wonga.pl/karta-kredytowa-czy-pozyczka-gotowkowa</a>, kredyty ratalne, czy nawet kredyty studenckie. Po pierwsze zawsze w budzecie, rata kredytu czy rachunki za mieszkanie musza byc priorytetem! Jesli nie zaplacimy ich w terminie narosna odsetki, a w wypadku pozyczek czy kredytow, jezeli notorycznie bedziemy sie spozniac z oplatami umowa moze zostac wypowiedziana, albo jesli nie placimy dluzej niz 3 miesiace wierzyciel moze podac dluznika do sadu i sprawa skonczy sie u komornika. Na tym nie wolno oszczedzac i trzeba placic w terminie.</p>
<p>Po drugie widzac wydatki na domowy budzet moze sie okazac, ze czesc z nich mozna uciac. Byc moze niektore rzeczy kupujemy niepotrzebnie na zapas i zamiast kupowac je co miesiac mozna co drugi. Albo tez jesli wiecznie brakuje nam pieniedzy mozemy zrezygnowac z kina, jedzenia na miescie czy zakupu nowych ubran przez pewien czas, az zaczniemy wychodzic na finansowa prosta.</p>
