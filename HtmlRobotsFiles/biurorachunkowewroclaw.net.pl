<p>Ubezpieczenie kredytu to rozwiazanie, po jakie siegaja banki w celu zabezpieczenia jego splaty w niepewnej rzeczywistosci finansowej. Ubezpieczenie samo w sobie nie jest obowiazkowe. Reguluje to Ustawa o ubezpieczeniach z 22 maja 2003 roku oraz Kodeks Cywilny. Mimo, ze ubezpieczenie kredytu ani nie jest obowiazkowe, ani - jezeli pozyczkobiorca juz sie na nie decyduje - nie musi byc wykupione w tym samym banku, ktory udziela kredytu, banki stosuja roznego rodzaju techniki, ktore maja naklonic lub wrecz zmusic pozyczkobiorcow do ubezpieczenia kredytu wlasnie u nich.</p>
<p><strong>Rezygnacja z ubezpieczenia - podstawy</strong></p>
<p>Rezygnacja z ubezpieczenia kredytu to proces, ktory moze pozyczkobiorcy przyniesc zysk, ale moze tez ciagnac za soba powazne konsekwencje. Po pierwsze, nalezy sie bardzo dokladnie zapoznac z odpowiednimi, regulujacymi te kwestie zapisami umowy kredytowej. Moze sie okazac, ze zrezygnowanie przez klienta z wykupionego z kredytem ubezpieczenia, znaczaco podniesie miesieczne raty splat tego ostatniego. W innych okolicznosciach, bank moze zazadac tez innego rodzaju zabezpieczenia kredytu, jak na przyklad poreczenia majatkowego przez osobe trzecia. W skrajnych przypadkach, bank moze rowniez zerwac umowe kredytowa i zazadac natychmiastowej splaty zobowiazan.</p>
<p>Kiedy osoba, ktora wykupila ubezpieczenie kredytu rezygnuje z niego, bank ma obowiazek wyplacenia zgromadzonych na jego rzecz srodkow. Ubezpieczenie moze byc oplacone przez kredytobiorce jednokrotnie - za pomoca jednorazowej wplaty ca caly okres kredytowania, albo byc oplacane comiesiecznie. ?Niezaleznie od tego, w jaki sposob ubezpieczenie zostalo oplacone, bank ma obowiazek wyplacenia niewykorzystanych srodkow na rachunek kredytobiorcy - jednoczesnie zmniejszajac saldo zadluzenia. Jezeli ubezpieczenie bylo oplacone jednorazowo, zwrotowi podlega calosc niewykorzystanej kwoty, a jezeli oplacane jest comiesiecznymi ratami - w wysokosci, w jakiej zostalo do danej chwili oplacone.</p>
<p><strong>Rezygnacja z ubezpieczenia &#8211; praktyka</strong></p>
<p>W celu zrezygnowania z ubezpieczenia kredytu, kredytobiorca powinien zglosic sie do banku (nie do ubezpieczyciela) i zlozyc w nim odpowiednie oswiadczenie. Oswiadczenie to moze byc zlozone na odpowiednim formularzu, jak rowniez byc przygotowane w calosci przez ubezpieczonego. Powinno ono zawierac pelne dane skladajacej je osoby - zgodne z danymi na umowie kredytowej oraz oswiadczenie woli odstapienia kredytobiorcy od ubezpieczenia. Takie oswiadczenie, powinno rowniez zawierac informacje o rachunku, na jaki bank powinien przelac niewykorzystane srodki.</p>
