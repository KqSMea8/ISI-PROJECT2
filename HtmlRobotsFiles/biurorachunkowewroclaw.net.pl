<p>Planujesz sprzedaz pojazdu? Zastanawiasz sie, jak powinna wygladac prawidlowa umowa kupna sprzedazy samochodu? W takim razie dobrze, ze trafiles na nasz artykul. W nim znajdziesz odpowiedz, jak powinna wygladac taka umowa, aby byla skutecznie zawarta.</p>
<p style="text-align: center;"><em><strong>Kluczowe postanowienia umowy sprzedazy samochodu</strong></em></p>
<p>Zanim przystapisz <a href="https://kapitalni.org/pl/artykuly/wzor-przykladowej-umowy-kupna-sprzedazy-samochodu,73,374">do podpisania umowy kupna-sprzedazy auta</a>, jezeli jestes kupujacym powinienes szczegolowo obejrzec pojazd, ktory zamierzasz kupic. Aby to zrobic w sposob kompleksowy, nalezaloby pojazd obejrzec w warsztacie, tak aby pozniej nie byc niemile zaskoczonym, ze pojazd ma wady, ktorych golym okiem nie bylo widac.</p>
<p>Gdy ogledziny wypadna pomyslnie i zdecydujesz sie jednak kupic pojazd, powinienes sporzadzic umowe najlepiej w formie pisemnej. Zawarcie umowy ustnej, choc dopuszczalne, to jest bardzo ryzykowne, gdyz z postanowien ustnych latwo sie wycofac, a druga strona wowczas nie jest w stanie udowodnic, ze okreslone ustalenia mialy miejsce. Ponadto w umowie kupna sprzedazy samochodu powinny sie znalezc:</p>
<p>* data i miejsce zawarcia umowy;</p>
<p>* podpisy stron (kupujacego i sprzedawcy) zawierajacych umowe;</p>
<p>* oznaczenie stron umowy. Jezeli strona jest osoba fizyczna podajemy: imie i nazwisko, adres zamieszkania, PESEL, numer dowodu osobistego. W przypadku, gdy strona jest osoba prawna wtedy nalezy podac: pelna nazwe spolki, adres siedziby spolki, numer KRS, REGON, NIP. Ponadto trzeba okreslic, kto ma prawo reprezentowac spolke i podpisac umowe, gdyz jezeli umowa zostanie podpisana przez niewlasciwa osobe, to umowa kupna sprzedazy samochodu jest niewazna. W tym celu nalezy przeanalizowac KRS spolki, gdzie pod pozycja &#8222;strony uprawnione do reprezentacji spolki&#8221; znajdziemy odpowiedz. KRS jest bezplatny i mozna go pobrac ze strony internetowej;</p>
<p>* precyzyjne okreslenie sprzedawanego pojazdu: okreslenie jego marki i modelu, numeru rejestracyjnego, daty produkcji i daty pierwszej rejestracji. Dzieki temu nie ma jakiejkolwiek watpliwosci, jaki pojazd zostal sprzedany;</p>
<p>* okreslenie ceny za sprzedaz pojazdu. Wskazanie, czy jest to kwota netto, czy brutto. Jezeli ma byc przelana na rachunek bankowy, to wowczas w umowie nalezy wskazac rowniez numer rachunku bankowego;</p>
<p>*oswiadczenie kupujacego i sprzedawcy, co do stanu technicznego pojazdu. Kupujacy powinien oswiadczyc, ze nie wnosi jakichkolwiek zastrzezen co do stanu pojazdu, a jezeli jakies dostrzega, powinien wskazac jakie. Z kolei sprzedawca powinien wskazac, ze pojazd nie zawiera jakichkolwiek wad, a jezeli zawiera to szczegolowo je opisac.</p>
