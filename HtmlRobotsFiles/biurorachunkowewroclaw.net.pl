<h2 style="text-align: center;">Rynek finansow</h2>
<p>Aby dobrze zrozumiec, jak dziala komisja, musimy zapoznac sie z samym rynkiem finansowym &#8211; miejsce pelne kupna i sprzedazy o roznych formach i instrumentach finansowych. Cel takich transakcji to oczywiscie zysk nakrecany przez podmioty, co powoduje popyt i podaz. Rynek finansowy to przede wszystkim wszelkie papiery wartosciowe, kontrakty, rynki kredytowe i terminowe. Powolana w 2006 roku komisja sprawuje nadzor nad kazdym sektorem gospodarczym naszego kraju. Kontroli podlega wiec zarowno obszar bankowy, rynek kapitalowy, ubezpieczenia jak i emerytury. Slowem &#8211; wszystko, co zwiazane z pieniadzem, nawet tym elektronicznym. Komisja czuwa rowniez nad bezpieczenstwem kazdego z tych obszarow, chroniac interesy podmiotow wchodzacych w sklad rynku. Na tym jednak nie koncza sie wszystkie prace Komisji Nadzoru Finansowego. Obowiazki sa o wiele wieksze i zawieraja sie takze w aktach prawnych jak i krokach prowadzacych do rozwiazywania sporow.</p>
<h2 style="text-align: center;">Dzialania KNF</h2>
<p>Komisja zajmuje sie takimi dzialaniami jak: staly nadzor nad dzialalnoscia rynku kapitalowego, rozwijanie polskiego rynku finansowego, udzielanie licencji na prowadzenie dzialalnosci, wspolpracowanie z zagranicznymi organami czy nakladanie sankcji na nieprzestrzegajacych prawa uczestnikow. Do glownych celow komisji naleza z kolei: nadzor nad rynkiem finansowym, zapewnienie prawidlowego jego funkcjonowania i stabilnosci, dbanie o bezpieczenstwo, ochrona interesow rynku. W sklad KNF wchodza: Przewodniczacy Komisji, 2 Zastepcow Przewodniczacego a takze 5 czlonkow Komisji. Dzieki temu organowi kwestie finansowe staja sie przejrzyste i jasne, a interesy poszczegolnych podmiotow chronione. Komisja chroni takze rynek finansowy kraju przed ewentualnym kryzysem ekonomicznym. KNF prowadzi takze nadzor nad instytucjami oferujacymi pozyczki, dzieki czemu mozemy sprawdzic, czy dana firma jest zaufana i mozemy z niej skorzystac. W tym celu powstal rejestr firm pozyczkowych. Podpowie nam on, gdzie warto pozyczyc pieniadze, by nie dac sie oszukac. O wpisanie do rejestru firmy musza ubiegac sie same, dlatego mamy gwarancje, ze kazda z nich zostanie dokladnie przejrzana pod katem rzetelnosci prowadzonych uslug.</p>
