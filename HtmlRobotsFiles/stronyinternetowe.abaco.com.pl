<ul class="post-tags">    <li><a href="http://www.stronyinternetowe.abaco.com.pl/index.php?tag/strona%20internetowa">strona internetowa</a></li>
</ul>

<div class="post-content"><p>Strona internetowa to bilans korzysci i start. Duzo oczywiscie zalezy od tego w jakim celu powstala dana strona. Jezeli szykujemy strone internetowa, ktorej glownym zajeciem bedzie sprzedaz towarow czy uslug to patrzac z tej perspektywy, strona jezeli przynosi dochod nie ma wad. Oczywiscie wymaga to pracy, jednak zazwyczaj jest to oplacalne. Inaczej ma sie sytuacja jezeli prowadzimy strone nie komercyjna. Typowo informacyjna lub blog.</p>


<p>Prowadzac blog zazwyczaj poswiecamy swoj czas, nierzadko nic z tego nie mamy. Owszem widzimy, ze ludzie czytaja nasze wpisy, jednak nie ma zadnych wymiernych korzysci finansowych. Wielu osobom jednak w zupelnosci wystarcza swiadomosc ze ich strona ma stalych czytelnikow.</p></div>

