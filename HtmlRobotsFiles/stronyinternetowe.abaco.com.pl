<h2 id="p3" class="post-title"><a href="http://www.stronyinternetowe.abaco.com.pl/index.php?post/2010/10/06/Czy-kolor-strony-www-zale%C5%BCy-od-bran%C5%BCy-kt%C3%B3r%C4%85-strona-przedstawia">Czy kolor strony www zalezy od branzy ktora strona przedstawia?</a></h2>
<p class="post-info">By admin admin, on Wednesday, October  6 2010 08:00 dans </p>

<ul class="post-tags">    <li><a href="http://www.stronyinternetowe.abaco.com.pl/index.php?tag/strony%20www">strony www</a></li>
</ul>

<div class="post-content"><p>Dzisiaj zastanowimy sie czy kolor strony internetowej jest okreslony i narzucony przez branze, jaka strona prezentuje. Byc moze wiekszosc z Was odpowie, ze zapewne nie ma roznicy. Ze najrozniejsze firmy z roznych branz maja strony kolorowe i to w najrozniejszych konfiguracjach. Generalnie sie z tym mozna zgodzic. Podobnie jak w wielu innych przypadkach tak i w Internecie sa wyjatki. Zdecydowanie nalezy tutaj wymienic jeden.</p>


<p>Branza uslug pogrzebowych. Chyba nikt z nas nie chcialby wejsc na strone zakladu pogrzebowego, ktora bedzie w kolorze rozowym czy zoltym. Prawda? Tutaj branza zdecydowanie narzuca kolorystyke. Podobnie jest w przypadku stron dla dzieci. /one dla odmiany powinny miec kolory pastelowe, jasne.</p></div>

