<h1>STAN-POL z Gliwic</h1>
<p><span style="font-size:20px">- solidna firma instalatorska</span></p>

<p>&nbsp;</p>

<p>Kupujesz dom lub mieszkanie? Oferujemy porady techniczne w sprawie wody, ogrzewania i nie tylko. Nasi specjalisci doradza jak wybrac najbardziej optymalna instalacje dla Ciebie!</p>

<p>Zaufaj nam - istniejemy na rynku od 26 lat!</p>

<p><strong>Firma Stan - Pol s.c. </strong>powstala w 1991 r. w Gliwicach jako Stan-Pol Materialy instalacyjno Budowlane i zostala zalozona przez Pana Stanislawa Kristman. Pierwotnym profilem firmy byl handel materialami budowlanymi i instalacyjnymi.</p>

<p>Dynamiczny rozw&oacute;j przemyslu budowlanego oraz zwiekszone zapotrzebowanie na uslugi instalacyjne firma Stan-Pol rozpoczyna dzialalnosc uslugowa w branzy grzewczej. W roku 1998 r. firma zostaje przemianowana na sp&oacute;lke cywilna pod nazwa Stan-Pol s.c. Krystyna i Krzysztof Kristman. Gl&oacute;wnym profilem dzialalnosci firmy zostaje swiadczenie uslug w zakresie techniki grzewczej oraz sanitarnej.</p>

<p>26 lat&nbsp; od rozpoczecia dzialalnosci firma oferuje Panstwu doswiadczenie jak r&oacute;wniez przygotowanie inzynierskie branzy grzewczej. Solidne podejscie do problematyki zaowocowala wsp&oacute;lpraca z szeregiem wiodacych producent&oacute;w branzy grzewczej.</p>

<p>Nasza dzialalnosc oparta jest o handel i uslugi w zakresie instalatorstwa wodnego, kanalizacyjnego, gazowego oraz techniki grzewczej.</p>

<p><strong>W naszej ofercie znajda Panstwo:</strong></p>

<ul>
<li>instalacje gazowe na gaz ziemny i propan</li>
<li>instalacje centralnego ogrzewania: tradycyjne, podlogowe, scienne</li>
<li>instalacje wodno - kanalizacyjne</li>
<li>kominki z plaszczem wodnym</li>
<li>kotly weglowe w pelni zautomatyzowane</li>
<li>odkurzacze centralne</li>
<li>przydomowe oczyszczalnie sciek&oacute;w</li>
<li>odnawialne zr&oacute;dla energii: kolektory sloneczne, pompy ciepla</li>
<li>kotlownie przemyslowe do ogrzewania hal, budynk&oacute;w wielorodzinnych, przemyslowych</li>
<li>projektowanie instalacji gazowych</li>
<li>kotly zasilane weglem, pelletem, mialem oraz gazem</li>
</ul>

<p>Prowadzimy doradztwo techniczne, sprzedaz, wykonawstwo, serwis.</p>

<p>Kazde zlecenie jest dla nas wyzwaniem, kt&oacute;rego sie nie boimy podjac i wykonac. Na wykonana instalacje udzielamy 36 miesiecy gwarancji.</p>

<p>Zapraszamy do wsp&oacute;lpracy.</p>
