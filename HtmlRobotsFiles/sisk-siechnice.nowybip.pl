<p>Nowy BIP oparty jest na nowoczesnym systemie zarzadzania trescia, dzieki czemu praca przy obsludze Biuletynu Informacji Publicznej staje sie wyjatkowo prosta. System pozwala w latwy sposob zarzadzac trescia i wygladem strony, posiada takie udogodnienia jak:</p>

<ul class="nice_list">
<li>bezposrednia edycje stron, bez koniecznosci czestego przechodzenia do panelu administracyjnego,</li>
<li>przeklejanie tekstow z edytora Microsoft Word z zachowaniem odpowiedniego formatowania,</li>
<li>automatyczne konwertowanie tabel z arkuszy takich jak Microsoft Excel, umozliwiajace niezwykle szybka publikacje danych tabelarycznych,</li>
<li>mozliwosc umieszczania plikow do pobrania, grafiki, animacji Flash,</li>
<li><strong>wbudowana funkcja wyswietlajaca wymagana informacje o plikach cookies</strong> (ciasteczkach),</li>
<li>mozliwosc wyboru szablonu zgodnego z wytycznymi <strong>WCAG 2.0</strong>,</li>
<li>mozliwosc wyboru szablonu dostosowujacego sie do urzadzen mobilnych ( <strong>szablon responsywny</strong> )</li>
<li>wiele innych cech, ktore czynia z systemu Nowy BIP aplikacje funkcjonalna i przyjazna dla uzytkownikow</li>
