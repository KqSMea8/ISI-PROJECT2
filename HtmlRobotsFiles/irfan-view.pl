<div id="col-middle"><h1>Co to jest IrfanView</h1>
<p><b><strong>IrfanView</strong> jest bardzo szybka, mala, kompaktowa i innowacyjna oraz darmowa (dla zastosowan niekomercyjnych)
przegladarka plikow graficznych dla systemow Windows 9x, ME, NT, 2000, XP, 2003 , 2008, Vista, Windows 7, 8, 8.1, 10.</b></p>
<p><strong>IrfanView</strong> jest prosty dla poczatkujacych ale i potezny dla zaawansowanych.</p>
<p><strong>IrfanView</strong> posiada wiele innowacyjnych funkcji, ktorych nie posiadaja programy konkurencyjne.</p>
<p><strong>IrfanView</strong> byl pierwsza na swiecie przegladarka z obsluga animowanych plikow GIF, wielostronicowych plikow TIFF i plikow ICO zawierajacych wiele ikon.</p>
<p>Najwazniejsze funkcje programu <strong>IrfanView</strong>:</p>
<ul>
<li>duza liczba wspieranych formatow plikow (<a href="/formaty">obslugiwane formaty</a>),</li>
<li>wielojezyczny interfejs,</li>
<li>podglad miniatur,</li>
<li>proste funkcje rysowania (linie, okregi, strzalki itp),</li>
<li>skorki dla paska narzedzi,</li>
<li>pokaz slajdow z mozliwoscia zapisu jako EXE/SCR i nagrania na CD,</li>
<li>podglad naglowkow EXIF/IPTC i komentarzy w pokazie slajdow,</li>
<li>obsluga filtrow z Adobe Photoshop,</li>
<li>szybkie przegladanie katalogow,</li>
<li>wsadowa konwersja plikow,</li>
<li>edycja wielostronicowych plikow TIF,</li>
<li>wyszukiwanie plikow,</li>
<li>wysylanie obrazow e-mailem,</li>
<li>odtwarzacz multimedialny,</li>
<li>wydruk obrazow,</li>
<li>obsluga wbudowanych profili kolorow w JPG/TIF,</li>
<li>zmiana glebi kolorow,</li>
<li>obsluga skanowania</li>
<li>wycinanie/kadrowanie,</li>
<li>edycja IPTC,</li>
<li>dodatkowe efekty,</li>
<li>przechwytywanie obrazu,</li>
<li>wyodbrebnianie ikon z EXE/DLL/ICL,</li>
<li>bezstratne obracanie plikow JPG,</li>
<li>wsparcie dla Unicode,</li>
<li>wiele skrotow klawiszowych,</li>
<li>wiele komend z wiersza polecen,</li>
<li>wiele wtyczek,</li>
<li>caly program znajduej sie w 1 pliku EXE,</li>
<li>program nie dokonuje zadnych modyfikacji rejestru bez wiedzy uzytkownika,</li>
<li>i wiele wiecej...</li>
</ul>
