<h2>Error Page</h2>
<p>Joga Centrum Adama Bielewicza jest jedna z najwiekszych i najbardziej renomowanych szkol jogi Iyengara w Polsce. Szkola istnieje od 1999 roku.</p>
<p>Nasz zespol tworza wylacznie dyplomowani nauczyciele z kilku-, kilkunastoletnim doswiadczeniem w praktyce i nauczaniu jogi. Stale doskonalimy swoje umiejetnosci.</p>
<p>Uczymy jogi wg metody Iyengara, jednej z najbardziej popularnych metod jogi na swiecie, cenionej ze wzgledu na swoja precyzje, glebie, szybkie efekty, oraz mozliwosc praktykowania przez wszystkich niezaleznie od wieku, sprawnosci i swiatopogladu.</p>
<p>W naszej szkole swoje pierwsze kroki w jodze stawialo wielu obecnych nauczycieli uczacych w szkolach jogi i fitness klubach w Warszawie.</p>
<p>Serdecznie zapraszamy do odwiedzenia Joga Centrum i dolaczenia do grona naszych uczniow i przyjaciol. Mamy nadzieje, ze przy pomocy jogi odnajdziecie wewnetrzna harmonie, ktora pomoze Wam w codziennym zyciu.</p>
<p><br />Dlaczego Joga Centrum:</p>
