<h1>O Parafii</h1>
<section>
<p>Kotulin Dla niektorych to tylko punkt na mapie, zwykla wioska, jedna z tysiecy innych. Jednak nie dla nas. To tu w wiekszosci sie wychowalismy, to stad mamy najpiekniejsze wspomnienia, to tu jest nasz dom... A o dom zawsze trzeba dbac.
</p>
</section>
<img src="img/o-nas.jpg" alt="panorama kotulina">
<h1>Historia</h1>
<section>
<p>
Poczatek osady wiaze sie z Kotulinem Malym, ktory posiadal swoj kosciol. Pierwsze udokumentowane wzmianki o osadzie na tym terenie pochodza z roku 1257. Mianowicie hrabia Januzjusz 20 stycznia 1257r. uzyskal zezwolenie od ks. Wladyslawa na zalozenie wedlug prawa niemieckiego nowego osiedla w lesie Chotulin. Osiedle to, pozniej wies, od 1305r. w dokumentach nosi nazwe Magnum Chotulin (Kotulin Wielki), w odroznieniu od Kotulina Malego, datowanego o wiele wczesniej. Wedlug zrodel w Kotulinie Wielkim juz przed 1305r. zostala erygowana parafia. W 1285r. z nieznanego miejsca przeniesiono do Kotulina Wielkiego kosciol pw. sw. Michala Archaniola i ustawiono go posrodku cmentarza. Pierwszym proboszczem zostal ks. Aleksius. Az do roku 1502 parafia miala dwa koscioly - w Kotulinie Malym i Wielkim. Do parafii w Kotulinie dolaczono takze wies Proboszczowice (zalozona okolo roku 1305). Wedlug niektorych zrodel dopiero od roku 1376 kosciol w Kotulinie Wielkim jest zwany parafialnym. W 1412r. Parafia kotulinska nalezala do hr.Jenkiego, a w 1495r. obejmuje ja Hrabia Mokrzeski. W 1562r. wlascicielem parafii zostal hr. Starzinsky. Kotulin przechodzi na protestantyzm. Trwa to jednak niedlugo, bo tylko do roku 1629r. W 1648r. Kotulin zostaje kupiony przez wlascicieli Toszka. Od 1890 roku az do zakonczenia II wojny swiatowej, wlascicielami Kotulina jest rodzina Hohenlohe ze Slawiecic. Z Kotulina wywodzi sie znana rodzina szlachecka Kottulinskich.</p>
</section>
