function openTaskModal(taskId, crmId) {
var taskInformation = $('#crmTaskInformation');
taskInformation.empty();

var element_id = {
"id": taskId,
"crmId": crmId
};
$.ajax({
type: 'post',
dataType: "json",
url: "/ajax/getTaskById",
data: element_id,
success: function (response) {
var responsiblePeople = '';
if(response.carrer_name == null) {
var separator = '';
$.each(response.responsible, function(index, value) {
responsiblePeople += separator + value;
separator = ', ';
});
responsiblePeople = responsiblePeople.replace(', ', ',').split(",");
} else {
responsiblePeople = response.carrer_name.replace(', ', ',').split(",");
}
var trInformation = [];
trInformation += '<div class="col-md-3"></div><div class="col-md-6 verticalAlignMiddle taskStatusModal font-white margin-bottom-5 taskCategory'+response.category+'">'+response.dictionary+'</div><div class="col-md-3"></div>';
if(response.when_date) {
trInformation += '<div class="col-md-12"><div class="col-md-12 disabledLabel font-size-12">Data</div><div class="col-md-12 font-size-15"><i class="fa fa-calendar disabledLabel"></i> '+response.when_date+'</div></div>';
}
trInformation += '<div class="col-md-12 padding-top-10"><div class="col-md-12 disabledLabel font-size-12">Nazwa zadania</div><div class="col-md-12 font-size-15"><i class="fa fa-tasks disabledLabel"></i> '+ response.title+'</div></div>';

var when_time = response.when_time ? response.when_time : '';
var when_time_end = response.when_time_end ? response.when_time_end : '';
if(when_time || when_time_end) {
var time_text = when_time ? (when_time_end ? 'Godzina rozpoczecia' + ' / ' + 'Godzina zakonczenia' : 'Godzina rozpoczecia') : (when_time_end ? 'Godzina zakonczenia' : '');
var time = when_time ? (when_time_end ? when_time + '-' + when_time_end : when_time) : (when_time_end ? when_time_end : '');
trInformation += '<div class="col-md-12 padding-top-10"><div class="col-md-12 disabledLabel font-size-12">'+time_text+'</div><div class="col-md-12 font-size-15"><i class="fa fa-clock-o disabledLabel"></i> '+time+'</div></div>';
}
if(responsiblePeople.length > 1) {
var halfLength = Math.ceil(responsiblePeople.length /2);
var leftSideOfArray = responsiblePeople.splice(0, halfLength);

var leftSide = [];
$.each(leftSideOfArray, function(index, value) {
leftSide += '<li><i class="fa fa-user disabledLabel"></i> '+value+'</li>';
});

var rightSide = [];
$.each(responsiblePeople, function(index, value) {
rightSide += '<li><i class="fa fa-user disabledLabel"></i> '+value+'</li>';
});
trInformation += '<div class="col-md-12 padding-top-10"><div class="col-md-12 disabledLabel font-size-12">Odpowiedzialny</div><div class="col-md-12 font-size-15"><div class="col-md-6"><ul class="textAlignLeft listStyleTypeNone">'+leftSide+'</ul></div><div class="col-md-6"><ul class="textAlignLeft listStyleTypeNone">'+rightSide+'</ul></div></div></div>';
} else {
trInformation += '<div class="col-md-12"><div class="col-md-12 disabledLabel font-size-12">Odpowiedzialny</div><div class="col-md-12 font-size-15"><i class="fa fa-user disabledLabel"></i> '+response.carrer_name+'</div></div>';
}
if(response.description) {
trInformation += '<div class="col-md-12 padding-top-10"><div class="col-md-12 disabledLabel font-size-12">Opis</div><div class="col-md-12 font-size-15">'+response.description+'</div></div>';
}
if(response.active == 1) {
trInformation += '<div class="col-md-12 padding-top-10 tasksActions"><a class="closeTask crmTaskInfoModal" data-task-id="'+response.id+'"><i class="fa fa-check-circle font-size-35 contrahentHistoryActions disabledLabel closeTaskTooltip" aria-hidden="true"></i></a><a href="/crm/addTask?crmId='+response.crm_id+'&amp;taskId='+response.id+'"><i class="fa fa-edit font-size-35 contrahentHistoryActions disabledLabel crmListEdit" aria-hidden="true" data-original-title="" title=""></i></a><a class="removeTask crmTaskInfoModal" data-id="'+response.id+'"><i class="fa fa-remove font-size-35 contrahentHistoryActions disabledLabel crmListRemove" aria-hidden="true"></i></a></div>';
}
taskInformation.append(trInformation);
$(".closeTaskTooltip").tooltip({
trigger:"hover",
container:"body",
title:"Zamknij zadanie"
});
$(".crmListEdit").tooltip({
trigger:"hover",
container:"body",
title:"Edytuj"
});
$(".crmListRemove").tooltip({
trigger:"hover",
container:"body",
title:"Usun zadanie"
});
}
});

$('#taskModal').modal();
}
$(".newMessageButtonNavbar").tooltip({
trigger:"hover",
container:"body",
title:"Napisz wiadomosc"
});
$(".addTaskFromNavbar").tooltip({
