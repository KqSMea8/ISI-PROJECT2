var data = {'peopleId': originalBuyerId};
$.ajax({
type: "POST",
async: false,
dataType: "json",
url: "/ajax/getEmailsByPeopleId/",
data: data,
success: function (response) {
if(response.email) {
message += ' ' + response.email;
}

if(response.email_bookkeeping) {
message += ' oraz ' + response.email_bookkeeping;
}

message += '?';
}
});

if (!confirm(strip(message))) {
event.preventDefault();
return;
}
});

