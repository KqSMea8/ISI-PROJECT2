<h1 class="entry-title">About us</h1>

</header><!-- .entry-header -->

<div class="entry-content">
<p lang="en-US" style="text-align: justify;" align="JUSTIFY">Our company has been established by&nbsp;a&nbsp;group of Savage Worlds enthusiasts, who want to&nbsp;promote this system in our country. There are three hotheads at its core: Piotr Korys (Editor of the Savage Worlds Line in Pinnacle Entertainment Group), Umberto Pignatelli (author, translator, editor and RPG fan) and Andy Slack (RPG fan, grognard and editor) + Lukasz &#8222;Chavez&#8221; Maludy (IT Crowd!) and Irek Winnicki (layout magic). More about our team<a href="http://wydawnictwogramel.pl/?page_id=17"> here</a>.</p>
<p style="text-align: justify;">We'll try to&nbsp;provide the highest quality of products to&nbsp;our fans - from the Polish version of the Savage Worlds core rulebook, through games created with that system, up to&nbsp;One Sheet Adventures and various aids for the players.</p>
<p style="text-align: justify;">We&nbsp;would like to&nbsp;thank the many fans that support our publishing house. We&nbsp;would also like to&nbsp;thank Mariusz Gandzel, Kuba Gora, Michal Szeja and Lukasz Maludy for their help.</p>
