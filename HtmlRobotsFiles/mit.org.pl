<h1 class="uk-article-title">
Zapraszamy na targi GLOBalnie oraz na stoisko PIT			</h1>


<p class="uk-article-meta">

Napisane: <time datetime="2019-03-28">28 marzec 2019</time>.
</p>



<p>Szanowni Panstwo, Czlonkowie Polskiej Izby Turystyki,</p>
<p>Zapraszam do wziecia udzialu w 25 edycji Miedzynarodowych Targow Turystycznych GLOBalnie 2019, ktore odbeda sie w dniach 29-31.03.2019r. w Miedzynarodowym Centrum Kongresowym w Katowicach. Juz od piatku czekamy na Panstwa na stoisku PIT, na ktore zapraszamy wspolnie z naszym partnerem MerlinX. Na stoisku bedzie okazja do spotkan z przedstawicielami Prezydium PIT, Zarzadu Oddzialu Slaskiego, porozmawiania o problemach i sprawach istotnych dla branzy turystycznej czy zapoznania sie z oferta partnera przy filizance kawy. Przypominamy takze, ze, jak co roku istnieje mozliwosc bezplatnego wystawienia swoich materialow promocyjnych na stoisku Izby, ulotek i folderow (w rozsadnej ilosci), ktore nalezy dostarczyc na stoisko w piatek rano lub w trakcie trwania targow. Bezplatne zaproszenie na targi w zalaczniku (wazne po rejestracji). Polska Izba Turystyki zaprasza rowniez na spotkanie polaczone z prezentacja: Nie plac ogromnych kar za nieznajomosc przepisow, ktore odbedzie sie w trakcie targow GLOBalnie 29 marca 2019 o godzinie 11.30 na terenie Miedzynarodowego Centrum Kongresowego w Katowicach. Prezentacje poprowadzi Pawel Niewiadomski, prezes Polskiej Izby Turystyki oraz prezes ECTAA - European Travel Agents and Tour Operators Associations. Poruszone zostana najwazniejsze tematy nurtujace obecnie branze turystyczna: 1. Jaka stawke podatku VAT stosowac, rozliczajac prowizje za sprzedaz imprez turystycznych poza Unie Europejska? 2. Rola agenta turystycznego na zmieniajacym sie rynku dystrybucji uslug turystycznych. 3. Jak ustrzec sie wysokich kar za naruszenie Ustawy o imprezach turystycznych i powiazanych uslugach turystycznych? 4. Informacja o planowanych szkoleniach przygotowanych przez PIT w ramach projektu Szkolenia lub doradztwo wynikajace z rekomendacji Sektorowych Rad ds. Kompetencji opracowanego przez PARP i dofinansowanego przez Unie Europejska. Serdecznie zapraszamy na trzy dni wypelnione spotkaniami biznesowymi, na rozmowy, wymiane doswiadczen i pogladow oraz po prostu mile spedzony czas. Mamy nadzieje, ze jak zwykle stoisko PIT bedzie tetnic turystycznym zyciem, a nawiazane w trakcie tych targow kontakty zaowocuja nowymi inicjatywami.</p>
<p>Do zobaczenia na targach! Grzegorz Chmielewski Wiceprezes Polskiej Izby Turystyki Prezes Slaskiego Oddzialu PIT</p><div class="attachmentsContainer">

