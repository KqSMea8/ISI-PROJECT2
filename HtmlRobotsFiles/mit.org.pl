<h1 class="uk-article-title">
III Miedzynarodowy Kongres Turystyki Religijnej i Pielgrzymkowej - 6-10 listopada 2019			</h1>


<p class="uk-article-meta">

Napisane: <time datetime="2019-02-22">22 luty 2019</time>.
</p>



<p>Szanowni Panstwo!</p>
<p>W dniach 6-10 listopada 2019 roku w Krakowie odbedzie sie III Miedzynarodowy Kongres Turystyki Religijnej i Pielgrzymkowej "100-lecie urodzin Karola Wojtyly - sw. Jana Pawla II".</p>
<p>O ogromnym potencjale Krakowa i Malopolski, jesli chodzi o turystyke religijna i pielgrzymkowa, swiadcza nie tylko przyjezdzajace tu miliony pielgrzymow i turystow religijnych, czego przykladem byly zorganizowane w 2016 roku Swiatowe Dni Mlodziezy, ale takze sukces I i II edycji Kongresu w latach ubieglych. Zdjecia, relacje i opinie z tych wydarzen mozna ogladac na: www.icortap.com/pl/galeria, www.facebook.com/ICoRTaP, www.instagram.com/icortap_congress/, www.icortap.com/pl/opinie. 7 listopada Kongres otworza swieckie i koscielne wladze Krakowa i Malopolski: odbedzie sie msza inauguracyjna, przemowy otwarcia, prelekcje i wyklady oraz workshop (expo) z przedstawicielami sanktuariow, miejsc kultu oraz miejsc turystycznych. 8, 9 i 10 listopada goscie z calego swiata poswieca na zwiedzanie Krakowa i Malopolski (Stare Miasto w Krakowie, Centrum Jana Pawla II na Bialych Morzach, Sanktuarium Bozego Milosierdzia w Lagiewnikach, Kopalnia Soli w Wieliczce, byly niemiecki nazistowski oboz koncentracyjny Auschwitz-Birkenau, parafia i dom rodzinny Ojca Swietego w Wadowicach, Bazylika w Kalwarii oraz sanktuarium Czarnej Madonny w Czestochowie). Celem Kongresu jest promocja Krakowa, Malopolski i Polski jako istotnej destynacji turystyki religijnej i pielgrzymkowej na arenie miedzynarodowej, wzmocnienie znaczenia turystyki religijnej i pielgrzymkowej w kontekscie globalnego przemyslu turystycznego oraz wymiana kontaktow handlowych wsrod uczestnikow. Na Kongres zaproszeni sa przedstawiciele zagranicznych biur podrozy i touroperatorow, blogerzy i dziennikarze, biskupi i ksieza oraz inni organizatorzy turystyki religijnej i pielgrzymkowej tacy jak koordynatorzy diecezjalni, szefowie fundacji, wspolnot i kongregacji zainteresowani organizowaniem przyjazdow obcokrajowcow do Polski (kupujacy = buyers). Inne podmioty, zarowno polskie jak i zagraniczne, takie jak samorzady, lokalne lub regionalne organizacje promujace miasta czy regiony, miejsca kultu - miejsca pielgrzymkowe, miejsca turystyczne, muzea itp. moga uczestniczyc w kongresie jako sprzedajacy = sellers. Link do programu kongresu: www.icortap.com/pl/program Link do workshopu (expo): www.icortap.com/pl/expo Zapraszamy do odwiedzenia strony internetowej Kongresu: www.ICoRTaP.com International Congress of Religious Tourism and Pilgrimages.</p>
<p>Zapraszamy do Krakowa. Ernest Miroslaw</p>
