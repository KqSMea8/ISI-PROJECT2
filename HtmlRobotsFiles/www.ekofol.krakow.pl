<b>4. Przedmiot zamowienia</b><br>
Przedmiotem zamowienia jest dostawa 2 szt. przesiewaczy klasyfikujacych jednopokladowych do filtracji wody do budowy urzadzenia do czyszczenia i separacji odpadow z linii recyklingu.<br>
Kod CPV 42912310-8 Aparatura do filtrowania wody<br>
Wymagania:<br>
1. Srednica - min. 900 mm<br>
2. Stopien filtracji (wielkosc oczka) - 500 mikronow (0,5 mm)<br>
3. Przepustowosc na poziomie min. 20 m3/h<br>
4. Maksymalna wysokosc urzadzenia - 70 cm<br><br>

