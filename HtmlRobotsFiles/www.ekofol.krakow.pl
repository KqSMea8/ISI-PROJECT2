a)	Pelne dane identyfikacyjne Wykonawcy (nazwa, adres, nr NIP)<br>
b)	Date przygotowania i date waznosci oferty (waznosc oferty min. 30 dni)<br>
c)	Specyfikacje wg punktu ,,Przedmiot zamowienia" wraz z odniesieniem do wszystkich powyzszych wymagan<br>
d)	Odniesienie do kazdego z zamieszczonych w ogloszeniu kryteriow oceny<br>
e)	Cene calkowita netto<br>
f)	Warunki i terminy platnosci<br>
g)	Warunki dostawy - wymagana baza dostawy CIP<br>
h)	Termin dostawy - oczekiwany termin dostawy 8 tygodni od podpisania umowy<br>
i)	Okres gwarancji liczony od daty dostawy do Zamawiajacego<br>
j)	Dane osoby do kontaktu (imie, nazwisko, numer telefonu, adres e-mail)<br>
k)	Podpis osoby upowaznionej do reprezentowania Wykonawcy, zgodnie z forma reprezentacji okreslona w rejestrze sadowym lub innym dokumencie, wlasciwym dla danej formy organizacyjnej Wykonawcy albo przez osobe, ktora nie jest umocowana, przy czym w takim przypadku pelnomocnictwo musi byc zalaczone do oferty, <br>
