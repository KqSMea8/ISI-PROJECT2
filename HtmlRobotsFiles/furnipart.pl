<p>Zapraszamy do naszego salonu z meblami kuchennymi przy ul A. Struga 25 w Szczecinie.<br />
Zajmuje sie kompleksowym doradztwem w zakresie projektowania mebli i  wyposazenia wnetrz.</p>
<p align="left">Kazdego klienta traktuje indywidualnie, kazdy projekt jest niepowtarzalny,<br />
tworzony od podstaw z uwzglednieniem potrzeb inwestora.</p>
<p align="left">Nadzoruje realizacje przez caly czas jej trwania, od wykonania projektu,<br />
przez profesjonalne doradztwo, az do finalnego montazu.</p>
<p align="left">Projekty realizuje na podstawie wlasnych pomiarow,<br />
badz projektow budowlanych dostarczonych przez inwestora.</p>
<p align="left">Wykonane projekty przedstawiam w postaci kolorowej wizualizacji 3D,<br />
tak aby klient mial pelen obraz projektowanego wnetrza.</p>
<p>Zapraszamy do obejrzenia kilkudziesieciu z moich projektow i realizacji w galerii.</p>
