<h2>Praktyki w Jarkopolu</h2>
<p class="akapit">W ostatnich dniach lipca zakonczyly sie praktyki dla studentow informatyki w naszej firmie. Przez cztery tygodnie, czterej studentw Uniwersytetu Lodzkiego z Wydzialu Matematyki i Informatyki zapoznawali sie ze specyfika dzialania firmy informatycznej a takze brali udzial w tworzeniu oprogramowania o charakterze uzytkowym. Stosowane techniki przy pracy nad projektami to: php, mysql, ajax, javascript, html, xhtml, flash. Studenci poszerzyli zakres wiedzi ze stosowanych technik, jak rowniez zapoznali sie z metodami i specyfika wykorzystania ich do celow uzytkowych.</p>

