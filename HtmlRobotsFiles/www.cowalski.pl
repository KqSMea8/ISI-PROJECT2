<div class="tekst" id="tekst_10"><h3>Coworking</h3><p><strong>CoWalski</strong> &ndash; Aleja Inspiracji to miejsce oferujace szeroko pojety COWORKING.</p>
<p>U nas mozna korzystac z BIURA na prostych zasadach, w pakietach dostosowanych do indywidualnych potrzeb. w godzinach od 9:00 &ndash; 17:00.</p>
<p>Aleja Inspiracji to uzyteczne rozwiazanie ulatwiajace organizacje biznesu, poprzez korzystanie z prestizowego adresu w ramach WIRTUALNEGO BIURA lub OFFICE SERWIS.</p>
<p>Doskonala lokalizacja na Alei Wielkopolskiej, usytuowanej w malowniczym Solaczu, umozliwia szybkie dotarcie do kluczowych miejsc i kontrahent&oacute;w w Poznaniu.</p></div>
<div class="tekst" id="tekst_11"><h3>Sala spotkan</h3><p>CoWalski dzieki zapleczu KONFERENCYJNEMU oferuje idealne miejsce do SPOTKAN, SZKOLEN dla kameralnych grup czy tez NEGOCJACJI BIZNESOWYCH.&nbsp;</p>
<p>W profesjonalnych i DESIGNERSKICH warunkach mozesz przeprowadzic spotkanie z Klientem czy tez warsztat.</p>
<p>Dobra lokalizacja pozwala z latwoscia dotrzec zainteresowanym. Spotkanie w komfortowych warunkach, w spokoju, przy dobrej kawie, w malowniczej okolicy Parku Solackiego to na pewno dobre rozwiazanie dla Twojego biznesu.</p></div>
<div class="tekst" id="tekst_15"><h3>Wirtualne biura</h3><p>CoWalski Aleja Inspiracji to mozliwosc korzystania z naszego prestizowego adresu w ramach uslugi wirtualne biuro. To rozwiazanie pozwalajace, szczeg&oacute;lnie marka na starcie, pozycjonowac sie w atrakcyjnej lokalizacji na Solaczu, przy Al. Wielkopolskiej 29. W zaleznosci od wybranego pakietu mozesz korzystac z szerokiej oferty uslug biurowych oraz profesjonalnej sali do spotkan biznesowych. By Tw&oacute;j biznes byl jeszcze bardziej inspirujacy!</p></div>
<div class="tekst" id="tekst_12"><h3>Warsztat</h3><p>CoWalski &ndash; Aleja Inspiracji udostepnia pomieszczenia osobom, kt&oacute;re prowadza r&oacute;znego rodzaju zajecia, warsztaty, szkolenia.</p>
<p>To oferta dla os&oacute;b, kt&oacute;re prowadza BIZNES i szukaja przestrzeni do wynajecia, tylko w okreslonym czasie &ndash; np. gdy maja skompletowana grupe. To ekonomiczne rozwiazanie by nie ponosic niepotrzebnych koszt&oacute;w zwiazanych z tradycyjnym wynajmem lokalu.</p>
<p>Do strefy warsztatowej zapraszamy w godzinach popoludniowych (po 16.00) i w weekendy, w godzinach dostosowanych do Twoich potrzeb.</p>
<p>Przylacz sie do nas i razem stw&oacute;rzmy Inspirujace miejsce!</p></div>
<div class="tekst" id="tekst_14"><h3>CoCreatve</h3><p><span>CoOperatives [Co-op] to nowoczesna forma pracy zespolowej - polaczmy nasze sily i umiejetnosci aby przygotowac wyjatkowe i efektywne kampanie promocyjne. Zapraszamy do wsp&oacute;lpracy wszystkich, kt&oacute;rym nie sa obce dzialania promocyjne i marketingowe!&nbsp; Co-op nie wyklucza, a doskonale uzupelnia prace kazdego Freelancera.</span></p>
<p>Osoby badz firmy i instytucje kt&oacute;re potrzebuja natomiast wsparcia promocyjno-marketingowego zapraszamy do korzystania z naszych wyjatkowych uslug. Wszystko czego potrzebujesz, by pokazac sie z jak najlepszej strony i wypromowac sw&oacute;j start&rsquo;up lub swoja nowo powstajaca marke &ndash; znajdziesz w naszej Alei Inspiracji.&nbsp;</p>
<div><span><br /></span></div></div>
<div class="tekst" id="tekst_13"><h3>Chillout</h3><p>CoWalski &ndash; Aleja Inspiracji to zapoczatkowanie nowego rozwiazania dla biznesu &ndash; biuro/warsztat. Nie zapominamy przy tym o atmosferze, relacjach i ludziach, dzieki kt&oacute;rym jestesmy wlasnie w tym miejscu. Aleja Inspiracji to szczeg&oacute;lne miejsce &ndash; kamienica z dusza, dom pamietajacy zapach i smak szarlotki, kt&oacute;ra jedlismy u babci popijajac goracym kakao, godzinami sluchajac inspirujacych historii opowiadanych przez dziadka, ciotki i sasiedzka smietanke towarzyska...</p>
<p>Z mysla o waznym dla kazdego czasie refleksji i relaksu stworzylismy INSPIRUJACA STREFE. To przestrzen do odpoczynku, budowania relacji i spotkan mniej formalnych. Zapros swojego ulubionego Klienta lub partnera biznesowego czy przyjaciela &ndash; na przekaske, wsp&oacute;lne ogladanie filmu, koncertu, dyskusje lub przyjdz i wymien sie ksiazka lub zawies dla kogos kawe.</p></div>
