<p>Naszym glownym przeslaniem jest dbanie o higiene oraz czystosc w Panstwa otoczeniu. Robimy to w zgodzie z ekologia przy uzyciu najnowoczesniejszych metod sprzatania oraz srodkow, ktore pozostaja neutralne dla srodowiska.</p>
<p>Dzialamy w Lodzi, oraz na terenie woj. lodzkiego.</p>
