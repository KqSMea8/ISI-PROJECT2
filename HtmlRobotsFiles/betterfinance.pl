function makePhoneNumber() {

var p = '#ctl0_Main_Contact_phone';
var str = $j(p).val();
var i, l=0;

var newstr = '';
for (i=0; i<str.length; i++) {
if (str[i] >= '0' && str[i] <= '9' && l<11) {
newstr += str[i]; l++;
}
if ((str[i] == ' ')) newstr += str[i];
}

$j(p).val(newstr);
return l;
}

