<h1>Strona Glowna</h1>

<p>
Witamy na naszej stronie glownej.
</p>

<a href="/images/galeria/poziom/9.jpg" rel="lyteshow[vacation]" title="Obrazek 9"><img src="/images/main/9.jpg" alt="Obrazek 9" style="float: right; padding: 10px 0 0 10px"/></a>

<p>
<strong>BJ Impet sp.z.o.o</strong> powstala z przeksztalcenia firmy ,,Niedoba" dzialajacej na rynku od 1987r. Posiada wiec ogromne
doswiadczenie. Hurtownia <strong>kamienia, zwiru, krzemienia</strong> oraz roznego rodzaju <strong>glazow i otoczakow</strong> powstala w
roku 2008 i szybko zyskala sobie spore grono zadowolonych Klientow, dzieki bardzo duzemu asortymentowi i niskim cenom.
</p>

<a href="/images/galeria/poziom/2.jpg" rel="lyteshow[vacation]" title="Obrazek 2"><img src="/images/main/2.jpg" alt="Obrazek 2" style="float: left; padding: 20px 10px 0 0"/></a>

<p>
Staramy sie, aby nasza oferta glazow, otoczakow zwirow oraz wszelkiego rodzaju kamieni od krzemieni po granit, byla propozycja
do tworzenia niepowtarzalnych aranzacji nowoczesnych ogrodow
z wykorzystaniem darow natury. Jednoczesnie oferta sprzedazy
wyrobow betonowych do zludzenia przypominajacych wyroby z
drewna, to gwarancja nie tylko duzej mozliwosci tworzenia stylowych
ogrodow ale rowniez ich trwalosci na dlugie lata.
</p>

<a href="/images/galeria/poziom/5.jpg" rel="lyteshow[vacation]" title="Obrazek 5"><img src="/images/main/5.jpg" alt="Obrazek 5" style="float: right; padding: 15px 0 0 10px"/></a>

<p>
Dbajac o niskie ceny sprzedawanych wyrobow staramy sie skutecznie
wyszukiwac kamienie o egzotycznym wygladzie, lecz stanowiace
polskie odpowiedniki drogich kamieni sprowadzanych z zagranicy.
Znosza one lepiej nasze warunki atmosferyczne. Z pelna satysfakcja
polecamy zwiry, piaski , grysy, krzemienie, granity,
wielogatunkowe glazy i wyroby drewnopodobne dostepne caly
czas na naszym skladzie. Zaopatrujemy rynek woj. malopolskiego
</p>

