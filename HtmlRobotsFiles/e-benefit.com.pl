<h1>O nas</h1>

<b>Agencja reklamowa Benefit - Studio Dzwieku i Reklamy</b> zostalo utworzone dzieki doswiadczeniu zdobytemu podczas pracy w agencjach reklamowych oraz innych firmach zwiazanych z ta branza. Oferujemy Panstwu wieloletnia praktyke w dziedzinie tworzenia reklam wizualnych, zaczynajac od projektu, poprzez realizacje, az do ostatecznego montazu.<br/> Cechuje nas sumiennosc, rzetelnosc oraz terminowa realizacja zlecen.<br/>
Benefit jako Studio Dzwieku istnieje na lodzkim rynku od 1996 roku. Od ponad dwoch lat zajmujemy sie rowniez skutecznym reklamowaniem naszych klientow w internecie. Reklama ta zapewnia calodobowa transmisje za wzglednie niska cene. Wiedza, doswiadczenie i zaufanie naszych klientow daly nam ogromne mozliwosci rozwoju.<br/> Reklama tworzona przez nasz zespol jest kreatywna i nieszablonowa. Nasi pracownicy oprocz doswiadczenia i wyksztalcenia zawodowego posiadaja nietuzinkowe pomysly, ktore z pasja i zaangazowaniem wykorzystuja w tworzeniu ciekawych projektow.<br/><br/>

Nasza agencja oferuje swoim klientom mnostwo narzedzi do najskuteczniejszej reklamy: profesjonalne strony www, e-sklepy, pozycjonowanie w Google, wizytowki, katalogi, ulotki, reklamy swietlne, banery itp. Reklama tworzona przez nasz zespol to gwarancja skutecznosci. Staramy sie realizowac wszystkie Panstwa pomysly, jak rowniez zasypywac Was najnowszymi trendami. <br/><br/>
Jestesmy mlodym, dynamicznym zespolem, otwartym na wszelkie wyzwania, takze te, ktore wydaja sie niemozliwe do realizacji.
