<h2 class="dark-blue section-width-80-h2 weight700">Inicjatywa</h2>
<p class="dark-blue font20">
Misja Klubu HR jest <strong>zbudowanie spolecznosci osob, dla ktorych istotne jest podniesienie kompetencji dzialow zarzadzania kapitalem ludzkim</strong> i uczynienie ich strategicznymi partnerami firmy aktywnie uczestniczacymi w procesie podejmowania decyzji. Rozumiejac role, jaka HR powinien pelnic we wspolczesnych organizacjach, konieczna jest zmiana podejscia do zadan stojacych przed nim. Aby zostac <strong>HR Business Partnerem</strong> nie wystarczy dzis wiedza z zakresu zarzadzania kapitalem ludzkim. Niezbedna jest znajomosc biznesu i procesow w nim zachodzacych oraz umiejetne laczenie z dzialaniami z obszaru HR.
</p>
<p class="dark-blue font20">
Klub HR ma pomoc osobom znajdujacych sie na roznych szczeblach rozwoju kariery w obszarze zarzadzania kapitalem ludzkim zdobyc wiedze i niezbedne kompetencje. Pozwoli to im spojrzec na organizacje z szerszej perspektywy, a nie tylko z punktu widzenia zadan jednego dzialu.
</p>
<p class="dark-blue font20">
<strong>Klub ma tworzyc przestrzen do dyskusji nad rozwojem HR i aktywnym wlaczeniem jego w procesy decyzyjne firm.</strong>
</p>

