Ewolucja roli specjalisty ds. personalnych jest naturalnym wynikiem zmian zachodzacych w srodowisku biznesowym. Tym samym wychodzi ona poza ramy prostego swiadczenia uslug i staje sie relacja partnerska, w wyniku ktorej tworzone sa innowacje, decydujace dzis o konkurencyjnosci firm. Dzial HR, nie chcac ograniczac sie jedynie do zadan kadrowo-placowych, musi zyskac szersza perspektywe, <strong>zrozumiec funkcjonowanie biznesu, zlozonosc procesow, funkcjonowanie kazdego dzialu i na podstawie tej wiedzy budowac polityke firmy</strong>, poczynajac od pozyskiwania pracownikow, poprzez zagwarantowanie im odpowiednich sciezek rozwoju, dbanie o przejrzysta komunikacje wewnatrz organizacji az po dzialania wizerunkowe i postrzeganie marki na zewnatrz. Dzieki takiemu podejsciu i otwartosci ma on szanse <strong>stac sie partnerem do dyskusji z zarzadem i wplywac na strategie firmy</strong> oraz wizje jej przyszlosci. Aby jednak uzyskac wszechstronny oglad sytuacji i zrozumiec zachodzace procesy biznesowe, niezbedna jest wiedza, ktora wykracza poza obszar HR.
</p>
<p class="dark-blue">
Nie wszystkie dzialy HR odnajduja sie w obecnej rzeczywistosci. Problemem moze byc brak kompetencji pozwalajacych decydowac o przyszlosci firmy, ale i checi do interdyscyplinarnego rozwoju. To powoduje, ze sa one odsuwane od strategicznych zadan firm, a tym samym ich rola jest marginalizowana.
</p>
<p class="dark-blue">
<strong>Klub HR ma pomoc dzialom HR wyjsc z tego impasu</strong>. Naszym celem jest wiec nie tylko przekazywanie wiedzy, ale rowniez pokazanie, <strong>jak skutecznie mozna ja wdrozyc w codzienna aktywnosc biznesowa</strong>.
</p>
