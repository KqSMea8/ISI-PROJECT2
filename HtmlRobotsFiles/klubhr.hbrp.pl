<h2 class="dark-blue section-width-80-h2 weight700">Aktywnosci</h2>
<h3 class="dark-blue">Zdobywaj nowe kluczowe umiejetnosci, dzieki ktorym zwiekszysz swoj udzial w tworzeniu wartosci firmy i realizacji jej strategii</h3>
<p>Uczestnicy Klubu HR maja mozliwosc brac udzial w cyklicznie organizowanych spotkaniach z prelegentami zajmujacymi sie roznymi obszarami biznesu. Poza zdobyciem wiedzy, spotkania te sa rowniez okazja do wymiany doswiadczen i interesujacych rozwiazan miedzy specjalistami ds. HR pracujacymi w firmach roznej wielkosci i w roznych branzach.  </p>

<p class="dark-blue font20"></p>
<div>
<div class="service width">
<p class="dark-blue"><strong class="weight700 "><a href="/spotkania-networkingowe#wiedza">SPOTKANIA NETWORKINGOWE</a></strong><br/>
Czlonkowie Klubu HR biora udzial w zamknietych sniadaniach biznesowych oraz spotkaniach networkingowych, podczas ktorych maja okazje spedzic czas na ciekawych rozmowach, a takze wysluchac wystapien ekspertow.</p>
</div>
<div class="service width">
<p class="dark-blue"><strong class="weight700 "><a href="/webinaria#wiedza">INSPIRUJACE NAGRANIA</a></strong><br/>
Regularnie zapraszamy czlonkow klubu do udzialu w webinariach z naszymi ekspertami. Kazda sesja online to dawka solidnej wiedzy opatrzonej merytorycznym komentarzem ze strony wykladowcow ICAN Institute oraz ekspertow klubu.</p>
</div>
<div class="service width">
<p class="dark-blue"><strong class="weight700 "><a href="/praktyczne-wskazowki#wiedza">STREFA WIEDZY</a></strong><br/>
Regularny zastrzyk inspiracji od redakcji Harvard Business Review Polska. W zakladce Klubu HR na stronie ,,Harvard Business Review Polska" publikujemy wyselekcjonowane artykuly i materialy dotyczace tematyki HR.</p>
