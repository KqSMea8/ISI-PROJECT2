<h2>O firmie</h2>

<p>
Jestesmy profesjonalnym zakladem stolarskim produkujacym trumny juz <strong>od ponad 15 lat</strong>.<br/>
Oferowane przez nas produkty cechuje: wysoka estetyka  wykonania, oryginalne wzornictwo, dostawy  zawsze na czas oraz najlepszy stosunek jakosci do ceny.
Zajmujemy  sie   produkcja  trumien zarowno z drewna miekkiego jak i twardego. Nasze produkty dostosowane sa do indywidualnych potrzeb klientow w oparciu o wieloletnie doswiadczenie, odpowiednie doradztwo, profesjonalne wykonanie oraz potrzeby lokalnych rynkow zbytu.
</p>

