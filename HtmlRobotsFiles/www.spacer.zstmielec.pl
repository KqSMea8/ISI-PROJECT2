<div class="right"><h1>2. Co jest zaleta takiego serwisu?</h1><br>
<b><span style="color: #ff9900">Wirtualny</span></b> spacer po Zespole Szkol Technicznych w Mielcu to innowacyjny projekt, dzieki ktoremu bez wychodzenia z domu, przed ekranem swojego monitora mozesz zwiedzic nasza szkole. <br><br>
<b><span style="color: #ff9900">Celem</span></b> tego serwisu jest przedstawienie szkoly osobom chetnym do kontynuowania w niej swojej dalszej edukacji.<br><br>
<b><span style="color: #ff9900">Przed</span></b> wyborem szkoly warto zapoznac sie z jej ukladem i wyposazeniem pracowni lekcyjnych. Projekt ten ci to umozliwia. Zespol Szkol Technicznych w Mielcu to jedyna szkola ponadgimnazjalna, ktora oferuje mozliwosc wirtualnego zwiedzenia kazdej sali.<br><br>
<b><span style="color: #ff9900">Do stworzenia</span></b> tego projektu wykonano okolo <span style="color: #ff9900"><b>1500 zdjec</b></span>, z ktorych powstalo <span style="color: #ff9900"><b>100 panoram</b></span> w technologii Flash polaczonych w wirtualny spacer.
