<div class="left"><h1>1. W jaki sposob dziala ten serwis?</h1><br>
<b><span style="color: #ff9900">Poruszanie sie po stronie:</span></b><br>
<b>1.</b> Wybierz interesujaca cie czesc szkoly.<br>
<b>2.</b> Kliknij w punkt, aby otworzyc dana sale.<br>
<b>3.</b> Kliknij w przycisk ,,Wstecz", aby powrocic do poprzednio odwiedzonej strony.<br>
<b>4.</b> Przycisk ,,Strona Glowna" przekieruje cie do widoku wyboru segmentu.<br><br>
<b><span style="color: #ff9900">Poruszanie sie po panoramach:</span></b><br>
<b>1.</b> Kliknij strzalke w lewo, aby obrocic panorame w lewa strone.<br>
<b>2.</b> Kliknij strzalke w prawo, aby obrocic panorame w prawa strone.<br>
<b>3.</b> Panorame mozesz obrocic rowniez za pomoca myszki.<br>
<b>4.</b> Kliknij zakrecona strzalke, aby wlaczyc/wylaczyc automatyczne obracanie panoramy.<br>
<b>5.</b> Kliknij w zaznaczone punkty w salach, aby przemieszczac sie pomiedzy obszarami.
