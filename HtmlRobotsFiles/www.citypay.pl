<!-- Jak placic -->
<h1>Informacja o CityPay</h1>
<p>Serwis platnosci mobilnych CityPay umozliwia dokonywanie platnosci za parking przy uzyciu telefonu komorkowego. Nie jest przy tym wymagane podchodzenie do parkomatow lub pozostawianie potwierdzenia dokonania oplaty parkingu za szyba. Kontroler, widzac numer rejestracyjny samochodu otrzyma informacje o dokonanej oplacie parkingowej.
Oplate za parkowanie mozna dokonac z telefonu komorkowego dowolnej marki oraz niezaleznie od posiadanego abonamentu i umowy z operatorem.
Aby placic za parkowanie telefonem nalezy jedynie zarejestrowac sie w serwisie CityPay. Nie sa przy tym potrzebne zadne dane osobowe a jedynie numer telefonu komorkowego z ktorego beda dokonywane platnosci oraz numer rejestracyjny samochodu.
</p>

<p>Serwis CityPay jest prowadzony przez Miasto - na podstawie zalaczonego Regulaminu CityPay.</p>
