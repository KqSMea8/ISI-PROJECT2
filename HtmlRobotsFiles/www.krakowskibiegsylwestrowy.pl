<h2 class="art-postheader">

<img src="/wp-content/themes/Sport_Girl/Sport_Girl/images/postheadericon.png" width="26" height="26" alt="postheadericon" />

<a href="/" rel="bookmark" title="Permanent Link to Informacje o Krakowskim Biegu Sylwestrowym">

Informacje o Krakowskim Biegu Sylwestrowym</a>

</h2>

<div class="art-postcontent">

<!-- article-content -->



<div style="margin: 1px; float: left;">


</div>

<p>Od kilku juz lat w ostatni dzien Starego Roku odbywa sie w Krakowie &#8211; Bieg Sylwestrowy. Cieszy sie on nie mniejszym zainteresowaniem niz <a title="Imprezy biegowe w Krakowie " href="/imprezy-biegowe-w-krakowie-i-okolicach/"><strong>Cracovia Maraton,</strong></a> ktory ma miejsce kilka miesiecy pozniej. W obydwu wypadkach zainteresowanie impreza jest ogromne. Do miasta specjalnie na te okazje sciagaja zawodnicy i turysci z kraju i z zagranicy.</p>
<p>Szczegolowe<strong> informacje o Krakowskim Biegu Sylwestrowym</strong>,  zawiera <a title="Regulamin Biegu Sylwestrowego w Krakowie" href="/regulamin-biegu-sylwestrowego/"><strong>regulamin imprezy sylwestrowej.</strong></a> Znajdziemy w nim wskazowki, co do samych zawodow ale rowniez przeczytamy o nagrodach, jakie czekaja na zwyciezcow. Nie trzeba chyba nikogo przekonywac do tego, ze udzial w <strong>rywalizacji sportowej</strong> to doskonaly pomysl na <a title="Gdzie spedzic Sylwestra?" href="/sylwester-krakow/"><strong>Sylwestra w Krakowie</strong></a>. Bo czyz bieg nie moze byc idealna rozgrzewka przed dalszymi zabawami i szalenstwami nocy sylwestrowej?</p>
<p>W <a title="Program Biegu Krakowskiego" href="/program-imprezy-sylwestrowej/"><strong>programie Biegu Sylwestrowego</strong></a> znajduje sie nie tylko sama rywalizacja ale rowniez rozgrzewka i wspolny poczestunek. Impreza stanowi wiec doskonala okazje do zawierania nowych znajomosci i rozmow. Oprocz tego zawodnicy i ich rodziny poznaja historie i kulture wspanialego grodu Kraka, ktore na co dzien gina gdzies w natloku zwyklych spraw.</p>
<p>Uczestnicy biegu maja do wyboru dwie trasy:<a title="Sciezki Biegu Sylwestrowego" href="/trasy-krakowskiego-biegu-sylwestrowego/"><strong> Smocza Piatke i Radosna Dziesiatke.</strong> </a>Pierwsza liczy 10km, druga 5km. Start w obydwu jest uzalezniony od wieku, wniesienia oplaty startowej i zlozenia oswiadczenia o braku przeciwwskazan lekarskich. Trasy Biegu Sylwestrowego pokrywaja sie czesciowo z jedna ze sciezek biegowych, jakie w ostatnim czasie powstaly w miescie.<a title="Sciezki Nordic Walking w Krakowie" href="/sciezki-biegowe-i-nordic-walking-w-krakowie/"><strong> Krakowskie sciezki do biegania i spacerowania</strong></a> to odrebny temat ale warto tutaj niewatpliwie o nim wspomniec. Ich popularnosc jest wyrazem wzrastajacej swiadomosci zdrowotnej Polakow. Potrafia oni bowiem dostrzec <a title="Czy warto biegac?" href="/dlaczego-warto-biegac/"><strong>korzysci z biegania.</strong></a></p>
<p><a title="Rady dla biegaczy" href="/porady-dla-biegaczy/"><strong>Wskazowki dla biegaczy</strong></a>, czyli o czym powinni pamietac uczestnicy Krakowskiego Biegu Sylwestrowego:</p>
<ul>
<li>na trasach obowiazuje zachowanie szczegolnych zasad bezpieczenstwa i rywalizacji fair play,</li>
<li>wszystkich uczestnikow obowiazuje obuwie a takze odpowiednia odziez taka jak pantofle dostepne na stronie <a href="http://naturalstyle.pl">naturalstyle.pl</a>,</li>
<li>przed startem nalezy wykonac odpowiednie cwiczenia rozciagajace i rozgrzewajace,</li>
<li>w razie jakichkolwiek problemow podczas biegu, nalezy niezwlocznie poinformowac o nich sluzby porzadkowe.</li>
</ul>
<p>&nbsp;</p>


