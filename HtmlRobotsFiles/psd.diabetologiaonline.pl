<div class="title">Zaproszenie</div>
<div class="text"><p><strong>Szanowni Panstwo,</strong></p>
<p>&nbsp;</p>
<p>juz <strong>19 marca 2016 roku</strong>, w Bialymstoku, bedziemy mogli spotkac sie z Panstwem po raz &oacute;smy na Podlaskich Spotkaniach Diabetologicznych.</p>
<p>&nbsp;</p>
<p>Spotkania staly sie waznym wydarzeniem edukacyjnym regionu podlaskiego &ndash; zyskaly uznanie w srodowisku medycznym i zostaly uznane za ciekawe forum wymiany poglad&oacute;w i doswiadczen.</p>
<p>&nbsp;</p>
<p>Podlaskie Spotkania Diabetologiczne:</p>
<ul>
<li>sa konferencja w regionie <strong>pod patronatem PTD</strong></li>
<li>goszcza znanych i doswiadczonych naukowc&oacute;w z calej Polski <strong>&nbsp;</strong></li>
<li>maja dlugoletnia tradycje &ndash; organizowane sa juz <strong>po raz &oacute;smy</strong></li>
<li>ciesza sie duzym zainteresowaniem uczestnik&oacute;w<strong></strong></li>
</ul>
<div>&nbsp;</div>
<p>Wysoki poziom merytoryczny konferencji gwarantuja bogate doswiadczenie i&nbsp;wiedza wykladowc&oacute;w, kt&oacute;rymi sa uznani eksperci w dziedzinie diabetologii. Tradycyjnie, organizatorem merytorycznym Podlaskich Spotkan Diabetologicznych jest Klinika Endokrynologii, Diabetologii i&nbsp;Chor&oacute;b Wewnetrznych Uniwersytetu Medycznego w&nbsp;Bialymstoku. Patronat naukowy nad konferencja objal ponownie Bialostocki Oddzial Polskiego Towarzystwa Diabetologicznego oraz Sekcja Diabetologiczna Polskiego Towarzystwa Endokrynologicznego.</p>
<p>W tym roku temat przewodni konferencji stanowi zagadnienie:&nbsp;<span style="font-size: medium;"><strong>&bdquo;Czy terapia cukrzycy jest trudna?&rdquo;.</strong></span></p>
<p><strong><br /></strong></p>
<p>Do sukcesu Podlaskich Spotkan Diabetologicznych przyczynilo sie r&oacute;wniez wsparcie firm farmaceutycznych oraz innych podmiot&oacute;w zaangazowanych w&nbsp;edukacje&nbsp; diabetologiczna. Udzial w konferencji jest zatem wyjatkowa okazja utrwalenia pozycji Panstwa firmy na Podlasiu jako partnera w edukacji, utrzymania wizerunku firmy jako nowoczesnej i zorientowanej na&nbsp; pacjenta, dotarcia do przedstawicieli podlaskiego srodowiska lekarskiego z&nbsp;wlasnym przekazem reklamowym oraz owocnych spotkan z&nbsp;uczestnikami konferencji podczas wydarzenia o uznanej wartosci naukowej <br /> i edukacyjnej.</p>
<p>&nbsp;</p>
<p>Bardzo liczymy na Panstwa zaangazowanie w projekt. Dolozymy wszelkich staran, aby zapewnic atrakcyjna forme prezentacji Panstwa oferty w dniu wydarzenia, z&nbsp;mozliwoscia jej rozszerzenia przed i po wydarzeniu.</p>
<p>&nbsp;</p>
<p>Goraco zachecamy Panstwa do kontynuacji lub podjecia wsp&oacute;lnych dzialan na rzecz propagowania wiedzy na temat cukrzycy.</p></div>
