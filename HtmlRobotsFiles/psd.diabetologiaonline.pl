<div class="title">Podsumowanie VII Spotkan</div>
<div class="text"><p><em>&nbsp; &nbsp; &nbsp; &nbsp;<strong> &bdquo;Terapia cukrzycy &ndash; latwa czy trudna?&rdquo;</strong> &ndash; pod takim haslem mialy miejsce kolejne, <strong>si&oacute;dme</strong> juz <strong>Podlaskie Spotkania Diabetologiczne w Bialymstoku</strong> pod patronatem portalu dla lekarzy i pacjent&oacute;w <strong>Diabetologionline.pl</strong></em></p>
<p><em><br /></em></p>
<p><em><em>&nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</em>Konferencje w dniu 28 lutego 2015 roku tradycyjnie otworzyla <strong>prof. Ida Kinalska</strong>,<strong> </strong>a wysoki poziom merytoryczny konferencji zagwarantowalo wielu <strong>wybitnych wykladowc&oacute;w</strong> w tym m.in. <strong>gosc specjalny konferencji prof. Maciej Malecki</strong> z Krakowa. Niezwyklym zainteresowaniem cieszyly sie r&oacute;wniez omawiane przypadki przez mlodych lekarzy z Uniwersytetu Medycznego w Bialymstoku. Wydarzenie w tym roku zgromadzilo ponad 150 os&oacute;b oraz kilkanascie firm &ndash; przedstawicieli branzy farmaceutycznej.</em></p>
<p><em><br /></em></p>
<p><em>&nbsp; &nbsp; &nbsp; &nbsp; Tradycyjnie, patronem merytorycznym Podlaskich Spotkan Diabetologicznych byla Klinika Endokrynologii, Diabetologii i&nbsp;Chor&oacute;b Wewnetrznych Uniwersytetu Medycznego w&nbsp;Bialymstoku &ndash; pod kierownictwem<strong> prof. Marii G&oacute;rskiej</strong>, a patronat naukowy nad konferencja objal ponownie Bialostocki Oddzial Polskiego Towarzystwa Diabetologicznego &ndash; pod przewodnictwem <strong>prof. Iriny Kowalskiej</strong> oraz Sekcja Diabetologiczna Polskiego Towarzystwa Endokrynologicznego &ndash; pod przewodnictwem <strong>prof. Malgorzaty Szelachowskiej.</strong></em></p>
