<h1>Willa Salamandra</h1>
<div id="strona2">
<p>Serdecznie witamy i zapraszamy do  <strong>Willi ,,Salamandra"</strong> w <strong>Krynicy-Zdroju</strong>. </p>
<p>Willa  polozona jest w malowniczym otoczeniu lasow i lak w poblizu kolejki gondolowej na Jaworzyne Krynicka (1114 m). Okolice stwarzaja doskonale warunki do uprawiania turystyki pieszej, rowerowej oraz konnej, a takze sportow letnich i zimowych. Atrakcje gwarantuja znajdujace sie w poblizu: kompleks narsiarski w Jaworzynie Krynickiej, lodowisko, sankostrada, Park Sportowy, Pijalnia Wod, biegowe trasy narciarskie. Okolo 100 m od willi znajduje sie kompleks sportowy z m.in. kortami tenisowymi i rampa do skateboardingu.</p>
<p>Willa oferuje kameralne, komfortowe  pokoje z lazienkami. Kazdy pokoj jest wyposazony w TV oraz bezplatny internet WiFi. </p>
<p>W osrodku jest  multimedialna sala konferencyjno-bankietowa na 30 osob, kacik kawowy i salon  wypoczynkowy. Istnieje mozliwosc calodziennego wyzywienia. </p>
<p>Wokol willi teren jest ogrodzony a miejsca parkingowe sa bezplatne i monitorowane. </p>
<p>Zapraszamy kazdego i o kazdej porze roku.</p>
