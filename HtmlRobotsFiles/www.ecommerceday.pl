<header class="header section-title mt-90 mb-50">
<h2>Witaj na eCommerce Day Poland 2015</h2>
</header>
<div class="content mb-110">
<p>EDP 2015 to juz druga edycja najwiekszej konferencji w Polsce skierowanej wprost do wlascicieli sklepow internetowych. Podczas wydarzenia e-sprzedawcy otrzymaja odpowiedzi na najwazniejsze pytania zwiazane ze sprzedaza w internecie, a informacji ponownie dostarcza im przedstawiciele takich firm jak <strong>Google, Facebook, PayPal</strong> czy <strong>Shoplo</strong>.</p>

<p>Zdobadz niezbedna wiedze ecommerce razem z nami. 1 dzien. 13 prezentacji. 510 minut z najwazniejszymi ekspertami w ehandlu. Jezeli sprzedajesz online, po prostu nie moze Cie tu zabraknac.</p>
</div>
</div>
</section>

