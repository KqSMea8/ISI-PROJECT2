function checkCookie(sSearchCookieName) {

var aCookies = document.cookie.split(';'),
iCookiesLen = aCookies.length,
sCookieName;

for (var i = 0; i < iCookiesLen; i++) {
sCookieName = aCookies[i].substr(0, aCookies[i].indexOf('='));
if ($.trim(sCookieName) == sSearchCookieName) {
return true;
}
}
return false;
}

