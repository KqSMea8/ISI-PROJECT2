<div class="module-body-center" id="content"><h1>Witamy serdecznie na stronie Ilawskiej Kroniki Cyfrowej</h1>
<div class="article"><p>Celem Ilawskiej Kroniki Cyfrowej jest prezentacja w Internecie kulturowego dziedzictwa Ilawy i jej regionu w dawnej i wspolczesnej roznorodnosci,  wspieranie dzialalnosci dydaktycznej i edukacyjnej oraz publikowanie naukowego dorobku regionu.</p><p>Zapraszajac Panstwa do korzystania z naszych zbiorow, prosimy takze o pomoc w ich gromadzeniu, a szczegolnie o dary w postaci zdjec, ulotek, programow, plakatow oraz o dzielenie  sie swoimi wspomnieniami.</p><p>Czekamy na Panstwa w Czytelni Ogolnej Miejskiej Biblioteki Publicznej w Ilawie, Ilawa, ul. Jagiellonczyka 3 w: poniedzialki, wtorki, czwartki i piatki od godz. 10:00 do 18:00 oraz  w srody od 9:00 do 15:00.</p><p>Wszystkich Czytelnikow prosimy o kontakt, jesli moga pomoc w identyfikacji autorow lub w dokladniejszym opisaniu zamieszczonych na naszych stronach fotografii.<br /></p></div></div>

