<h1 class="entry-title">O firmie</h1>
</header><!-- .entry-header -->

<div class="entry-content">
<p>&nbsp;</p>
<p style="text-align: justify;">Witam Panstwa na naszej stronie poswieconej siatkom transportowym, ktore sa uzywane w urzadzeniach pracujacych w transporcie wewnetrznym w zakladach przemyslowych.</p>
<p style="text-align: justify;">Nasze siatki transportowe spelniaja rygorystyczne normy jakosciowe co powoduje, ze wiele z nich moze z powodzeniem pracowac w przedsiebiorstwach farmaceutycznych oraz spozywczych. Tasmy stalowe dostarczamy wraz z atestem PZH HZ.</p>
<p style="text-align: justify;">Wszystkie siatki transportowe sa wykonywane pod projekt klienta, posiadamy szeroka game dostepnych modeli tasm tkanych - zaplatanych jak i tasm modulowych.<img class="aligncenter size-full wp-image-86" src="http://siatkistalowe.pl/wp-content/uploads/2013/09/tasmy-modulowe-Intraloz-tasmy-habasit-amerrall.jpg" alt="" width="1875" height="471" srcset="http://siatkistalowe.pl/wp-content/uploads/2013/09/tasmy-modulowe-Intraloz-tasmy-habasit-amerrall.jpg 1875w, http://siatkistalowe.pl/wp-content/uploads/2013/09/tasmy-modulowe-Intraloz-tasmy-habasit-amerrall-300x75.jpg 300w, http://siatkistalowe.pl/wp-content/uploads/2013/09/tasmy-modulowe-Intraloz-tasmy-habasit-amerrall-768x193.jpg 768w, http://siatkistalowe.pl/wp-content/uploads/2013/09/tasmy-modulowe-Intraloz-tasmy-habasit-amerrall-1024x257.jpg 1024w, http://siatkistalowe.pl/wp-content/uploads/2013/09/tasmy-modulowe-Intraloz-tasmy-habasit-amerrall-500x126.jpg 500w" sizes="(max-width: 1875px) 100vw, 1875px" /></p>
<p style="text-align: justify;">
<div></div>
<div> Zapraszam do zapoznania sie z oferowanymi stalowymi siatkami transportowymi. Nasi doradcy techniczni z przyjemnoscia i cala odpowiedzialnoscia doloza wszelkich staran by opracowac dla Panstwa najbardziej optymalna tasme transportowa</div>
