<h2 class="center title ico-border">Globalny zasieg</h2>
<div class="center-box">
<div class="line-head"></div>
</div>
<div class="bm-desc-box">
<div class="bm-display">
<div class="lrc-content content">
<p>Jestesmy jedna z najwiekszych firm obslugujacych fundusze inwestycyjne w Europie. Siedziba firmy miesci sie w Warszawie. W Luksemburgu dziala nasza platforma dystrybucyjna. Mamy przedstawicielstwa we Frankfurcie, Wiedniu i Brnie. Obslugujemy Klient&oacute;w z blisko 140 kraj&oacute;w i ponad 25 jurysdykcji. Dzialamy globalnie, realizujac indywidualne potrzeby.</p>                    </div>
