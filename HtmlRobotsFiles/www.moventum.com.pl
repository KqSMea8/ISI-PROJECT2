<section class="section section--box_map"  id="ob298" style="background-image: url('')">
<div class="inner">
<h2 class="center title ico-border">Wlasne systemy</h2>
<div class="center-box">
<div class="line-head"></div>
</div>
<div class="bm-desc-box">
<div class="bm-display">
<div class="lrc-content content">
<p>W obsludze funduszy inwestycyjnych stosujemy wlasne systemy informatyczne, stworzone przez nasz zesp&oacute;l inzynier&oacute;w. Sa oparte na najnowszych technologiach i wysoce skalowalne, a ich funkcjonalnosc umozliwia obsluge firm w r&oacute;znych systemach prawnych. Stawiamy na innowacyjnosc rozwiazan, szybkosc i&nbsp;skutecznosc wdrozen. Elastycznie dostosowujemy technologie do obslugi produkt&oacute;w z nietypowymi wymaganiami. Wysoka jakosc naszych uslug w zakresie agenta transferowego i ksiegowosci funduszy byla potwierdzana wysokimi ocenami w globalnych rankingach.&nbsp;Nasze rozwiazania technologiczne w obszarze enkrypcji danych byly nominowane do prestizowych miedzynarodowych nagr&oacute;d branzowych.&nbsp;</p>                    </div>
</div>
</div>
</div>
</section>
<section class="section section--box_map"  id="ob299" style="background-image: url('')">
<div class="inner">
<h2 class="center title ico-border">Najwyzsze standardy</h2>
<div class="center-box">
<div class="line-head"></div>
</div>
<div class="bm-desc-box">
<div class="bm-display">
<div class="lrc-content content">
<p>Od lat cieszymy sie zaufaniem naszych Klient&oacute;w,&nbsp;wsr&oacute;d kt&oacute;rych sa najwieksze miedzynarodowe instytucje finansowe. Stosowany przez nas System Zarzadzania Bezpieczenstwem Informacji jest zgodny z norma ISO 27001. System kontroli wewnetrznych jest corocznie badany przez audytora zgodnie ze standardem ISAE 3402, opublikowanym przez International Federation of Accountants (IFAC). Cyklicznie zlecamy badanie podatnosci naszych system&oacute;w informatycznych, a ich rozw&oacute;j jest realizowany w oparciu o najwyzsze standardy i najlepsze praktyki rynkowe.</p>                    </div>
