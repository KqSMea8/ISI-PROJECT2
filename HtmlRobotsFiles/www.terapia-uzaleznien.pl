<p>Najczesciej kiedy myslimy o uzaleznieniach mamy na mysli substancje psychoaktywne takie jak: alkohol, narkotyki, leki. Trudniej jest sobie wyobrazic, ze uzaleznic mozemy sie rowniez od przedmiotow czy zachowan.</p>
<p>Tymczasem&#8230;</p>
<p>dzisiejszy swiat, cywilizacja, technika niosa ze soba wiele nowoczesnych rozwiazan, ulatwien, ciekawostek &#8230;. nie mniej jednak niz zagrozen, stresu, pulapek, w ktore wspolczesny czlowiek wpada, nie majac nawet tego swiadomosci.</p>
<p>To co mialo byc przyjemnoscia, zabawa &#8230; stalo sie przymusem&#8230;</p>
<p>Najwieksze pulapki naszych czasow, poza uzaleznieniem od alkoholu, nikotyny, narkotykow, lekow, to praca, hazard, zakupy, komputer, internet, telefon komorkowy, jedzenie.</p>
