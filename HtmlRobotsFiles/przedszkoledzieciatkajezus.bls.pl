<div class="witamy">WITAMY NA NASZEJ STRONIE</div>
<div class="tresc-strony-l">
<h3></h3>
<p>
<div class="_5pbx userContent _3576" id="js_ml">
<p style="text-align:center">&nbsp;</p>

<p><span style="color:#0000FF"><span style="font-size:18px"><strong>Oferta na rok szkolny 2019/2020</strong></span></span></p>

<p style="text-align:justify">Nasze Przedszkole to miejsce, w kt&oacute;rym dzieci spedzaja czas w &nbsp;milej, rodzinnej atmosferze. Podczas swojego pobytu zdobywaja one wiedze o otaczajacym nas swiecie, ucza sie wsp&oacute;ldzialania w grupie oraz samodzielnego podejmowania decyzji.</p>

<p style="text-align:justify">To wszystko pod opieka wykwalifikowanej i przyjaznej kadry. Proces wychowawczy prowadzimy w oparciu o system wartosci uznanych przez Kosci&oacute;l Katolicki, w szczeg&oacute;lnosci personalistycznej koncepcji czlowieka jako osoby, gloszonej przez sw. Jana Pawla II. &nbsp;Dzieci ucza sie szacunku do drugiego czlowieka, empatii oraz wrazliwosci na dobro i piekno.</p>

<p><strong>W naszych przedszkolach odbywaja sie zajecia dodatkowe:</strong></p>

<ul>
<li>jezyk angielski</li>
<li>logorytmika</li>
<li>religia</li>
<li>tance</li>
</ul>

<p style="margin-left:-15.0pt">&nbsp; &nbsp; &nbsp; &nbsp;<strong>Realizujemy r&oacute;wniez projekty edukacyjne:</strong></p>

<ul>
<li>Dogoterapia</li>
<li>&bdquo;Poznaje zawody&rdquo;-spotkania z ciekawymi przedstawicielami r&oacute;znych zawod&oacute;w</li>
<li>Maly fizyk- doswiadczenia</li>
<li>Kuchcikowo- zajecia kulinarne</li>
<li>Zajecia z ekologii</li>
<li>Zajecia regionalne</li>
<li>Zajecia teatralne</li>
<li>Zajecia plastyczno-techniczne</li>
<li>Zajecia muzyczne- tance, spiew oraz granie na instrumentach</li>
<li>spotkania z ciekawymi ludzmi</li>
</ul>

<p>Organizujemy wyjazdy do teatru, muze&oacute;w oraz innych ciekawych miejsc, kt&oacute;re maja wplyw na rozw&oacute;j naszych przedszkolak&oacute;w.</p>

<p>Serdecznie zapraszamy rodzic&oacute;w do osobistego kontaktu z dyrekcja oraz pracownikami przedszkola.</p>
