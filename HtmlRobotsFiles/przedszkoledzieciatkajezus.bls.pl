<p style="text-align: center;">&nbsp;</p>

<p style="text-align: center;">Jestesmy przedszkolem, kt&oacute;re umozliwi&nbsp;dzieciom ich wszechstronny rozw&oacute;j w duchu wartosci uniwersalnych i chrzescijanskich&rdquo;</p>
