Zaklad powstal w 1974 roku, poprzez kolejne lata zostal powiekszony i zmodernizowany tak, aby mogl sprostac zmianom technoligii
i rosnacym potrzebom rynku. Firma rodzinna przekazywana z pokolenia na pokolenie. W obecnych czasach dzialalnosc polega glownie
na wykonywaniu schodow i drzwi jak rowniez wykonujemy uslugi wedlug zlecenia klienta. Pomiary i kalkulacje wykonujemy
bezplatnie.
