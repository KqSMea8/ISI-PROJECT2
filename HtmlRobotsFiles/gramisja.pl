<p class="p1"><strong>Gra toczy sie do chwili, w ktorej jeden z graczy podniesie ostatni zeton</strong> (zeton odrzucany jest wraz z dociagnieta karta na stos zuzytych kart i zetonow gracza, ktory go podniosl - patrz: liczenie punktow). Zwyciezca zostaje zdobywca najwiekszej ilosci punktow (symbol gwiazdki).</p>
<p>&#8222;Badz iskra, ktora zapali swiat&#8221; mowil zalozyciel jezuitow do swoich przyjaciol, ktorych wysylal na misje. Dzieki grze MISJA odkryjesz w sobie <strong>przywodce</strong>, obudzisz <strong>odwage</strong>, a moze nawet <strong>heroizm</strong>.</p>
