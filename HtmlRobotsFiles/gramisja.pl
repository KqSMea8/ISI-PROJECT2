<div class="et_pb_text et_pb_module et_pb_bg_layout_light et_pb_text_align_left  et_pb_text_4">

<p><span style="color: #ffffff;">Nie od dzis wiadomo, ze z wielka wladza idzie w parze wielka odpowiedzialnosc. Prawdziwi liderzy umieli jednak laczyc obie te rzeczy. <strong>MISJA to gra wlasnie o takim przywodztwie, w ktorym wladza jest srodkiem do celu, nigdy odwrotnie.</strong> Wcielajac sie w postac papieza Franciszka, Ignacego Loyoli, Franciszka Ksawerego czy Stanislawa Kostki, bedziecie mogli poznac nie tylko burzliwa i intrygujaca historie zakonu jezuitow, ale przede wszystkim sprawdzic swoje zdolnosci planowania i strategii.<br />
<strong><br />
Wasze zadanie?</strong> Wspierajcie dziela na terenie miasta, stawiajcie uniwersytety, szpitale, ale tez madrze pomagajcie ubogim i prowadzcie misje. Wraz z biegiem gry, odkryjecie, czym jest prawdziwe przywodztwo Dynamiki dostarcza karty nieoczekiwanych wydarzen. W drodze do celu pomoga Wam rowniez slynni jezuici, ktorzy w swoim zyciu laczyli cnoty duchowe z odwaga liderow, budujacych nie tylko wspolnote ludzi wiary, ale niejednokrotnie sprawnie dzialajace miasta.</span></p>
<p style="text-align: center;"><span style="color: #ffffff;"><strong>Pomyslowosc, heroizm, przywodztwo i milosc &#8211; oto cechy, ktore musi miec kazdy, kto wyruszy na MISJE. Odwazysz sie zbudowac swoje dziedzictwo?</strong></span></p>
