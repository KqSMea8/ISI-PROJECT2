<h2>PRZYDATNE INFORMACJE</h2>

<div class="box">

<h3>ESKK edukacja na odleglosc</h3>
<p>ESKK jest najwieksza europejska szkola prowadzaca nauczanie na odleglosc. Od 25 lat oferuje kursy oraz metody ksztalcenia na najwyzszym poziomie. Jako lider na rynku edukacyjnym w naszym kraju stwarza rowne szanse podjecia i kontynuowania nauki bez wzgledu na wiek, poziom wyksztalcenia czy stopien sprawnosci. W swojej ofercie posiada ponad 80 roznych tytulow: kursy jezykowe, zawodowe i hobbystyczne.</p>

</div>

