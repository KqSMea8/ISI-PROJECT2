<h2>Salon Tu i Teraz zostal stworzony z mysla o zaspokojeniu pragnien klientow.</h2>

<p>W wyjatkowym wnetrzu, przy wysmienitej kawie i wsrod subtelnych dzwiekow muzyki, goscie naszego salonu moga oderwac sie od codziennosci. W naszym salonie wykwalifikowani fryzjerzy i manicurzystki dbaja o wysoka jakosc uslug, poniewaz dla nas jest to nie tylko praca, ale przede wszystkim pasja, w ktora wkladamy cale swoje serce i talent. Wyjatkowo cenimy sobie pielegnacje wlosow i paznokci, dlatego korzystamy z wysokiej jakosci produktow profesjonalnych marek: do wlosow firmy  DAVINES, KEVIN MURPHY i do paznokci firmy ZOYA.</p>

<p>W Salonie Tu i Teraz zachwyca mila atmosfera, nietuzinkowy wystroj i wyspecjalizowany zespol. Kazda wizyta w nim bedzie dla Panstwa chwila relaksu i zapomnienia, do ktorej beda Panstwo chetnie wracac.</p>

