<p style="text-align: justify;">Kancelaria prowadzona przez radce prawnego Wojciecha Strzebinczyka swiadczy kompleksowa obsluge prawna z zakresu prawa polskiego i europejskiego. Kancelaria zajmuje sie zarowno biezaca obsluga prawna, jak i rozwiazywaniem konkretnych problemow wymagajacych specjalistycznej wiedzy.</p>
<p style="text-align: justify;">Oferta kancelarii skierowana jest zarowno do przedsiebiorcow jak i klientow indywidualnych i stawia na skutecznosc oraz wysoka jakosc uslug. Zarowno wlasciciel, jak i partnerzy kancelarii, to eksperci zajmujacy sie roznymi dziedzinami prawa, dzieki czemu sprawnie i skutecznie rozwiazywane sa problemy Klientow.</p>
<p style="text-align: justify;">Misja kancelarii jest swiadczenie uslug na najwyzszym poziomie merytorycznym przy zachowaniu profesjonalizmu, obiektywizmu, wiarygodnosci i pelnej lojalnosci.</p>
<p style="text-align: justify;">Priorytetem jest budowanie relacji opartej na wzajemnym zaufaniu i dobrej znajomosci potrzeb Klientow. Pelna wiedza o prowadzonej przez Klienta dzialalnosci zapewnia swiadczenie uslug prawniczych na poziomie spelniajacym oczekiwania Klienta. Pozwala to takze na indywidualne podejscie do kazdego Klienta.</p>
<p style="text-align: justify;">Klient otrzymuje gwarancje terminowej i rzetelnej realizacji zadan, a kancelaria przyklada szczegolna uwage do jasnego i wyczerpujacego przedstawienie Klientowi jego sytuacji prawnej. Jednoczesnie nadrzedna zasada dzialalnosci kancelarii jest zapewnienie Klientowi maksymalnych, wymiernych efektow, rzetelnej, konkretnej i wyczerpujacej pomocy prawnej oraz poczucia komfortu w kontakcie z kancelaria.</p>
