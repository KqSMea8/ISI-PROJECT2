<h3>&#8222;Wstep do programowania w C#. Latwy podrecznik dla poczatkujacych&#8221;</h3>
<p>Jak glosi tytul, ksiazka jest przeznaczona dla poczatkujacych - osob, ktore w ogole nie programowaly lub mialy niewielki kontakt z programowaniem. Osoby, ktore znaja juz dobrze kilka innych jezykow programowania i chca sie nauczyc C# &#8211; raczej nie skorzystaja z tego podrecznika.</p>
<p>Podrecznik jest dostepny w wersji elektronicznej bezplatnie, nie wolno go bez zgody autorow wykorzystywac w celach komercyjnych.</p>
<p><strong>Jest dostepna takze drukowana wersja ksiazki (platna).</strong> W zakladce <a title="Ksiazka drukowana" href="http://c-sharp.ue.katowice.pl/?page_id=170" >Ksiazka drukowana</a> mozna znalezc informacje na temat mozliwosci zakupu ksiazki drukowanej.</p>
<p>&nbsp;</p>
<p>Na tej stronie beda pojawialy sie informacje zwiazane z podrecznikiem oraz jego <a title="Ksiazka do pobrania" href="http://c-sharp.ue.katowice.pl/?page_id=36" >aktualna wersja (pdf)</a><strong> (wersja 2.0, styczen 2018)</strong>.</p>
<p>Czytelnicy maja tu takze mozliwosc wypowiedzenia sie na temat ksiazki - wskazania zauwazonych bledow i opisania swoich uwag. W tym celu prosimy o wypelnienie <a title="Formularz bledow" href="http://c-sharp.ue.katowice.pl/?page_id=8" >formularza bledow.</a></p>
