<h2>
O Firmie
</h2>

<p>Firma <b>Dachy-Kaczmarek</b> jest prezna firma dzialajaca na rynku
mazowieckim. Wiele wykonanych inwestycji m.in. Kosciol Rzymsko-Katolicki
w Barcicach, czy Zespol Wiezienny w Popowie, przemawia za wysokim
profesjonalizmem i jakoscia Uslug. Inwestycje wykonywane sa rowniez
kompleksowo z materialem pochodzacym z wlasnej hurtowni.<br><br>W
kontakcie z klientem stawiamy na profesjonalizm i sprawdzone
technologie. Nasza recepta na sukces to szeroka gama akcesoriow,
nieustanne samodoskonalenie oraz podwyzszanie standardow w oparciu o
fachowa i praktyczna wiedze w doborze produktow najwyzszej jakosci.
Posiadamy certyfikowane ekipy montazowe jak i kompetentnych doradcow
handlowych.</p>

