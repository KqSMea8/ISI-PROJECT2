<h2 class="oswald">Sklep motoryzacyjny MGM S.C.</h2>
<div class="panel panel-default">
<div class="panel-body">
<p>w Lublinie ul. Bursaki 19 zajmuje sie dystrybucja czesci samochodowych do wszystkich marek samochodow. Naszymi Klientami sa mieszkancy Lublina i okolic.</p>
<p>Doswiadczenie zdobyte na przestrzeni lat pozwala nam dopasowac oferte adekwatna do wymogow rynku i potrzeb Klientow. Mocna strona pracownikow naszego sklepu motoryzacyjnego jest umiejetnosc odnalezienia i dostarczenia kazdego, nawet najbardziej problemowego podzespolu.</p>
<p> Rzetelna obsluga, konkurencyjne ceny oraz mozliwosc udzielania rabatow w przypadku dlugofalowych relacji i programow lojalnosciowych to kolejne atuty, ktore sprawiaja, ze nasz sklep motoryzacyjny jest pewnym i niezawodnym partnerem w segmencie motoryzacji.</p>
<p>Sklep motoryzacyjny MGM S.C. zapewnia profesjonalne doradztwo techniczne w kazdym zakresie. Skupiamy sie na optymalnym wyborze czesci samochodowych w zaleznosci od indywidualnych potrzeb. Przeklada sie to na zadowolenie Klientow, czego najlepszym dowodem jest ich powracalnosc. Dzieki temu posiadamy liczna rzesze stalych odbiorcow czesci samochodowych, zarowno hurtowych jak i detalicznych.</p>
