<h2 class="oswald">Auto serwis MGM S.C.</h2>
<div class="panel panel-default">
<div class="panel-body">
<p>Samochody to nasza pasja, ktorej poswiecamy sie od wielu lat. Posiadamy szeroka wiedze z zakresu mechaniki pojazdowej, elektroniki samochodowej i diagnostyki komputerowej. W branzy uslug motoryzacyjnych dzialamy od 2011 roku. Nasza oferta glownie skierowana jest do wlascicieli aut z silnikami benzynowymi i silnikami Diesla.</p>
<p>Zajmujemy sie zarowno samochodami osobowymi, jak i dostawczymi o ladownosci do 3,5 t. Ponadto zapewniamy kompleksowa obsluge flot samochodowych.Siedziba naszego auto serwisu znajduje sie w na ul. Bursaki 19 box G-J.</p>
<p>Szeroka oferta uslug oraz ich wysoki poziom zadecydowaly o tym, ze jestesmy postrzegani jako solidna i rzetelna firma. Zachecamy do zapoznania sie z oferta. W naszym warsztacie samochodowym obowiazuja ceny umowne na naprawe samochodow.</p>
<h3>Auto serwis MGM S.C. - gwarancja bezpieczenstwa!</h3>
<p>Dajemy gwarancje doskonalej sprawnosci technicznej pojazdow, ktore opuszczaja nasz auto serwis. Jestesmy w pelni zaangazowani w naprawe kazdego typu samochodu. Zdajemy sobie sprawe z tego, ze cala odpowiedzialnosc za sprawnosc auta i bezpieczenstwo kierowcy spoczywaja w naszych rekach. Jesli wlasnie nam chca Panstwo powierzyc swoj samochod - zapraszamy!</p>
