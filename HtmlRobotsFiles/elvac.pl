<h3 class="h3-body-title">O nas</h3>
<div class="title-seperator"></div>
</div>
<p>
Zostala zalozona w roku 1989 na bazie doswiadczonej kadry technicznej.
Od poczatku dzialalnosci specjalizowalismy sie wylacznie w wytwarzaniu i montazu suszarni prozniowych do drewna.
Zastosowane przy budowie nowoczesne rozwiazania konstrukcyjne sprawiaja, ze produkowane przez nas urzadzenia spelniaja wszelkie wymogi dotyczace zarowno jakosci prowadzonych procesow suszenia drewna, jak rowniez funkcjonalnosci i trwalosci.
Suszarnie prozniowe wykazuje szereg zalet, ktore w krotkim czasie kompensuja znacznie wiekszy naklad na jej budowe w porownaniu do suszarni komorowej.
</p>
<p>
Suszarnie prozniowe idealnie nadaja sie do suszenia fryz wszelkiego gatunku drewna lisciastego i iglastego bez ograniczen grubosciowych.
Wszystkie elementy stanowiace wyposazenie naszych suszarni prozniowych sa produkcji polskiej co przy dzisiejszych wahaniach kursu walut, latwym i szybkim serwisem niewatpliwie sa bardzo duzym atutem tego typu urzadzen.
Firma ELVAC wyprodukowala juz ponad 300 komor cisnieniowo - prozniowych do przyspieszonego suszenia drewna.
</p>

