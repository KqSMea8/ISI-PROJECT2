<p>Podstawowa forma dzialalnosci Stowarzyszenia Polskiej Grupy Uzytkownikow Systemu Oracle jest organizowanie corocznych, jesiennych konferencji w Zakopanem. Konferencje te (zwane konferencjami PLOUG) sa organizowane nieprzerwanie od roku 1995. Glownym ich celem jest, po pierwsze &#8211; stworzenie forum dyskusyjnego dla osob chcacych podzielic sie swoimi doswiadczeniami w uzytkowaniu i projektowaniu systemow informatycznych opartych o Oracle (i nie tylko). Po drugie, prezentacja nowych trendow, technologii i rozwiazan dotyczacych zarowno oprogramowania Firmy Oracle, jak i technologii budowania systemow informatycznych i zarzadzania projektami.</p>
<p>Od roku 1997 konferencji towarzysza zajecia praktyczne w formie specjalistycznych seminariow i cwiczen, prowadzonych m.in. przez doswiadczonych deweloperow i administratorow baz danych oraz przez specjalistow firmy Oracle.</p>
<p>Druga bardzo wazna forma dzialalnosci Stowarzyszenia jest wydawanie czasopisma Oracle&#8217;owe PLOUG&#8217;tki. Czasopismo to posiada stale dzialy: &#8222;Komentarze i polemiki&#8221;, &#8222;Biuletyn bezpieczenstwa PLOUG&#8221;. Zawiera tez artykuly techniczne z roznych dziedzin zwiazanych z oprogramowaniem Oracle oraz coroczny bogaty opis programu konferencji w numerze poprzedzajacym ja.</p>
<p>Od poczatku 2001 roku Stowarzyszenie organizuje cykl seminariow jednodniowych o charakterze technicznym. Seminaria odbywaja sie w Warszawie w bezposrednim sasiedztwie Dworca Centralnego.</p>
<p>Czwarta forma dzialalnosci jest niniejszy serwis WWW, w ktorym, procz informacji organizacyjnych, umieszczono w calosci materialy (artykuly) konferencyjne, wszystkie numery czasopisma, bogate opisy wszystkich szkolen z formularzami zgloszenia uczestnictwa wlacznie.</p>
<p>Od 2002 roku Stowarzyszenie organizuje cykl &#8222;Szkol PLOUG&#8221; &#8211; dwu- lub trzydniowych monotematycznych spotkan o charakterze technicznym, uzupelniajacych merytorycznie nasza doroczna konferencje i seminaria. Szkoly odbywaja sie w lutym w Poznaniu.</p>
