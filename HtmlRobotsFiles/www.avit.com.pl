<img src="./Upload/01.jpg" alt=""/> <b>Brokuly</b><br/><br/>
Brokul to niesamowite warzywo. Ze wzgledu na ogromne ilosci witamin i mineralow,
a przede wszystkim cennych przeciwutleniaczy brokul to prawdziwy lider w kategorii najzdrowszych warzyw, zapobiegajacych wielu groznym chorobom, w tym nowotworom i chorobom serca.
</div><div class="sekwencja2">
<img src="./Upload/0lodowa_www.jpg" alt=""/> <b>Salata Lodowa</b><br/><br/>
Powszechnie uwaza sie, ze salata pochodzi z Bliskiego Wschodu.
Znali ja starozytni Egipcjanie, Grecy i Rzymianie. Do Polski zostala sprowadzona przez krolowa Bone,
ale nie od razu zyskala popularnosc. Dzis nalezy do najczesciej podawanych warzyw.
</div><div class="sekwencja2">
<img src="./Upload/0roszponka_www.jpg" alt=""/> <b>Roszponka</b><br/><br/>
Roszponka to roslina podobna do salaty o malych lisciach. Jednak na tle salat wyroznia sie nie tylko rozmiarem lisci, lecz takze wartosciami odzywczymi, a co za tym idzie - wlasciwosciami leczniczymi.


</div><div class="sekwencja2">
<img src="./Upload/05.jpg" alt=""/> <b>Radicchio </b><br/><br/>
Odmiana cykorii o fioletowych lisciach, nazywana salata radicchio. Znana byla juz w starozytnosci.
</div><div class="sekwencja2">
<img src="./Upload/06.jpg" alt=""/> <b>Rukola </b><br/><br/>
Rukola jest lisciastym warzywem o aromatycznym zapachu i oryginalnym, lekko pikantnym smaku.
Tworzy rozete lisci o dlugosci do 40 cm wysokosci.
</div><div class="sekwencja2">
<img src="./Upload/0baby_spinach_www.jpg" alt=""/> <b>Szpinak baby</b><br/><br/>
Szpinak to bogactwo skladnikow odzywczych, ktore pomagaja nam zachowac zdrowie i urode. Doceniany jest przez osoby uwielbiajace przygotowywane z jego dodatkiem zup, ciast francuskich, pierogow, nalesnikow czy makaronow.
</div><div class="sekwencja2">
<img src="./Upload/08.jpg" alt=""/> <b>Salata rzymska </b><br/><br/>
Salata rzymska ma bardzo dobry smak, zewnetrzne liscie sa nieco gorzkawe (maja tak zwany orzechowy posmak), ale okrywajace i wewnetrzne -- bardzo slodkie.
