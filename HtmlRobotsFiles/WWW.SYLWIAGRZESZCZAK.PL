<h1><span></span>BIOGRAFIA<span></span></h1>
</div>
</div>
<div class="container">
<p>Sylwia Grzeszczak. Wokalistka, kompozytorka, autorka tekstow. Slynaca z niezwyklego glosu oraz umiejetnosci gry na pianinie, ktorych polaczenie z&nbsp;wielka gracja i&nbsp;niebywala latwoscia przekuwa w kolejne hity.</p>
<p>Swoje pierwsze kroki na scenie stawiala juz w wieku pieciu lat. Zadebiutowala w programie ,,Od przedszkola do Opola", gdzie wykonala piosenke z&nbsp;repertuaru Krzysztofa Krawczyka ,,Byle bylo tak". Krotko po tym Sylwia rozpoczela nauke na pianinie, ktora kontynuowala w kolejnych szkolach muzycznych, a&nbsp;zakonczyla dyplomem Panstwowej Szkoly Muzycznej II stopnia im. Fryderyka Chopina w Poznaniu.</p>
<p>Pierwszym istotnym przelomem w karierze Sylwii byl rok 2004, kiedy zajela pierwsze miejsce w finale konkursu Drzwi do kariery. Zaledwie rok pozniej otrzymala glowna nagrode w konkursie poezji spiewanej za skomponowanie muzyki do wiersza Ernesta Brylla.</p>
<p>W 2006 roku nagrala trzy piosenki na plyte ,,Adsum" grupy Ascetoholix: ,,Tak wyszlo", ,,Afrodyzjak" oraz ,,Chodz ze mna".                          Ta kooperacja zaowocowala niezwykle wazna w dalszej karierze Sylwii wspolpraca z jednym z czlonkow wspomnianego zespolu - Liberem, z ktorym w 2008 roku wydala swoj debiutancki album ,,Ona i&nbsp;On". Znajdujace sie na krazku ,,Co z nami bedzie?", ,,Mijamy sie" oraz ,,Nowe szanse" blyskawicznie podbily serca publicznosci, wspinajac sie na szczyty list przebojow najwazniejszych rozglosni radiowych w kraju, takich jak RMF FM, Radio ZET czy Radio Eska.</p>
<p>Po ogromnym sukcesie debiutanckiego krazka przyszedl czas na przelomowy w karierze Sylwii rok, gdy swiatlo dzienne ujrzal jej pierwszy solowy singiel, ,,Male rzeczy". Utwor szybko stal sie niekwestionowanym hitem lata 2011. W niecaly miesiac po premierze utwor ,,Male rzeczy" byl pierwsza polska piosenka na liscie Hit Lata 2011 stacji VIVA Polska, a przez kolejne osiem tygodni z rzedu - najczesciej granym utworem w polskich stacjach radiowych. W tym samym roku artystka otrzymala rowniez wyroznienie dla najlepszego utworu podczas koncertu Lato ZET i Dwojki.</p>
<p>Nie osiadajac na laurach 16 lipca 2011 roku Sylwia wystapila na Festiwalu Piosenki Rosyjskiej, gdzie wykonaniem utworu              ,,Za Toboj" zajela trzecie miejsce, zdobywajac tym samym Brazowy Samowar.</p>
<p>W sierpniu 2011 swiatlo dzienne ujrzal drugi solowy singiel Sylwii, zatytulowany ,,Sen o przyszlosci". Singiel szybko powielil sukces ,,Malych rzeczy", opanowujac kolejne listy przebojow najwiekszych stacji radiowych. Juz 13 wrzesnia piosenka zadebiutowala na dwudziestym miejscu POPlisty RMF FM, by po zaledwie trzech dniach dotrzec na szczyt zestawienia. Sylwia szybko stala sie najczesciej wyszukiwana osoba w Google w Polsce, a&nbsp;teledysk do utworu ,,Sen o przyszlosci" zagwarantowal artystce pierwsza w historii Polski wygrana w 36 edycji miedzynarodowego konkursu World Music Trophy.</p>
<p>11 pazdziernika ukazala sie druga plyta studyjna Sylwii ,,Sen o przyszlosci", ktora szybko znalazla sie na pierwszym miejscu listy OLiS. Plyta w niecale 2&nbsp;tygodnie od premiery uzyskala status platynowej. Jeszcze w&nbsp;tym samym roku album zostal nominowany w otwartym plebiscycie RMF ON, wygrywajac miedzy innymi z&nbsp;albumem ,,21" Adele. </p>
<p>Wraz z ukazaniem sie trzeciego singla z plyty - ,,Karuzeli" - album ,,Sen o przyszlosci" osiagnal status dwukrotnie platynowej plyty juz w pierwszym kwartale 2012 roku. W kwietniu, podczas gali wreczenia Fryderykow, Sylwia otrzymala nagrode specjalna od Nielsen Music za najczesciej grany utwor w radiu (,,Male rzeczy").</p>
<p>Rok pozniej, w 2013 roku, odbyla sie premiera nowego singla artystki pt. ,,Flirt", ktory byl zapowiedzia trzeciego krazka, zatytulowanego ,,Komponujac siebie". W&nbsp;tym samym roku Sylwia wystapila podczas koncertu ,,Najwieksze przeboje roku" na festiwalu TOPtrendy w Sopocie, zostala uhonorowana SuperNagroda podczas Superjedynek na jubileuszowym 50 Krajowym Festiwalu Piosenki Polskiej w Opolu, a podczas gali Eska Music Awards odebrala nagrode dla Najlepszej Artystki. W&nbsp;trakcie koncertu ,,5 lat z Muzodajnia. Najwieksze przeboje lata", podczas drugiego dnia Sopot Top of the Top Festival 2013, Sylwia odebrala zlota plyte za album ,,Komponujac siebie" oraz otrzymala nagrode specjalna za singel ,,Male rzeczy", ktory w&nbsp;serwisie Muzodajnia.pl sprzedal sie w&nbsp;liczbie wiekszej niz jakikolwiek inny utwor w historii istnienia tej platformy. Krotko po tym odbyla sie radiowa premiera trzeciego singla promujacego wydawnictwo ,,Komponujac siebie" - ,,Ksiezniczka".</p>
<p>Kolejny rok byl rownie obfitym w nagrody okresem kariery Sylwii, a rozpoczal sie od nominacji do nagrody Wiktory w kategorii Gwiazda Piosenki. Wkrotce artystka otrzymala tez trzy nominacje do Superjedynek, w kategoriach SuperArtystka, SuperAlbum oraz SuperPrzeboj. W maju wystapila na koncercie ,,TOP" w&nbsp;ramach festiwalu TOPTrendy zorganizowanym dla artystow                     z najwieksza iloscia sprzedanych plyt w Polsce. Nie bylo to jedyne wyroznienie tego festiwalu - dzien pozniej wokalistka odebrala nagrode za Najwiekszy Przeboj Lata ze swoim utworem ,,Ksiezniczka". Jeszcze w lipcu album ,,Komponujac siebie" pokryl sie podwojna platyna. W sierpniu podczas gali Eska Music Awards odebrala siodma w swojej karierze statuetke Eska Music Awards, pobijajac tym samym rekord w ilosci wygranych statuetek. Niecaly rok pozniej, Sylwia zajela dwunaste miejsce w plebiscycie radia RMF FM na artyste 25-lecia oraz otrzymala Telekamere ,,Tele Tygodnia" w kategorii Muzyka.</p>
<p>W 2016 roku, podczas gdy dwie poprzednie plyty Sylwii pokryly sie potrojna platyna, artystka pracowala nad kolejnym studyjnym albumem. W&nbsp;sierpniu wystapila na gali Eska Music Awards 2016, gdzie zaspiewala premierowy utwor ze swojego najnowszego krazka - ,,Tamta dziewczyna". Podczas wydarzenia otrzymala dwie statuetki w kategoriach Najlepsza Artystka i Eska TV Award - Najlepsze Video za ,,Tamta dziewczyne". W&nbsp;listopadzie ukazal sie jej czwarty album studyjny, zatytulowany ,,Tamta dziewczyna". Niecaly miesiac po premierze plyta uzyskala status platynowej. W marcu 2017 roku singiel "Tamta dziewczyna" osiagnal status podwojnego diamentu.</p>
</div>
