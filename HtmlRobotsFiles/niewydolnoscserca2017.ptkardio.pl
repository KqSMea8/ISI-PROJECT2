Zaproszenie			</div>
<div class="col-xs-12">
<h3>Szanowni Panstwo!<br />Drogie Kolezanki i Drodzy Koledzy!</h3>
<p>&nbsp;</p>
<p>W imieniu Zarzadu Sekcji Niewydolnosci Serca PTK, Komitetu Naukowego i Organizacyjnego z wielka przyjemnoscia zapraszamy Panstwa ponownie do Poznania, w dniach <span style="color: #ff0000;"><strong>2-3 czerwca 2017 r</strong>.</span>, na Konferencje Sekcji Niewydolnosci Serca 2017 <strong><span style="color: #ff0000;">&bdquo;Postepy w terapii niewydolnosci serca 2017&rdquo;</span></strong>. Tym razem tematem wiodacym Konferencji beda nowosci w leczeniu niewydolnosci serca, implementacja zeszlorocznych zalecen ESC w praktyce klinicznej oraz dotychczasowe doswiadczenia z nowymi lekami. Program tegorocznej Konferencji bedzie obejmowac ponadto wszystkie najwazniejsze aspekty leczenia chor&oacute;b towarzyszacych i rehabilitacji pacjent&oacute;w z niewydolnoscia serca. Zgodnie z tradycja w pierwszym dniu konferencji odbeda sie sesje warsztatowe, na kt&oacute;re zapraszamy nie tylko kardiolog&oacute;w. W tym roku eksperci w przystepny i interaktywny spos&oacute;b om&oacute;wia &bdquo;krok po kroku&rdquo; sposoby oceny echokardiograficznej (funkcja rozkurczowa, wady zastawkowe, stress echo), przyblizony zostanie problem rozpoznawania i leczenia zaburzen rytmu w r&oacute;znych grupach pacjent&oacute;w z niewydolnoscia serca, przedstawione zostana techniki telemedyczne.</p>
<p>Szczeg&oacute;lna uwage poswiecimy ocenie skutecznosci realizacji nowych zalecen w praktyce klinicznej oraz dotychczasowym doswiadczeniom z nowo wprowadzonymi lekami (sakubitril/walsartan). W programie konferencji znajda sie r&oacute;wniez zagadnienia dotyczace organizacji nowoczesnego systemu opieki nad pacjentem z niewydolnoscia serca. Nie zabraknie sesji dedykowanej lekarzom rodzinnym, pielegniarkom i rehabilitantom.</p>
<p>W trakcie Konferencji odbedzie sie Walne Zebranie czlonk&oacute;w sekcji NS PTK, majace na celu wylonienie nowych wladz na kolejna dwuletnia kadencje oraz przewodniczacego elekta na lata 2019-2021.</p>
<p>Poziom naukowy Konferencji zagwarantuja najwybitniejsi specjalisci z poszczeg&oacute;lnych dziedzin, kt&oacute;rzy juz przyjeli zaproszenie do wygloszenia wyklad&oacute;w i prezentacji. Stworzymy r&oacute;wniez okazje do zaprezentowania wlasnych badan w zakresie niewydolnosci serca podczas interaktywnej sesji plakatowej. Juz teraz zachecamy do przesylania streszczen!</p>
<p>W trakcie Konferencji bedzie tez czas i okazja do spotkan towarzyskich i wymiany poglad&oacute;w w mniej formalnej atmosferze.</p>
<p>W imieniu organizator&oacute;w i swoim wlasnym juz teraz zapraszam do Poznania i udzialu w Konferencji Sekcji Niewydolnosci Serca PTK.</p>
<p style="text-align: right;">Prof. dr hab. med. Ewa Straburzynska-Migaj</p>			</div>
