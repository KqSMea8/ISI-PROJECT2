<h1>Witamy w swiecie kamienia</h1>
<p>
<img alt="granit, marmur, kamien naturalny" class="fright" src="/content/images/ob1.jpg" />
<strong>LIBERTA</strong> to firma z rodzinnymi tradycjami, kt&oacute;rej poczatki siegaja 1979 roku. W ciagu 36 lat dzialalnosci&nbsp; na rynku krajowym zdobylismy olbrzymie doswiadczenie w branzy kamieniarskiej. Wieloletnia wsp&oacute;lpraca z dostawcami&nbsp; na calym swiecie sprawila, ze stalismy sie znaczacym importerem kamienia z Brazylii, Indii, Wloch, Chin oraz Hiszpanii. Zawsze starannie dobieralismy dostawc&oacute;w, co dawalo nam mozliwosc zapewnienia naszym klientom material&oacute;w najwyzszej jakosci.<br />
<br />
