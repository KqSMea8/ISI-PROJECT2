<div class="mainText">
<p>
Dzialalnosc kulturalna, szczegolnie w zakresie promocji czytelnictwa, prowadzimy od  1947 r. Wieloletnie doswiadczenie, a zarazem otwarcie na nowatorskie formy dzialania i realizacja wielu projektow przy wsparciu funduszy krajowych i europejskich, pozwolily uczynic Biblioteke instytucja o niepowtarzalnym charakterze, z szeregiem oryginalnych imprez i kreatywnych form edukacyjnych. Nasze najwazniejsze zadania to promocja wiedzy, upowszechnianie i pobudzanie czytelnictwa, propagowanie ambitnej literatury, ksztaltowanie umiejetnosci oceny i selekcji informacji, profesjonalna pomoc dla osob uczacych sie i szukajacych rzetelnej informacji.</p>
<p>
Duze znaczenie ma dla nas edukacja kulturalna, ktora prowadzimy w oparciu o roznorodne formy. Organizujemy spotkania autorskie, promocje ksiazek, projekcje adaptacji filmowych, koncerty piosenki poetyckiej. Dzialaja kluby dla dzieci, mlodziezy i doroslych, odbywaja sie dyskusje poswiecone literaturze, happeningi i gry uliczne, organizowane sa lekcje, kursy i warsztaty. Swoje miejsce maja tu czytajace rodziny, milosnicy podrozy, artysci ludowi.</p>
<p>
Zapewniajac dostep do edukacji, informacji i kultury poprzez tradycyjne i multimedialne zbiory oraz przyjazne, oparte o nowoczesne technologie uslugi, pomagamy mieszkancom Jasla i regionu w zdobywaniu wiedzy, po to by wzbogacic ich zycie. Myslimy globalnie, ale dzialamy przede wszystkim na rzecz spolecznosci lokalnej. Jestesmy otwarci na wspolprace z osobami, organizacjami, instytucjami i firmami, ktorym tak jak nam zalezy na tworzeniu nowoczesnej i atrakcyjnej oferty kulturalnej i edukacyjnej.</p>
<p>
Zapraszamy do czytania, do dzialania i do wspolpracy!</p>
