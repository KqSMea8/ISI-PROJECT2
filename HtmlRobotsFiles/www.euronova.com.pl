<h3>O firmie</h3>
<p class="text-justify">Firma Euronova sp. z o.o. powstala w roku 1996. Tworzy ja zespol osob, posiadajacych doswiadczenie na rynku mebli biurowych od 1990 roku.</p>
<p class="text-justify">Euronova jest producentem wysokiej klasy mebli, krzesel i foteli biurowych i hotelowych, ktore zachowuja bardzo korzystna relacje pomiedzy cena a jakoscia. Produkujemy rowniez szeroka game scianek dzialowych typu "open space".</p>
<p class="text-justify">Od roku 2009 dzialamy w nowo wybudowanej fabryce wraz z budynkiem biurowym i salonem ekspozycyjnym o lacznej powierzchni kilku tysiecy mkw. Pracujemy stosujac najnowoczesniejsze linie technologiczne sterowane cyfrowo, co zapewnia wysoka precyzje obrobki materialow i jakosc naszych mebli.</p>
<p class="text-justify">Od poczatku istnienia firmy wspolpracujemy z grupa renomowanych wloskich producentow mebli biurowych. Nasi partnerzy reprezentuja innowacyjna mysl, zaawansowane technologie produkcji, bogata oferte i nowatorskie rozwiazania. Naleza do nich miedzy innymi: Estel S.p.A, Uffix S.p.A., Sitland S.p.A., Luxy s.r.l., Frezza S.p.A., Infiniti S.r.l</p>
<p class="text-justify">Zapewniamy profesjonalny serwis projektowy, montazowy, gwarancyjny i pogwarancyjny. Udzielamy dlugich okresow gwarancji. Gwarantujemy transport i montaz w dowolnej czesci kraju. Dewiza firmy jest dbalosc o zapewnienie ergonomii i komfortu pracy naszych klientow.</p>


<h3>Jakosc i zaufanie</h3>

<p class="text-justify">Nasza firma posiada certyfikat zarzadzania jakoscia ISO 9001:2008.</p>
<p class="text-justify">Kolejny rok z rzedu mozemy poszczycic sie rowniez certyfikatem ZLOTEGO PLATNIKA. Jest to prestizowe wyroznienie przyznawane za zachowanie najwyzszych standardow platniczych. Otrzymanie tego wyroznienia potwierdza, ze firma ma dobra kondycje i plynnosc finansowa oraz rzetelnie i terminowo wypelnia swoje zobowiazania finansowe wobec partnerow biznesowych.
</p>
