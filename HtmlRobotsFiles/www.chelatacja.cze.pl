<h1>Terapia</h1>
<p>
<strong>Leczenie Miazdzycy - Efektywna terapia z udzialem chelatacji.
Nieoperacyjna metoda lecznicza rozpuszczajaca blaszki miazdzycowe w naczyniach krwionosnych,
jak dotychczas pierwsza w dziejach ludzkosci metoda eliminujaca zrodlo wolnych rodnikow w ustroju.
</strong>
</p>
<p>
Jest to nowa w Polsce procedura lecznicza, ktora szczegolnie korzystnie i skutecznie wplywa na zdrowie osob z
powaznymi komplikacjami krazeniowymi, takimi jak: zawal miesnia sercowego, przebyty lub zagrazajacy udar
mozgu czy bole niedokrwienne konczyn wywolujac zdrowotnie zjawisko zwane REWITALIZACJA - powrot zycia i
proporcjonalnych do wieku sil zyciowych - gdyz wiekszosc chorych ludzi, ktorzy w zwiazku z choroba czuja sie
znacznie starzej niz by to wynikalo z ich wieku, a poddajacych sie tej terapii zglasza, iz po procedurze leczniczej
z udzialem chelatacji czuja sie, gdy chodzi o sprawnosc biologiczna, jakby im ujeto 10 - 15 lub nawet wiecej lat.
Procedura ta jest stosowana z wielkim powodzeniem juz przez dziesiatki lat w USA. Jedyne jej ograniczeni
polegaja na tym, ze trzeba znalezc wykwalifikowanego i odpowiednio zorganizowanego lekarza do jej
przeprowadzenia zgodnego ze sztuka lekarska i procedura, a takich samodzielnych lekarzy jest w dzisiejszych
czasach niewielu. Na skuteczna kuracje wymaganych jest minimum 20 zabiegow. Zabieg taki to standartowy wlew
kroplowy, w ktorym znajdujemy miedzy innymi EDTA jako zasadniczy srodek leczniczy. Pozostale komponenty
kroplowek maja wazne dzialanie wspomagajace i utrwalajace dzialanie glowne. Najczesciej wlew kroplowy
wykonywany jest w pozycji siedzacej. Terapia z udzialem chelatacji odbywa sie zwykle w pozycji siedzacej i srednio
trwa od 2 do 4 godz. Zabiegow jest zwykle od 20 do 50, kazdy trwajacy 2 - 4 godziny.
Koszty terapii z udzialem chelatacji, ktore ponosi pacjent, sa znacznie nizsze w porownaniu z oplata za przecietna
operacje "by - pass" czy angioplastyke, nie wspominajac nawet o tym, ze kazdy z tych wspanialych i jeszcze do
niedawna niewykonalnych technicznie zabiegow chirurgicznych omija tylko kilka centymetrow najmocniej zatkanej
tetnicy, calej pozostalej, chorej reszty, nawet nie dotykajac, podczas gdy terapia z udzialem chelatacji oczyszcza
caly uklad krazenia.
Kuracja miazdzycy - podstawowe i najbardziej spektakularne wskazanie do chelatacji - przecietnie obejmuje 30
zabiegow kroplowych. Przewaznie 10 pierwszych wlewow wykonuje sie 3 razy w tygodniu, 10 nastepnych - 2 razy
w tygodniu, 10 ostatnich - raz lub dwa razy w tygodniu, decyduje dynamika postepow leczenia. Ilosc ewentualnie
dodatkowych przetoczen indywidualnie okresla lekarz prowadzacy proporcjonalnie do efektow kuracji pod koniec
zaplanowanej serii zabiegow. Leczenie trwa zazwyczaj od 3 do 5 miesiecy.
Oprocz samego EDTA, ktory, mozna by tak powiedziec, robi glowna robote, podczas kuracji w okreslonym czasie
podawane sa do konkretnych wlewow istotne w dawce i proporcji skladniki utrwalajace efekt przeciwmiazdzycowy
oraz istotne leki potegujace ten efekt. Podawane sa ponadto indywidualne wskazowki przy konkretnych zachorowaniach by nie doszlo szybko do nawrotow. Dalsze leczenie jest zalezne od zaawansowania choroby oraz
od zmiany niewlasciwego trybu zycia chorego, czesto wystarcza dwie trzy wskazowki i to niezbyt uciazliwe. Chory
powinien zmienic ze zlych na dobre swoje przyzwyczajenia dotyczace odzywienia, ruchu (wysilku fizycznego),
nalogow, w ktorych to warunkach powstala miazdzyca.
Jesli dzialanie terapii z udzialem chelatacji - zahamowanie procesow degeneracyjnych - wesprzemy innymi,
rowniez bez dzialan ubocznych, zabiegami energetyzujacymi, mobilizujacymi procesy regeneracji, takimi jak:
akupunktura, akupresura, bioenergoterapia, homeopatia, joga, czi-kung i podobnymi z tej dziedziny, jak to dosc
czesto laczone jest w "Osrodkach Chelatacji Doktor Krasicki - Rewitalizacja" uzyskamy wtedy optymalne
zregenerowanie organizmu i jego sil. Tym samym w efekcie doprowadzamy do niespotykanego w innych
kombinacjach leczniczych wzmocnienia zdrowia. Po tak kompleksowej rewitalizacji, niosacej rowniez gruntowne
oczyszczenie organizmu, ten pozytywny efekt jest potem odczuwany wiele lat.
Lekarze, ktorzy probowali stosowac sam wersenian - EDTA, bez ww. indywidualnie dobieranych dla konkretnych
pacjentow procedur uzupelniajacych zauwazyli, ze miazdzyca, ktora pieknie znika bezposrednio po przetoczeniach,
juz po kilku - kilkunastu miesiacach ma tendencje odbudowywac sie dokladnie na tych samych miejscach i wlewy
trzeba powtarzac. Dzieje sie to jesli nie przeprowadzi sie tych okreslonych procedur bedacych tak na prawde
najwazniejszym elementem trwalosci sukcesu przeciwmiazdzycowego w rewitalizacji z udzialem chelatacji w
Osrodkach Doktor Krasicki w calym kraju, nad ktora to procedura czuwa i ja na biezaco doskonali nasz Krajowy
Koordynator.
W pojedynczych przypadkach prowadzi sie rowniez tzw. leczenie przypominajace np. jedno, dwa przetoczenia na
1 do 3 miesiecy po zakonczonej kuracji podstawowej, jednak rzadko przekracza sie ogolnie liczbe 50 wlewow dla
jednego pacjenta.
Umiejetnie przeprowadzona kuracja z udzialem EDTA daje dlugotrwala, wieloletnia poprawe w zaawansowanej
miazdzycy, eliminuje skutki degenerujacego ustroj dzialania wolnych rodnikow, zmniejsza odczuwanie przeroznych
dolegliwosci i opoznia powstawanie chorob cywilizacyjnych jak rowniez objawow przedwczesnego starzenia sie.
Kuracje zalecane z innych niz miazdzyca powodow nie wymagaja przewaznie az tylu zabiegow i indywidualnie
okresla to lekarz. Moze to byc np. 3, 5, 7, 15 zabiegow - przetoczen kroplowych.
Ogromna, nie kwestionowana zaleta tej procedury leczniczej jest fakt, ze jest to leczenie praktycznie jeden razna zycie, dosc scisle okreslona ilosc zabiegow i efekty dosc jasno przedstawiane sa z gory, w przeciwienstwie do
"cudownych" leczen roznymi biopreparatami, gdzie nalezy brac te specyfiki czesto do konca zycia najlepiej 3, a
jeszcze lepiej 5 razy dziennie. Tego typu zalecenia, by cokolwiek brac dluzej niz trzy miesiace zawsze wzbudzaja w
nas czujnosc, czy przypadkiem nie trafilismy na naciagacza. Gdy chodzi o leczenie z udzialem chelatacji, tu z gory
mowi sie o jednej kuracji. O powtorce na razie domniemywa sie tylko, ale ewentualnie, najszybciej, moze za kilka
lat.
</p>

