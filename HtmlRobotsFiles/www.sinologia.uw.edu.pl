<br />
<span style="background-color: rgb(240, 255, 240);">https://www.facebook.com/Sinologia-UW-243008679089848/</span></span></span></p>
<p>
&nbsp;</p>
<p>
Zaklad Sinologii Wydzialu Orientalistycznego Uniwersytetu Warszawskiego jest najstarszym polskim osrodkiem ksztalcacym mlodziez akademicka w&nbsp;zakresie jezyka i&nbsp;kultury chinskiej, dzialajacym od&nbsp;1933 roku. Wsr&oacute;d najznamienitszych profesor&oacute;w warszawskiej sinologii znalezli sie: <a href="/pods/539_profesor-witold-jablonski-sylwetka">Witold Jablonski (1901&ndash;1957)</a>, Janusz Chmielewski (1916&ndash;1998), Olgierd Wojtasiewicz (1916&ndash;1995), Tadeusz Zbikowski (1930&ndash;1989) czy Mieczyslaw Jerzy K&uuml;nstler (1933&ndash;2007), kt&oacute;rych dorobek stanowia liczne przeklady z&nbsp;literatury chinskiej (<em>Czuang-tsy, K&rsquo;&uuml; Juana piesni z&nbsp;Cz&rsquo;u</em>, <em>Dialogi konfucjanskie, Wedr&oacute;wka na&nbsp;zach&oacute;d, Dzieje Trzech Kr&oacute;lestw</em>), prace naukowe (<em>Notes on Early Chinese Logic, Jezyki chinskie</em>) oraz publikacje popularyzatorskie (<em>Antologia literatury chinskiej, Sztuka Chin, Dzieje kultury chinskiej</em>).</p>
<p>
Obecnie, w&nbsp;Zakladzie Sinologii zajecia prowadzi dw&oacute;ch profesor&oacute;w (Zbigniew Slupski, Lidia Kasarello), dw&oacute;ch doktor&oacute;w habilitowanych (Irena Kaluzynska, Ewa Zajdler), piecioro doktor&oacute;w, doktoranci, stali wsp&oacute;lpracownicy oraz lektorzy z&nbsp;Chin i&nbsp;Tajwanu.</p>
<p>
Zaklad Sinologii prowadzi coroczny nab&oacute;r na&nbsp;studia licencjackie dzienne i&nbsp;wieczorowe oraz na&nbsp;studia magisterskie (dzienne). Program studi&oacute;w obejmuje praktyczna nauke jezyka chinskiego (w tym pisma w&nbsp;systemach znak&oacute;w uproszczonych i&nbsp;tradycyjnych), wiedze o&nbsp;historii, literaturze, jezyku i&nbsp;sztuce Chin, nauke chinskiego jezyka klasycznego. Studenci studi&oacute;w magisterskich moga ubiegac sie o&nbsp;roczne stypendia do&nbsp;Chin oraz na&nbsp;Tajwan. W&nbsp;ramach Zakladu funkcjonuja takze stacjonarne studia doktoranckie.</p>
<p>
Warszawska sinologia prowadzi wymiane naukowa z&nbsp;licznymi osrodkami w&nbsp;Europie i&nbsp;na&nbsp;swiecie, a&nbsp;dzieki wsp&oacute;lpracy z&nbsp;chinska instytucja rzadowa Hanban oraz tajwanska fundacja Chiang Ching-kuo, stale poszerza i&nbsp;aktualizuje sw&oacute;j ksiegozbi&oacute;r. Pracownicy Zakladu, poza dzialalnoscia dydaktyczna, prowadza badania wlasne i&nbsp;szeroko publikuja. Wsr&oacute;d najnowszych publikacji wymienic mozna jeden z&nbsp;pierwszych polskich podrecznik&oacute;w do&nbsp;nauki chinskiego <em>Wsp&oacute;lczesny jezyk chinski </em>(cz. 1&ndash;2), przeklady literackie (zbi&oacute;r wsp&oacute;lczesnych opowiadan chinskich <em>Wezly duszy</em>, powiesc Han Shaogonga <em>Slownik Maqiao</em>, nowe tlumaczenie <em>Zhuangzi</em>) oraz prace naukowe (<em>Chinese Female Namings</em>).</p>
<p>
W 2003 roku Zaklad Sinologii otrzymal <a href="/pliki/CertyfikatEuropeanLabel2003-p.jpg">certyfikat</a> European Label dla&nbsp;nowatorskich inicjatyw w&nbsp;nauczaniu jezyk&oacute;w obcych za&nbsp;projekt &quot;Poznac Jezyk i&nbsp;Kulture - Integralne Nauczanie Jezyka Chinskiego&quot;.&nbsp;</p>
<p>
Na sinologii stale prowadzone sa badania nad&nbsp;zagadnienieniami przyswajania jezyka chinskiego, standaryzaja nauczania i&nbsp;certyfikacji bieglosci jezykowej, kt&oacute;rych rezultaty&nbsp;byly <a href="/pliki/QianLi-PiszczekEwaZajdlerChiskietestyjzykowewperspektywieeuropejskiej.pdf">prezentowane</a> podczas&nbsp;VI Miedzynarodowej Konferencji Edukacyjnej <a href="http://www.konferencje-spnjo.polsl.pl/download/li-piszczek-q-chinskie-testy-jezykowe.pdf">&bdquo;Kompetencja jezykowa - drogi, cele, aspekty&rdquo;</a>.&nbsp;</p>
<p>
Kierownikiem Zakladu Sinologii WO UW jest dr Malgorzata Religa.</p>
