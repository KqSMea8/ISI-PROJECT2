<div class="title">Pensjonat Lysica</div>
<div class="content">G&oacute;ry Swietokrzyskie (342.34) &ndash; masyw g&oacute;rski polozony w poludniowo-wschodniej Polsce, w centralnej czesci Wyzyny Kieleckiej. Najwyzszy szczyt to Lysica (612 m n.p.m.) w pasmie Lysog&oacute;r. Nazwa g&oacute;r pochodzi od relikwii Krzyza Swietego przechowywanych w klasztorze na Lysej G&oacute;rze.<br />G&oacute;ry Swietokrzyskie, obok Sudet&oacute;w, sa jednym z najstarszych pasm g&oacute;rskich w Polsce. Wypietrzyly sie w czasie kaledonskich ruch&oacute;w g&oacute;rotw&oacute;rczych na granicy syluru i dewonu. Nastepnie zostaly odmlodzone w czasie orogenezy hercynskiej (dolny karbon) i ponownie podczas orogenezy alpejskiej.<br /><br />Charakterystyczne dla krajobrazu najwyzszych partii G&oacute;r Swietokrzyskich sa strome stoki, gleboko wciete doliny, skalki ostancowe i goloborza. G&oacute;ry Swietokrzyskie porosniete sa lasami jodlowymi (Puszcza Jodlowa) i bukowymi. Na ich terenie utworzono Swietokrzyski Park Narodowy.<br /><br />Gospodarczym i turystycznym centrum regionu sa Kielce. Do wazniejszych osrodk&oacute;w turystycznych naleza Swieta Katarzyna i Nowa Slupia.<br /><br />W przeszlosci na terenie G&oacute;r Swietokrzyskich znajdowaly sie osrodki g&oacute;rnictwa rud zelaza, olowiu i miedzi.</div>	</div>
