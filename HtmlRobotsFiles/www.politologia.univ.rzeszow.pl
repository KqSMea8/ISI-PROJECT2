<p>Zaklad Stosunkow Miedzynarodowych prowadzi badania naukowe w&nbsp;obrebie trzech zasadniczych obszarow zainteresowan. Sa nimi: polityka zagraniczna panstwa, mniejszosci narodowe i&nbsp;etniczne w&nbsp;Europie Srodkowo-Wschodniej oraz polityka bezpieczenstwa w&nbsp;aspekcie lokalnym, regionalnym i&nbsp;miedzynarodowym.</p>
