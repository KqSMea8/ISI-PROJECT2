<h1>Instytut Nauk o Polityce</h1>
<div class="photo"><a href="/robots.txt/instytut" title=""><img src="image/budynekKP.jpg" alt="Rekrutacja" /></a><div class="bar"></div></div>
<p>Instytut Nauk o&nbsp;Polityce na Wydziale Socjologiczno-Historycznym funkcjonuje od 1 lipca 2001 r. Do lutego 2015 r. w&nbsp;strukturze Wydzialu dzialala Katedra Politologii, ktorej pierwszym Kierownikiem byl prof. dr hab. Henryk Cimek. Od 1 wrzesnia 2012 r. Katedra kierowala prof. nadzw. dr hab. Agnieszka Pawlowska. Decyzja JM Rektora Uniwersytetu Rzeszowskiego, prof. dr. hab. Aleksandra Bobko, Katedra zostala przeksztalcona w&nbsp;Instytut. Uchwala nr 27/2006 PKA z&nbsp;dnia 5 stycznia 2006 r. w&nbsp;sprawie oceny jakosci ksztalcenia na poziomie I stopnia kierunek politologia otrzymal pozytywna ocene.			&nbsp; &nbsp; &nbsp;<a href="/robots.txt/instytut" title="wiecej" class="more">>> wiecej</a></p>
