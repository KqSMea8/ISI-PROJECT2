<div class="panelMT15 newspaper">
<h3>Polityka i Spoleczenstwo</h3>
<div class="thumb"><a href="/robots.txt/polityka-i-spoleczenstwo" title="Polityka i Spoleczenstwo"><img src="image/polityka.png" alt=""/></a></div>

<p>,,Polityka i Spoleczenstwo" jest naukowym czasopismem politologicznym o charakterze interdyscyplinarnym. W latach 2004-2012 ukazalo sie lacznie 10 numerow, a artykuly byly publikowane rownolegle w jezykach polskim i&nbsp;angielskim. Poczawszy od 2013 roku pismo zostalo przeksztalcone w kwartalnik, a artykuly sa publikowane w jezyku polskim lub angielskim. Czasopismo zostalo stworzone i ukazuje sie dzieki staraniom pracownikow Instytutu Nauk o Polityce Uniwersytetu Rzeszowskiego, a jego wydawca jest Wydawnictwo Uniwersytetu Rzeszowskiego.</p>
