<h1>Kancelaria</h1>
<p>
<img alt="" src="/upload/images/kancelaria_m.jpg" style="width: 133px; height: 200px;" />Kancelaria Adwokacka Lukasz Pokropinski to nowoczesna kancelaria prawna oferujaca kompleksowa i skuteczna pomoc prawna dla przedsiebiorc&oacute;w i klient&oacute;w indywidualnych.</p>
<p>
Nasze uslugi obejmuja wszechstronna pomoc z takich dziedzin prawa jak prawo cywilne, prawo gospodarcze, prawo karne, prawo pracy a takze prawo rodzinne.</p>
<p>
Podejmujac wsp&oacute;lprace z Kancelaria otrzymujecie Panstwo pewnosc, ze powierzone nam sprawy beda prowadzone w spos&oacute;b profesjonalny i z pelnym zaangazowaniem a prawnik zajmujacy sie problemem zawsze bedzie do Panstwa dyspozycji.</p>
<p>
Naszym celem jest poszukiwanie rozwiazan najkorzystniejszych z punktu widzenia Klienta. Z tego wzgledu do kazdego przedstawionego nam problemu podchodzimy w spos&oacute;b indywidualny, zachowujac jednoczesnie konkurencyjne ceny.</p>
<p>
Chcac zapewnic naszym uslugom najwyzsza jakosc, adwokat Lukasz Pokropinski &ndash; zalozyciel Kancelarii &ndash; korzysta z pomocy ekonomist&oacute;w, doradc&oacute;w podatkowych i rzeczoznawc&oacute;w sadowych. Kancelaria wsp&oacute;lpracuje takze z wybranymi Kancelariami Notarialnymi oraz Komorniczymi.</p>
<p>
Gl&oacute;wnym obszarem Naszego dzialania sa miasta Olsztyn i Ostr&oacute;da, gdzie mieszcza sie biura Kancelarii. W zaleznosci od potrzeb Klient&oacute;w nasze uslugi swiadczymy r&oacute;wniez w takich miastach jak: Morag, Nowe Miasto Lubawskie, Ilawa, Lidzbark Warminski, Mragowo, Szczytno, Nidzica czy Ketrzyn. Jesli zachodzi taka potrzeba podejmujemy konieczne czynnosci takze w innych miastach i regionach kraju.</p>
<p>
Zapraszamy do skorzystania z Naszej oferty.</p>
<p style="text-align: right;">
<em>adw. Lukasz Pokropinski</em></p>
