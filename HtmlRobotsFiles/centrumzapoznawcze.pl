<h2 class="text-center title">O <strong>Nas</strong></h2>
<div class="separator"></div>
<p class="large text-center">BYC RAZEM - CENTRUM ZAPOZNAWCZE</p>
</div>
<div class="row">
<div class="col-md-6">
<h3 class="title">Zostales sam? Jestes sama?</h3>
<p>Jestesmy odpowiednim miejscem dla kazdej osoby, ktora chce odnalezc tego kogos nie na chwile, ale na cale zycie. Jestesmy widoczni dla tych, ktorzy szukaja i wiedza czego naprawde pragna.</p>
<p>Podjecie decyzji o wspolpracy z nami spowoduje, ze nie poddasz sie przypadkowemu biegowi zdarzen. <strong>Bedziesz szukac swiadomie.</strong> Z nami dajesz sobie zwiekszone szanse spotkania osoby, ktora potrafi byc bardzo blisko Ciebie i pozwoli Ci na to samo, abys Ty byl/a blisko.</p>
<p>Nie zastanawiaj sie dlugo i skorzystaj z uslug naszego biura. Zapewniamy pelna dyskrecje, zaufalo nam wiele kobiet i mezczyzn. Znalezli oni partnerow, o jakich marzyli. Co tydzien statystyka naszych Klientow jest inna, gdyz codziennie ktos nowy zaczyna poszukiwac, a ktos inny przestaje, poniewaz juz odnalazl.</p>
<p>W naszej bazie sa tylko osoby samotne, w trakcie rozmowy kwalifikacyjnej sprawdzamy ich stan cywilny. Poznajemy ludzi w zroznicowanym przedziale wiekowym, pomiedzy 20-tym a 80-tym rokiem zycia. Z naszego posrednictwa korzystaja osoby, dla ktorych budowanie kazdego dnia we dwoje da im mozliwosc poczucia pelniejszego spelnienia, wiekszej radosci i przyjemnosci.</p>
<p class="scrollspy smooth-scroll">Istnieje dwie mozliwosci zarejestrowania sie w Centrum Zapoznawczym, osobista rozmowa z doradca w biurze oraz rejestracja internetowa.
Czlonkowie internetowi moga korzystac z adresu mailowego biura lub z gotowego <a href="#zgloszenie">formularza zgloszeniowego</a>, po uprzednim zapoznaniu sie z regulaminem.
W tym celu nalezy wypelnic formularz zgloszeniowy, dolaczyc dwa zdjecia oraz uiscic oplate za wybrany pakiet na numer konta wskazany przez Centrum Zapoznawcze.
Doradca rejestruje i opracowuje anonimowa ankiete po czym umieszcza ja w fotokatalogu.
Na adres mailowy nalezy przekazac numery ofert, ktorymi klient jest zainteresowany. W odpowiedzi klient Centrum Zapoznawczego otrzymuje wyczerpujaca informacje, doradca przekazuje numery telefonow i aranzuje spotkanie. O efekcie spotkania nalezy poinformowac Centrum Zapoznawcze.</p>
<p>Zycie dla samego/j siebie juz Cie nie cieszy, ciagle powtarzasz sobie ze "ktoregos dnia sie odwaze"... Nie odkladaj decyzji na pozniej, nie rezygnuj nawet gdyby osiagniecie celu wymagalo czasu, <strong>czas i tak uplywa.</strong> Zrob krok, aby zmienilo sie cos. Zapraszamy, poznaj nas.</p>
<p><strong>Bez Ciebie nic nie jestesmy w stanie zrobic dla Ciebie!</strong></p>
<p>Nie przyzwyczajaj sie do samotnosci - nie warto!</p>
