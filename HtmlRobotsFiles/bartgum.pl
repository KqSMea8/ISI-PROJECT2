<h1><span>Strona</span> glowna</h1>
<p>
<img alt="" src="http://bartgum.pl/img/bieznik_2.jpg" title="bieznik_2" style="float:right; margin: 10px;" class="size-full wp-image-340 alignright" height="170" width="260">Ostatnimi czasy bardzo czesto mowi sie na temat potrzeby sezonowej wymiany opon; w szczegolnosci w okresie przedzimowym. To znaczy dyskusje dotycza kwestii; kiedy nalezy wymienic opony, czy jak juz lezy snieg, czy w momencie spadku temperatury ponizej 7degC - bo zagadnienie czy w ogole to nalezy robic mamy na szczescie juz za soba. Rzecz jasna nikt juz nie ma chyba w tym zakresie watpliwosci.<br>
<br>
Oczywiscie; jak to najczesciej bywa, bardzo wielu kierowcow, tak jakby liczac na to, ze ,,zimy w tym roku nie bedzie", zatem bardzo dlugo niektorzy wstrzymuja sie z decyzja dotyczaca wymiany ogumienia, czesto do czasu az lezacy snieg calkowicie uniemozliwi bezpieczne poruszanie sie po drogach lub gdy aura splata kierowcom psikusa zaskakujac z reszta jak co roku drogowcow i utworzy na jezdni tak zwana szklanke. Wtedy to warsztaty nie tylko blacharskie przezywaja prawdziwe oblezenie, ale i takze serwisy opon, a obsluga i tych i tych jak w ukropie zmaga sie z niezmierzonymi kolejkami zdesperowanych kierowcow.<br>
<br>
Ale jest wyjscie, bardzo prosta metoda unikniecia atmosfery nerwowosci i co za tym idzie zminimalizowania ryzyka stania w kolejach oraz ograniczenia ponoszenia dodatkowych kosztow. Wystarczy przestrzegac prostej zasady; na zime <img alt="" src="http://bartgum.pl/img/JBC10263-249x300.jpg" title="JBC1026" style="float:left;" class="size-medium wp-image-345 alignleft" height="310" width="250">wymieniac opony na poczatku pazdziernika bez wzgledu na to czy jest snieg czy go nie ma, a w okresie pozimowym wymieniac opony pod koniec marca lub nieco pozniej az do momentu znikniecia sniegow.<br>
Stosujac takie proste rozwiazanie opona zimowa i letnia jest uzywana po 6 miesiecy kazda, w kazdym roku eksploatacji, co znacznie wplywa na jej zywotnosc. Czyli to juz kolejna korzysc wynikajaca ze stalej daty wymiany opon sezonowych. I ostatnia; chyba najwazniejsza - bezpieczenstwo. Najprawdopodobniej zmniejszamy do niezbednego minimum ryzyko utraty przyczepnosci stosujac odpowiednie opony, ktore dzieki swym wlasciwosciom sa bezwzglednie bardziej uzyteczne, gwarantujac wyzszy poziom bezpieczenstwa. Jednak nie mozna zapominac, ze na lodzie nawet najlepsza zimowa opona nic nie wskora kiedy brawura polaczona z brakiem wyobrazni kierowcy bierze gore nad rozsadkiem.</p>		</div>
