<div class="col-md-12"><h2>Warsztaty edukacyjne</h2><div class="row text_medium">
<div class="col-md-12">


<p>Uczestniczac w projekcie ,,Praktyczne rozwijanie talentow - jesienne warsztaty edukacyjne" bedziesz miec mozliwosc wziecia udzialu w nastepujacych warsztatach edukacyjnych: </p>

<ol>
<li>
<p><span class="orange"><b>Media spolecznosciowe w promowaniu inicjatyw mlodych ludzi </b></span></p>
<p><b>16.10.12014 r., Uniwersytet Ekonomiczny w Katowicach</b> (ul. 1 Maja 50, Katowice)</p>
<p>Po tych warsztatach planowanie kampanii i wykorzystanie mediow spolecznosciowych do propagowania przedsiewziec nie bedzie mialo dla Ciebie tajemnic. Podczas zajec poznasz bowiem praktyczne aspekty zastosowania nowoczesnych narzedzi komunikacji oraz spoleczno-ekonomiczny wymiar dzialalnosci mlodziezy.</p>
</li>
<li>
<p><span class="orange"><b>Budowanie portfolio przyszlych kompetencji zawodowych </b></span></p>
<p><b>22.10.2014 r., Szkola Glowna Handlowa w Warszawie</b> (Al. Niepodleglosci 128, Warszawa)</p>
<p><b>28.11.2014 r., Szkola Glowna Handlowa w Warszawie </b>(Budynek S - ul. Batorego 8, Warszawa)</p>
<p>W ich trakcie przygotujesz sie do wejscia na rynek pracy. Dowiesz sie jak tworzyc profesjonalne portfolio (w tym: e-portfolio) poprzez swiadoma prace nad sciezka edukacyjna i gromadzenie doswiadczen.</p>
</li>
<li>
<p><span class="orange"><b>Warsztaty z komunikacji i pracy grupowej</b></span></p>
<p><b>27.10.2014 r., Uniwersytet Ekonomiczny w Krakowie</b> (ul. Rakowicka 27, Krakow)</p>
<p>To praktyczny warsztat z zakresu kompetencji komunikacyjnych oraz pracy w zespolach czyli niezbednych umiejetnosci menedzera. W jego trakcie dokonasz autodiagnozy oraz dowiesz sie w praktyce w jaki sposob wykorzystywac potencjal kazdego czlonka zespolu.</p>
</li>
<li>
<p><span class="orange"><b>Tworzenie innowacji metoda Design Thinking </b></span></p>
<p><b>07.11.2014 r., Uniwersytet Ekonomiczny we Wroclawiu</b> (ul. Komandorska 118/120, Wroclaw)</p>
<p>Zapoznasz sie z procesami tworzenia innowacji spolecznych i gospodarczych oraz praktycznie zastosujesz ta wiedze. Warsztat pozwoli Ci wspoltworzyc praktyczne innowacje spoleczne - istotne dla lokalnych spolecznosci i srodowiska szkolnego, a w przyszlosci - innowacje gospodarcze.</p>
</li>
<li>
<p><span class="orange"><b>Tworzenie przedsiewziecia e-biznesowego </b></span></p>
<p><b>14.11.2014 r., Uniwersytet Ekonomiczny w Poznaniu </b>(Al. Niepodleglosci 10, Poznan)</p>
<p>Myslisz o wlasnej firmie? Ten warsztat pozwoli rozwinac Ci w sobie przedsiebiorcze postawy oraz zdobyc wiedze niezbedna do zalozenia wlasnej dzialalnosci gospodarczej z wykorzystaniem narzedzi e-commerce.</p>
</li>
</ol>

<p style="margin-top:30px;"><a href="/pliki/Harmonogram.pdf" target="_blank"><b>Zobacz harmonogram warsztatow</b></a>.</p>

<p style="margin-top:30px;"><b>Jak to bedzie przebiegac?</b></p>

<ul>
<li>wybierasz jeden warsztat - kazdy z nich odbywa sie w innym miescie (tj. w Katowicach, Krakowie, Poznaniu, Warszawie lub Wroclawiu)</li>
<li>przyjezdzasz na wybrana uczelnie na jeden dzien (zajecia zajma 8 godzin akademickich) - zobacz harmonogram</li>
<li>pozniej ogladasz filmy z zajec z innych warsztatow (mozesz obejrzec ze wszystkich badz niektorych, minimum dwoch)</li>
<li>bierzesz udzial w dyskusjach i zadaniach online na tematy z trzech wybranych warsztatow (tego w ktorym uczestniczyles/las na uczelni + dwoch z obejrzanych filmow)</li>
<li>na zakonczenie projektu otrzymasz certyfikat potwierdzajacy zdobyte kompetencje oraz cenna publikacje ksiazkowa z wybranego przez Ciebie obszaru</li>
</ul>

<p>W wybranym warsztacie na uczelni uczestniczyc bedzie ok. 50 licealistow. Bedzie to dobra okazja do nawiazania nowych znajomosci. Poza wykladem eksperta odbeda sie prace zespolowe i prezentacje ich wynikow. Zapowiadaja sie bardzo interesujace zajecia!</p>

<p><a class="makescroll orange" href="#zgloszenie">Zapisz sie na wybrany warsztat edukacyjny i poznaj z bliska jedna z pieciu uczelni!</a></p>

<p>Warunki uczestnictwa w warsztatach edukacyjnych opisane sa szczegolowo w Regulaminie <b><a href="/pliki/Regulamin.pdf" target="_blank">[pobierz]</a></b></p>


