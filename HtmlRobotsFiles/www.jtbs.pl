<p><strong><u>KLAUZULA INFORMACYJNA<br />
</u></strong></p>
<p>Jar<em>oslawskie Towarzystwo Budownictwa Spolecznego Sp. z o.o. ul. Ks. Jakuba Makary 1, 37-500 Jaroslaw informuje, ze jest Administratorem Danych Osobowych: </em></p>
<p>najemcow i partycypantow oraz ich rodzin; ktorzy zamieszkuja w zasobach Spolki bez tytulu prawnego; osob bedacych przyszlymi najemcami i ich rodzin; wlascicieli oraz ich rodzin; ktorzy posiadaja prawo odrebnej wlasnosci lokalu; osob bedacych przyszlymi wlascicielami i ich rodzin; oferentow skladajacych oferty, dokumenty przetargowe.</p>
<p><em> Cel przetwarzania: </em>Dane osobowe przetwarzane sa przez administratora w celach realizacji zadan, obowiazkow i uprawnien okreslonych w Umowie i Regulaminie Spolki oraz powszechnie obowiazujacych przepisach prawa a zwiazanych z prowadzeniem dzialalnosci budowania domow mieszkalnych i ich eksploatacji na zasadach najmu oraz zarzadzanie i administrowanie nieruchomosciami.</p>
<p><em> Podstawa prawna przetwarzania wynika m.in. z:</em></p>
<ol>
<li>Ustawy z dnia 10 maja 2018 r. o ochronie danych osobowych (Dz. U. 2018 r. poz. 1000)</li>
<li>Ustawy z dnia 26 pazdziernika 1995 roku o niektorych formach popierania budownictwa mieszkaniowego (Dz. U. z 2018 r. poz. 1020, 1540.)</li>
</ol>
<ol start="3">
<li>Ustawy z dnia 21 czerwca 2001 o ochronie praw lokatorow, mieszkaniowym zasobie gminy i o zmianie kodeksu cywilnego (Dz. U. 2018 poz. 1234, 1496)</li>
</ol>
<ol start="4">
<li>Ustawy z dnia 16 wrzesnia 2011 r. o ochronie praw nabywcy lokalu mieszkalnego lub domu jednorodzinnego (Dz. U. 2017 r. poz. 1468)</li>
</ol>
<ol start="5">
<li>Ustawy z dnia 29 stycznia 2004 r. Prawo Zamowien Publicznych <strong>(Dz. U. z 2017 r. poz. </strong></li>
</ol>
<p><strong>    1579, 2018 oraz z 2018 r. poz. 1560, 1603, 1669 i 1693)</strong></p>
<p>&nbsp;</p>
<p><em>Odbiorcami Pani/Pana danych moga byc</em> partnerzy, banki, operatorzy pocztowi, przewoznicy, firmy drukujace korespondencje lub obslugujace korespondencje otrzymywana od klientow, firmy archiwizujace dokumenty.</p>
<p><em>Pani/Pana dane osobowe beda przechowywane przez</em><em> okres </em>wypelnienia obowiazku prawnego Administratora wynikajacego z przepisow prawa badz gdy dane osobowe przetwarzane na podstawie odrebnej zgody beda przechowywane do czasu jej odwolania.</p>
<p><em>Dane moga byc udostepnione odbiorcom uprawnionym do ich pozyskania wylacznie na podstawie przepisow prawa.</em></p>
<p><em>Pani/Pana dane osobowe nie beda przekazywane do panstwa trzeciego lub organizacji miedzynarodowej.</em></p>
<p><em>Posiada Pan/Pani </em><em>prawo do zadania od administratora dostepu do danych osobowych dotyczacych osoby, ktorej dane dotycza, ich sprostowania, usuniecia lub ograniczenia przetwarzania, prawo do wniesienia sprzeciwu wobec przetwarzania, a takze prawo do przenoszenia danych i prawo do cofniecia zgody w dowolnym momencie (bez wplywu na zgodnosc z prawem przetwarzania, ktorego dokonano na podstawie zgody przed jej cofnieciem), wniesienia skargi do organu nadzorczego</em></p>
<p><em>Obowiazek podania danych osobowych jest wymogiem ustawowym i warunkiem zawarcia umowy.</em></p>
<p><em>Pani/Pana dane nie beda przetwarzane w sposob zautomatyzowany, w tym profilowaniu.</em></p>
