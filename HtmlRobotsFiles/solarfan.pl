<h2>Pytania i odpowiedzi</h2>
<h4>Tu znajdziesz odpowiedzi na czesto zadawane pytania, oraz przesledzisz proces calej inwestycji.</h4>


<div class="panel-group pb3" id="accordion">
<div class="panel panel-default">
<div class="panel-heading">
<h4 class="panel-title">
<a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion" href="#collapse7">Dlaczego rekuperacja?</a>
</h4>
</div>
<div id="collapse7" class="panel-collapse collapse in">
<div class="panel-body">
<strong>1. Jest to instalacja, ktora ma jeden z najkrotszych czasow zwrotu poniesionych nakladow.</strong><br />
- Nie inwestujemy w budowe kominow wentylacyjnych<br />
- Zmniejszamy zapotrzebowania na energie CO.<br />
- Wykorzystujemy dodatkowe zrodla energii (GWC - energia z gruntu, energia sloneczna)<br />
- System dziala niezaleznie od pogody i pory roku (zima, latem, w dzien, w nocy - sami decydujemy kiedy go uruchomic i jak wykorzystac energie)<br />
- System moze bys sterowany przez instalacje inteligentnego domu.<br />
- Zwieksza sie wartosc nieruchomosci.<br />
<br />
<strong>2. Podnosi komfort uzytkowania domu.</strong><br />
- Temperatura powietrza jest stabilna w calym domu<br />
- Nawiewane swieze powietrze jest filtrowane, pozbawione kurzu i pylu.<br />
- Nie musimy otwierac okien przez co obnizamy poziom halasu z zewnatrz oraz nie dopuszczamy do wnetrza insektow zwlaszcza latem.<br />
- Wplywa na nasze zdrowie i samopoczucie chocby poprzez komfort snu.
</div>
</div>
</div>
<div class="panel panel-default">
<div class="panel-heading">
<h4 class="panel-title">
<a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion" href="#collapse1">Czy uwzgledniac instalacje rekuperacji w fazie projektu?</a>
</h4>
</div>
<div id="collapse1" class="panel-collapse collapse in">
<div class="panel-body">
Tak. To jest jedno z zalozen zmniejszenia wydatkow na nasza inwestycje.<br />
Przede wszystkim nie projektujemy kominow wentylacji grawitacyjnej. Dobrze jest juz zdecydowac sie na technologie wykonania instalacji wentylacyjnej. W przypadku technologii PE-FLEX nalezy uwzglednic w projekcie wysokosc izolacji na stropie (przynajmniej 8cm), oraz przewidziec przestrzen w suficie na poddaszu uzytkowym (ok. 12-15cm). Czyli podniesc jetki (kleszcze) o ok. 20cm wyzej. Uwzgledniamy takze miejsce na rekuperator np.: garaz, kotlownia, lub schowek (160cm/szer. 120cm/wys, 50cm/gleb) wraz z miejscem na czerpnie i wyrzutnie.
</div>
</div>
</div>
<div class="panel panel-default">
<div class="panel-heading">
<h4 class="panel-title">
<a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion" href="#collapse2">Czy potrzebny jest projekt instalacji wentylacyjnej jeszcze przed budowa?</a>
</h4>
</div>
<div id="collapse2" class="panel-collapse collapse in">
<div class="panel-body">
Najlepsze projekty sa wykonywane w wiekszosci przez architektow projektujacych nasz dom. Ale wiekszosci do budowy instalacji wentylacyjnej podchodzi sie tak jak przy budowie instalacji CO lub elektrycznej. Wazne jest aby uwzglednic zalozenia z 1 punktu.
</div>
</div>
</div>
<div class="panel panel-default">
<div class="panel-heading">
<h4 class="panel-title">
<a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion" href="#collapse3">Jak dobrac rekuperator do naszego domu</a>
</h4>
</div>
<div id="collapse3" class="panel-collapse collapse in">
<div class="panel-body">
Najlepiej powierzyc to firmie projektujacej lub wykonujacej nasza instalacje wentylacyjna. Na dobor rekuperatora ma wplyw nie tylko kubatura budynku ale przede wszystkim ilosc i rodzaj pomieszczen w naszym domu.<br /><br />
Niektore z przyjmowanych zalozenia to:<br />
<strong>Nawiewy:</strong><br />
20 m3/h dla kazdej osoby,<br />
30 m3/h dla kazdej osoby w pomieszczeniu, w ktorym mozna palic,<br />
15 m3/h dla dziecka,<br />
30 m3/h dla kazdej osoby w pomieszczeniu klimatyzowanym oraz wentylowanym o nieotwieralnych oknach,<br />
50 m3/h dla kazdej osoby w pomieszczeniu klimatyzowanym oraz wentylowanym o nieotwieralnych oknach w przypadku palenia.<br />
<br />
<strong>Wywiew:</strong><br />
<i class="fa fa-caret-right" aria-hidden="true"></i> 70 m3/h w kuchni wyposazonej w kuchenke gazowa,<br />
<i class="fa fa-caret-right" aria-hidden="true"></i> 50 m3/h w kuchni wyposazonej w kuchenke elektryczna,<br />
<i class="fa fa-caret-right" aria-hidden="true"></i> 50 m3/h w lazience,<br />
<i class="fa fa-caret-right" aria-hidden="true"></i> 30 m3/h w WC,<br />
