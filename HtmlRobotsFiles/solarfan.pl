</h4>
</div>
<div id="collapse6" class="panel-collapse collapse in">
<div class="panel-body">
Tak. Ale dotyczy to stropow monolitycznych (zelbetonowych). Przed wylaniem stropu umieszcza sie formy dla otworow wentylacyjnych. Obniza to koszty zwiazane z przewiertami oraz nie narusza konstrukcji plyty, poniewaz przewierty przecinaja zbrojenie.
