</h4>
</div>
<div id="collapse4" class="panel-collapse collapse in">
<div class="panel-body">
Jest to jedna z pierwszych instalacji montowanych w domu. Najlepiej rozpoczac w stanie surowym zamknietym, ale czesto mozna prace rozpoczac wczesniej jeszcze w stanie surowym otwartym. Jest to instalacja ktora nie lub kolizji z innymi mediami,  rury musza byc prowadzone mozliwie lagodnymi lukami.
</div>
</div>
</div>
<div class="panel panel-default">
<div class="panel-heading">
<h4 class="panel-title">
<a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion" href="#collapse5">Ile trwa montaz instalacji wentylacyjnej?</a>
</h4>
</div>
<div id="collapse5" class="panel-collapse collapse in">
<div class="panel-body">
Sklada sie z 2 lub 3 etapow. Etap 1: przewierty stropow, montaz rur na stropie i w konstrukcji dachu, przygotowanie czerpni i wyrzutni (ok. 3-4 dni). Etap 2 i 3: po wykonczeniu wnetrza: montaz anemostatow, czerpni i wyrzutni oraz montaz rekuperatora. Uruchomienie wentylacji po zakonczeniu prac zwiazanych z zapyleniem pomieszczen.
</div>
</div>
</div>
<div class="panel panel-default">
<div class="panel-heading">
<h4 class="panel-title">
