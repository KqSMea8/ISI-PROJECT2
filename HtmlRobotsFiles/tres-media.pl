Od 1994 roku zawodowo zajmuje sie udzielaniem pomocy prawnej Firmom i osobom oraz
doradztwem prawnym w ramach wlasnej kancelarii prawnej.<br><br>
W swojej praktyki mam doswiadczenie w udzielaniu pomocy prawnej i doradztwie malym, srednim i
duzym przedsiebiorcom, dla spolek branzy produkcji materialow budowlanych, poligraficznej,
spozywczej, hotelarskiej oraz innych uslug z terenu calego kraju.<br><br>
Posiadam praktyczna znajomosc wielu dziedzin prawa ze szczegolnym uwzglednieniem, praw
zwiazanych z prowadzeniem dzialalnosci gospodarczej, prawa pracy, kodeksu spolek handlowych.
Reprezentuje Firmy i osoby fizyczne w sprawach sadowych i administracyjnych na terenie calego
Kraju.<br><br>
Bedac czlonkiem Samorzadowego Kolegium Odwolawczego przez 7 lat zyskalem rozlegle
doswiadczenie w prawie administracyjnym.<br><br>
Jednoczesnie w ponad 20 letniej praktyce rozwiazywalem problemy osob fizycznych zajmujac sie
prawem rodzinnym, spadkowym, czy tez wynikajacym z zawartych umow.<br><br>
Radca prawny od 1994 , ukonczyl Wydzial Prawa i Administracji Uniwersytetu Mikolaja Kopernika w
Toruniu oraz aplikacje radcowska w Okregowej Izbie Radcow Prawnych w Toruniu.<br><br>
Czlonek Izby Przemyslowo-Handlowej w Toruniu.<br><br>

Posiadam takze bogate doswiadczenia w sprawach rozwiazywania sporow i reprezentowania w
sprawach rodzinnych - rozwodowych , podzialow majatku , spadkowych.<br><br>

Urodzilem w 1967 r., Szkola srednia w Bydgoszczy VI LO , Studnia UMK w Toruniu<br>
Szczesliwy maz i ojciec , zamieszkaly w Toruniu od czasu studiow .<br>
Wolny czas - jesli jest - spedzam w rodzina ,<br>
Staram sie byc aktywny sportowo dosc regularnie plywanie i silownia -<br>
A wypoczynek najlepiej w gorach a zima wiadomo nartki ...<br>
