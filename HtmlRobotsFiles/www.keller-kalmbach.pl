<p class="bodytext"><font size="3"><b>Keller &amp; Kalmbach</b></font></p>
<p class="bodytext">&nbsp;</p>
<p class="bodytext">Firma Keller &amp; Kalmbach zostala zalozona w 1878 r. i nalezy dzisiaj do czolowych przedsiebiorstw sprzedazy hurtowej elementow zlacznych, srub, elementow do mocowania, kolkow, czesci wykonanych wedlug rysunkow, narzedzi recznych, narzedzi recznych z napedem elektrycznym, dzwignic, produktow ochrony pracy, wyposazenia zakladow, produktow chemicznych itd. </p>
<p class="bodytext">&nbsp;</p>
<p class="bodytext">&nbsp;</p>
<p class="bodytext">Od roku 1988 przedsiebiorstwo Keller &amp; Kalmbach rozwijalo sie w kierunku firmy specjalistycznej w dziedzinie managementu artykulow o niskiej wartosci zakupu. Ponad 2.000 zrealizowanych projektow i ponad 2.200.000 napelnien zbiornikow rocznie mowia same za siebie. Keller &amp; Kalmbach zatrudnia 900 pracownikow i osiaga obrot roczny w wysokosci 333 milionow Euro. Przedsiebiorstwo ma trzy zakresy dzialalnosci: przemysl samochodowy, przemysl i rzemioslo.</p>
