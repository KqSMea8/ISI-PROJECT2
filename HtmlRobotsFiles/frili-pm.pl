<p style="text-align: justify;">Frili Project Management Sp. z o.o. zajmuje sie zarzadzaniem budowlanymi projektami inwestycyjnymi. Oferujemy kompleksowe uslugi w tym zakresie od wsp&oacute;lpracy z organami administracji publicznej przez wyb&oacute;r lokalizacji, projektanta, Generalnego Wykonawcy, koordynacje prac projektowych, organizacje pelnobranzowego nadzoru inwestorskiego, przeprowadzamy odbiory czesciowe i koncowe rob&oacute;t.</p>
<p style="text-align: justify;">Stoi za nami ponad 10-letnie doswiadczenie na rynku inwestycji budowlanych, w tym industrialnych, komercyjnych, mieszkaniowych. Projekty realizujemy wg standard&oacute;w FIDIC, kt&oacute;rych stosowanie jest podstawa um&oacute;w finansowanych przez najwieksze instytucje takie jak Bank Swiatowy czy jednostki administracji publicznej.</p>
<p style="text-align: justify;">Wsp&oacute;lpraca z nami to gwarancja optymalizacji koszt&oacute;w inwestycji przy zachowaniu najwyzszej jakosci uslug.</p>
