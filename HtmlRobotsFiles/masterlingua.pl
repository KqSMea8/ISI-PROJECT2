</div><div style="padding-top: 17px; padding-bottom: 17px; border-top: dotted 1px #898989; width: 607px;"><h2><img src="/static/fonts/upload/Technologia.gif" alt="Technologia" /></h2><br /><p>
<b>Platforma tlumaczeniowa</b></p>
<p style="text-align: justify; ">
Przewaga biura jest szerokie wykorzystanie najnowszych osiagniec w dziedzinie komputerowego wspomagajacych procesu tlumaczenia oraz przeplywu dokumentow i informacji pomiedzy klientem, naszym biurem, oraz tlumaczami pracujacymi nad projektem.</p>
<p style="text-align: justify; ">
</p>
<p style="text-align: justify; ">
Dzieki wykorzystaniu nowoczesnej platformy tlumaczeniowej SaaS (software as a service - oprogramowanie jako usluga) jestesmy w stanie wykonac praktycznie kazde zlecenie niezaleznie od kombinacji jezykowej oraz geograficznego polozenia klienta.&nbsp;</p>
</div><div style="padding-top: 17px; padding-bottom: 17px; border-top: dotted 1px #898989; width: 607px;"><h2><img src="/static/fonts/upload/Korzy%25C5%259Bci.gif" alt="Korzysci" /></h2><br /><p>
<span class="Apple-style-span" style="font-weight: 800;">Czas to pieniadz...</span></p>
<p style="text-align: justify; ">
Powiedzenie traci myszka lecz kto nie zgodzi sie z nami, ze w dzisiejszym swiecie czas jest rzadkim dobrem? Szanujemy czas naszych klientow i dlatego oferujemy im w pelni zautomatyzowany modul dostepu do projektu za pomoca zwyklej przegladarki internetowej.&nbsp;</p>
<p style="text-align: justify; ">
</p>
<p style="text-align: justify; ">
Dostep do platformy SaaS zapewnia klientowi otrzymanie wszelkich informacji na temat projektu w jednym miejscu - natychmiastowa wstepna wycene projektu wraz z uwzglednieniem powtarzalnych jednostek tlumaczeniowych, sprawdzenie postepu prac oraz mozliwosc wprowadzenia zmian do projektu nawet w ostatniej chwili. Wszystko to pozwala klientowi lepiej kontrolowac koszty zwiazane z projektem oraz maksymalnie skrocic czas potrzebny na tlumaczenie.&nbsp;</p>
