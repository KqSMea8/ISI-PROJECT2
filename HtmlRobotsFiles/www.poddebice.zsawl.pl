<h2 class="art-postheader">
<font face="Tahoma, Arial, Helvetica, Sans-Serif" size="2">&nbsp;&nbsp;o nas</font></h2>
</div>

<div class="art-postcontent art-postcontent-0 clearfix"><div class="art-content-layout-wrapper layout-item-0">
<div class="art-content-layout layout-item-1">
<div class="art-content-layout-row">
<div class="art-layout-cell layout-item-2" style="width: 100%" >        <p>
<font size="2">
<font face="Arial" style="font-size: 9pt">Klub
&#8220;Zyczliwych Serc&#8221; jest stowarzyszeniem
abstynenckim zrzeszajacym osoby uzaleznione i
wspoluzaleznione od alkoholu , czlonkow ich
rodzin oraz osoby sympatyzujace z ruchem
trzezwosciowym, ktore deklaruja postawe
proabstynencka i propaguja idee trzezwego zycia.</font><p align="justify">
<font face="Arial" style="font-size: 9pt">Stowarzyszenie powstalo w 1998
roku.</font><p align="justify">
<font face="Arial" style="font-size: 9pt">
Dzialamy terenie miasta, gminy i powiatu
poddebickiego. Swa dzialalnosc opieramy przede
wszystkim na spolecznej pracy naszych czlonkow,
choc do realizacji niektorych zadan zatrudniamy
pracownikow: koordynatorow, psychologow,
terapeutow uzaleznien, opiekunow grup.</font><p align="justify">
<font face="Arial" style="font-size: 9pt">Dzialamy w oparciu o Statut
Stowarzyszenia. Celem organizacji jest
ksztaltowanie u swoich czlonkow trwalych nawykow
abstynencji, propagowanie idei trzezwosci w
srodowisku lokalnym, profilaktyka i praca z
mlodzieza. Dzialamy na rzecz poprawy warunkow
terapii oraz zwiekszenia dostepu do pomocy
terapeutycznej osobom uzaleznionym i
wspoluzaleznionym.</font><p align="justify">
<font face="Arial" style="font-size: 9pt">Pomagamy osobom, ktorych
bezposrednio lub posrednio dotycza problemy
alkoholowe. Dzialamy  rowniez na rzecz osob niepelnosprawnych.</font><p align="justify">
<font face="Arial" style="font-size: 9pt">Wspolpracujemy z:</font><ul>
<li>
<p align="justify">
<font face="Arial" style="font-size: 9pt">Urzedem Miasta Poddebice</font></li>
<li>
<p align="justify">
<font face="Arial" style="font-size: 9pt">Gminna Komisja Rozwiazywania Problemow Alkoholowych</font></li>
<li>
<p align="justify">
<font face="Arial" style="font-size: 9pt">Starostwem Powiatowym</font></li>
<li>
<p align="justify">
<font face="Arial" style="font-size: 9pt">Miejskim i Gminnym Osrodkiem Pomocy Spolecznej w Poddebicach</font></li>
<li>
<p align="justify">
<font face="Arial" style="font-size: 9pt">Zwiazkiem Druzyn Harcerskich Gminy Poddebice</font></li>
</ul>
<p align="justify">
<font face="Arial" style="font-size: 9pt">Klub dysponuje
czterema, odpowiednio wyposazonymi
pomieszczeniami. <br>
Dysponujemy trzema komputerami z pelnym
oprzyrzadowaniem i oprogramowaniem oraz stalym
dostepem do Internetu. W roku 2005 otrzymalismy
trzy zestawy komputerowe od sponsora z Niemiec.
Dwa z nich pozostaly w Klubie, trzeci zestaw
przekazalismy harcerzom.</font></p>
<p align="justify">
<font face="Arial" style="font-size: 9pt">Dzialalnosc
nasza finansowana jest nie tylko z dotacji
Urzedu Miasta, jaka otrzymujemy w konkursie
ofert w ramach realizacji gminnego programu
profilaktyki i rozwiazywania problemow
alkoholowych, choc jest to podstawowe zrodlo
dochodow, jakie przeznaczamy na nasza
dzialalnosc.<br>Do tej pory realizowalismy
Programy:<br><b>8 projektow</b> z konkursu ofert
na realizacje zadan w ramach Gminnego Programu
Rozwiazywania Problemow Alkoholowych,
wspolfinansowanych przez Urzad Miejski w
Poddebicach.<br><b>8 projektow</b>
wspolfinansowanych z Panstwowego Funduszu
Rehabilitacji Osob Niepelnosprawnych w ramach
konkursow ofert oglaszanych przez Powiatowe
Centrum Pomocy Rodzinie w Poddebicach<br><b>1 projekt</b> wspolfinansowany przez Regionalne
Centrum Polityki Spolecznej ( R.C.P.S.) w Lodzi
&#8220;Moje wakacje &#8211; ze zdrowiem na ty&#8221;.<br>
<b>1 projekt</b> zrealizowany w ramach Programu
Fundusz Inicjatyw Obywatelskich &#8222;FIO 2008&#8221;
Ministerstwa Pracy i Polityki Spolecznej.</font></p>
<p align="justify">
<font face="Arial" style="font-size: 9pt">Do stalych
dzialan w ramach realizowanych programow weszly
wyjazdy do Czestochowy, Lichenia, jestesmy
uczestnikami Ogolnopolskiego Zlotu Rodzin
Abstynenckich w Malym Cichym i &#8220;Rajdu Klonowego
Liscia&#8221; w wielkopolskim Sierakowie.<br>
Corocznie na poczatku maja organizujemy
uroczystosci z okazji Rocznicy powstania naszego
Klubu, gdzie goscimy ok. 300 osob z terenu nie tylko woj.
lodzkiego, ale i woj. wielkopolskiego, slaskiego, opolskiego
i mazowieckiego.<br>
Corocznie spotykamy sie z dziecmi i mlodzieza
Poddebic na Spotkaniach Wigilijnych, polaczonych
z wizyta Sw. Mikolaja i wreczaniem paczek dla
naszych najmlodszych beneficjentow.</font></p>
<p align="justify">
<font face="Arial" style="font-size: 9pt">Na rzecz
Stowarzyszenia dzialaja i pracuja nieodplatnie
wszyscy jego czlonkowie.
</font></p>
<p align="justify">
<font face="Arial" style="font-size: 9pt">Stowarzyszenie jest od roku 2004 czlonkiem &#8211;
zalozycielem Zwiazku Stowarzyszen Abstynenckich
Wojewodztwa Lodzkiego.<br>Jestesmy rowniez wsrod
czlonkow Polskiej Rady Ruchu Abstynenckiego. <br>Bierzemy czynny udzial w
pracach tych organizacji - kursach, szkoleniach,
warsztatach.</font></p>
<p align="justify">
<font face="Arial" style="font-size: 9pt">Jestesmy w
powiecie poddebickim jedynym stowarzyszeniem,
ktore fachowo odpowiada na potrzeby naszego
lokalnego srodowiska, w zakresie
specjalistycznej pomocy osobom zagrozonym
wykluczeniem spolecznym z powodu uzaleznienia od
alkoholu. Realizujemy rowniez samopomocowa forme
wsparcia rodzin z problemem alkoholowym.</font></p>
<p align="center">
<b>
<font face="Arial" style="font-size: 9pt">Stowarzyszenie Abstynentow
&#8222;ZYCZLIWYCH SERC&#8221;<br>oferuje
wszystkim potrzebujacym pomocy</font></b><p align="justify">
<font face="Arial" style="font-size: 9pt">PUNKT I KONTAKTU czynny w
godzinach otwarcia Klubu.<br>
Sluzy osobom, ktore chca uzyskac wiadomosci
na temat istoty choroby alkoholowej, sposobie
radzenia sobie z problemami, jakie niesie ze
soba zycie pod jednym dachem z osoba
uzalezniona. Tu motywujemy nowo przychodzace
osoby do podjecia leczenia.<br>GRUPE WSPARCIA w
zaleznosci od potrzeb.<br>Zajecia prowadzone sa
w formie warsztatow lub grup samopomocowych.<br>
GRUPE AA.<br>Spotkania grypy
anonimowych alkoholikow odbywaja sie raz w
tygodniu w siedzibie Klubu abstynenta.</font><p align="center">
<b><font face="Arial" style="font-size: 9pt">Jesli Twoja rodzina ponosi
skutki naduzywania alkoholu<br>Przyjdz do Nas -
sprobujemy Ci pomoc.</font></b></p>
<p align="justify">
<font face="Arial" style="font-size: 9pt">
Zapewniamy dyskrecje, zrozumienie i fachowa
pomoc, a nasze uslugi sa bezplatne.</font></p>


</font></p>
