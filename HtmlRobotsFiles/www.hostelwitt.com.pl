Plater
9/11,
na pierwszym pietrze pieknie odrestaurowanej XIX wiecznej kamienicy, w wielce
imponujacym
dawnym
mieszkaniu budowniczego i gisera Adolfa Witta.</p>
<p>Nasz hostel oferuje <b>tanie noclegi w Warszawie</b> oraz liczne atrakcje. Wszystko to w
dogodnej lokalizacji, w samym centrum Warszawy co sprawia, ze aby korzystac z urokow
miasta
nie trzeba
tracic
czasu na dojazdy.</p>
<p>Budynek mieszkalny, podworze wraz z zabytkowa fabryczka Witta stanowia wyjatkowy element
kultury
i
