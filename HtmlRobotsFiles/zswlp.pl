<p>Zielona baza rowerowa znajduje sie w Zawoi Welczy. Jest to miejscowosc polozona w Beskidach u stop Babiej Gory w gminie Zawoja. Baze zorganizowano na skwerku edukacyjnym prowadzonym przez Zawojskie Stowarzyszenie Wlascicieli Lasow Prywatnych. To dobre miejsce, ma dogodny dojazd. Jadac droga wojewodzka nr 957 od Bialki w kierunku Jablonki, skrecamy w prawo w kierunku Welczy. Po przejechaniu jednego kilometra skrecimy w prawo w kierunku Jaworskiego i praktycznie jestesmy na miejscu.<br />
Po krotkim odpoczynku mozemy wyruszyc na szlak rowerowy. W bezposrednim sasiedztwie Bazy przebiegaja dwa z szesciu wyznaczonych, oznakowanych i opisanych szlakow rowerowych.<br />
Co wiecej, w Pasmie Jalowca, z ktorym graniczymy z gmina Stryszawa rowniez przebiegaja szlaki rowerowe. W niektorych miejscach szlaki obu gmin maja punkty styczne, zatem jezdzacy rowerami ,,przeskakuja" z jednego szlaku na drugi.</p>
<p>Dlaczego warto odwiedzic Zielona baze rowerowa?</p>
<p>Jest to przyjazne miejsce dla turystow przemierzajacych na rowerach gorskie szlaki rowerowe jak rowniez dla tych, ktorzy dotra tu samochodem z rowerami na bagazniku.<br />
Zapraszamy turystow indywidualnych i grupy zorganizowane.<br />
Do dyspozycji gosci trzy zadaszone wiaty mogace pomiescic lacznie 50 osob. Mozna pod nimi spozyc posilek, przeczekac deszcz w razie deszczowej pogody, odpoczac po wyczerpujacej jezdzie na trudnych trasach gorskich.<br />
Rowery mozna bezpiecznie postawic na stojakach. Jest stanowisko do ich mycia.<br />
W malym drewnianym budynku mozna zagotowac wode na kawe czy herbate i przyrzadzic posilek.</p>

