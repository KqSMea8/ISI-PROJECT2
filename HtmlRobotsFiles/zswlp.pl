<p><strong><a href="http://zswlp.pl/?attachment_id=491" rel="attachment wp-att-491"><img class="aligncenter  wp-image-491" src="http://zswlp.pl/wp-content/uploads/dsc-0038_8.jpg" alt="dsc-0038_8" width="657" height="370" srcset="http://zswlp.pl/wp-content/uploads/dsc-0038_8.jpg 980w, http://zswlp.pl/wp-content/uploads/dsc-0038_8-200x112.jpg 200w, http://zswlp.pl/wp-content/uploads/dsc-0038_8-102x57.jpg 102w" sizes="(max-width: 657px) 100vw, 657px" /></a></strong></p>
<p>Zawojskie Stowarzyszenia Wlascicieli Lasow Prywatnych przystapilo  do realizacji Projektu ,,Edukacja ekologiczna i poprawa zroznicowania gatunkowego drzewostanow w lasach prywatnych". Przedsiewziecie bylo realizowane na terenie skwerku edukacyjnego i szkolki lesnej prowadzonej przez nasze Stowarzyszenie.</p>
<p>Wykonawcami projektu byli i sa przede wszystkim czlonkowie ZSWLP i ich dzieci, jednak kazdy mieszkaniec Zawoi mogl i moze dalej w nim uczestniczyc. W prawdzie zakonczono<br />
juz szkolenia i konsultacje, dokonano zakupowale hodowla sadzonek bedzie trwala jeszcze przez co najmniej  4 lata. A wiec kazda pomoc sie przyda, zachecam tym bardziej, ze mozna za to otrzymac wyhodowane sadzonki. W realizacji projektu uczestniczyli uczniowie z Zespolu Szkolno-Przedszkolnego Zawoja Mosorne i Zespolu Szkol w Zawoi Wilcznej.Celem projektu byla edukacja ekologiczna i poprawa zroznicowania gatunkowego w lasach prywatnych, poprawa ich zdrowotnosci, a tym samym przywrocenie im wlasciwych pierwotnych funkcji, przez hodowle zalecanych gatunkow drzew takich jak: jodla modrzew polski, buk zwyczajny, jawor, sosna i wysadzanie ich w lasach oraz podnoszenie wiedzy obecnych i przyszlych wlascicieli lasow. Podczas szkolen konsultacji uswiadomilismy uczestnikom, ze las to nie tylko produkcja drzewa dla osiagniecia dochodu ale takze srodowisko zycia ptakow i malych gryzoni lesnych,<br />
ktore sa wazne i im tez nalezy stworzyc dobre warunki bytowania. Posadzenie krzewow lesnych stwarza dobre warunki do gniazdowania i zapewnia im pokarm. Uczestnicy zajec nauczyli  sie takze jak prawidlowo sadzic drzewka, bo wykonywali te prace pod opieka pana lesniczego. Ta umiejetnosc na pewno im sie przyda jak dorosna i beda sie opiekowac wlasnym lasem. Dobra atmosfera podczas konsultacji i sadzenia drzewek a takze spotkanie przy ognisku na zakonczenie zajec bedzie milym wspomnieniem z udzialu<br />
w waznym dla ludzi i przyrody babiogorskiej wydarzeniu.<br />
Realizacja tak wielkiego zadania byla mozliwa dzieki zyczliwosci i pomocy pracownikow  Babiogorskiego Parku Narodowego i Nadlesnictwa w Suchej Beskidzkiej. Bardzo im za te pomoc dziekujemy. Jestem przekonany, ze korzysci jakie zakladano: dla lokalnego<br />
srodowiska naturalnego, uczestnikow inicjatywy i spolecznosci lokalnej zostaly osiagniete. W ramach projektu:<br />
1. Zakupiono nasiona drzew lesnych,z ktorych czesc wysiano jesienia a pozostale wlasnie sa wysiewane.<br />
2. Zakupiono 500 szt. sadzonek krzewow lesnych, ktore juz czesciowo nieodplatnie przekazano czlonkom Stowarzyszenia.<br />
3. Zakupiono zadaszenia na wiate, w ktorej mozna organizowac spotkania bez wzgledu na pogode.<br />
4. Wiata zostala wyposazona w stoly i lawy dla 30 osob.<br />
5. W najblizszym czasie zamontujemy 2 tablice edukacyjne.</p>
