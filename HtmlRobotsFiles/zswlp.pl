<p>Pierwsze spotkanie calego zespolu realizujacego projekt odbylo sie  6 wrzesnia 2014r. Rozdzielono obowiazki pomiedzy poszczegolnych czlonkow i ustalono terminy najwazniejszych dzialan. Koordynator  projektu zostal zobowiazany do opracowania konspektu zajec dotyczacych dwudziestej piatej rocznicy odzyskania wolnosci i przygotowania materialow, ktore posluza w trakcie warsztatow do podsumowania dorobku Stowarzyszenia przez 12 lat istnienia. Do opracowania konspektu postanowiono wykorzystac broszure &#8221;Polska 25 lat wolnosci&#8221;, ktora otrzymali uczniowie na rozpoczecie roku szkolnego, a do drugiego zadania : ksiege pamiatkowa, dyplomy podziekowania, projekty zrealizowane przez ten czas, dokumentacje zdjeciowa, informacje prasowe i strony internetowe . Dorocie Piwko zlecono przygotowanie projektu tablicy dotyczacej Lasu Wolnosci i przygotowanie podziekowan na zakonczenie projektu. Maciej Metel zobowiazal sie przygotowac stelaz pod tablice. Franciszek Marek wzial na siebie prace z mlodzieza podczas prac szkolkarskich. Do zakupow zobowiazano Jana Smyraka. 19 wrzesnia odbyly sie pierwsze warsztaty na terenie szkolki  i skwerku edukacyjnym. Uczestniczyli w nim uczniowie z Zespolu Szkol im.Walerego Goetla z Suchej Beskidzkiej i uczniowie z Zespolu Szkolno-Przedszkolnego  z Zawoi Mosorne. Pierwsze dwie godziny przeznaczono na zapoznanie uczestnikow z pracami szkolkarskimi i pikowaniem siewek do doniczek. Nastepnie posadzono  symboliczny &#8221;Las wolnosci&#8221; w tej czesci brali udzial zaproszeni goscie , po posadzeniu drzewek ustawiono wczesniej przygotowana tablice edukacyjna i wykonano pamiatkowe zdjecie. Na zakonczenie przeprowadzono prelekcje na temat ostatnich 25 lat i zapoznano uczestnikow z dorobkiem ZSWLP. Calosc podsumowano przy ogniskuz poczestunkiem.  Wedlug tego samego scenariusza przeprowadzono warsztaty 21 wrzesnia byly jednak pewne roznice z powodu mlodszego wieku uczestnikow. Na zakonczenie rozdano chetnym sadzonki w doniczkach do posadzenia Drzewek Wolnosci w swoich lasach. Wydano lacznie 123 drzewka. 12 pazdziernika na Placu Jana Pawla II posadzono Dab sw. Jana Pawla II . Byla to duza uroczystosc, najpierw msza w kosciele z czynnym udzialem czlonkow stowarzyszenia a nastepnie na Placu z udzialem Stowarzyszenia Nasza Skawica i Dzieciecego Zespolu Regionalnego &#8222;Zbojnik&#8221;, zaproszonych gosci i licznie zgromadzonej ludnosci lokalnej. Samego posadzenia debu dokonal Wojt Pan Tadeusz Chowaniak, Prezes Stowarzyszenia Jan Smyrak i radny Pan Kazimierz Pajak . Nastepnie zakupiono antyramy oprawiono zdjeci i urzadzono prezentacje na skwerku edukacyjnym a nastepnie w siedzibie Stowarzyszenia. Na oficjalnym zakonczeniu projektu rozdano podziekowania wszystkim tym ktorzy przyczynili sie w realizacji projektu.</p>
<p>Zarzad Stowarzyszenia dziekuje wszystkim ktorzy przyczynili sie do  realizacji projektu i zaprasza do dalszej wspolpracy.</p>
