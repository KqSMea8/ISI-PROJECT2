<div class="article_title">Witamy na stronie</div><div class="article_header"><p>
Jestesmy szkola nurkowania, ktora wprowadzi Cie w swiat wspanialej pasji i przygody w podwodnym swiecie. Zawodowo zajmujemy sie tez praca z nowofundlandami, psami szkolonymi do ratownictwa wodnego.</p>
</div><div class="article_content">
<p style="text-align: center;">
<img alt="" src="/upload/witamy1.jpg" style="width: 300px; height: 225px; margin: 10px;" /><img alt="" src="/upload/witamy2.jpg" style="width: 300px; height: 225px; margin: 10px;" /></p>
<p>
&nbsp;</p>
<p>
Stad nasza nazwa czyli niuf-dive - NIUF to pieszczotliwe okreslenie psow tej rasy. Prowadzimy kursy nurkowe na poziomie podstawowym oraz zaawansowanym a takze szkolenia specjalistyczne - liste znajdziecie w zakladce kursy. Dodatkowo prowadzimy takze kursy pierwszej pomocy oraz nauke i doskonalenie plywania.</p>
<p>
Zapraszamy wszystkich chetnych do rozpoczecia przygody z nurkowaniem wraz z nami, a osoby juz nurkujace do wspolnych wyjazdow nurkowych i rozszerzenia swoich umiejetnosci o kolejne. Prowadzimy doradztwo i sprzedaz sprzetu nurkowego a takze jego serwis.</p>
<p>
Na zakonczenie zapraszamy wszystkich na organizowane przez nas wyjazdy, zarowno te nad najblizsze oczko wodne, jak i w odlegle strony swiata.</p>
