<h2 class="fflora">Lato</h2>
<p><strong>Wycieczki  Piesze</strong><br>
Domki Cyrhlica to  idealna lokalizacja do wedrowek pieszych. Juz zaledwie kilka krokow od domku  zaczyna sie malowniczy szlak widokowy na Gubalowke przez lokalne wsie. Panorame  Tatr mozna takze podziwiac z grani Skoruszowki, Gubalowki i Bzdykowki, ktore  wrecz otaczaja domek. Bliskosc doliny Chocholowskiej oraz Koscieliskiej ucieszy  bardziej zaawansowanych turystow, chcacych wybrac sie w zachodnie partie Tatr.  Czas dojazdu to zaledwie kilka minut. Takze wycieczka do Zakopanego nie bedzie  stanowila problemow. Omijajac najbardziej zatloczone drogi w spokoju bedziecie  Panstwo mogli w kazdej chwili dojechac do Zakopanego na Krupowki, w drodze podziwiajac  Panorame Tatr.<br>
<strong><br>
Rowerem  dookola Tatr</strong><br>
Wsiadz na rower i  wyjedz z domku bezposrednio najpiekniejsza sciezke rowerowa na Podhalu. W  odleglosci 200 m. od domkow &bdquo;Dookola Tatr&rdquo;, ktora prowadzi az na Slowacje do  Trsteny i Nowego Targu. Widoki gwarantowane. Przed wyjazdem nalezy pamietac o  koniecznosci posiadania kaskow po slowackiej stronie. Dla Panstwa wygody na  miejscu do wypozyczenia rowery gorskie. <br>
<strong><br>
Wedkarstwo </strong><br>
Wokol domku plyna dwa  strumienie, gdzie w asyscie profesjonalnego wedkarza sprobujesz swoich sil w  wedkarstwie muchowym. Doskonala alternatywa dla ludzi ceniacych sobie bliskie  obcowanie z natura. Polecamy kursy wedkarskie organizowane przez lokalna firme  Tatra Fly Fishing.</p>
