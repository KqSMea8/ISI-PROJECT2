<div id="a-trasy" class="venoframe-container toggleonload">
<div style="background:#fff; width:100%; height:100%;  float:left; padding:10px;" class="fcgray2 venoframe text-justify">
<p class="padding-top20"><strong>Szlak rowerowy wokol Tatr</strong> ma laczyc  polska czesc gor ze Slowacja. <strong>Prowadzi on przez malownicze okolice o  niepowtarzalnych krajobrazach</strong>. Trasa ta powstala w oparciu o koncepcje  dziedzictwa historyczno - kulturowego. Dzieki stworzeniu tego obiektu turysci  moga poznac wiele ciekawostek na temat przeszlosci i zapoznac sie z pieknem  gor. Ponadto przyjrzec sie tutaj mozna oryginalnemu folklorowi, sztuce ludowej,  a takze tradycyjnemu dla tamtych okolic budownictwu, drewnianym Kosciolom oraz  wielu zabytkom. <strong>Trasa szlaku rowerowego liczy az 250 kilometrow</strong>, dlatego jest  to atrakcja dla wytrwalych, jednak wcale nie oznacza to, ze ktos, kto nie jest  rowerzysta - wyczynowcem nie podola temu wyzwaniu.</p>
<p class="padding-top20">W ten sposob mozna poznac zarowno  kulture wlasnego kraju, jak i naszego sasiada - rownie efektownej Slowacji.  Jest to wiec wycieczka ksztalcaca, ale jednoczesnie rekreacyjna, poniewaz w jej  trakcie mozemy pooddychac swiezym powietrzem i nacieszyc oczy niesamowitym  widokiem.</p>
<p class="padding-top20">        Przed  wyjazdem nalezy pamietac o koniecznosci posiadania kaskow po slowackiej stronie.  Dla Panstwa wygody na miejscu do wypozyczenia rowery gorskie.</p>
