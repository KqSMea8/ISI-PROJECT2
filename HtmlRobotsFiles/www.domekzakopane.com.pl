<p class="padding-top20"><strong>Ferie  w gorach</strong> to nie  tylko bierny, ale takze - a moze przede wszystkim - aktywny wypoczynek. Procz  wspinaczek, na ktore decydujemy sie zwykle podczas cieplejszych okresow w ciagu  roku, najbardziej popularnym sportem jest tu narciarstwo. Oczywistym jest, ze w  Tatrach nie brakuje miejsc do tego przystosowanych, takich jak stoki. Warte  odwiedzenia sa <strong>stoki narciarskie w Witowie.</strong> Sa one swietnie przygotowane  do jazdy na nartach czy snowboardzie. Warto wiec zaplanowac swoje <strong>ferie  zimowe, czy swieta Bozego Narodzenia </strong>wlasnie tutaj, by w pelni skorzystac z  urokow tej pory roku.</p>
<p class="padding-top20"> <strong>Narty,  biegowki</strong><br>
Narciarzom i snowboardzistom polecamy polozona w  niedalekiej stacje narciarska Witow-Ski na szczycie ktorej znajduja sie takze  trasy biegowe. Dla chetnych organizujemy wycieczki ski-tourowe, gdzie mozesz  sprobowac swoich sil sam zdobywajac szczyty gor. Nagroda z postaci zjazdu w  dzikich warunkach i niezapomnianych widokow gwarantowana. Milosnikom  narciarstwa biegowego polecamy trasy biegowe w Koscielisku. <br>
Pozostale polecane kompleksy narciarskie w  okolicy: Male Ciche, Polana Szymoszkowa, Rusin-Ski, Osrodek Narciarski  Kotelnica Bialczanska, Oravice</p>
<p class="padding-top20"><strong>Skutery sniezne</strong><br>
Jesli chcecie Panstwo przezyc niezapomniane  wrazenia podczas wyprawy skuterami snieznymi, bezposrednio z domkow zaczynamy  nasze wycieczki widokowe. Przejezdzamy przez Gran Gubalowki az do Zakopanego,  aby z tamtych okolic podziwiac panorame Tatr.</p>
<p class="text-justify fs12 fcgray2">&nbsp;</p>
