<h1 class="fcgold">Cyrhlica House</h1>
<p class="text-justify">Cyrhlica House <strong>to nowy drewniany domek w zabudowie blizniaczej</strong> z dwoma osobnymi apartamentami z komikiem. Polozony jest w sercu zabytkowej goralskiej wioski Chocholow. Nazwa pochodzi od znajdujacego sie nieopodal szczytu "Cyrhlica".</p>
<p class="text-justify"><br>
Do Panstwa dyspozycji oddajemy <strong>dwa w pelni wyposazone komfortowe&nbsp;apartamenty z kominkiem</strong> dla 4-6 osob.  Kazdy z nich posiada sypialnie z balkonem, lazienke, aneks kuchenny, salon z TV  i kominkiem i taras. Domek posiada centralne elektryczne ogrzewanie. </p>
<p class="text-justify"><br>
Majac na uwadze fakt, iz&nbsp;<strong>wioska  Chocholow</strong>&nbsp;wpisana jest na liste swiatowego dziedzictwa UNESCO  drewniane domki powstaly z zachowaniem goralskiej tradycji z elementami  nowoczesnosci. <strong>Wykonczenie i regionalne akcenty</strong> zostaly <strong>wykonane przez  lokalnych artystow i rzezbiarzy</strong>, dzieki czemu podczas pobytu mozecie Panstwo  poczuc ducha goralszczyzny. W niedalekiej odleglosci od domkow polozone sa&nbsp;baseny  termalne <strong>Chocholowskie Termy</strong>,&nbsp;sciezka  rowerowa dookola Tatr oraz <strong>wyciag narciarski Witow-Ski.</strong> W zaledwie kilka minut  samochodem mozna dojechac na szczyt Gubalowki i do Zakopanego. </p>
<p class="text-justify"><br>
W niedalekiej odleglosci od Domkow znajduja sie: Karczma regionalna  &bdquo;U Sliwy&rdquo;, <span class="nowrap">2 sklepy</span> spozywcze (800 m), Karczma Witowianka i pizzeria (ok. 4  km). Polecamy takze restauracje w Termach Chocholowskich zaledwie  900 m od Domku.</p>
<p class="text-justify"><br>
Wlozylismy serce w powstanie Domkow Cyrhlica, dlatego mamy nadzieje, ze  pobyt u nas dostarczy Panstwu niezapomnianych i milych wspomnien.</p>
<p class="text-justify">&nbsp;</p>
