<p class="padding-top20"><strong>Wycieczki Piesze<br>
</strong>
Domki Cyrhlica to idealna lokalizacja do  wedrowek pieszych. Juz zaledwie kilka krokow od domku zaczyna sie malowniczy  szlak widokowy na Gubalowke przez lokalne wsie. Panorame Tatr mozna takze  podziwiac z grani Skoruszowki, Gubalowki i Bzdykowki, ktore wrecz otaczaja domek.  Bliskosc doliny Chocholowskiej oraz Koscieliskiej ucieszy bardziej  zaawansowanych turystow, chcacych wybrac sie w zachodnie partie Tatr. Czas  dojazdu to zaledwie kilka minut. Takze wycieczka do Zakopanego nie bedzie  stanowila problemow. Omijajac najbardziej zatloczone drogi w spokoju bedziecie  Panstwo mogli w kazdej chwili dojechac do Zakopanego na Krupowki, w drodze  podziwiajac Panorame Tatr.</p>
<p class="padding-top20"><strong><strong>Wedkarstwo&nbsp;</strong></strong><br>
Obok domku plynie gorski potok &bdquo;Mlynowka&rdquo;, oraz w odleglosci 200 m rzeka  &bdquo;Czarny Dunajec&rdquo; gdzie w asyscie profesjonalnego wedkarza sprobujesz swoich sil  w wedkarstwie muchowym. Doskonala alternatywa dla ludzi ceniacych sobie bliskie  obcowanie z natura. Polecamy kursy wedkarskie organizowane przez lokalna firme  Tatra Fly Fishing: <a href="http://www.flyfishing.pl" target="_blank" class="fcgray2 fs10">www.flyfishing.pl</a></p>
<p class="padding-top20"><strong>Wycieczki krajoznawcze</strong><br>
Aby blizej poznac  region Podhala proponujemy skorzystac z wycieczek organizowanych przez lokalne  biura podrozy m.in. Zamek w Niedzicy i Splyw Dunajcem czy wycieczka Dookola  Tatr: <a href="http://www.strama.pl" target="_blank" class="fcgray2 fs10">www.strama.pl</a></p>
<p class="text-justify fs12 fcgray2">&nbsp;</p>
