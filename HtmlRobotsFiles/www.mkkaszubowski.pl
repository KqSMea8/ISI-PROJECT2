<h2 class="title">Nasze uslugi</h2>
<p>Nasza firma swiadczy uslugi:</p>
<ul>
<li>z zakresu szerokorozumianego utrzymania czystosci (regularna obsluga biur i mieszkan, mycie okien, pranie tapicerek, utrzymanie czystosci ciagow komunikacyjnych, sprzatanie wielkopowierzchniowe),</li>
<li>utrzymania zieleni (pielegnacja trawnikow, krzewow, zywoplotow, drzew, wykaszanie traw, wykonywanie nasadzen, przycinka drzew wysokich z podnosnika)</li>
<li>utrzymania zimowego obiektow (odsniezanie reczne, odsniezanie mechaniczne, odsniezanie terenow utwardzonych i podjazdow, ulic, chodnikow, odsniezanie dachow, usuwanie sopli)</li>
<li>uslugi pralnicze, specjalizacja przemysl farmaceutyczny oraz szpitalny (pranie odziezy roboczej, fartuchow, filtrow maszyn)</li>
<li>uslugi szwalnicze (szycie elementow filtracyjnych, flag)</li>
<li>sprzatanie po budowie i remoncie</li>
<li>uslugi specjalistyczne (mycie przeszklen z podnosnika, nakladanie powlok ochronnych, koszenie kosiarka bijakowa, mechaniczne zamiatanie ulic i podjazdow)</li>
</ul>
<p><strong>Nasza firme cechuje indywidualne podejscie do Klienta. Serdecznie zapraszamy do kontaktu celem uzyskania spersonalizowanej oferty oraz wyceny.</strong></p>
