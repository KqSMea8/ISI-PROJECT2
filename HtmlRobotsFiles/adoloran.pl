<h1>O serialu</h1>
<p style="font-size: 10px; border-top: 1px solid #cccccc; color: #cccccc">Ostatnia aktualizacja: 28.08.2018 - ciekawostki, forum</p>
<p>
<strong>"W labiryncie"</strong> to serial obyczajowy z lat 1988 - 1990 wyprodukowany na zlecenie TVP, czesto nazywany
"pierwsza polska opera mydlana". Opowiada o srodowisku farmaceutow i naukowcow z warszawskiego
laboratorium farmakologii klinicznej przy Instytucie Medycyny Klinicznej i Doswiadczalnej oraz powiazanych z nim osob.
Osia serialu sa poczatkowo perypetie Ewy Glinickiej, ktora skonczywszy studia
rozpoczyna prace we wspomnianym instytucie. W kolejnych epizodach poznajemy
losy pracownikow instytutu, ich rodzin, przyjaciol i wrogow. Jednoczesnie obserwujemy codzienne zycie Polakow
z przelomu lat 80-tych i 90-tych. Uchwycenie trendow, mod i stylu zycia tego czasu to, z dzisiejszej perspektywy,
wielka zaleta tego serialu.
</p>
<p>
Serial realizowany byl metoda telewizyjna na tasmie magnetycznej
zamiast dotychczas uzywanego w serialach TV celuloidu. W sumie powstaly dwie serie: pierwsza 52 odcinkow, druga 68.
Pierwszy odcinek serialu po raz pierwszy wyemitowano 30 grudnia 1988 roku. Ostatni epizod
widzowie ujrzeli w czerwcu 1991 roku. "W labiryncie", ze wzgledu na dosc zwarta formule
i rozsadna ilosc odcinkow, powtarzano potem jeszcze kilkakrotnie, m. in. w 2005 czy 2009 roku.
Podczas premierowej emisji odcinki byly wyswietlane co tydzien (w srody o 21:45),
kazdy z nich trwal okolo 25 minut. Ogladalnosc serialu w szczytowym momencie popularnosci wynosila
ponoc 16 milionow widzow, co zapewne stanowilo 3/4 calego "rynku".
Czas akcji serialu byl w calosci zbiezny z rzeczywistym kalendarzem.
</p>


<p>
Produkcja serialu zostala przerwana "w trakcie", bez zadnego przygotowania w fabule czy rozwiazania watkow.
Wrecz przeciwnie: nawet w ostatnim odcinku pojawia sie nowa, wazna postac.
</p>
<p>
"W labiryncie", mimo ze koncepcyjnie i (ogolnie) technicznie taki sam jak wspolczesne telenowele,
mial jednak swoje smaczki takie jak:
</p>
<ul>
<li>mnostwo plenerow</li>
<li>wykorzystanie rzeczywistych miejsc i zwyczajnych, codziennych rekwizytow</li>
<li>dluzsze sceny</li>
<li>liczne ujecia kamery "z reki", sporo nietypowych dla tego gatunku ujec i sposobow kadrowania</li>
<li>gwiazdorska obsada (Niemczyk, Voith, Kondrat, Lipowska, piosenke tytulowa spiewa Markowski)</li>
<li>niedobor lub calkowity brak oswietlenia ;-)</li>
</ul>
<p>
Do dzis serial nie zostal wydany na DVD. Oficjalne wydanie przez TVP  poki co nawet <u>nie jest planowane</u>.
</p>
