<h2 class="section-title-text">ZAPROSZENIE</h2>
</div>
</div>
</div>
<div class="row">
<div class="col">
<div class="px-4 pt-4 pb-2 border">
<p><span style="font-size: 18px;"><strong>Szanowni Panstwo,</strong></span><br /><span style="font-size: 18px;"> <strong>Kolezanki i Koledzy,</strong></span></p>
<p><span style="font-size: 18px;">mamy przyjemnosc zaprosic do udzialu w Kongresie Sekcji Niewydolnosci Serca Polskiego Towarzystwa Kardiologicznego.</span></p>
<p><span style="font-size: 18px;">Niewydolnosc serca to schorzenie, kt&oacute;re dotyka wielu sposr&oacute;d naszych pacjent&oacute;w. To r&oacute;wniez dynamicznie rozwijajaca sie dziedzina kardiologii. Z kazdym rokiem pojawiaja sie nowe mozliwosci diagnostyki i leczenia. Coraz czesciej m&oacute;wimy o koniecznosci opieki kompleksowej nad pacjentami z niewydolnoscia serca, kt&oacute;rej wdrazanie rozpoczynamy w ramach projektu KONS.</span></p>
<p><span style="font-size: 18px;">Mamy nadzieje, ze nasz Kongres bedzie miejscem spotkania lekarzy, pielegniarek, fizjoterapeut&oacute;w, psycholog&oacute;w, student&oacute;w zainteresowanych niewydolnoscia serca jak r&oacute;wniez okazja do wzajemnego korzystania z bogatej wiedzy oraz doswiadczen wykladowc&oacute;w i uczestnik&oacute;w.</span></p>
<p><span style="font-size: 18px;">Atrakcyjny program naukowy obejmie aktualne zagadnienia dotyczace diagnostyki, farmakoterapii, leczenia inwazyjnego przezsk&oacute;rnego / chirurgicznego, elektroterapii, niewydolnosci serca, wraz z przeszczepem serca oraz postepowaniem w chorobach towarzyszacych.</span></p>
<p><span style="font-size: 18px;">Dogodna lokalizacja w centrum Warszawy ulatwi uczestnikom przyjazd, a zr&oacute;znicowana baza hotelowa pozwoli znalezc wygodny nocleg dostosowany do wymagan i mozliwosci.</span></p>
<p><span style="font-size: 18px;"><strong>Zyczymy wspanialych przezyc naukowych i towarzyskich!</strong></span></p>
<p><span style="font-size: 18px;">Zachecamy do wczesnej rejestracji i rezerwacji hoteli.</span></p>
<p><span style="font-size: 18px;">&nbsp;</span></p>
<p><span style="font-size: 18px;">Przewodniczacy Komitetu Organizacyjnego i Naukowego</span></p>
