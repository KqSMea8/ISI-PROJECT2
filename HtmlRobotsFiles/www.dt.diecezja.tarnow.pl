Godziny nabozenstw Wielki Tydzien &#8217;19            </a>
</h2>

<ul class="post-meta">
<li class="date-inline has-icon">
<i class="fa fa-clock-o"></i>    	<p class="post-date-inline">
<abbr class="published" title="2019-04-14T12:52:02+02:00">14 kwietnia 2019</abbr>
</p>
</li>
</ul>

<div class="entry-content clearfix">



<p><strong>WIELKI CZWARTEK</strong> - <strong>Msza sw. o godz. 18-tej</strong> a po niej przeniesienie Pana Jezusa do ciemnicy i mozliwosc adoracji przez cala noc.</p>
<p><strong>WIELKI PIATEK</strong> &#8211;&nbsp; <strong>Od godz. 6.oo do 9.oo adoracja prywatna Pana Jezusa w ciemnicy. O godz. 9.oo Droga Krzyzowa i adoracja dzieci szkolnych.</strong></p>
<p>Od godz. 10.oo bedzie adoracja wiernych z poszczegolnych rejonow parafii - tak jak w czasie 40-to godzinnego nabozenstwa.</p>
<ul>
<li>godz. 10.oo - Os. Westerplatte</li>
<li>godz. 11.oo - Olesnica</li>
<li>godz. 12.oo - Gruszow Maly</li>
<li>godz. 13.oo - Bagienica</li>
<li>godz. 14.oo - Ruda - Zazamcze</li>
<li>godz. 15.oo - Centrum Dabrowy i Podkosciele</li>
<li>godz. 16.oo - Os. Kosciuszki</li>
<li>godz. 17.oo - Os. Domkow Jednorodzinnych</li>
</ul>
<p style="text-align: justify;">Adoracje przez pierwsze pol godziny prowadza Zelatorzy i Zelatorki Zywego Rozanca, nastepnie adoracja w ciszy. <strong>Uwaga: O godz. 15.oo w ramach adoracji Godzina Milosierdzia!</strong></p>
<p style="text-align: justify;"><strong>O godz. 17.3o Droga Krzyzowa, a o godz. 18.oo rozpoczna sie ceremonie liturgiczne Wielkiego Piatku.</strong> Po ich zakonczeniu adoracja Pana Jezusa przez cala noc. <strong>Od 21 do 22 adoracje prowadzi mlodziez a od 22 do 23 Akcja Katolicka.</strong></p>
<p style="text-align: justify;"><strong>WIELKA SOBOTA &#8211;&nbsp;</strong> <strong>Adoracja jak w Wielki Piatek</strong>. O godz. <strong>10.3o adoracja dla dzieci z Ochronki</strong>. Od godz. <strong>10.oo do 15.oo w nowym kosciele bedzie poswiecenie pokarmow oraz wody</strong>, ktora zabieramy do naszych domow. W Olesnicy poswiecenie pokarmow bedzie o godz. 11-tej. <strong>O godz. 20.oo</strong> <strong>rozpoczynaja sie Obrzedy Liturgiczne Wigilii Paschalnej, ktore zakonczy procesja rezurekcyjna.</strong></p>
<p style="text-align: justify;"><strong>WIELKA NIEDZIELA</strong> - pamiatka Zmartwychwstania Pana Jezusa. Porzadek Mszy sw. jak w kazda niedziele.</p>




