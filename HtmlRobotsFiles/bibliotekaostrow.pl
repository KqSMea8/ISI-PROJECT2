<p><strong><span style="color: #000000;"><span style="font-size: 14px;"><span style="font-family: arial,helvetica,sans-serif;">Czytelnik roku 2018<br /></span></span></span></strong><span style="color: #000000;"><span style="font-size: 14px;"><span style="font-family: arial,helvetica,sans-serif;">						<div class="gallery_box_single">
24 stycznia w Samorzadowej Bibliotece Publicznej w Ostrowie Lubelskim odbyl sie kolejny final gminnego konkursu ,,Czytelnik Roku".

Wsrod finalistow znalezli sie najbardziej ,,zaczytani wsrod zaczytanych", zarowno dzieci, mlodziez jak i dorosli. Czytelnicy z najwieksza liczba wypozyczonych ksiazek oraz stosujacy sie do regulaminu zostali nagrodzeni dyplomem oraz nagroda ksiazkowa.

Cieszy fakt, ze z kazdym rokiem jest wiecej nominowanych do zaszczytnego tytulu ,,Czytelnik Roku".

Kochajmy ksiazki, one ulatwiaja nam zycie albowiem,  jak zwykle mawial Petrarka:

,, W ksiazkach jest cos szczegolnego. Ksiazki przenikaja nas do glebi (...)A zadna nie przychodzi samotnie, ale wprowadza imiona innych i jedna kaze laknac drugiej".

Do zobaczenia na kolejnym finale konkursu w 2020 r.

