<h1><a href="http://www.mikolajwilcza.katowice.opoka.org.pl/?p=1307">PARAFIA RZYMSKOKATOLICKA SWIETEGO MIKOLAJA W WILCZY (ARCHIDIECEZJA KATOWICKA, DEKANAT KNUROW)</a></h1>
<small>4 czerwca 2017</small>
<div style="text-align: justify;">Pierwszy kosciol w Wilczy, ktory zbudowano najprawdopodobniej juz w  1480 roku, zostal zniszczony w wyniku dzialan wojennych prowadzonych  podczas wojny trzydziestoletniej. Wtedy to parafia zostala przylaczona  do parafii w Pilchowicach. Kolejny budynek koscielny wzniesiono i  konsekrowano w 1657 roku. Wkrotce zmieniono jego tytul z Matki Bozej  Jasnogorskiej na sw. Mikolaja. Do XX wieku kosciol w Wilczy byl  podporzadkowany parafii w Pilchowicach. Stan ten zmienil sie dopiero w  1908 roku. Od 7 sierpnia 1920 roku obowiazki duszpasterskie w Wilczy  pelnil ksiadz Pawel Staffa. Dekretem z dnia 1 sierpnia 1925 roku  Administracja Apostolska erygowala nowa parafie w Wilczy.</div>
<div style="text-align: justify;">Istniejacy do dzis kosciol w Wilczy, ostatecznie wzniesiony  zostal w 1755 przez ciesle Jakuba Siedlaczka z Gliwic. Ma konstrukcje  zrebowa. Jednonawowy z wezszym zamknietym trojbocznie prezbiterium, przy  ktorym od polnocy znajduje sie zakrystia. Do Kosciola prowadza dwa  wejscia: od strony poludniowej (wejscie boczne) i od strony zachodniej  (glowne). Oba poprzedzone sa kruchtami. Budynek otaczaja soboty. Dach  pokryty jest gontem. Od strony frontowej kosciola wybudowano wieze,  posiadajaca oszalowana izbice, zwienczona helmem iglicowym. Prezbiterium  pokryte jest pozornym sklepieniem kolebkowym, a nawa &#8211; stropem z  fasetami. Pod sklepieniem i stropem znajduja sie: profilowany gzyms, luk  teczowy polkolisty. Nad nawa widnieje osmioboczna wiezyczka zwienczona  cebulastym helmem; od zachodu chor muzyczny z wypuklym parapetem w  czesci srodkowej, wsparty na dwoch slupach z organami poznobarokowymi z  XVIII w. Sciana prezbiterialna (od wschodu) posiada rzadko spotykane  okragle okienko z krata kowalska z zadziorami. Wnetrze kosciola zdobi  barokowe wyposazenie. Oltarz glowny barokowy z obrazem sw. Mikolaja z  drugiej polowy XVII w. pochodzi z kosciola w Gorzycach. W kosciele  znajduja sie rowniez ambona i dwa oltarza boczne poznobarokowe z XVIII  w. Szczegolnie cenny jest obraz wotywny Matki Boskiej Frydeckiej z 1709 z  napisem w jezyku morawskim oraz inne obrazy takie jak obraz Matki  Boskiej Czestochowskiej (autorstwa Franciszka Habrowskiego, 1946), Matki  Boskiej Rozancowej, pochodzacy z XIX w. a takze obraz Sw. Rodziny.</div>
<div style="text-align: justify;">Przy kosciele znajduja sie nagrobki ksiezy szczegolnie  zwiazanych z parafia w Wilczy. Znalezc mozna tam takie nazwiska jak:  Pawel Staffa &#8211; pierwszy proboszcz owej parafii, Teodor Walenta, Antoni  Danecki, Teodor Galazka, Robert Pucher, Stanislaw Kus, Rudolf Switala,  Stefan Mieszczanin, Jerzy Pluta.</div>
<div style="text-align: justify;">Przy bocznym wejsciu do kosciola umieszczono zabytkowa,  kamienna kropielnice. W poblizu kosciola znajduje sie grota Matki Bozej z  Lourdes, figura Chrystusa z gorejacym sercem oraz kapliczka z figura  sw. Franciszka z Asyzu. Budynek probostwa znajduje sie ok. 50 metrow na  zachod od kosciola przy ulicy Karola Miarki.</div>
<div style="text-align: justify;">Do parafii nalezy rowniez niewielki cmentarz z kaplica  znajdujacy sie przy ulicy Stawowej zalozony w 1906 roku. Znajduja sie na  nim miedzy innymi mogily: ofiar wypadku w fabryce ,,Lignoza" w  Krywaldzie z 1921 roku, zolnierzy polskich, poleglych w kampanii  wrzesniowej w 1939 roku oraz wiezniow obozu oswiecimskiego zamordowanych  w czasie ewakuacji obozu w styczniu 1945 roku.</p>
