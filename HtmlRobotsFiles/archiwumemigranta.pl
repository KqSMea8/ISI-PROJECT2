<h2>Historia milionow Polakow</h2><p>Historia wyjazdow z&nbsp;ziem polskich toczyla sie na&nbsp;przestrzeni setek lat. W&nbsp;najrozniejsze czesci swiata wyruszano zarowno za&nbsp;chlebem, jak w&nbsp;poszukiwaniu wolnosci i&nbsp;innego zycia. Nie zmienilo sie to&nbsp;po&nbsp;odzyskaniu przez Polske niepodleglosci. W&nbsp;wedrowke ruszano pieszo, pociagami, na&nbsp;pokladach statkow, a&nbsp;pozniej i&nbsp;samolotow. Po&nbsp;wstapieniu do&nbsp;Unii Europejskiej emigracja stala sie pokoleniowym doswiadczeniem milionow mlodych Polakow. Dzis prawie kazdy zna kogos, kto zdecydowal sie na&nbsp;emigracje. </p><p>Na calym swiecie zyje ponad 20 milionow ludzi polskiego pochodzenia. Co&nbsp;wiemy dzis o&nbsp;jednym z&nbsp;najwazniejszych zjawisk w&nbsp;polskiej historii? Czy&nbsp;potrafimy ocalic przed zapomnieniem pamiec o&nbsp;milionach ludzi, ktorzy pamiec o&nbsp;Polsce zaszczepili swoim dzieciom i&nbsp;wnukom? Czy&nbsp;mozemy poczuc to, czego doswiadczali Polacy opuszczajacy swoje domy pod&nbsp;koniec XVIII wieku lub&nbsp;zrozumiec, co&nbsp;to&nbsp;znaczy emigrowac na&nbsp;poczatku XXI wieku? Czym jest emigracja w&nbsp;dobie podrozy lotniczych?</p>
</div>
<div class="text-column text-column--orange">
<h2>Jedyne takie miejsce w&nbsp;Polsce</h2><p>W Gdyni powstaje pierwsze w&nbsp;kraju muzeum poswiecone historii polskiej emigracji. Z&nbsp;inicjatywy wladz miasta w&nbsp;historycznym gmachu Dworca Morskiego, skad przez dziesieciolecia wyplywaly polskie transatlantyki, powstaje instytucja, ktora o&nbsp;dziejach wyjazdow i&nbsp;losach Polakow na&nbsp;swiecie opowie w&nbsp;scislym zwiazku ze&nbsp;wspolczesnoscia. Historia emigracji pisze sie bowiem&nbsp;kazdego dnia. Jej najrozniejsze wymiary przedstawi wystawa stala.</p>
</div>
<div class="text-column text-column--orange">
<h2>Laczymy historie</h2><p>Misja Muzeum Emigracji w&nbsp;Gdyni jest opowiedziec o&nbsp;losach milionow zarowno anonimowych ludzi, jak i&nbsp;tych slawnych, ktorych nazwiska pojawiaja sie w&nbsp;kontekscie wielkich osiagniec naukowych, sportowych, biznesowych i&nbsp;artystycznych. Ambicja tej instytucji jest przyblizanie ich osiagniec Polakom w&nbsp;kraju, a&nbsp;jednoczesnie zachecanie do&nbsp;wzajemnego poznania sie rodakow zyjacych w&nbsp;Polsce i&nbsp;za&nbsp;granica. Dzieki projektom o&nbsp;charakterze edukacyjnym i&nbsp;kulturalnym Muzeum ma nadzieje stac sie miejscem spotkan i&nbsp;dyskusji. W&nbsp;sposob szczegolny zobowiazuje do&nbsp;tego najlepszy z&nbsp;mozliwych adresow - <a target="_blank" href="http://www.polska1.pl" "http:="" polska1.pl="" "="">ul. Polska 1</a>.</p><p><a target="_blank" href="https://www.facebook.com/muzeumemigracjigdynia/" "http:="" www.facebook.com"""="" "_blank"="">FACEBOOK</a></p><p><a target="_blank" href="https://twitter.com/muzeumemigracji" "http:="" www.twitter.com"""="" "_blank"="">TWITTER</a></p><p><a target="_blank" href="https://plus.google.com/102147642216607430050" "http:="" www.plus.google.com"""="" "_blank"="">GOOGLE+</a><br></p>
