<h2>Archiwum Emigranta</h2><p>Archiwum Emigranta to&nbsp;projekt Muzeum Emigracji w&nbsp;Gdyni, w&nbsp;ramach ktorego zbieramy i&nbsp;prezentujemy emigracyjne historie Polek i&nbsp;Polakow. Sa wsrod nich opowiesci o&nbsp;niezwyklej odwadze, ale&nbsp;tez o&nbsp;codziennych zmaganiach z&nbsp;nieznana rzeczywistoscia. Razem tworza szeroka i&nbsp;barwna panorame polskiej obecnosci na&nbsp;wszystkich kontynentach swiata.<br><br>Archiwum chce zapewnic przestrzen dla&nbsp;tych wszystkich opowiesci, ktore emigranci nosza w&nbsp;sobie. Pragniemy je nie tylko archiwizowac i&nbsp;zachowywac od&nbsp;zapomnienia, ale&nbsp;tez pokazywac szerokiej publicznosci i&nbsp;dzielic sie nimi. <br> </p><p>W Muzeum Emigracji historia Polski rozumiana jest jako historia wszystkich Polakow - zarowno tych zyjacych w&nbsp;kraju, jak i&nbsp;poza jego granicami. Chcemy, aby&nbsp;ten wieloglos emigrantow, reemigrantow i&nbsp;imigrantow inspirowal do&nbsp;dyskusji i&nbsp;debaty spolecznej na&nbsp;temat dawnej i&nbsp;wspolczesnej emigracji.<span></span><br></p>
</div>
<div class="text-column">
<h2>Nasz cel<br></h2><p>Zadaniem osob pracujacych w&nbsp;Archiwum Emigranta jest gromadzenie opowiesci - zarowno w&nbsp;formie pisanej, jak i&nbsp;mowionej. Staramy sie dotrzec do&nbsp;osob, ktore doswiadczaly emigracji w&nbsp;roznych momentach polskiej historii.</p><p>Emigracyjne narracje sa systematycznie archiwizowane w&nbsp;postaci relacji pisanych oraz notacji dzwiekowych i&nbsp;filmowych. W&nbsp;perspektywie lat utworza one wielowymiarowy obraz dawnej i&nbsp;wspolczesnej emigracji.<span></span><br></p>
</div>
<div class="text-column">
<h2>Kontakt</h2><p>Zachecamy emigrantow z&nbsp;calego swiata do&nbsp;spisywania swoich wspomnien, przesylania zdjec, skanow pamiatek. Historia mowiona to&nbsp;unikalna szansa na&nbsp;dotarcie do&nbsp;osobistych doswiadczen swiadkow historii. Sa w&nbsp;nich emocje, ktorych nie znajdziemy w&nbsp;nawet najwazniejszych dokumentach. Dzieki nim mozemy opowiedziec o&nbsp;jednym z&nbsp;najwazniejszych zjawisk w&nbsp;polskiej historii glosami samych jej uczestnikow.<span></span></p><p>Na stronie znajda Panstwo <a href="http://archiwumemigranta.pl/pl/dodaj_historie">formularz</a> sluzacy przeslaniu swoich wspomnien oraz informacje kontaktowe Archiwum Emigranta. Na&nbsp;kazde pytanie chetnie odpowiemy mailowo lub&nbsp;telefonicznie.</p>
