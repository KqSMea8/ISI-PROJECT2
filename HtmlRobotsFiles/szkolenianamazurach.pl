</strong></h4>
<h4><strong>Trening rozwoju osobistego </strong><strong>-</strong><strong> samodzielnie czy z trenerem? </strong></h4>
<h4>Ucz sie na doswiadczeniach i wykorzystujac wiedze innych.</h4>
<h4>Czy jest cos, co od dawna chcialas/chciales osiagnac, ale jeszcze nie udalo Ci sie tego zaczac? Jezeli tak, to indywidualne treningi rozwoju osobistego moga Ci pomoc w przejsciu od podjecia zamiaru dokonania zmiany do wprowadzenia jej w zycie.</h4>
<h4>Indywidualne treningi rozwoju osobistego zasypuja przepasc pomiedzy miejscem, w ktorym znajdujesz sie w tej chwili, a miejscem, do ktorego chcesz sie dostac.</h4>
<h4>Spotkania z trenerem pomoga skupic Twoja uwage na wynikach, ktore pragniesz osiagnac i powstrzymaja Cie przed zbaczaniem z kursu i trwonieniem energii na to czego nie chcesz.</h4>
<hr>
<h5><strong>Co czeka na Ciebie podczas indywidualnych treningow rozwoju osobistego:</strong></h5>
<h5>&#8211; Idea koncentrowania sie na mocnych stronach. Realizowanie jej bywa nielatwe w swiecie pelnym ocen i krytyki. Ale to wydobywanie mocnych stron jest kluczem do wyjscia,&nbsp;dlatego warto sie go uczyc. Bez wzgledu na to czego dotyczy Twoj&nbsp;problem- mozesz sobie z nim poradzic, a te spotkania udowodnia Ci, ze&nbsp;mozesz tego dokonac i stac sie silniejsza osoba.</h5>
<h5>&#8211; Na bazie wybranej trudnej sytuacji z Twojego zycia dokonasz&nbsp;analizy wlasnych wartosci, okreslisz na ich podstawie cel, a nastepnie&nbsp;zaplanujesz swoja droge.</h5>
<h5>&#8211; Ustalanie wlasnego repertuaru kotwic - kotwiczenie stanow i zasobow;</h5>
<h5>&#8211; Elementy coachingu, poznasz miedzy innymi: zmiana perspektywy, kolo priorytetow, horyzont dostepnych mozliwosci;</h5>
<h5>&#8211; Zasady komunikowania sie w roznych sytuacjach;</h5>
<h5>&#8211; Na spotkaniu doswiadczysz intensywnego procesu przejscia od problemu do&nbsp;rozwiazania.</h5>
<hr>
<h5><strong>Na kazdym spotkaniu dostaniesz materialy, ktore beda Twoja wlasnoscia i pomoga Ci w dalszej pracy nad swoim rozwojem.<a href="http://szkolenianamazurach.pl/?attachment_id=1617" rel="attachment wp-att-1617"><img class="aligncenter size-medium wp-image-1617" src="http://szkolenianamazurach.pl/wp-content/uploads/2017/09/roslina-300x181.png" alt="" width="300" height="181" srcset="http://szkolenianamazurach.pl/wp-content/uploads/2017/09/roslina-300x181.png 300w, http://szkolenianamazurach.pl/wp-content/uploads/2017/09/roslina-768x462.png 768w, http://szkolenianamazurach.pl/wp-content/uploads/2017/09/roslina-1024x616.png 1024w, http://szkolenianamazurach.pl/wp-content/uploads/2017/09/roslina.png 1186w" sizes="(max-width: 300px) 100vw, 300px" /></a></strong></h5>
<h5>Podczas spotkania pracujemy metodami TSR, NLP, Coachingu.&nbsp;Bys mogla/mogl osiagac efekty podzielimy cele na mniejsze, bardziej realistyczne kroki.</h5>
<h5>Bedzie to Twoja ,,SKRZYNKA Z NARZEDZIAMI"</h5>
