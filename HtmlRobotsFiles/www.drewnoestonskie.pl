<h2 class="side-heading">O firmie PUIDUKODA</h2>

</div>

<div class="col-md-6">
<p><b>Puidukoda - to polaczenie rzeczownika i przymiotnika z jezyka estonskiego: <i>koda</i> - izba, dom; <i>puidu</i> - drewniana.</b></p>

<p>Produkty OU Puidukoda zaczely pojawiac sie na polskim rynku w 2004 roku i do 2011 mozna bylo je nabyc wylacznie w systemie zakupu hurtowego w zakladzie produkcyjnym w Estonii.</p>

<p>W 2011 powolana zostala Puidukoda Polska sp z o.o, w ktorej polaczono potencjaly: estonski - produkcyjny i polski - handlowy. Poprzez Program Sklady Polska: w Piszu na Mazurach i w Koninie - Wielkopolska poprawila sie dostepnosc produktow w mniejszych ilosciach, osiagnieto tez wieksza elastycznosc logistyczna.</p>
<a class="btn btn-black btn-sm" href="/strona/o-firmie">Dowiedz sie wiecej <i class="fa fa-angle-right"></i></a>
</div>
<div class="col-md-6">
<h4>Wspolpraca</h4>

<p>Nadal tak jak i 14 lat temu podstawowym kanalem dystrybucji jest Sale Direct - sprzedaz bezposrednio z Estonii pakietu produktow i dostawa pelnych ladunkow dla polskich firm,ktore realizuja swoje cele biznesowe.</p>

<p>Poprzez kanal dystrybucji Sklady Polska mozemy dopasowac logistyke i dostawy dla innej grupy Naszych Partnerow: firm remontowych, budowlanych i dla inwestorow indywidualnych. Prog minimum logistycznego zostal dostosowany do potrzeb. Grupe Klientow Detalicznych obslugujemy lokalnie z naszych skladow w Polsce.</p>

