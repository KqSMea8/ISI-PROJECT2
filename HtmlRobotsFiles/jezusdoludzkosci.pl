<h2>O stronie</h2>

<p class="lead">Niniejsza strona jest poswiecona Oredziom, ktore dotycza zblizajacego sie globalnego wydarzenia nazwanego Ostrzezeniem, zawieraja przekaz z Nieba, wskazania duchowe i proroctwa odnoszace sie do Kosciola i swiata, majace sie wypelnic teraz, w czasie prowadzacym do Powtornego Przyjscia Jezusa Chrystusa. Nastapi ono za zycia tego pokolenia, jak sam Jezus powiedzial to w Oredziach.</p>

<h3>Oredzia i objawienia</h3>

<p>Oredzia otrzymuje Maria od Bozego Milosierdzia, katolicka wizjonerka z Europy. Pierwsze Oredzie zostalo jej przekazane 9 listopada 2010 roku. Do maja 2015 roku otrzymala ona od Trojcy Swietej i Matki Bozej ponad 1330 Oredzi. Choc obecnie nastapila przerwa w przekazywaniu Oredzi, Jezus powiedzial, ze Oredzia beda kontynuowane w przyszlosci - beda przekazywane az do czasu Jego Powtornego Przyjscia.</p>

<p>Calosc Oredzi stanowi Ksiege Prawdy, ktora w Starym Testamencie poznal juz prorok Daniel, a w Nowym Testamencie mial do niej wglad sw. Jan Ewangelista. Tresc tej Ksiegi oddal za pomoca symboli w Ksiedze Apokalipsy.</p>

<h3>Pieczecie i proroctwa</h3>

<p>W objawieniach Jezus otwiera Pieczecie z Ksiegi Apokalipsy - ujawnia ich sens.</p>

<p>Proroctwa zawarte w Oredziach przepowiedzialy abdykacje Benedykta XVI oraz nadejscie falszywego proroka, zasiadajacego obecnie na tronie Piotrowym w Watykanie.</p>

<p>Oredzia ujawniaja wydarzenia takie jak podzial w Kosciele katolickim i szerokie odstepstwo od wiary katolickiej (Pierwsza Pieczec), globalne zmiany klimatu, obowiazkowe swiatowe szczepienia, ktore przyniosa smierc, przewroty polityczne, III wojna swiatowa (Druga Pieczec), pojawienie sie antychrysta, jeden swiatowy porzadek, nowy swiatowy kosciol, glod na swiecie (Trzecia Pieczec), wprowadzenie chipa - znamienia bestii, i wiele innych.</p>

<p>Pozostale Pieczecie z Ksiegi Apokalipsy, to jest Czwarta i kolejne, nie zostaly jeszcze zlamane i wyjasnione.</p>

<p>Wszystkie te wydarzenia wskazuja, ze zyjemy teraz w koncu czasow i wkrotce nastapi Dzien Panski. Taki tez jest zasadniczy cel Oredzi - ostrzezenie odnosnie do tego, co ma nastapic wczesniej, i przygotowanie ludzkosci na najwazniejsze wydarzenie w historii chrzescijanstwa od czasu Smierci i Zmartwychwstania Chrystusa, to jest wlasnie Jego bliskie Powtorne Przyjscie, ktore nastapi po trzech dniach ciemnosci.</p>

<p>Celem tych Oredzi jest zbawienie jak najwiekszej ilosci dusz.</p>

<h3>Ostrzezenie</h3>

<p>Najbardziej pilne Oredzie dotyczy mistycznego wydarzenia, ktore wkrotce nastapi na calym swiecie. Wydarzenie opisane przez Jezusa Chrystusa jako Wielkie Ostrzezenie i Oswietlenie Sumien dane bedzie swiatu jako najwiekszy akt Bozego Milosierdzia. Ostrzezenie, cudowny Dar osobistego spotkania kazdej duszy z Bogiem, pomoze przygotowac ludzi na powrot Jezusa Chrystusa. To wydarzenie bedzie doswiadczone w jednej chwili przez kazdego czlowieka na swiecie w wieku powyzej siedmiu lat. Bedzie trwalo okolo 15 minut. Bedzie to tak silne przezycie, ze nikt nie bedzie mogl go zignorowac. Wszyscy, nawet ateisci, przekonaja sie, ze Bog istnieje. Kazdemu zostana ukazane jego dobre czyny i szczegolnie grzechy - takie, jakimi one sa w Oczach Boga. Ten Dar pobudzi ludzi do ponownej oceny swojego zycia i poproszenia Boga o przebaczenie. Dzieki Ostrzezeniu nastapi wiele cudownych nawrocen.</p>

<h3>Pieczec Boga Zywego</h3>

<p>Zostala przepowiedziana w Ksiedze Apokalipsy i jest teraz dana w tych objawieniach jako Modlitwa Krucjaty 33. Jezus poleca nam, abysmy zawsze nosili Pieczec Boga Zywego przy sobie w jakiejkolwiek formie (tresc tej Modlitwy) i mieli w swoich domach. Pieczec nalezy odmawiac codziennie. Ona nas chroni, nasze ciala i dusze, i zadna krzywda nie stanie sie tym, ktorzy maja Pieczec. Nie wolno jej sprzedawac i powinna byc dostepna dla kazdego. W Oredziach znajdujemy takze wiele innych nawiazan do Pieczeci. Tlumaczy sie w nich jej role i znaczenie. Jest ona Darem od Boga, by nas chronic i pomoc nam dotrwac do Ery Pokoju.</p>

<h3>Krucjata Modlitwy</h3>

<p>W Oredziach Jezus przekazuje takze Modlitwy i Litanie tworzace Krucjate Modlitwy. Prosi nas, bysmy codziennie modlili sie nimi. Z tymi modlitwami zwiazanych jest wiele Lask, wiele nawrocen, maja one potezna moc. Jezus poleca nam takze zakladanie grup Krucjaty Modlitwy, bysmy modlili sie wspolnie z innymi o zbawienie dusz i o ochrone w tych trudnych czasach. Grupy Krucjaty Modlitwy pomoga stworzyc Armie Jezusa Chrystusa Reszty Kosciola katolickiego, wierna Pismu Swietemu i Tradycji katolickiej.</p>

<p>Wszyscy, ktorzy sie nawroca i przyjma Boga, zostana przez Jezusa zabrani w Dniu Ostatecznym do Nowego Raju, Nowego Nieba i Ziemi - Ery pokoju i szczescia.</p>
