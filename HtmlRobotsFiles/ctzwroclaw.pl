<div class="entry-content">


<p>Wroclaw  to miasto z  duza liczba psychoterapeutow. Znalezienie dobrego moze byc zniechecajace. Pacjenci czesto nie maja pojecia, na co zwracac uwage szukajac terapeuty. Ten artykul zawiera wskazowki, ktore pomoga pacjentom poruszac sie w zlozonym krajobrazie psychoterapii we Wroclawiu.</p>



<p>We Wroclawiu jest kilka uczelni, ktore oferuja stopnie naukowe z psychiatrii, psychologii i pracy socjalnej. Te trzy stopnie sa najlepiej rozpoznawane i najbardziej znane w spolecznosci psychoterapeutow we Wroclawiu.</p>



<p>Pacjenci, ktorzy szukaja terapeuty, powinni zapytac, gdzie terapeuta otrzymal dyplom. Niestety, jest coraz wiecej internetowych programow studiow, ktore nie wymagaja nadzorowanych stazy czy  nadzoru klinicznego. Podczas gdy absolwenci tych programow maja papierowe poswiadczenia, nie maja niezbednego doswiadczenia klinicznego ani nadzoru, aby byc psychoterapeutami.</p>



<p>Wazne jest rowniez pytanie, jak dlugo ktos praktykuje. <a href="https://notatkiterapeutyczne.pl/co-to-jest-psychoterapia-44f9984c3b2d">Psychoterapia</a> to sztuka i nauka. Im dluzej ktos zajmuje sie psychoterapia, tym wieksze jest prawdopodobienstwo, ze bedzie dobry w swoim zawodzie.</p>



<p>Pacjenci sa czesto zachecani do poszukiwania terapeuty, z ktorym &#8222;dobrze sie beda rozumiec i dogadywac&#8221;. Jednak nie oznacza to znalezienia kogos, kto jest sympatyczna osoba. Wiekszosc terapeutow to mili ludzie, ale nie wszyscy terapeuci sa wykwalifikowani w tym, co robia. Pacjenci powinni szukac kogos, kogo lubia i komu ufaja pod wzgledem poziomu doswiadczenia i umiejetnosci. Umiejetnosc to cos, co mozna latwo okreslic. Nie trzeba duzo czas, zeby zobaczyc czy terapeuta rozumie Cie i Twoje problemy, z ktorymi sie borykasz i czy ma jakis pomysl, jak pomoc ci rozwiazac te problemy.</p>



<p>Terapeuci maja rozne podejscia do pracy z ludzmi. Niektore podejscia do leczenia koncentruja sie glownie na zmniejszeniu objawow, np. wyeliminowanie konfliktu w zwiazku lub zmniejszenie leku. Ten rodzaj terapii wydaje sie krotkotrwaly. W tym przypadku relacja miedzy klientem a terapeuta ma drugorzedne znaczenie dla wyeliminowania symptomow, z ktorymi przyszedl klient.</p>



<p>Inni terapeuci koncentruja sie na rozwoju osobistym i zmianie. Ten rodzaj terapii wydaje sie byc glebszy i trwalszy. Terapeuci, ktorzy koncentruja sie na tego typu rozwiazaniach, maja tendencje do rozwijania relacji z klientami, ktorzy przychodza do nich  przez wiele lat. <a href="http://www.aumerpsychoterapia.wroclaw.pl">Psychoterapeuta Wioletta Aumer</a> z Wroclawia stosuje tego typu podejscie. Znajdziesz jej gabinet pod tym adresem:</p>
<p>Psychoterapeuta Dr Wioletta Aumer</p>
<p>aleja Hallera 3/4</p>
<p>53-319 Wroclaw</p>
<p>tel. 609 822 532</p>
