<p>&nbsp;</p>





<p>W ciagu ostatnich dwudziestu lat psychoterapia we Wroclawiu przeszla radykalna zmiane. Ta zmiana zbiega sie z proliferacja zarzadzanych systemow opieki i ubezpieczen. Obecnie znacznie wiekszy nacisk kladzie sie na krotkoterminowa psychoterapie i redukcje objawow. Relacja klient-terapeuta ma drugorzedna role, a ci terapeuci, ktorzy zapewniaja najglebsza prace i najwyzsza jakosc leczenia,  sa najtrudniejsi do zbalezienia.</p>



<p>Wazne jest, aby jasno okreslic swoje oczekiwania co do psychoterapii i poinformowac o tym swojego terapeute. Zwykle prowadzi to do zwiekszenia satysfakcji z terapii.</p>

