<h1 class="entry-title">Strona glowna</h1>
</header><!-- .entry-header -->

<div class="entry-content cf">
<p style="text-align: justify;"><div class="ezcol ezcol-one-half">
<p style="text-align: center;">Zapraszamy do udzialu w VI Ogolnopolskiej Konferencji Naukowej: <strong>,,Psychologia w sluzbie rodziny</strong> &#8222;<span style="color: #29610f;"><em>Relacje w podsystemach rodzinnych. Szanse i zagrozenia"</em></span>, ktora odbedzie sie w dniach od <strong>18-19 maja 2018 roku</strong> w Gdansku.</p>
<p style="text-align: justify;"><a href="http://www.psychologiarodziny.ug.edu.pl/wp-content/uploads/2015/11/ug1-e1448037645652.png" data-rel="lightbox-0" title=""><img class=" size-full wp-image-162 aligncenter" src="http://www.psychologiarodziny.ug.edu.pl/wp-content/uploads/2015/11/ug1-e1448037645652.png" alt="ug1-e1448037645652" width="270" height="179" /></a></p>
<p style="text-align: justify;">W naukach spolecznych rodzina jest traktowana, z jednej strony jako grupa spoleczna, z drugiej zas, jako wspolnota osob tworzacych system skladajacy sie z szeregu podsystemow (malzenskiego, rodzicielskiego, rodzenstwa) i powiazanych osobliwa wiezia uczuciowa. Liczne badania dowodza, ze przez wiekszosc ludzi,  rodzina, obok milosci, w hierarchii wartosci zajmuje jedna z najwyzszych pozycji.  Zaznaczyc jednak nalezy, ze ranga rodziny, jako podstawowej struktury spolecznej,  bedzie zalezala od tego, czy gwarantuje ona, a jesli tak to w jakim zakresie, mozliwosc zaspokajania podstawowych potrzeb czlowieka, w tym przede wszystkim: potrzeb biologicznych, psychologicznych (bezpieczenstwa, uznania, poszanowania godnosci osobistej), oraz potrzeb spoleczno-ekonomicznych (przynaleznosci do grupy czy tez potrzeb socjalno-bytowych).</p>
<p style="text-align: justify;">O randze rodziny rozpatrywanej w kategoriach wartosci w znacznym stopniu decyduja takze relacje rodzinne, z ktorymi wspolwystepuja okreslone uczucia i emocje. Relacje rodzinne, ktorym towarzysza uczucia i emocje pozytywne, takie jak: spokoj, milosc, radosc, szczescie sprzyjaja rozwojowi oraz wzmacnianiu wiezi czlonkow nalezacych do danego podsystemu rodzinnego, natomiast relacje, u podstaw ktorych leza uczucia o charakterze negatywnym, takie jak zlosc, zazdrosc, zawisc, zdrada, niepokoj i lek niszcza wiezi rodzinne, hamuja rozwoj czlowieka, niszcza jego osobowosc  i sprzyjaja ksztaltowaniu sie aspolecznych form zachowania. W kontekscie powyzszych stwierdzen mozna powiedziec, ze rola rodziny w budowaniu zdrowych, pozytywnych relacji spolecznych, opartych na milosci i wzajemnym poszanowaniu godnosci osobistej wszystkich czlonkow tego systemu jest niekwestionowana, niezaleznie od ich wieku, plci, predyspozycji osobistych czy stanu zdrowia. Jest to najwazniejsze zadanie rozwojowe dla rodzicow odpowiedzialnych za proces wychowania, a takze dla wszystkich czlonkow systemu rodzinnego gdyz przygotowuja mlodego czlowieka do funkcjonowania w innych pozarodzinnych grupach spolecznych.</div>
<p style="text-align: justify;"><div class="ezcol ezcol-one-half ezcol-last">
<p style="text-align: justify;">Celem Konferencji jest integracja wiedzy z roznych dyscyplin naukowych, ktore sluzyc moga pomoca w zrozumieniu, jakie czynniki sa odpowiedzialne za prawidlowe funkcjonowanie rodziny jako systemu i tym samym daja szanse na zaspokojenie potrzeb jej czlonkow nalezacych do roznych podsystemow rodzinnych. Konferencja stanowic bedzie rowniez przestrzen do dyskusji nad zagrozeniami dla jakosci relacji rodzinnych plynacymi z zewnatrz systemu, jak i wynikajacymi z jego przemian wewnetrznych. Mamy nadzieje, ze zapoznanie sie z najnowszymi wynikami autorskich badan stanie sie inspiracja i zacheta do wymiany pogladow, ale takze impulsem do poszukiwania rozwiazan  praktycznych. Zagadnienia dotyczace <em>relacji w podsystemach rodzinnych</em> poruszane beda w trakcie wykladow plenarnych, sympozjow i sesji plakatowych. Warsztaty sluzyc beda natomiast zapoznaniu sie z oferta pomocowa instytucji, ktore wspieraja rodziny zmagajace sie ze zmianami plynacymi z zewnatrz i kryzysami dotykajacymi calych systemow rodzinnych i poszczegolnych jej czlonkow.</p>
<p><strong>Proponowane obszary tematyczne:<div class="su-list su-list-style-"></strong></p>
<ul>
<li><i class="fa fa-angle-right" style="color:#308016"></i> Narzeczenstwo i malzenstwo podstawa relacji rodzinnych</li>
<li><i class="fa fa-angle-right" style="color:#308016"></i> Rodzicielstwo jako wyznacznik relacji rodzinnych</li>
<li><i class="fa fa-angle-right" style="color:#308016"></i> Aktywnosc zawodowa a relacje rodzinne</li>
<li><i class="fa fa-angle-right" style="color:#308016"></i> Dziecko w szkole i w domu a relacje rodzinne</li>
<li><i class="fa fa-angle-right" style="color:#308016"></i> Psychologiczne wyznaczniki relacji w podsystemie rodzenstwa</li>
<li><i class="fa fa-angle-right" style="color:#308016"></i> Miedzypokoleniowe konteksty relacji rodzinnych</li>
<li><i class="fa fa-angle-right" style="color:#308016"></i> Choroba w rodzinie wyznacznikiem relacji rodzinnych</li>
<li><i class="fa fa-angle-right" style="color:#308016"></i> Seniorzy w rodzinie a specyfika relacji rodzinnych</li>
<li><i class="fa fa-angle-right" style="color:#308016"></i> Psychofizjologiczne aspekty bliskich wiezi w rodzinie<strong></div></strong></li>
</ul>
<p><strong>Formy uczestnictwa:<div class="su-list su-list-style-"></strong></p>
<ul>
<li><i class="fa fa-angle-right" style="color:#308016"></i> Sympozjum</li>
<li><i class="fa fa-angle-right" style="color:#308016"></i> Referat</li>
<li><i class="fa fa-angle-right" style="color:#308016"></i> Plakat (wymiary: do 70 x 100 cm pionowo)</li>
<li><i class="fa fa-angle-right" style="color:#308016"></i> Warsztat</li>
</ul>
