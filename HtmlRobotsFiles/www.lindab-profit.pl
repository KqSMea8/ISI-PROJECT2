<a name="pytanie-14"></a>

<div class="zakladka_3Klik">
<div>
<b class="niebieski f16 lapka">Co to znaczy, ze faktura zostala oplacona w terminie ?</b>
</div>
</div>

<div class="zakladka_3" style="display:none">
Faktura oplacona w terminie, to faktura w przypadku kt&oacute;rej&nbsp;cala kwota zostala&nbsp;uiszczona przez Uczestnika Programu i zaksiegowana przez Lindab najp&oacute;zniej 3 dni po uplywie terminu waznosci podanego na tej fakturze.&nbsp;<br />
<br />
Transakcje w elektronicznym systemie rozliczen ELIKSIR moga&nbsp;potrwac nawet kilka dni kalendarzowych (szczeg&oacute;lnie w przypadku weekend&oacute;w i okres&oacute;w swiatecznych). Istnieje bardzo mala szansa, ze naleznosc przelana w ostatnim dniu okresu platnosci zostanie w tym samym dniu zaksiegowana w naszym systemie. Aby nie stracic punkt&oacute;w za dana fakture zachecamy wiec do oplacenia jej 2 dni robocze przed terminem platnosci podanym na tej fakturze.<br />
<br />
<em>Przyklad 1:<br />
11 stycznia 2016 r. uczestnik Programu dokonal ostatniego przelewu bankowego, w pelni regulujacego fakture o numerze FV-01, kt&oacute;ra ma termin platnosci do 31 stycznia 2016 r. Lindab zaksiegowal przelew 12 stycznia 2016 r.&nbsp;<span style="color:rgb(87, 119, 33)">Wszystkie punkty naliczone za zakupy z faktury FV-01 beda wiec wazne od 31 stycznia 2016 r. do 31 lipca 2016 r.</span></em><br />
<br />
<em>Przyklad 2:<br />
28 stycznia 2016 r. uczestnik Programu dokonal ostatniego przelewu bankowego, w pelni regulujacego fakture o numerze FV-02, kt&oacute;ra ma termin platnosci do 28 stycznia 2016 r.. Lindab zaksiegowal przelew 1 lutego 2016 r.&nbsp;<span style="color:rgb(204, 51, 0)">Punkty za fakture FV-02 nie zostana naliczone, poniewaz faktura zostala zaksiegowana 4 dni po terminie platnosci.</span></em><br />
