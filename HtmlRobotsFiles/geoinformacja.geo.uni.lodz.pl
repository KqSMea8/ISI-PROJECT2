<h2 class="PostHeaderIcon-wrapper"> <span
class="PostHeader">GEOINFORMACJA </span> </h2>
<div class="PostContent"> <br />
<p><span style="font-size: medium;">Kierunek na poziomie licencjackim i magisterskim oraz studia podyplomowe prowadzone sa</span><span style="font-size: medium;"> na  Wydziale Nauk Geograficznych przy wspoludziale Wydzialu Matematyki i Informatyki Uniwersytetu Lodzkiego</span></p>
<p><span style="font-size: medium; color: #49585b; font-weight: bold;">mamy ciekawa oferte przedmiotow i bogate oprogramowanie w trakcie studiow - zakladka oprogramowanie - </span><strong style="font-size: medium; color: #49585b;">wiecej na stronie<a title="mgr" href="http://geoinformacja.uni.lodz.pl/"> http://geoinformacja.uni.lodz.pl/</a></strong></p>
<h2>Serdecznie zapraszamy do <a title="rekrut" href="https://rekrutacja.uni.lodz.pl/">rekrutacji</a> na Uniwersytecie Lodzkim</h2>
<p><strong style="color: #49585b; font-size: 22px;">Gdzie studiujemy?</strong></p>
<p>Zajecia odbywaja sie w sasiadujacych ze soba gmachach Wydzialu Nauk Geograficznych, polozonych w centrum Lodzi: przy ul. G.Narutowicza 88 i St. Kopcinskiego 31 (Collegium Geographicum) oraz w gmachu Wydzialu Matematyki i Informatyki przy ulicy St. Banacha 22</p>
<p><img style="display: block; margin-left: auto; margin-right: auto;" src="uploads/images/collegimm.png" alt="" width="320" height="427" /></p>
<p><br /><br /></p>
<p><a title="WMII UL" href="http://www.math.uni.lodz.pl/">Wydzial Matematyki i Informatyki </a></p>
<p> </p>
<samp><img src="uploads/images/geoinformacja_plakat.jpg" alt="" width="707" height="1000" /></samp><samp><br /></samp><samp><br /></samp><samp>
<p>W poprzednim roku akdemickum roku akademickim 2013/2014 kierunek  geoinformacja byl czescia projektu ktory zakonczy sie po 3 latach.</p>
<p><a title="projekt" href="http://infogeolog.uni.lodz.pl/"><em>InfoGeoLog: Informatyka - Geoinformacja - Logistyka. Ksztalcenie w zawodach przyszlosci w odpowiedzi na oczekiwania pracodawcow</em> realizowanego na Uniwersytecie Lodzkim</a>.</p>
<p>Projekt realizowany jest w ramach konkursu 1/POKL/4.3/2012 na dofinansowanie projektow w ramach Dzialania 4.3 Priorytet IV PO KL Wzmocnienie potencjalu dydaktycznego uczelni w obszarach kluczowych w kontekscie celow Strategii Europa 2020.</p>
<p>Najlepsi studenci geoinformacji (32 osoby)  beda mogli skorzystac ze szkolen i kursow specjalistycznych (szkolenia w zakresie  przedsiebiorczosci, umiejetnosci interpersonalnych oraz drugiego jezyka obcego) oraz atrakcyjnych stazy krajowych i zagranicznych. Czesc zajec prowadzic beda zaproszeni wykladowcy (w tym z zagranicy). Szkolenia realizowane beda przez Akademickie Biuro Karier Zawodowych UL.  Studenci beda mieli  mozliwosc wyboru modulow specjalistycznych z kazdego z 3 kierunkow oraz wspolnie realizowane projekty zadaniowe. W przygotowanie uruchamianych w projekcie kierunkow ksztalcenia wlaczeni sa pracodawcy.</p>
<p>wiecej na stronie internetowej projektu <a title="InfoGEOLOG" href="http://infogeolog.uni.lodz.pl/?page_id=6">InfoGeoLog</a><a title="InfoGeoLog" href="http://infogeolog.uni.lodz.pl/?page_id=6"><img src="uploads/images/infogeolog-web-banner270.png" alt="" width="673" height="176" /></a></p>
