<h1 class="entry-title">O nas</h1>

<!-- ========== Post Featured Image ========== -->

<!-- ========== Post content in single post page ========== -->
<div class="entry-content content-block">
<div>
<div align="justify">
<p><span style="color: #ffffff;"><a style="color: #ffffff;" href="http://www.klub-malawi.pl/wp-content/uploads/2018/08/Logo-klubowe.png"><img class="wp-image-6 alignleft" src="http://www.klub-malawi.pl/wp-content/uploads/2018/08/Logo-klubowe-300x279.png" alt="" width="110" height="102" srcset="http://www.klub-malawi.pl/wp-content/uploads/2018/08/Logo-klubowe-300x279.png 300w, http://www.klub-malawi.pl/wp-content/uploads/2018/08/Logo-klubowe.png 317w" sizes="(max-width: 110px) 100vw, 110px" /></a></span></p>
<div><span style="font-size: 12pt; color: #ffffff;">Klub Malawi jest stowarzyszeniem non profit, skupiajacym hodowcow i milosnikow endemicznych pielegnic jeziora Malawi. Naszym celem jest propagowanie malawistyki, przez nasze forum i galerie. <img class=" wp-image-247 alignright" src="http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.885293936_l.t.front_.JPG.fcf3716784204c7ff1a0d9eb7be6e71b-300x253.jpg" alt="" width="128" height="108" srcset="http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.885293936_l.t.front_.JPG.fcf3716784204c7ff1a0d9eb7be6e71b-300x253.jpg 300w, http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.885293936_l.t.front_.JPG.fcf3716784204c7ff1a0d9eb7be6e71b-768x647.jpg 768w, http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.885293936_l.t.front_.JPG.fcf3716784204c7ff1a0d9eb7be6e71b-1024x863.jpg 1024w, http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.885293936_l.t.front_.JPG.fcf3716784204c7ff1a0d9eb7be6e71b.jpg 1424w" sizes="(max-width: 128px) 100vw, 128px" />Idea naszego Klubu jest dzielenie sie doswiadczeniami i wiedza, oraz informowanie o wszystkim co bezposrednio i posrednio dotyczy akwarystyki. Na naszych stronach chcemy pokazywac piekno ryb, zyjacych w Jeziorze Malawi. <img class=" wp-image-282 alignleft" src="http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.287271649_c.z.front_.JPG.602efc9c7ba9397299271eb929d09bb1-300x268.jpg" alt="" width="151" height="135" srcset="http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.287271649_c.z.front_.JPG.602efc9c7ba9397299271eb929d09bb1-300x268.jpg 300w, http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.287271649_c.z.front_.JPG.602efc9c7ba9397299271eb929d09bb1-768x686.jpg 768w, http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.287271649_c.z.front_.JPG.602efc9c7ba9397299271eb929d09bb1-1024x914.jpg 1024w, http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.287271649_c.z.front_.JPG.602efc9c7ba9397299271eb929d09bb1.jpg 1344w" sizes="(max-width: 151px) 100vw, 151px" />Dazymy do zapewnienia jak najlepszych warunkow naszym podopiecznym, kategorycznie sprzeciwiamy sie krzyzowkom miedzygatunkowym (hybrydyzacji), poniewaz my, jako akwarysci jestesmy odpowiedzialni za zachowanie bioroznorodnosci zaproponowanej przez nature.<br />
Klubowa strona internetowa zostala w calosci stworzona przez czlonkow i sympatykow Klubu Malawi.</span></p>
<div style="display: inline; margin: 5px 10px; float: right;"><span style="font-size: 12pt; color: #ffffff;"><a style="color: #ffffff;" href="http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.ekipa_.JPG.08d97f2ec91d6ee7a88160757c390e7a-1.jpg"><img class="alignnone wp-image-263" src="http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.ekipa_.JPG.08d97f2ec91d6ee7a88160757c390e7a-1-300x208.jpg" alt="" width="185" height="128" srcset="http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.ekipa_.JPG.08d97f2ec91d6ee7a88160757c390e7a-1-300x208.jpg 300w, http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.ekipa_.JPG.08d97f2ec91d6ee7a88160757c390e7a-1-768x533.jpg 768w, http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.ekipa_.JPG.08d97f2ec91d6ee7a88160757c390e7a-1-1024x711.jpg 1024w, http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.ekipa_.JPG.08d97f2ec91d6ee7a88160757c390e7a-1.jpg 1600w" sizes="(max-width: 185px) 100vw, 185px" /></a></span></div>
<p><span style="font-size: 12pt; color: #ffffff;">Wszyscy klubowicze wspoltworza klub i forum. Naszym motto sa slowa dawnego amerykanskiego prezydenta Johna F.Kennedy'ego, zaadaptowane na nasze potrzeby: ,,Nie pytaj co Klub moze zrobic dla Ciebie, ale co Ty mozesz zrobic dla Klubu".</span><br />
<span style="font-size: 12pt; color: #ffffff;">Historia klubu siega konca 2000 roku. Wtedy to grupa milosnikow ryb z jeziora Malawi, z Krzysztofem Kazmierczakiem na czele zalozyla Klub Malawi. Powstala takze zamknieta lista dyskusyjna dla klubowiczow. W tym samym czasie K.Kazmierczak zalozyl pierwszy polski portal poswiecony malawistyce. Klub Malawi wspolistnial z serwisem www.malawi.pl przez wiele lat, a czlonkowie Klubu zapelniali serwis tekstami i artykulami. Na poczatku 2006 roku, po przeprowadzeniu wyborow do Zarzadu Klubu Malawi, oddzielil sie on od Malawi.pl.</span></p>
<div style="display: inline; margin: 5px 10px 0px 0px; float: left;"><span style="font-size: 12pt; color: #ffffff;"><a style="color: #ffffff;" href="http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.2131521954_n.l.samiec.JPG.50964f51c182bd53fa060a51a0e9a591.jpg"><img class="alignnone wp-image-249" src="http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.2131521954_n.l.samiec.JPG.50964f51c182bd53fa060a51a0e9a591-300x227.jpg" alt="" width="207" height="157" srcset="http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.2131521954_n.l.samiec.JPG.50964f51c182bd53fa060a51a0e9a591-300x227.jpg 300w, http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.2131521954_n.l.samiec.JPG.50964f51c182bd53fa060a51a0e9a591-768x580.jpg 768w, http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.2131521954_n.l.samiec.JPG.50964f51c182bd53fa060a51a0e9a591-1024x774.jpg 1024w, http://www.klub-malawi.pl/wp-content/uploads/2018/09/large.2131521954_n.l.samiec.JPG.50964f51c182bd53fa060a51a0e9a591.jpg 1600w" sizes="(max-width: 207px) 100vw, 207px" /></a></span></div>
<p><span style="font-size: 12pt; color: #ffffff;">Powstal internetowy serwis Klubu Malawi pod adresem www.klub-malawi.pl</span></p>
<p><span style="font-size: 12pt; color: #ffffff;">Czlonkowie Klubu Malawi to akwarysci, hobbysci, pasjonaci. Ludzie, ktorzy oprocz hodowli pyszczakow, odnajduja przyjemnosc w dzieleniu sie swoimi doswiadczeniami. Portal www.klub-malawi.pl to miejsce spotkan, wymiany doswiadczen. <img class=" wp-image-400 alignright" src="http://www.klub-malawi.pl/wp-content/uploads/2018/09/28927830_1022063921275184_2025635735_o-300x225.jpg" alt="" width="207" height="155" srcset="http://www.klub-malawi.pl/wp-content/uploads/2018/09/28927830_1022063921275184_2025635735_o-300x225.jpg 300w, http://www.klub-malawi.pl/wp-content/uploads/2018/09/28927830_1022063921275184_2025635735_o-768x576.jpg 768w, http://www.klub-malawi.pl/wp-content/uploads/2018/09/28927830_1022063921275184_2025635735_o-1024x768.jpg 1024w" sizes="(max-width: 207px) 100vw, 207px" />To miejsce gdzie kazdy akwarysta moze dzielic sie swoja wiedza na forum. Wspieramy sie wzajemnie, pomagamy sobie, wymieniamy sie nie tylko wiedza, ale rybami i sprzetem. Wszyscy czlonkowie Klubu Malawi wspolnie utrzymuja Klub z wlasnych srodkow. Robimy to za posrednictwem skladek czlonkowskich, ale tak naprawde zaden z nas nie szczedzi wysilkow i wlasnego czasu dla budowania tego wspolnego dziela. </span></p>
<p><span style="font-size: 12pt; color: #ffffff;">Jesli chcesz wspoltworzyc Klub Malawi, wniesc swoj wklad, to serdecznie zapraszamy do grona naszych czlonkow.</span></p>
