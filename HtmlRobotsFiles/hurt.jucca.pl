<hgroup>
<h2>Sprzedaz hurtowa</h2>
<h3>Kwiatow sztucznych<br /> i artykulow dekoracyjnych</h3>
</hgroup>
<p>Nasza firma jest bezposrednim importerem artykulow dekoracyjnych z krajow dalekiego wschodu. Specjalizujemy sie przede wszystkim w imporcie kwiatow sztucznych, zarowno poliestrowych jak i piankowych, papierowych, welwetowych, tiulowych oraz wykonanych z innych materialow.</p>
</div>
</div>

<div class="content clearfix wrapper" >
<div class="col col-1-3">
<p>Dodatkowo importujemy susz egzotyczny, wikline a takze wiele innych artykulow dekoracyjnych takich jak wstazki (satynowe, poliestrowe, organzowe), artykuly z filcu dekoracyjnego, wianki naturalne, latarnie, oslonki wykonane z ocynku oraz malowane.</p>

<div class="offer">
<h4>Oferujemy</h4>
<ul>
<li>Kwiaty sztuczne</li>
<li>Susz egzotyczny</li>
<li>Wiklina</li>
<li>Wstazki</li>
<li>Filc dekoracyjny</li>
<li>Wianki naturalne</li>
<li>Latarnie</li>
<li>Oslony</li>
</ul>
</div>

</div>

<div class="col col-2-3">
<p>Dzieki dlugoletniej wspolpracy z fabrykami z Chin, jestesmy w stanie zaproponowac Panstwu naprawde szeroki asortyment w konkurencyjnych cenach. Zamowiony towar dostarczamy wlasnym transportem lub poprzez firmy spedycyjne. Odbiorcom hurtowym oferujemy atrakcyjne rabaty i fachowe doradztwo produktowe. Serdecznie zapraszamy Panstwa do odwiedzenia naszej wzorcowni mieszczacej sie w Tarnowie Podgornym. Zainteresowanych prosimy o kontakt z naszymi pracownikami.</p>
