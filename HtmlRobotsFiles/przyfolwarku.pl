<div class="btext">Osiedla przy Folwarku to kompleksowe osiedle w Mierzynie/Skarbimierzycach pod Szczecinem. Osiedle przy Folwarku polozone jest jedynie 10 km od centrum Szczecina - niewielka odleglosc od Szczecina jest atutem dla wszystkich, ktorzy nie chca rezygnowac z atrakcji, jakie oferuje miasto. Dojazd do centrum w 15 minut to znakomity wynik dla mieszkania pod Szczecinem. Osiedle w Skarbimierzycach to nowoczesne bloki mieszkalne. Kazdy blok mieszkalny z hala garazowa wyposazony bedzie w winde. W pierwszym etapie powstanie jeden blok a w nim 33 mieszkania pod Szczecinem. Kazdy z blok z hala garazowa (dalsze etapy) przewidziane ma miejsca parkingowe w ilosci 23 miejsc. Niskie bloki mieszkalne z dwoma poziomami zapewnia wysoki komfort zamieszkania. Lokalizacja Osiedla przy Folwarku pod wzgledem komunikacyjnym jest bardzo dogodna - jest osiedle blisko drogi krajowej nr 10. Osiedle polozone blisko granicy z Niemcami (przejscie graniczne Lubieszyn-Linken) - to kolejny z atutow osiedla pod Szczecinem. Oferowane w ramach Osiedla mieszkania pod Szczecinem sa zroznicowane pod wzgledem metrazu i ukladu. Blisko Osiedla przy Folwarku w miejscowosci Skarbimierzyce znajduje sie bardzo ciekawa atrakcja - Folwark Skarbimierz. Infrastruktura osiedla jest przemyslana tak, aby bylo to osiedle dla dzieci - w ramach calego osiedla powstana trzy place zabaw i wiele elementow malej architektury, z ktorej dzieci beda mogly korzystac. Z osiedla bedzie blisko do sklepu, przystanku PKS, szkol i innych waznych dla kazdej rodziny punktow. Osiedle przy Folwarku to jakosc jak mieszkania w Szczecinie w korzystnej, niskiej cenie - po prostu tanie mieszkania pod Szczecinem. Osiedle w Skarbimierzycach zostalo zaprojektowane tak, aby byly to jak najpiekniejsze mieszkania Szczecin, natomiast oferuje wiele atrakcji, w poblizu osiedla: dyskonty, kina, basen. Osiedle pod Szczecinem to jednoczesnie spokoj i cisza typowe dla osiedla podmiejskiego oraz atrakcje miasta wojewodzkiego. Deweloper - firma Akropol Inwestycje, ktora jest inwestorem mieszkan pod Szczecinem to znana na rynku poznanskim firma. Akropol Inwestycje buduje od wielu lat w miejscowosciach pod Poznaniem, teraz deweloper Szczecin bedzie chcial podbic swoja oferta - a bedzie to z pewnoscia atrakcyjna oferta mieszkaniowa. Deweloper kieruje sie w swoich inwestycjach haslem: Twoje miejsce, Twoja przyszlosc. Inwestycje dewelopera sa projektowane z mysla o przyszlosci ich mieszkancow - zadowolenie klientow jest szczegolnie wazne dla Akropol Inwestycje. Generalny wykonawca Osiedla przy Folwarku w Skarbimierzycach to firma POZBUD. POZBUD to sprawdzony partner w inwestycjach. Chcemy, aby nasz Klient byl zadowolony i szczesliwy, ze mieszka w domu wybudowanym przez Akropol Inwestycje.</div>
