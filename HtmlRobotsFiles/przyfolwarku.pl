<div class="title">Osiedle przy Folwarku</div>
<div class="text"><p>Osiedle przy Folwarku w Mierzynie/Skarbimierzycach pod Szczecinem to <strong>idealne miejsce dla os&oacute;b niezaleznych</strong>, kt&oacute;re pracujac w&nbsp;duzym miescie <strong>pragna mieszkac z&nbsp;dala od miejskiego zgielku</strong> i&nbsp;jednoczesnie na tyle blisko centrum, aby <strong>nadal miec dogodny dostep do atrakcji duzego miasta</strong>.</p>
<p>&nbsp;</p>
<p>Osiedle przy Folwarku to <strong>kompleksowo zaplanowana przestrzen podmiejska</strong>. Powstajace w&nbsp;Skarbimierzycach osiedle to, <strong>w&nbsp;ramach drugiego etapu, jeden blok mieszkalny</strong> &ndash; 33&nbsp;mieszkania o&nbsp;zr&oacute;znicowanym ukladzie i&nbsp;metrazu, z ogr&oacute;dkiem, balkonem lub tarasem.</p>
