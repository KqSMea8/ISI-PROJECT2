<p>Realizujac swoja misje Instytut Allerhanda pragnie przyczyniac sie do ubogacania domeny publicznej poprzez dostarczanie wynikow studiow i analiz dla instytucji powolanych do tworzenia i stosowania prawa w Polsce i Unii Europejskiej, w szczegolnosci dla Komisji Kodyfikacyjnej Prawa Cywilnego, Rzadowego Centrum Legislacji, Biura Analiz Sejmowych, czy tez Komisji Europejskiej, Parlamentu Europejskiego, OECD i innych organow, gremiow i agend miedzynarodowych.</p>
<p>Instytut dostrzega wazny odcinek swojej misji w przekazywaniu wiedzy i doswiadczen Polski do krajow kroczacych sciezka transformacji ustrojowej, czesciowo podobnej do tej, jaka Polska przeszla przez ostatnie dwie dekady. Instytut angazuje sie w pomoc ekspercka, projekty badawcze i wspolprace, zwlaszcza z Ukraina oraz krajami Wspolnoty Niepodleglych Panstw.</p>
