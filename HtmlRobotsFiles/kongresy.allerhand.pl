<p>Szczegolnie istotne jest zaangazowanie sie w wymiane ze wschodzacymi potegami gospodarczymi, ktore juz od dluzszego czasu odgrywaja istotna role w wymianie handlowej. Chodzi tu przede wszystkim o Chiny i Indie. Na swiecie istnieja liczne osrodki badawcze i eksperckie zajmujace sie prawnymi aspektami handlu oraz prowadzenia dzialalnosci gospodarczej i inwestycji na Dalekim Wschodzie. Instytut Allerhanda pragnie wyjsc naprzeciw takim potrzebom ewidentnym rowniez w Polsce i podjac probe wypelnienia tej istotnej niszy.</p>
