<div class="et_pb_tab_content">
<h4>Dzialalnosc Instytutu Allerhanda obejmuje w szczegolnosci:</h4>
<ul>
<li>Badanie i opiniowanie majacych znaczenie dla gospodarki projektow ustaw poprzez szacowanie skutkow ekonomicznych i spolecznych okreslonych rozwiazan legislacyjnych (ocena skutkow regulacji, ekonomiczna analiza prawa);</li>
<li>Poszukiwanie metod implementacji prawa wspolnotowego oraz transplantow prawnych do krajowego porzadku prawnego;</li>
<li>Stymulowanie rozwoju polskiej mysli prawnej, etyki zawodowej oraz jakosci debaty publicznej;</li>
<li>Czynienie staran dla wzmocnienia pozycji Polski i innych nowych krajow czlonkowskich UE w publicznych konsultacjach inicjowanych przez Komisje Europejska a dotyczacych takich tematow jak: europeizacja prawa prywatnego, eliminacja barier administracyjnoprawnych dla przedsiebiorczosci, corporate governance, swoboda przeplywu uslug, kapitalu i przedsiebiorczosci, fundusze inwestycyjne, instytucje kredytowe, nadzor nad rynkami finansowymi etc.;</li>
<li>Organizacja sesji naukowych, wykladow monograficznych, sympozjow, seminariow oraz paneli dyskusyjnych z udzialem wiodacych znawcow prawa - akademikow i praktykow, poswieconych rozwazaniom nad wezlowymi zagadnieniami prawnymi, zarowno w aspektach praktycznych, dotyczacych polityki prawa jak rowniez wizjonerskich; Wydawanie publikacji ksiazkowych oraz czasopism we wspolpracy z wiodacymi wydawcami literatury fachowej;</li>
<li>Integracja mlodych naukowcow, w szczegolnosci z krajow takich jak: Polska, Niemcy, Ukraina, Austria, Izrael, poprzez fundowanie stypendiow naukowych oraz doktoranckich; Utrzymywanie miedzynarodowej wspolpracy naukowej ze swoimi odpowiednikami w innych krajach.</li>
</ul>
