<div class="et_pb_tab_content">
<p style="text-align: justify;">Instytut Allerhanda jest <strong>organizacja pozytku publicznego</strong> oraz <strong>osrodkiem zaawansowanych studiow prawnych</strong> z siedziba w Krakowie. Specjalizuje sie m.in. w prawie handlowym, prawie wlasnosci intelektualnej, prawie upadlosciowym, prawie rynku finansowego, corporate governance i organizacji wymiaru sprawiedliwosci. Instytut prowadzi takze badania interdyscyplinarne, zwlaszcza z wykorzystaniem ekonomicznej analizy prawa. Zostal pomyslany jako think tank, ktory ma prowadzic do podnoszenia jakosci prawodawstwa poprzez wypracowanie efektywnych rozwiazan prawnych w gronie specjalistow, ktore beda mogly byc wlaczone do porzadku prawnego.</p>
