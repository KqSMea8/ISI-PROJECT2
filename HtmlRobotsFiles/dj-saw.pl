<h1 class="page-title">O mnie</h1>										<!-- /page-title -->


<p>Moja przygoda z muzyka rozpoczela sie jeszcze w latach osiemdziesiatych ubieglego wieku, jako nastolatek stawialem pierwsze kroki prowadzac dyskoteki szkolne. Bylem pierwszym Dj-em w Lodzi i okolicy w latach 90-tych, ktory prowadzil przyjecia weselne odtwarzajac muzyke z plyt, wtedy ta forma byla malo popularna. Od tamtej pory prowadzenie imprez muzycznych stalo sie moja pasja.</p>
<p>Wyrozniam sie kreatywnoscia, poczuciem humoru, mila aparycja, ogromna wiedza muzyczna, latwoscia nawiazywania kontaktow z publicznoscia. W roku 1995 ukonczylem kurs dla Dj-ow w Warszawie prowadzony przez cenionego dziennikarza muzycznego Bogdana Fabianskiego. Zdobylem certyfikat i stalem sie profesjonalista w branzy muzycznej. Podnioslem swoje kwalifikacje wspolpracujac z Bogdanem Fabianskim. Gralem miedzy innymi w kultowych klubach Lodzi: ,,Studio", ,,Kabaret", ,,West side", ,,77". Prowadzilem imprezy w trzytysiecznej, belgijskiej dyskotece La Bocca, w ktorej bylem rowniez managerem.</p>


<p>Jako DJ &amp; Wodzirej jestem osoba, ktora odpowiedzialna jest za prowadzenie zabawy od strony muzycznej.</p>



<p>Prezentuje muzyke w oryginalnym wykonaniu z plyt CD na profesjonalnych deckach miksujac ja na zywo. Nie prezentuje muzyki z laptopa jak dzis wiekszosc dj-ow. Odpowiadam za jej wlasciwy dobor do charakteru imprezy i gustow gosci.<br>Oprocz muzyki zapewniam inne atrakcje. Prowadze zabawy taneczne, wokalne, a takze organizuje konkursy i zabawy integracyjne. Po ponad 20 latach dzialalnosci na rynku uslug muzycznych moge pochwalic sie mnostwem udanych imprez, np. 100-lecie, Spolem, 20-lecie firmy Dell, eventy dla firm Deante, Mercedes, Indesit Company, Bosh, H.Skrzydlewska.</p>



<p>Prowadzilem imprezy muzyczne w hotelach Lodzi (Andel&#8217;s, Tobaco, Qubus, Ambasador, Stacja Nowa Gdynia) Toruniu (Retman, Polonia, Olender, Copernicus) Warszawa (Lord, Polonia Palace Hotel, Mariott, Club Capitol) oraz restauracjach Lodzi i okolicach. Jednak moim najwiekszym atutem jest zebrane doswiadczenie, ktore po latach owocuje profesjonalnym podejsciem do wykonywanej pracy, wyczuciem gustu muzycznego klientow oraz stworzenie odpowiedniej do sytuacji atmosfery.<br>Priorytetem dla mnie jest zadowolenie klienta.</p>


