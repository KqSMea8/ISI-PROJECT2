<a href="http://wzpwarszawa.pl/" title="Permalink to Wojewodzki Zwiazek Pszczelarzy w Warszawie" rel="bookmark">Wojewodzki Zwiazek Pszczelarzy w Warszawie</a>
</h1>



<div class="entry">
<p>jest organizacja dzialajaca spolecznie.</p>
<p>Celem dzialania naszego Zwiazku jest inicjowanie i prowadzenie wszechstronnej dzialalnosci w zakresie pszczelarstwa zawodowego i amatorskiego w szczegolnosci rozwijanie i doskonalenie hodowli pszczol. Naszym zadaniem jest zapewnienie odpowiedniego zapylenia roslin polowych i sadowniczych, ochrona srodowiska naturalnego oraz krzewienie wiedzy pszczelarskiej wsrod spoleczenstwa.</p>
<p><strong>Zrzeszamy 10 kol pszczelarzy</strong>, ktorym zapewniamy pomoc w zakresie ciaglego polepszania poglowia pszczol oraz ich zdrowotnosci poprzez umozliwianie pszczelarzom refundowanych zakupow matek pszczelich oraz lekow zwalczajacych warroze u pszczol. Kladziemy duzy nacisk na edukacje pszczelarzy zapraszajac na wyklady do Dyskusyjnego Klubu Pszczelarza specjalistow z dziedziny pszczelarstwa i ochrony srodowiska. Pamietajmy, ze pszczoly sa ogromnym dobrem, o ktore powinnismy wszyscy wspolnie dbac.</p>
<p><strong>Wszystkich niezrzeszonych pszczelarzy oraz osoby pragnace zapoznac sie z pszczelarstwem zapraszamy na spotkania do Dyskusyjnego Klubu Pszczelarza oraz do wybranych kol zrzeszonych w naszym zwiazku.<br />
</strong></p>
