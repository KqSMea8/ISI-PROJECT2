<h1>Granit</h1>
<p>Granit to skala kwasna magmowa - glebinowa, ktora ma budowe jawnokrystaliczna, najczesciej sredniokrystaliczna. Jego struktura uwarunkowana jest pochodzeniem i miejscem powstania, jakim jest srodowisko glebinowe.
Granit w swoim skladzie zawiera 80-100% objetosciowych mineralow glownych takich, jak: kwarc, skalenie alkaliczne czy plagioklaz oraz 0-20% objetosciowych mineralow akcesorycznych. Najczesciej wchodzacymi w sklad mineralami sa: ortoklaz, plagioklaz, kwarc i biotyt, nieco rzadziej: muskowit i amfibole, jeszcze rzadziej moga to byc pirokseny,
a takze w niewielkich ilosciach mineraly akcesoryczne, jak: apatyt, cyrkon, monacyt, ksenotym, turmalin, tytanit, beryl, rutyl, anataz, magnetyt, fluoryt, granat i inne.
Charakterystyczna cecha struktury granitu sa liczne spekania i szczeliny, przebiegajace zwykle w trzech prostopadlych kierunkach. Sa to tzw. ciosy. Taka budowa ulatwia proces wietrzenia oraz eksploatacje granitu. W procesie wietrzenia granit tworzy charakterystyczne skalki, owalne lub kuliste bloki skalne, czasami rowniez goloborza, a w wyniku wietrzenia chemicznego w wilgotnym i cieplym klimacie powstaje tzw. kasza granitowa.
W ostatnim czasie granit cieszy sie duzym zainteresowaniem ze wzgledu na jego szerokie zastosowanie oraz praktyczne i estetyczne walory.</p>		</div>
