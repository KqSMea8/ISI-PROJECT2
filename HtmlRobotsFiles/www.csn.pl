<div>Za transport placisz raz, niezaleznie od ilosci zakupionych sztuk.<br />
Koszt transportu na terenie kraju wynosi 23 zl lub 48 zl w przypadku kratownic. <br />
Dla przesylek platnych za pobraniem nalezy doliczyc 11 zl. <br />
Wycieraczki sa b.dobrze zabezpieczone i starannie zapakowane. Czesto wykonujemy zabezpieczenia usztywniajace np. dla obramowan aluminiowych.
