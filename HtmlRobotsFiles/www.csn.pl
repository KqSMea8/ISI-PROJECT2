<div class="row">
<div class="col-md-6 col-sm-12 item-1">
<label>Jak wybrac dobra wycieraczke?</label>
<div>Dobra wycieraczka znacznie ulatwia utrzymanie czystosci w domu, firmie, urzedzie, szkole czy sklepie. Najlepsze wycieraczki beda wtedy, kiedy zostana dobrane "na miare". <br />
<span style="display: none;">W naszej ofercie znajda Panstwo kilkaset roznych produktow, aby uzyskac fachowa porade wystarczy zadzwonic do nas 91 486 98 34 lub 801 011 709, lub wyslac email na info@csn.pl.
Dzialamy w branzy wycieraczek od ponad 15 lat. Z naszych rozwiazan korzystaja nie tylko najwieksze i najbardziej prestizowe firmy z kraju i zagranicy, ale rowniez klienci indywidualni.</span>
