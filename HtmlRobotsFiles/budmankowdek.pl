<div class="panel-body">
<!-- Tab Content-->
<p>Naszym celem jest zapewnienie jak najlepszej obslugi naszym klientom.
Realizujemy to juz na etapie sprzedazy poprzez fachowe i rzetelne doradztwo przy wyborze materialow.
Dodatkowym atutem jest zapewnienie klientom bezplatnego transportu zakupionych u nas produktow. </p></div>
