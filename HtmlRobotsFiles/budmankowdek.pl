<p> Firma Budman & Kowdek zajmuje sie sprzedaza pokryc dachowych od 2005 roku.
Przez ten okres, dzieki naszemu doswiadczeniu i  profesjonalnej obsludze, zdobylismy
silna pozycje na rynku pokryc dachowych, zdobywajac uznanie wielu zadowolonych klientow.
Zajmujemy sie sprzedaza produtow wylacznie renomowanych producentow. Dzieki temu mamy pewnosc,
ze zapewniamy naszym klientom najwyzsza jakosc, oferujac tylko sprawdzone produkty renomowanych marek.</p>
