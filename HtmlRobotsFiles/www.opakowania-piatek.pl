<p>Przedsiebiorstwo Uslugowo Handlowo Produkcyjne Export-Import Czeslaw Piatek<strong> </strong>powstalo 20 grudnia 1995 roku. Zajmuje sie produkcja opakowan z kartonu, tektury, tektury 3-warstwowej, tektury 5-warstwowej i tektury 7-warstwowej. Wykonujemy rowniez z wyzej wymienionych tektur , roznego rodzaju elementy i zabezbieczenia pakowanych wyrobow. Wlasciciel firmy Czeslaw Piatek w branzy papierniczej i opakowaniowej przepracowal 38 lat, pozwala to na szeroki zakres doradztwa i wykonawstwa opakowan Obecnie posiadamy dwa zaklady produkcyjne. Z niektorymi   odbiorcami jestesmy zwiazani od 13 lat. Produkujemy na rynek lokalny jak rowniez na rynek europejski. Od 9 sierpnia 2007 roku posiadamy certyfikat jakosci ISO 9001:2000 Dzieki nam produkcja opakowan niemozliwych moze byc mozliwa. W razie pytan prosimy o kontakt, mozecie byc mile i pozytywnie zaskoczeni.</p>
