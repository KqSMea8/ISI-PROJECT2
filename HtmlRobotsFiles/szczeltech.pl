<p>Firma "JAKUBOWSKI" istnieje na polskim rynku od 1992 roku. Na poczatku produkowalismy reczne narzedzia traserskie takie jak: punktaki, rysaki widiowe, noze widiowe, cyrkle, piony, kt&oacute;re dzieki zastosowaniu najwyzszej jakosci material&oacute;w zyskaly uznanie odbiorc&oacute;w. Wraz z rozwojem firmy poszerzylismy sw&oacute;j asortyment o artykuly stosowane w kotlarstwie i zdunstwie, m.in. szczeliwo szklane, szczeliwo grafitowe, szczeliwo lojowe, szczeliwo ceramiczne, beton ogniotrwaly, raczki do pieca, pokretla, rekojesci, oraz wiele innych. Od pieciu lat produkujemy r&oacute;wniez artykuly do czyszczenia kotl&oacute;w, piec&oacute;w i komin&oacute;w takie jak: szczotki-wyciory do pieca C.O., szczotki kominowe, komplety do czyszczenia piecy - szczotka do pieca, przepychacze, sznur azbestowy,&nbsp; oprawy do wycior&oacute;w. Od kilku lat zajmujemy sie dystrybucja : plyt uszczelkarskich, uszczelniajacych, plyt zaroodpornych, szczotek technicznych( szczotki doczolowe, szczotki tarczowe, szczotki katowe), oraz filcy.&nbsp; Wszystkie nasze produkty, dzieki zastosowaniu najwyzszej jakosci material&oacute;w i sprawdzonej technologii spelnia oczekiwania kazdego klienta. Dzieki duzemu doswiadczeniu naszego zespolu zapewniamy fachowe doradztwo techniczne i materialowe, a dobrze rozwinieta logistyka zapewnia bardzo szybka dostawe zam&oacute;wionych towar&oacute;w. Kazdego klienta traktujemy indywidualnie odpowiednio dobierajac najlepsze rozwiazania dla jego potrzeb. Zapraszamy do wsp&oacute;lpracy solidne firmy oraz klient&oacute;w indywidualnych.</p></div>
