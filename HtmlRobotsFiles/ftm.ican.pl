<h2 class="text-center">To unikalny program nakierowany na natychmiastowe rozwiazania oraz wielomiesieczne wsparcie. Co jeszcze nas wyroznia?</h2>
<div class="visible-lg">
<div class="row">
<div class="col-md-4">
<h3 class="container-fluid-abs width">Indywidualna diagnoza i wyznaczenie kierunku rozwoju</h3>
<img src="images/box/zaawansowana-diagnoza-indywidualna-uczestnikow.jpg" class="img-responsive container-fluid-rv" alt="Indywidualna diagnoza i wyznaczenie kierunku rozwoju">
<p>W planowaniu rozwoju na stanowisku menedzerskim niezwykle istotna role odgrywa rozwoj samoswiadomosci, pozwalajacy wykorzystac w codziennych dzialaniach mocne strony przy jednoczesnym rozwoju obszarow deficytowych. Dlatego w programie First-Time Manager<sup>TM</sup> pojawiaja sie dwa narzedzia diagnostyczne ICAN Research: <strong>Learning Style Questionnaire</strong> (Badanie stylu uczenia sie) oraz <strong>Influence Skills Inventory</strong> (Badanie umiejetnosci wywierania wplywu). Kazdy uczestnik programu otrzymuje takze <strong><q>Kontrakt na rozwoj</q></strong>, ktory pozwala na dokladne sprecyzowanie celow rozwojowych oraz ocene postepow w ich realizacji. </p>
</div>
<div class="col-md-4">
<h3 class="container-fluid-abs width">Kompleksowe, praktyczne podejscie</h3>
<img src="images/box/2.png" class="img-responsive container-fluid-rv" alt="Kompleksowe, praktyczne podejscie">
<p>W trakcie programu skupiamy sie na rozwoju umiejetnosci miekkich kluczowych dla osiagniecia sukcesu na stanowisku kierowniczym, ze szczegolnym naciskiem na wyrobienie w menedzerach pewnosci w nowej roli. Pomagamy uniknac bledow, ktore czesto juz na starcie znacznie utrudniaja mlodym liderom kariere... Lub prowadza do jej szybkiego konca. Podczas intensywnych sesji warsztatowych uczestnicy otrzymuja <strong>wiele konkretnych narzedzi i wskazowek, ktore pozwalaja im zwiekszyc swoje kompetencje jeszcze w trakcie trwania programu</strong>.  </p>
</div>
<div class="col-md-4">
<h3 class="container-fluid-abs width">Metoda ICAN Smart Learning<sup>TM</sup></h3>
<img src="images/box/unikalna-metoda-nauczania.jpg" class="img-responsive container-fluid-rv" alt="Unikalna metoda nauczania">
<p>Podobnie jak wszystkie programy rozwojowe ICAN Institute, First-Time Manager<sup>TM</sup> wykorzystuje metode  <a href="https://ican.pl/ican-smart-learning/" target="_blank"><strong>ICAN Smart Learning<sup>TM</sup></strong></a>, menedzerow. Skladaja sie na nia m.in. metoda blended learning (laczaca tradycyjne formy ksztalcenia oraz prace poprzez portal wiedzy online) oraz harwardzka metoda studiow przypadkow (dostarczamy ich najwiecej na polskim rynku). <strong>Naczelnym wyroznikiem metody jest podejscie <em>evidence-based management</em>: przekazujemy uczestnikom wylacznie sprawdzone rozwiazania poparte badaniami skutecznosci.</strong> </p>
</div>
</div>
<div class="row">
<div class="col-md-4">
<h3 class="container-fluid-abs width">Intensywna formula, opracowana z mysla o menedzerach, ktorzy potrzebuja rezultatow tu i teraz</h3>
<img src="images/box/wygodna-formula.jpg" class="img-responsive container-fluid-rv" alt="Intensywna formula, opracowana z mysla o menedzerach, ktorzy potrzebuja rezultatow tu i teraz">
<p>Bazujac na naszym 20-letnim doswiadczeniu, okreslilismy mozliwie najkrotszy czas do zdobycia i przecwiczenia umiejetnosci zarzadzania zespolem. Dlatego wlasnie First-Time Manager<sup>TM</sup> to <strong>5 dwudniowych sesji rozlozonych w czasie 3 miesiecy</strong>. Sesje odbywaja sie w cyklach sroda-czwartek, piatek-sobota. Uczestnicy moga z wyprzedzeniem zaplanowac udzial w zajeciach, a w razie potrzeby skorzystac z opcji odrobienia danej sesji w innym terminie. Wystarczy, ze zglosza sie do koordynatora FTM<sup>TM</sup>, ktory juz teraz z przyjemnoscia  <a href="#">odpowie na Twoje pytania</a>. </p>
</div>
