tj.: rejestrowania uzytkownikow i kontaktowania sie w celach zgodnych z trescia
przeslanej do nas wiadomosci, a takze na potrzeby dochodzenia ewentualnych
roszczen przed sadem oraz w celach archiwalnych i statystycznych.</li>
<li>Stowarzyszenie ma prawo udostepniac dane osobowe podmiotom upowaznionym na
podstawie wlasciwych przepisow prawa, a takze dostawcom uslug bazodanowych na
podstawie stosownych umow.</li>
<li>Panstwa dane osobowe beda przetwarzane przez Stowarzyszenie przez okres
niezbedny do zrealizowania celu, w jakim zostaly zebrane, z tym ze Stowarzyszenie
zastrzega sobie prawo do przetwarzania Panstwa danych rowniez przez okres
