<h3>Gielda Partnerstw</h3>
<h2><strong>Zapraszamy do wspolpracy</strong></h2>
<p style="padding:0 100px;"><em>Celem programu jest inicjowanie i rozwijanie partnerstw transgranicznych pomiedzy samorzadami i ich jednostkami organizacyjnymi, instytucjami pozarzadowymi oraz pozostalymi podmiotami dzialajacymi na terenie Euroregionu Slask Cieszynski. Baza partnerstw umozliwia polskim i&nbsp;czeskim podmiotom nawiazanie wspolpracy i&nbsp;wyszukiwanie potencjalnych partnerow, z ktorymi chcieliby realizowac wspolne przedsiewziecia i projekty.</em></p>
