<h1 class="entry-title"><a href="http://www.sklepnagrzewnice.pl/?p=375" rel="bookmark">Oszczedzaj na ogrzewaniu, wymieniajac swoj stary kociol gazowy na nowoczesny - kondensacyjny</a></h1>

<div class="entry-meta">
<span class="sep">Opublikowany </span><a href="http://www.sklepnagrzewnice.pl/?p=375" title="08:00" rel="bookmark"><time class="entry-date" datetime="2019-01-17T08:00:15+00:00">17 stycznia 2019</time></a><span class="by-author"> <span class="sep"> przez </span> <span class="author vcard"><a class="url fn n" href="http://www.sklepnagrzewnice.pl/?author=1" title="Zobacz wszystkie wpisy, ktorych autorem jest admin" rel="author">admin</a></span></span>			</div><!-- .entry-meta -->

</header><!-- .entry-header -->

<div class="entry-content">
<p>Jezeli jeszcze nie podjelismy decyzji o wymianie starego kotla niekondensacyjnego na nowy, ktory pozwoli nam na mniejsze wydatki na paliwo, to czas najwyzszy to zmienic. Wedlug szacunkow koszty na paliwo, w przypadku kotlow kondensacyjnych sa o minimum 15% (w niektorych przypadkach wartosc ta siega nawet 40%) nizsze niz w przypadku stosowania urzadzen starego typu. Jak to mozliwe?</p>
<p>Glownym powodem, dla ktorego kociol kondensacyjny pozwala na uzyskanie konkretnych oszczednosci, jest zjawisko skraplania sie pary wodnej, ktora obecna jest w spalinach - kociol kondensacyjny wykorzystuje energie, ktora uwalniania jest podczas tego procesu (podczas spalania 1 m3 gazu ziemnego powstaje az 2 m3 pary wodnej). Zwykly kociol starej generacji nie jest do tego przystosowany, w zwiazku z czym czesc energii zawartej w paliwie marnuje sie, wylatujac przez komin wraz ze spalinami.</p>
<h2>Zmienna moc, a oszczednosci</h2>
<div id="attachment_376" style="width: 211px" class="wp-caption alignright"><img class=" wp-image-376" src="http://www.sklepnagrzewnice.pl/wp-content/uploads/2019/01/gazowy-piec-kondensacyjny-244x300.png" alt="Gazowy piec kondensacyjny" width="201" height="245" /><p class="wp-caption-text">Gazowy piec kondensacyjny</p></div>
<p>Z punktu widzenia oszczednosci najlepsze rozwiazanie byloby takie, gdyby palnik kotla pracowal przez caly sezon grzewczy, zmieniajac i dostosowujac jedynie moc - takie wlasnie rozwiazanie stosowane w nowoczesnych kotlach gazowych zapobiega stratom energii, ktore powstaja w trakcie uruchamiania palnika. Kociol kondensacyjny pracuje w ten sposob, aby jego wylaczanie bylo mozliwie jak najrzadsze - sterowanie dazy do tego, aby kociol caly czas pracowal z minimalna moca, ktora zapewni dostarczenie do instalacji wymaganej ilosci ciepla. Bardzo wazna zaleta nowoczesnych kotlow kondensacyjnych jest ich duzy zakres modulacji mocy - minimalna moc takiego urzadzenia to w wiekszosci przypadkow zaledwie 10% maksymalnej. Sytuacja ta sprawia, ze kociol taki pracuje stabilnie, nie tylko w przypadku koniecznosci dostarczenia do instalacji duzej ilosci ciepla np. podczas mrozow, ale rowniez w przypadku niewielkiego obciazenia cieplnego, z ktorym mamy do czynienia przez wieksza czesc roku.</p>
<p>Warto wiedziec, ze kotly kondensacyjne szczegolnie dobrze sprawdzaja sie w przypadku ogrzewania podlogowego, co wynika z faktu, ze im nizsza temperatura na wyjsciu z kotla, tym wyzsza bedzie sprawnosc urzadzenia.</p>
<h2>Ogrzewanie, a bezpieczenstwo</h2>
<p>Bezsprzeczna zaleta kotlow kondensacyjnych jest ich zamknieta komora spalania, ktora eliminuje ryzyko zwiazane z wydostaniem sie spalin do pomieszczenia - dzieki temu kociol taki moze byc zainstalowany nawet w malej lazience w bloku.</p>
<p>Zamknieta komora spalania daje rowniez mozliwosc doprowadzenie powietrza do palnika spoza budynku przy wykorzystaniu specjalnego przewodu - w ten sposob unika sie niepotrzebnego i nieekonomicznego wychladzania pomieszczen, ktore mialoby miejsce na skutek naplywu zimnego powietrza w trakcie pracy kotla oraz mogacych wystapic zakloceniach w funkcjonowaniu wentylacji.</p>
<p>Kotly kondensacyjne to solidna inwestycja na lata, ktora zapewni doskonaly komfort cieplny oraz pozwoli na zauwazalne zredukowanie kosztow ogrzewania, biorac pod uwage fakt, ze ceny paliw stosowanych w kotlach wciaz rosna, z pewnoscia warto rozwazyc zakup urzadzenia, ktore jest nie tylko oszczedne, ale rowniez bezpieczne i niezawodne.</p>
