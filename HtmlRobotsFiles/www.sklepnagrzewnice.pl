<h1 class="entry-title"><a href="http://www.sklepnagrzewnice.pl/?p=350" rel="bookmark">Wady i Zalety Ogrzewania Gazowego</a></h1>

<div class="entry-meta">
<span class="sep">Opublikowany </span><a href="http://www.sklepnagrzewnice.pl/?p=350" title="11:32" rel="bookmark"><time class="entry-date" datetime="2018-09-04T11:32:52+00:00">4 wrzesnia 2018</time></a><span class="by-author"> <span class="sep"> przez </span> <span class="author vcard"><a class="url fn n" href="http://www.sklepnagrzewnice.pl/?author=1" title="Zobacz wszystkie wpisy, ktorych autorem jest admin" rel="author">admin</a></span></span>			</div><!-- .entry-meta -->

</header><!-- .entry-header -->

<div class="entry-content">
<p>Jak pokazuja dane statystyczne, w ostatnich latach znaczaco wzrosla liczba domostw korzystajacych z ogrzewania gazowego. Wiekszosc osob stosujacych to rozwiazanie jest zadowolona, choc oczywiscie nie brakuje tu rowniez i pewnych krytycznych glosow. Tak naprawde jedna to, czy w danym przypadku ogrzewanie gazowe bedzie korzystne czy tez nie, uzaleznione jest od wielu czynnikow.</p>
<p><img class="alignright size-medium wp-image-351" src="http://www.sklepnagrzewnice.pl/wp-content/uploads/2018/09/palnik-gazowy-300x210.png" alt="" width="300" height="210" srcset="http://www.sklepnagrzewnice.pl/wp-content/uploads/2018/09/palnik-gazowy-300x210.png 300w, http://www.sklepnagrzewnice.pl/wp-content/uploads/2018/09/palnik-gazowy-768x537.png 768w, http://www.sklepnagrzewnice.pl/wp-content/uploads/2018/09/palnik-gazowy-1024x716.png 1024w, http://www.sklepnagrzewnice.pl/wp-content/uploads/2018/09/palnik-gazowy-429x300.png 429w" sizes="(max-width: 300px) 100vw, 300px" />W przypadku ogrzewania gazowego kazdy ma dwie mozliwosci. Pierwsza z nich to podlaczenie domu do zewnetrznej sieci gazowej, natomiast drugie rozwiazanie polega na korzystaniu z plynnego gazu, ktory przy domostwie musi byc w przechowywany w odpowiednim zbiorniku oraz odpowiednich warunkach (dla bezpieczenstwa zarowno domownikow jak i ich sasiadow). Oczywiscie przy tym rozwiazaniu konieczne jest rowniez wybudowanie odpowiedniej instalacji, ktora bedzie doprowadzala gaz do domu. Nie da sie ukryc, ze popularnosc ogrzewania gazowego wynika przede wszystkim z jego wygody stosowania, gdyz jest ono niemal bezobslugowe, co oznacza, ze przy minimalnym wysilku mozemy miec bardzo cieplo w kazdym pomieszczeniu (dowolnie regulujac przy tym temperature odpowiednio do swoich preferencji). Po uruchomieni instalacji wystarczy corocznie przeprowadzac jej kontrole, co cieszy szczegolnie wszystkich tych, ktorzy lubia miec cieplo, ale brakuje im czasu na rozpalanie w kominku czy korzystanie z innych bardziej pracochlonnych systemow ogrzewania. Co wazne, przy gazowym ogrzewaniu nie ma koniecznosci montazu dodatkowego systemu odpowiedzialnego za podgrzewanie wody.</p>
<p>W ogrzewaniu gazowym wykorzystywane sa gazowe kotly, czyli urzadzenia o bardzo wysokiej wydajnosci i sprawnosci, ktore przy tym sa rowniez wbrew pozorom (o czym czesto zapominaja przeciwnicy tego systemu ogrzewania). W przypadku nowoczesnych urzadzen (chodzi tu przede wszystkim o kondensacyjne kotly) mozna z jednej dawki paliwa uzyskac nawet dwukrotnie wiecej ciepla niz z niekondensacyjnych piecow. A to oznacza o wiele nizsze koszty ogrzewania.</p>
<p style="text-align: center;">
<iframe src="https://www.youtube.com/embed/RYVzdTr9pvs" width="560" height="315" frameborder="0" allowfullscreen="allowfullscreen"></iframe></p>
<p>Kolejna zaleta plynaca z korzystania z ogrzewania gazowego jest fakt, iz zajmuja one znacznie mniej miejsca niz tradycyjne piece na wegiel czy drewno, a przy tym nie pozostawiaja tyle zabrudzen, dzieki czemu domownicy nie musza dodatkowo dokladac wielu staran i pracy do utrzymania w czystosci okolic pieca. Co wazne, w przypadku ogrzewania gazowego korzystajac z pieca kondensacyjnego o mocy mniejszej niz 30 kW nie ma koniecznosci umieszczania go w specjalnie do tego wyznaczonym pomieszczeniu, czyli kotlowni. W takim przypadku kociol moze byc umieszczony w dowolnym miejscu.<br />
Choc ogrzewanie gazowe jest rozwiazaniem ekonomicznym, to jednak ogromna jego wada jest koniecznosc poniesienia duzych koszow inwestycyjnych, jakie poniesc nalezy na zalozenie calej instalacji. W najgorszym przypadku za wykonanie instalacji ogrzewania gazowego nalezy zaplacic nawet do kilkunastu tysiecy zlotych. Kolejna przeszkoda, przemawiajaca za tym, ze byc moze instalacja gazowa wcale nie jest taka korzystna jakby moglo sie wydawac, sa pewne ograniczenia w dostepnosci gazu. Aby moc w pelni korzystac z tego typu ogrzewania, absolutnie niezbedne jest stworzenie odpowiedniego przylacza. Niestety nie wszedzie takie przylacze moze powstac, bywaja tez sytuacje w ktorych wykonanie go jest ogromnie nieoplacalne.</p>
