<h1 class="entry-title"><a href="http://www.sklepnagrzewnice.pl/?p=378" rel="bookmark">Wentylacja z odzyskiem ciepla</a></h1>

<div class="entry-meta">
<span class="sep">Opublikowany </span><a href="http://www.sklepnagrzewnice.pl/?p=378" title="11:21" rel="bookmark"><time class="entry-date" datetime="2019-02-25T11:21:12+00:00">25 lutego 2019</time></a><span class="by-author"> <span class="sep"> przez </span> <span class="author vcard"><a class="url fn n" href="http://www.sklepnagrzewnice.pl/?author=1" title="Zobacz wszystkie wpisy, ktorych autorem jest admin" rel="author">admin</a></span></span>			</div><!-- .entry-meta -->

</header><!-- .entry-header -->

<div class="entry-content">
<p>Wentylacja dziala w ukryciu i spelnia wazna funkcje w systemie zabezpieczenia komfortu mieszkancow wielu obiektow budowlanych. Zajmuje sie uzdatnianie powietrza. Szczegolna odmiana wentylacji jest rekuperacja.</p>
<h2>Czym jest rekuperacji?</h2>
<p><img class="alignright  wp-image-379" src="http://www.sklepnagrzewnice.pl/wp-content/uploads/2019/02/wentylacja-mechaniczna-1-300x201.png" alt="" width="212" height="145" />Jest to rodzaj wentylacji ze wspomaganiem. Wentylacja mechaniczna wspomagajaca wymuszony obieg powietrza oraz odzyskujaca cieplo z atmosfery wewnatrz pomieszczen. Jej centralnym punktem jest rekuperator. Tam nastepuje wymiana powietrza zuzytego ze swiezym, a przy okazji przekazywane sa kalorie energii. Szacunkowo wychwyt ciepla okresla sie na poziomie nawet do 90%.</p>
<h2>Jak dziala system?</h2>
<p>Laczac dwa obiegi, powietrza znajdujacego sie wewnatrz i zuzytego, a zarazem cieplego i drugi powietrza czerpanego z zewnatrz, pozwala na odzyskanie ciepla ze znacznej czesci powietrza wywiewnego. Rekuperator to niewielkie urzadzenie, ktore jest w stanie wykonac te zdawaloby sie nie do wykonania prace. Dzieki budowie rekuperatora od strumienia powietrza spotykaja sie nie mieszajac.</p>
<h2>Jak zbudowany jest rekuperator?</h2>
<p>Przemysl oferuje trzy rodzaje konstrukcji rekuperatorow.</p>
<ul>
<li>Wymiennik krzyzowy - obie strugi powietrzne przeplywaja obok siebie pod katem 90 stopni.</li>
<li>Wymiennik obrotowy - umieszczona wewnatrz wymiennika porowata masa obraca sie raz w strefie cieplego, a na nastepnie zimnego powietrza. Porowata masa stanowi ladunek akumulacyjny. Urzadzenie ma bardzo wysoka sprawnosc oceniana nawet na 95%.</li>
<li>Wymiennik przeciwpradowy - jak nazwa wskazuje, oba strumienie powietrza poruszaja sie w przeciwnych kierunkach. Dla zmniejszenia wymiarow urzadzenia poszczegolne strefy cieplego i zimnego powietrza umieszczone sa wspolsrodkowo.</li>
</ul>
<h2>Komfort i dobre samopoczucie</h2>
<p>Rekuperacja skutecznie izoluje pomieszczenia przed wplywem niekorzystnych warunkow zewnetrznych. Usuwa nieprzyjemne zapachy. Dba o pozbywanie sie zanieczyszczen miejskich. Wentylacja z odzyskiem ciepla skazana jest dla osob cierpiacych na alergie. Filtrujac powietrza usuwa z niego znaczna liczbe patogenow.</p>
<p><img class="aligncenter size-large wp-image-380" src="http://www.sklepnagrzewnice.pl/wp-content/uploads/2019/02/rekuperacja-1-1024x681.png" alt="" width="584" height="388" srcset="http://www.sklepnagrzewnice.pl/wp-content/uploads/2019/02/rekuperacja-1-1024x681.png 1024w, http://www.sklepnagrzewnice.pl/wp-content/uploads/2019/02/rekuperacja-1-300x199.png 300w, http://www.sklepnagrzewnice.pl/wp-content/uploads/2019/02/rekuperacja-1-768x511.png 768w, http://www.sklepnagrzewnice.pl/wp-content/uploads/2019/02/rekuperacja-1-451x300.png 451w" sizes="(max-width: 584px) 100vw, 584px" /></p>
<h2>Strona finansowa</h2>
<p>Dzieki rekuperacji mozna zaoszczedzic znaczne ilosci pieniedzy. W perspektywie czasu rachunki za ogrzewanie zmniejsza sie zdecydowanie.</p>
