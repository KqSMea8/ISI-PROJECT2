<span class="sep">Opublikowany </span><a href="http://www.sklepnagrzewnice.pl/?p=353" title="06:31" rel="bookmark"><time class="entry-date" datetime="2018-10-02T06:31:49+00:00">2 pazdziernika 2018</time></a><span class="by-author"> <span class="sep"> przez </span> <span class="author vcard"><a class="url fn n" href="http://www.sklepnagrzewnice.pl/?author=1" title="Zobacz wszystkie wpisy, ktorych autorem jest admin" rel="author">admin</a></span></span>			</div><!-- .entry-meta -->

</header><!-- .entry-header -->

<div class="entry-content">
<p>Oczywistym zadaniem wentylacji mechanicznej jest wymiana powietrza na swiezsze, sprowadzane z zewnatrz. Jednak, jak kazda instalacja, bedzie dzialala poprawnie wylacznie wtedy, gdy utrzymamy ja w ciaglej czystosci. <b>Jakie czynnosci konserwacyjne sa niezbedne, aby przedluzyc zywotnosc instalacji? Jak i kiedy czyscic instalacje wentylacji mechanicznej? </b>Wyjasniamy wszystko w ponizszym artykule.<b><span class="Apple-converted-space"> </span></b></p>
<h2>Wymiana filtrow</h2>
<div id="attachment_354" style="width: 310px" class="wp-caption alignright"><img class="size-medium wp-image-354" src="http://www.sklepnagrzewnice.pl/wp-content/uploads/2018/10/filtry-rekuperatora-300x272.png" alt="Filtry - Rekuperator" width="300" height="272" srcset="http://www.sklepnagrzewnice.pl/wp-content/uploads/2018/10/filtry-rekuperatora-300x272.png 300w, http://www.sklepnagrzewnice.pl/wp-content/uploads/2018/10/filtry-rekuperatora-330x300.png 330w, http://www.sklepnagrzewnice.pl/wp-content/uploads/2018/10/filtry-rekuperatora.png 458w" sizes="(max-width: 300px) 100vw, 300px" /><p class="wp-caption-text">Filtry &#8211; Rekuperator</p></div>
<p>Jest to czynnosc podstawowa i najbardziej istotna, jesli chodzi o dzialanie instalacji wentylacyjnej. To na filtrach osadzaja sie najwieksze ilosci brudu czy kurzu, z ktorych oczyszczane jest powietrze. Jesli nie beda one wymieniane dostatecznie czesto, zanieczyszczenia osiada sie na kolejnych elementach instalacji, co przelozy sie na szybsze jej uszkodzenia. <b>Powinnismy je wymieniac co ok. 3 miesiace</b> (w zaleznosci od srodowiska, w jakim zyjemy); dotyczy to obu filtrow w rekuperatorze. Warto takze regularnie czyscic z kurzu anemostaty.</p>
<h2>Okresowy przeglad &#8211; kiedy?</h2>
<p>O ile wymiana filtrow i dbanie o czystosc zewnetrznych elementow nie jest trudne, tak po gruntowne czyszczenie czy przeglady warto zwrocic sie do specjalisty. <b>Przeglad rekuperatora oraz calej instalacji nalezy wykonac raz w roku.</b><span class="Apple-converted-space"> </span></p>
<p>Zostanie wtedy oczyszczone wnetrze urzadzenia &#8211; podobnie jak skrzydelka wentylatorow, wymiennik ciepla oraz anemostaty. Fachowiec powinien rowniez skontrolowac stan czerpni i wyrzutni, a takze ogolna czystosc calej instalacji. Podczas przegladu nalezy tez zmierzyc wielkosc przeplywu powietrza.</p>
<h2>Czyszczenie kanalowe oraz gruntowego wymiennika ciepla</h2>
<p>Tego typu czynnosci wykonuje sie juz znacznie rzadziej, co kilka lat. Tym rowniez powinien zajmowac sie zawodowiec, gdyz osoba niedoswiadczona moglaby przez przypadek uszkodzic istotne elementy konstrukcji.</p>
<p style="text-align: center;"><iframe src="https://www.youtube.com/embed/8jwF-4mzaU8" width="560" height="315" frameborder="0" allowfullscreen="allowfullscreen"></iframe></p>
<p><b>O czyszczenie przewodow wentylacji musimy zadbac co ok. 5 lat</b> &#8211; glownie wtedy, gdy wewnatrz anemostatow zauwazymy zbyt duza ilosc nagromadzonego kurzu. Taki zabieg &#8211; w przypadku sztywnych, wytrzymalych materialow instalacji &#8211; wykonuje sie przy pomocy obrotowej szczotki. Jesli zas posiadamy przewody wykonane z aluminium, najbezpieczniej jest je wymienic na nowe.</p>
<p><b>Gruntowy wymiennik ciepla powinien zostac czyszczony co ok. 10 lat.</b> Sposob czyszczenia i konserwacji zalezy jednak od jego rodzaju. Wymienniki zwirowe trzeba, niestety, wymienic, zas rurowe zostaja po prostu przeplukane woda. W przypadku glikolowych wymiennikow wystarczy wymiana glikolu w rurach oraz wyczyszczenie pozostalych elementow urzadzenia.</p>
</div><!-- .entry-content -->

