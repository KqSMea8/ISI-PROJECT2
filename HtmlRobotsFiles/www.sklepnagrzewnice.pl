<p>Coraz wiecej nowobudowanych obiektow wyposazonych jest w urzadzenia klimatyzacyjne. Sluzyc one maja zapewnieniu wlasciwych warunkow pracy. Dla wielu uzytkownikow obiektow budowlanych (centrum handlowych, biurowcow itp.) zachodzi koniecznosc uruchamiania urzadzen klimatyzacyjnych, szczegolnie w okresie letnim, w celu utrzymania stalej optymalnej temperatury w pomieszczeniu. Nieumiejetne korzystanie z urzadzenia moze stac sie zrodlem zlego samopoczucia, a nawet wystepowania stanow chorobowych.</p>
<h2>Zasady, o ktorych nalezy pamietac</h2>
<p>Dla zapewnienia wlasciwych warunkow i komfortu prac, nalezy pamietac o kilku podstawowych zasadach, mianowicie:</p>
<ol>
<li>Klimatyzacja powinna byc ustawiona na wlasciwa temperature, optymalne wartosci temperatury to:<br />
&#8211; 21-22st. C latem,<br />
&#8211; 19-20st. C zima,</li>
<li>Ustawiajac wartosc temperatury nalezy pamietac, aby roznica pomiedzy temperatura na zewnatrz a wewnatrz pomieszczenia nie byla wieksza niz 6st. C. Zbyt duza roznica temperatur wplywa niekorzystnie na organizm czlowieka i moze doprowadzic do szoku termicznego oraz obnizenia odpornosci organizmu, a wiec zwiekszonej podatnosci na infekcje wirusowe.</li>
<li>Dobra praktyka jest wylaczanie co pewien czas klimatyzacji i wietrzenie pomieszczenia.<br />
<blockquote><p>Pamietaj!! Temperatura pracy w biurze nie moze byc nizsza niz 18st. C.</p></blockquote>
</li>
<li>Nigdy nie ustawiaj nawiewu z klimatyzatora bezposrednio na siebie.</li>
<li>Nie ustawiaj nawiewu na wartosci maksymalne. Zbyt duzy nawiew prowadzi do przeciagow, co wplywa niekorzystnie na organizm ludzki.</li>
<li>Nawadniaj organizm. Klimatyzacja sprawia, ze powietrze w pomieszczeniu jest suche, przez co dziala niekorzystnie na skore, i blony sluzowe drog oddechowych powodujac kaszel, chrypke, suchosc i uczucie drapania w gardle, zwiekszajac tym samym podatnosc na infekcje gornych drog oddechowych. Popijanie duzej ilosci wody malymi lykami zmniejsza ryzyko zapadania na infekcje gornych drog oddechowych.</li>
<li>Zbyt suche powietrze w pomieszczeniu wywolane czestym stosowaniem urzadzen klimatyzacyjnych moze doprowadzic rowniez do przesuszenia spojowek powodujac uczucie pieczenia, swedzenia i szczypania oczu. Skuteczna metoda zapobiegawcza jest stosowanie preparatow o wlasciwosciach nawilzajacych strukture oka (tzw. sztuczne lzy).<br />
<blockquote><p>Pamietaj!! Optymalna wilgotnosc w pomieszczeniach, w ktorych czlowiek czuje sie najlepiej miesci sie w granicach 40-60%.</p></blockquote>
</li>
<li>Legionelloza &#8211; Bakteria (Legionella pneumophila) rozwija sie w urzadzeniach klimatyzacyjnych, ktore nie sa regularnie poddawane okresowemu czyszczeniu i wymianie filtrow zgodnie z zaleceniami producenta. Objawy zakazenia Legionella przypominaja dolegliwosci grypopodobne, czyli bole glowy, miesni, goraczka, po jakims czasie dochodzi do suchego kaszlu.</li>
</ol>
