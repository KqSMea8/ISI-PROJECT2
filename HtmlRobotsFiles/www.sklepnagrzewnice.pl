<h1 class="entry-title"><a href="http://www.sklepnagrzewnice.pl/?p=346" rel="bookmark">Regularny serwis klimatyzacji to koniecznosc</a></h1>

<div class="entry-meta">
<span class="sep">Opublikowany </span><a href="http://www.sklepnagrzewnice.pl/?p=346" title="06:34" rel="bookmark"><time class="entry-date" datetime="2018-07-23T06:34:29+00:00">23 lipca 2018</time></a><span class="by-author"> <span class="sep"> przez </span> <span class="author vcard"><a class="url fn n" href="http://www.sklepnagrzewnice.pl/?author=1" title="Zobacz wszystkie wpisy, ktorych autorem jest admin" rel="author">admin</a></span></span>			</div><!-- .entry-meta -->

</header><!-- .entry-header -->

<div class="entry-content">
<p><img class="alignright size-medium wp-image-347" src="http://www.sklepnagrzewnice.pl/wp-content/uploads/2018/07/klimatyzator-serwis-300x198.png" alt="" width="300" height="198" srcset="http://www.sklepnagrzewnice.pl/wp-content/uploads/2018/07/klimatyzator-serwis-300x198.png 300w, http://www.sklepnagrzewnice.pl/wp-content/uploads/2018/07/klimatyzator-serwis-768x506.png 768w, http://www.sklepnagrzewnice.pl/wp-content/uploads/2018/07/klimatyzator-serwis-1024x675.png 1024w, http://www.sklepnagrzewnice.pl/wp-content/uploads/2018/07/klimatyzator-serwis-455x300.png 455w" sizes="(max-width: 300px) 100vw, 300px" />Kazdy rodzaj klimatyzacji wymaga regularnego serwisowania. Dotyczy sie to zarowno klimatyzacji samochodowej jak i w domach, mieszkaniach czy biurach. Klimatyzacja wymaga regularnego wykonywania trzech podstawowych czynnosci. Jest to czyszczenie, odgrzybianie i ozonowanie. Wszystkie te powyzsze czynnosci nalezy wykonac przed zblizajacym sie sezonem letnim. Dobrze zadbana klimatyzacja bedzie sluzyc w domu i biurze przez wiele dlugich lat, ale nieserwisowana klimatyzacja moze byc powaznym zagrozeniem dla zdrowia przebywajacych w jej otoczeniu osob.</p>
<h2>Klimatyzacja musi byc dobrze wyczyszczona</h2>
<p>Skupiajac sie na urzadzeniach chlodzacych w uzytku domowym trzeba napisac, ze najwazniejsza czynnoscia serwisowa jest czyszczenie. Klimatyzacja musi zostac wyczyszczona z wszelkich kurzow i drobnoustrojow. Trzeba ja dokladnie zdezynfekowac. Nastepna czynnoscia, jakiej musi byc poddawana klimatyzacja jest jej odgrzybianie. To rowniez podobnie do klimy w samochodzie. Przez urzadzenie chlodzace przeplywa bardzo duza ilosc wilgotnego powietrza. Wiadomo, ze wilgoc sprzyja powstawaniu roznego rodzaju grzybow. A domowa klimatyzacja musi byc wolna od jakichkolwiek zagrzybien. Ma to ogromne znaczenie dla bezpieczenstwa osob, ktore korzystaja z jej dzialania. Kompletnym serwisowaniem domowej klimy zajmuja sie wyspecjalizowani technicy chlodnictwa.</p>
<p style="text-align: center;"><iframe src="https://www.youtube.com/embed/4yTyQ2OoZsE" width="560" height="315" frameborder="0" allowfullscreen="allowfullscreen"></iframe></p>
<h2>Nowa klimatyzacja ma serwisowanie w pakiecie</h2>
<p>Zamawiajac klimatyzacje w firmie ja sprzedajaca mozemy liczyc na to, ze w pakiecie otrzymamy rowniez jej darmowe serwisowanie. Pewnie nie bedzie to na wiecznosc, ale mozemy liczyc na to, ze klimatyzacja bedzie dokladnie wyczyszczona po pierwszym roku od zalozenia. Zasadniczo kazda klimatyzacja wymaga tych prac serwisowych z czestotliwoscia jednego roku. Czesciej nie ma sensu tego robic, bo klimatyzacja w domu pracuje jedynie latem. W warunkach domowych klimatyzacja nie pelni przeciez roli grzewczej a jedynie chlodzaca. Przy zakupie urzadzenia do swojego domu warto zapytac o kwestie serwisowania. Nawet jesli po pierwszym roku klimatyzacja nie wymaga czyszczenia i ozonowania to i tak warto zlecic wykonanie tych czynnosci.</p>
