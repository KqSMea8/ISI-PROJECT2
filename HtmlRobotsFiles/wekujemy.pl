<p>Sloiki i&nbsp;butelki WECK sa wykonane z&nbsp;trwalego i&nbsp;grubego szkla. Wytrzymuja wielokrotne pasteryzowanie lub sterylizacje przetworow w&nbsp;wysokich temperaturach. Domowe dzemy i&nbsp;konfitury, poprawnie zawekowane w&nbsp;sloikach weck nabieraja wyjatkowego charakteru. Nasze sloiki z&nbsp;powodzeniem moga byc forma dla przetworow miesnych. Nasze "weczki" sluza jako naczynia do przekasek, dan glownych i&nbsp;deserow. Zostaly doskonale przyjete przez Szefow kuchni najlepszych restauracji, hoteli, pensjonatow i&nbsp;firm cateringowych. W&nbsp;ofercie naszej firmy znajdziecie Panstwo wyjatkowe, szklane butelki weck, dostepne w&nbsp;trzech rozmiarach: 290, 530 i&nbsp;1062 ml. Domowe soki lub kompoty przezentuja sie wspaniale w&nbsp;naszych gustownych butelkach. Szeroki otwor umozliwia latwe wlewanie i&nbsp;wylewanie gestych przecierow. Firma WECK dostarcza wysokiej klasy akcesoria niezbedne w&nbsp;wekowaniu. Nasze wolne od toksycznych fenoli uszczelki, wykonane sa z&nbsp;kauczuku pochodzacego z&nbsp;legalnych, kontrolowanych plantacji a&nbsp;spninki - sluzace do trwalego i&nbsp;bezpiecznego zamykania sloikow i&nbsp;butelek WECK - wykonane sa z&nbsp;nierdzewnej stali inox. Kapsle WECK beda bardzo pomocne w&nbsp;bezpiecznym przechowywaniu wczesniej otwartych sloikow z&nbsp;przetworami w&nbsp;lodowce. Wekowanie w&nbsp;sloikach WECK to prawdziwa przyjemnosc i&nbsp;satysfakcja! Nasza firma to ponad 140 lat tradycji w&nbsp;produkcji doskonalej jakosci szkla oraz akcesoriow pomocnych w&nbsp;domowym wekowaniu.</p>
