<h1 class="art_title"><a href="/art/id/792">Swiadczenie ,,Dobry Start"</a></h1>
<br class="clear" />
<p class="art_head"></p>
<p style="text-align: center;"><strong>Swiadczenie &bdquo;Dobry Start&rdquo;</strong></p>
<ul>
<li><strong>Dla kogo wsparcie?</strong></li>
</ul>
<p style="text-align: justify;">&nbsp;Swiadczenie &bdquo;Dobry Start&rdquo; przysluguje raz w roku, w wysokosci 300 zl na kazde dziecko uczace sie w szkole do&nbsp; ukonczenia 20 lub - w przypadku dzieci niepelnosprawnych - 24 roku zycia.</p>
<p style="text-align: justify;">Rodziny otrzymaja wsparcie bez wzgledu na doch&oacute;d.</p>
<ul>
<li><strong>Jak otrzymac swiadczenie &bdquo;Dobry Start&rdquo;?</strong></li>
</ul>
<p style="text-align: justify;">Aby otrzymac wsparcie, nalezy zlozyc wniosek. Moze to zrobic matka lub ojciec dziecka, opiekun prawny lub opiekun faktyczny dziecka.</p>
<ul>
<li><strong>Skladanie wniosk&oacute;w</strong></li>
</ul>
<p style="text-align: justify;">Wnioski o swiadczenie dobry start od 1 lipca 2018 r. do 31 lipca 2018 r. mozna skladac wylacznie droga elektroniczna przez strone Ministerstwa Rodziny empatia.mrpips.gov.pl oraz przez bankowosc elektroniczna, a od 1 sierpnia 2018 r. zar&oacute;wno droga tradycyjna (papierowa) jak i&nbsp; elektroniczna.</p>
<p style="text-align: justify;">Komunikat o miejscu skladania wniosk&oacute;w droga tradycyjna (papierowa) zostanie wydany w terminie p&oacute;zniejszym.</p>
<p><strong>Wazne! Wniosek nalezy zlozyc do 30 listopada.</strong></p>
<p><strong>Druki wniosk&oacute;w o przyznanie prawa do swiadczenia &bdquo;Dobry Start&rdquo; dostepne sa na stronie internetowej Ministerstwa Rodziny, Pracy i Polityki Spolecznej:</strong></p>
<p>&nbsp;</p>
<p style="text-align: center;"><span style="font-size: 18px;"><a href="https://www.mpips.gov.pl/download/gfx/mpips/pl/defaultopisy/11082/2/1/1.pdf" target="_blank" rel="noopener">Wz&oacute;r wniosku</a></span></p>
<p style="text-align: center;"><a href="https://www.mpips.gov.pl/download/gfx/mpips/pl/defaultopisy/11082/2/1/SDS_1Zv3_2.pdf" target="_blank" rel="noopener"><span style="font-size: 18px;">Zalacznik do wniosku</span></a></p>
<p style="text-align: center;">&nbsp;</p>
<ul>
<li><strong>Kiedy rodzina otrzyma wsparcie?</strong></li>
</ul>
<p style="text-align: justify;">W przypadku wniosk&oacute;w zlozonych w lipcu i sierpniu 2018 r., rodziny swiadczenie otrzymaja nie p&oacute;zniej niz do dnia 30 wrzesnia 2018 r.</p>
<p style="text-align: justify;">W przypadku wniosk&oacute;w zlozonych w kolejnych miesiacach ustalenie prawa do swiadczenia dobry start oraz wyplata przyslugujacego swiadczenia nastapi w terminie 2 miesiecy, liczac od dnia zlozenia wniosku wraz z dokumentami.</p>
<p style="text-align: center;"><strong>&nbsp;</strong><strong>Wazne<br /></strong></p>
<p style="text-align: justify;">Zgodnie z przepisami rozporzadzenia Rady Ministr&oacute;w z 30 maja 2018 r. w sprawie szczeg&oacute;lowych warunk&oacute;w realizacji rzadowego programu &bdquo;Dobry Start&rdquo; w miesiacu lipcu 2018 r. nie ma mozliwosci skladania wniosk&oacute;w o ustalenie prawa do swiadczenia &bdquo;Dobry Start&rdquo; w wersji papierowej.</p>


