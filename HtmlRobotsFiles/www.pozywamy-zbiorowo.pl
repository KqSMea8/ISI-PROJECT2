<p style="text-align: justify;">Szanowni Panstwo,</p>
<p style="text-align: justify;">29 listopada 2017 roku zlozylismy <strong><a href="http://pozywamy-zbiorowo.pl/?p=213">pozew zbiorowy przedsiebiorcow przeciwko</a></strong> <strong>VW Group Polska sp. z o.o.</strong> i czekamy na postanowienie Sadu o dopuszczalnosci rozstrzygania sporu w trybie pozwu zbiorowego. To juz drugi pozew zbiorowy w tej sprawie, bowiem kilka miesiecy temu zlozylismy w zwiazku z ,,afera spalinowa" w grupie VW <strong>pozew zbiorowy konsumentow</strong> (wszystkich tych, ktorzy nabyli pojazd prywatnie) .</p>
<p style="text-align: justify;">Nad tematem uzyskania odszkodowania na rzecz naszych klientow od grupy VW w zwiazku z ,,afera spalinowa" pracujemy juz od jesieni 2015 roku. Przez caly ten okres dazylismy do polubownego i w sposob nieprocesowy rozwiazania tematu. Odbylismy wiele spotkan, liczac na mozliwosc zainicjowania mediacji. Niestety okazalo sie, ze nikt nie byl zainteresowany taka sciezka postepowania. Dlatego tez, po wyczerpaniu drogi pozasadowej (niespornej), zlozylismy pozwy i teraz czekamy na postanowienia Sadow. Po wydaniu postanowien o dopuszczalnosci rozstrzygania sporu w trybie pozwu zbiorowego, bedzie mozna sie do tych pozwow &#8222;dopisywac&#8221;.</p>
