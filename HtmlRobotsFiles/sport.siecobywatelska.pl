<h1><a href="http://sport.siecobywatelska.pl/?page_id=52">Czym jest OMS?</a></h1><p align="JUSTIFY"><span style="font-family: Calibri, serif;">Obywatelski Monitoring Sportu jest dzialaniem z zakresu kontroli spolecznej o wymiarze ogolnopolskim.</span></p>
<p align="JUSTIFY"><span style="font-family: Calibri, serif;">Organizatorem monitoringu jest Siec obywatelska - Watchdog Polska. W monitoringu uczestnicza mieszkanki i mieszkancy polskich gmin, ktorzy chca sie dowiedziec, ,,co w sporcie piszczy" i sprawdzic, czy lokalne wladze godza sie, by mieszkancy je kontrolowali. Wszyscy uczestnicy i uczestniczki dzialaja na rzecz interesu publicznego!</span><span style="font-family: Calibri, serif;"> Niektorzy chca doprowadzic do bardziej powszechnego uprawiania sportu przez mieszkancow gmin. Innym z kolei zalezy na bardziej przejrzystym i racjonalnym rozdysponowywaniu srodkow publicznych na sport. Lokalni Straznicy i Strazniczki musza kierowac sie zasadami obiektywnosci, jawnosci, rzetelnosci i dobra wspolnego.</span></p>
<p align="JUSTIFY"><span style="font-family: Calibri, serif;">Celem monitoringu jest</span><span style="font-family: Calibri, serif;"><b> zwiekszenie roli mieszkancow w tworzeniu gminnej polityki sportowej.</b></span></p>
<p align="JUSTIFY"><span style="font-family: Calibri, serif;">Uczestniczki i uczestnicy badaja, co zwykli mieszkancy gminy maja z prowadzonej polityki sportowej i jak kazdy mieszkaniec moze na nia wplywac. W pierwszym kroku analizuja urzedowe dokumenty. Szukaja odpowiedzi na pytanie, jak realizacja konkretnych zapisow wplywa na zycie mieszkancow w zakresie sportu. Drugim krokiem jest stworzenie katalogu dzialan, za pomoca ktorych mieszkancy moga wplywac na polityke sportowa gminy. Uczestniczki i uczestnicy monitoringu wnioskuja o informacje publiczna, zbieraja dane od wladz gminy, mieszkancow i przedstawicieli organizacji sportowych.</span></p>
<p align="JUSTIFY"><span style="font-family: Calibri, serif;">Monitoring zostal rozpoczety w 18 lokalizacjach <!-- Tutaj podlinkowac strone ze spisem monitorowanych gmin -->przez 21 Lokalnych Strazniczek i Lokalnych Straznikow. Raporty i tresci narzedzia opublikowane w ramach monitoringu dotycza 8 gmin. Uczestnicy monitoruja gminy, w ktorych mieszkaja lub z ktorymi sa zwiazani w inny sposob. Dzialaja </span><span style="font-family: Calibri, serif;">samodzielnie lub ze wsparciem reprezentowanych przez siebie lokalnych organizacji, a takze mieszkancow i mieszkanek.</span><span style="font-family: Calibri, serif;"> Siec obywatelska zapewnia uczestnikom monitoringu dwa szkolenia, wsparcie eksperckie, internetowa ankiete do przechowywania i analizy danych oraz mozliwosc wymiany doswiadczen w zakresie obywatelskiej kontroli polityk sportowych gmin.</span></p>
<p align="JUSTIFY"><span style="font-family: Calibri, serif;">Monitoring bedzie trwal od pazdziernika 2013 roku do wrzesnia 2014 roku. Dane zebrane w trakcie trwania monitoringu zostana upublicznione na stronie Sieci obywatelskiej - Watchdog Polska.</span></p>
<p align="JUSTIFY"><span style="font-family: Calibri, serif;">Na pytania zwiazane z monitoringiem odpowiada opiekunka monitoringu Agnieszka Zdanowicz: <a href="mailto:agnieszka.zdanowicz@siecobywatelska.pl">agnieszka.zdanowicz@siecobywatelska.pl</a></span></p>    </div>
