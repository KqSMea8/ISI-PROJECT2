<p style="TEXT-ALIGN: center">WIOSENNA OBNIZKA CEN WEGLA !!!</p>
<p>Jestesmy Autoryzowanym sprzedawca PKW SA, Kompani Weglowej S.A.
oraz Katowickiego Holdingu Weglowego.</p>
<p>Oferujemy Panstwu:</p>
<ul>
<li>Najlepsze ceny w regionie.</li>
<li>Gwarancja calorocznych dostaw.</li>
<li>Mozliwosc odbioru wlasnym samochodem.</li>
</ul>
<p>Posiadamy w ciaglej sprzedazy kazdy sortyment wegla (kostka,
orzech, orzech II, groszek, mial oraz ekogroszki), sprzedaz wegla
workowanego <strong>Wegiel przesiewany!</strong></p>
<p>Zapewniamy transport oraz zaladunek.</p>
<p>Posiadamy specjalistyczny transport o ladownosci do 0,5t do
27t</p>
<p>Wspolpracujemy z klientami indywidualnymi, firmami oraz
instytucjami samorzadowymi. Naszym celem jest kompleksowa obsluga i
pelne zadowolenie klienta.</p>
<p>Prowadzimy <strong>sprzedaz ratalna</strong> Credit Agricole.
<strong>U nas zaplacisz karta platnicza</strong> .</p>
