<div id="WPROWADZENIE">
<p>Organem prowadzcym Szkolnego Punktu Konsultacyjnego przy Ambasadzie RP w Dublinie z siedzib w Limerick jest Minister Edukacji Narodowej, Orodek Rozwoju Polskiej Edukacji za Granic w Warszawie.<span class="strong"> Uczniowie SPK Limerick uczszczaj do szkoy polskiej jeden raz w tygodniu (w sobot lub niedziel) i realizuj uzupeniajcy plan nauczania.</span> <span class="strong">Jednoczenie uczniowie uczszczaj do szk irlandzkich.</span> Obecnie istnieje pi placwek tego typu na terenie Republiki Irlandii.</p>
<p>Przy szkole dziaa Stowarzyszenie "Polonijna Organizacja Owiatowa", ktra organizuje:</p>
<li>nauczanie w ograniczonym zakresie przedmiotw: matematyka oraz religia.</li>
<li>zajcia w klasie zerowej.</li>
<p>Zajcia te nie wchodz w struktury Orodka Rozwoju Polskiej Edukacji za Granic w Warszawie.</p>
<p>Uzupeniajcy plan nauczania obejmuje nauczanie pocztkowe, jzyk polski oraz wiedz o Polsce.</p>
