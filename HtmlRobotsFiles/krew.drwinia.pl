<h2 class="posttitle"><a href="http://krew.drwinia.pl/?p=2493">W Dziewinie zebrano krew dla dzieci</a></h2>
<div class="postmeta">by <a href="http://krew.drwinia.pl/?author=1" title="Wpisy, ktorych autorem jest krewdrwinia" rel="author">krewdrwinia</a> under <a href="http://krew.drwinia.pl/?cat=4" title="Zobacz wszystkie wpisy z kategorii &#8222;Akcje&#8221;" rel="category">Akcje</a></div>

<div class="clr16"></div>
<div class="postcontent"><p>Zarzad Gminnego Klubu Krwiodawcow PCK w Drwini sklada serdeczne podziekowanie tym dawcom krwi, ktorzy w miniona niedziele wzieli udzial w szlachetnej akcji majacej na celu zbiorke krwi dla dzieci przebywajacych w klinikach hematologii dzieciecej.</p>
<p>Na apel organizatorow odpowiedzialo zaledwie 14 osob!</p>
