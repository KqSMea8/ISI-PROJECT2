<h2 class="posttitle"><a href="http://krew.drwinia.pl/?p=2407">Akcja w Dziewinie</a></h2>
<div class="postmeta">by <a href="http://krew.drwinia.pl/?author=1" title="Wpisy, ktorych autorem jest krewdrwinia" rel="author">krewdrwinia</a> under <a href="http://krew.drwinia.pl/?cat=4" title="Zobacz wszystkie wpisy z kategorii &#8222;Akcje&#8221;" rel="category">Akcje</a></div>

<div class="clr16"></div>
<div class="postcontent"><p>W dniu 9 wrzesnia 2018r. na parkingu przed Osrodkiem Zdrowia w Dziewinie zaparkowal  autobus ,,Mobilny Punkt Poboru Krwi" z Regionalnego Centrum Krwiodawstwa i Krwiolecznictwa w Krakowie. Gminny Klub Honorowych Dawcow Krwi PCK w Drwini zorganizowal w nim otwarta akcje krwiodawstwa na terenie gminy Drwinia.  Na apel odpowiedzialo 13  osob. Po badaniu krew moglo oddac 11 dawcow (w tym 6 kobiet). Ogolem zebrano 4950 ml krwi pelnej, ktora trafila do banku krwi dla potrzeb mieszkancow gminy Drwinia oraz Malopolski.</p>
<p>Akcja zostala przeprowadzona przez Regionalne Centrum Krwiodawstwa i Krwiolecznictwa w Krakowie, ktorego pracownicy wykonywali czynnosci bezposrednio zwiazane z pobieraniem krwi i badaniem oraz rejestracja dawcow.</p>
<p>W obsludze akcji krwiodawstwa pracowali czlonkowie; Maria Klasa, Stanislaw Data, Ireneusz Zacharski oraz Leszek Machaj.</p>
<p>Zarzad Klubu HDK PCK w Drwini dziekuje mieszkancom gminy Drwinia, ktorzy zdecydowali sie podzielic czastka wlasnego zycia z najbardziej potrzebujacymi.</p>
<p>Kolejna otwarta akcja krwiodawstwa bedzie miala miejsce w dniu</p>
<p style="text-align: center;"><span style="color: #ff0000;"><strong>18 listopada w godz. 10 - 14 </strong></span></p>
<p style="text-align: center;"><span style="color: #ff0000;"><strong>w </strong> <strong>Osrodku Zdrowia w Dziewinie.</strong></span></p>

