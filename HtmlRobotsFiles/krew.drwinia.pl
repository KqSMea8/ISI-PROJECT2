<h2 class="posttitle"><a href="http://krew.drwinia.pl/?p=2369">Relacja z pielgrzymki</a></h2>
<div class="postmeta">by <a href="http://krew.drwinia.pl/?author=1" title="Wpisy, ktorych autorem jest krewdrwinia" rel="author">krewdrwinia</a> under <a href="http://krew.drwinia.pl/?cat=4" title="Zobacz wszystkie wpisy z kategorii &#8222;Akcje&#8221;" rel="category">Akcje</a></div>

<div class="clr16"></div>
<div class="postcontent"><p>Malopolski Oddzial Okregowy PCK po raz XII zorganizowal Ogolnopolska Pielgrzymke Krwiodawcow do Sanktuarium Maryjno-Pasyjnego w Kalwarii Zebrzydowskiej polaczona z akcja krwiodawstwa. Pielgrzymka odbyla sie 21 lipca 2018 roku, w ramach akcji ,,Wakacyjna kropla krwi".</p>
<p>Przedstawiciele honorowych dawcow krwi i szpiku kostnego z calej Polski i zagranicy pielgrzymowali do Sanktuarium Pasyjno-Maryjnego w Kalwarii Zebrzydowskiej.</p>
<p>W pielgrzymce rowniez uczestniczyla 50 osobowa grupa Honorowych Dawcow Krwi i sympatykow z Gminy Drwinia, ktora zorganizowal Gminny Klub HDK w Drwini. Pielgrzymow, ktorym przewodniczyl ks. pralat Jakub Gil, przywital w imieniu strozow Polskiej Jerozolimy o. Ludwik Zolnierczyk.</p>
<p>W darze oltarza krwiodawcy z Gminy Drwinia zlozyli kwiaty oraz serce w ksztalcie kropelki krwi. Po posilku uczestnicy pielgrzymki z Gminy Drwinia udali sie do Sanktuarium Jana Pawla II w Lagiewnikach.</p>
<p>Honorowi dawcy krwi uczestniczyli w Drodze Krzyzowej na kruzgankach DK. Wraz z nimi w kalwaryjskim Sanktuarium modlily sie wladze Polskiego Czerwonego Krzyza i duszpasterze krwiodawcow.</p>
<p>Pielgrzymka byla takze okazja do oddawania krwi. Oddalo ja 25 osob, co dalo 11,250 l. krwi.</p>

