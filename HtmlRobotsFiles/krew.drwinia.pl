<h2 class="posttitle"><a href="http://krew.drwinia.pl/?p=2421">Regionalny jubileusz krwiodawcow w Mikluszowicach</a></h2>
<div class="postmeta">by <a href="http://krew.drwinia.pl/?author=1" title="Wpisy, ktorych autorem jest krewdrwinia" rel="author">krewdrwinia</a> under <a href="http://krew.drwinia.pl/?cat=4" title="Zobacz wszystkie wpisy z kategorii &#8222;Akcje&#8221;" rel="category">Akcje</a></div>

<div class="clr16"></div>
<div class="postcontent"><p>W niedziele 14 pazdziernika na terenie gminy Drwinia w Zespole Szkol w Mikluszowicach odbyla sie regionalna uroczystosc polaczona z jubileuszem 60 - lecia PCK.<br />
Na zaproszenie organizatorow- Gminnego Klubu Honorowych Dawcow Krwi w Drwini przybyli krwiodawcy z rejonu powiatu bochenskiego i brzeskiego oraz sympatycy i dzialacze krwiodawstwa z Malopolski.</p>
<p>Wsrod zaproszonych gosci do Mikluszowic przybyli miedzy innymi: Wiceprezes Polskiego Czerwonego Krzyza Jerzy Kornaus oraz Malgorzata Pyka - Dyrektor Malopolskiego Oddzialu Okregowego Polskiego Czerwonego Krzyza.</p>
<p>Uroczystosc miala oficjalny charakter, tak wiec rozpoczela sie od wprowadzenie pocztu sztandarowego i hymnu. Nastepnie prowadzacy uroczystosc prezes Gminnego Klubu Honorowych dawcow Krwi w Drwini Stefan Styczen powital wszystkich zaproszonych gosci.<br />
Za dzialalnosc na rzecz promocji idei krwiodawstwa wiele osob uhonorowanych zostalo medalami i odznaczeniami. Najwyzszym odznaczeniem nadanym przez Prezydenta RP wyrozniona zostala Maria Klasa z Grobli. Wojewoda Malopolski, ktory mial przybyc na uroczystosc, nie dotarl i Pani Maria odbierze medal w innym terminie.</p>
<p>Podziekowania za wspolprace i propagowanie krwiodawstwa przez wladze samorzadowe otrzymali: burmistrz Stefan Kolawinski i wojt Jan Pajak. Podziekowano rowniez wszystkim dyrektorom z placowek oswiatowych na terenie gminy Drwinia.</p>
<p>Waznym akcentem bylo uhonorowanie seniorow krwiodawstwa, ktorzy z nieskrywanym wzruszeniem odbierali wyroznienia.</p>
