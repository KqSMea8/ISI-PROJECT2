<h2 class="posttitle"><a href="http://krew.drwinia.pl/?p=2529">Dzien Kobiet</a></h2>
<div class="postmeta">by <a href="http://krew.drwinia.pl/?author=1" title="Wpisy, ktorych autorem jest krewdrwinia" rel="author">krewdrwinia</a> under <a href="http://krew.drwinia.pl/?cat=4" title="Zobacz wszystkie wpisy z kategorii &#8222;Akcje&#8221;" rel="category">Akcje</a></div>

<div class="clr16"></div>
<div class="postcontent"><p><span style="font-size: medium;">W Dniu Kobiet w Osrodku Zdrowia w Dziewinie odbylo sie uroczyste spotkanie czlonkow Gminnego Klubu Honorowych Dawcow Krwi w Drwini. Wojt Gminy Drwinia Pan Jan Pajak oraz Prezes Klubu Pan Stefan Styczen zlozyli zyczenia i wreczyli uczestniczacym w spotkaniu kobietom kwiaty i slodkie upominki. </span></p>
<p><span style="font-size: medium;">Spotkanie bylo rowniez okazja do przeprowadzenia zebrania sprawozdawczego za miniony 2018 rok. Prezes Stefan Styczen podsumowal dzialania oraz finanse a takze przedstawil plan dzialan Gminnego Klubu HDK na rok 2019. Plan zostal przyjety do realizacji jednoglosnie. Wsrod dzialan glownym punktem jest uczczenie 100 lecia PCK oraz 35 lecia dzialalnosci Gminnego Klubu HDK. Spotkanie uswietnila swoja obecnoscia sekretarz Zarzadu Rejonowego PCK w Bochni Zofia Kusnierz, ktora podziekowala za zaangazowanie i aktywnosc Klubu a szczegolnie Prezesa.</span></p>
<p><span style="font-size: medium;">Zarzad Klubu dla wszystkich uczestnikow przygotowal slodki poczestunek podczas ktorego dyskutowano nad projektem tablicy pamiatkowej, organizacji obchodow oraz podejmowanym dzialaniami w biezacym roku. </span></p>
<p><span style="font-size: medium;">Na zakonczenie Prezes Klubu zwrocil sie do wszystkich uczestnikow o przekazanie 1% podatku na rzecz PCK oraz zaprosil na kolejna otwarta akcje honorowego oddawania krwi, ktora odbedzie sie 31 marca w Osrodku Zdrowia w Dziewinie w godz. od 10.00 do 14.00.</span></p>
<p>&nbsp;</p>
<p><span style="font-size: medium;">Opracowal</span></p>
