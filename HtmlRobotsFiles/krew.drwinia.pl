<h2 class="posttitle"><a href="http://krew.drwinia.pl/?p=2509">Wiosenna akcja krwiodawcow</a></h2>
<div class="postmeta">by <a href="http://krew.drwinia.pl/?author=1" title="Wpisy, ktorych autorem jest krewdrwinia" rel="author">krewdrwinia</a> under <a href="http://krew.drwinia.pl/?cat=4" title="Zobacz wszystkie wpisy z kategorii &#8222;Akcje&#8221;" rel="category">Akcje</a></div>

<div class="clr16"></div>
<div class="postcontent"><p><strong>W dniu 31 marca br. Gminny Klubu Krwiodawcow PCK w Drwini zorganizowal otwarta akcje  krwiodawstwa w ktorej wzielo udzial 21 dawcow. W zaparkowanym obok Osrodka Zdrowia w Dziewinie ambulansie RCKiK z Krakowa cennym darem moglo podzielic sie z potrzebujacymi 16 osob oddajac nieco ponad 7 litrow krwi. Wsrod dawcow bylo 7 kobiet.</strong></p>
<p>Akcja byla rowniez okazja do spotkania sie czlonkow Klubu i udekorowania Pana Lukasza Daty odznaka ,,Zasluzony Honorowy Dawca Krwi III stopnia". Odznaka taka przysluguje dawcom po oddaniu co najmniej 6 litrow krwi.</p>
<p>Organizatorzy  serdecznie dziekuja wszystkim osobom, ktore znalazly czas i podzielily sie ,,kropelka siebie" oraz obsludze ambulansu.</p>
<p>Kolejna akcja, tym razem <span style="color: #ff0000;"><strong>w Strzelcach Wielkich - 12 maja (niedziela) w godz. 11 - 15.</strong></span></p>

