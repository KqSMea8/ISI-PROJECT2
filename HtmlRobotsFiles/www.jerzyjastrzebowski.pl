<h1 class="entry-title">Biografia</h1>
</header>

<div class="entry-content">
<div id="mw-content-text" dir="ltr" lang="pl">
<p><b>Jerzy Michal Jastrzebowski</b> (ur. <a title="1937" href="http://pl.wikipedia.org/wiki/1937">1937</a> w <a title="Warszawa" href="http://pl.wikipedia.org/wiki/Warszawa">Warszawie</a>) - polski dzialacz opozycyjny, dzialacz <a title="Niezalezny Samorzadny Zwiazek Zawodowy &quot;Solidarnosc&quot;" href="http://pl.wikipedia.org/wiki/Niezale%C5%BCny_Samorz%C4%85dny_Zwi%C4%85zek_Zawodowy_%22Solidarno%C5%9B%C4%87%22">&#8222;Solidarnosci&#8221;</a>, publicysta.</p>
<p>Jest absolwentem <a title="Gimnazjum i Liceum im. Stefana Batorego w Warszawie" href="http://pl.wikipedia.org/wiki/Gimnazjum_i_Liceum_im._Stefana_Batorego_w_Warszawie">Liceum im. Stefana Batorego</a> i <a title="Uniwersytet Warszawski" href="http://pl.wikipedia.org/wiki/Uniwersytet_Warszawski">Uniwersytetu Warszawskiego</a>. W latach 1959-1981 byl pracownikiem <a title="Polskie Radio dla Zagranicy" href="http://pl.wikipedia.org/wiki/Polskie_Radio_dla_Zagranicy">Polskiego Radia dla Zagranicy</a>.</p>
<p>W 1981 zostal czlonkiem pierwszej Komisji Krajowej NSZZ &#8222;S&#8221;. W noc stanu wojennego uciekl z oblawy. Po aresztowaniu <a title="Zbigniew Romaszewski" href="http://pl.wikipedia.org/wiki/Zbigniew_Romaszewski">Zbigniewa Romaszewskiego</a> latem 1982 zostal koordynatorem warszawskiego podziemnego <a title="Radio Solidarnosc" href="http://pl.wikipedia.org/wiki/Radio_Solidarno%C5%9B%C4%87">Radia Solidarnosc</a>. W 1983 przebywal w <a title="Areszt Sledczy Warszawa-Mokotow" href="http://pl.wikipedia.org/wiki/Areszt_%C5%9Aledczy_Warszawa-Mokot%C3%B3w">wiezieniu przy Rakowieckiej</a>.</p>
<p>Publicysta-wspolpracownik ,,<a title="Kultura (miesiecznik)" href="http://pl.wikipedia.org/wiki/Kultura_%28miesi%C4%99cznik%29">Kultury</a>" paryskiej i ,,Zeszytow Historycznych" (1984-1990), ,,<a title="Tygodnik Powszechny" href="http://pl.wikipedia.org/wiki/Tygodnik_Powszechny">Tygodnika Powszechnego</a>" (1986-1991), ,,<a title="Rzeczpospolita (czasopismo)" href="http://pl.wikipedia.org/wiki/Rzeczpospolita_%28czasopismo%29">Rzeczpospolitej</a>" (1993-2005) i innych.</p>
<p>Fundator stypendiow m.in. w Studium Europy Wschodniej UW i w ramach Funduszu Nowego Tysiaclecia Jana Pawla II.</p>
</div>
