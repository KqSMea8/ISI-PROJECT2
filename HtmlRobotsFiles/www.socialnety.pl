<p>Dostep do sieci internetowej www spowodowal wzrost zainteresowania nauka programowania juz u najmlodszych dzieci. Nauczyciele rowniez uzyskali mozliwosc zwiekszenia swoich kompetencji z zakresu programowania. Dzieki dostepowi do sieci internetowej www dzieci zyskaly mozliwosc nauki programowania, na ktore to przedsiewziecie Ministerstwo wydalo ponad kilkaset milionow zlotych. Kompetencje programistyczne sa obecnie ksztaltowane juz w klasach 1-3 szkoly podstawowej. Zostaly rowniez rozszerzone o lasy wiejskie, ktore teraz rowniez moga uczyc sie programowania. Minister Anna Strezynska dodala, ze dzieci uzyskaly mozliwosc dorownania kompetencjom dzieci z innych panstw, a co za tym idzie, wiekszej szansy na rynku pracy w Polsce oraz Europie, a czasami nawet na swiecie.</p>



<p>Fakty mowia same za siebie. Panstwo wydalo niebagatelna sume: 124 miliony zlotych na ksztalcenie malych programistow, ktorzy wedlug wladz panstwa Polskiego, beda w przyszlosci ksztaltowaly gospodarke panstwa oraz rynek pracy. Dzieki mozliwosci zalatwiania spraw urzedowych przez internet obywatele sygnalizuja wzrost poczucia bezpieczenstwa.</p>



<p>Najnowsza technologia biznesowa jest zwiazana z doskonaleniem sie systemow informatycznych, ktore umozliwiaja coraz sprawniejsze i wygodniejsze poslugiwanie sie stronami informatycznymi. Instytucje zyskaly mozliwosc bardziej sprawnego przeplywu informacji miedzy nimi.</p>



<p>Obslugiwanie stron internetowych www ulatwia zdobywanie informacji, a co za tym idzie wyzszy poziom nauki domowej czy tez autoedukacji. Ministerstwo rowniez wskazuje, ze najnowsza technologia umozliwila zwiekszenie bezpieczenstwa danych poufnych, danych osobowych.</p>



