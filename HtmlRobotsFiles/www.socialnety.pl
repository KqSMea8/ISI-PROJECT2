<p>Minister podala do informacji publicznej rowniez plany na przyszlosc. Jednym z nich jest usprawnienie ekonomii i gospodarki panstwa przy wykorzystaniu najnowszej technologii informatycznej. Jest to realizacja tak zwanego planu Programu Zintegrowanej Informatyzacji. Kolejnym celem ministerstwa jest zwiekszenie mobilnosci spoleczenstwa oraz poszerzenie dostepu do informacji i wiedzy przy wykorzystaniu internetu o darmowym dostepie. Taki zabieg rozdzielony na przestrzeni lat, ma zwiekszyc koordynacje dzialu Technologii Informacyjnej, informatyki i technologii w ogole. Skutkiem ma byc zmniejszenie kosztow administracyjnych.</p>



<p>Obywatelom pozostaje czekanie na rozwoj wydarzen i nadzieje, ze planowane rozwiazania przyczynia sie rowniez do lepszej sytuacji na rynku pracy. Niewatpliwie cyfryzacja zycia publicznego i osobistego bedzie nadal rozwijana, a wkrotce przyspieszy zalatwianie spraw w urzedzie o jeszcze kilka minut.</p>

