</div><div  class="entry-media" >
<a href="http://www.socialnety.pl/?p=21" class="entry-thumb" title="Permalink to: &quot;Kolejna cenzura internetu?&quot;"><img width="780" height="551" src="http://www.socialnety.pl/wp-content/uploads/2017/06/cenzurowanie-sieci-780x551.jpg" class="attachment-WRT-post-image size-WRT-post-image wp-post-image" alt="" srcset="http://www.socialnety.pl/wp-content/uploads/2017/06/cenzurowanie-sieci-780x551.jpg 780w, http://www.socialnety.pl/wp-content/uploads/2017/06/cenzurowanie-sieci-300x212.jpg 300w, http://www.socialnety.pl/wp-content/uploads/2017/06/cenzurowanie-sieci-768x542.jpg 768w, http://www.socialnety.pl/wp-content/uploads/2017/06/cenzurowanie-sieci.jpg 960w" sizes="(max-width: 780px) 100vw, 780px" /></a>
</div>			<div class="entry-content">

<p>&nbsp;</p>
<h2>Jeszcze nie zdazyla dobrze ruszyc hazardowa cenzura sieci, a powstal juz pomysl na kolejny rejestr blokowanych domen.</h2>
<p>Idea ta wyszla od KNF, pragnacej wprowadzic blokowanie naduzyc finansowych, przede wszystkich tych zwiazanych z FOREX. Od przyszlego miesiaca, w naszym kraju maja zostac zablokowane witryny o charakterze hazardowym, za czym stoi Ministerstwo Finansow.</p>
<p>Przeforsowalo ono ustawe hazardowa bez zwazania na uwagi wprowadzane przez Ministerstwo Cyfryzacji. Sama ustawa weszla juz w zycie, lecz przepisy dotyczace blokowania wspomnianych stron zostana wdrozone dopiero od lipca. Na chwile obecna, <a href="https://hazard.mf.gov.pl/">lista cenzurowanych stron</a> zawiera juz ponad 320 pozycji.</p>
<p>&nbsp;</p>
<h3>Co chce zablokowac Komisja Nadzoru Finansowego? <a href="http://www.socialnety.pl/wp-content/uploads/2017/06/hazard-internet-polska.png"><img class="wp-image-24 size-medium alignright" src="http://www.socialnety.pl/wp-content/uploads/2017/06/hazard-internet-polska-150x300.png" alt="" width="150" height="300" srcset="http://www.socialnety.pl/wp-content/uploads/2017/06/hazard-internet-polska-150x300.png 150w, http://www.socialnety.pl/wp-content/uploads/2017/06/hazard-internet-polska.png 360w" sizes="(max-width: 150px) 100vw, 150px" /></a></h3>
<p>O ile projekt posiada ledwie 5 stron, to mozna w nim przeczytac, ze KNF posiada prawo do informowania o stronach powiazanych z dzialalnoscia, wobec ktorej zostalo zlozone zawiadomienie o popelnieniu przestepstwa. Chodzi jednak nie o zawiadomienia dowolne, lecz o te, ktore zostaly zlozone przez sama komisje.</p>
<p>Jezeli zatem dana strona dziala w jezyku polskim oraz jest przedstawiana na zasadzie okreslonej uslugi dla Polakow, to komisja moze przedstawic detale dotyczace jej domeny bez skladania wspomnianego podejrzenia.</p>
<p><a href="http://www.socialnety.pl/wp-content/uploads/2017/06/internet-ustawa-cenzura.png"><img class="aligncenter size-full wp-image-22" src="http://www.socialnety.pl/wp-content/uploads/2017/06/internet-ustawa-cenzura.png" alt="Cenzura polskiego internetu" width="600" height="331" srcset="http://www.socialnety.pl/wp-content/uploads/2017/06/internet-ustawa-cenzura.png 600w, http://www.socialnety.pl/wp-content/uploads/2017/06/internet-ustawa-cenzura-300x166.png 300w" sizes="(max-width: 600px) 100vw, 600px" /></a></p>
<p>&nbsp;</p>
<p><strong>Jak mozna przeczytac na 4 stronie projektu:</strong></p>
<p>&nbsp;</p>
<p><span style="text-decoration: underline;">Przedsiebiorca telekomunikacyjny swiadczacy uslugi dostepu do sieci Internet jest obowiazany do:</span></p>
<p>&nbsp;</p>
<p><em>1) nieodplatnego uniemozliwienia dostepu do stron internetowych wykorzystujacych nazwy domen internetowych wpisanych do rejestru poprzez ich usuniecie z systemow teleinformatycznych przedsiebiorcow telekomunikacyjnych, sluzacych do zamiany nazw domen internetowych na adresy IP, nie pozniej niz w ciagu 48 godzin od dokonania wpisu do rejestru;</em></p>
<p><em>2) nieodplatnego przekierowania polaczen odwolujacych sie do nazw domen internetowych wpisanych do rejestru do strony internetowej prowadzonej przez Komisje, zawierajacej komunikat, skierowany do odbiorcow uslugi dostepu do Internetu zawierajacy w szczegolnosci informacje o lokalizacji rejestru, wpisaniu szukanej nazwy domeny internetowej do tego rejestru;</em></p>
<p><em>3) nieodplatnego umozliwienia dostepu do stron internetowych wykorzystujacych nazwy domen wykreslonych z rejestru, nie pozniej niz w ciagu 48 godzin od wykreslenia nazwy domeny internetowej z rejestru.";</em></p>
<p>&nbsp;</p>
<p>Badzmy szczerzy - nie jest to skuteczne blokowanie. Nie chodzi jednak o to, by bylo ono szczelne, lecz aby stanowilo uprzykrzenie, z ktorym przecietnemu Nowakowi nie bedzie sie chcialo uzerac. Wiekszosc uzytkownikow nie bedzie bowiem sie bawic w konfiguracje VPN czy stosowanie Tora.</p>

</div>

