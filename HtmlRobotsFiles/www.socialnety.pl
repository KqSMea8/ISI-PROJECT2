<h2 class="entry-title"><a href="http://www.socialnety.pl/?p=21">Kolejna cenzura internetu?</a></h2>		<div class="entry-meta">
<span class="entry-author">By <a href="http://www.socialnety.pl/?author=2" title="Wpisy, ktorych autorem jest administrator" rel="author">administrator</a> </span>
