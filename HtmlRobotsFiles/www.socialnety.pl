<h2>Jak chronic dane na dysku?</h2>
<p>Ostatnimi czasy coraz czesciej slyszy sie o roznorodnych atakach na rozmaite firmy - nie tylko w kontekscie swiatowym, lecz zwlaszcza krajowym.</p>
<p>Od dawna wiadomo, ze robienie kopii zapasowej jest srodkiem ostroznosci, na ktory powinno sie zdecydowac kazde szanujace sie przedsiebiorstwo, jak i rowniez przecietny Kowalski, ktory dba o swoje dokumenty wraz z innymi danymi.</p>
<p>Kwestie te pomaga rozwiazac zaprojektowany przez Western Digital Corporation dysk przenosny o nazwie <em>My Passport Ultra.</em></p>
<p>Badania przeprowadzone przez Parks Associates dostarczaja danych, jakoby przecietne gospodarstwo domowe korzystalo z mniej wiecej trzech roznych chmur cyfrowych, jak np. <strong>Google Drive</strong>, <strong>Dropbox</strong>, <strong>OneDrive</strong>.</p>
<p>Poprzez korzystanie z opisywanego dysku <em>My Passport Ultra</em> wraz z oprogramowaniem <em>WD Discovery</em>, uzytkownik jest w stanie w prosty sposob tworzyc backupy danych z social mediow, jak np. Facebook oraz Instagram.</p>
<p><a href="http://www.socialnety.pl/wp-content/uploads/2017/06/szyfrowanie-danych-na-dysku.png"><img class="aligncenter size-full wp-image-41" src="http://www.socialnety.pl/wp-content/uploads/2017/06/szyfrowanie-danych-na-dysku.png" alt="szyfrowanie danych na dysku" width="425" height="370" srcset="http://www.socialnety.pl/wp-content/uploads/2017/06/szyfrowanie-danych-na-dysku.png 425w, http://www.socialnety.pl/wp-content/uploads/2017/06/szyfrowanie-danych-na-dysku-300x261.png 300w" sizes="(max-width: 425px) 100vw, 425px" /></a></p>
<p><strong>Jak to dziala?</strong></p>
<p>Nalezy pobrac wspomniane pliki na dysk - moga to byc zdjecia, filmy oraz inne multimedia. <em>WD Discovery</em> zapewnia sposobnosc do generowania lokalnych kopii danych, ktore nastepnie przesylane sa do chmury.</p>
<h2>Szyfrowany dysk</h2>
<p>My passport ultra to nie tylko komfortowe przechowywanie plikow oraz oryginalny design, lecz takze gwarancja zabezpieczenia zapisanych na nim danych.</p>
<p>Gwarancja ta opiera sie na 256-bitowym <strong>szyfrowaniu sprzetowym AES</strong> oraz wspominanym <em>WD Security</em>, za pomoca ktorego dane sa dodatkowo zabezpieczane.</p>
<p>&nbsp;</p>
<p style="text-align: center;"><em>W sytuacji, gdy do dyspozycji mamy tak wiele wspanialych platform i narzedzi do tworzenia oraz udostepniania zdjec, filmow i innych plikow, bardzo wazne jest, by klienci mogli w wygodny sposob zebrac w jednym miejscu wszystkie swoje dane. Skupilismy sie na dostarczeniu znakomitego rozwiazania, ktore laczy w sobie najwyzsza jakosc, wydajnosc i niezawodnosc. Efektem jest odswiezenie naszej oferty przenosnych dyskow. Nowe produkty wyrozniaja sie intuicyjnym w obsludze, wszechstronnym oprogramowaniem, wieksza pojemnoscia oraz pieknym wzornictwem, ktore doskonale wpisuje sie w dzisiejsze trendy.</em></p>
<p>&#8211; Sven Rathjen, wiceprezes ds. marketingu w Western Digital</p>
<p>&nbsp;</p>
