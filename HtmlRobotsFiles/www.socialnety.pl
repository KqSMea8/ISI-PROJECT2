<p>Mowa o serwisach takich jak Wykop.pl, Dziennik.pl, PAP.pl, Pajacyk.pl, Money.pl</p>
<p>Stosuja one bowiem skrypty, ktore wykradaja adresy email uzytkownikow z menadzerow hasel w przegladarkach, co pozwala im na poznanie ich dzialan w sieci internetowej.</p>
<p>Dzialanie narzedzi do sledzenia uzytkownikow w siedzi zostaly zbadane, a nastepnie opisane przez badaczy z Uniwersytetu Princeton - &gt;<a href="https://freedom-to-tinker.com/2017/12/27/no-boundaries-for-user-identities-web-trackers-exploit-browser-login-managers/">klik</a>&lt;</p>
<p>&nbsp;</p>
<h2><b>Jak dzialaja skrypty sledzace?</b></h2>
<p>Kazdy z nas na pewno zauwazyl, ze w momencie zakladania konta, lub gdy juz je masz i logujesz sie w okreslonym serwisie spolecznosciowym, twoja przegladarka zapyta Cie o zapisanie loginu wraz z haslem.</p>
<p>&nbsp;</p>
<h4>Czy warto?</h4>
<p>Kiedys odradzano to rozwiazanie, lecz w dniu obecnym, bezpieczenstwo danych przechowywanych przez przegladarki internetowe jest na znacznie wyzszym poziomie, zatem malo kto uwaza to rozwiazanie za negatywne, jako ze w znacznym stopniu ulatwia nam korzystanie z internetu.</p>
<p>Strony logowania z reguly sa pozbawione skryptow sledzacych, co jest czyms normalnym, po prostu przyzwoitym i etycznym.</p>
<p>Niestety, na poszczegolnych serwisach znajduja sie skrypty sledzace, ktorych funkcja jest wstrzykniecie niewidocznych formularzy logowania, ktore sa automatycznie wypelniane przez przegladarki i skutkuja wykradnieciem loginow / adresow email.</p>
<p>Proces zostal zobrazowany na ponizszej grafice:</p>
<p><a href="http://www.socialnety.pl/wp-content/uploads/2018/01/Skrypty-sledzace1.png"><img class="aligncenter size-full wp-image-59" src="http://www.socialnety.pl/wp-content/uploads/2018/01/Skrypty-sledzace1.png" alt="Skrypty sledzace" width="580" height="358" srcset="http://www.socialnety.pl/wp-content/uploads/2018/01/Skrypty-sledzace1.png 580w, http://www.socialnety.pl/wp-content/uploads/2018/01/Skrypty-sledzace1-300x185.png 300w" sizes="(max-width: 580px) 100vw, 580px" /></a></p>
<p>Weryfikujac tysiace rozmaitych skryptow sledzacych, badaczom udalo sie odnalezc dwa, ktore funkcjonuja w sposob opisany powyzej. Te skrypty to OnAudience, funkcjonujacy na behavioralengine.com oraz Adthink ze strony audienceinsights.net</p>
<p>&nbsp;</p>
<h2>Na ilu stronach odnalezione zostaly<b> skrypty sledzace?</b></h2>
<p>Sprawdzono milion najpopularniejszych, sposrod ktorych skrypty te mozna bylo odnalezc az na ponad tysiacu z nich.</p>
<p>Ciekawostka - skrypt OnAudience zostal odnaleziony takze na 45 polskich domenach, ktore zostana podane na koncu tego wpisu.</p>
<p>Kto za nie jednak odpowiada? Firma Cloud Technologies S.A z siedziba w Warszawie.</p>
<p>&nbsp;</p>
<p>Fragment wstrzykiwanego skryptu:</p>
<p><a href="http://www.socialnety.pl/wp-content/uploads/2018/01/Jak-dzialaja-skrpyty-sledzace.png"><img class="aligncenter size-full wp-image-58" src="http://www.socialnety.pl/wp-content/uploads/2018/01/Jak-dzialaja-skrpyty-sledzace.png" alt="Jak dzialaja skrpyty sledzace" width="580" height="487" srcset="http://www.socialnety.pl/wp-content/uploads/2018/01/Jak-dzialaja-skrpyty-sledzace.png 580w, http://www.socialnety.pl/wp-content/uploads/2018/01/Jak-dzialaja-skrpyty-sledzace-300x252.png 300w" sizes="(max-width: 580px) 100vw, 580px" /></a></p>
<p>&nbsp;</p>
<p>Skrypt obecny na domenach .pl odpowiada za wykradniecie loginu, ktorym najczesciej jest adres poczty elektronicznej. Nastepnie skrypt oblicza skrot MD5, po czym przesyla go do wlasnego serwera.</p>
<p>To jednak nie wszystko - OnAudience zapisuje dane takie jak obslugiwane typy plikow, lista wtyczek, strefa czasowa, wersja przegladarki, informacje dotyczace systemu operacyjnego oraz procesora.</p>
<p><i>Ale po co to wszystko?</i></p>
<p>Wykradzione dane przyczyniaja sie do wykreowania unikalnego wzorca, ktory tworzy sposobnosc do sledzenia dzialania przegladarki.</p>
<p>Zmieniasz komputer? Trudno, Twoj adres email jest juz zapisany, zatem korzystanie z innej przegladarki czy tez nawet urzadzenia nie ma znaczenia.</p>
