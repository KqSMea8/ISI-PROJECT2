<div class="clear"></div>
<div class="tab-content">
<div id="tab-1" class="current""><p>Sitodruk moze byc stosowany na wielu typach podlozy. Dzieki zastosowaniu bardzo wytrzymalych farb drukarskich, nadruki sa odporne m.in. na wilgoc, promieniowane UV, uszkodzenia mechaniczne czy pranie. Zastosowanie duzej ilosci farby umozliwia uzyskanie bardzo wyrazistych i w pelni nasyconych barw. Sitodruk moze byc wykonywany zarowno na powierzchniach plaskich, jak i cylindrycznych. Technologia sitodruku jest atrakcyjna cenowo, zarowno przy znakowaniu pojedynczych egzemplarzy, jak i w przypadku calych serii.</p>
</div><div id="tab-2""><p>Flex &#8211; metoda wykonywania nadrukow polegajaca na ploterowym wycieciu wzoru z foli pokrytej klejem i wgrzaniu jej za pomoca termoprasy w material. Nie wymaga przygotowywania matryc. Technika pozwala wykonac nadruki metaliczne, odblaskowe, bardzo gladkie lub uzyskac efekt sztywnosci. Najczesciej uzywana do ozdabiania nadrukow lub personalizacji odziezy.</p>
</div><div id="tab-3""><p>Jest to  rodzaj opakowania, skladajacego sie ze sztywnego spodu (np. kartonu) i sztywnego przezroczystego tworzywa sztucznego. Tworzywo sztuczne jest tak wyprofilowane, by mozliwe bylo zobaczenie tak zapakowanego produktu, ktory jest umieszczony miedzy tymi warstwami.</p>
<p>Typowe zastosowania: opakowania tabletek, baterii, akcesoriow komputerowych, samochodowych i innych. Opakowanie takie gwarantuje doskonala ekspozycje wyrobu, wysokie walory estetyczne oraz dobra ochrone przed wplywami zewnetrznymi na drodze od producenta do konsumenta. Dzieki temu wyrob jest latwo dostrzegany i chetnie kupowany.</p>
</div><div id="tab-4""><p>To drugi pod wzgledem popularnosci sposob uszlachetniania. Polega na nalozeniu na zadrukowana powierzchnie warstwy lakieru przy uzyciu techniki sitodruku w maszynach zwanych &#8222;lakierowkami&#8221;. Warstwa lakieru utrwalana jest za pomoca promieniowania UV, tworzac trwala strukture. Lakierowanie ma na celu uwydatnienie druku jak i zabezpieczenie przed scieraniem i wilgocia.</p>
<p>Sklad lakierow utrwalanych promieniami UV: modyfikowane zywice, fotoinicjatory, ktore inicjuja proces polimeryzacji, dodatki, ktore modyfikuja wlasnosci optyczne i inne. Lakiery UV schna poprzez tworzenie przez polimery (zywice) dlugich wiazan.</p>
<p>Lakiery UV mozna podzielic na blyszczace i matowe, podatne na klejenia, podatne na aplikacje folie hot-stampingowa, kationowe stosowane we fleksografii a nieszkodliwe fizjologicznie, czyli nadajace sie na artykuly spozywcze.</p>
