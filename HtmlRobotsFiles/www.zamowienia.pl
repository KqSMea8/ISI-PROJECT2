<div class="crew-descr" id="crew_descr_5"><p>Radca prawny w Kancelarii GJW. Jest odpowiedzialna za kompleksowe doradztwo w zakresie zamowien publicznych oraz prawa pracy. Klientom zapewnia biezace wsparcie w calym procesie udzielania zamowien publicznych oraz na etapie realizacji kontraktow. Sporzadza, negocjuje i opiniuje umowy cywilne oraz handlowe, zarowno w jezyku polskim jak i angielskim. Ocenia planowane transakcje pod katem zgodnosci z prawem ochrony konkurencji i konsumentow oraz ustawa o zwalczaniu nieuczciwej konkurencji. W ramach dotychczasowej praktyki zawodowej wspierala zarowno wykonawcow jak i zamawiajacych na wszystkich etapach udzielania zamowienia publicznego. Doradzala jednostkom sektora finansow publicznych, instytucjom kultury oraz przedsiebiorcom, w tym sektorowym. Uczestniczyla takze w procesie kontroli postepowan o udzielenie zamowienia publicznego z wykorzystaniem srodkow Mechanizmu Finansowego Europejskiego Obszaru Gospodarczego oraz Norweskiego Mechanizmu Finansowego, gdzie analizowala zasadnosc wymierzania korekt finansowych dla beneficjentow. Posiada wieloletnie doswiadczenie w obsludze prawnej przedsiebiorstw, ktore zdobywala pracujac w kancelariach prawnych oraz jako prawnik in-house w spolce o zasiegu miedzynarodowym. Legitymuje sie tez bogatym doswiadczeniem procesowym w zakresie sporow pracowniczych i gospodarczych. Z sukcesem reprezentowala przedsiebiorcow przed sadami powszechnymi oraz przed Krajowa Izba Odwolawcza. Radca prawny, czlonek Okregowej Izby Radcow Prawnych w Poznaniu. Ukonczyla studia magisterskie na Wydziale Prawa i Administracji Uniwersytetu Mikolaja Kopernika w Toruniu. Biegle wlada jezykiem angielskim.</p></div>
