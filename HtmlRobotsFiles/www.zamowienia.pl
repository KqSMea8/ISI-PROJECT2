<div class="crew-descr" id="crew_descr_1"><p>Zalozyciel Kancelarii GJW. Kieruje praktykami energetyczna oraz prawa zamowien publicznych i procedur zakupowych. Jego specjalizacja obejmuje rowniez obsluge procesow transakcyjnych (M&amp;A, pozyskiwanie finansowania, reorganizacje) oraz procesow inwestycyjnych. Od kilkunastu lat wspolpracuje z przedsiebiorstwami sektora energetycznego, w tym m.in. z jedna z wiodacych grup energetycznych w Polsce, ktora wraz z zespolem wspieral w wielu projektach biznesowych w obszarach: doradztwa regulacyjnego, zamowien publicznych, procedur zakupowych, procesow transakcyjnych, przygotowania oraz realizacji inwestycji, pozyskiwania finansowan, prawa konkurencji oraz projektow IT. Jest doswiadczonym specjalista w obszarze prawa zamowien publicznych. Zapewnial wsparcie na etapie przygotowania oraz przeprowadzania wielu zlozonych merytorycznie procedur o znaczacych wartosciach. Przeprowadzal szkolenia, bral udzial w projektowaniu oraz tworzeniu systemow e-procurement, wielokrotnie i z sukcesami reprezentowal zamawiajacych w postepowaniach odwolawczych prawa zamowien publicznych.  W latach 2005-2006 pelnil funkcje Atestatora Urzedu Zamowien Publicznych, uprawnionego do badania zamawiajacych sektorowych. Doradzal w wielu projektach o kluczowym znaczeniu dla sektora elektroenergetycznego. Legitymuje sie tez doswiadczeniem w zarzadzaniu i nadzorowaniu przedsiebiorstw, zdobytym w czasie pelnienia funkcji czlonka zarzadu i czlonka rad nadzorczych. Autor lub wspolautor wielu analiz i ekspertyz prawnych oraz publikacji w ,,Prawie Przedsiebiorcy", ,,Przewodniku Prawniczym", Biuletynie ,,Energia Elektryczna. Klient. Dystrybucja. Przesyl", Biuletynie ,,Pod Pradem", portalu RegulacjewEnergetyce.pl oraz wspolautor raportu Instytutu Kosciuszki ,,Analiza infrastruktury gazowej w Polsce z perspektywy przyszlych wyzwan energetycznych i rozwoju sektora gazu niekonwencjonalnego". Absolwent Wydzialu Prawa Uniwersytetu im. Adama Mickiewicza w Poznaniu oraz programu Master of Business Administration w WSB przy Uniwersytecie Ekonomicznym w Poznaniu i The Nottingham Trent University. Ukonczyl studium podyplomowe ,,Wzrost konkurencyjnosci polskiego sektora energetycznego poprzez programy pomocowe UE" w Centrum Integracji Europejskiej UAM oraz studium podyplomowe ,,Odnawialne zrodla energii" w Wyzszej Szkole Bankowej w Poznaniu. Wpisany na liste radcow prawnych Okregowej Izby Radcow Prawnych w Poznaniu.</p></div>
<div class="crew-descr" id="crew_descr_2"><p>Wspolnik w Kancelarii GJW. Kieruje zespolem obslugujacym procesy inwestycyjne. Specjalizuje sie rowniez w prawie energetycznym, zamowieniach publicznych oraz prawie pracy. Posiada duza praktyke procesowa. Legitymuje sie wieloletnim doswiadczeniem w doradztwie prawnym swiadczonym na rzecz przedsiebiorstw z branzy energetycznej oraz budowlanej. Swiadczyla pomoc prawna m.in. na rzecz duzej spolki obrotu energia elektryczna oraz lidera w branzy budownictwa komunikacyjnego i przemyslowego. Zapewniala obsluge prawna wielu inwestycjom liniowym takim jak budowa linii tramwajowej wraz z przebudowa ukladu drogowego w Bydgoszczy, budowa drogi wojewodzkiej w wojewodztwie dolnoslaskim, budowa obwodnicy Jeleniej Gory oraz wezla drogowego wraz z mostami, budowa mostu przez Wisle w wojewodztwie lubelskim. Wykonywala obsluge prawna na rzecz  spolek  z grupy kapitalowej PKP S.A. oraz podmiotow z zakresu administracji publicznej (reprezentacja w sprawach upadlosciowych, radach wierzycieli, sporach sadowych dotyczacych odpowiedzialnosci czlonkow zarzadu za zobowiazania publicznoprawne). Udzielala takze wsparcia prawnego w ramach obslugi korporacyjnej organow spolek prawa handlowego, projektowaniu ich struktur organizacyjnych oraz opracowywaniu aktow wewnetrznych. Wielokrotnie wystepowala przed Sadem Najwyzszym oraz sadami powszechnymi, prowadzila sprawy w postepowaniu egzekucyjnym i reprezentowala zamawiajacych oraz wykonawcow przed Krajowa Izba Odwolawcza. Posiada takze doswiadczenie w prowadzeniu negocjacji handlowych i zawieraniu kontraktow z Narodowym Funduszem Zdrowia. Aktualnie zaangazowana w obsluge prawna inwestycji realizowanych przez Generalna Dyrekcje Drog Krajowych i Autostrad. Sprawuje tez nadzor prawny nad realizacja inwestycji budowlanych prowadzonych przez Inspektorat Wsparcia Sil Zbrojnych za posrednictwem Rejonowego Zarzadu Infrastruktury , w tym takze w zakresie  gospodarowania nieruchomosciami bedacymi w trwalym zarzadzie Ministra Obrony Narodowej. Absolwentka Wydzialu Prawa i Administracji Uniwersytetu  Gdanskiego. Wpisana na liste radcow prawnych Okregowej Izby Radcow Prawnych w Bydgoszczy.</p></div>
<div class="crew-descr" id="crew_descr_3"><p>Radca prawny w Kancelarii GJW. Specjalizuje sie w obsludze projektow finansowanych z funduszy unijnych oraz zapewnia kompleksowa obsluge prawna procesow zakupowych realizowanych zarowno w trybach ustawy Prawo zamowien publicznych, jak i w oparciu o zasade konkurencyjnosci. Wspiera klientow w realizacji projektow dofinansowywanych ze srodkow UE, w ramach programow takich jak POIR, WRPO, RPLB, POIS i in. Od wielu lat jest zaangazowana w swiadczenie  wieloaspektowej pomocy prawnej  dla spolek sektora elektroenergetycznego. Jej specjalizacja obejmuje rowniez prawo pracy. Doradza w kwestiach zwiazanych z rekrutacja pracownikow, nawiazaniem, zmiana oraz ustaniem stosunkow pracy, zakazem konkurencji, wypadkami przy pracy, BHP, opracowywaniem wewnatrzzakladowych zrodel prawa pracy, motywacja placowa i swiadczeniami pozaplacowymi. Opracowywala oraz negocjowala kontrakty menedzerskie. Doradzala w toku procesu negocjowania ukladu zbiorowego pracy, sporzadzala protokoly dodatkowe do ZUZP oraz opracowywala wykladnie postanowien ZUZP. Wielokrotnie doradzala w procesie restrukturyzacji pracodawcow, w tym przejscia zakladu pracy w trybie art. 23(1) KP. Legitymuje sie wieloletnia praktyka zawodowa. Bezposrednio przed rozpoczeciem wspolpracy z Kancelaria GJW pelnila funkcje asystenta sedziego w Sadzie Rejonowym Poznan-Grunwald i Jezyce w Poznaniu w Wydziale Cywilnym oraz Wydziale Pracy. Reprezentuje klientow realizujacych projekty unijne w toku kontaktow z instytucjami finansujacymi (m.in. na etapie  kontroli wnioskow o platnosc). Posiada doswiadczenie w wystepowaniu  przed Krajowa Izba Odwolawcza. Legitymuje sie doswiadczeniem zdobytym w wielu zlozonych projektach biznesowych. Uczestniczyla w pracach zwiazanych z uzyskaniem zgody UOKiK na koncentracje zwiazana z utworzeniem przez Enea S.A. i Energa S.A. wspolnego przedsiebiorcy pod firma Elektrowni Ostroleka, swiadczyla uslugi doradztwa prawnego w toku wieloaspektowego projektu zakupowego realizowanego przez jedna z najwiekszych grup kapitalowych w Polsce. Rocznie zapewnia wsparcie prawne ponad stu procesom zakupowym, w tym m.in. w toku wielu projektow badawczo-rozwojowych. Doradzala w toku inwestycji zwiazanych z budowa farm wiatrowych, prowadzila szkolenia z zakresu prawa energetycznego, publikuje w prawniczych portalach tematycznych. Radca prawny. Absolwentka Wydzialu Prawa Uniwersytetu im. A. Mickiewicza w Poznaniu. Uczestniczyla w rocznym Programie Academic Exchange of America w USA.</p></div>
<div class="crew-descr" id="crew_descr_4"><p>Radca prawny w Kancelarii GJW. Specjalizuje sie w obsludze procesow inwestycyjnych oraz projektow finansowanych z funduszy unijnych. Zapewnia kompleksowa obsluge prawna procesow zakupowych realizowanych zarowno w trybach ustawy Prawo zamowien publicznych, jak i w oparciu o zasade konkurencyjnosci. Wspiera klientow w realizacji projektow dofinansowywanych ze srodkow UE, w ramach programow takich jak POIR, WRPO, RPLB, POIS i in. Posiada doswiadczenie w doradztwie prawnym swiadczonym na rzecz jednostek sektora finansow publicznych, spolek komunalnych, samorzadowych instytucji kultury i przedsiebiorstw z branzy energetycznej (m.in. operatora sieci gazowniczej). W ramach swojej dotychczasowej praktyki zawodowej doradzal wykonawcom jak i zamawiajacym na wszystkich etapach postepowania o udzielania zamowienia publicznego oraz na etapie realizacji zamowienia. Zapewnial obsluge projektow infrastrukturalnych (budynki kubaturowe) realizowanych przez jedna z poznanskich uczelni wyzszych. Legitymuje sie wieloletnia praktyka zawodowa. Przed rozpoczeciem wspolpracy z Kancelaria GJW pracowal na stanowisku urzedniczym w jednostce administracji rzadowej, gdzie samodzielnie prowadzil sprawy z zakresu zamowien publicznych, w tym m.in. odpowiadal za przygotowanie dokumentacji przetargowych oraz zapewnial wsparcie prawne w toku postepowan. Posiada doswiadczenie w nadzorowaniu przedsiebiorstw zdobyte podczas pelnienia funkcji czlonka rady nadzorczej. Wielokrotnie wystepowal przed sadami powszechnymi, Naczelnym Sadem Administracyjnym, Regionalna i Glowna Komisja Orzekajaca w sprawach o naruszenie dyscypliny finansow publicznych oraz reprezentowal zamawiajacych i wykonawcow przed Krajowa Izba Odwolawcza. Aktualnie zaangazowany w obsluge prawna inwestycji realizowanych w ramach projektow dofinansowanych ze srodkow Unii Europejskiej. Zapewnia takze biezaca pomoc prawna w toku postepowan zakupowych (w tym postepowan prowadzonych w trybach PZP). Absolwent Wydzialu Prawa Uniwersytetu im. Adama Mickiewicza w Poznaniu. Wpisany na liste radcow prawnych Okregowej Izby Radcow Prawnych w Poznaniu.</p></div>
