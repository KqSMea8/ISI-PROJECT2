<div align="center"><span style="font-size: small;"><br /> <span style="color: #0000ff;"><strong><span style="color: #000000;">www.pupslubice.pl</span></strong><br /> </span></span><a target="_blank"><br /> </a><span style="font-size: small;">Godziny pracy: <br /> <br /> Pon.-Ptk 7.00-15.00 <br /> </span><hr /><br />
<p style="text-align: left;"><strong>Ochrona Danych Osobowych </strong></p>
<p style="text-align: left;">&nbsp;</p>
<p style="text-align: left;">Zgodnie z art. 13 Rozporzadzenia Parlamentu Europejskiego i Rady (UE) 2016/679 z dnia 27 kwietnia 2016 r. w sprawie ochrony os&oacute;b fizycznych w zwiazku z przetwarzaniem danych osobowych i w sprawie swobodnego przeplywu takich danych oraz uchylenia dyrektywy 95/46/WE (Dz. Urz. UE.L Nr 119),zwanego dalej RODO Powiatowy Urzad Pracy w Slubicach informuje, ze:</p>
<p style="text-align: left;">Administratorem danych, w tym danych osobowych jest Powiatowy Urzad Pracy&nbsp; majacy siedzibe przy ul. Mickiewicza 3, 69-100 Slubice, reprezentowany przez Dyrektora Powiatowego Urzedu Pracy w Slubicach Pania Genowefe Borowiak.</p>
<p style="text-align: left;"><strong>Dla os&oacute;b bezrobotnych lub poszukujacych pracy:</strong></p>
<p style="text-align: left;">-Dane osobowe przetwarzane sa zgodnie z art. 6 ust. 1 pkt e RODO dla cel&oacute;w realizacji zadania publicznego ujetego w ustawie z dnia 20 kwietnia 2004 r. o promocji zatrudnienia i instytucjach rynku pracy (Dz. U. z 2017r. poz.1065, z p&oacute;zn. zm.) i aktach wykonawczych wydanych na jej podstawie.</p>
<p style="text-align: left;">-Odbiorca danych osobowych sa podmioty, kt&oacute;rym Administrator przekazuje dane osobowe na podstawie przepis&oacute;w prawa.</p>
<p style="text-align: left;">-Dane osobowe zwiazane z realizacja zadania publicznego beda przechowywane przez okres trwania rejestracji w Urzedzie jako osoby bezrobotnej/poszukujacej pracy oraz w obowiazkowym okresie przechowywania dokumentacji zwiazanej z rejestracja osoby bezrobotnej, ustalanym zgodnie z odrebnymi przepisami.</p>
<p style="text-align: left;">-Osobie bezrobotnej lub poszukujacej pracy przysluguje prawo dostepu do tresci swoich danych oraz ich poprawienia i sprostowania.</p>
<p style="text-align: left;">-Osobie bezrobotnej lub poszukujacej pracy przysluguje prawo wniesienia skargi do organu nadzorczego wlasciwego do przetwarzania danych osobowych.</p>
<p style="text-align: left;"><strong>Dla pracodawc&oacute;w:</strong></p>
<p style="text-align: left;">-Dane, w tym dane osobowe przetwarzane sa zgodnie z art. 6 ust. 1 pkt e RODO dla cel&oacute;w realizacji zadania publicznego ujetego w ustawie z dnia 20 kwietnia 2004 r. o promocji zatrudnienia i instytucjach rynku pracy oraz aktach wykonawczych wydanych na jej podstawie.</p>
<p style="text-align: left;">-Odbiorca danych osobowych sa podmioty, kt&oacute;rym Administrator przekazuje dane osobowe na podstawie przepis&oacute;w prawa.</p>
<p style="text-align: left;">-Dane, w tym dane osobowe zwiazane z realizacja form wsparcia dla pracodawc&oacute;w przechowywane sa przez okres niezbedny do zakonczenia danej formy wsparcia oraz przez okres przechowywania dokumentacji okreslony w odrebnych przepisach.</p>
<p style="text-align: left;">-Pracodawcy przysluguje prawo dostepu do tresci swoich danych oraz ich poprawienia i sprostowania.</p>
<p style="text-align: left;">-Pracodawcy przysluguje prawo wniesienia skargi do organu nadzorczego wlasciwego do przetwarzania danych osobowych.</p>
<p style="text-align: left;">Dane kontaktowe do Administratora Bezpieczenstwa Informacji/Inspektora Ochrony Danych: <a href="mailto:organizacyjny@pupslubice.pl">organizacyjny@pupslubice.pl</a></p>
