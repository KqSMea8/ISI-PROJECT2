<blockquote>
<div align="center">
<div style="text-align: left;"><a href="http://www.pupslubice.pl"><img style="display: block; margin-left: auto; margin-right: auto;" title="www.pupslubice.pl" src="/wysiwyg/FileUpload/pics/logoWhiteBIP.jpg" alt="www.pupslubice.pl" width="369" height="95" /></a></div>
