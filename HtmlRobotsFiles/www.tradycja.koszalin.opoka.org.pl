<h2 class="entry-title">Msza Swieta</h2>

<div class="entry-content">
<p><strong>Uwaga! </p>
<p>14 kwietnia nie ma Mszy Swietej</p>
<p>Najblizsza Msza Swieta w Koszalinie:<br />
 21 kwietnia (Wielkanoc) - godz. 17.30</p>
<p>Prosimy o pomoc w przekazaniu informacji zainteresowanym wiernym!</strong></p>
<p>Msza Swieta w formie nadzwyczajnej rytu rzymskiego (tzw. trydencka) odprawiana jest w kazda niedziele  o godzinie 18.00 w kosciele sw. Jozefa Oblubienca Najswietszej Maryi Panny w Koszalinie. Kosciol sw. Jozefa znajduje sie nieopodal katedry, przy ul. Biskupa Czeslawa Domina 10 w Koszalinie, jest kosciolem pomocniczym w rzymskokatolickiej parafii Niepokalanego Poczecia Najswietszej Maryi Panny w Koszalinie.<br />
Na terenie Diecezji Koszalinsko-Kolobrzeskiej Msze Swiete stale odprawiane sa wylacznie w Koszalinie i w Pile. Tzw. msza trydencka jest nadzwyczajna forma rytu rzymskiego, odprawiana jest w jezyku lacinskim, w nieco innych szatach liturgicznych, kazanie gloszone jest z ambony. Mszaliki dla wiernych (w jezyku lacinskim) mozna pobrac przy wejsciu. Zwyczajem jest zajmowanie przez niewiasty lawek po stronie lekcji, a mezczyzn po stronie ewangelii. Niewiasty maja, w przeciwienstwie do mezczyzn, glowe nakryta.</p>
