<h1 style="text-align: justify;">&nbsp;</h1>
<h1 style="text-align: justify;"><strong>O nas</strong></h1>
<p style="text-align: justify;"><strong>Stowarzyszenie Faros</strong> istnieje od maja 2003 roku. Zostalo zalozone przez 3 mlode dziewczyny, kt&oacute;re zauwazyly problem braku organizacji pozarzadowej zajmujacej sie problematyka <strong>HIV/AIDS</strong> na terenie wojew&oacute;dztwa podlaskiego.</p>
<p style="text-align: justify;">Faros zostal utworzony z inicjatywy mlodych ludzi dla innych mlodych ludzi potrzebujacych wsparcia. Przez kolejne lata zebrala sie grupa aktywnych czlonk&oacute;w, kt&oacute;ra zaczela tworzyc projekty potrzebne do zwalczania epidemii HIV/AIDS. Dotarlismy ze swoimi dzialaniami do wielu miejsc i wielu os&oacute;b, kt&oacute;re wczesniej nie mialy okazji spotkac sie z ta tematyka. Programy profilaktyczne w szkolach, w zakladach karnych, na uczelniach wyzszych, koncerty, eventy okazaly sie bardzo potrzebne i bardzo dobrze przyjete przez odbiorc&oacute;w.</p>
<p style="text-align: justify;"><strong>Chcemy aby nasza dzialalnosc rosla w sile i poruszala r&oacute;wniez inna problematyke zwiazana z promocja zdrowia czy wsparciem spolecznym.<br /></strong></p></div>


