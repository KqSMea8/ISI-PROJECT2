<h2><a href="http://sep.piotrkow.pl/?p=819" title="Permalink to Relacja z wyjazdu techniczno-turystycznego Oddzialu Piotrkowskiego SEP." rel="bookmark">Relacja z wyjazdu techniczno-turystycznego Oddzialu Piotrkowskiego SEP.</a></h2>
<span class="postdate">piatek, Listopad 30, 2018</span>
<p><p><strong>LUBELSKIE CIEPLO I LWOWSKIE ZABYTKI</strong> Stalym elementem dzialalnosci Oddzialu Piotrkowskiego Stowarzyszenia Elektrykow Polskich sa wyjazdy techniczno-turystyczne. Lacza one w sobie aspekty poznawcze, integracyjne i turystyczne. W biezacym roku Zarzad Oddzialu zorganizowal wyjazd do Elektrocieplowni Lublin-Wrotkow, w ktorej zainstalowany jest nowoczesny blok gazowo-parowy. Przedluzeniem tego kierunku byl wyjazd do Lwowa. Inspiracja do tej wizyty bylo zblizajace sie 100-lecie SEP i fakt, ze wsrod zalozycieli Stowarzyszenia byli lwowscy elektrycy.</p>
<p>Liczaca 47 osob grupa uczestnikow wyjazdu rozpoczela pierwszy dzien imprezy od zapoznania sie z trescia wydanego przez nalezace do Oddzialu Piotrkowskiego Kolo SEP przy PGE GiEK S.A. Oddzial Elektrownia Belchatow Zeszytu Naukowo-Technicznego. Kolejny tegoroczny zeszyt poswiecony zostal budowie turbiny gazowej, technologii bloku gazowo-parowego oraz Elektrocieplowni Lublin-Wrotkow bedacej Oddzialem PGE GiEK S.A. Material ten pozwolil na wstepne zapoznanie sie z obiektem, ktorego zwiedzanie mialo rozpoczac sie wkrotce.</p>
<p>W Elektrocieplowni uczestnikow wyjazdu przywital jej Dyrektor Techniczny. Dokonal krotkiego wprowadzenia oraz zaprezentowal osoby, ktore przejely prowadzenie ciagu dalszego wizyty. Zanim ta kontynuacja nastapila, Prezes Oddzialu Piotrkowskiego SEP, Jan Musial wreczyl Dyrektorowi Medal Oddzialu Piotrkowskiego w podziekowaniu za dotychczasowa wspolprace. Nastepnie nastapila krotka prezentacja zakladu oraz obligatoryjne szkolenie BHP przed zwiedzeniem obiektu. W ramach zwiedzania zaprezentowane zostaly najwazniejsze obiekty elektrocieplowni, ze szczegolnym akcentem polozonym na blok gazowo-parowy. Tego rodzaju bloki produkujace zarowno energie elektryczna jak i cieplna na potrzeby grzewcze charakteryzuja sie wysoka sprawnoscia i niska emisja zanieczyszczen do powietrza, w szczegolnosci dwutlenku wegla. W obecnej sytuacji, kiedy dekarbonizacja produkcji energii staje sie obowiazujacym trendem, stosowanie tej technologii nabiera duzego znaczenia. <a href="http://sep.piotrkow.pl/wp-content/uploads/2018/11/Wycieczka-tt-Lwow-00.jpg"><img class="aligncenter wp-image-823" src="http://sep.piotrkow.pl/wp-content/uploads/2018/11/Wycieczka-tt-Lwow-00-300x225.jpg" alt="Wycieczka tt Lwow 00" width="400" height="300" /></a></p>
<h6>Grupa uczestnikow wyjazdu po zwiedzeniu PGE G i EK S.A. Oddzial Elektrocieplownia Lublin-Wrotkow.</h6>
<p>Kolejny dzien przypomnial nam, ze istnieja jeszcze w Europie granice w tradycyjnym rozumieniu. Po wszystkich formalnosciach dojechalismy do lezacej w niewielkiej odleglosci od granicy Zolkwi. W tej niewielkiej miejscowosci, ktora znaczaco zaistniala w historii Polski znajduja sie liczne, wymagajace pilnej konserwacji zabytki. Prace konserwatorskie wspomagane sa przez instytucje Rzeczypospolitej Polskiej oraz osoby prywatne. Wsrod tych ostatnich znalezli sie miedzy innymi koledzy z organizacji inzynierskich ze wspomnianej juz belchatowskiej elektrowni, w tym rowniez SEP-owcy oraz wlasciciele belchatowskich firm. Pomoc ta uwidoczniona jest na pamiatkowej tablicy w Kolegiacie p.w. sw. Wawrzynca.</p>
<p>Wieczorem zaaranzowane zostalo spotkanie kolezenskie. Dzialajac w kolach bazujemy na bezposrednich kontaktach w otoczeniu. Oddzial ze wzgledu na rozleglosc terytorialna nie sprzyja tak scislym powiazaniom. Dlatego spotkanie posluzylo lepszemu poznaniu sie i poglebieniu wiezi. Bedziemy w 2019 roku obchodzic 40-lecie Oddzialu Piotrkowskiego SEP wiec takie zaciesnienie kontaktow stanowic bedzie moglo punkt wyjscia do realizacji wspolnych przedsiewziec. A mile spedzenie czasu w dobrym towarzystwie na dlugo pozostanie w pamieci.</p>
<p>Kolejny dzien rozpoczal sie od wizyty w miejscu tradycyjnie odwiedzanym przez gosci z Polski, na cmentarzu, na Lyczakowie. Kojarzony jest on powszechnie z miejscem pochowku obroncow Lwowa ale na terenie tej nekropolii odnajdujemy miejsca spoczynku wielu osob, ktore pozostawily slad w naszej historii. Jako czlonkowie Stowarzyszenia Elektrykow Polskich zaczelismy wedrowke po cmentarzu od poszukiwania grobu profesora Romana Dzieslewskiego, patrona roku 2013 w SEP. Przy grobie Kolega Jan Musial przedstawil postac profesora Dzieslewskiego i jego zaslugi dla SEP, a nastepnie zapalil znicz Oddzialu Piotrkowskiego SEP. Zlozylismy rowniez bialo-czerwony wieniec czczac pamiec tej niezwykle zasluzonej dal polskiej elektryki osoby. <a href="http://sep.piotrkow.pl/wp-content/uploads/2018/11/Wycieczka-tt-Lwow-02.jpg"><img class="aligncenter wp-image-826" src="http://sep.piotrkow.pl/wp-content/uploads/2018/11/Wycieczka-tt-Lwow-02-300x225.jpg" alt="Wycieczka tt Lwow 02" width="400" height="300" /></a></p>
<h6>Uczestnicy wyjazdu po zlozeniu wienca na grobie profesora Romana Dzieslewskiego.</h6>
<p>Kolejne znicze Oddzialu Piotrkowskiego SEP zapalilismy na Cmentarzu Obroncow Lwowa, bedacym czescia Cmentarza Lyczakowskiego, przy mogile Nieznanego Zolnierza oraz na grobie najmlodszego z Orlat, niespelna dziesiecioletniego Jasia Kukawskiego. Po dokonaniu przez Prezesa Oddzialu wpisu w ksiedze pamiatkowej udalismy sie na dalsze poszukiwanie polskich sladow. <a href="http://sep.piotrkow.pl/wp-content/uploads/2018/11/Wycieczka-tt-Lwow-03.jpg"><img class="aligncenter wp-image-828" src="http://sep.piotrkow.pl/wp-content/uploads/2018/11/Wycieczka-tt-Lwow-03-300x225.jpg" alt="Wycieczka tt Lwow 03" width="400" height="300" /></a></p>
<h6>Kolega Jan Musial z malzonka Grazyna wpisuja sie do ksiegi pamiatkowej.</h6>
<p>Sposrod wielu z nich wyroznia sie miejsce wiecznego spoczynku Marii Konopnickiej. Pomiedzy wieloma zniczami zapalanymi przez wdziecznych rodakow znalazl miejsce rowniez nasz znicz. Po jego zlozeniu nasz przewodnik zainicjowal odspiewanie ,,Roty", nieformalnego hymnu polonii autorstwa Marii Konopnickiej. Nie sposob opisac wszystkich miejsc na tym cmentarzu wartych zatrzymania sie i chwili zadumy. <a href="http://sep.piotrkow.pl/wp-content/uploads/2018/11/Wycieczka-tt-Lwow-04.jpg"><img class="aligncenter wp-image-827" src="http://sep.piotrkow.pl/wp-content/uploads/2018/11/Wycieczka-tt-Lwow-04-225x300.jpg" alt="Wycieczka tt Lwow 04" width="300" height="400" /></a></p>
<h6>Kolega Jerzy Antczak zapala znicz na grobie Marii Konopnickiej.</h6>
<p>Centrum Lwowa to miejsce pelne zabytkow. Niektore z nich odzyskaly juz swoja swietnosc, a niektore jeszcze czekaja na swoja kolej, na odrestaurowanie. Niewatpliwie najcenniejszym z nich jest gmach Opery, ktory moglismy poznac rowniez od wewnatrz. Nastepnie odwiedzilismy lwowskie pomniki, koscioly i wiele innych charakterystycznych dla tego miasta miejsc.</p>
<p>Ostatni dzien we Lwowie to wizyta na Wysokim Zamku, na punkcie widokowym z ktorego widac cale miasto. Sloneczna pogoda pozwolila jeszcze raz, tym razem z gory przypomniec wszystkie odwiedzone miejsca. Po tej prezentacji pozostalo tylko pozegnanie przewodnika i powrot do domu.</p>
<p>Mozna stwierdzic, ze wyjazd spelnil nasze oczekiwania. Nowoczesna technika energetyczna zaprezentowana z elektrocieplowni w Lublinie dopelniona zostala wizyta w miejscach pelnych polskich sladow, z nuta przezycia patriotycznego. Do tego wspolnie spedzany czas, ktory pozwala potwierdzic ze ,,SEP jest stowarzyszeniem przyjaciol".</p>
<p style="text-align: right;"><em>Jerzy Antczak</em></p>
