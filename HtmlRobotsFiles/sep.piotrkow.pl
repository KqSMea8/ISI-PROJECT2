<p>W tegorocznej edycji zwrocono uwage na zrownany poziom zgloszonych prac w kategorii stron WWW wsrod uczniow szkol podstawowych, jesli chodzi o techniki informatyczne, dostrzegajac zroznicowanie zawartosci merytorycznej. Zaobserwowano wzrost wykorzystania gotowych komponentow i modulow powszechnie dostepnych w sieci internet, co jest dowodem postepu w wykorzystaniu z intelektualnej zdobyczy spolecznosci informatycznej.</p>
