<li>Jolanta Trojan- sekretarz</li>
</ol>
<p>na posiedzeniu w dniu 28 maja b.r. dokonala oceny zgloszonych prac.</p>
<p>Wplynelo 13 prac. Prace zgloszone zostaly w czterech z pieciu regulaminowych kategoriach, czyli:</p>
<ul>
<li>programow uzytkowych,</li>
<li>programow edukacyjnych,</li>
