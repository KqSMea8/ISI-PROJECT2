<div class="column-about about-background">
<h3>Kim jestesmy?</h3>
<p>Firma Tuin zajmuje sie kreatywnym i innowacyjnym projektowaniem ogrodow i terenow zielonych dostosowanych do Twoich potrzeb i budzetu. Dzialamy na terenie calej Polski. Tworzymy piekne i wspaniale ogrody, poczawszy od wspolczesnych po tradycyjne architektonicznie. Nasadzenia zaprojektowane sa w sposob oryginalny, majacy na celu stymulowanie Twoich zmyslow przez caly rok.</p>
<p>Jezeli szukasz inspiracji, doswiadczenia w projektowaniu, chcesz wydobyc caly potencjal ze swojego ogrodu - duzego czy malego - chetnie pomozemy, aby stworzyc projekt ogrodu, ktory jest idealny dla Ciebie- taki, ktory bedzie dodawal wartosci dla Twojego domu i przyniesie wiele lat przyjemnosci.</p>
<p>Z dobrym okiem do detalow i silnym naciskiem na jakosc i rzemioslo, wspolpracujemy z zaufanymi firmami zajmujacymi sie wykonaniem ogrodow, zakladaniem automatycznego nawadniania, oswietlenia, tworzeniem nawierzchni ogrodowych, ktore beda budowac Twoj ogrod w najwyzszym standardzie.</p>
