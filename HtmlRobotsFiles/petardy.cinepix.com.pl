<h2 class="post-title"><a
href="http://www.petardy.cinepix.com.pl/index.php?post/2009/03/14/Petardy">Petardy</a></h2>

<p class="post-info">By admin mike    on Saturday, March 14 2009, 00:25        - <a href="http://www.petardy.cinepix.com.pl/index.php?category/fajerwerki">fajerwerki</a>
</p>




<div class="post-content"><p>Ludzie bardzo czesto chodza na wszelakie pokazy, kiedy moga zobaczyc fajerwerki. Kazdemu sie one bardzo podobaja, chociaz wielu ludzi boi sie ich. Oczywiscie nie sa one calkowicie bezpieczne., jednak zachowujac sie odpowiednio w stosunku do nich nie ma sie czego obawiac. Kazdy kto tylko jest juz odpowiedzialny moze zakupic fajerwerki i urzadzic samodzielnie taki pokaz. Nie sa one jednak tak tanie jakby sie wydawalo, a wykorzystanie ich jest kwestia jedynie minut.</p>


<p>Mamy wielki wybor wsrod fajerwerkow. Kazdy moze wybrac cos, co mu sie spodoba. Oczywiscie dzieci najbardziej ciesza sie z takich pokazow i to one bardzo czesto chca kupic cos takiego dla siebie. Jednak nie wszystko mozna im sprzedac, gdyz nie jest w pelni dla nich bezpieczne, a one nie zdaja sobie z tego sprawy.</p></div>


