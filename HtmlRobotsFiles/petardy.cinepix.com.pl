<h2 class="post-title"><a
href="http://www.petardy.cinepix.com.pl/index.php?post/2009/03/14/Fajerwerki">Fajerwerki</a></h2>

<p class="post-info">By admin mike    on Saturday, March 14 2009, 00:24        - <a href="http://www.petardy.cinepix.com.pl/index.php?category/fajerwerki">fajerwerki</a>
</p>

<ul class="post-tags">    <li><a href="http://www.petardy.cinepix.com.pl/index.php?tag/petardy">petardy</a></li>
</ul>


<div class="post-content"><p>Ludzie lubia bardzo ekstremalne rozrywki. Wiele osob lubi zabawe z ogniem, inni natomiast kochaja pokazy fajerwerkow. To one sprawiaja im niesamowita przyjemnosc. Podziwianie niesamowitych wyczynow sztucznych ogni w powietrzu praktycznie kazdemu sprawia przyjemnosc. Sa jednak tacy, ktorzy potrafia wykonywac niesamowite pokazy fajerwerkow.</p>


<p>Wymagaja one jednak duzego nakladu pracy, wyobrazni i oczywiscie wielu pieniedzy. Ktos, to zajmuje sie takimi pokazami na szerszym polu musi byc niebywale odpowiedzialny, bo zabawa z fajerwerkami nie jest tak bezpieczna i trzeba trzymac sie wszelkich regul, aby wszystko bylo w nalezytym porzadku.</p></div>


