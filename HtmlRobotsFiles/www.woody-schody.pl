<h1>O firmie</h1>
<div class="content">
<blockquote>
<h3>Jestesmy bardzo doswiadczona firma zajmujaca sie <strong>produkcja drewnianych schodow oraz drzwi.</strong></h3>
<p>Nasze wyroby zdobywaja uznanie klientow w Polsce i za granica.</p>
<p><strong>Od poczatku naszej dzialalnosci zrealizowalismy z powodzeniem ponad 700 projektow</strong></p></blockquote>
<p><strong>Do projektowania schodow</strong> uzywamy ekskluzywnego oprogramowania firmy Compass Software, dzieki ktoremu <strong>mozemy szybko i precyzyjnie zaprojektowac kazdy rodzaj schodow</strong> jaki tylko zapragna miec nasi klienci.</p>
<p><strong>Oto jak wygladaja przykladowe wizualizacje:</strong></p>
<p><img class="alignnone  wp-image-59" title="dwuzabiegowe z dwoma podestami" src="/wp-content/uploads/dwuzabiegowe-z-dwoma-podestami-300x217.jpg" alt="" width="162" height="117" /> <img title="Giete" src="/wp-content/uploads/Giete.jpg" alt="" width="162" height="117" /> <img title="jednozabiegowe" src="/wp-content/uploads/jednozabiegowe.jpg" alt="" width="162" height="117" /></p>
<p><a href="/wp-content/uploads/CCF20130513_000132.jpg"><img class="alignnone  wp-image-63" title="dwuzabiegowe z dwoma podestami" src="/wp-content/uploads/CCF20130513_000132-1024x717.jpg" alt="" width="573" height="402" /></a></p>
<h2><strong></strong><br />
<strong>Schody drewniane</strong> to nie tylko desing, ale rowniez <strong>wygoda, bezpieczenstwo i jakosc</strong> (nasze schody objete sa 3-letnia gwarancja)</h2>
<ul>
<li style="font-weight: bold;"><strong> Wysoka jakosc</strong> naszych produktow gwarantuje <strong>zadowolenie naszych klientow</strong>, co jest bardzo waznym aspektem naszej dzialalnosci.</li>
<li>Doswiadczenie i wdrazanie <strong>najnowszych technologii</strong> pozwala nam produkowac <strong>schody i drzwi o roznych ksztaltach z zastosowaniem i polaczeniem roznych materialow</strong>.</li>
<li>Polaczenie metalu, drewna, szkla oraz kamienia pozwala dobrac optymalny wizerunek wyrobu, ktory przy doradztwie naszych fachowcow daje sie <strong>idealnie wkomponowac w klimat kazdego wnetrza.</strong></li>
</ul>
<p><strong>W cenie: POMIAR, PROJEKT, DOJAZD DO KLIENTA, MONTAZ (z wyjatkiem schodow na beton )<br />
</strong></p>
<blockquote>
<h2><strong>Jestesmy profesjonalistami &#8211; oferujemy:</strong></h2>
</blockquote>
<ul>
<li><strong>Materialy najwyzszej jakosci (dab, buk, jesion, sosna, swierk, a na zyczenie klienta kazdy gatunek drewna)</strong></li>
</ul>
<p>Precyzja obrobki oraz dobre i suche drewno gwarantuja najwyzszy standard jakosci oferowanych przez nas wyrobow<br />
Nasze schody wykonane sa w 100% z naturalnego drewna najwyzszej jakosci przez doswiadczonych stolarzy, to czyni je produktem z ,gornej polki'</p>
<ul>
<li><strong>Indywidualne rozwiazania</strong></li>
</ul>
<p>Schody wykonujemy pod katem potrzeb indywidualnych klientow, ich gustu oraz staramy sie dopasowac je do konkretnego wnetrza domu lub mieszkania</p>
<ul>
<li><strong>Korzystne ceny</strong></li>
</ul>
<p>Mimo wielu zalet, cena naszych uslug pozostaje niska co sprawia, ze produkty drewniane naszego zakladu sa przystepne dla kazdego. Z pewnoscia sa Panstwo przekonani, ze na dobrej jakosci nie powinno sie oszczedzac, wiec pieniadze wydane na nasze produkty okaza sie najlepsza i mila dla oka inwestycja.<br />
W cenie: POMIAR, PROJEKT, DOJAZD DO KLIENTA, MONTAZ (z wyjatkiem schodow na beton )</p>
<ul>
<li><strong>Profesjonalny montaz</strong></li>
</ul>
<p>Bezwzglednie usuwamy wady anatomiczne, takie jak seki, zawoje, skret wlokien, majace wplyw na pozniejsze wlasciwosci materialu</p>
<ul>
<li><strong>Doskonala wspolprace &#8211; dzialamy sprawnie i szybko</strong></li>
</ul>
<p>Naszym celem jest zadowolenie kazdego klienta, wiec dajemy z siebie 110% i dotrzymujemy umowionych terminow<br />
Doradzamy, sugerujemy, rozwiewamy wszelkie watpliwosci</p>
<ul>
<li><strong>Nadajemy ekskluzywny charakter kazdemu wnetrzu</strong></li>
</ul>
<p>Nasza oferta zawiera bardzo szeroka game wzorow tralek i slupow, co sprawia ze praktycznie kazde schody sa niepowtarzalne.</p>
<p>&nbsp;</p>
<blockquote><p><strong><img class="alignleft" src="/wp-content/uploads/logo.gif" alt="" width="130" height="130" /></strong></p>
<p><strong>Nasze wyroby produkowane sa na indywidualne zlecenie klienta, uwzgledniajac jego osobiste preferencje.</strong></p>
<p><strong>Zarowno rodzaj drzewa z jakiego sa wykonywane schody lub drzwi a takze kolor i wzor sa konsultowane z kazdym klientem indywidualnie. </strong></p>
<p>&nbsp;</p></blockquote>
<p>&nbsp;</p>
<h2>Wykonujemy:</h2>
