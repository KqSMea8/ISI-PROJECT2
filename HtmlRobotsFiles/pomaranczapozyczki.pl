<div style="text-align: left; padding-left: 55px;"><p><span style="font-size: small; font-family: tahoma, arial, helvetica, sans-serif;">Istnieje mozliwosc otrzymania pozyczki mimo zlej historii kredytowej. Kazda sytuacje rozpatrujemy indywidualnie.</span></p></div>
<h3><i class="noMobile fa fa-long-arrow-right"></i> Mam ZAJECIE KOMORNICZE. Czy moge otrzymac u Was pozyczke? </h3>
<div style="text-align: left; padding-left: 55px;"><p style="text-align: left;"><span style="font-size: small; font-family: tahoma, arial, helvetica, sans-serif;">Zajecie komornicze nie stanowi przeszkody w otrzymaniu pozyczki.</span></p></div>
<h3><i class="noMobile fa fa-long-arrow-right"></i> Jak moge dokonac splaty pozyczki? </h3>
<div style="text-align: left; padding-left: 55px;"><p style="text-align: left;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: small;">Splaty pozyczki mozna dokonac&nbsp;w kazdej <strong>plac&oacute;wce Poczty Polskiej</strong>&nbsp;na terenie calego kraju bez prowizji&nbsp;na numer konta: </span></p>
<p style="text-align: left;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: small;"><strong>48 1320 0019 1633 6942 2000 0004</strong></span></p></div>
<h3><i class="noMobile fa fa-long-arrow-right"></i> Jakie dokumenty sa niezbedne, aby otrzymac pozyczke? </h3>
<div style="text-align: left; padding-left: 55px;"><p style="text-align: left;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: small;">Skontaktuj sie z nami, dzwoniac pod numer Infolinii. Podchodzimy elastycznie w kwestii dokument&oacute;w niezbednych do udzielenia pozyczki. Nasz cel to maksymalne upraszczanie Twojej drogi do uzyskania got&oacute;wki.</span></p></div>
<h3><i class="noMobile fa fa-long-arrow-right"></i> Czy sa jakies ukryte koszty w umowie? </h3>
<div style="text-align: left; padding-left: 55px;"><p style="text-align: left;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: small;">Nie, nie ma zadnych ukrytych koszt&oacute;w. Informacja na temat koszt&oacute;w Twojej pozyczki zawsze podawana jest w spos&oacute;b zrozumialy i przejrzysty.</span></p></div>
<h3><i class="noMobile fa fa-long-arrow-right"></i> Czy w tym samym czasie mozna otrzymac kilka pozyczek? </h3>
<div style="text-align: left; padding-left: 55px;"><p style="text-align: left;"><span style="font-size: small; font-family: tahoma, arial, helvetica, sans-serif;">W tym samym czasie mozesz otrzymac tylko jedna pozyczke, ale Pomarancza Owocne Pozyczki Sp. z o.o. oferuje mozliwosc otrzymania dodatkowej kwoty do juz istniejacej pozyczki. Informacje o mozliwosciach otrzymania pozyczki otrzymasz dzwoniac na Infolinie: 535-704-705.</span></p></div>

