<div style="text-align: left; padding-left: 55px;"><p style="text-align: left;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: small;">Udzielamy pozyczek&nbsp;<strong>do 2000 zl </strong>dla stalych klient&oacute;w.</span></p></div>
<h3><i class="noMobile fa fa-long-arrow-right"></i> Jakie warunki nalezy spelnic, aby otrzymac pozyczke? </h3>
<div style="text-align: left; padding-left: 55px;"><p style="text-align: left;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: small;">Pozyczke mozesz otrzymac, jezeli:</span></p>
