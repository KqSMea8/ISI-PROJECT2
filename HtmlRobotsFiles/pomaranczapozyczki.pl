<li><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: small;">masz ukonczone 18 lat, nie wyznaczamy g&oacute;rnej granicy wiekowej - do kazdego klienta podchodzimy indywidualnie</span></li>
<li><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: small;">uzyskujesz staly doch&oacute;d (umowa o prace, umowa zlecenia, dzialalnosc gospodarcza, renta, emerytura)</span></li>
<li><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: small;">posiadasz dow&oacute;d osobisty.</span></li>
</ul>
<p style="text-align: left;">&nbsp;</p>
<ul>
</ul></div>
