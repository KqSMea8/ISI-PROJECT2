<h3>dr n. med. Jerzy Pawelczyk specjalista chirurg<br />
</h3>
<img src="/photos/strona_glowna/1.jpg" alt="Jerzy Pawelczyk - specjalista chirurg" class="float-right" />
<p>Jestem absolwentem Wydzialu Lekarskiego Akademii Medycznej we Wroclawiu z 35-letnim doswiadczeniem klinicznym z zakresu chirurgii ogolnej. Od 1983 roku czlonkiem Towarzystwa Chirurgow Polskich, Polsko-Niemieckiego Towarzystwa Chirurgow Naczyniowych i European Association for Endoscopic Surgery.</p>
<p style="padding-bottom:5px;">Liczne Certyfikaty z zakresu proktologii i flebologii, a takze ukonczenie szkolenia w Avelana Vein Center i warsztatow organizowanych przez Laser&Health Academy "Aesthetics, dermatology and surgery treatments" with Fotona Nd:YAG, Er:YAG, Q-switched Nd:YAG lasers pozwalaja mi w sposob perfekcyjny wykorzystywac nabyte umiejetnosci w diagnostyce i leczeniu operacyjnym z wykorzystaniem lasera Nd:YAG.</p>
