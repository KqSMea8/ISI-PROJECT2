<h2 class="titles"><a href="http://www.partner.sacz.com.pl/" rel="bookmark" title="Permanent Link to O Firmie">O Firmie</a></h2>

<div class="entry">
<p>PARTNER istnieje od 1997 roku. Koncentruje sie na nowoczesnych technologiach i urzadzeniach wspomagajacych procesy sprzedazy hurtowej i detalicznej. Oferujemy nie tylko urzadzenia fiskalne, ale rowniez kompleksowe systemy sprzedazy wraz z dostawa sprzetu i oprogramowania, wdrozeniami, szkoleniami i serwisem. Nasza oferta obejmuje szeroki zakres odbiorcow,  poczawszy od malych kas fiskalnych m.in. dla niewielkich punktow handlowych i gastronomicznych,  a skonczywszy na rozbudowanych systemach sprzedazy dla marketow, aptek, gastronomii i sluzby zdrowia. Wieloletnie doswiadczenie pozwala nam na zaoferowanie atrakcyjnych cenowo i skutecznie funkcjonujacych systemow sprzedazy.</p>
<p>Nasza oferta:</p>
<ul>
<li>kasy fiskalne</li>
<li>drukarki fiskalne</li>
<li>systemy fiskalne</li>
<li>terminale kasowe POS</li>
<li>wagi sklepowe</li>
<li>drukarki etykiet</li>
<li>drukarki zamowien (barowe)</li>
<li>czytniki kodow kreskowych</li>
<li>sprawdzarki cen / weryfikatory</li>
<li>kolektory danych / inwentaryzatory</li>
<li>szuflady kasowe</li>
<li>metkownice</li>
<li>materialy eksploatacyjne (rolki termiczne, metki, etykiety)</li>
<li>oprogramowanie sprzedazowe, magazynowe, dla gastronomii, aptek, sluzby zdrowia  itp.</li>
<li>systemy sieciowe i telewizja przemyslowa</li>
<li>serwis sprzetu komputerowego (komputery, kasy fiskalne, laptopy itp.)</li>
<li>nadzor i stala opieka serwisowa</li>
</ul>
<p>Dzialajac na sadeckim rynku od wielu lat, posiadamy ugruntowana pozycje jako stabilny partner w zakresie dostawy urzadzen techniki fiskalnej i kompleksowych systemow sprzedazy dla handlu, uslug, gastronomii, aptek i sluzby zdrowia.  Wspolpracujac z wiodacymi firmami z branzy jestesmy w stanie zaoferowac naszym odbiorcom szeroka game produktow wspomagajacych funkcjonowanie duzego i malego  biznesu.</p>
