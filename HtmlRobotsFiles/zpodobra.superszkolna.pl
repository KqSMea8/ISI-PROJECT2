<h2>Jak to dziala: dopalacze?</h2>
<p class="center objectFilm"><div style="width: 480px; height:295px;"><iframe id="player1556164478" src="https://www.youtube.com/embed/DJUmFx9jf58?rel=0&amp;wmode=transparent&amp;html5=true"  style="border: none; width: 480px; height: 295px"></iframe></div></p>
<p>     Szukasz informacji jak moga dzialac dopalacze?
Wejdz na strone http://dopalaczeinfo.pl/ i dowiedz sie wiecej z wiarygodnego zrodla.

Jak to dziala: dopalacze - to krotki film animowany o charakterze edukacyjnym, wykonany w nowoczesnej technice sketchnoting. W ciekawy, ale tez przystepny sposob zwraca uwage na mozliwe konsekwencje zazywania dopalaczy oraz na mechanizm dzialania substancji na organizm czlowieka.

Film dostarcza rzetelnych informacji na temat wplywu nowych substancji psychoaktywnych - zwanych potocznie dopalaczami - na osrodkowy uklad nerwowy, mozg, a w konsekwencji na caly organizm czlowieka - nie tylko pod wzgledem biologicznym,
ale rowniez psychicznym czy spolecznym.

Przeprowadzone badania kampanii spolecznych wskazuja, ze straszenie, zwlaszcza w przekazie skierowanym do mlodych osob, jest nieskuteczna strategia wplywu na podejmowane w przyszlosci zachowania.</p>
