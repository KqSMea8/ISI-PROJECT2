<p>Od ponad 50 lat YAMAHA rozwija kursy nauczania muzyki dla osob w kazdym wieku.</p>
<p>Programy te sa stosowane w Szkolach Muzycznych YAMAHA na calym swiecie.</p>
<p>Od momentu otwarcia pierwszej Szkoly Muzycznej YAMAHA w 1954 roku w Japonii, liczba uczniow w systemie siega prawie 700 000, a absolwentow - 5 mln. Do ich dyspozycji pozostaje ponad 20 000 nauczycieli w ponad 6000 placowek na calym swiecie. YAMAHA pragnie rozbudzic uzdolnienia muzyczne u wszystkich osob, poczynajac od najmlodszych, a w celu dalszego rozwoju umiejetnosci muzycznych, oferuje szeroki zakres ciaglego wsparcia w formie nauczania.</p>
