W kamerze otworkowej swiatlo nie jest skrepowane gorsetem optycznym. Obraz sprawia wrazenie bezposredniego "dotkniecia" materialu swiatloczulego przez fotografowany przedmiot. Przedluzony czas naswietlania sprawia, ze fotografia otworkowa zyskuje wymiar duchowy. W czasie naswietlania "cos sie dzieje", cos nierealnego, cos metafizycznego. Fotografia rejestruje ruch reki, drgania lisci, falowanie powietrza, a w niektorych przypadkach umozliwia utrwalenie cienia badz osoby fotografa (przy pelnej lub czesciowej ekspozycji). Przy odpowiednim kacie padania promieni slonecznych, slizgaja sie one po brzegu otworka i ulegaja rozszczepieniu, ugieciu i zalamaniu. Material swiatloczuly rejestruje droge tych promieni w kamerze i przedstawia ja w postaci poswiaty ulozonej w regularna wiazke rozblaskow, niekiedy rozlozona na barwy podstawowe. Taki efekt nie jest mozliwy do uzyskania w obiektywie optycznym! Przyroda w kamerze otworkowej rysuje sie w sposob naturalny. Duza glebia ostrosci (a praktycznie ostre widzenie calego planu), a przy tym niewielka nieostrosc i malarskosc, przypominaja widzenie oka ludzkiego.
<br>
Pawel Pierscinski
