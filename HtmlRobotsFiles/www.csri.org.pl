<h2><a href="http://www.csri.org.pl/?p=1171" rel="bookmark" title="Staly link do wpisu Warsztaty CSR">Warsztaty CSR</a></h2>
<div class="entry">
<p><strong>Studenckie Kolo Naukowe SKN Ekobiznes</strong> wraz z <strong>Ambasadorka Ligi Odpowiedzialnego Biznesu</strong> zaprasza na <strong>Warsztaty CSR</strong> dla studentow lodzkich uczelni i wszystkich zainteresowanych tematyka. Warsztaty odbeda sie <strong>12 kwietnia</strong> na <strong>Wydziale Ekonomiczno &#8211; Socjologicznym UL</strong>.<br />
Jest to spotkanie, ktore bedzie dotyczyc szeroko rozumianej koncepcji spolecznej odpowiedzialnosci biznesu. Zaproszeni goscie jako eksperci w tej dziedzinie zaglebia uczestnikow w tematyke prowadzac interaktywne zajecia. Beda nimi:<br />
1. <strong>The Sustainers</strong> &#8211; w ramach Warsztatow CSR poprowadza gre szkoleniowa, ktora uczy praktycznego wdrazania w firmach spolecznej odpowiedzialnosci biznesu (CSR) i zrownowazonego rozwoju, jest rowniez narzedziem tworzenia innowacji i crowdsourcingu. (www.sustainers.pl)<br />
2. <strong>IKEA Retail </strong>&#8211; w ramach wydarzenia bedziemy goscic Pania Joanne Sitek &#8211; koordynatora ds. zrownowazonego rozwoju IKEA Lodz. Pani Joanna podejmie temat spolecznej odpowiedzialnosci biznesu w tekstyliach.<br />
3. Fundacja <strong>CSR Impact</strong> &#8211; podczas spotkania w ramach Warsztatow CSR bedziemy goscic Pania Agate Rudnicka &#8211; prezeske fundacji, a takze ekspertke zewnetrzna Forum Odpowiedzialnego Biznesu. Pani Agata odniesie sie do komunikacji CSR w media spolecznosciowych.(www.csri.org.pl)<br />
Harmonogram wydarzenia:<br />
godz. 11.30 &#8211; 13.00 Gra CSR (sala E305)<br />
godz. 13.05 &#8211; 14.50 CSR w tekstyliach (sala F220)<br />
godz. 16.50 &#8211; 18.20 Komunikacja CSR w mediach spolecznosciowych (sala E10)<br />
Zapisy poprzez formularz zgloszeniowy: goo.gl/VFpahm UWAGA! Ilosc miejsc ograniczona &#8211; decyduje kolejnosc zgloszen.<br />
Dla uczestnikow warsztatow przewidziane sa gadzety od naszych sponsorow, a takze certyfikaty potwierdzajace uczestnictwo.</p>
