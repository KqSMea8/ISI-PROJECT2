<h2><a href="http://www.csri.org.pl/?p=1183" rel="bookmark" title="Staly link do wpisu Wspolpracujemy">Wspolpracujemy</a></h2>
<div class="entry">
<p>Wspolpracujemy z INSPRO!<br />
Dzieki wspolpracy z INSPRO skorzystalismy z:<br />
&#8211; indywidualnego doradztwa prawnego. </p>
<p>Poprawiamy nasze funkcjonowanie, dzialamy na rzecz innych.<br />
Wsparcie udzielane jest w ramach Osrodka Wsparcia Ekonomii Spolecznej Centrum KLUCZ prowadzonego przez Instytut Spraw Obywatelskich (INSPRO).</p>
<p><strong><em>Instytut Spraw Obywatelskich</em></strong> (INSPRO)<br />
<a href="http://www.inspro.org.pl">www.inspro.org.pl</a><br />
Instytut Spraw Obywatelskich (INSPRO) jest organizacja pozarzadowa niezalezna od partii politycznych. Od 2004 roku slucha glosu obywateli i zamienia go w czyn. Celem INSPRO sa systemowe zmiany spoleczne. Fundacja prowadzi kampanie ogolnopolskie np. Tiry na tory, Obywatele decyduja, Wolne od GMO? Chce wiedziec, Centrum KLUCZ.</p>
<p><strong><em>Centrum KLUCZ</em></strong><br />
<a href="http://www.centrumklucz.pl" target="_blank">www.centrumklucz.pl</a><br />
Centrum KLUCZ - inicjatywa INSPRO dedykowana rozwojowi przedsiebiorczosci spolecznej. Od 2011 roku w jej ramach INSPRO wspiera podmioty ekonomii spolecznej. W 2015 roku uchwala Ministra Pracy i Polityki Spolecznej Centrum KLUCZ otrzymalo status akredytowanego Osrodka Wsparcia Ekonomii Spolecznej Wysokiej Jakosci. </p>
