<p><a href="wp-content/uploads/MG_6490-800x462.jpg"><img class="alignnone size-medium wp-image-1147" title="_MG_6490 (800x462)" src="wp-content/uploads/MG_6490-800x462-300x115.jpg" alt="" width="300" height="115" /></a></p>
<p>P.P.Ch. LODMOR S.A. ropoczela swoja dzialalnosc w 1958 roku, jako Panstwowa Chlodnia Skladowa w Gdansku. W 1960 roku uruchomiona zostala produkcja lodow pod marka Calypso. P.P.Ch. Lodmor byl wowczas pierwszym i jedynym producentem lodow jadalnych w Polsce, dzialajacym na skale przemyslowa.</p>
<p>Od 1993 roku, firma po procesie prywatyzacji, kontynuowala dzialalnosc jako P.P.Ch. LODMOR Spolka Akcyjna. Do dzis pozostaje jednym z liczacych sie producentow lodow w Polsce i jest postrzegana jako stabilny i wiarygodny partner, ktorego marka lodow Calypso kojarzona jest z doskonalym smakiem i wysoka jakoscia.</p>
<p><a href="wp-content/uploads/MG_6458-533x800.jpg"><img class="alignnone size-medium wp-image-1135" title="_MG_6458 (533x800)" src="wp-content/uploads/MG_6458-533x800-199x300.jpg" alt="" width="199" height="300" /></a>     <a href="wp-content/uploads/MG_6361-800x533-732x357.jpg"><img class="alignnone size-medium wp-image-1127" title="_MG_6361 (800x533) (732x357)" src="wp-content/uploads/MG_6361-800x533-732x357-300x146.jpg" alt="" width="300" height="146" /></a>    <a href="wp-content/uploads/MG_6447-800x390.jpg"><img class="alignnone size-medium wp-image-1129" title="_MG_6447 (800x390)" src="wp-content/uploads/MG_6447-800x390-300x205.jpg" alt="" width="300" height="205" /></a></p>
<p>Od ponad 50 lat, dla wielu juz pokolen Polakow produkujemy i sprzedajemy lody o niezapomnianym smaku i najwyzszej jakosci.</p>
<p>Potwierdzeniem stosowania wysokich standardow jakosciowych w zakresie produkcji oraz w obszarze zarzadzania jest posiadany przez firme certyfikat ISO 9001 i objecie dzialalnosci firmy unijnym systemem HACCP.</p>
<p>Pozostajac wierni tradycji, sprawdzonym wzorcom i recepturom, starmy sie rownoczesnie podazac za zmieniajacymi sie gustami konsumentow. Do naszej oferty wprowadzamy sukcesywnie nowe smaki i produkty.</p>
<p>Nasza starania doceniaja konsumenci, a grono naszych stalych klientow stale sie powieksza.</p>
<p><a href="wp-content/uploads/MG_6442-800x390.jpg"><img class="alignnone size-medium wp-image-1139" title="_MG_6442 (800x390)" src="wp-content/uploads/MG_6442-800x390-300x131.jpg" alt="" width="300" height="131" /></a>                                       <a href="wp-content/uploads/MG_6411-800x531.jpg"><img class="alignnone size-medium wp-image-1143" title="_MG_6411 (800x531)" src="wp-content/uploads/MG_6411-800x531-300x199.jpg" alt="" width="300" height="199" /></a></p>
<p>P.P.Ch. LODMOR jest rowniez jednym z glownych dystrybutorow w regionie pomorskim produktow i towarow mrozonych oraz chlodzonych. Korzystajac z dogodnej lokalizacji firmy w centralnym rejonie Gdanska tuz przy trasie nr 7 (Gdansk - Warszawa) oraz w oparciu o posiadana baze logistyczno - transportowa i siec sprawnych przedstawicieli handlowych, stworzylismy profesjonalny dzial handlowy i dystrybucji zajmujacy sie klientami z segmentu detalicznego i HoReCa.</p>
<p>&nbsp;</p>
<p>Firma P.P.Ch. LODMOR S.A. jest dystrybutorem wiodacych marek i producentow zarowno towarow mrozonych jak i schladzanych miedzy innymi takich jak: Algida, Ice-Mastry, Nestle, Zielona Budka, Unilever, Grycan, Nordis, Jawo, Elbro, Hortex, Oerleman's, Hortino, D'aucy, Frosta, Dr.Oetker, McCain, Farm Frites, Aviko, Animex, Knorr, Tortex, Carte D&#8217;or, Lipton, Unifreeze, Lanthmannen, Lila, Prymat, Kier, Fanex, Vandemoortele, Vicziunai-Pol, Pinguin, Felco, Indykpol, Aves, Abel, La Sevillana i inne.</p>
<p>Serdecznie zapraszamy do wspolpracy handlowej.</p>

