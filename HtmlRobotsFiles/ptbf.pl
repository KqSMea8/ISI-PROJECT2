<p style="text-align: right;">Olsztyn, 30 stycznia 2019 r.</p>
<p><em>Szanowni Panstwo,</em></p>
<p>Czas biegnie nieublaganie i za chwile minie kolejna trzyletnia kadencja obecnych wladz naszego Towarzystwa. Tradycyjne jest to tez okres przygotowan do kolejnego spotkania naszej biofizycznej Rodziny i przyjaciol. Na wstepnie pragniemy podziekowac za wszystkie mile slowa, ktore naplynely pod naszym adresem, dotyczace ostatniego XVI Zjazdu PTBF w Rynie. Jest nam niezmiernie milo, ze tak dobrze wspominacie Panstwo nasze ostatnie spotkanie a zarazem czujemy sie zobowiazani do jeszcze bardziej wytezonej pracy przygotowujac XVII Zjazd PTBF.</p>
<p>W tym roku pragniemy zaprosic Panstwa na Warmi<a href="http://ptbf.pl/wp-content/uploads/2019/02/logo17ZjazdPTBF.png"><img class="wp-image-1154 alignright" src="http://ptbf.pl/wp-content/uploads/2019/02/logo17ZjazdPTBF.png" alt="" width="221" height="190"/></a>e do Olsztyna. XVII Zjazd Polskiego Towarzystwa Biofizycznego odbedzie sie w dniach 24 czerwca - 27 czerwca 2019 roku w jednym z najpiekniejszych kampusow uniwersyteckich w Polsce w Kortowie. Obrady beda obywac sie w zrewitalizowanej ,,Starej Kotlowni". Mamy nadzieje, ze spodoba sie Panstwu polaczenie nowoczesnosci z wspaniala atmosfera zabytkowego budynku. <a href="http://ptbf.pl/?p=1120#more-1120" class="more-link">Czytaj dalej <span class="meta-nav">&rarr;</span></a></p>
