<H1>EKOPOL</H1>



<p class="right_01s">
Serdecznie witamy naszych klientow. Firma <b>Ekopol</b> daje Panstwu do dyspozycji
katalog swoich dystrybutorow z Calego kraju. Narzedzie to przeznaczone jest do
odnalezienia najblizszego dostawce uslugi produktow naszej firmy. Klient
bez problemu moze zlokalizowac profesjonalnych sprzedawcow, monterow,
serwisantow z dziedziny oczyszczalni sciekow. <br>
<br>
<b>Jakosc i bezpieczenstwo.</b><br>
Produkujemy oczyszczalnie ekologiczne, biologiczne oraz szamba - zbiorniki
bezodplywowe. Wytwarzamy <b>przydomowe oczyszczalnie sciekow</b> oraz przemyslowe
<b>oczyszczalnie sciekow</b>. Zajmujemy sie produkcja, montazem oraz serwisem
gwarancyjnym i pogwarancyjnym. Wszystkie produkty wytwarzane
sa w naszej polskiej fabryce. Od wielu lata wspolpracujemy jedynie z dostawcami
najwyzszej jakosci materialow. Nasze oczyszczalnie posiadaja niezbedne
materialy certyfikujace, opinie oraz aprobaty techniczne.<br>
<br>
<b>Profesjonalizm i doswiadczenie.</b><br>
Ekopol to rodzinne przedsiebiorstwo z wieloletnim stazem. Firma nowoczesna i
kreatywna. Jestesmy liderem na rynku oczyszczalni sciekow zarowno przydomowych
jak i przemyslowych. Nasz obszar dzialania obejmuje cala Polske. W nasze
produkty zaopatruja sie wszystkie wieksze hurtownie na terenie calego kraju.
Wspolpracujemy z dziesiatkami firm zajmujacymi sie sprzedaza, montazem i
serwisem oczyszczalni sciekow. Dzieki naszemu doswiadczeniu nie ograniczamy sie
jedynie do standardowych rozwiazan. Nasi specjalisci bez problemu wykonaja
instalacje na indywidualne zamowienie. Czesto i zawsze z sukcesem wykonujemy
skomplikowane rozwiazania. Nasza kadra zmierzy sie i w 100% podola wszystkim
zleceniom z naszej dziedziny.<br>
<br>
<b>10 lat gwarancji</b><br>
Jaki jedyni na rynku dajemy 10 letnia gwarancje na naszej produkty, takze przy
samodzielnym montazu. Dzieki naszej praktyce jestesmy pewni o jakosc i
bezpieczenstwo naszych produktow. Wykozystane rozwiazania sa
wczesniej poddane surowym testom zarowno wydajnosciowym jak i jakosciowym.
Dlatego nie obawiamy sie o ich trwalosc i bezpieczenstwo.<br>
<br>
<b>Bezplatny transport i rozladunek.</b><br>
Oferujemy bezplatny transport naszych oczyszczalni do klienta docelowego na
terenie calego kraju. Posiadamy wlasny tabor transportowy. Pomagamy przy
rozladunku. Dzieki przemyslanym rozwiazaniom logistycznym zamowienia
dostarczamy szybko i bez problemu do kazdego miejsca w Polsce. Transport <a href="http://www.biosanit.pl">ekopol</a> gwarantuje niezawodnosc i terminowosc dostawy zamowionych produktow.

