<div class="about-us">
<div class="content__container">
<h2>Spolka TERMALL </h2>
<p style="text-align: center;">Sp&oacute;lka TERMALL z siedziba w Belchatowie powstala w roku 1991. W poczatkowym okresie istnienia dzialalnosc pod nazwa Przedsiebiorstwo Produkcyjno &ndash; Uslugowe Termall prowadzona byla w formie sp&oacute;lki cywilnej. Od 1994 Termall funkcjonuje jako sp&oacute;lka prawa handlowego pod nazwa Energetyka i Technika Grzewcza Termall Sp. z o.o. Dzialalnosc sp&oacute;lki pod obecna nazwa Termall Sp. z o.o. Rozruch Moc Ekologia datuje sie od roku 2004.</p>
<p style="text-align: center;">Obecnie Sp&oacute;lka TERMALL ma ugruntowana pozycje na rynku w obszarze dzialan zwiazanych z energetyka, zwlaszcza w sektorze wytwarzania, ochrona srodowiska naturalnego oraz wdrazaniem nowych technologii w tych obszarach. Firma zatrudnia wysokokwalifikowana kadre specjalist&oacute;w rozruchu, eksploatacji, pomiar&oacute;w akrredytowanych emisji, instalacji odsiarczaniaspalin ISO, oraz nadzor&oacute;w z zakresu inwestycji energetycznych i ochrony srodowiska.</p>
<p style="text-align: center;">&nbsp;</p>
<p style="text-align: center;"><a class="btn" href="o-nas">Dowiedz sie wiecej</a></p>
<p style="text-align: center;">&nbsp;</p>
<p style="text-align: center;"><strong><em>Projekt Energetyczne Laboratorium Badawcze </em></strong><em>[</em><em>numer RPLD.01.02.01-10-0016/17-00]</em></p>
<p style="text-align: center;"><em>Projekt <strong>Energetyczne Laboratorium Badawcze</strong> jest projektem inwestycyjnym w aktywa trwale (rozumiane jako infrastruktura badawczo-rozwojowa), niezbednym dla naszej Sp&oacute;lki Termall sp. z o.o. Rozruch Moc Ekologia do utworzenia w ramach prowadzonej dzialalnosci gospodarczej nowej kom&oacute;rki w strukturze organizacyjnej &ndash; Pionu Handlu i Rozwoju w sklad kt&oacute;rego bedzie wchodzilo laboratorium, w kt&oacute;rym beda prowadzone zaplanowane w planie prac badawczych i zgodne ze specjalizacjami regionalnymi prace badawczo-rozwojowe pozwalajace Wnioskodawcy na kompleksowe podejscie do obslugi sektora energetycznego, ze szczeg&oacute;lnym uwzglednieniem wsparcia energetycznego rynku regionalnego.</em></p>
<p style="text-align: center;"><em>Utworzone dzieki realizacji niniejszego projektu <strong>Energetyczne Laboratorium Badawcze</strong> bedzie wykorzystywane przez Termall sp. z o.o. Rozruch Moc Ekologia do prowadzenia dzialalnosci badawczo-rozwojowej sluzacej optymalizacji proces&oacute;w w instalacjach energetycznych, instalacjach ochrony srodowiska, a takze innych instalacjach przemyslowych.</em></p>
<p style="text-align: center;"><em>Calkowita wartosc projektu wynosi: 2 553 972,00 zl. Projekt uzyskal dofinansowanie ze srodk&oacute;w Europejskiego Funduszu Rozwoju Regionalnego (wsp&oacute;lfinansowanie UE) w ramach &bdquo;Regionalnego Programu Operacyjnego Wojew&oacute;dztwa L&oacute;dzkiego na lata 2014 &ndash; 2020&rdquo; w kwocie 934 380,00 zl.</em></p>
