<h2>STRATEGIA</h2>
<p>Pozyskiwanie nowych inwestycji w zakresie energetyki zawodowej i przemyslowej.&nbsp;<br />Rozwiniecie dzialalnosci w sektorze spalarni oraz biogazowni.&nbsp;<br />Rozw&oacute;j techniczno &ndash; organizacyjny i technologiczny naszej Firmy.&nbsp;<br />Dbalosc o srodowisko i bezpieczne warunki pracy.&nbsp;<br />Ciagle inwestowanie w rozw&oacute;j intelektualny naszych specjalist&oacute;w.</p>    </div>
</div>

<div class="mission">
<div class="content__container">
<h2>MISJA</h2>
<p>Nasza misja jest swiadczenie najwyzszej jakosci uslug dla sektora energetycznego w zakresie modernizacji, remont&oacute;w i wdrazania nowych technologii. Kluczowym elementem jest zaufanie do jakosci prac naszego personelu o wysokich kwalifikacjach i duzym doswiadczeniu. <br /> <br /> Organizacje naszych prac opieramy na certyfikowanym systemie zarzadzania jakoscia, srodowiskiem i BHP zgodnym z normami ISO 9001, 14001 i PN-N 18001.</p>    </div>
