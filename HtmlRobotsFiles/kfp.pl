<div style="text-align:justify; padding-top:8px;" class="desc">Jaroslaw Walesa apeluje do rzadu o podjecie rozmow i przyjecie postulatow nauczycieli. Ojciec europosla nie widzi szans na pozytywny dialog z wladza. Przedluzanie strajku, jego zdaniem, nie ma sensu. <br>
- Oni sa juz bardzo zdesperowani, prosza o ten dialog - powiedzial Jaroslaw Walesa podczas porannej konferencji prasowej przed historyczna Brama nr 2 Stoczni Gdanskiej. Z dziennikarzami spotkal sie tu rowniez byly prezydent Lech Walesa. - W tym symbolicznym miejscu w roku 1980 strajkujacy nie zadali tylko pieniedzy, zadali rowniez godnosci. I o to apelujemy - o poszanowanie godnosci nauczycieli, o zwrocenie uwagi na to, ze  silna edukacja i dobrze wyksztalcona mlodziez to przyszlosc Polski. Musimy do tego podchodzic systemowo, musimy to robic, korzystajac z doswiadczen ,,Solidarnosci". I dlatego tez... mam tutaj przy sobie swojego ojca, ktory moze o tym najlepiej powiedziec - mowil Jaroslaw Walesa. Legendarny przywodca ,,S" zaczal od tego, ze nie widzi szans na porozumienie z obecna wladza. - Ci ludzie wyciagneli zle wnioski i prowadza Polske w zlym kierunku. Starzy na pewno to dobrze pamietaja, ze w taki sam sposob dwa razy chciano pokonac nasz strajk w 1980 roku. Stojac na tej bramie - w momencie, kiedy zebraly sie tu rodziny, zeby odebrac wyplaty stoczniowcow, uslyszalem, ze dyrekcja oglasza: ,,placimy tylko niestrajkujacym". To byl prowokacja, podobna jak teraz, aby nas poklocic, podzielic, aby zony i matki wyciagnely nas ze strajku. W imieniu komitetu strajkowego oglosilem, ze natychmiast placimy strajkujacym podwojnie. Dalem w twarz tamtej wladzy. Tej wladzy nauczyciele powinni zrobic to samo. Nauczyciele, posluchajcie starego Walesy... albo zrobcie po swojemu, ale ja wiem, ze niczego nie osiagniecie, bo z tymi ludzmi nie da sie nic madrego osiagnac - przekonywal Walesa senior, dodajac, ze tylko zmiana wladzy moze przyniesc porozumienie. - Czyli mozna powiedziec, ze bylo to spotkanie idealizmu z pragmatyzmem - posumowal wspolne wystapienie Jaroslaw Walesa. A co ze strajkiem? - Trzeba poprosic nauczycieli: ,,wytrzymajcie jeszcze troche"... i zrobmy wszystko teraz wszyscy, aby nastepna wladza byla madrzejsza, aby nie niszczyla nauki i innych dziedzin. Przedluzanie tego, co sie teraz dzieje, jest bez sensu - ocenil Lech Walesa. 18.04.2019 / fot. Anna Rezulak / KFP, Maciej Kosycarz / KFP</div>
