<div style="text-align:justify; padding-top:8px;" class="desc">Do gdynskiego portu w piatkowy poranek wplynelo szesc okretow Stalego Zespolu Sil Morskich NATO (Standing NATO Maritime Group One). To prawdziwa gratka dla fanow militariow. Trzy  jednostki cumujace przy nabrzezach Francuskim i Bulgarskim w sobote i niedziele bedzie mozna zwiedzac. Marynarze z Hiszpanii, Niemiec, Turcji, Stanow Zjednoczonych, Wielkiej Brytanii i Polski pozostana w Gdyni przez caly okres swiat wielkanocnych. Wizyta sojuszniczego zespolu ma charakter roboczy.  Jednostka flagowa - z dowodca zespolu kontradmiralem Edwardem Cashmanem na pokladzie - jest amerykanski niszczyciel USS ,,Gravely" (zwiedzanie, sobota, godz. 13-15, nabrzeze Francuskie). <br>
