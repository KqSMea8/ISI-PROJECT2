<p style="text-align: center;"><span style="color: #339966;"><strong><em>OKO W OKO Z DYSLEKSJA  W PRZEDSZKOLU</em></strong></span></p>
<p> Uczniowie ze specyficznymi trudnosciami czytaniu i pisaniu, z tak zwana <strong>DYSLEKSJA ROZWOJOWA</strong> sa zazwyczaj zauwazani w szkole wowczas, gdy ich niepowodzenia w nauce szkolnej nasilaja sie i staja sie coraz czestsze.</p>
<p>Trudnosci w czytaniu i pisaniu moga pojawiac sie juz w <strong>WIEKU PRZEDSZKOLNYM</strong>, zapowiadajac specyficzne trudnosci w czytaniu i pisaniu takze na dalszych etapach ksztalcenia.</p>
<p>Wczesna diagnoza i rozpoznanie  <strong>RYZYKA DYSLEKSJI</strong> dziecka przed rozpoczeciem nauki szkolnej pozwala na wlasciwa interwencje i pomoc psychologiczno- pedagogiczna w formie zajec korekcyjno-kompensacyjnych.</p>
<p><strong><span style="color: #ff0000;">Powiatowa Poradnia Psychologiczno-Pedagogiczna w Bialej Podl. prowadzi badania przesiewowe w kierunku wykrycia ryzyka dysleksji  </span><span style="color: #ff0000;">u dzieci z kl.O. </span></strong></p>
<p><strong><span style="color: #339966;">Badaniami moga byc objeci rowniez uczniowie z klas I-III.</span></strong></p>
<p>Badania przeprowadzane beda na terenie Szkol i Przedszkoli.</p>
