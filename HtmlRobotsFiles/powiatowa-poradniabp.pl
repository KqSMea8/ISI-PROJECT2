<h1 style="text-align: center;"><strong><em><span style="color: #ff0000;">Bezpieczne dziecko w sieci</span> </em></strong></h1>
<p style="text-align: center;">dedykowanego dla dzieci 6-8 letnich.</p>
<p style="text-align: center;">Program bedzie realizowany na terenie placowek i obejmuje cykl 8-10 zajec grupowych.</p>
