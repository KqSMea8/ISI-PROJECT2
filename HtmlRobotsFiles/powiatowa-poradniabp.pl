<p><strong><span style="color: #0000ff;">W ramach programu beda realizowane dodatkowe zajecia wczesnego wspomagania rozwoju dziecka i zajecia specjalistyczne dla dzieci objetych wczesnym wspomaganiem rozwoju  w Powiecie Bialskim.</span></strong></p>
<p>Powiatowa Poradnia Psychologiczno - Pedagogiczna w Bialej Podlaskiej na mocy podpisanego porozumienia Powiatu Bialskiego <strong>z dnia 1 grudnia 2017 r.</strong> <strong>miedzy Ministerstwem Edukacji Narodowej i Powiatem Bialskim zostala wyznaczona do realizacji zadan Rzadowego programu wsparcia dla rodzin ,,Za zyciem".</strong></p>
<p>Celem dzialania programu jest zapewnienie dziecku niepelnosprawnemu i jego rodzinie dostepu do aktywnej pomocy ze strony panstwa poprzez udzielenie interdyscyplinarnej specjalistycznej opieki.</p>
<p><span style="color: #ff0000;"><strong>Program skierowany jest dla dzieci objetych zajeciami wczesnego wspomagania rozwoju dziecka od urodzenia do podjecia nauki w szkole, ze szczegolnym uwzglednieniem dzieci do 3 roku zycia.</strong></span></p>
<p><strong><u>Rodzicu</u></strong></p>
<ul>
<li>jezeli Twoje dziecko uczeszcza na zajecia z wczesnego wspomagania rozwoju, skorzystaj z mozliwosci dodatkowych <strong>bezplatnych</strong> zajec ze specjalistami</li>
<li><strong>jezeli Twoje dziecko realizuje juz zajecia z wczesnego wspomagania rozwoju w innej placowce, moze rownolegle korzystac z terapii i wsparcia w Powiatowej Poradni Psychologiczno - Pedagogicznej w Bialej Podlaskiej</strong></li>
<li>Oferujemy w zaleznosci od potrzeb dziecka<br />
<h3><span style="color: #ff0000;"><strong>do 5 godzin tygodniowo</strong></span></h3>
<p>ze specjalistami <span style="color: #0000ff;"><strong>(psycholog, logopeda, terapeuta SI) oraz uslugi fizjoterapeuty</strong></span></li>
<li><span style="color: #ff0000;"><strong>projekt realizowany jest od 01.01.2018 do 31.12.2021 roku</strong></span></li>
</ul>
