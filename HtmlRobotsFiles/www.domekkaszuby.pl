<h1>DREWNIANY DOMEK LETNISKOWY OLPUCH KASZUBY</h1>
<B>ZAPRASZAMY PANSTWA DO SPEDZENIA WOLNEGO CZASU NA KASZUBACH W DOMKU LETNISKOWYM.</B><br><br>
Domek jest polozony na Kaszubach w miejscowosci Olpuch ma styku trzech wydzielonych obszarow krajobrazowych tj. <b>Wdzydzkiego Parku Krajobrazowego - Doliny Wiezycy - Borow Tucholskich.</b>
Kaszuby zwane Szwajcaria Kaszubska, to kraina jezior i lasow, czystego powietrza, roznorodnosci gatunkow roslin i zwierzat. Kaszuby jako region, posiadaja swoj wlasny jezyk i folklor.
<br><br>

Domek polozony jest na ogrodzonej dzialce. Na parterze znajduje sie salon z aneksem kuchennym i lazienka. Na pietrze znajduja dwie sypialnie, w tym jedna zamknieta.
Przy domku sa dwa tarasy: poludniowy otwarty i zachodni zadaszonym.
Na dzialce znajduja sie: zagospodarowane miejsce do zabaw dla dzieci z hustawka i piaskownica oraz wydzielone miejsce na samochody.
W domku moze jednoczesnie goscic do 8 osob. Zapraszamy bez zwierzat. <br><br> <CENTER><B>SERDECZNIE ZAPRASZAMY</B></CENTER>
