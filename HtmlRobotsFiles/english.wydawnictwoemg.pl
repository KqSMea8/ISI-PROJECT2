<header class="entry-header"><h1 class="entry-title">About Us</h1></header><!-- .entry-header -->
<div class="entry-content">
<p>EMG publishing company was established in 2003 by the painter Ewa Mankowska. EMG's first project was the collection of crime stories which continues to be published today.</p>
<p>Since her childhood, Ewa Mankowska has been inspired by her grandmother who would read detective stories every day. In 2001, Ewa Mankowska met her future husband Irek Grin, the author of action and crime novels, and their partnership resulted in the establishment of EMG.</p>
<p>The publishing activities of the EMG have been very prolific encompassing a variety of materials such as the poetry by Polish authors as well as critical essays on literature.</p>
<p>EMG has also published foreign authors such as: Juan Gelman, Mickey Spilane, Walter Mosley, Judith Katzir, Batya Gur, and Franz Kafka.</p>
<p>Since 2015 EMG publishes books devoted to various issues related to architecture. The most important is Architecture series is dedicated to the memory of prof. Tomasz Mankowski, an architect and pedagogue, who passed away in 2012.</p>
<p>The company is located in Krakow.</p>
