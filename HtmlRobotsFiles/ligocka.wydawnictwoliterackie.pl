<td width="453" class="tlo02"><div align="justify">Roma Ligocka - malarka i pisarka. Jej ksiazki: "Dziewczynka w czerwonym plaszczyku", "Kobieta w podrozy" oraz "Tylko ja sama" sa bestsellerami, nie tylko w Polsce ale i w Europie. Wielokrotnie nagradzana, przez wiele lat pracowala jako kostiumolog i scenograf w licznych europejskich teatrach, operach, takze w filmie i telewizji. Roma Ligocka wydala rowniez cztery tomy felietonow: "Znajoma z lustra", "Czulosc i obojetnosc", "Wszystko z milosci" oraz "Ksiezyc nad Taormina". Jej ksiazka "Roza. Obrazy i slowa" jest z kolei pieknym, albumowym przewodnikiem po malarstwie Autorki. W listopadzie 2012 roku  ukazala sie jej najnowsza powiesc, "Dobre dziecko", bedaca zapisem intymnych wspomnien nastoletniej "dziewczynki w czerwonym plaszczyku". </div></td>
