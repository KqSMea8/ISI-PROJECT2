<p style="text-align: left;"><strong>Podrecznik akademicki "Hydrologia Polski" juz w ksiegarniach</strong></p>
<p style="text-align: justify;"><strong><img style="margin-right: 10px; margin-left: 10px; float: right;" src="uploads/images/aktualnosci/Hydro_polski.jpg" alt="" height="300" />,,Hydrologia Polski",</strong> to pierwszy od ponad 60 lat podrecznik akademicki, calosciowo prezentujacy wiedze na temat zasobow i stosunkow wodnych Polski. Zawiera bowiem tresci dotyczace zarowno wod podziemnych, jak i powierzchniowych wraz z opisem czynnikow i warunkow determinujacych ich rozmieszczenie oraz dynamike. Uwzglednia zmiany metodyczne i merytoryczne jakie zaszly w zakresie oceny i zarzadzania zasobami wodnymi po wstapieniu Polski do Unii Europejskiej. Pokazuje historie, czasy wspolczesne i projekcje ewolucji zasobow wodnych Polski w nawiazaniu do innych elementow srodowiska, scenariuszy zmian klimatycznych na tle wzrastajacej antropopresji.<br />Podrecznik jest bogato ilustrowany. Czytelnik znajdzie w nim szereg nowych, autorskich map tematycznych, schematow i fotografii, a takze wiele zestawien tabelarycznych zawierajacych informacje pochodzace z najnowszych baz danych. Ich dopelnieniem jest obszerny spis literatury, dzieki ktoremu mozna poszerzyc wiedze z danego zakresu. Autorami poszczegolnych rozdzialow sa wysokiej klasy specjalisci w zakresie hydrologii i nauk geograficznych, a jednoczesnie doswiadczeni wykladowcy oraz dydaktycy reprezentujacy niemal wszystkie uniwersyteckie osrodki hydrologiczne w Polsce.<br />Idea opracowania podrecznika narodzila sie w 2016 roku na jednym z posiedzen Komisji Hydrologicznej Polskiego Towarzystwa Geograficznego. Jego koncepcje, zakres merytoryczny i sklad Zespolu Autorskiego, przygotowali redaktorzy naukowi.</p>
