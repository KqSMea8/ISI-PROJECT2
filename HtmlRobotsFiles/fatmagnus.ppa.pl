<h2>Wstepniak</h2></font>

Niniejsza strona jest zbiorem tworczosci polskich milosnikow komputera Amiga, ktorzy swoja kreatywnosc stunelowali w kierunku slowa pisanego. Magazyny Dyskowe powstawaly czesto spontanicznie - z potrzeby serca, poczucia misji czy tez z wszechogarniajacej nudy w czasie letnich wakacji. Jedne bardziej, drugie mniej udane. Wszystkie jednak zawieraja unikalny ladunek bardzo pozytywnej energi, plynacej wprost z otwartych umyslow mlodych ludzi. Bez cenzury, bez odgornych ram, po prostu czysta nieokielznana tworczosc (czesto na bardzo wysokim poziomie). Oto i esencja Amigowych Magazynow Dyskowych, o ktorych (niestety) ginie powoli sluch w naszej globalnej wiosce i natloku jalowych informacji.
<br><br>
Teraz juz wiesz, drogi czytelniku, skad pojawil sie pomysl na AMIGOWE ARCHIWUM POLSKICH MAGAZYNOW DYSKOWYCH. Strona powstala aby zachowac przed zapomnieneim te drogocenne perly naszej wspolczesnej kultury niezalezniej. Nie moglismy dopuscic do tego, aby zaginely gdzies pod kolejnymi warstwami informacyjnego mulu. Swoja droga obcowanie z tak wyjatkowym rodzajem sztuki dostarczylo nam - tworcom tego archiwum - wiele frajdy i pozwolilo splacic choc po czesci dlug wdziecznosci wobec Amigi. Tego wspanialego komputera, na ktorym w wiekszosci sie wchowalismy i ktory wciaz nas fascynuje pod wieloma roznymi postaciami (MorphOS, AmigaOS4, AROS). To dzieki pasji jaka obudzila w nas Amiga, dzisiaj jestesmy programistami, grafikami, muzykami, czy pisarzami. Jestesmy po prostu dumni z tego ze mozemy nadal kreowac i wspoltworzyc nasza wyjatkowa subkulture, nie zapominajac przy tym o Amigowym dziedzictwie ostatnich 20 lat.
<br><br>
Oddajemy w wasze rece naszego FAT MAGNUSa, ktorego nazwalismy na czesc jednego z najlepszych Amigowych magazynow dyskowych  (Fat Agnus). Tymsamym zapraszamy do zatopienia sie w setkach magazynow i tysiacach artykulow - do przeniesienia sie w swiat Amigi. Oto unikalna mozliwosc wczucia sie jeszcze raz w Amigowy klimat, ktorym kazdy z magazynow wypelniony jest po brzegi.
<br><br>
<b><p align="right">Milego czytania!</b><br><br>
