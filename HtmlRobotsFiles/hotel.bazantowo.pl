<h1>Bazantowo Sport Hotel</h1>

<p><strong>Bazantowo Sport Hotel***</strong>&nbsp; oceniany jako jeden z najlepszych hoteli w Katowicach.</p>

<p>Najwyzsze&nbsp;rekomendacje&nbsp;<strong>na podstawie ponad 200 opinii gosci na portalach: HotelsCombined 9,0/10, HRS.com 9,2/10, Booking.com 8,9/10, HolidayCheck.pl 5,6/6</strong>.</p>

<p><em><strong>Hotel Bazantowo Sport moze poszczycic sie najwyzszym poziomem zadowolenia gosci hotelowych oraz ekspert&oacute;w branzy hotelarskiej, osiagajac imponujacy wynik 9.0/10 na podstawie 187 opinii. Otrzymane w listopadzie 2016r. wyr&oacute;znienie Recognition of Excellence przyznawane jest hotelom, kt&oacute;re znalazly sie wsr&oacute;d 3% najlepszych obiekt&oacute;w noclegowych na swiecie!</strong></em></p>

<p>Hotel przez swoje unikalne w skali kraju zaplecze sportowo-rekreacyjne wpisuje sie w kategorie hotelu &bdquo;emocjonalnego&rdquo;, kt&oacute;ry zaspokoi wszelkie oczekiwania Gosci, dla kt&oacute;rych zdrowie i aktywnosc fizyczna sa stylem zycia. Na powierzchni blisko 10.000m2 znajduje sie&nbsp;<strong>basen, jacuzzi, saunarium ze strefa relaksu, hala tenisowa, korty do squash-a, nowoczesna silownia z salami fitness oraz wielofunkcyjna hala sportowa</strong>. Ponadto 2 boiska oraz plac zabaw na najmlodszych gosci.&nbsp;Atrakcyjnym uzupelnieniem oferty sportowej jest bogato wyposazone&nbsp;<strong>studio Beauty &amp; SPA</strong>&nbsp;oraz&nbsp;<strong>Centrum Rehabilitacji Galen</strong>.&nbsp;</p>

<p>W hotelu znajduje sie 18 komfortowych pokoi dwuosobowych, w tym 8 z tarasami, 4 z aneksem kuchennym oraz studio z sypialnia i pokojem dziennym. Klimatyzowane pokoje wyposazone sa w telewizory, WiFi, lod&oacute;wki, sejfy, zestawy do parzenia kawy i herbaty, bogato wyposazone lazienki (w opcji z wanna, prysznicem, bidetem).<br />
<strong>Sala konferencyjna na 50 os&oacute;b z pelnym zapleczem gastronomicznym i audiowizualnym</strong>&nbsp;- idealna do organizacji konferencji i spotkan biznesowych.<br />
<strong>Restauracja</strong>&nbsp;w nowoczesnej sportowej oprawie zaprasza juz od wejscia. Zdrowa i smaczna oferta naszej restauracji, kawiarni i baru stanowi apetyczne dopelnienie oferty sportowej i rekreacyjnej.</p>

<p>Goscie hotelowi wieczorem moga spedzic czas w angielskim&nbsp;<strong>pubie i kregielni "Bazantowo Bowling Station"</strong>.</p>

<p><strong>Bazantowo Sport Hotel***</strong>&nbsp;miesci sie na drugim pietrze nowoczesnego kompleksu sportowo rekreacyjnego, kt&oacute;ry zostal oddany do uzytkowania w pierwszym kwartale 2013r. Caly obiekt, wraz z hotelem, jest przyjazny dla rodzin z dziecmi i os&oacute;b niepelnosprawnych.</p>

<p>To idealne miejsce dla os&oacute;b podr&oacute;zujacych sluzbowo, bedacych takze w dluzszych delegacjach, gdzie kazdy znajdzie wytchnienie i relaks po wytezonej pracy.</p>

