<p class="boldface" style="text-transform: uppercase; text-align: center">
INFORMACJA DOTYCZACA PRZETWARZANIA DANYCH OSOBOWYCH<br />
W SZKOLE PODSTAWOWEJ IM. JERZEGO KUKUCZKI W KALNEJ
</p>

<p>
W zwiazku z przetwarzaniem Pani/Pana danych osobowych - zgodnie z art. 13 ust. 1 i ust. 2 Rozporzadzenia Parlamentu
Europejskiego i Rady (UE) 2016/679 z dnia 27.04.2016 r. w sprawie ochrony osob fizycznych w zwiazku z przetwarzaniem danych
osobowych i w sprawie swobodnego przeplywu takich danych oraz uchylenia dyrektywy 95/46/WE (ogolne rozporzadzenie o
ochronie danych) (Dz. Urz. UE L z 04.05.2016 r., Nr 119, s. 1), zwanego dalej w skrocie ,,RODO" oraz  ustawy z dnia 10 maja
2018 r. o ochronie danych osobowych (Dz. U. 2018 poz. 1000) informujemy, iz:
</p>

<p class="boldface" style="text-transform: uppercase">
I. Administrator danych.
</p>

<p style="padding: 0 0 0 20px">
Administratorem danych osobowych Pani/Pana/Dziecka danych osobowych jest Szkola Podstawowa im. Jerzego Kukuczki w  Kalnej
reprezentowana przez Dyrektora,<br />
z siedziba przy ulicy Widokowej 59<br />
tel. 33 81 77 235<br />
e-mail: sp@szkolakalna.pl<br />
adres WWW: kalna.edu.pl
</p>

<p class="boldface" style="text-transform: uppercase">
II.Inspektor ochrony danych.
</p>

<p style="padding: 0 0 0 20px">
Administrator wyznaczyl Inspektora Ochrony Danych, z ktorym moze sie Pani/Pan skontaktowac w sprawach zwiazanych z ochrona
danych osobowych, w nastepujacy sposob pod adresem poczty elektronicznej: iod@szkolakalna.pl lub pisemnie na adres siedziby
Administratora.
</p>

<p class="boldface" style="text-transform: uppercase">
III. Cele i podstawa prawna przetwarzania danych osobowych
</p>

<p style="padding: 0 0 0 20px">
Przetwarzanie Pani/Pana/Dziecka danych osobowych odbywa sie w zwiazku z realizacja zadan opiekunczych, wychowawczych i
dydaktycznych powierzonych okreslonymi przepisami prawa i pelnomocnictwami, w szczegolnosci na podstawie ustawy z dnia 7
wrzesnia 1991 r. o systemie oswiaty, ustawy z dnia 26 stycznia 1982 r. Karta Nauczyciela, ustawy z dnia 15 kwietnia 2011 r.
o systemie informacji oswiatowej, ustawy z dnia 14 grudnia 2016 r. Przepisy wprowadzajace ustawe - Prawo oswiatowe, ustawy
z dnia 14 grudnia 2016 r. Prawo oswiatowe, ustawy z dnia 27 pazdziernika 2017 r. o finansowaniu zadan oswiatowych oraz
rozporzadzen do w/w ustaw.
</p>

<p class="boldface" style="text-transform: uppercase">
podstawa przetwarzania danych wynikajaca z rodo:
</p>

<ol style="padding: 0 0 0 40px; text-align: justify; line-height: 140%;">
<li>
Realizacja przyslugujacych Administratorowi uprawnien, badz spelnienie przez Administratora obowiazkow okreslonych
przepisami prawa.
</li>
<li>
Wykonanie zadania realizowanego w interesie publicznym lub w ramach sprawowania wladzy publicznej przez Administratora.
</li>
<li>
Wykonania umowy, ktorej Pani/Pan jest strona lub do podjecia dzialan, na Pani/Pana zadanie przed zawarciem umowy.
</li>
<li>
Wyrazona przez Pania/Pana zgoda na przetwarzanie danych osobowych w okreslonym celu i zakresie.
</li>
</ol>

<p class="boldface" style="text-transform: uppercase">
IV.Odbiorcy danych osobowych
</p>

<p style="padding: 0 0 0 20px">
W zwiazku z przetwarzaniem danych w celach, o ktorych mowa w pkt III odbiorcami Pani/Pana/Dziecka danych osobowych moga byc
organy wladzy publicznej oraz podmioty wykonujace zadania publiczne lub dzialajace na zlecenie organow wladzy publicznej, w
zakresie i w celach, ktore wynikaja z przepisow powszechnie obowiazujacego prawa oraz inne podmioty, ktore na podstawie
stosownych umow podpisanych z Administratorem, przetwarzaja dane osobowe na jego polecenie.
</p>

<p class="boldface" style="text-transform: uppercase">
V.Okres przechowywania danych osobowych.
</p>

<ol style="padding: 0 0 0 40px; text-align: justify; line-height: 140%;">
<li>
Pani/Pana/Dziecka dane osobowe beda przechowywane jedynie w okresie niezbednym do spelnienia celu, dla ktorego zostaly
zebrane lub w okresie wskazanym przepisami prawa.
</li>
<li>
Po spelnieniu celu, dla ktorego Pani/Pana/Dziecka dane zostaly zebrane, moga one byc przechowywane jedynie w celach
archiwalnych, przez okres, ktory wyznaczony zostanie przede wszystkim na podstawie rozporzadzenia Prezesa Rady Ministrow
w sprawie instrukcji kancelaryjnej, jednolitych rzeczowych wykazow akt oraz instrukcji w sprawie organizacji i zakresu
dzialania archiwow zakladowych, chyba ze przepisy szczegolne stanowia inaczej.
</li>
</ol>

<p class="boldface" style="text-transform: uppercase">
VI.Prawa osob, ktorych dane dotycza, w tym dostepu do danych osobowych.
</p>

<p style="padding: 0 0 0 20px">
Na zasadach okreslonych przepisami RODO, posiada Pani/Pan prawo do zadania od administratora:
</p>

<ol style="padding: 0 0 0 40px; text-align: justify; line-height: 140%;">
<li>
dostepu do tresci swoich oraz dziecka danych osobowych,
</li>
<li>
sprostowania (poprawiania) swoich oraz dziecka danych osobowych,
</li>
<li>
usuniecia swoich oraz dziecka danych osobowych,
</li>
<li>
ograniczenia przetwarzania swoich oraz dziecka danych osobowych,
</li>
<li>
przenoszenia swoich oraz dziecka danych osobowych,
</li>
</ol>

<p style="padding: 0 0 0 20px">
a ponadto, posiada Pani/Pan prawo do wniesienia sprzeciwu wobec przetwarzania Pani/Pana/Dziecka danych.
</p>

<p class="boldface" style="text-transform: uppercase">
VII.Prawo do wycofania zgody.
</p>

<p style="padding: 0 0 0 20px">
I.Tam, gdzie do przetwarzania danych osobowych konieczne jest wyrazenie zgody, zawsze ma Pani/Pan prawo nie wyrazic zgody,
a w przypadku jej wczesniejszego wyrazenia, do wycofania zgody. Wycofanie zgody nie ma wplywu na zgodnosc z prawem
przetwarzania Pani/Pana/Dziecka danych do momentu jej wycofania.
</p>

<p class="boldface" style="text-transform: uppercase">
VIII.Prawo wniesienia skargi do organu nadzorczego.
</p>

<p style="padding: 0 0 0 20px">
W przypadku uznania, iz przetwarzanie Pani/Pana/Dziecka danych osobowych narusza przepisy o ochronie danych osobowych,
przysluguje Pani/Panu prawo do wniesienia skargi do organu nadzorczego, ktorym jest Prezes Urzedu Ochrony Danych Osobowych.
</p>

<p class="boldface" style="text-transform: uppercase">
IX.Informacja o wymogu/dobrowolnosci podania danych oraz konsekwencjach niepodania danych osobowych.
</p>

<p style="padding: 0 0 0 20px">
Podanie przez Pania/Pana danych osobowych Pani/Pana/Dziecka moze byc wymogiem ustawowym, umownym lub warunkiem zawarcia umowy.
</p>

<ol style="padding: 0 0 0 40px; text-align: justify; line-height: 140%;">
<li>
W przypadku, gdy bedzie istnial obowiazek ustawowy, a nie poda Pani/Pan swoich lub Dziecka danych, nie bedziemy mogli
zrealizowac zadania ustawowego, co moze skutkowac konsekwencjami przewidzianymi przepisami prawa.
</li>
<li>
W przypadku, gdy bedzie istnial obowiazek ustawowy, a nie poda Pani/Pan swoich lub Dziecka danych, nie bedziemy mogli
zrealizowac zadania ustawowego, co moze skutkowac konsekwencjami przewidzianymi przepisami prawa.
</li>
<li>
W przypadku, kiedy podanie danych bedzie warunkiem zawarcia umowy, a nie poda Pani/Pan swoich lub Dziecka danych, nie
bedziemy mogli zawrzec takiej umowy.
</li>
</ol>

<p class="boldface" style="text-transform: uppercase">
X.Zautomatyzowane podejmowanie decyzji, profilowanie.
</p>

<p style="padding: 0 0 0 20px">
Pani/Pana/Dziecka dane osobowe nie podlegaja zautomatyzowanemu podejmowaniu decyzji, w tym profilowaniu.
</p>

