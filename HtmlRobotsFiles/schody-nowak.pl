<div class="title">O firmie</div>
<p style="text-align: justify; text-indent: 30pt;">Nasza firma istnieje na rynku juz prawie od 30 lat. Dzialamy na terenie Krakowa, jak i calej malopolski. Oferujemy szeroko rozumiane uslugi stolarskie. Specjalizujemy sie w wykonywaniu schod&oacute;w drewnianych: samonosnych lub na betonie. Uzywamy material&oacute;w tylko najwyzszej jakosci. Oferujemy gl&oacute;wnie wyroby z drzewa debowego, jesionowego, bukowego i innych. Schody moga byc na bazie drewna naturalnego lub w r&oacute;znych odcieniach na zyczenie klienta.</p>
<p style="text-align: justify; text-indent: 30pt;">Firma stawia przede wszystkim na dopasowanie uslug do indywidualnych potrzeb klienta. Sluzy porada oraz wykonuje projekty. Charakteryzujemy sie terminowoscia, profesjonalnym wykonaniem i estetycznym wykonczeniem oraz trwaloscia naszych produkt&oacute;w. Dojezdzamy do klienta i zapewniamy montaz.</p>
<p>Nasze wyroby nie tylko zdobia wnetrze lecz r&oacute;wniez sa bezpieczne i wygodne w korzystaniu.</p>
