<p style="text-align: left;"><strong>Fotografia</strong> (gr. phos, phos, D. photos - swiatlo; grapho - pisze, graphein - rysowac, pisac; rysowanie za pomoca swiatla) - zbior wielu roznych technik, ktorych celem jest zarejestrowanie trwalego, pojedynczego obrazu za pomoca swiatla. Potoczne znaczenie zaklada wykorzystanie ukladu optycznego, choc nie jest to konieczne np. przy rayografii.</p>
<p style="text-align: left;"><strong>Podstawowy podzial technik fotograficznych</strong></p>
<p style="text-align: left;">&#8211; fotografia tradycyjna (blednie okreslana jako analogowa) - od swego powstania oparta na chemii swiatloczulych zwiazkow srebra. Dzieli sie na fotografie monochromatyczna i fotografie barwna. Materialy fotograficzne dzielily sie na dwa rodzaje: pierwsze sluzyly do utrwalania obrazu w aparacie i produkowane byly w postaci klisz cietych (do aparatow wielkoformatowych - fotografia wielkoformatowa), zas drugie w postaci blon fotograficznych (filmow) perforowanych (do aparatow maloobrazkowych) i blon zwojowych, najczesciej o szerokosci 60 mm (do aparatow srednioformatowych), ktore nastepnie musialy byc wywolywane. Obraz zapisany na obu tych rodzajach materialow mogl byc przenoszony na papiery fotograficzne o roznych formatach.</p>
<p style="text-align: left;">&#8211; szlachetne techniki fotograficzne - techniki oparte o wykorzystanie swiatloczulosci i garbujacych wlasciwosci soli chromianowych, a takze innych zwiazkow swiatloczulych, np. gumy, pigmentu, bromoleju.</p>
<p style="text-align: left;">&#8211; fotografia cyfrowa - w tym przypadku rejestracja obrazu odbywa sie nie na materiale swiatloczulym, ale w karcie pamieci, po uprzednim przetworzeniu swiatla /obrazu/ wpadajacego przez obiektyw na sygnal elektryczny &#8211; w matrycy (np. w ukladzie CCD lub CMOS) zamontowanej w aparacie cyfrowym. Obiektywy aparatow cyfrowych sa identyczne (pod wzgledem konstrukcji optycznej) ze stosowanymi w aparatach do fotografii tradycyjnej (analogowej), ale wystepuja tez w postaci zmodyfikowanej, np. z wbudowanymi mechanizmami redukcji wstrzasow.</p>
<p style="text-align: left;"><strong>Fotografia tradycyjna a analogowa<br />
</strong><br />
Bardzo czesto fotografie tradycyjna (oparta na wykorzystaniu swiatloczulych wlasnosci halogenkow srebra) kompletnie mylnie okresla sie fotografia analogowa. Jest to powazny blad merytoryczny, gdyz fotografia analogowa mozna okreslic proces reprodukcji obrazu z pomoca urzadzen elektronicznych analogowych (np. fotopowielacz). Wynikiem dzialania swiatla na taki element swiatloczuly jest powstanie sygnalu analogowego, a nie efektu fotochemicznego na blonie filmowej czy innym nosniku emulsji swiatloczulej.</p>
