<h1><img src="/public/images/cms//th_witamy.gif" alt="Witamy na stronach Parafii pw. Znalezienia Krzyza Swietego i Sw. Andrzeja Apostola."/></h1>
<img class="left" src="/public/images/cms/zd_witamy.jpg" alt="Parafia pw. Znalezienia Krzyza Swietego i Sw. Andrzeja Apostola" />
<p>
<strong>Kosciol parafialny w Konskowoli pod rozszerzonym wezwaniem Znalezienia Krzyza Sw. i sw. Andrzeja Apostola</strong>, ufundowany zostal w polowie XVI w. przez Andrzeja Teczynskiego, wojewode lubelskiego i kasztelana krakowskiego. Jego metryka jest jednak starsza: pierwszy, drewniany kosciol p.w. Znalezienia Krzyza Sw. powstal w koncu XIV w. Jak chce tradycja, murowane prezbiterium wystawiono w 1442 r. z fundacji Jana Koninskiego.
Przebudowa, zlecona ok. 1670 r. przez marszalka wielkiego koronnego Stanislawa Herakliusza Lubomirskiego, prowadzona przez Tylmana z Gameren, nadala kosciolowi bryle trojnawowej bazyliki. Kolejna przebudowa, prowadzona w I polowie XVIII w. z inicjatywy Elzbiety Sieniawskiej i nastepnie Augusta Czartoryskiego oraz jego zony Zofii z Sieniawskich nadala swiatyni obecny ksztalt.
