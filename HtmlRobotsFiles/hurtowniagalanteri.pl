<center><p style="font-size: 24px;"><strong><a href='http://galanteriaosobista.pl/'>Katalog/Cennik</a></strong></p></center><br /><br />

Przedsiebiorstwo Cedar funkcjonuje w branzy galanteryjnej juz od ponad dwudziestu lat. Dzieki stalemu rozwojowi oraz naciskowi na najwyzsza jakosc proponowanych przez nas produktow, uzyskalismy renome, o ktorej swiadcza reklamy naszych produktow w najbardziej popularnych czasopismach oraz referencje Klientow.
<br>
<h2 style="font-size: 20px;font-family: 'Times New Roman';">Producent torebek skorzanych</h2>
Jestesmy producentem torebek skorzanych, a takze innych elementow galanterii, lacznie z akcesoriami. Posiadamy wlasna marke, ALWAYS WILD, ktorej zadaniem jest wypuszczanie na rynek eleganckich wyrobow skorzanych, przeznaczonych dla wymagajacych Klientow o okreslonym guscie. Dzialamy takze jako importer portfeli i torebek, ktore sprowadzamy z Indii oraz Chin, a nastepnie dystrybuujemy na terenie calego kraju. Przy wyborze galanterii kierujemy sie przede wszystkim jakoscia jej wykonania oraz gatunkiem wykorzystanych materialow.
<br><br>
Siedziba naszej hurtowni znajduje sie w Warszawie, dysponujemy biurami, sklepem, wzorcownia oraz magazynami, co pozwala nam na produkcje i sprzedaz na miejscu. Nasza lokalizacja wiaze sie takze z terminowa realizacja kazdego zlecenia. Zapewniamy fachowa i mila obsluge.
<br><br><br>



<div style="margin-left:30px;width:230px; border:1px dashed gray; padding:5px; font-size:13px; float:left;">
<strong>Paski meskie skorzane</strong><br>
<hr>
Znalezienie solidnego i wytrzymalego paska moze nastreczac problemow. W naszej ofercie posiadamy  paski meskie, wykonane w 100% z naturalnej skory, co zapewnia im niebywala trwalosc, a takze estetyczny wyglad. Proponujemy wysokiej jakosci produkty w tradycyjnym wzornictwie z klasycznymi badz nowoczesnymi detalami. Zapewniamy, ze nasze paski meskie sa nie do znoszenia.
<br><br>
<hr>
