<div style="width:230px; border:1px dashed gray; padding:5px; font-size:13px; margin-left:330px;">
<strong>Skorzane portfele meskie</strong><br>
<hr>
Stylowe i eleganckie portfele meskie stanowia jeden z naszych glownych produktow. Oferujemy produkty trwale i wytrzymale, wykonane ze stuprocentowo naturalnej skory. Ponadto posiadaja liczne przegrody na karty i wizytowki, ktore docenia praktyczni mezczyzni. Poza funkcjonalnoscia glownym atutem naszych skorzanych portfeli meskich jest ich oryginalna, nieco surowa stylistyka.
<hr>
