<h2 class="section-heading">witam na stronie mojej <b>kancelarii</b></h2>
<hr>

<p>Jezeli szukaja Panstwo prawnika w Bialymstoku lub Sokolce, to zachecam do zapoznania sie z moja oferta.<br />
<br />
Kancelaria swiadczy pomoc prawna obejmujaca w szczegolnosci udzielanie porad prawnych, sporzadzanie opinii prawnych oraz pism procesowych, zastepstwo przed sadami oraz przed organami administracji rzadowej i samorzadowej z wyjatkiem wystepowania w charakterze obroncy w sprawach karnych i karnoskarbowych do czego wylacznie uprawnionym jest adwokat. Jako prawnik oraz radca prawny posiadam bogate doswiadczenie zarowno w dziedzinie prawa cywilnego, gospodarczego jak i administracyjnego. <strong>Dzialania kancelarii ukierunkowane sa na zapewnienie kompleksowej pomocy prawnej w przystepnych cenach skierowanej do przedsiebiorcow jak i do klientow indywidualnych.</strong> Skorzystanie z uslug kancelarii pozwala ograniczyc ryzyko podpisania niekorzystnej umowy czy tez niekorzystnego rozstrzygniecia sadu lub organu administracji. Za cel stawiam sobie rzetelna i profesjonalna ochrone interesow prawnych moich klientow. Przed podjeciem sie sprawy szczegolowo analizuje problem i informuje klienta o moim stanowisku oraz przedstawiam szanse na osiagniecie celu. Dokladam wszelkich staran, aby swiadczyc pomoc prawna rzeczowo, w sposob przyjazny i dostepny dla Klienta. Dlatego tez oferuje rozne formy kontaktu w tym mozliwosc umowienia spotkania w miejscu zamieszkania klienta lub jego siedzibie.<br />
<br />
Swiadcze pomoc prawna nie tylko na terenie Bialegostoku i Sokolki, ale rowniez przyjmuje zlecenia z terenu wojewodztwa podlaskiego.<br />
<br />
Zapraszam do skorzystania z moich uslug.<br />
