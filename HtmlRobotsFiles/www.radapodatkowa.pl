<p>Gdy w 2007 r. KPMG przylaczylo sie do prac Rady Podatkowej, Tomasz zostal powolany na wiceprzewodniczacego Grupy VAT. W latach 2010-2016 pelnil funkcje przewodniczacego Grupy. W 2016 r. zrezygnowal z kierowania Grupa VAT, lecz pozostal czlonkiem przeydium Rady, jako czlonek Komitetu Wspierajacego.</p></div>
