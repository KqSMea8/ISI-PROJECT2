<td id="mainpage_article">
<p>
Witamy na stronie internetowej, ktora powstala w ramach realizacji projektu &quot;Turystyka aktywna z kultura w tle na Ziemi Krasnickiej&quot;, na realizacje kt&oacute;rego Powiat Krasnicki pozyskal srodki unijne w ramach Osi Priorytetowej VII Kultura, turystyka i wsp&oacute;lpraca miedzyregionalna, Dzialania 7.2 Promocja kultury i turystyki RPO WL. Projekt realizowany byl do czerwca 2011 r.&nbsp;w partnerstwie z piecioma gminami powiatu krasnickiego (Annopol, Krasnik, Urzed&oacute;w, Goscierad&oacute;w i Trzydnik Duzy) oraz Gminnym Osrodkiem Kultury w Urzedowie i Centrum Kultury w Annopolu.<br />
<br />
Goraco zapraszamy do skorzystania z oferty aktywnego wypoczynku w naszym powiecie. Zapraszamy pieszo, rowerem, samochodem. &nbsp;Na dzien, weekend lub kilka dni. a jezeli opr&oacute;cz swiezego powietrza, zabytk&oacute;w i przyrody lubisz sie pobawic - wpadnij na jedna z organizowanych przez nas powiatowych imprez.&nbsp;</p>
<p>
Polecamy lekture strony, szczeg&oacute;lnie:</p>
<p>
- galerie zdjec z calego powiatu - zobaczysz co na Ciebie tu czeka</p>
<p>
- gotowe &nbsp;propozycje wycieczek: sladem ginacych zawod&oacute;w, zabytk&oacute;w kultury, krajobrazu...</p>
<p>
Zachecamy do kontaktu z Wydzialem Promocji Starostwa Powiatowego w Krasniku (tel.. 81 826 17 92)- pomozemy Ci zaplanowac ciekawa wizyte w powiecie krasnickim :).</p>
<p>
&nbsp;</p>
<p>
Jezeli...<br />
<br />
- jestes organizatorem ciekawej imprezy<br />
<br />
- masz atrakcyjna oferte rekreacji (kuligi, jazda konna, prywatne mini-muzeum itp)<br />
<br />
- chcesz podzielic sie informacja o malo znanej ciekawostce, atrakcji z naszego powiatu<br />
<br />
- masz ciekawe zdjecie z powiatu, kt&oacute;re chcesz opublikowac w galerii na naszej stronie<br />
<br />
... nie czekaj, skontaktuj sie z nami - pomozemy ci naglosnic twoje przedsiewziecie i wsp&oacute;lnie wypromujemy Powiat Krasnicki</p>		</td>
