<h3>REGULAMIN ZAPISOW dla studentow Politechniki Wroclawskiej</h3>
<h2>JEZYKI OBCE, WYCH. FIZYCZNE, PRZEDMIOTY HUMANISTYCZNO-MENEDZERSKIE <br /> SEMESTR LETNI 2018/2019</h2>
<h6>Dodane: 2018-01-19 8:52:10</h6>
<ul>
<li><b>W dniu 13.02.2019 zostana aktywowane, a tym samym stana sie widoczne na portalu Edukacja.Cl zapisy ogolnouczelniane.</b> Prosimy o sprawdzenie swojego konta, wlasnych danych oraz  przydzielonego terminu zapisowego. W przypadku braku informacji w systemie o indywidualnym terminie zapisow, braku prawa do zapisow lub braku mozliwosci odnalezienia w systemie na stronie ,,Zapisy" zapisow ,,PWr_Zap_s_letni_2018/2019", prosimy wyjasniac sprawe w swoim dziekanacie.</li>
<li>Zapisy na jezyki obce, WF i przedmioty humanistyczno-menedzerskie odbywaja sie na podstawie uprzednio ustalonej listy studentow, ktora okresla dla kazdego studenta jego miejsce w kolejce zapisowej. Student ma prawo zapisac sie jedynie w podanym terminie (dopuszczalny jest wpis po wyznaczonym terminie, jednak nie wczesniej). Kazdy student jest zobowiazany do sprawdzenia czy ma przydzielony termin zapisow oraz czy jego dane (numer albumu, nazwisko, rok studiow) podane sa prawidlowo. <b>Wszelkie niezgodnosci nalezy zglaszac w dziekanacie, przed terminem zapisow.</b></li>
<li>Student jest zobowiazany zapisac sie na jezyki obce, WF i przedmioty humanistyczno-menedzerskie w ogloszonym terminie. <b>Na zajecia nalezy zapisywac sie w dniach zapisow, a nie korekty!</b> Zajecia, na ktore nie zapisze sie wymagana liczba studentow, moga zostac zlikwidowane przed korekta zapisow.</li>
<li>Po zapisach student jest zobowiazany sprawdzic, czy wszystkie kursy, na ktore sie zapisal, zostana uruchomione. Na ewentualne zmiany dokonanych wczesniej wpisow lub sprawdzenie, czy wpis zostal dokonany prawidlowo, przeznaczone sa korekty zapisow (,,PWr_Korekty_s_letni_2018/2019"). Wpisy mozna zmieniac juz w trakcie zapisow (nie czekajac na korekte zapisow).</li>
<li>Ewentualny wpis, zmiana lub wypis z kursu po terminie korekty zapisow mozliwe sa tylko w wyjatkowych sytuacjach za zgoda Dziekana wlasnego Wydzialu oraz Dziekana lub Dyrektora jednostki dydaktycznej (wydzialu, studium) prowadzacej kurs. <b>Inny sposob rezygnacji z kursu po terminie korekty jest rownoznaczny z jego niezaliczeniem.</b> Kazdy kurs, na ktory student sie zapisal, obciaza jego indywidualne konto.</li>
<li>Mozliwosc wpisow na jezyki obce, WF i przedmioty humanistyczno-menedzerskie moze zalezec od decyzji kazdego z dziekanatow. Przed zapisami nalezy sie zapoznac z wlasnym planem i programem studiow.</li>
<li>Na osiagniecie wymaganego poziomu B2 (lub C1) student ma 120 godzin podzielonych na dwa semestry (B2.1 i B2.2 lub C1.1 i C1.2). W przypadku osiagniecia wymaganego poziomu w ciagu 60 godzin i uzyskania 3 punktow ECTS student moze wybrac z oferty SJO 60-godzinny lektorat innego dowolnego jezyka na poziomie adekwatnym do jego potrzeb zawodowych za 2 punkty ECTS. W przypadku programu studiow pierwszego stopnia prowadzonych w jezyku angielskim student jest zobowiazany do realizacji 120-godzinnego lektoratu na poziomie C (tzn. C1.1 i C1.2 lub C1.2 i innego jezyka na dowolnym poziomie) z jezyka obcego, w ktorym prowadzony jest dany program studiow lub za zgoda Rady Wydzialu moze realizowac 120-godzinny lektorat dowolnego, innego jezyka obcego na dowolnym poziomie (np. dla cudzoziemcow  "jezyk polski jako obcy" na poziomie A1).</li>
<li>W przypadku kontynuacji nauki jezyka obcego po zrealizowaniu jednego semestru lektoratu wymagana jest realizacja wyzszego stopnia zaawansowania, niezaleznie od stopnia studiow.</li>
<li>W jednym semestrze mozna zapisac sie maksymalnie na 2 zajecia z jezyka obcego - <b>pod warunkiem, ze sa to rozne jezyki</b>, oraz 2 kursy humanistyczno - menedzerskie.</li>
<li>Do sekcji sportowych obowiazuje kwalifikacja trenera (nie mozna zapisac sie samodzielnie) - na pierwszych zajeciach sekcji.</li>
<li>W trakcie zapisow dnia 20.02.2019 (sroda) w godzinach 8.00-18.00 oraz dnia 21.02.2019 (czwartek) w godzinach 8.00-13.00, nastepujace jednostki beda pelnily dyzury:</li>
