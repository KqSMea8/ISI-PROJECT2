<h3>Zapisy dla studentow Politechniki Wroclawskiej semestr letni 2018/2019</h3>

<h6>Dodane: 2018-01-19 8:52:10</h6>
<h2><b>ZAPISY</b> - DOKTORANCI, STUDENCI NIEPELNOSPRAWNI - <b>ZAPISY NA WF</b></h2>
<p style="color:#000">Doktoranci oraz studenci niepelnosprawni (wymagane orzeczenie lekarskie) moga zapisywac sie na zajecia sportowe poza kolejnoscia w dniu 19.02.2019 (wtorek) w godz. 9.00-14.00. Zapisy odbeda sie w budynku H-14 (Wybrzeze Stanislawa Wyspianskiego 40), pok. -1-19 (biuro AKS). W WYJATKOWYCH przypadkach mozliwy bedzie wpis droga poczty elektronicznej na adres: <a href="mailto:sek.swfis@pwr.edu.pl">sek.swfis@pwr.edu.pl</a> (z podanym kodem zajec odczytanym w AKZ oraz telefonem kontaktowym). Nalezy rowniez zalaczyc zeskanowana legitymacje doktorancka lub studencka, a w przypadku osob niepelnosprawnych dodatkowo orzeczenie o niepelnosprawnosci. <b>Dla niepelnosprawnych przewidziano specjalna oferte, w sklad ktorej wchodza m.in. kursy plywania, brydza, kulturystyki-korekcji, zdrowe plecy.</b> Poczta wyslana poza terminem zapisow dla doktorantow oraz niepelnosprawnych nie bedzie rozpatrywana.</p>
<h2><b>ZAPISY</b> - STUDENCI NIEPELNOSPRAWNI - ZAPISY NA JEZYKI OBCE</h2>
<p style="color:#000">Studenci niepelnosprawni moga zapisywac sie na lektoraty przed zapisami w dniu 19.02.2019 (wtorek) w godz. 10.00-14.00 w bud. H-4 s.105. Warunkiem zapisu jest przedlozenie kopii legitymacji studenckiej oraz orzeczenia o niepelnosprawnosci. W WYJATKOWYCH  przypadkach mozliwy bedzie wpis droga elektroniczna - <a href="mailto:jolanta.szabla@pwr.edu.pl">jolanta.szabla@pwr.edu.pl</a> (ze skanami obu dokumentow, podanym kodem zajec odczytanym w AKZ oraz telefonem kontaktowym). Poczta wyslana poza terminem zapisow dla studentow niepelnosprawnych nie bedzie rozpatrywana.</p>
<h2>ZAPISY</h2>
<ul>
<li><b>Zapisy odbeda sie w dniach 20-21.02.2019 (sroda, czwartek) od godziny 8.00 dnia 20.02.2018 do godz. 13.00 dnia 21.02.2019.</b> Student bedzie mogl zapisac/ wypisac sie w  wyznaczonym indywidualnym terminie zapisow <b>oraz po nim.</b></li>
<li>Zapisy oraz korekta zapisow odbywaja sie z portalu <a href="http://edukacja.pwr.wroc.pl">edukacja.pwr.wroc.pl</a>.</li>
<li>O mozliwosci wpisu w okreslonym semestrze, dla danego stopnia i formy studiow decyduja poszczegolne wydzialy, zamieszczajac odpowiednie wpisy w planie i programie studiow.</li>
<li><b>Plan zajec</b> oraz informacje o zapisach i grupach zajeciowych dostepne beda na witrynie AKZ - <a href="http://akz.pwr.edu.pl">http://akz.pwr.edu.pl</a> od od 1.02.2019. Czesc informacji dodatkowych (np. jak wybrac poziom jezyka obcego, jakie zasady obowiazuja na zajeciach sportowych, informacje o sekcjach sportowych itp.) bedzie dostepnych na witrynach internetowych jednostek oferujacych kursy.</li>
</ul>
<h2>KOREKTA ZAPISOW</h2>
<ul>
<li>Po zapisach niektore zajecia moga zostac zlikwidowane, w przypadku braku miejsc moga byc uruchomione dodatkowe zajecia. Plan zajec bedzie modyfikowany na biezaco w AKZ.</li>
<li><b>Korekta zapisow odbedzie sie od godz. 15.00 dnia 21.02.2019 do godz. 7.30 dnia 22.02.2019 (piatek). </b></li>

</ul>
<br />
<h2>ZAPISY NA KURSY SPOZA PROGRAMU STUDIOW</h2>
<p style="color:#000">Zapisy na kursy spoza programu studiow beda mozliwe tylko za zgoda dziekana wlasnego wydzialu lub dyrektora jednostki - w ramach wolnych miejsc po korekcie zapisow. Wpis bedzie mozliwy w drodze administracyjnej (poza indywidualnymi zapisami) w sekretariacie Studium Nauk Humanistycznych i Spolecznych, sekretariacie Studium Wychowania Fizycznego i Sportu oraz w pokoju 105 w budynku H4 w Studium Jezykow Obcych w dniu 22.02.2019 w godz. 09.00-14.00. Wpis na kurs spoza programu studiow podlega takim samym rygorom (wymog zaliczenia, obowiazek powtarzania), jak wpis na kurs w ramach programu studiow.</p>

<h2>UWAGI</h2>
<ul>

<li>Studenci, ktorzy nie zaliczyli kursu realizowanego poza programem studiow sa zobowiazani do ukonczenia kursu powtorkowego (pod rygorem niedopuszczenia do egzaminu dyplomowego). W zwiazku z powyzszym zostanie umozliwiony wpis administracyjny na taki kurs w dniu <b>20.02.2019</b> w godz. 8.00-18.00 we wlasciwym Studium (wpis na zajecia sportowe w budynku H-14, pok. -1-19).</li>
<li>Po terminie korekty zapisow na jezyki obce, WF i przedmioty humanistyczno-menedzerskie wypisanie sie z zajec nie bedzie mozliwe. Wpis na zajecia kolidujace z innymi nie jest blokowany, ale nie moze byc powodem do wypisania z takich zajec po korektach zapisow.</li>
<li>Wpis na kurs powtorkowy wygeneruje odpowiednie kwoty w zobowiazaniach finansowych studenta. <b>Do dnia wskazanego w podpisanej umowie na swiadczenie uslug edukacyjnych nalezy oplacic i odpowiednio rozliczyc oplaty powstale z tytulu zapisu na kursy powtorkowe.</b> Wobec studentow, ktorzy tego nie zrobia, zostana podjete odpowiednie kroki przewidziane Regulaminem studiow wyzszych w Politechnice Wroclawskiej  - lacznie ze skresleniem z listy studentow.</li>
<li>Dla zapisow na jezyki obce, WF i przedmioty humanistyczno-menedzerskie zostanie stworzona kolejka zapisowa, gdzie kazdy student bedzie mial przydzielony swoj termin zapisow. W uzasadnionych i udokumentowanych przypadkach losowych mozna wnioskowac do Prorektora ds. Nauczania o zmiane terminu zapisow ogolnouczelnianych. Wnioski w tej sprawie mozna skladac, w dniach 13-18.02.2019 w godz. 9.00-14.00, w pok. 153 w budynku A-1, tel. 71-320-34-41, mail: barbara.cop-kowalska@pwr.edu.pl. Problemy i watpliwosci zwiazane z konkretnymi zajeciami oraz kursami nalezy wyjasniac w jednostkach oferujacych dane zajecia i kurs. Bledy w danych osobowych nalezy zglaszac na wlasnym wydziale.</li>
</ul>
