<p><b>16 lutego 2010 Alawar wydal wazna aktualizacje do gry Farma marzen</b></p>

<p>Milo nam poinformowac o wydaniu nowej wersji Farmy marzen, naszej popularnej gry farmerskiej!  Developerzy, na podstawie otrzymanych opinini graczy rozbudowali i ulepszyli gre. W rezultacie rozrywka jest teraz jeszcze bardziej ekscytujaca!</p>

<p>Ten duzy dodatek do gry Farma marzen wprowadza siedem nowych budynkow produkcyjnych i umozliwia wytwarzanie 27 rodzajow dobr glownych i 60 super dobr. Super dobra czasami pojawiaja sie na uprawianym przez gracza polu i mozna wymienic je na klejnoty.</p>

<p>Deweloperzy dodali takze nowe sciezki kariery. Gracz moze wybrac jedna z trzech profesji, by rozbudowac swoje istniejace miasto lub wzniesc nowe. Setki graczy moze kumunikowac sie za pomoca w pelni funkcjonalnego czatu, wymieniac sie dobrami, podpisywac zbiorowe kontrakty, otrzymywac premie za wykonana prace, a takze zaludniac i rozbudowywac miasta.</p>
<h2 class="center_text online">Witaj na Farmie marzen!</h2>
<p><b>Witaj na Farmie marzen, symulacji dla wszystkich, ktorzy lubia laczyc rozrywke z prowadzeniem biznesu</b></p>
<p>Stworz swoja wlasna farme online! Farma marzen to symulacja, ktora oddaje w rece gracza mozliwosc prowadzenia wirtualnego biznesu z uzytkownikami na calym swiecie.
Bawi i jednoczesnie uczy jak prowadzic biznes i zostac odnoszacym sukcesy przedsiebiorca!</p>
<p>Ale to nie wszystko. W Farmie marzen za swoje dokonania mozna otrzymac zlote gwiazdki, a nastepnie kupic za nie nasiona, narzedzia, ulepszenia lub ziemie pod budynki produkcyjne.</p>
<p>Farma marzen jest nieustannie rozwijana i ulepszana, dlatego juz dzis dolacz do fascynujacej spolecznosci online, by na wlasne oczy przekonac sie jak gra, a wraz z nia twoja writualna farma rozwija sie jak paki kwiatow na wiosne!
Czy zdolasz zostac najbogatszym farmerem na swiecie?</p>
