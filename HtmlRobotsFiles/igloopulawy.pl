<div id="content">	<h2>O firmie</h2>
<p class="hr"></p>
<div>
<p style="text-align: justify;">&quot;<strong>IGLOO</strong>&quot; jest rozwijajaca sie firma, zajmujaca sie <strong>sprzedaza, swiadczeniem uslug</strong> i <strong>serwisem</strong> w zakresie <strong>urzadzen gastronomicznych, klimatyzacyjnych</strong> i<strong> chlodniczych</strong>.</p>
<p style="text-align: justify;">Specjalizujemy sie w kompleksowym wyposazaniu w sprzet niezbedny do wlasciwego i zgodnego z przepisami funkcjonowania r&oacute;znych obiekt&oacute;w gastronomicznych m.in. takich jak:</p>
<ul class="unIndentedList">
<li>stol&oacute;wki szkolne</li>
<li>stol&oacute;wki przedszkolne</li>
<li>bursy</li>
<li>kawiarnie</li>
<li>bary</li>
<li>domy weselne</li>
<li>restauracje i inne</li>
</ul>
<p style="text-align: justify;">Wyposazamy takze sklepy spozywcze i plac&oacute;wki handlowe w niezbedne urzadzenia do prawidlowego funkcjonowania takiego obiektu - sprzet chlodniczy, krajalnice, wagi, meble i inne.</p>
<p style="text-align: justify;">Firma &bdquo;IGLOO&quot; poza kompleksowym zaopatrzeniem obiekt&oacute;w w sprzet gastronomiczny i chlodniczy zajmuje sie takze realizacja inwestycji w zakresie <strong>wykonywania system&oacute;w klimatyzacyjnych</strong> jak r&oacute;wniez <strong>kom&oacute;r chlodniczych i mrozniczych </strong>dostosowanych do potrzeb r&oacute;znych klient&oacute;w. Wykonywane przez nas realizacje sa o r&oacute;znej skali wielkosci i zlozonosci poczawszy od malych pomieszczen po kilkukondygnacyjne budynki.</p>
<p style="text-align: justify;">Zrealizowane przez nas inwestycje objete sa <strong>gwarancja</strong> jak r&oacute;wniez <strong>obsluga serwisowa</strong> po zakonczeniu okresu gwarancyjnego.</p>
<p style="text-align: justify;"><strong>Uslugi serwisowe</strong> swiadczymy r&oacute;wniez w zakresie instalacji klimatyzacyjnych i chlodniczych na zlecenie klient&oacute;w dla istniejacych juz system&oacute;w.</p>
<p style="text-align: justify;">Dokladamy wszelkich staran, by sprostac wymaganiom klient&oacute;w w kazdej fazie inwestycji. Sluzymy nasza wiedza i doswiadczeniem przy doborze urzadzen. Szeroki asortyment oferowanych towar&oacute;w pozwala na dostosowanie oferty do potrzeb i mozliwosci finansowych klienta.</p>
<p style="text-align: justify;">Oferowane przez nas urzadzenia sa zgodne ze standardami miedzynarodowymi i polskimi wymaganiami.</p>
<p style="text-align: justify;">Wsp&oacute;lpracujemy z wieloma firmami takimi jak: <strong>COLD, RAPA, ES SYSTEM K ,KROMET, RM GASTRO, FAGOR, STALGAST, GASTRO-TAR, EGAZ, GASTMED, ATUT, STOLARSTWO WALCZAK, MA-GA, BECKERS, CAS, DIGILAB, NOVITUS, POL-STOWEST, TERMO SCHIESSL, SYSTHERM, BERLING, JANPOL, ICATEQ</strong> i inne.</p>
