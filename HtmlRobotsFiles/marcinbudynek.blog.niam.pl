<a href="/pl/post/192" class="clr title lt">Jesienne smaki</a>

<div class="clr"></div>
<a href="/pl/posts/1/kuchnia_polska" class="category-s lt">Kuchnia polska</a>

<div class="clr rlv">
<div class="clr post-content"> <br />
Jesien to przede wszystkim czas zbiorow wielu cennych i smacznych produktow, warto wiec zainteresowac sie wykorzystaniem ich w swoim menu....<br />
Najsmaczniejsze warzywa o tej porze to warzywa dyniowate, do ktorych zaliczamy patisony, kabaczki czy cukinie. Co ciekawe w Polsce sezon na dynie trwa od wrzesnia az do grudnia. W naszym regionie wystepuja dwa glowne gatunki: dynia olbrzymia i dynia zwyczajna. Dynia jest niezwykle ceniona ze wzgledu na swoje wlasciwosci odzywcze. Dlatego tez warzywo to zostalo docenione w kuchni, a z jego miazszu mozna przyrzadzac przerozne potrawy. Warzywo to wykorzystywane jest w kuchniach calego swiata. Dynie chetnie dodaje sie do ciast, a jej pestki do pieczywa czy slodkich ciasteczek. Kabaczki czy cukinie doskonale sprawdzaja sie jako baza smazonych placuszkow np. z dodatkiem fety czy sera plesniowego, oraz warzywnych gulaszy. Ich miazsz mozna dodawac takze do slodkich wypiekow.<br />
Innym obok dyniowatych cenionym jesiennym warzywem sezonowym jest baklazan, zaliczany tak jak pomidor czy papryka do rodziny psiankowatych, wspaniale nadaje sie do pieczenia, podsmazania, duszenia w potrawkach warzywnych i miesnych. Baklazan nie lubi jednak przymrozkow, dlatego zbiera sie go we wczesnych miesiacach jesiennych.<br />
Do warzyw, ktore w przeciwienstwie do baklazana lubia dosc niskie temperatury, mozna zaliczyc wszystkie warzywa kapustne - zajmujace w kuchni polskiej bardzo wazne miejsce oraz warzywa korzeniowe: pietruszke, marchew czy buraki. Wsrod kapustnych, poza popularna kapusta, nalezy wymienic brukselke. Wiele z warzyw czy owocow jesiennych nadaje sie do przyrzadzania z nich przetworow i marynat. Polska kuchnia slynie z przedluzania naturalnych wlasciwosci warzyw i zamykania ich smakow w sloiku. Owoce, ktore zbierane sa w okresie jesiennym, takie jak: jablka, sliwki, gruszki to znakomita baza powidel, dzemow, kompotow, a takze marynat. Marynowane gruszki czy sliwki to w wielu domach nieodzowny element zblizajacych sie zimowych obiadow.<br />
Jesien to przeciez pora na wszelkie dziko rosnace smakolyki, chetnie wykorzystywane w kuchni polskiej. Na czolowe miejsce wysuwaja sie oczywiscie grzyby. Jesien to najlepsza pora na grzybobranie, gdyz teraz wlasnie jednoczesnie wystepuje najwiecej gatunkow. Wsrod nich sa podgrzybki i borowiki, zaliczane do grzybow kapeluszowych, ktore w urodzajnych w grzyby latach goszcza w lasach az do pozniej jesieni. Borowiki rosna pojedynczo, ale niedaleko siebie. Spotyka sie je w lasach lisciastych. Czasami trzeba dlugo ich wypatrywac, gdyz czesto ukrywaja sie w sciolce lesnej. Maja dosc jasne kapelusze, od barwy blado-brazowej po orzechowo-brazowa. Znany jest rowniez wspomniany podgrzybek zaliczany do rodziny borowikow. Rozrozniamy miedzy innymi podgrzybki brunatne oraz zajaczki. Sa one najbardziej popularne i najlatwiejsze do rozpoznania. Czesto wykorzystuje sie je do suszenia i marynowania. Wystepuja zarowno w lasach iglastych, jak i lisciastych, uwielbiaja wilgotne podloze. W naszych polskich lasach czesto spotykany jest rowniez kozlarz, potocznie zwany kozlakiem. Grzyb ten jest uznany za bardzo smaczny i czesto wykorzystywany kulinarnie. Natomiast grzybem, ktory zdecydowanie upodobal sobie lasy iglaste jest maslak o kleistym kapeluszu. Bardzo smaczny szczegolnie z odrobina smietany i cebulka, niemniej jednak pracochlonny w przygotowaniu, gdyz przed przyrzadzeniem nalezy dokladnie obrac kapelusze. Wsrod rownie popularnych grzybow mozna rowniez wymienic: gaski, pieczarki, opienki, golabki oraz coraz rzadziej spotykane w Polsce, ale niezwykle cenione i smaczne - szlachetne rydze.<br />
Wczesna jesienia las obdarowuje nas rowniez innymi przysmakami np. dzikimi malinami i jezynami, ktore sa zrodlem witaminy C. Mozna z nich przyrzadzac dzemy oraz dodawac do ciast.<br />
Przetwory - pigwa, deren, sosna...w koncu powracaja do lask unikatowe produkty kuchni polskiej. Bardzo lubie robic wlasne nalewki, ktore pozniej wykorzystuje do sosow czy marynat. Nie tylko dodaja potrawa smak ale tez swietnie rozgrzewaja w chlodne jesienne wieczory.<br />
Kolejnym waznym i cenionym produktem sezonowym, ktory ostatnimi laty wraca do lask, jest gesina. Dawniej bardzo czesto goscila na polskich stolach, ale na dlugie lata o niej zapomniano, a mieso gesi wysylano glownie na eksport.  Mieso, a takze gesie podroby, mozna wykorzystac w kuchni na wiele sposobow. Mieso tychze ptakow jest niezwykle smaczne a co wiecej bardzo zdrowe, choc pewnie nie wiele osob o tym wie. Gesina staje sie coraz bardziej popularna zwlaszcza wsrod restauratorow, ktorzy holduja tradycji i walcza o dobry smak i wysoka jakosc potraw opartych na produktach sezonowych i lokalnych.<br />
Jesienne miesiace to takze wspanialy czas na polow ryb, zwlaszcza tych drapieznych takich jak np: szczupak czy sandacz, ktore wlasnie o tej porze roku staja sie bardzo zarloczne. Szczupak ceniony jest ze wzgledu na delikatne, biale mieso. Mieso szczupaka doskonale nadaje sie do wedzenia. Mozna je takze piec, smazyc, dusic. Sandacz, to druga typowo polska ryba, dawniej mile widziana na szlacheckich stolach. Sandacz ma chude mieso, dlatego tez ryba ta zalecana jest w diecie odchudzajacej. Rybe mozna przyrzadzac na wiele sposobow. Mozna ja dusic, smazyc, a takze serwowac w galarecie.<br />
Stek wolowy z kaszanka, borowikami i kremem paprykowym<br />
Skladniki:<br />
Stek wolowy - 200 g<br />
olej - 20 ml<br />
Pigwowka - 20 ml<br />
Kaszanka - 100 g<br />
Borowiki suszone - 50 g<br />
Szalotka - 1 szt<br />
Czosnek - 1 zabek<br />
Papryka czerwona, zolta - 1 szt<br />
Swieze ziola<br />
Sol, pieprz<br />
Sposob wykonania:<br />
Na patelni przesmazamy cebule szalotke, borowiki, czosnek, dodajemy kaszanke calosc chwile smazymy. Na koniec doprawiamy sola, pieprzem oraz swiezymi ziolami.<br />
Papryki pieczemy w piekarniku ok 15 min, nastepnie usuwamy gniazdo nasienne oraz skore i miksujemy do uzyskania puree, na koniec przyprawiamy do smaku.<br />
Steka marynujemy w oleju, pigowce oraz swiezych ziolach okolo godzine w lodowce. Po tym czasie smazymy ok 3 minut z kazdej strony, nastepnie wkladamy do piekarnika rozgrzanego do 180 st C na 10 minut. Steka wyciagamy z piekarnika czekamy chwile aby ,,odpoczal", nastepnie kroimy w plasterki. Gotowe mieso podajmy z kaszanka i borowinami, grzanka z chleba orkiszowego oraz kremem paprykowym.<br />
