<h1 class="subtitle is-1" id="z__O-nas">
O nas                        </h1>
<div>
<p><strong>Studio Urody Anna</strong>&nbsp;powstalo z mysla o kompleksowej i profesjonalnej obsludze coraz bardziej wymagajacych klientek, dla kt&oacute;rych wazna jest wysoka jakosc uslug, wyspecjalizowany personel, najwyzszej jakosci kosmetyki, komfort i ... przystepna cena. Kazda z nas dobrze wie, jak wiele zmienia fryzura, makijaz, styl i kolor.<br />
Niekiedy jednak brakuje nam czasu, odwagi lub po prostu wiedzy by wygladac tak, jak bysmy chcialy. Kazda kobieta to dla nas inspiracja. Reka w reke idziemy z nia prosta droga do piekniejszego ja. Pokazujemy jej, ze moze cieszyc sie nie tylko efektem, ale r&oacute;wniez pielegnacja, kt&oacute;ra do niego wiedzie. Uczymy sie od siebie nawzajem, jak byc piekna. W naszym studio oferujemy pelen zakres uslug kosmetycznych i fryzjerskich, sluzymy porada w doborze fryzury, koloru czy makijazu.<br />
Naszym celem jest zadowolony klient, dlatego wszystkie uslugi wykonujemy przy uzyciu tylko najwyzszej klasy kosmetyk&oacute;w pielegnacyjnych i stylizacyjnych.<br />
Studio Urody Anna jest komfortowo i profesjonalnie wyposazone w najwyzszej jakosci sprzet. Panuje tu mila atmosfera, a do Panstwa dyspozycji sa kosmetyczki i fryzjerka, kt&oacute;re zawsze chetnie odpowiedza na wszelkie pytania.<br />
<br />
Serdecznie zapraszamy.</p>
