<h4>O konkursie<br />,,ZABYTEK ZADBANY"</h4>
<hr>
</li>
<li class="col-md-8">
<p style="text-align: justify;">Stowarzyszenie Ochrony Narodowej Dziedzictwa Materialnego zacheca do uczestnictwa w corocznym konkursie budowlanym, oglaszanym przez Ministra Kultury i Dziedzictwa Narodowego pod nadzorem Generalnego Konserwatora Zabytkow.</p>

<p style="text-align: justify;">Kto moze wziad udzial w konkursie? Wlasciciele, posiadacze i zarzadcy zabytkow wpisanych do rejestru zabytkow.</p>

<p style="text-align: justify;">Co jest celem konkursu? Rozpowszechnienie idei opieki nad zabytkami oraz przekazanie pozytywnych wzorcow, potrzebnych do wlasciwego utrzymania i zagospodarowania zabytkowych obiektow. Konkurs ,,ZABYTEK ZADBANY" ma rowniez charakter edukacyjny i sluzy popularyzacji wiedzy na temat wlasciwego postepowania z zabytkami architektury podczas przeprowadzanych remontow, a takze codziennego uzytkowania.</p>

<p style="text-align: justify;">Konkurs ,,ZABYTEK ZADBANY" podzielony jest na kategorie:</p>


<ul class="list">
<li>Utrwalenie wartosci zabytkowej obiektu</li>
<li>Rewaloryzacja przestrzeni kulturowej i krajobrazu (w tym zalozenia dworskie i palacowe)</li>
<li>Adaptacja obiektow zabytkowych</li>
<li>Architektura i budownictwo drewniane</li>
<li>Architektura przemyslowa i budownictwo inzynieryjne</li>
<li>Kategoria specjalna: wlasciwe uzytkowanie i stala opieka nad zabytkiem</li>
</ul>

<p style="text-align: justify; margin-top: 0px"><br><br>Instytucja realizujaca konkurs jest Narodowy Instytut Dziedzictwa (NID).</p>

<a class="phone" href="/#kontakt">Skontaktuj sie z nami</a></li>
</ul>
