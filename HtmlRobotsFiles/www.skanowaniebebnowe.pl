Efektem naszej pracy jest <b>16&nbsp;bitowy obraz w formacie TIFF z profilem Adobe RGB bez zadnych korekcji</b> (wyostrzania czy tez zmian kolorow poza oprofilowaniem materialow pozytywowych).<br/>
Dodatkowo oferujemy usuwanie ,,smieci", ktore nie zostana wyeliminowane przez plyn skanujacy. Cena postprocessingu zalezy od ilosci pracy jaka trzeba wlozyc w retusz zdjecia i jest ustalana indywidualnie.

