<h1>Jakosc</h1>
Jakosc = dobry skan, lecz czym sie on wyroznia? W wiekszosci przypadkow jakosc kojarzona jest z ostroscia zdjecia przy duzym powiekszeniu - duza rozdzielczoscia/iloscia megapikseli. Jednak nie jest to jedyny aspekt skanu, na ktory nalezy zwrocic uwage. <!--Wiekszosc cech ukazujacych roznice pomiedzy skanami zostala opisana ponizej wraz z przykladami.-->
