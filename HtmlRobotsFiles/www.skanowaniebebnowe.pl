<h1>Skanowanie bebnowe</h1>
W dobie aparatow cyfrowych fotografia analogowa jest przede wszystkim <b>medium, ktorego uzywaja artysci i specjalisci</b>. Dla obydwu tych grup w wiekszosci przypadkow jakosc zdjecia jest priorytetem. Obrazy analogowe przetworzone na postac cyfrowa musza byc <b>jak najblizsze rzeczywistemu odpowiednikowi</b> pod wzgledem kolorow, gradacji przejsc tonalnych oraz informacji w swiatach, jak i w cieniach. Urzadzeniami, ktore bez problemow sprawdzaja sie w takich przypadkach sa skanery bebnowe. Technologia ta znana jest od lat i do tej pory jest to najlepszy sposob na cyfryzacje filmow fotograficznych. O tym jakie sa roznice miedzy urzadzeniami i co potrafi taki sprzet mozesz przeczytac ponizej.
