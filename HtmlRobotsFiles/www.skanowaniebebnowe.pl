<h2>Rozdzielczosc</h2>
Wedlug producenta nasze urzadzenie potrafi skanowac materialy transparentne z rozdzielczoscia do 11000 dpi jednak z przeprowadzonych przez nas testow wynika, iz realna rozdzielczosc to okolo 5500 dpi. Wystarcza to do zeskanowania wszystkich zawartych na filmie detali bez nadmiernego uwydatniania ziarna filmu. Daje to obraz o wielkosci prawie 15000 x 12000 pixeli <b>przy skanie klatki 6x7</b>, co przy wydruku z jakoscia 360 dpi (natywna rozdzielczosc drukarek firmy Epson) <b>daje wydruk o wielkosci 105 x 85 cm</b>. Oczywiscie ziarno filmu, ostrosc obiektywu oraz sposob wykonania zdjecia maja bardzo duzy wplyw na jakosc wywolanego filmu, a tym samym pozniejszego skanu i wydruku.

