<h2>Efekt flary</h2>
Zjawisko flary wystepuje gdy na aktualnie skanowane ciemne fragmenty filmu maja wplyw sasiadujace z nim jasne powierzchnie. Skutkuje to efektem blyszczenia/swiecenia jasnych elementow. Powodowane jest ono rozproszeniem swiatla i jego wplywem na przylegajace pixele. W idealnym skanerze swiatlo powinno oswietlac tylko jeden, skanowany pixel, dzieki temu kazdy pixel moglby zostac odczytany niezaleznie. Wbrew pozorom roznica jasnosci punktow wcale nie musi byc duza, aby efekt flary wplywal na jakosc zdjecia. Wystepowanie tego niechcianego efektu zalezy od budowy urzadzenia skanujacego i jego jakosci.

