<h1>Dlaczego my?</h1>
<h2 class='mt-30 special_h2'>Krotko o mojej przygodzie z analogami...</h2>
Moja przygoda z aparatami analogowymi rozpoczela sie dosc niedawno - w 2014 roku, gdy moj Przyjaciel stwierdzil, iz zdjecia wygladaja dobrze tylko na filmie. Po dokladnym  przeanalizowaniu zalet i wad techniki analogowej stwierdzilem, ze powinienem sprobowac. Zainteresowany bylem glownie kolorami, ich separacja oraz mozliwoscia tworzenia duzych odbitek, ktora daja slajdy. Z tego powodu od razu postawilem na sredni format - aparat <b>Bronica GS1</b>. Niedlugo potem nabylem pierwszy skaner - Epson V750. Wielce zachwalany skaner plaski, ktory wg niektorych (lub wielu) dorownuje jakoscia skanerom dedykowanym, a nawet bebnowym. Po wielu godzinach walki udalo mi sie uzyskac satysfakcjonujace mnie rezultaty dzieki uzyciu dodatkowych szklanych szybek, oprofilowaniu skanera oraz pieczolowitemu czyszczeniu filmow przed skanowaniem. Wszystko bylo by rewelacyjnie gdyby nie wspomniany wczesniej fotomaniak, wg ktorego jakosc skanow byla dobra dopiero po skanowaniu bebnowym (ot taki swiety gral skanerzystow). Kilka miesiecy pozniej stalem sie szczesliwym posiadaczem skanera <b>SCANMATE 4000</b>. Jakosc jego skanow, pomimo wielu wad, zostawiala w tyle Epsona V750. Niestety jego wiek, wrodzona tendencja do falowania i szumow w cieniach doprowadzaly mnie do granic wytrzymalosci psychicznej. Odpuscilem sobie skanowanie, zabralem sie za robienie zdjec, a w miedzyczasie szukaniem innego skanera. <b>Obecnie posiadam Heidelberg Primescan D8200</b>, ktory wyciaga praktycznie wszystko co jest mozliwe z kazdej klatki.
