<h2>Od czego zalezy ogolna jakosc skanu</h2>
<b>Stan i jakosc dostarczonych filmow ma bezposredni wplyw na jakosc skanow</b>. Owszem technologia skanowania na mokro eliminuje sporo problemow dotyczacych zabrudzen i rys ale nie jest to magia, ktora usunie wszelkie uszkodzenia mechaniczne. Mocno podrapane, zabrudzone, pogiete filmy maja mala szanse na doskonale zeskanowanie. Film w lepszym stanie bedzie lepiej zeskanowany. Za ostrosc elementow na zdjeciu odpowiada oczywiscie optyka aparatu, jego ustawienie oraz sam proces tworzenia zdjecia (wiatr, uzycie statywu, czas naswietlania itp).
