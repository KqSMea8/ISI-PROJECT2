<div class="article_title">Witamy na stronie</div><div class="article_content"><p>
&nbsp;</p>
<div>
W trwajacej od 2000 r. dzialalnosci zdobylismy bogate doswiadczenie w projektowaniu roznego typu instalacji i sieci dla budynkow i obiektow budowlanych. Nasza firma oferuje uslugi w zakresie:</div>
<table style="width: 900px;">
<tbody>
<tr>
<td style="vertical-align: top;">
<img alt="Rurociagi pary i kondensu na estakadzie. Kompania Piwowarska Lech w Poznaniu." src="/upload/IMG_3456.jpeg" style="width: 320px; height: 213px; float: left;" /></td>
<td style="vertical-align: top;">
<ul>
<li>
projektowania instalacji wentylacji,</li>
<li>
projektowania systemow wentylacji oddymiajacej i zapobiegajacych zadymieniu,</li>
<li>
projektowania instalacji klimatyzacji,</li>
<li>
projektowania instalacji i sieci wodno-kanalizacyjnych,</li>
<li>
projektowania instalacji grzewczych,</li>
<li>
projektowania instalacji gazowych,</li>
<li>
projektowania instalacji sprezonego powietrza i prozni technologicznej,</li>
<li>
projektowania instalacji parowych,</li>
<li>
projektowania kotlowni i wezlow parowych,</li>
<li>
projektowania instalacji hydrantowych i tryskaczowych,</li>
<li>
wykonania symulacji numerycznych CFD (Computational Fluid Dynamics) wentylacji oddymiajacej,</li>
<li>
wykonania symulacji ewakuacji.</li>
</ul>
</td>
</tr>
</tbody>
</table>
<div>
Szczegolna uwage przywiazujemy do zabezpieczen przeciwpozarowych, a w szczegolnosci do wentylacji oddymiajacej i zapobiegajacej zadymieniu.</div>
<div>
Zdajac sobie sprawe z koniecznosci weryfikacji skutecznosci wentylacji oddymiajacej juz na etapie projektowania, w 2010 r. utworzylismy komorke zajmujaca sie symulacjami numerycznymi CFD. Opracowywane przez nasze biuro projekty wentylacji oddymiajacej sa weryfikowane na podstawie symulacji numerycznych. Symulacje prowadzimy w oparciu o program <strong>Fire Dynamic System </strong>(FDS) z NIST (National Institute of Standards and Technology - Narodowy Instytut Standaryzacji i Technologii, USA), z wykorzystaniem systemu modelowania PyroSim &nbsp;firmy Thunderhead.</div>
<div>
Kluczowa kwestia w ocenie skutecznosci wentylacji oddymiajacej jest prawidlowe wyznaczenie wymaganego czasu bezpiecznej ewakuacji (RSET). Z tego powodu podczas weryfikacji skutecznosci wentylacji oddymiajacej nie ograniczamy sie do analitycznego okreslenia RSET, lecz dodatkowo sprawdzamy przebieg ewakuacji z plonacego budynku symulatorem ewakuacji ludzi <strong>Pathfinder </strong>firmy Thunderhead.</div>
<div>
&nbsp;</div>
<div>
U podstaw naszej dzialalnosci lezy wysoka jakosc i spojnosc opracowywanej dokumentacji. Z tego powodu wdrozylismy specjalistyczne oprogramowanie <strong>Autodesk Autocad MEP</strong> na wszystkich stanowiskach roboczych. Dzieki temu mamy mozliwosc projektowania wszystkich instalacji w przestrzeni 3D oraz koordynacji miedzybranzowej.&nbsp;</div>
<div>
Na zyczenie Inwestora i przy wspolpracy z biurami architektonicznymi pracujacymi w programie Autodesk Revit jestesmy w stanie opracowac pelnobranzowy projekt w technologii BIM (Building Information Modeling) przy uzyciu licencjonowanego programu <strong>Autodesk Revit MEP</strong>.</div>
<div>
&nbsp;</div>
<div>
W czasie dzialalnosci naszego biura zdobylismy doswiadczenie w projektowaniu instalacji i sieci dla szerokiego spektrum obiektow. &nbsp;W naszym portfolio znajduja sie obiekty wielko- i malokubaturowe. Do wszystkich realizowanych przez nas projektow podchodzimy z wielkim zaangazowaniem i starannoscia.</div>
<div>
&nbsp;</div>
<div>
Zapraszamy do wspolpracy.</div>
