<h1 class="title-home">O naszej firmie</h1>
</div>
<div class="col-md-8 col-sm-12 col-xs-12 wrapper-text-about-us">
<p>Jestesmy polskim producentem okien PCV o nietypowych ksztaltach, wymiarach i kolorach. W naszej ofercie znajduja sie okna okragle, owalne, polokragle, lukowe, w formie trojkatow, trapezow oraz nieregularnych ksztaltow wykonywanych na podstawie szablonow. Produkujemy okna rozwierne, uchylne, fixy, obrotowe, rozwierno-uchylne, jednoskrzydlowe lub dwuskrzydlowe. Do naszych okien oferujemy rowniez szeroki wybor klamek, moskitier, szprosow, parapetow, listw wykonczeniowych oraz akcesoriow montazowych. Kola, luki, elipsy, trojkaty i trapezy - to nasza codziennosc.
</p>
