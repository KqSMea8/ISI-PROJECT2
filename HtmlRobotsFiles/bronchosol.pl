<div class="qa">
<h2>Warto wiedziec</h2>
<div class="columns">
<div class="col1">
<ul class="qa-list">
<li>
<p class="question"><a href="#">Czym jest kaszel?</a></p>
<p class="answer">Kaszel to naturalny odruch, stanowi mechanizmem obronnym organizmu i&nbsp;pojawia sie, jako wynik pobudzenia receptorow kaszlu znajdujacych sie w&nbsp;drogach oddechowych.<br /><br />Kaszel moze byc spowodowany draznieniem receptorow przez rozne nieswoiste czynniki takie jak: zimne powietrze, zanieczyszczenia powietrza, wdychany kurz lub pyl, dym papierosowy, czyli przez czynniki srodowiskowe. Dziecko moze kaszlec, kiedy sie zachlysnie sie slina lub je kwasne owoce. Taki kaszel pozwala oczyszczac drogi oddechowe ze wszystkiego, co je drazni nie jest objawem, ktory powinien niepokoic rodzicow.<br /><br />Inaczej jest, kiedy u dziecka wystapi nagly lub uporczywy kaszel lub towarzysza mu inne objawy jak katar i&nbsp;podwyzszona temperatura ciala. Wtedy kaszel jest wynikiem stanu zapalnego drog oddechowych, nadmiernej ilosci wydzieliny albo ciala obcego znajdujacego sie w&nbsp;drogach oddechowych.<br /><br />Charakteru kaszlu, jego nasilenie, sezonowosc, zwiazek z&nbsp;pora dnia, pozycja ciala,  wysilkiem, czas trwania oraz okolicznosci w&nbsp;jakich kaszel wystapil po raz pierwszy  pozwalaja zidentyfikowac czy kaszel jest juz jest objawem chorobowym czy tylko naturalna reakcja obronna organizmu na podraznienie.</p>
</li>
<li>
<p class="question"><a href="#">Skad bierze sie kaszel?</a></p>
<p class="answer">Przyczyny kaszlu moga byc bardzo rozne. Do najczestszych winowajcow kaszlu zaliczamy:<br /><br />- infekcje wirusowe i&nbsp;bakteryjne (wirusy sa najczestsza z&nbsp;przyczyn zapalen ukladu oddechowego, jednak czesto dolaczaja sie nadkazenie bakteryjne)<br />- cialo obce<br />- podglosniowe zapalenie krtani<br />- przewlekle zapalenie zatok<br />- przerost migdalka gardlowego (,,trzeci migdal")<br />- alergiczny niezyt nosa i&nbsp;przewlekly niezyt nosa<br />- astma oskrzelowa<br />- refluks zoladkowo-przelykowy</p>
</li>
<li>
<p class="question"><a href="#">Czas wystepowania kaszlu</a></p>
<p class="answer"><strong>Kaszel ostry</strong> czesto jest zwiazany z:<br />- zakazeniem wirusowym lub bakteryjnym ukladu oddechowego<br />- moze byc efektem aspiracji (wessania podczas zachlysniecia) ciala obcego (orzeszkow, pestek slonecznika, dyni, drobnych elementow zabawek itp.).<br /><br />Zaaspirowanie ciala obcego sygnalizuja napady gwaltownego, krotko trwajacego kaszlu. Pozniej bedzie to przewlekle pokaslywanie. Podejrzenie zaaspirowania jakiegos przedmiotu jest wskazaniem do wykonania badania bronchoskopowego.<br /><br /><strong>Kaszel przewlekly</strong> moze byc spowodowany splywaniem wydzieliny po tylnej scianie gardla, a stamtad do oskrzeli. Taki kaszel jest objawem zapalenia zatok, niezytu nosa, przerostu migdalka. Taki kaszel jest suchy, ma zmienne nasilenie. Jego rozpoznanie wymaga konsultacji laryngologa<br /><br /><strong>Kaszel poinfekcyjny</strong> - czesto kaszel przewlekly jest wynikiem powoli ustepujacego ostrego zapalenia oskrzeli, wtedy kaszel moze wydluzyc sie nawet do 6 tygodni, pomimo ustapienia innych objawow.</p>
</li>
<li>
<p class="question"><a href="#">Rodzaje kaszlu</a></p>
<p class="answer"><strong>Kaszel suchy</strong> wystepuje czesto w&nbsp;pierwszym okresie infekcji (ok. 3 dni), gdy rozmnazajace sie bakterie uszkadzaja blone sluzowa pokrywajaca drogi oddechowe. Kaszel suchy - nieproduktywny (bo nie towarzyszy mu zadna wydzielina) bardzo podraznia drogi oddechowe, jest meczacy, zwlaszcza wtedy, gdy wystepuje w&nbsp;nocy. Towarzyszy mu czesto ostry bol gardla i&nbsp;pieczenie.<br /><br /><strong>Kaszel wilgotny (produktywny)</strong> to taki, ktoremu towarzyszy wydzielina. W&nbsp;czasie infekcji bakteryjnej lub wirusowej po 2-3 dniach slyszymy bardzo charakterystyczny odglos z&nbsp;pluc, tak jakby zaczynalo ,,grac w&nbsp;plucach" i&nbsp;odkasluje sluz. Taki kaszel to wlasnie kaszel wilgotny. Gesty sluz znajdujacy sie w&nbsp;drogach oddechowych pobudza odruch kaszlu, i&nbsp;w&nbsp;ten sposob organizm probuje sie go pozbyc. Mokry kaszel utrzymuje sie nawet kilka tygodni dopoki drogi oddechowe nie zostana calkowicie oczyszczone.<br /><br /><strong>Kaszel suchy szczekajacy</strong> - moze byc objawem zapalenia krtani. Czesto towarzyszy mu chrypka. Atak kaszlu pojawia sie nagle nad ranem. Osoba traci glos i&nbsp;ma klopoty z&nbsp;mowieniem. Z&nbsp;trudnoscia lapie oddech, a kiedy stan sie pogorszy obrzek bony sluzowej krtani powoduje, ze czlowiek sie dusi. W&nbsp;tej sytuacji nalezy niezwlocznie wezwac lekarza.<br /><br /><strong>Kaszel moze byc tez objawem alergii.</strong> Kiedy czlowiek jest zdrowy nic mu nie dolega, ale kaszel pojawia sie na przyklad po kontakcie ze zwierzetami, wtedy nalezy zglosic sie do alergologa. Czesto osoby leczone z&nbsp;powodu infekcji ukladu oddechowego maja w&nbsp;tle alergie lub astme. Lekarza wprowadza w&nbsp;blad kaszel i&nbsp;katar, ktore u osob alergicznych nie sa objawem infekcji, lecz alergii np. na siersc zwierzat, roztocza czy pylki roslin. Nieleczona alergia wziewna moze prowadzic do astmy oskrzelowej.<br /><br /><strong>Swiszczacy oddech</strong>, przewlekly kaszel i/ lub okresowa dusznosc sa objawem astmy. Taki stan wymaga bezwzglednej konsultacji pulmonologa i&nbsp;alergologa, wykonania testow skornych  alergicznych i&nbsp;badania spirometrycznego, po ktorych dobiera sie wlasciwa terapie. Astma to choroba na cale zycie, jednak wczesnie rozpoznana i&nbsp;kontrolowana przez specjaliste pozwala normalnie funkcjonowac.<br /><br /><strong>Kaszel psychogenny</strong> - jest to uporczywy, suchy, szczekajacy kaszel, ktory pojawia sie lub nasila pod wplywem bodzcow emocjonalnych (strach), w&nbsp;obecnosc osob trzecich (zle relacje z&nbsp;otoczeniem), ale nie w&nbsp;czasie snu.  Taki kaszel odzwierciedla emocje i&nbsp;wymaga dodatkowej konsultacji psychologa.</p>
</li>
</ul>
</div>
<div class="col2">
<ul class="qa-list">
<li>
<p class="question"><a href="#">O czym swiadczy typ kaszlu?</a></p>
<p class="answer"><strong>Suchy</strong> - poczatek infekcji, alergia, zapalenie pluc, zakrztuszenie sie orzeszkami lub malym przedmiotem<br /><strong>Wilgotny</strong> - druga faza infekcji, zapalenie oskrzeli<br /><strong>Szczekajacy</strong>- zapalenie krtani, tchawicy<br /><strong>Suchy i&nbsp;swiszczacy oddech</strong> - astma, choroby krtani i&nbsp;pluc<br /><strong>Napadowy</strong> - alergia, astma, koklusz</p>
</li>
<li>
<p class="question"><a href="#">Jak postepowac  z&nbsp;kaszlem?</a></p>
<p class="answer">W przypadku zwyklego przeziebienia mozemy strac sie opanowac objawy bez konsultacji z&nbsp;lekarzem. <strong>Kaszel w&nbsp;poczatkowej fazie infekcji jest suchy</strong> i&nbsp;wynika z&nbsp;podraznienia blony  sluzowej drog oddechowych, dlatego <strong>mozemy podac syropy lagodzace podraznienia drog oddechowych</strong> jak syrop z&nbsp;babki lancetowatej, prawoslazowy lub  z&nbsp;lisci podbialu. <strong>Suchy kaszel zwykle trwa 3 dni, a nastepnie przechodzi w&nbsp;kaszel wilgotny, wtedy powinnismy podac syrop wykrztusny</strong>. Lepka wydzielina zalegajaca w&nbsp;drogach oddechowych stanowi doskonala pozywke dla bakterii, ktore w&nbsp;sprzyjajacych warunkach szybko sie namnazaja i&nbsp;stwarzaja ryzyko zakazenia drog oddechowych.<br /><strong>Jesli odpowiednio nie zadbamy o siebie i&nbsp;przegapimy ten moment zwykle przeziebienie moze zamienic sie w&nbsp;zapalenie oskrzeli czy pluc.</strong><br /><br />Gdy kaszlesz przestrzegaj nastepujacych zasad:<br />&bull; Pomieszczenie w&nbsp;ktorym przebywasz powinno miec odpowiednia wilgotnosc. Zastosuj nawilzanie powietrza lub umiesc na kaloryferze pojemnik z&nbsp;woda.Wilgotne powietrze zlagodzi podraznienie sluzowki drog oddechowych, zmniejszy podraznienie i&nbsp;pieczenie gardla oraz suchy kaszel<br />&bull;	Zastosuj inhalacje z&nbsp;pary wodnej lub ziolowe (z dodatkiem olejku tymiankowego, eukaliptusowego lub suchych ziol: rumianku, tymianku). Inhalacje takie zlagodza podraznienie drog oddechowych oraz odkrztuszanie wydzieliny zalegajacej w&nbsp;drogach oddechowych.<br />&bull;	Pij czesto cieple napoje. Najlepiej herbatke z&nbsp;lipy, zwykla z&nbsp;dodatkiem soku malinowego, wode z&nbsp;miodem i&nbsp;cytryna (u dzieci powyzej 1 roku zycia).<br />&bull;	Czesto wietrz pomieszczenie, w&nbsp;ktorym przebywasz.<br />&bull;	Uloz pod glowe dodatkowe poduszki. Taka pozycja ciala ulatwi splywanie wydzieliny z&nbsp;nosa na tylna sciane gardla oraz jej odkrztuszanie.<br /><br />Oklepuj plecy (w przypadku wilgotnego kaszlu) kilka razy dziennie. Ulatwia to odrywanie sie wydzieliny zalegajacej w&nbsp;drogach oddechowych. Oklepywanie powinno sie wykonac pol godziny od podania syropu wykrztusnego.</p>
</li>
<li>
<p class="question"><a href="#">Jakie badania wykona lekarz?</a></p>
<p class="answer">Jesli cos Cie niepokoi albo domowa terapia nie przynosi poprawy najlepiej udaj sie do lekarza.<br />W celu rozrzedzenia zalegajacej wydzieliny zleci przyjmowanie lekow wykrztusnych i&nbsp;rozrzedzajacych wydzieline.<br />Jesli okaze sie, ze kaszel jest objawem bakteryjnego zapalenia oskrzeli lekarz z&nbsp;pewnoscia przepisze antybiotyk. Wazne aby przestrzegac zalecanej dawki i&nbsp;czasu przyjmowania leku.<br />W przypadku kaszlu przewleklego zaleca sie RTG klatki pieskowej, badanie laryngologiczne, badanie czynnosciowe ukladu oddechowego, diagnostyczne testy alergologiczne lub badanie plwociny.<br /><br /><strong>Wazne! badanie RTG klatki piersiowej</strong><br />Czesto ludzie negatywnie podchodza do skierowania na badanie RTG klatki piersiowej. Jednak bardziej szkodliwe moze byc zastosowanie kolejnego antybiotyku niz wykonanie zdjecia. Jest ono niezbednym i&nbsp;podstawowym elementem diagnostyki  w&nbsp;przypadku przewleklego kaszlu. Pozwala wykluczyc wady rozwojowe, srodmiazszowe zapalenie pluc, przerost grasicy niedodme pluc spowodowana zatkaniem przez ciala obce lub gesta wydzielina (mukowiscydoza).</p>
</li>
<li>
<p class="question"><a href="#">Jakiego rodzaju leczenie stosuje sie w&nbsp;kaszlu?</a></p>
<p class="answer">Terapia kaszlu  zalezy od charakteru kaszlu i&nbsp;jego przyczyny.<br /><br />W suchym kaszlu podaje sie  preparaty przeciwkaszlowe. O ich stosowaniu zadecyduje lekarz. Mozemy w&nbsp;tym okresie podac syrop z&nbsp;lisci babki lancetowatej, prawoslazu lub preparaty z&nbsp;porostem islandzkim, ktore nawilza blone sluzowa drog oddechowych i&nbsp;zalagodza jej podraznienie.<br /><br />W kaszlu wilgotnym stosuje sie leki wykrztusne, rozrzedzajace wydzieline zalegajaca w&nbsp;drogach oddechowych.<br />Nalezy pamietac, ze stosujac leki mukolityczne (uplynniajace wydzieline) ostatnia dawke podajemy najpozniej 4 godziny przed snem.</p>
</li>
</ul>
</div>
