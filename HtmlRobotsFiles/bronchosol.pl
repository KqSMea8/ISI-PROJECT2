<h1>Tymianek i&nbsp;pierwiosnek</h1>
<div class="content">
<p class="lead">Poznaj bogactwo wlasciwosci Tymianku, Pierwiosnka i Tymolu. Dowiedz sie,  jak naturalne skladniki ziolowe znajdujace sie w leku Bronchosol pozytywnie wplywaja na zdrowie Twojego dziecka.</p>
<div class="core-ingredients">
<img src="img/pages/thyme-and-primrose/banner.jpg" alt="" />
<div class="columns">
<div class="ingredient thyme col1">
<h3>TYMIANEK<span class="latin">(Thymus vulgaris)</span></h3>
<div class="descr">
<p>Tymianek jest niewielka, wieloletnia roslina zielna lub krzewinka o cienkich pedach, drobnych listkach i&nbsp;niewielkich fioletowych kwiatach. Jego naturalne srodowisko to wybrzeze Morza Srodziemnego oraz Ameryka Poludniowa. Uprawiany jest w&nbsp;wielu krajach Europy i&nbsp;Ameryki.<br />Tymianek juz w&nbsp;Starozytnosci byl ceniona przyprawa i&nbsp;roslina lecznicza. W&nbsp;Starozytnym Egipcie uzywano go do balsamowania zwlok.<br />W celach leczniczych stosuje sie ziele tymianku (Thymi Herba).</p>
<p class="list-header">Ziele tymianku zawiera:</p>
<ul class="dotted-list">
<li>olejek eteryczny, ktorego glownymi skladnikami sa tymol i&nbsp;karwakrol (stanowia ok. 40 % wszystkich olejkow zawartych w&nbsp;surowcu)</li>
<li>flawonoidy</li>
<li>garbniki</li>
<li>zwiazki fenolowe</li>
</ul>
<p>Dzieki tym skladnikom ziele tymianku wykazuje dzialanie wykrztusne, rozkurczajace, przeciwzapalne i&nbsp;bakteriobojcze.<br />Znajduje zastosowanie w&nbsp;schorzeniach gornych drog oddechowych, w&nbsp;uporczywym kaszlu, w&nbsp;problemach trawiennych.<br />Badania naukowe potwierdzily skutecznosc stosowania tymianku w&nbsp;opisanych dolegliwosciach. Ziele tymianku posiada monografie Farmakopei Europejskiej, Komisji E, ESCOP, Europejskiej Agencji Lekow (EMA).</p>
<p><strong>Tymol</strong><br />Skladnik olejku eterycznego wystepujacego w&nbsp;tymianku. Stanowi on ok. 35 % wszystkich olejkow zawartych w&nbsp;olejku tymiankowym. Tymol dziala bakteriobojczo i&nbsp;grzybobojczo, dzieki czemu olejek zawarty w&nbsp;zielu tymianku dodatkowo wspomaga dzialanie surowca w&nbsp;schorzeniach gornych drog oddechowych.</p>
</div>
</div>
<div class="ingredient primrose col2">
<h3>PIERWIOSNEK<span class="latin">(Primula officinalis)</span></h3>
<div class="descr">
<p>Pierwiosnek lekarski to pierwszy kwiat pojawiajacy sie wiosna. Jest roslina wieloletnia z&nbsp;rozeta pomarszczonych lisci u nasady i&nbsp;zloto-zoltych kwiatow . Pierwiosnek rosnie w&nbsp;calej Europie, Azji Mniejszej oraz na Syberii. W&nbsp;Polsce rosnie na suchych lakach, slonecznych wzgorzach, jasnych polanach, wsrod lisciastych lasow. Do celow leczniczych stosuje sie tylko pierwiosnek z&nbsp;upraw.<br />Wlasciwosci lecznicze pierwiosnka znane sa juz od sredniowiecza.</p>
<p class="list-header">Pierwiosnek zawiera nastepujace substancje aktywne:</p>
<ul class="dotted-list">
<li>saponiny</li>
<li>flawonoidy</li>
<li>glikozydy fenolowe</li>
</ul>
<p>Preparaty zawierajace korzen pierwiosnka stosuje sie w&nbsp;niezytach gornych drog oddechowych, jako srodek wykrztusny, rozluzniajacy wydzieline oskrzelowa. Zazwyczaj pierwiosnek wystepuje w&nbsp;mieszankach ziolowych. Czesto w&nbsp;towarzystwie tymianku.<br />Badania naukowe potwierdzily dzialanie lecznicze pierwiosnka. Posiada on monografie ESCOP, Komisji E, Europejskiej Agencji Lekow.</p>
</div>
