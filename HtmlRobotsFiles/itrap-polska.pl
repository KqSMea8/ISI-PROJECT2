<h1 class="pages-title">Monitoring szkodnikow iTrap Polska</h1>
<div class="entry-content">
<p><strong>iTrap Polska</strong> to ogolnopolski projekt monitorowania szkodnikow w sadach. Dzieki niemu sadownicy uzyskuja dostep do danych z kilkudziesieciu pulapek feromonowych rozmieszczonych na terenie calego kraju.</p>
<h3>SKUTECZNY MONITORING ZAGROZEN W SADACH</h3>
<p>Skuteczna ochrona sadu przed szkodnikami wymaga ciaglego monitorowania pojawiajacych sie zagrozen i szybkiego reagowania. Aby wspomoc sadownikom w tym trudnym zadaniu, powstal monitoring szkodnikow<strong> iTrap Polska</strong>.</p>
<h3>DZIALANE SYSTEMU ITRAP POLSKA</h3>
<p><strong>System iTrap Polska</strong> oparty jest o pulapki feromonowe austriackiej firmy <strong>Pessl Instruments</strong>. Urzadzenia te z pozoru przypominaja typowe pulapki typu delta z trojkatnym daszkiem, lepem i dyspenserem feromonu. Cecha, ktora je wyroznia jest kamera, ktora wykonuje zdjecia zlapanych owadow. Wykonane zdjecia nastepnie przesylane sa przez Internet na serwery, gdzie po przetworzeniu obrazu owady sa identyfikowane i zliczane. Gromadzone dane pozwalaja na okreslenie nasilenia lotow szkodliwych motyli.</p>
<p><img class="alignnone wp-image-204 size-full" src="http://itrap-polska.pl/wp-content/uploads/2014/04/itrap.jpg" alt="itrap" width="605" height="151" /></p>
<p><strong>Pulapki umieszczone sa w regionach sadowniczych w 19 lokalizacjach:</strong></p>
<p><img class="alignnone size-full wp-image-206" src="http://itrap-polska.pl/wp-content/uploads/2014/04/mapka.jpg" alt="mapka" width="605" height="497" /></p>
<p>W kazdej lokalizacji monitorowana jest <strong>owocowka jablkoweczka</strong> oraz<strong> zwojka siatkoweczka</strong>.</p>
<p>Taki system monitoringu to znaczne ulatwienie zarowno dla doradcow sadowniczych jak i dla samych sadownikow. Pozwala na obserwacje szkodnikow na duzym obszarze minimalizujac naklad pracy ekspertow.</p>
<h3>DOSTEP DO INFORMACJI</h3>
<p>Zdjecia wykonane przez pulapki oraz dodatkowe informacje o zagrozeniach sa codziennie publikowane na stronie<strong> www.itrap-polska.pl. Dostep do strony jest bezplatny!</strong> Uzytkownicy strony, maja dostep do danych ze wszystkich pulapek dzialajacych w systemie. Po wyborze lokalizacji, nastepuje wybor gatunku szkodnika. Mozliwe jest takze przegladanie zdjec archiwalnych.</p>
<p>W momentach, kiedy zagrozenie ze strony szkodnikow bedzie szczegolnie wysokie, zarejestrowani uzytkownicy systemu otrzymaja wiadomosci SMS i email. Beda one zawieraly komunikat o zagrozeniu wraz z zaleceniami ochrony. Informacje pochodzace z systemy iTrap Polska sa wykorzystywane takze w komunikatach sadowniczych Info-Karta.</p>
