<div class="zapa_intro f_5"><a href="o_zapa">Zwiazek Autorow i Producentow Audiowizualnych</a></div>
<div class="zapa_intro_lead"><p align="justify"><a href="o_zapa">ZAPA jest jedna z&nbsp;najbardziej skutecznych organizacji zbiorowego zarzadu w&nbsp;Polsce. Od momentu powstania konsekwentnie umacnia swoja pozycje na rynku praw autorskich w&nbsp;Polsce oraz swoja pozycje miedzynarodowa. Ma wynegocjowane i&nbsp;podpisane umowy z&nbsp;uzytkownikami publicznymi i&nbsp;prywatnymi, wspolpracuje z&nbsp;wieloma organizacjami krajowymi i&nbsp;zagranicznymi, monitoruje dynamiczny rynek operatorow kablowych. Fundamentalnym elementem aktywnosci SFP - ZAPA jest wlasciwa identyfikacja dziel audiowizualnych, pozwalajaca na szybkie wyplaty naleznych autorom i&nbsp;producentom tantiem, a&nbsp;takze wspieranie walki z&nbsp;piractwem w&nbsp;obrocie utworami filmowymi. </p><br/><p class="font_c1" style="text-align:right"><i>wiecej >></i></p></a></div>



