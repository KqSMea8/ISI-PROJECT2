<h1>O Firmie<span class="border"></span></h1>
<p>Co nas wyroznia</p>
</header>

<div class="description">
<p>
<strong><span style="font-size: 13px;">Firma remontowo-budowlana zostala zalozona w 1991 roku przez Miroslawa Baluka, ktory jest jej wlascicielem. Firma dziala na rynku w Warszawie i okolicach. Profil naszej dzialalnosci to remonty i kompleksowa adaptacja wnetrz w rezydencjach, domach, apartamentach i mieszkaniach.</span></strong></p>
<p>
</p>
<p>
Na zyczenie naszych Klientow wykonujemy rowniez kompleksowa realizacje inwestycji, ktora obejmuje wykonanie projektu aranzacji wnetrza oraz porady w doborze materialow budowlanych, mebli oraz calego wyposazenia.</p>
<p>
</p>
<p>
Firma remontowo-budowlana Miroslawa Baluka wspolpracuje z najlepszymi architektami i projektantami wnetrz. Nasi partnerzy doloza wszelkich staran, aby przy Panstwa udziale stworzyc wnetrze spelniajace wizje idealnego miejsca do zamieszkania. Architekci moga rowniez prowadzic nadzor na wykonywanymi pracami.  Nasza Firma szczegolna uwage zwraca na jakosc, wysoki standard i terminowosc wykonania naszych uslug. Zespol sklada sie z wykwalifikowanych, doswiadczonych i stalych pracownikow.</p>
<p>
</p>
<p>
Kwalifikacje, jakie zdobylismy w trakcie naszej wieloletniej praktyki, umozliwiaja nam swiadczenie uslug na najwyzszym poziomie, a bardzo dobra znajomosc rynku sprawia, ze korzystamy z najnowszych trendow i najnowoczesniejszych w branzy remontowo-budowlanej rozwiazan technologicznych. Zaufanie, jakim obdarzaja nas Klienci dostarcza nam satysfakcji, a jednoczesnie mobilizuje i zobowiazuje do dalszej wytezonej pracy.</p>
<p>
</p>
<p>
Nasze wykonawstwo wg aranzacji wspolpracujacych architektow zostalo opublikowane w branzowych czasopismach: "Dobre Wnetrze", "Ladny Dom", "VILLA" oraz francuskiej edycji "Elle Decoration". Na potwierdzenie jakosci naszych uslug przedstawiamy wybrane Listy Referencyjne, swiadczace o zadowoleniu naszych Klientow.</p>
<p>
</p>
<p>
Dodatkowym autem jest fakt, ze nasza Firma posiada pelny zakres ubezpieczenia OC.</p>
