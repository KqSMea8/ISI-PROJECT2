<div style="text-align: center;"><strong><span style="font-size: 18pt; color: #ff0000;">Lepsi z pierwszej pomocy</span></strong></div>
<div style="text-align: center;">
<p style="text-align: justify;"><span style="font-size: 14pt;">Co roku w Polsce umiera kilka tysiecy osob. Tylko dlatego, ze swiadkowie zdarzen nie ratuja zycia przed przyjazdem pogotowia.</span></p>
<p style="text-align: justify;">&#8211; Dlaczego tak sie dzieje? Bo zwykle paralizuje ludzi strach. Ale to od nich zalezy jakie bedzie dalsze zycie poszkodowanej osoby. Czy wyzdrowieje, czy zostanie roslinka - tlumaczyl  instruktor Medycznego Centrum Szkoleniowego na zorganizowanych  15 i 17 marca przez LOP<strong> certyfikowanych kursach pierwszej pomocy przedmedycznej dla pracownikow i pracodawcow</strong>.</p>
<p style="text-align: justify;">Oprocz solidnej porcji teorii i praktyki, kursanci zobaczyli filmy z akcji ratowniczych oraz zdarzen obrazujacych inne zagrozenia. Uczestnicy chwalili taka forme zajec. Bo wiekszosc z nich fantomy miala juz przecwiczone. A teraz  pokazano im zycie.</p>
<p style="text-align: justify;">Do udzialu w kursie na ochotnika zglosili sie pani Monika i pan Mariusz z PUBR w Miedzyrzeczu. Bo nigdy nie wiadomo, kiedy im sie to przyda w zyciu. W opinii pana Mariusza tego typu szkolenia powinny byc organizowane jeszcze czesciej: &#8211; Jestem nawet za dwudniowymi, w weekendy. Zeby lepiej wiedziec, jak postepowac w naglych i nieprzewidzianych sytuacjach.</p>
<p style="text-align: justify;">Na zakonczenie kursu kazdy uczestnik otrzymal certyfikat. Przypominamy! Pracodawca ma obowiazek wyznaczenia w swoim zakladzie osoby do udzielania pierwszej pomocy. W tym zakresie podlega bowiem kontroli Panstwowej Inspekcji Pracy. Konsekwencja  braku w firmie przeszkolonej pod tym katem osoby jest kara grzywny nawet do 30 tysiecy zlotych.</p>
