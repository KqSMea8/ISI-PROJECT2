<p style="text-align: justify;"><span style="font-size: 10pt;">Na posiedzeniu Komitetu Monitorujacego, ktore odbylo sie w dniach 14-15 grudnia 2016 r. w Poczdamie zatwierdzono projekt EuRegioNet nr 85011249 z Programu Wspolpracy INTERREG VA Brandenburgia-Polska 2014-2020 w ramach celu ,,EUROPEJSKA WSPOLPRACA TERYTORIALNA ,,Europejskiego Funduszu Rozwoju Regionalnego (EFRR).</span></p>
<p style="text-align: justify;"><span style="font-size: 10pt;">Projekt EuRegioNet (EuroRegional Network) &#8211; Euroregionalna siec na rzecz inteligentnej specjalizacji oraz umiedzynarodowienia klastrow i inicjatyw sieciowych w euroregionach: ,,Pro Europa Viadrina" i ,,Sprewa-Nysa-Bobr" bedzie realizowany w osi priorytetowej IV (cel tematyczny 11 EWT) Integracja mieszkancow i wspolpraca administracji, w okresie od 1 kwietnia 2017 r. do 31 marca 2020 r.</span></p>
<p style="text-align: justify;"><span style="font-size: 10pt;">Partnerami projektu sa: BIC Centrum Biznesu i Innowacji/Business and Innovation Centre  Frankfurt (Oder) GmbH, &#8211; lider projektu, CIT Centrum Innowacji i Technologii GmbH w Forst, Euroregion Sprewa-Nysa-Bobr w Gubinie i LOP Lubuska Organizacja Pracodawcow w Gorzowie Wielkopolskim.  </span></p>
<p style="text-align: justify;"><span style="font-size: 10pt;">Projekt zostal dofinansowany w wysokosci 85 proc. wartosci projektu tj. w kwocie 678.915,64 Euro. Calkowita wartosc projektu realizowanego przez Lubuska Organizacje Pracodawcow w Gorzowie Wielkopolskim  stanowi kwota 102.627,00 Euro. Dofinansowanie ze srodkow UE stanowi kwota 87.232,95 Euro. Wklad wlasny LOP wynosi 15.394,05 Euro.</span></p>
<p style="text-align: justify;"><span style="font-size: 10pt;">Na wniosek Zarzadu Lubuskiej Organizacji Pracodawcow w Gorzowie Wielkopolskim koordynatorem projektu zostal Henryk Maciej Wozniak, kierownikiem Jozef T. Finster, asystentami projektu: doradztwo/organizacja &#8211; Marlena Girsa, organizacja &#8211; Joanna Sondej-Pierzynska.</span></p>
<h1 style="text-align: center;"><span style="color: #0000ff;"><span style="font-size: 14pt;"> </span></span></h1>
<h1 style="text-align: center;"><span style="color: #0000ff;"><span style="font-size: 14pt;">EUREGIONET</span></span></h1>
<p style="text-align: center;"><strong>EuRegioNet - Umiedzynarodowienie sieci i klastrow (NWC/USK)</strong></p>
<p style="text-align: left;"><strong>Cele projektu:</strong></p>
<p style="text-align: left;"><span style="font-size: 10pt;">Stworzenie transgranicznej platformy informacyjno-, konsultingowo-, wspierajacej istniejace lub tworzace sie sieci i klastry (NWC/USK) na terenie realizacji projektu, ktore powstaly z MiSP i/lub instytucji naukowych.</span><br />
<span style="font-size: 10pt;">Celem priorytetowym jest przekazanie tym sieciom informacji, wynikow i ekspertyz dot. ich dlugoterminowych ustalen, umocnienia sie czy tez sieci transgranicznych:</span></p>
<div style="text-align: center;">
<ul style="text-align: left;">
<li>rozpoczecie polsko-niemieckiego dialogu klastrow, sieci transgranicznych</li>
<li>zbudowanie NWC/USK w odniesieniu do struktur uslugowych</li>
<li>wspieranie NWC/USK w projektach transgranicznych/w pozyskiwaniu srodkow wspierajacych</li>
<li>wspieranie inteligentnej specjalizacji, strategicznej rozbudowy, umiedzynarodowienia</li>
<li>marketing lokalny poprzez zapoczatkowanie rozwoju polsko-niemieckiego klastra-marki</li>
</ul>
</div>
<p style="text-align: left;"><strong>Okres trwania projektu:</strong></p>
<ul style="text-align: left;">
<li>04/2017 - 03/2020, 36 miesiecy</li>
</ul>
<p style="text-align: left;"><strong>Partnerzy projektu:</strong></p>
<ul style="text-align: left;">
<li>BIC Centrum Biznesu i Innowacji/Business and Innovation Centre, Frankfurt nad Odra, Niemcy - Leadpartner</li>
<li>CIT Centrum Innowacji i Technologii GmbH w Forst, Niemcy</li>
<li>LOP Lubuska Organizacja Pracodawcow, Gorzow Wlkp., Polska</li>
<li>Euroregion Sprewa-Nysa-Bobr, Gubin, Polska</li>
</ul>
<p><strong>Problemy zidentyfikowane przez partnerow:</strong></p>
<ul style="text-align: left;">
<li>potrzeba polepszenia istniejacych NWC/USK strategii marketingowych, skierowanych na umiedzynarodowienie</li>
<li>istnieja nieliczne kooperacje pomiedzy przedsiebiorstwami</li>
<li>istnieja deficyty w transgranicznym marketingu lokalnym</li>
</ul>
<p style="text-align: left;"><strong>Dzialania projektu:</strong></p>
<ul style="text-align: left;">
<li>ocena 16 NWC/USK regionow partnerskich</li>
<li>poszerzenie/powiazanie sieciowe/umiedzynarodowienie 8 NWC/USK</li>
<li>stworzenie 2 nowych transgranicznie dzialajacych NWC/USK</li>
</ul>
<p style="text-align: left;"><strong>Projekt-Roadshow:</strong></p>
<ul style="text-align: left;">
<li><strong>29 czerwca 2017:</strong>Glowna uroczystosc inaugurujaca w Gorzowie Wlkp., Polska</li>
</ul>
<p>Projekt wsparty ze srodkow  Europejskiego Funduszu Rozwoju Regionalnego</p>
<p>,,Redukowac bariery &#8211; wspolnie wykorzystywac silne strony"</p>
<p>&nbsp;</p>
<p style="text-align: center;"><span style="color: #0000ff; font-size: 14pt;"><strong>EUREGIONET</strong></span></p>
<p style="text-align: center;"><strong>EuRegioNet - Internationalisierung von Netzwerken &amp; Clustern (NWC)</strong></p>
<p>&nbsp;</p>
<p style="text-align: left;"><strong>Projektziel:</strong></p>
<p style="text-align: left;">Schaffung einer grenzubergreifenden Informations-, Consulting- und Unterstutzungsplattform fur im Projektgebiet bestehende bzw. in Bildung befindliche Netzwerke und Cluster (NWC), die aus KMU und/oder wissenschaftlichen Einrichtungen bestehen.<br />
<strong>Vorrangiges Ziel</strong> ist es, diesen Netzwerken Informationen, Leistungen und Expertise bei deren nachhaltigen Ausgestaltung, Verankerung und grenzubergreifender Vernetzung zu unterbreiten:</p>
<ul style="text-align: left;">
<li>Anstoss eines DE/PL Cluster-Dialogs, grenzubergreifende Vernetzung</li>
<li>Aufbau einer NWC bezogenen Dienstleistungsstruktur</li>
<li>Unterstutzung der NWC bei grenzuberschreitender Projektarbeit/Fordermittelakquisition</li>
<li>Unterstutzung intelligenter Spezialisierung, strategischer Erweiterung, Internationalisierung</li>
<li>Standortmarketing durch Anstoss zur Entwicklung einer DE/PL Cluster-Marke</li>
</ul>
<p style="text-align: left;"><strong>Projektlaufzeit</strong><strong>:</strong></p>
<ul style="text-align: left;">
<li>04/2017 - 03/2020, 36 Monate</li>
</ul>
<p style="text-align: left;"><strong>Projektpartner:</strong></p>
<ul style="text-align: left;">
<li>BIC Business and Innovation Centre, Frankfurt/Oder, Deutschland - Leadpartner</li>
<li>CIT Centrum fur Innovation Und Technologie GmbH</li>
<li>LOP Lebuser Arbeitgeberverband, Gorzow Wlkp., Polen</li>
<li>Euroregion Spree-Neisse-Bober, Gubin, Polen</li>
</ul>
<p style="text-align: left;"><strong>Von den Partnern identifizierte Probleme:</strong></p>
<ul style="text-align: left;">
<li>Verbesserungsbedarf bei der auf Internationalisierung ausgerichteten Vermarktungsstrategien innerhalb der bestehenden NWC</li>
<li>Es gibt wenige grenzubergreifende Unternehmenskooperationen</li>
<li>Es sind Defizite im grenzubergreifenden Standortmarketing vorhanden</li>
</ul>
<p style="text-align: left;"><strong>Projektaktivitaten:</strong></p>
<ul style="text-align: left;">
<li>16 NWC in den Partnerregionen evaluieren</li>
<li>Erweiterung, Vernetzung, Internationalisierung von 8 NWC</li>
<li>Bildung von 2 neuen grenzubergreifend tatigen NWC</li>
</ul>
<p style="text-align: left;"><strong>Projekt-Roadshow:</strong></p>
<ul style="text-align: left;">
<li><strong>Juni 2017:</strong>Zentrale Auftaktveranstaltung in Gorzow Wlkp., Polen</li>
</ul>
<p>Gefordert durch den Europaischen Fonds fur regionale Entwicklung</p>
<p>,,Barrieren reduzieren - gemeinsame Starken nutzen"</p>
