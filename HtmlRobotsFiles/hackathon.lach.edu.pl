<div class="row">
<h2>Co mozesz zyskac?</h2>
</div>
</div>
</div>

<div class="wrapper wrapper--white">
<div class="container-fluid cards">
<div class="row">
<div class="col-md-12 txt ">

<div class="row">
<div class="col-md-4 card">
<h3>Rozwiaz realny problem</h3>
<p>Praca dyplomowa nie musi byc tylko kolejnym plikiem wgranym do APD. Nie trzeba byc Alanem Turingiem ani Grace Hopper, zeby stworzyc cos, co zmieni swiat. Decydujac sie na wspolprace z przedstawicielem innego kierunku, dajesz sobie mozliwosc rozwiazania problemu, o ktorego istnieniu nie mialas/ miales do tej pory pojecia.</p>
</div>
<div class="col-md-4 card">
<h3>Profesjonalna ocena Twojego pomyslu</h3>
<p>Nie kazdy pomysl ma potencjal, zeby przerodzic sie w dobry projekt lub prace magisterska czy doktorska. Nasi eksperci pomoga Ci ocenic, ktore pomysly sa warte rozwijania, a ktore nie. Doradza, jak dalej rozwijac projekt, sprawdza dotychczasowe osiagniecia i skieruja Cie na odpowiednie tory.</p>
</div>
<div class="col-md-4 card">
<h3>Rozwin swoje zdolnosci i poznaj nowe technologie</h3>
<p>Udzial w zespolowej realizacji projektu to doskonala okazja, zeby poznac nowe metody i technologie - nie z podrecznika, tylko na zywo, wspolpracujac z osobami, ktore juz je znaja.</p>
</div>
</div>

<div class="row">
<div class="col-md-6 card">
<h3>Inspiracje</h3>
<p>Nie masz zupelnie pomyslu na swoja prace, a moze utknales, nie wiedzac gdzie isc dalej? Tworcza atmosfera hackathonu, jak rowniez obecnosc przedstawicieli bardzo roznych kierunkow pomoze Ci ruszyc z miejsca. To szansa, by znalezc temat, ktory z przyjemnoscia rozwiniesz, czy droge, ktora powinienes podazyc, zeby ukonczyc swoja prace.</p>
</div>
<div class="col-md-6 card">
<h3>Pomoc w pracy nad istniejacym projektem</h3>
<p>W rozwijaniu swojej pracy przeszkadza Ci brak wiedzy technicznej, lub problem, ktorego nie potrafisz rozwiazac? Na Hackathonie lamiacym bariery spotkasz zarowno ekspertki i ekspertow, gotowych sluzyc pomoca, jak i studentki i studentow, wsrod ktorych moze udac Ci sie znalezc kogos gotowego podjac wyzwanie.</p>
</div>

