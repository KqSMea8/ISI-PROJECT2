<div class="col-md-12 txt">
<p class="lead">
Na atmosfere podczas imprezy wplyw ma kazdy jej uczestnik i uczestniczka, zachowania takie jak dokuczanie, gnebienie, aktywne przeszkadzanie w pracy, powoduja jej pogorszenie.
</p>
<p>Szerzenie negatywnej energii nie bedzie akceptowane, kazdy/ kazda swiadomie dopuszczajacy sie takich czynow moze zostac usuniety z terenu imprezy. Osad, jakie postepowanie jest dopuszczalne, a jakie nie, nalezy do organizatorow. Ponadto na teren imprezy zakazuje sie wnoszenia broni i innych niebezpiecznych przedmiotow, materialow wybuchowych, wyrobow pirotechnicznych i innych materialow latwopalnych, napojow alkoholowych, srodkow odurzajacych i substancji psychotropowych. W budynku obowiazuje takze zakaz palenia tytoniu.</p>
