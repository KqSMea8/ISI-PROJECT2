<h2>Kto moze wziac udzial?</h2>
</div>
</div>
</div>
<div class="wrapper wrapper--white">
<div class="container">
<div class="row">
<div class="col-md-12 txt">
<p class="lead">
Kazdy! W <strong>Hackathonie lamiacym <span class="red">bariery</span></strong> wziac moze udzial kazdy(/a) student(ka), doktorant(ka) lub badacz(ka). Warunkiem udzialu jest wypelnienie formularza zgloszeniowego oraz pozytywne przejscie etapu ewaluacji projektu.
</p>

<p>
Wszystkie zgloszenia ocenimy indywidualnie, ale najwieksze szanse zakwalifikowania beda mialy osoby zwiazane z Uniwersytetem Warszawskim. W miare wolnych miejsc przyjmowac bedziemy takze zgloszenia od osob spoza uczelni.
</p>
<p>
W naszym hackathonie moze wziac udzial do 50 osob. Zapraszamy zarowno juz istniejace zespoly, jak i te osoby, ktore (na razie) pracuja w pojedynke i chcialyby stac sie czescia interdyscyplinarnego zespolu projektowego - w razie potrzeby pomozemy w jego zawiazaniu.
</p>
