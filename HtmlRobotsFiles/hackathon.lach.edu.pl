<h2>Hackathon</h2>
</div>
</div>
</div>
<div class="wrapper wrapper--white">
<div class="container">
<div class="row">
<div class="col-md-12 txt">
<p class="lead">
Laboratorium Cyfrowe Humanistyki UW oraz Wydzial Matematyki, Informatyki i Mechaniki UW zapraszaja na <strong>Hackathon lamiacy <span class="red">bariery</span></strong>
</p>
<p>
Hackathon to maraton projektowania i programowania - jego uczestnicy i uczestniczki spotykaja sie, zeby w kilkuosobowych zespolach intensywnie pracowac nad wspolnymi projektami. Czas przeznaczony na prace bywa rozny, zazwyczaj sa to 24 godziny. Na koniec hackathonu zespoly prezentuja i omawiaja wyniki swojej pracy.
</p>
<p>
Do udzialu w Hackathonie lamiacym bariery zapraszamy osoby, ktore maja pomysly na projekty (aplikacje, narzedzia, strony, analizy danych, itp.), bedace rozwiazaniem konkretnych problemow zwiazanych z dowolna dziedzina humanistyki oraz tych, ktorzy chcieliby zaangazowac swoje programistyczne umiejetnosci w ich realizacje.
</p>
<p>
W hackathonie wezmie takze udzial Stowarzyszenie 61 - organizacja pozarzadowa gromadzaca, analizujaca i udostepniajaca informacje dotyczace osob pelniacych wybieralne funkcje publiczne w serwisie <a href="http://www.MamPrawoWiedziec.pl/">www.MamPrawoWiedziec.pl</a>. Uczestnicy i uczestniczki hackathonu beda mogli takze zdecydowac sie na prace z danymi udostepnionymi przez stowarzyszenie.
</p>
</div>
