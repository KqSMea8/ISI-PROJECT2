<div class="markdown-body" itemprop="articleBody"><p><img src="http://parafia.oksy.net.pl/storage/app/uploads/public/5bf/931/e05/5bf931e05590c411124423.jpg" alt="1.jpg" /></p>
<p><strong>PIELGRZYMKA &quot;Sladami Sw. Stefana - Wegry&quot; - 7 dni (09.06 - 15.06.2019)</strong></p>
<p><strong>DZIEN:1</strong>
Godz. 5.30 wyjazd do Budapesztu. Zakwaterowanie w hotelu, wypoczynek w termach hotelowych.</p>
<p><strong>DZIEN:2</strong>
W programie : Pest - Plac Bohaterow, Vajdahunyad vara, najcieplejsze termy na Wegrzech,  Andrassy ut, Lasek Miejski, Bazylika Sw. Stefana, Parlament, nastepnie Wyspa Malgorzaty- przejazdzka kolejka po wyspie, odwiedzenie miejsca grobu sw. Malgorzaty, spacer po odrestaurowanych ruinach klasztoru siostr Dominikanek. Godzinny rejs po Dunaju. Wypoczynek w termach hotelowych.</p>
<p><strong>DZIEN:3</strong>
W programie : Buda - Wzgorze Zamkowe, Baszty Rybackie, Plac sw. Trojcy, Kosciol Macieja, zaplecza hotelu Hilton z ruinami klasztoru Dominikanow. Cukiernia Sissi, Palac Budanski, Palac Prezydencki, Kosciol na Skale, Gora Gelerta. Przerwa na Hali Targowej, najstarszym i najwiekszym 3 poziomowym rynku. Popoludniu przejazd Godollo - Mariabesnyo, tam barokowy klasztor kapucynow, kosciol Najswietszej Marii Panny.</p>
<p><strong>DZIEN:4</strong>
Wyjazd do Zakola Dunaju. W programie: Szentendre- barokowy ryneczek, Muzeum Bozego Narodzenia, stary kosciolek z XIII wieku, Kosciol sw. Piotra i Pawla, pomnik Jana Pawla II. Wyszegrad uroki zakola Dunaju, jak rowniez strategicznie korzystne usytuowanie Wyszegradu docenili juz starozytni Rzymianie, wznoszac na jego terenie twierdze Ponte Navata. Palac krolewski i cytadela. Nastepnie przejazd do Esztergom kusi swa niezwykla historia i zacheca do wizyty w najwiekszej pod wzgledem wielkosci na Wegrzech (i piatej na swiecie) bazylice oraz do ogladniecia ruin zamku krolewskiego z okresu panowania dynastii Arpadow. Powrot do Budapesztu.
Ok godz.18.30 - obiadokolacja w miescie. Powrot do hotelu, nocleg .</p>
<p><strong>DZIEN:5</strong>
Wyjazd w kierunku Egeru. Zwiedzanie Egeru -Kosciol Minorytow /najpiekniejszy barokowy kosciol na Wegrzech/. Zwiedzanie zamku z XV w, Bazylika - druga co do wielkosci na Wegrzech, Liceum (dzisiaj uniwersytet, biblioteka), Plac Istwana Dobo, Minaret z XVI wieku. Nastepnie przejazd na kapielisko w Egerze, pobyt na kapielisku . Przejazd do Doliny Pieknej Pani na obiadokolacje i degustacje win w oryginalnej piwnicy winnej. </p>
<p><strong>DZIEN:6</strong>
Przejazd do Mariapocs /sanktuarium maryjne/, barokowa bazylika, ktora odwiedzil Jan Pawel II w 1991r. nastepnie kapiel w goracych zrodlach jaskin w  Miszkolc- Tapolca.                                                                                         </p>
<p><strong>DZIEN:7</strong>
Wykwaterowanie i wyjazd w droge powrotna do Polski. Zakonczenie pielgrzymki w godzinach wieczornych.</p>
<p><strong>TERMIN : 9.06-15.06.2019 CENA: 2030 zl + bilety wstepu 40 EUR i 8.000 HUF</strong></p>
<p>Swiadczenia w cenie:</p>
<p>*transport komfortowym  autokarem ( WC, bar, dvd, klimatyzacja)</p>
<ul>
<li>6 noclegow : 4 w hotelu w okolicach Budapesztu, 2 w hotelu w Egerze.hotele3* kategorii lokalnej. Pokoje 2,3 osobowe z lazienkami.</li>
<li>wyzywienie: 6 sniadan  + 6 obiadokolacji  (w tym jedna regionalna )
*opieka duchowa         </li>
<li>opieka i informacja turystyczna pilota </li>
<li>ubezpieczenie turystyczne firmy Signal Iduna Polska TU  S.A KL-20.000EUR,NNW-15.000PLN, bagaz 1.000PLN  ,CP  </li>
<li>skladka na Turystyczny Fundusz Gwarancyjny
Cena nie zawiera:
kosztu biletow wstepow, oplaty miejscowego przewodnika i systemu tour guide nalezy posiadac ok.40 EUR i 8.000 HUF ,<br />
wydatkow wlasnych , napojow do obiadokolacji
Uwagi:<br />
*program wycieczki moze ulec nieznacznym zmianom  np. kolejnosc zwiedzania,</li>
<li>ubezpieczenia od kosztow rezygnacji  dodatkowo platne z zaliczka ( w wysokosci 3,5% wartosci ceny pielgrzymki )</li>
