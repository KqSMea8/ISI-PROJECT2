<h2>Nawigacja po witrynie</h2>
<p class="akapit">Do nawigacji po witrynie sluza poziome zakladki umieszczone na belce w gornej czesci strony. Klikniecie wybranej zakladki przenosi do odpowiedniego dzialu. Aktualnie wybrana zakladka wyroznia sie sposrod pozostalych inna barwa. Zestaw zakladek jest staly - niezalezny od miejsca witryny, w ktorym sie znalazales.</p>
