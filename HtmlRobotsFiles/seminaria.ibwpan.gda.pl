<h2 class="section-title">Seminaria instytutowe</h2>
</div>
</div>
<div class="row">
<div class="col-lg-12">
<img alt="Marek Kulczykowski" src="http://www.ibwpan.gda.pl/storage/app/media/employee/108.jpg" style="margin-left: 8px; float: right;">
<h4>Szanowni Panstwo,</h4>

<p>informujemy, ze z dniem <strong>2 pazdziernika 2017 roku, o godzinie 10.00</strong>, rozpoczynamy kolejny, 10 juz rok seminariow instytutowych. <strong>W tym roku, podobnie, jak w &nbsp;latach poprzednich, nasze seminaria odbywac sie beda do czerwca 2018 roku, w kazdy poniedzialek o godzinie 10.00</strong>, wg nastepujacego, tradycyjnego schematu:</p>

<p>10.00-10.10 - najnowsze informacje Dyrektora IBW PAN lub jego zastepcy oraz krotkie przedstawienie prelegenta,</p>

<p>10.10-10.40 - wystapienie prelegenta (ok. 30 minut, maks. 40 min.)</p>

<p>10.40-11.00 - dyskusja.</p>

<p>Najpozniej o 11.00 - definitywny koniec spotkania!</p>

<p>Wystapienia maja charakter roboczy i dotycza, przede wszystkim, aktualnie prowadzonych prac. Celem seminariow jest m.in. zapoznanie ogolu pracownikow z badaniami wykonywanymi w poszczegolnych Zakladach (co, byc moze, zaowocuje nawiazaniem miedzyzakladowej wspolpracy) oraz wlaczenie do dyskusji ludzi spoza "zakladowej branzy", byc moze z odmiennymi pogladami.</p>

<p><strong>Nie wyklucza sie rowniez, a wrecz zacheca, do zapraszania na seminaria gosci spoza Instytutu oraz emerytowanych, choc nadal czynnych naukowo, pracownikow.</strong></p>

<p>Za organizacje seminariow odpowiedzialny jest Marek Kulczykowski z Zakladu Geomechaniki (tel. 2913, 2983 - mejl: <span class="mailto">c20a3153eb8ba5b225c14597de104829a7afc1</span>), ktory przyjmuje zgloszenia prelegentow i udziela ew. dodatkowych informacji.</p>

<p>Informujemy, ze w celu stworzenie uczestnikom seminariow mozliwosci dalszej publicznej wymiany pogladow na tematy poruszane na seminariach URUCHAMILISMY Z DNIEM 9 LUTEGO 2007 ROKU FORUM DYSKUSYJNE. Zainteresowanych prosimy o przesylanie swoich wypowiedzi e-mailem (na adres: <span class="mailto">c20a3153eb8ba5b225c14597de104829a7afc1</span>), a zostana one zamieszczone na stronie FORUM w kolejnosci ich nadsylania.</p>        </div>
