<h3>Strona glowna</h3>
<div class="content" id="pageDescription"><p><span style="color: #c10000;"><strong>Szanowni Panstwo</strong></span><br /><br /><strong>Firma</strong> "<span style="color: #c10000;">Wispec</span>" wykonuje kompleksowe prace w zakresie montazu kominkow ( projektowanie niepowtarzalnych wzorow przy udziale zleceniodawcy ) z materialow wlasnych jak rowniez powierzonych.<br /><br /><br /><strong>Oferujemy</strong> uslugi najwyzszej jakosci dzieki ponad dwudziesto letniej tradycji w zakresie projektowania i budowy piecow kaflowych. Profesjonalnie i energicznie wykonamy kazde zlecenie. <br />Zajmujemy sie:<br /><br /></p>
<ul>
<li><strong>odnowa</strong></li>
<li><strong>naprawa</strong></li>
<li><strong>rozbiorka</strong></li>
<li><strong>rekonstrukcja</strong></li>
</ul>
<p><br />starych jak rowniez zabytkowych piecow kaflowych.. <br /><br />Wspolpracujemy z najwieksza polska wytwornia kafli ceramicznych <a style="text-decoration: none;" href="http://www.kafle.com.pl/">ZYGMUNT KULIG</a> - <a style="text-decoration: none;" href="http://www.kafle.com.pl/">www.kafle.com.pl</a> , dzieki czemu jestesmy w stanie zaoferowac pelna palete wzorow (185 roznych elementow ceramicznych w siedmiu kolorach). Wszystkie produkty posiadaja atest Krajowej Komisji Artystycznej i Etnograficznej Fundacji "Cepelia" - Polska Sztuka i Rekodzielo - <span style="color: #0000ff;">galeria kafli</span></p>
<p><strong>Piec kaflowy</strong> w tradycyjnej postaci jest budowany z cegly szamotowej w postaci slupa oblozonego z zewnatrz materialem ceramicznym - kaflami (latwo utrzymac w czystosci). Najwazniejszym miejscem jest palenisko. Z wielkosci paleniska oblicza sie mase pieca (przykladowo palenisku na 6kg drewna powinno odpowiadac 600kg masy akumulacyjnej).<br />Jako zamkniecie paleniska moga byc wykorzystane drzwiczki zeliwne, lub nowoczesniej - szyba. Szyby stosowane we wkladach do piecow kaflowych sa grubsze od szyb do wkladow kominkowych lub podwojne. Powodem sa duzo wyzsze temperatury w palenisku pieca kaflowego.<br />Odbior ciepla z dymu realizowany jest w specjalnych kanalach dymowych, ktore sa przewaznie umieszczone pionowo z bokow pieca. W zaleznosci od budowy pieca dym po uzyskaniu zaru wypelnia kanaly lub w wersji z przelacznikiem musi przebyc wszystkie kanaly aby wyleciec do komina. Przelacznik sluzy do ulatwienia rozpalania - otwiera lub zamyka bezposrednia droge do komina.</p></div>




