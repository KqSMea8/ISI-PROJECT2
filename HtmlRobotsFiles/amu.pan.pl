<h2 itemprop="headline">
Wydarzenia			</h2>
</div>





<div itemprop="articleBody">
<p>W czerwcu 2019 roku Zgromadzenie Ogolne Polskiej Akademii Nauk wybierze <strong>17 czlonkow Akademii Mlodych Uczonych.</strong> W dniu 20 listopada 2018 r. Prezydium PAN okreslilo, ze w Wydziale I Nauk Humanistycznych i Spolecznych zostanie wybranych 3 czlonkow AMU, w Wydziale II Nauk Biologicznych i Rolniczych - 4, Wydziale III Nauk Scislych i Nauk o Ziemi - 4, Wydziale IV Nauk Technicznych - 4 i Wydziale V Nauk Medycznych - 2. Zgloszenia kandydatow adresowane do Prezesa Polskiej Akademii Nauk nalezy przekazac w nieprzekraczalnym terminie do dnia 15 marca 2019 r., na adres: 00-901 Warszawa, Pl. Defilad 1, Palac Kultury i Nauki. Tresc ogloszenia o wyborach dostepna jest na stronie internetowej Polskiej Akademii Nauk po adresem:</p>
<p><a href="https://instytucja.pan.pl/index.php/korporacja/5013-wybory-czlonkow-akademii-mlodych-uczonych-w-2019-roku">https://instytucja.pan.pl/index.php/korporacja/5013-wybory-czlonkow-akademii-mlodych-uczonych-w-2019-roku</a></p>
<p>Czlonkiem Akademii Mlodych Uczonych moze zostac osoba, ktora nie przekroczyla 38 roku zycia i legitymuje sie co najmniej stopniem naukowym doktora. Kandydatury na czlonkow Akademii Mlodych Uczonych moze zglosic: trzech czlonkow Polskiej Akademii Nauk, rada naukowa instytutu naukowego lub instytutu badawczego posiadajacego prawo nadawania stopnia naukowego doktora habilitowanego, rada wydzialu uczelni posiadajacego prawo nadawania stopnia naukowego doktora habilitowanego lub piec osob, ktore wyrozniaja sie szczegolnym dorobkiem naukowym i autorytetem w srodowisku naukowym oraz reprezentuja rozne osrodki</p>
<p>Wiecej na: <a href="/index.php/aktualnosci/127-wybory-czlonkow-amu-2019">http://amu.pan.pl/index.php/aktualnosci/127-wybory-czlonkow-amu-2019</a></p>	</div>


