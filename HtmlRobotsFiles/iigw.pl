<div id="slide1" class="current">
<h2>Katerdra<br/> Hydrauliki i Hydrologii</h2>
<p>Obszarem dzialalnosci w zakresie hydrologii sa podstawy zjawisk ekstremalnych (w tym wykonywanie ekspertyz) oraz modelowanie matematyczne procesow hydrologicznych w zlewniach rzecznych.</p>
</div>

<div id="slide2">
<h2>Zaklad Budownictwa Wodnego i Gospodarki Wodnej </h2>
<p>Obszarem dzialalnosci w sferze gospodarki wodnej sa metody oceny skutecznosci i efektywnosci dzialania systemow i obiektow gospodarki wodnej oraz bilanse wodno-gospodarcze.</p>
</div>

<div id="slide3">
<h2>Zaklad Budownictwa Wodnego I Gospodarki Wodnej</h2>
<p>Obszarem dzialalnosci w zakresie budownictwa wodnego sa zagadnienia zwiazane z utrzymaniem, regulacja rzek i potokow, obiektami malej retencji oraz ze specjalnymi konstrukcjami hydrotechnicznymi (jazy, zapory).</p>
</div>

<div id="slide4">
<h2>Katedra<br />Hydrauliki i Hydrologii</h2>
<p>Obszarem dzialalnosci w sferze hydrauliki I hydromechaniki sa rozwiazania dotyczace min.: przepustowosci koryt, przeplywu w zbiornikach i upustow budowli wodnych.</p>
</div>
