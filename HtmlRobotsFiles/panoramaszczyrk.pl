<h1 title="">O naszym osrodku</h1>
Fajny osrodek wczasowy, pieknie polozony na zboczu Klimczoka. Cudne widoki o kazdej porze roku na gorska przyrode zapieraja dech w piersi. Dziesieciominutowy spacerek zaprowadzi nas do centrum Szczyrku z wszystkimi jego atrakcjami. Milosnicy gorskich wedrowek wychodza prosto z Osrodka na szlaki turystyczne. Wypoczynek polaczony z dobra zabawa zadowoli zarowno doroslych jak i dzieci przebywajace na koloniach, zimowiskach oraz obozach sportowych. Do dyspozycji gosci  65 pokoi z lazienkami i TV. <br /><br />
Osrodek posiada wpis do rejestru osrodkow, w ktorych moga odbywac sie turnusy rehabilitacyjne i uprawniony jest do przyjmowania zorganizowanych grup turnusowych osob niepelnosprawnych nr OD/24/0010/13 wydany przez Wojewode Slaskiego.
</div>

