<h2 title="">Polozenie</h2>
<div class="def-box-content">
Szczyrk to jedna z najbardziej znanych miejscowosci Beskidu Slaskiego.  Klimatyczne miasteczko polozone  jest w malowniczej kotlinie  rzeki Zylicy. Dookola rozciagaja sie masywy gorskie Skrzycznego, Klimczoka, Magury,Kotarza, Malinowa, Skalitego.W bliskiej odleglosci mozemy odwiedzac atrakcyjne turystycznie miejscowosci takie jak : Wisla, Ustron, Zywiec, Bielsko - Biala. Polozenie Szczyrku sprawia, ze jest swietnym miejscem do uprawiania turystyki pieszej, rowerowej a zima liczne wyciagi zapraszaja milosnikow bialego szalenstwa. Najwyzsza gora Skrzyczne jest takze znanym centrum paralotniarstwa.
