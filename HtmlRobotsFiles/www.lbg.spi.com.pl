<p class="h1">Szanowni Panstwo,</p>

<p>witamy serdecznie na stronach Laboratorium Badan Genetycznych. Od samego poczatku dzialalnosci, od 1998r. dokladamy wszelkich staran, aby nasze dzialania oparte byly o najnowsze osiagniecia nauk medycznych w zakresie diagnostyki chorob nowotworowych, oraz chorob o podlozu genetycznym. Przedmiotem naszych badan sa geny, a konkretniej pewne, dobrze poznane fragmenty genow, o ktorych wiemy, ze odpowiedzialne sa za powstawanie chorob.</p>
<p class="acapite">
Stwierdzenie wystepowania konkretnej mutacji (zmian DNA), niekoniecznie musi prowadzic do powstania choroby, jednak osoba, ktora nosi zmutowany gen moze wykorzystac te wiedze, w celu zmniejszenia ryzyka zachorowania, lub wykrycia np. nowotworu, w takim momencie, gdy jest on jeszcze calkowicie uleczalny.</p>
<p class="acapite">
Wystepowanie mutacji w genach BRCA1, BRCA2 zwieksza ryzyko zachorowania na raka jajnika do ok. 40% - jednak stosowanie sie do zalecen lekarza, moze wydatnie to ryzyko zmniejszyc. Mutacje w genach NBS1 i CHEK2 swiadcza o podwyzszonym ryzyku wystepowania raka prostaty - szczegolnie narazone sa osoby, u ktorych w rodzinie, byly takie przypadki - jednym z zalecen jest regularne oznaczanie markerow nowotworowych (PSA).</p>
<p class="acapite">
Niestety nie istnieja uniwersalne rozwiazania. Kazdy przypadek wymaga indywidualnego podejscia i podjecia optymalnych dzialan.</p>
<p class="acapite">
Przy laboratorium funkcjonuje poradnia genetyczna, gdzie moga Panstwo skorzystac z fachowej pomocy lekarza genetyka.</p>
<p class="acapite">
Zapewniamy profesjonalna obsluge i nieodplatna porade w zakresie interpretacji wykonanych u nas badan.</p>

