<p>I. Powiatowy Zarzad Dr&oacute;g w Bialymstoku zarzadza siecia dr&oacute;g powiatowych, kt&oacute;rej laczna dlugosc wynosi 1103 km (1020 km &ndash; drogi zamiejskie + 83 km &ndash; drogi miejskie) i przebiega ona przez teren 15 gmin.</p>
<p>Na obszarze Powiatu Bialostockiego znajduje sie:</p>
<p>na drogach zamiejskich: <br />
1) 196 odcink&oacute;w dr&oacute;g o lacznej dlugosci 1020 km, <br />
2)&nbsp; 84 mosty o lacznej dlugosci 1259 m, <br />
3)&nbsp; 874 przepusty. <br />
na drogach miejskich:<br />
1) 59 odcink&oacute;w dr&oacute;g o lacznej dlugosci 83 km, <br />
2) 9 most&oacute;w o lacznej dlugosci 194 m,<br />
3) 47 przepust&oacute;w.</p>
<p><strong>II.&nbsp; Status prawny</strong></p>
<p>Powiatowy Zarzad Dr&oacute;g w Bialymstoku jest samodzielna jednostka organizacyjna Powiatu Bialostockiego utworzona na podstawie:</p>
<ul>
<li>Art.6 ustawy z dnia 5 czerwca 1998r. o samorzadzie powiatowym (tekst jednolity Dz. U. z 2001r.&nbsp;&nbsp;&nbsp; Nr 142 poz. 1592,</li>
<li>Art. 21 ust.1 ustawy z dnia 21 marca 1985r. o drogach publicznych (tekst jednolity Dz. U. z 2004r.&nbsp; Nr 204 poz. 2086),</li>
<li>Ustawy z dnia 29 sierpnia 1997r. o finansowaniu dr&oacute;g publicznych (Dz. U. z 1997r. Nr 123 poz. 780),</li>
<li>Rozporzadzenia Prezesa Rady Ministr&oacute;w z dnia 28 grudnia 1998r. zmieniajacego rozporzadzenie w sprawie okreslenia wykazu instytucji i jednostek organizacyjnych podleglych lub podporzadkowanych wlasciwym ministrom i centralnym organom administracji rzadowej, wojewodom i innym terenowym organom administracji rzadowej albo przez nich nadzorowanych przekazywanych okreslonym jednostkom samorzadu terytorialnego (Dz. U. z 1998r. Nr 165, poz. 1203),</li>
<li>Uchwaly Nr V/14/99 Rady Powiatu Bialostockiego dnia 6 lutego 1999r. w sprawie utworzenia Powiatowego Zarzadu Dr&oacute;g w Bialymstoku, zmienionej uchwala Nr VII/29/99 Rady Powiatu Bialostockiego z dnia 29 kwietnia 1999r. w sprawie zmiany uchwaly Nr V/14/99 Rady Powiatu Bialostockiego z dnia 6 lutego 1999r. w sprawie utworzenia Powiatowego Zarzadu Dr&oacute;g w Bialymstoku.</li>
</ul>
