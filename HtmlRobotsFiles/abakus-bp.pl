</li><li><p style="margin-bottom: 0cm;">obiekty biurowe</p>
</li><li><p style="margin-bottom: 0cm;">budynki mieszkalne </p></li><li><p style="margin-bottom: 0cm;">obiekty inzynierskie - fundamenty elektrowni wiatrowych, wiez kratowych<br/><br/>
</p>
</li></ul>
<p style="margin-left: 1.25cm; margin-bottom: 0cm;">Budynki i budowle
projektujemy w oparciu o konstrukcje stalowe, zelbetowe, murowane i
drewniane.</p>
<p style="margin-left: 1.25cm; margin-bottom: 0cm;">Dla obiektow
