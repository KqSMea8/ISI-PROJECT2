<h1 class="style3">"ABAKUS Biuro Projektow" zostalo zalozone w 1993 roku.</h1> <br/> <div align="justify">
<p style="margin-left: 2.25cm; text-indent: -1cm; margin-bottom: 0cm;">
Biuro zajmuje sie projektowaniem konstrukcji budowlanych i
inzynierskich.
</p>
<p style="margin-left: 2.25cm; text-indent: -1cm; margin-bottom: 0cm;">
<br/>
</p>
<p style="margin-left: 2.25cm; text-indent: -1cm; margin-bottom: 0cm;">
Nasza specjalnoscia sa:</p>
<ul style="margin-left: 0.6cm;"><li><p style="margin-bottom: 0cm;">konstrukcje przemyslowe - hale
magazynowe, produkcyjne, wiaty</p>
</li><li><p style="margin-bottom: 0cm;">obiekty handlowo-uslugowe -
galerie handlowe</p>
</li><li><p style="margin-bottom: 0cm;">obiekty uzytecznosci publicznej
- sale gimnastyczne i sportowe, przedszkola, szkoly, budynki
