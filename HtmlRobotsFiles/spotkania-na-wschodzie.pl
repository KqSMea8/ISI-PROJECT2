<strong class="tytul">PALAC ZIMOWY W SANKT PETERSBURGU</strong><br />Palac Zimowy jest przykladem przepieknej carskiej rezydencji. Posiada ciekawa architekture, bogata kolekcje dziel sztuki, ktore znajduja sie w Ermitazu. Jest to najciekawszy i najwazniejszy zabytek  Sankt Petersburga, a takze unikat w skali Europy. Zapraszamy do lektury kolejnego artykulu Moniki Run.<br />
<span style="text-decoration:underline;color:#007890;">Zabytki, Rosja</span>
