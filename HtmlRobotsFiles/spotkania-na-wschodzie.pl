<strong class="tytul">MIR</strong><br />Mir - miejscowosc w obwodzie grodzienskim na Bialorusi. Lezy okolo 175 kilometrow od granicy z Polska oraz 88 kilometrow od Minska. Mieszka tam 2,4 tysiaca mieszkancow. Po drugiej wojnie swiatowej miejscowosc ta znalazla sie poza granicami Polski i weszla w sklad dzisiejszej Bialorusi. <br />
