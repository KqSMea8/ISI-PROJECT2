<strong class="tytul">Dyneburg - miasto polskie</strong><br />Autorka artykulu na temat polskiego miasta Dyneburg jest Anna Szukstule. Praca zostala zgloszona na konkurs dziennikarski zorganizowany przez Stowarzyszenie Edukacja i Nauka.
Miasto bylo zalozone w 1275 roku przez zakon kawalerow mieczowych, na miejscu tym zostal zbudowany zamek. Historia Dyneburgu jest scisle zwiazana z Polska. W latach 1559-1772 Dyneburg znajdowal sie w granicach Rzeczypospolitej Obojga Narodow, jako stolica Inflant Polskich.
Zapraszamy do lektury! <br />
