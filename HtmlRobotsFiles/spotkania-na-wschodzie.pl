<h2>Wprowadzenie</h2>
<div class="tekst">
,,Spotkania na Wschodzie" sa portalem o tematyce obejmujacej glownie dawne polskie Kresy Wschodnie. Jednak ma on tez sluzyc spotkaniom z naszymi rodakami, ktorych losy rzucily  jeszcze dalej na wschod - juz poza obszar kresow. Chcemy rowniez pisac o dzisiejszym polskim wschodnim pograniczu, o miejscach ktore warto odwiedzic i zabytkach wartych zobaczenia. Ludzkie losy, miejsca, w ktorych sie one rozgrywaly, podroze sentymentalne w przestrzeni ale i w czasie - to wszystko bedzie stanowic tresc naszego portalu. A jesli ktos zechce posluchac opowiesci o losach ciekawych, tragicznych czy skomplikowanych, acz nie zawsze zwiazanych jedynie z terenami na wschodzie, zapraszamy do dzialu ,,Wspomnienia".<br /><br />W niedalekiej przyszlosci nasz portal bedzie dwujezyczny. Obecnie ma tylko jedna wersje - w jezyku polskim.<br /><br />
Pragniemy Panstwa zaprosic do wspolpracy z portalem. Zachecamy wszystkich zainteresowanych do przesylania artykulow, ktore bedziemy chcieli tutaj opublikowac.
Prosimy o zapoznanie sie z zasadami dotyczacymi przygotowywania tekstow.	<p><a class="button" href="teksty/32/all/wprowadzenie/30/1/wprowadzenie">wiecej</a></p>
</div>
