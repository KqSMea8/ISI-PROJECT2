<div id="tekst">	<b>Witamy na stronie gabinetu rehabilitacji OSTEOfit.</b>

Gabinet wspoltworzony jest przez dwojke wykwalifikowanych i doswiadczonych fizjoterapeutow z
wieloletnia praktyka w zawodzie.
<br><br>
W swojej pracy codziennie spotykamy sie z pacjentami, ktorzy zbyt pozno podjeli leczenie. Czesto
opoznienie w rozpoczeciu leczenia wynika z dlugiego oczekiwania na wizyte u specjalisty, badz z
powodu odkladania leczenia.
Nasz gabinet powstaje z mysla o tych pacjentach, ktorzy potrzebuja natychmiastowej pomocy.
Oferujemy rowniez inne programy rehabilitacyjne dostosowane do potrzeb pacjentow; takie jak:
treningi przygotowujace do sportu rekreacyjnego, zajecia dla kobiet po ciazy, masaze relaksacyjne i
lecznicze. Itp.
<br><br>
Stawiamy na rozwoj i postep. Dlatego chcemy rozwijac nasza oferte, aby jeszcze lepiej dostosowac ja
do potrzeb naszych pacjentow.<br><br>
Gwarantujemy profesjonalne i rzetelne podejscie do problemu pacjenta.
<br><br>
Zapraszamy

