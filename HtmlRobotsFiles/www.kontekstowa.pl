<h2>O nas</h2>
<div class="entry"><p>Firma Digital powstala z mysla o zmieniajacych sie trendach w branzy marketingowej, ktore wynikaja z coraz wiekszego wykorzystania internetu, jako glownego zrodla informacji o uslugach i produktach. Naszym glownym celem jest efektywne wykorzystanie dostepnych narzedzi z zakresu reklamy i e-commerce, tak aby przelozyly sie one na biznesowy sukces naszych Klientow. Zespol  wykwalifikowanych specjalistow w zakresie innowacyjnych form reklamy i komunikacji zapewnia profesjonalna obsluge kazdego z powierzonych nam projektow i dba o skuteczne prowadzenie kampanii e-marketingowych.</p>
<p>Dzieki indywidualnemu podejsciu do kazdego Klienta tworzymy wyjatkowe i niepowtarzalne kreacje, ktore w skuteczny sposob docieraja do okreslonych grup docelowych. Kreatywnosc w naszym rozumieniu to nie tylko dobor oryginalnego designu i tresci, to rowniez znalezienie odpowiedniej drogi dotarcia do wlasciwie wytypowanego odbiorcy.</p>
<p><strong>W ramach naszych uslug oferujemy:</strong></p>
<ul>
<li>identyfikacja wizualna</li>
<li>pozycjonowanie stron internetowych</li>
<li>kompleksowe wsparcie sklepow internetowych</li>
<li>reklame kontekstowa &#8211; kampanie Google Adwords i AdSense</li>
<li>kampanie Social media</li>
<li>tworzenie stron www</li>
<li>kampanie e-mailingowe</li>
</ul>
