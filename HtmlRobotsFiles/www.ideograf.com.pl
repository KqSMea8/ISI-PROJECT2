Strona internetowa firmy IDEOGRAF wykorzystuje cookies - niewielkie informacje tekstowe, wysylane przez serwer WWW i zapisywane po stronie uzytkownika. Domyslne parametry ciasteczek pozwalaja na odczytanie informacji w nich zawartych jedynie serwerowi, ktory je utworzyl. Ciasteczka moga zawierac rozmaite rodzaje informacji o uzytkowniku danej strony WWW i historii jego lacznosci z nia oraz moga sluzyc do monitorowania aktywnosci odwiedzajacych. Ciasteczka sa nieszkodliwe dla komputera i dla uzytkownika oraz jego danych. Kazdy moze je wylaczyc w ustawieniach swojej przegladarki.

<h2>Google Analytics</h2>
Firma IDEOGRAF wykorzystuje usluge Google Analytics do monitorowania ogladalnosci strony internetowwej. Google Analytics uzywa cookies, czyli plikow tekstowych umieszczanych na komputerze uzytkownika w celu umozliwienia witrynie przeanalizowania sposobu, w jaki uzytkownicy z niej korzystaja. Informacje generowane przez cookie na temat korzystania z witryny przez uzytkownika (wlacznie z jego adresem IP) beda przekazywane spolce Google i przechowywane przez nia na serwerach w Stanach Zjednoczonych.

Google bedzie korzystala z tych informacji w celu oceny korzystania z witryny przez uzytkownika, tworzenia raportow dotyczacych ruchu na stronach dla operatorow witryn oraz swiadczenia innych uslug zwiazanych z ruchem na witrynach internetowych i korzystaniem z internetu. Google moze rowniez przekazywac te informacje osobom trzecim, jezeli bedzie zobowiazana to uczynic na podstawie przepisow prawa lub w przypadku, gdy osoby te przetwarzaja takie informacje w imieniu Google.

