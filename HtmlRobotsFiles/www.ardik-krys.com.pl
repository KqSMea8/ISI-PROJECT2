<h2>O firmie</h2>
Witamy na stronach internetowych firmy <strong>ARDIK</strong>.<br/>
Firma ARDIK istnieje od 1993 roku i jest producentem spodow (podeszew) do obuwia.<br/><br/>
Produkujemy spody z PU, PCV i TR . Oferta firmy zawiera spody damskie, meskie oraz dzieciece.<br/>
Od poczatku dzialalnosci produkcyjnej inwestujemy w rozwoj firmy. Efektem tego jest mozliwosc
