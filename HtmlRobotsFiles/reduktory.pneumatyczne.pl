<p id="texth">OFERTA CENOWA REDUKTOROW</p>
<p id="text">
Reduktory cisnienia  utrzymuja  stala wartosc cisnienia na wyjsciu, niezaleznie od zmian np. wyzszego cisnienia wejsciowego lub przy zmiennej wartosci natezenia przeplywu czynnika przez zawor.<br/>
<br/>
Sposob dzialania reduktorow cisnienia:<br/>
<br/>
Regulatory cisnienia sa urzadzeniami regulacyjnymi, ktorych uklad pomiarowy bada cisnienie na wylocie z reduktora i wskazuje je poprzez np.manometr. Regulacja cisnienia odbywa sie najczesciej poprzez pokretlo, iglice i membrane. Uklad regulacji dziala bezposrednio poprzez zwiekszenie lub zmniejszenie cisnienia i upust sprezonego powietrza .<br/>
<br/>
Reduktory cisnienia cechuja sie wysoka wydajnoscia (nawet 36000 l/min). Przylacza reduktorow sa zroznicowane (M5, 1/4", 3/8", 1/2", 1" lub wieksze), a wiec dopasowane do potrzeb danej instalacji.<br/>
<br/>
Oferujemy reduktory o roznych parametrach cisnienia wlotowego od 1 do 200 bar, jak i cisnienia regulacyjnego - standardowe lub precyzyjne ponizej 1 bar.<br/><br />
