<h2>Dlaczego niektorzy posiadacze witryn WWW nie maja problemu z pozyskiwaniem klientow przez Internet?</h2>

<p><p>Czy zastanawiales sie kiedykolwiek do czego potrzebna jest Twojej firmie strona internetowa?</p>
<p>Odpowiedzi na to pytanie moga byc rozne:</p>
<ul>
<li>prestiz firmy;</li>
<li>zaistnienie w nowym medium, jakim jest Internet;</li>
<li>wymagaja tego obecne czasy, itd. </li>
</ul>
<p>Oczywiscie, wszystko to jest prawda, ale najbardziej OPLACALNA z Twojego punktu widzenia odpowiedzia jest:</p>
<p style="text-align:center;"><strong>Witryny internetowe sa po to, aby zdobywac klientow!</strong></p>
<p>Inwestujesz w strone WWW, chcesz zaistniec w sieci Internet - skoro juz wydajesz na to swoje ciezko zarobione pieniadze, to dlaczego nie zrobic z tego inwestycji, ktora zwroci sie wielokrotnie?</p>
<p>Witryna to INWESTYCJA, ktora MOZE zwrocic sie wielokrotnie, poniewaz Internet to:</p>
<ul>
<li>nieograniczone mozliwosci wizualne i kreacyjne;</li>
<li>polaczenie zalet telewizji i prasy;</li>
<li>bezposrednia interakcja z uzytkownikiem;</li>
<li>tani i efektywny sposob POZYSKIWANIA i obslugi klientow oraz utrzymywania relacji z nimi. </li>
</ul>
<p style="text-align:center;"><strong>Brak klientow moze przestac byc dla Ciebie problemem, gdy jestes wlascicielem EFEKTYWNEJ witryny internetowej.</strong></p>
<p>Ale... nie mysl o Tym teraz. Nie mysl o klientach, ktorzy zasypuja Cie pytaniami o szczegoly oferty; nie mysl o nowo pozyskanych klientach, ktorych liczba zwiekszy sie o kilkadziesiat (kilkaset?) procent; nie wyobrazaj sobie jak szeleszcza pieniadze, ktore zarobisz dzieki swojej witrynie WWW; nie mysl tez o zwiekszajacej sie liczbie odwiedzin twojej witryny. Teraz o tym nie mysl, lecz ...</p>
<p>... zastanow sie nad tym, co sprawia, ze witryna przynosi zyski?</p>
<p><strong>Odpowiedz brzmi: EFEKTYWNOSC !</strong></p>
<p>Efektywnosc to niestety rzadko spotykane slowo wsrod wlascicieli biznesow internetowych i jeszcze rzadziej wsrod tworcow witryn WWW. A to wlasnie efektywnosc witryny jest gwarancja zyskow - TWOICH zyskow.</p>
<p class="paragraph-info"><strong>Zalozmy, ze jestes wlascicielem</strong> firmy szkoleniowej. Postanowiles zainwestowac w serwis internetowy wydajac na ten cel 2000 zl. Masz piekna grafike, przejrzysty uklad strony, rzetelne informacje. Miesiecznie pozyskujesz poprzez Internet srednio 12 nowych uczestnikow szkolen sposrod 6234 odwiedzajacych. Poniewaz informacje na stronie musza byc aktualizowane co pewnie czas, to ponosisz dodatkowe koszty w wysokosci 100 zl miesiecznie. Na kazdym zamowieniu zarobiles 276 zl. Twoj przychod miesieczny wynosi 12 x 276 zl = 3312 zl.<br /><br /><strong>Tymczasem, Twoj konkurent</strong> posiadajacy witryne WWW, za ktora zaplacil rowniez 2000 zl i ktory ponosi podobne miesieczne koszty utrzymania witryny, pozyskal 29 nowych klientow przy porownywalnej liczbie odwiedzin. Dodatkowo, 7 jego dotychczasowych klientow wykupilo nowe szkolenia (otrzymujac w prezencie 10% rabatu). Na kazdym nowym kliencie zarobil 293 zl, natomiast na dotychczasowym kliencie 251 zl. Przychod w miesiacu wyniosl zatem: 29 x 293 zl + 7 x 251 zl = 10254 zl.</p>
<p>Podsumowujac: Twoj konkurent zarobil o 309% wiecej od Ciebie.</p>
<p>Skad ta roznica?</p>
<p><strong>Odpowiedzia jest wlasnie EFEKTYWNOSC.</strong></p>
<p class="paragraph-info" style="text-align:left;">"<strong>Stara regula internetu glosila</strong>, ze wygrywa ten, kto pierwszy sie tam znajdzie. My uwazamy, ze to jest kompletna bzdura. Nowa regula brzmi: Pierwsza osoba, ktora zrobi to we WLASCIWY sposob, bedzie miala lojalnych klientow."<br /><em>Karl Peterson, CEO Hotwire<br /></em></p>
<p>Latwo powiedziec.... efektywnosc. Ale jak to osiagnac? Odpowiedz na to pytanie jest rozna, w zaleznosci od prowadzonego biznesu i grupy docelowej. Przykladowo, inaczej bedzie wygladala oferta/witryna skierowana do wlascicieli firm, a inaczej do nastolatkow. Mimo tego, istnieja pewne ogolne zasady, ktorych poznanie gwarantuje wzrost efektywnosci.</p>
<p style="text-align:center;"><strong>Czy wiesz, ze... sama tylko zmiana naglowka oferty moze zwiekszyc sprzedaz o 84% ?</strong></p>
<p>Zainteresowanie odwiedzajacego ciekawa i rzetelna trescia to pierwszy krok do zyskania jego zaufania i lojalnosci. Jesli zdobedziemy ZAUFANIE to juz tylko krok dzieli nas od POZYSKANIA klienta.</p>
<p>O tym, w jaki sposob Internauta oceni Twoja strone, czy pozostanie na niej, czy tez nie, decyduje wlasciwie wrazenie kilkunastu pierwszych sekund. Jesli sekundy te spedzi na ogladaniu bialego okna przegladarki czekajac na wczytanie strony, to jest wysoce prawdopodobne, ze nigdy nie ujrzy wspanialej grafiki, ktora nadal sie sciaga, poniewaz wczesniej zamknie okno przegladarki. Zatem pierwsza zlota rada na to, jak tracic klientow brzmi: zrob wspaniala, piekna, rozbudowana i... bezuzyteczna grafike.</p>
<p>Skoro juz wspomnialem o wygladzie witryny... Uzytkownik odwiedzajacy Twoja witryne internetowa (nie liczac rodziny i znajomych) nie wie, jaka firma jest jej wlascicielem, czy jest to mala firma, czy duzy koncern? czy jest rzetelna? czy tworza ja profesjonalisci, czy amatorzy? Na wszystkie te pytania odpowiada sobie podswiadomie ogladajac Twoja strone, oceniajac jej wyglad, tresc, zamieszczone oferty, informacje, sposob nawigacji i mnostwo innych szczegolow.</p>
<p>W wirtualnym swiecie Internetu obowiazuja te same zasady, co w swiecie rzeczywistym! Zastanow sie - przeciez gdy jedziesz na spotkanie z waznym klientem, to ubierasz schludny garnitur, odpowiednio przygotowujesz sie merytorycznie do spotkania, redagujesz oferte itd. Pamietaj, ze tworzenie strony WWW to PRZYGOTOWANIE sie na SPOTKANIE z klientem. Spotkanie to rozni sie tylko tym od spotkania twarza w twarz, ze korzystamy z posrednika, jakim jest siec komputerowa. Jesli dobrze sie przygotujesz do spotkania, to spotkanie przyniesie zamierzone efekty - pozyskasz klienta.</p>
<p>Faktem jest, ze na efektywnosc witryny WWW wplywa wiele czynnikow, zwiazanych z marketingiem i technologia. Nalezy pamietac, ze klient docierajacy do naszej oferty za pomoca Internetu jest bardzo wymagajacy - najmniejszy szczegol moze spowodowac, ze zamknie okno przegladarki i nigdy wiecej nie wroci na Twoja witryne. Ze wzgledu na ograniczony kontakt z klientem podczas tego wirtualnego spotkania, marketing internetowy wymaga pewnej dodatkowej wiedzy na temat zachowan Internautow. Nawet elementy, ktore WYDAJA SIE byc malo znaczace moga miec ogromne znaczenie.</p>
<p class="paragraph-info"><strong>Czy wiesz, ze...</strong> popularne ruchome elementy graficzne (ruchome prezentacje Flash, banery reklamowe) umieszczone na stronie z oferta firmy rozpraszaja uwage Twojego klienta, przeszkadzaja Mu w czytaniu oferty, a przez to zmniejszaja efektywnosc serwisu!?! Inaczej mowiac... ladna animacja zabiera Ci pieniadze! Dzieje sie tak dlatego, poniewaz WSZYSTKO co rozprasza klienta - uszczupla TWOJ ZYSK.<br /><br /><strong>Czy wiesz, ze...</strong> nawet liczba znakow w pojedynczym wierszu ma wplyw na komfort czytania oferty?</p>
<p>Skad masz wiedziec o tym jak przyciagnac uwage klienta?</p>
<p>Tym sie nie przejmuj - jesli chodzi o strone technologiczna i graficzna (strukture, nawigacje, ergonomie, mechanizmy aktualizacyjne itd.) my zajmiemy sie wszystkim. Jesli chodzi o prawidlowe przygotowanie oferty - tak, aby przyciagnela klienta - otrzymasz od nas materialy, ktore pozwola Ci na zapoznanie sie z podstawowymi elementami dobrze napisanej oferty internetowej.</p>
<p>TY znasz najlepiej swoich klientow, znasz ich potrzeby, wymagania, oczekiwania, znasz ich zarobki, wyksztalcenie, wiesz za co placa, co cenia oraz co daje im Twoj produkt. Dysponujac tymi danymi, jestes NAJBARDZIEJ kompetentna osoba do napisania oferty skierowanej do klienta nalezacego do Twojej grupy docelowej - grupy, do ktorej chcesz trafic z oferta.</p>
<p class="paragraph-info"><strong>Badania Internetu pokazuja</strong>, ze polscy Internauci to osoby o wyzszym niz przecietnym statusie spolecznym i majatkowym. Posiadaja ponadprzecietne dochody, a co trzeci uzytkownik Internetu zalicza sie do grupy prywatnych przedsiebiorcow, przedstawicieli wyzszej kadry kierowniczej i reprezentantow wolnych profesji lub pracownikow biurowych. Z Internetu korzysta 83% pracownikow umyslowych (TNS OBOP, luty 2008, pow. 15 roku zycia).</p>
<p>Pamietaj, ze witryne tworzymy TYLKO dla potencjalnych klientow - reszta nas nie interesuje. Sprawdza sie tu bardzo dobrze powiedzenie, ze ,,jesli cos jest do wszystkiego to jest do niczego". Oczywiscie Twoja strone moze obejrzec kazdy, ale NIE KAZDY jest Twoim potencjalnym klientem.</p></p>
