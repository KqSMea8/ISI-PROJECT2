<h2>
<a href="/klubmaczki/aktualnoci-mainmenu-9/188-naturalne-oryginalne-wasnorcznie-zrobione">
Warsztaty wyplatania z wikliny</a>
</h2>

</header>




<h3><img src="/klubmaczki/images/stories/artykuly/2015/baner_www_wikliniarstwo.jpg" alt="baner www wikliniarstwo" /></h3>
<p>&nbsp;</p>
<h3>czwartki od godz 16:00 / zajecia platne 20zl/msc</h3>
<p>Koszyki z wikliny. Te zrobione wlasnorecznie na naszych zajeciach beda wyjatkowe i nieszablonowe, a to dlatego, ze wzbogacone o czastke Ciebie. Nie musza byc symetryczne, rowne, proste, ba- wrecz niech beda asymetryczne, nieregularne- sztuka nie lubi symetrii. Zrob koszyk taki zeby nie przypominal zadnego z tych co widujesz w sklepie.</p>
