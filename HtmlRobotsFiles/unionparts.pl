<p><font face="arial, helvetica, sans-serif"><strong style="font-size: 13px; line-height: 19px;">UNION </strong><span style="font-size: 13px; line-height: 19px;">PARTS Sp. z o.o. - jestesmy firma przemyslowa produkujaca czesci zamienne do samochodow osobowych i wykonujaca uslugi w zakresie obrobki skrawaniem. Spolka prowadzi dzialalnosc od 1992 r., co przeklada sie na kilkunastoletnie doswiadczenie w branzy i zaufanie szerokiej grupy klientow. Siedziba firmy zlokalizowana jest w Bobrowej Woli (powiat debicki, wojewodztwo podkarpackie). Od 1 stycznia 2006 roku firma dziala pod nowa nazwa UNION PARTS. Zatrudniamy ok. 40 osob w produkcji, logistyce i handlu. Specjalizujemy sie w produkcji czesci dla przemyslu motoryzacyjnego, lotniczego i podzespolow automatyki przemyslowej. Wykonujemy uslugi obrobki skrawaniem dla przemyslu kolejowego, transportowego, wojskowego i innych. Produkujemy tarcze i bebny hamulcowe, elementy zawieszen, walki rozrzadu, waly korbowe, piasty, kola lancuchowe i zebate, oraz inne elementy wg dokumentacji klienta. Od 2000 roku posiadamy Certyfikat Jakosci ISO 9001 wydany przez DEKRA Certyfication. W 2001 roku uzyskalismy status dostawcy czesci FIAT AUTO POLAND, tym samym, nasze produkty zyskaly pozytywna opinie producenta samochodow i sa dostepne w sieci dealerow Fiata jako oryginalne czesci zamienne. Posiadamy rowniez zatwierdzenie jakosciowe z FSO na tarcze i bebny hamulcowe do Daewoo Lanos, co pozwala nam dostarczac te wyroby na pierwszy montaz. Nasz park maszynowy stanowia najnowszej generacji tokarki numeryczne, szlifierki i frezarki specjalizowane. Posiadamy wlasne Biuro Konstrukcyjno-Technologiczne oraz Izbe Pomiarowa odpowiedzialne za ciagla kontrole procesu produkcyjnego oraz przeprowadzanie wszelkich zadan w zakresie pomiarow parametrow technicznych i jakosciowych wyrobow. W odleglosci 2 km od siedziby zakladu znajduje sie nasze centrum magazynowo-logistyczne, gdzie przechowywane sa produkty gotowe i skad realizowana jest kompletacja towaru do klientow.</span></font></p>
