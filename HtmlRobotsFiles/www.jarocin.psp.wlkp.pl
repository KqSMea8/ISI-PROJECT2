<p style="text-align:left">Dnia 19 kwietnia br. o godzinie 14.50 Dyzurny Stanowiska Kierowania Komendanta Powiatowego w Jarocinie otrzymal zgloszenie z Centrum Powiadamiania Ratunkowego z Poznania  dotyczacego wypadku drogowego na ul. Moniuszki w Jarocinie. Na miejsce zdarzenia zadysponowano dwa zastepy z JRG Jarocin oraz jeden zastep z OSP Jarocin. Po przybyciu na miejsce zdarzenie zabezpieczono teren prowadzonych dzialan. Ratownicy udzielili kwalifikowanej pierwszej pomocy 5 osobom poszkodowany. Po przybyciu na miejsce zdarzenia ZRM postanowil przetransportowac jedna osobe do SOR w Jarocinie. Strazacy rozlaczyli akumulatory w pojazdach, kierowali ruchem, posypano sorbentem plamy plynow eksploatacyjnych oraz zebrano pozostalosci powypadkowe z jezdni. Dzialania trwaly 2 godziny. Rowniez w dniu dzisiejszym Dyzurny SKKP w Jarocinie otrzymal zgloszenie dotyczace pozaru sadzy w kominie w miejscowosci Witaszyce. Na miejsce zdarzenia zostaly zadysponowane dwa zastepy JRG Jarocin oraz OSP Witaszyce. Ponadto dyzurny SKKP w Jarocinie otrzymal zgloszenie o pozarze trawy na poboczu drogi w miejscowosci Osiek. Na miejsce zdarzenia zostal zadysponowany jeden zastep z JRG Jarocin oraz OSP Cielcza.<em>i</em></p>



