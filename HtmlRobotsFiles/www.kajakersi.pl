<p style="text-align: justify;"><span style="color: #000000;">Przedmiotem nas</span><span style="color: #000000;">zej dzialalnosci jest <strong>pro</strong></span><span style="color: #000000;"><strong>fesjo</strong></span><span style="color: #000000;"><strong>nalne wypozyczanie kajakow</strong> ora</span><span style="color: #000000;">z sprzetu niezbednego podczas splywania kajakami. Specjalizujemy sie przede wszystkim w splywach rzeka <a href="https://www.kajakersi.pl/?page_id=361"><strong>Wieprza</strong></a> ora</span><span style="color: #000000;">z <a href="https://www.kajakersi.pl/?page_id=384"><strong>Studnica</strong></a>, ale rowniez inne rzeki Pomorz</span><span style="color: #000000;">a nie sa nam obce.</span></p>
