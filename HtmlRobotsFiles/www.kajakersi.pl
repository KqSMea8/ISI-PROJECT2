<h4 style="text-align: center;"><a href="https://www.kajakersi.pl/?page_id=86"><span style="color: #339966;"><span style="text-decoration: underline;"><strong>Zadzwon, badz napisz</strong></span>, </span></a></h4>
<p style="text-align: justify;"><span style="color: #000000;">a z przyjemnoscia pomozemy Ci w doborze odpowiedniej trasy splywu w zaleznosci od poziomu Twoich umiejetnosci, liczebnosci grupy oraz ilosci czasu, jaki chcesz poswiecic na przygode z kajakami. Odpowiemy na nurtujace Cie pytania dot. wyprawy kajakowej oraz pomozemy przy organizacji splywu. Z checia podzielimy sie naszym doswiadczeniem w splywaniu, tak abys czul sie komfortowo oraz bezpiecznie podczas splywania i cieszyl sie pieknem okolicznej przyrody.</span></p>
