<div class="spacer"><h2 class="circle dziennik">e-dziennik (elektroniczny dziennik szkolny)</h2>
<h3><i class="fa fa-book"></i>&nbsp;  Dziennik nowej ery</h3>
<p>Dualny  szkolny dziennik elektroniczny.Ten cyfrowy dziennik to aplikacja elektroniczna zasilana danymi z cyfrowego dlugopisu, ktorym nauczyciel dokonuje wpisow w dzienniku papierowym. Oceny wpisane recznie przesylane sa wprost do aplikacji elektronicznej bez uzywania klawiatury i bez Internetu. Aplikacja automatycznie zamienia reczne wpisy w dzienniku w ich elektroniczna postac i dalej je przetwarza wg potrzeb szkoly. Aplikacja obsluguje wszystkie dzienniki w szkole, tworzac metadane i statystyki.</p>
