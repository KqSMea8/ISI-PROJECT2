<div class="postentry"><p>To, co jeszcze jakis czas temu wydawalo sie niemozliwe, zaczyna bardzo powoli nabierac ksztaltow. Z wielka radoscia informujemy, ze w Lublinie rusza pierwsza regionalna edycja Akcji &#8222;Dziele sie ksiazkami&#8221;! Z inicjatywy pani Sylwii Blaszczak (<a href="http://www.mamawdomu.pl">www.mamawdomu.pl</a>), ktora wiosna wlaczyla sie w nasza ogolnopolska Akcje, od grudnia 2013 do lutego 2014 prowadzona bedzie zbiorka ksiazek, gier planszowych i puzzli dla placowek z regionu.</p>
<p><a href="http://www.dzielesieksiazkami.pl/wp-content/uploads/2013/12/DsKLublin.jpg"><img class="aligncenter size-medium wp-image-775" title="DsKLublin" src="http://www.dzielesieksiazkami.pl/wp-content/uploads/2013/12/DsKLublin-300x90.jpg" alt="" width="300" height="90" /></a></p>
<p>A oto, co na temat Akcji pisza organizatorzy:</p>
<p><em>Po sukcesie ogolnopolskiej akcji ,,Dziele sie ksiazkami", jaka miala miejsce wiosna biezacego roku, postanowilismy kontynuowac propagowanie czytelnictwa. W maju 2013 roku zbieralismy ksiazki w dwoch punktach w Lublinie. Mimo spontanicznej, szybkiej i nierozreklamowanej na szeroka skale akcji, udalo nam sie w ciagu jednego miesiaca zebrac 160 ksiazek, ktore przekazalismy do ogolnej puli. Przy prowadzeniu zbiorki, czesto spotykalismy sie z zapytaniami od darczyncow o to, gdzie powedruja przyniesione przez nich ksiazki. Wiele osob pragnelo, aby ich dary zostaly przekazane do lokalnych placowek. Wzielismy sobie do serca te wszystkie zapytania i rady i postanowilismy zapoczatkowac akcje regionalna.</em></p>
<p><em>Mamy nadzieje, ze dzieki przeprowadzeniu zbiorki w ramach akcji ,,Dziele sie ksiazkami Lublin", uda nam sie przekazac ksiazki dzieciom, ktore na co dzien maja do nich ograniczony dostep. Chcemy rowniez propagowac idee dzielenia sie z rowiesnikami wsrod dzieci w wieku przedszkolnym i szkolnym.</em></p>
<p><em>Zglaszaja sie do nas przedszkola, klubokawiarnie i osoby prywatne, ktore chca przekazac ksiazki. Podczas lubelskiej edycji chcielibysmy zebrac nie tylko ksiazki, ale i gry planszowe oraz puzzle.</em></p>
<p><em>Liczymy na to, ze kilkukrotnie zwiekszymy ilosc zebranych darow wzgledem edycji wiosennej, a tym samym uda nam sie wspomoc wieksza liczbe placowek z naszego regionu.</em></p>
<p><em>Swoja pomoca pragniemy wesprzec placowki, w ktorych na co dzien przebywaja dzieci, tj. szpitale, domy dziecka i swietlice srodowiskowe z terenu wojewodztwa lubelskiego.</em></p>
