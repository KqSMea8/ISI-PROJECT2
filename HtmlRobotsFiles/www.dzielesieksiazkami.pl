<div class="postentry"><p>Wczoraj byl ten wielki dzien, kiedy to 50 wielkich i ciezkich paczek, dzieki naszemu sponsorowi, firmie DPD Polska, pojechalo do placowek na terenie calej Polski.</p>
<p><a href="http://www.dzielesieksiazkami.pl/wp-content/uploads/2013/06/IMAG3143.jpg"><img class="aligncenter size-medium wp-image-750" title="IMAG3143" src="http://www.dzielesieksiazkami.pl/wp-content/uploads/2013/06/IMAG3143-179x300.jpg" alt="" width="179" height="300" /></a></p>
<p><a href="http://www.dzielesieksiazkami.pl/wp-content/uploads/2013/06/IMAG3149.jpg"><img class="aligncenter size-medium wp-image-751" title="IMAG3149" src="http://www.dzielesieksiazkami.pl/wp-content/uploads/2013/06/IMAG3149-300x179.jpg" alt="" width="300" height="179" /></a></p>
<p>Mamy pierwsze sygnaly, ze ksiazki juz dzis dotarly:</p>
<p><em>Wlasnie kilka minut temu odebralam paczke z ksiazkami. Oj radosc ogromna i zaangazowanie nauczycieli w zorganizowaniu profesjonalnej biblioteki w naszej malej szkole&#8230;nieoceniona! Bardzo, bardzo dziekujemy!</em></p>
<p>Paulina Wasowicz<br />
nauczyciel w Szkole Podstawowej w Buszkowach</p>
<p><em>Serdecznie dziekujemy za radosc dzieciaczkow i tych malych, i tych duzych. Nikt dzisiaj nie chce isc na podworko (i bardzo dobrze , upal siega  zenitu), sluchaja bajek, ukladaja puzzle, a starszaki robia wulkan. Mlodziez wchlonely ich ulubione ksiazki. Wieczorkiem zagramy w gre . Jestescie kochani.</em></p>
<p>Pozdrawiamy<br />
Grazyna i zaloga Magicznego Domu</p>
<p>Bardzo sie cieszymy i czekamy na potwierdzenie od DPD, ze wszystkie paczki zostaly odebrane.</p>
