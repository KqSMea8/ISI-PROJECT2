<div class="postentry"><p>Jak mozna sie bylo spodziewac &#8211; majac w pamieci poprzednie edycje Akcji spolecznej &#8222;Dziele sie ksiazkami&#8221;, darczyncy znow nie zawiedli. Podczas Targow, ale tez po nich, udalo sie nam zebrac pokazna liczbe ksiazek. W tym miejscu szczegolne podziekowania dla pracownikow Wydawnictwa Nowa Era, ktorzy zorganizowali zbiorke podrecznikow i innych materialow edukacyjnych.</p>
<p>Zgodnie z zalozeniami wyprawa <a href="https://www.facebook.com/PolandMongoliaTransSiberianExpedition?ref=ts&amp;fref=ts" target="_blank">Poland - Mongolia Trans-Siberian Expedition</a> dotarla do wsi Wierszyna na Syberii, gdzie mieszkaja potomkowie Polakow, ktorzy na poczatku XX wieku wyruszyli tam w poszukiwaniu lepszego zycia. Mieszkancy wciaz uzywaja jezyka polskiego, dlatego tym starszym zalezy, by dzieci mialy dostep do polskich ksiazek.</p>
