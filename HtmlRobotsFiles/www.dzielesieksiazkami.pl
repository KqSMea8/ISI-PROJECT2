<h3>Slow kilka o Akcji</h3>			<div class="textwidget">"Dziele sie ksiazkami" to akcja spoleczna zainicjowana w 2011 roku przez firme Qlturka.pl, prowadzaca portal Qlturka.pl - dziecko i kultura. Podczas I edycji Akcji zebralismy ponad 1 tys. ksiazek dla dzieci przebywajacych w domach dziecka, szpitalach oraz tych, ktore spedzaja czas w swietlicach srodowiskowych.
W 2012 roku za organizacje Akcji odpowiadala Fundacja "Dziecko i Kultura", ktora przeprowadzila dwie edycje - w Warszawie i Katowicach. Lacznie udalo sie zebrac okolo 4 tys. ksiazek, ktore powedrowaly do 100 placowek w calej Polsce.
W 2013 roku odbyla sie jedna edycja Akcji - podczas majowych Warszawskich Targow Ksiazki. Znow udalo sie nam obdarowac 50 placowek na terenie calego kraju.
W 2014 roku odbedzie sie Edycja Syberyjska, w ramach ktorej wesprzemy Poland - Mongolia Trans-Siberian Expedition, ktora dotrze do wsi Wierszyna na Syberii, gdzie mieszkaja potomkowie Polakow, ktorzy na poczatku XX wieku wyruszyli tam w poszukiwaniu lepszego zycia. Mieszkancy wciaz uzywaja jezyka polskiego, dlatego tym starszym zalezy, by dzieci mialy dostep do polskich ksiazek.
